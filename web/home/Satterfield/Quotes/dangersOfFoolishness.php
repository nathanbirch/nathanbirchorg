                <p style="margin-left: 40px; font-family: arial;"><big><big>One of Mr.
                        Hyde&#8217;s [of Dr. Jekyll and Mr. Hyde] deceptions is what some erroneously
                        call &#8220;premeditated repentance.&#8221; There is no such doctrine in this
                        Church. This may sound subtly appealing, but it is in fact pernicious
                        and a false concept. Its objective is to persuade us that we can
                        consciously and deliberately transgress with the forethought that quick
                        repentance will permit us to enjoy the full blessings of the gospel,
                        such as temple blessings or a mission. True repentance can be a long,
                        painful process. This foolish doctrine was foreseen by Nephi:
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big>&#8220;And there
