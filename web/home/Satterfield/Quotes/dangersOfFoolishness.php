                <p style="font-family: arial; margin-left: 40px;"><big><big>To excuse
                        misconduct by blaming others is presumptuous at best and is fatally
                        flawed with regard to spiritual things, for &#8220;we believe that men will
                        be punished for their own sins, and not for Adam&#8217;s transgression.&#8221; (<a
                            class="scriptureRef"
                            onclick="newWindow('http://scriptures.lds.org/a_of_f/1//2#2')"
                            href="http://scriptures.lds.org/a_of_f/1/2#2" target="contentWindow">A
                            of F 1:2</a>.) This not only means that we will not be punished for
                        what Adam did in the Garden, but also that we cannot excuse our own
                        behavior by pointing a finger to Adam or anyone else. The real danger
                        in failing to accept responsibility for our own actions is that unless
                        we do, we may never even enter on the strait and narrow path.
                        Misconduct that does not require repentance may be pleasant at first,
                        but it will not be for long. And it will never lead us to eternal life.
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Just as
                        foolish as believing that we can &#8220;pass it on&#8221; is the idea that the
                        satisfaction of being in the circle, whatever that may be, can somehow
                        excuse any wrongs committed there. This notion is widely shared and is
                        most often expressed by the phrase, &#8220;The end justifies the means.&#8221; Such
                        a belief, if left undisturbed and unchecked, can also impede the
                        repentance process and cheat us out of exaltation.
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Those who
                        teach it are almost always attempting to excuse the use of improper or
                        questionable means. Such people seem to be saying, &#8220;My purpose was to
                        do good or to be happy; therefore, any little lie, or
                        misrepresentation, or lapse of integrity, or violation of law along the
                        way is justified.&#8221;
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>In certain
                        circumstances, some say it is &#8220;okay&#8221; to conceal the truth, to dig just
                        a small pit for an adversary, to pursue an advantage of some kind&#8212;such
                        as superior knowledge or position&#8212;against another. &#8220;This is just common
                        practice,&#8221; or &#8220;I&#8217;m just looking after Number One,&#8221; they say. &#8220;All&#8217;s
                        fair in love and war,&#8221; or &#8220;That&#8217;s the way the ball bounces,&#8221; they say.
                        But if the means which prompt the saying of these things are wrong, no
                        amount of rationalization or verbal whitewash can ever make them right.
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>To those
                        who believe otherwise, Nephi said: &#8220;Yea, and there shall be many which
                        shall teach after this manner, false and vain and foolish doctrines,
                        and shall be puffed up in their hearts, and shall seek deep to hide
                        their counsels from the Lord.&#8221; (<a class="scriptureRef"
                                                                 onclick="newWindow('http://scriptures.lds.org/2_ne/28//9#9')"
                                                                 href="http://scriptures.lds.org/2_ne/28/9#9" target="contentWindow">2
                            Ne. 28:9</a>.)
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Some seek
                        to justify their actions by quoting scripture. They often cite Nephi&#8217;s
                        killing of Laban as an example of the need to violate a law to
                        accomplish a greater good and to prevent a nation from dwindling in
                        unbelief. But they forget that Nephi twice refused to follow the
                        promptings of the Spirit. In the end, he agreed to break the
                        commandment only when he was convinced that &#8220;the <em>Lord</em> slayeth
                        the wicked to bring forth <em>his</em> righteous purposes&#8221; (<a
                            class="scriptureRef"
                            onclick="newWindow('http://scriptures.lds.org/1_ne/4//13#13')"
                            href="http://scriptures.lds.org/1_ne/4/13#13" target="contentWindow">1
                            Ne. 4:13</a>; italics added) and also (I believe) when he knew that the
                        penalty for shedding blood had been lifted, in that one exceptional
                        case, by Him whose right it is to fix and waive penalties.
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>The truth
                        is that we are judged by the means we employ and not by the ends we may
                        hope to obtain. It will do us little good at the last day to respond to
                        the Great Judge, &#8220;I know I was not all I could have been, but my heart
                        was in the right place.&#8221;
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>In fact,
                        there is danger in focusing merely on ends. To some who did, the Savior
                        said:
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>&#8220;Many will
                        say to me in that day: Lord, Lord, have we not prophesied in thy name,
                        and in thy name have cast out devils, and in thy name done many
                        wonderful works?
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>&#8220;And then
                        will I profess unto them: I never knew you; depart from me, ye that
                        work iniquity.&#8221; (<a class="scriptureRef"
                                                  onclick="newWindow('http://scriptures.lds.org/3_ne/14//22-23#22')"
                                                  href="http://scriptures.lds.org/3_ne/14/22-23#22"
                                                  target="contentWindow">3 Ne. 14:22&#8211;23</a>.)
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>The war in
                        heaven was essentially about the means by which the plan of salvation
                        would be implemented. It forever established the principle that even
                        for the greatest of all ends, eternal life, the means are critical. It
                        should be obvious to all thinking Latter-day Saints that the wrong
                        means can never attain that objective.
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>The danger
                        in thinking that the end justifies the means lies in making a judgment
                        we have no right to make. Who are we to say that the Lord will pardon
                        wickedness done to attain a perceived &#8220;greater good.&#8221; Even if the goal
                        is good, it would be a personal calamity to look beyond the mark and
                        fail to repent of the wrong we do along the way.&nbsp; <small>(&#8220;Repentance,&#8221;
                            <i>Ensign</i>, May 1991, pp. 12-13)</small><br>
                    </big></big></p>
