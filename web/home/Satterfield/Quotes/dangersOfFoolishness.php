                <p style="font-family: arial; margin-left: 40px;"><big><big>As a young
                        man I served a mission to Brazil. It was a marvelous experience. One of
                        the wonders of the world in that great country is Igua&ccedil;u Falls.
                        In the flood season, the volume of water spilling over the brink is the
                        largest in the world. Every few minutes, millions of gallons of water
                        cascade into the chasm below. One part of the falls, where the deluge
                        is the heaviest, is called the Devil&#8217;s Throat.
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>There are
                        some large rocks standing just above, before the water rushes down into
                        Devil&#8217;s Throat. Years ago, reckless boatmen would take passengers in
                        canoes to stand on those rocks and look down into the Devil&#8217;s Throat.
                        The water above the falls is usually calm and slow moving, and the
                        atmosphere tranquil. Only the roar of the water below forewarns of the
                        danger lurking just a few feet away. A sudden, unexpected current could
                        take a canoe into the rushing waters, over the cliff, and down into the
                        Devil&#8217;s Throat. Those foolish enough to leave the canoes to stand on
                        these treacherous wet rocks could so easily lose their footing and be
                        swept away into the swirling currents below.
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>I recognize
                        that some of you think of yourselves as daredevils, ready to take on
                        almost any challenge. But some of these excursions for excitement will
                        inevitably take you down into the Devil&#8217;s Throat. The only safe course
                        is to stay well away from the dangers of the Devil&#8217;s Throat. President
                        George Albert Smith strongly cautioned, &#8220;If you cross to the devil&#8217;s
                        side of the line one inch, you are in the tempter&#8217;s power, and if he is
                        successful, you will not be able to think or even reason properly,
                        because you will have lost the spirit of the Lord.&#8221;
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Some of you
                        young men may be letting others set your standards. You defend
                        yourselves by saying, &#8220;Who said we shouldn&#8217;t do this or we shouldn&#8217;t do
                        that?&#8221; There are so many shades of right and wrong that each of you has
                        to decide where the line will be. I strongly urge you that if there is
                        any question in your minds or hearts about whether your personal
                        conduct is right or wrong, don&#8217;t do it. Each of us has moral agency,
                        and the gift of the Holy Ghost will sharpen our impressions of what is
                        right and wrong, true and false. It is the responsibility of the
                        prophets of God to teach the word of God, not to spell out every jot
                        and tittle of human conduct. If we are conscientiously trying to avoid
                        not only evil but the very appearance of evil, we will act for
                        ourselves and not be acted upon.
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Much of
                        what comes from the devil is alluring and enticing. It glitters and is
                        appealing to the sensual parts of our nature. His message sounds so
                        reasonable and easy to justify. His voice is usually smooth and
                        intriguing. If it were harsh or discordant, nobody would listen, nobody
                        would be enticed. Some of Satan&#8217;s most appealing messages are: Everyone
                        does it; if it doesn&#8217;t hurt anybody else, it&#8217;s all right; if you feel
                        there is no harm in it, it&#8217;s okay; it&#8217;s the &#8220;cool&#8221; thing to do. Satan
                        is the greatest imitator, the master deceiver, the arch counterfeiter,
                        and the greatest forger ever in the history of the world. He comes into
                        our lives as a thief in the night. His disguise is so perfect that it
                        is hard to recognize him or his methods. He is a wolf in sheep&#8217;s
                        clothing&nbsp; <small>(&#8220;The Devil&#8217;s Throat,&#8221; <i>Ensign</i>, May 2003,
                            p. 51).</small></big></big></p>
                <p style="font-family: arial;"><big><big><br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Sometimes
                        we make poor choices when we yield to peer pressure. Kieth Merrill had
                        such an experience when he was a young man. He and his friends were
                        diving from sheer rock walls at the East Canyon Reservoir, northeast of
                        Salt Lake City. It inevitably turned into a teenage contest when one
                        young man climbed up to the top of the dam and dived 50 feet into the
                        deep water of the reservoir. The rest of the young men all went to the
                        top of the dam and made the same high dive. One boy wasn&#8217;t satisfied
                        with that, so he said, &#8220;All right, I&#8217;ll do one better!&#8221; He climbed 60
                        feet up the side of the cliff. Not wanting to be outdone, Kieth climbed
                        up beside him. After the other boy had dived into the water and seemed
                        to be all right, Kieth took courage and made his dive. The contest was
                        now down to these two boys. Kieth&#8217;s friend then climbed up to 70 feet
                        and dived. He came up from the water laughing, rubbing his shoulders
                        and his eyes. He then challenged Kieth, &#8220;Well, are you going to do it?&#8221;
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>&#8220;Of course,
                        I&#8217;m going to do it!&#8221; And everybody on the shore said, &#8220;Of course, he&#8217;s
                        going to do it!&#8221;
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>So Kieth
                        swam back to the shore and climbed up the rocks. He knew if he jumped
                        from the same height of 70 feet that his friend would want to go
                        higher, so he scrambled up 80 feet to the very top of the cliff. No one
                        could go any higher than the top. As Kieth looked down, he was
                        terrified to see the water so very far away. He had made a rash
                        decision. It was not what he wanted to do nor what he felt was right.
                        Instead he had based his decision on the prodding and dares of a half
                        dozen young men whose names he cannot now even remember.
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>He backed
                        up and ran as hard as he could toward the edge. He found the mark he
                        had carefully laid at the edge of the rock and sprang out into space.
                        On the way down he remembered his parents teaching him to be careful
                        when making decisions, because a wrong one could kill him. And now he
                        thought, &#8220;You have done it, because when you hit the water you&#8217;ll be
                        going so fast that it might as well be concrete.&#8221; When he hit the
                        water, it even felt like concrete. How grateful he was when his head
                        finally popped above water.
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Why did he
                        jump? What was he trying to prove? The young men who dared him didn&#8217;t
                        care and probably don&#8217;t even remember that foolish act. But Kieth
                        realized afterward that he had made what could easily have been a fatal
                        decision. He had yielded to the pressure of friends expecting him to do
                        what he didn&#8217;t want to do. He knew better. He said: &#8220;I was living in
                        the world, and at that moment I was of the world because I was not in
                        control of myself. I was not making decisions about my own life. The
                        world made the decisions for me, &#8230; and [I] had barely avoided being in
                        the world about six feet deep.&#8221;
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>It takes a
                        certain kind of courage to stand back rather than leaping forward,
                        foolishly allowing someone else to make our choices for us. We can more
                        readily take firm stands when we have a clear idea of our identity as
                        sons of God and bearers of the holy priesthood, having a bright
                        potential for a meaningful future.&nbsp; <small>( &#8220;Choices,&#8221; <i>Ensign</i>,
                            May 2004, p. 51)</small><br>
                    </big></big></p>
