                        become aware of it; but we can become aware of it if we will reflect.
                        And this will be more and more the case as the power of God increases
                        and the responsibility of the Latter-day Saints becomes greater.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Therefore, I
                        am looking for destructions and for judgments; I am looking for the
