                <p style="margin-left: 40px;"><big><big><font face="Arial">Our Blessed
                        Lord will come at the appointed time. The great conference at
                        Adam-ondi-Ahman will assemble to worship the King on schedule. There
                        will be an exact moment when his foot first touches the Mount of
                        Olives. He will stand on Mount Zion with the 144,000 high priests at a
                        given point in time. Armageddon will spread its fire and horror and
                        death when and as decreed in the divine timetable. The tares will be
                        burned and the vineyard cleansed of corruption when the Lord Jesus
                        returns. And there will be a day and an hour and a split second that
                        marks the beginning of the Millennium. The seventh of the thousand-year
                        periods can only commence at one single instant in tame. As we have
                        seen, many of the events incident to the Second Coming will take place
                        during the close of the sixth seal and others after the opening of the
                        seventh seal. And yet the formal beginning of the Millennium will take
                        place at a fixed and determined and set moment. It cannot be hastened
                        by righteousness nor delayed by wickedness. The old earth will die, and
                        the new heaven and the new earth will be born at as exact an instant as
                        is the case with the birth or death of any form of life. We speak thus
                        so there will be no confusion or misunderstanding when we also say that
                        the full glory and wonder of the millennial day will unfold gradually;
                        that there will be wicked men on earth after the Millennium has
                        commenced; and that the final glory and triumph of Israel will take
                        place gradually after the Millennium itself has been ushered in. Let us
                        now see what the holy word has to say relative to these things.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">First, the
                        cleansing of the vineyard, the burning of the earth, and the
                        destruction of the wicked by fire shall all come to pass in a short
                        time, in a single day, as it were. And then there will be but few men
                        left on earth. "I will remove the iniquity of that land in one day,"
                        the Lord told Zechariah. (Zech. 3:9.) And in Isaiah we find numerous
                        prophetic expressions phrased in that imagery in which he so excelled.
                        "Many houses shall be desolate, even great and fair, without
                        inhabitant." (Isa. 5:9.) Those sent to raise the warning voice to the
                        world are to do so "until the cities be wasted without inhabitant, and
                        the houses without man, and the land be utterly desolate, and the Lord
                        have removed men far away." (Isa. 6:11-12.) In the day of the Lord's
                        coming, "the light of Israel shall be for a fire, and his Holy One for
                        a flame: and it shall burn and devour his thorns and his briers in one
                        day; and shall consume the glory of his forest, and of his fruitful
                        field, both soul and body: and they shall be as when a standard bearer
                        fainteth. And the rest of the trees of his forest shall be few, that a
                        child may write them." (Isa. 10:17-19.) "Behold, the Lord maketh the
                        earth empty, and maketh it waste, and turneth it upside down, and
                        scattereth abroad the inhabitants thereof. . . . The land shall be
                        utterly emptied, and utterly spoiled. . . . Therefore hath the curse
                        devoured the earth, and they that dwell therein are desolate: therefore
                        the inhabitants of the earth are burned, and few men left." (Isa.
                        24:1-6.) Who can doubt that there will be but few men left at the
                        beginning of the Millennium?</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">The
                        prophetic word that sets forth what is to be during the Millennium
                        speaks of "new heavens and a new earth, wherein dwelleth
                        righteousness." (2 Pet. 3:13.) It says that every corruptible thing
                        shall be consumed when the vineyard is burned. And yet the Prophet
                        Joseph Smith said: "There will be wicked men on the earth during the
                        thousand years. The heathen nations who will not come up to worship
                        will be visited with the judgments of God, and must eventually be
                        destroyed from the earth." (<em>Teachings</em>, pp. 268-69.) Taken
                        together, these concepts mean that wickedness that is telestial in
                        nature, wickedness that consists of living after the manner of the
                        world, that wickedness which is carnal, sensual, and devilish by nature
                        - all such shall cease. Those who so live will be destroyed. They are
                        the tares of the earth.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">But
                        wickedness is a matter of degree, and even those who are upright and
                        decent by worldly standards but who reject the gospel and do not
                        worship the true God are considered to be wicked by gospel standards.
                        They are "under the bondage of sin." They do not accept the message of
                        the restoration and gain a remission of their sins in the waters of
                        baptism. "And by this you may know the righteous from the wicked, and
                        that the whole world groaneth under sin and darkness even now."
                        (D&amp;C 84:49-53.) Thus the divine word, as given by Zechariah, says
                        that "it shall come to pass" - in the millennial day -- that every one
                        that is left [after Armageddon] of all the nations which came against
                        Jerusalem shall even go up from year to year to worship the King, the
                        Lord of hosts, and to keep the feast of tabernacles. And it shall be,
                        that who so will not come up of all the families of the earth unto
                        Jerusalem to worship the King, the Lord of hosts, even upon them shall
                        be no rain. And if the family of Egypt go not up, and come not, that
                        have no rain; there shall be the plague, wherewith the Lord will smite
                        the heathen that come not to keep the feast of tabernacles. This shall
                        be the punishment of Egypt, and the punishment of all nations that come
                        not up to keep the feast of tabernacles." (Zech. 14:16-19.) Thus there
                        will be many churches on earth when the Millennium begins. False
                        worship will continue among those whose desires are good, "who are
                        honorable men of the earth," but who have been "blinded by the
                        craftiness of men." (D&amp;C 76:75.) Plagues will rest upon them until
                        they repent and believe the gospel or are destroyed, as the Prophet
                        said. It follows that missionary work will continue into the Millennium
                        until all who remain are converted. Then "the earth shall be full of
                        the knowledge of the Lord, as the waters cover the sea." (Isa. 11:9.)
                        Then every living soul on earth will belong to The Church of Jesus
                        Christ of Latter-day Saints. <small>(<em>The Millennial Messiah</em>,
                            pp.649-652)<br>
