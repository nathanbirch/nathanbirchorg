                <p style="margin-left: 40px;"><big><big><font face="Arial">When the
                        Millennium is ushered in the earth is to pass through a cleansing. This
                        will not be the final cleansing when the earth shall be consumed and
                        pass away to be renewed again a celestial globe, but it will be the end
                        of unrighteousness. 'All who have lived the telestial law -- that is
                        those who are unclean, "they who are liars, and sorcerers, and
                        whoremongers, and whosoever loves and makes a lie" and who suffer the
                        wrath of God on earth and "suffer the vengeance of eternal fire" --
                        shall be swept off from the face of the earth. All of these will be
                        cast down to hell where they shall remain until Christ shall have
                        subdued all enemies under his feet, and shall have perfected his work.
                        During the thousand years all of these will be in this torment of mind,
                        having ample time to reflect over their misdeeds and receive training
                        in obedience to law, so that they may be prepared to come forth in the
                        resurrection at the end of the world.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">It will be
                        impossible for the people of this class to remain on the earth during
                        the Millennium, for they would be as much out of their element as a
                        fish out of water. The changed condition of the earth, which will be of
                        a terrestrial order during this thousand years, will be suited to the
                        capacity of those of the terrestrial world as well as those who have
                        kept the celestial law, and they shall have part in the first
                        resurrection. "And then shall the heathen nations be redeemed, and they
                        that knew no law shall have part in the first resurrection, "And it
                        shall be tolerable for them." (D.C. 45:54.) But with those of the
                        telestial order, this will not be so. These are they who are as stubble
                        who shall be consumed when Christ comes, according to the words of
                        Malachi. (4:1.) It is of this class the Lord speaks when he says:</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">"For the
                        hour is nigh and the day soon at hand when the earth is ripe and all
                        the proud and they that do wickedly shall be as stubble; and I will
                        burn them up, saith the Lord of Hosts, that wickedness shall not be
                        upon the earth;</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">"For the
                        hour is nigh, and that which was spoken by mine apostles must be
                        fulfilled; for as they spoke so shall it come to pass;</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">"For I will
                        reveal myself from heaven with power and great glory, with all the
                        hosts thereof, and dwell in righteousness with men on earth a thousand
                        years, and the wicked shall not stand." (D.C. 29:9-11) <small>[<em>The
                                Way to Perfection</em>, pp.302-303]</small></font></big></big></p>
                <p><big><big><font face="Arial"><strong><br>
                            <span style="color: rgb(0, 0, 153);">Bruce R. McConkie</span></strong></font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Enoch sought
                        to learn from the Lord when the curse would be removed and when the
                        earth would be blessed again. After he and his city had been
                        translated, "Enoch looked upon the earth; and he heard a voice from the
                        bowels thereof, saying: Wo, wo is me, the mother of men; I am pained, I
                        am weary, because of the wickedness of my children. When shall I rest,
                        and be cleansed from the filthiness which is gone forth out of me? When
                        will my Creator sanctify me, that I may rest, and righteousness for a
                        season abide upon my face?" How graphic and wondrous is this way of
                        teaching -- to let the very earth itself cry out for rest and
                        blessings! "And when Enoch heard the earth mourn, he wept, and cried
                        unto the Lord, saying: O Lord, wilt thou not have compassion upon the
                        earth? Wilt thou not bless the children of Noah?" And the Lord did then
                        bless the earth to this extent: he decreed that life on its face should
                        never again be destroyed by a flood. But the basic questions remained
                        unanswered, and so Enoch yet "cried unto the Lord, saying: When the Son
                        of Man cometh in the flesh, shall the earth rest? I pray thee, show me
                        these things." He then saw the crucifixion, the convulsions of nature
                        that attended it, the opening of the prison doors, and the reserving of
                        the wicked "in chains of darkness until the judgment of the great day.
                        And again Enoch wept and cried unto the Lord, saying: When shall the
                        earth rest? And Enoch beheld the Son of Man ascend up unto the Father;
                        and he called unto the Lord, saying: Wilt thou not come again upon the
                        earth?</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">I ask thee
                        if thou wilt not come again on the earth." How great were the pleadings
                        of this holy prophet to know what would be in the last days!</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">"And the
                        Lord said unto Enoch: As I live, so will I come in the last days, in
                        the days of wickedness and vengeance. . . . And the day shall come that
                        the earth shall rest, but before that day the heavens shall be
                        darkened, and a veil of darkness shall cover the earth; and the heavens
                        shall shake, and also the earth; and great tribulations shall be among
                        the children of men, but my people will I preserve." Then the Lord told
                        Enoch of the restoration of the gospel, the coming forth of the Book of
                        Mormon, the building of the New Jerusalem, and the return of the
                        original Zion, and gave him the promise that "for the space of a
                        thousand years the earth shall rest. And it came to pass that Enoch saw
                        the day of the coming of the Son of Man, in the last days, to dwell on
                        the earth in righteousness for the space of a thousand years." (Moses
                        7:48-65.) <small>[<em>The Millennial Messiah</em>, p.615]</small></font></big></big></p>
