                        no coming of the Christ? Moses says: "And the Lord called his people
                        Zion, because they were of one heart and one mind, and dwelt in
                        righteousness, and there was no poor among them." Such a condition will
                        be again, and the Lord will come again, as he has promised:</font></big></big></p>
