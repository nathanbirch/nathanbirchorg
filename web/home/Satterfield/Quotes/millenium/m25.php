                <p style="margin-left: 40px;"><big><big><font face="Arial">First, I
                        shall begin by quoting from the prophecy of Enoch, speaking of the last
                        days: "Righteousness will I send down out of heaven, and truth will I
                        send forth out of the earth, to bear testimony of mine Only Begotten,
                        His resurrection from the dead (this resurrection I understand to be
                        the corporeal body); yea, and also the resurrection of all men;
                        righteousness and truth will I cause to sweep the earth as with a
                        flood, to gather out mine own elect from the four quarters of the
                        earth, unto a place which I shall prepare, a Holy City, that my people
                        may gird up their loins, and be looking forth for the time of my
                        coming, for there shall be my tabernacle, and it shall be called Zion a
                        New Jerusalem." (Moses 7:62)</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Now I
                        understand by this quotation, that God clearly manifested to Enoch the
                        redemption which He prepared, by offering the Messiah as a Lamb slain
                        from before the foundation of the world; and by virtue of the same, the
                        glorious resurrection of the Savior, and the resurrection of all the
                        human family, even a resurrection of their corporeal bodies, is brought
                        to pass; and also righteousness and truth are to sweep the earth as
                        with a flood. And now, I ask, how righteousness and truth are going to
                        sweep the earth as with a flood? I will answer. Men and angels are to
                        be co-workers in bringing to pass this great work, and Zion is to be
                        prepared, even a new Jerusalem, for the elect that are to be gathered
                        from the four quarters of the earth, and to be established an holy
                        city, for the tabernacle of the Lord shall be with them.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Now Enoch
                        was in good company in his views upon this subject: "And I heard a
                        great voice out of heaven, saying, Behold, the tabernacle of God is
                        with men, and He will dwell with them, and they shall be His people and
                        God Himself shall be with them, and be their God" 21:3).</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">I discover
                        by this quotation, that John upon the isle of Patmos, saw the same
                        things concerning the last days, which Enoch saw. But before the
                        tabernacle can be with men, the elect must be gathered from the four
                        quarters of the earth. And to show further upon this subject of the
                        gathering, Moses, after having pronounced the blessing and cursing upon
                        the children of Israel, for their obedience or disobedience, says thus:</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">"And it
                        shall come to pass, when all these things are come upon thee, the
                        blessing and the curse which I have set before thee, and thou shalt
                        call them to mind, among all the nations whither the Lord thy God hath
                        driven thee, and shalt return unto the Lord thy God, and shalt obey His
                        voice, according to all that I command thee, this day, thou and thy
                        children, with all thine heart, and with all thy soul, that then the
                        Lord thy God will turn thy captivity, and have compassion upon thee,
                        and will return and gather thee from all the nations whither the Lord
                        thy God hath scattered thee. If any of thine be driven out unto the
                        outmost parts of heaven, from thence will the Lord thy God gather thee,
                        and from thence will He fetch thee (Deut. 30:1-4). . . .</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">And again
                        (see Book of Mormon, 3 Nephi 20:22, current edition, which says),
                        "Behold this people will I establish in this land, unto the fulfilling
                        of the covenant which I made with your father Jacob, and it shall be a
                        New Jerusalem." Now we learn from the Book of Mormon the very identical
                        continent and spot of land upon which the New Jerusalem is to stand,
                        and it must be caught up according to the vision of John upon the isle
                        of Patmos.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Now many
                        will feel disposed to say, that this New Jerusalem spoken of, is the
                        Jerusalem that was built by the Jews on the eastern continent. But you
                        will see, from Revelation 21:2, there was a New Jerusalem coming down
                        from God out of heaven, adorned as a bride for her husband; that after
                        this, the Revelator was caught away in the Spirit, to a great and high
                        mountain, and saw the great and holy city descending out of heaven from
                        God. Now there are two cites spoken of here. As everything cannot be
                        had in so narrow a compass as a letter, I shall say with brevity, that
                        there is a New Jerusalem to be established on this continent, and also
                        Jerusalem shall be rebuilt on the eastern continent (see Book of
                        Mormon, Ether 13:1-12). "Behold, Ether saw the days of Christ, and he
                        spake also concerning the house of Israel, and the Jerusalem from
                        whence Lehi should come; after it should be destroyed, it should be
                        built up again, a holy city unto the Lord, wherefore it could not be a
                        New Jerusalem, for it had been in a time of old." This may suffice,
                        upon the subject of gathering, until my next. <small>(<em>Teachings of
                                the Prophet Joseph Smith</em>, pp.84-86)</small></font></big></big></p>
