                        JERUSALEM: TWO WORLD CAPITALS. When Joseph Smith translated the Book of
                        Mormon, he learned that America is the land of Zion which was given to
                        Joseph and his children and that on this land the City Zion, or New
                        Jerusalem, is to be built. He also learned that Jerusalem in Palestine
                        is to be rebuilt and become a holy city. These two cities, one in the
                        land of Zion and one in Palestine, are to become capitals for the
                        kingdom of God during the millennium.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">In the
                        meantime, while the work of preparation is going on and Israel is being
                        gathered, many people are coming to the land of Zion saying: "Come ye,
                        and let us go up to the mountain of the Lord, to the house of the God
                        of Jacob." The Latter-day Saints are fulfilling this prediction, since
                        they are being gathered from all parts of the earth and are coming to
                        the house of the Lord in these valleys of the mountains. Here they are
                        being taught in the ways of the Lord through the restoration of the
                        gospel and by receiving blessings in the temples now erected. Moreover,
                        before many years have passed away, the Lord will command the building
                        of the City Zion, and Jerusalem in Palestine will in due time be
                        cleansed and become a holy city and the habitation of the Jews after
                        they are cleansed and are willing to accept Jesus Christ as their
                        Redeemer.</font></big></big></p>
