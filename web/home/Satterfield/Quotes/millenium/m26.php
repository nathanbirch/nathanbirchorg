                        promise of the Risen Lord to his Nephite sheep. "And it shall come to
                        pass that I will establish my people, O house of Israel." This is the
                        gathering that is now going on. And every gathering place, for the
                        scattered remnants who there assemble, becomes to them as a New
                        Jerusalem. It becomes a City of Holiness, a place where they can
                        worship the Father in spirit and in truth, a place where temples are
                        available in which they may receive the fulness of the priesthood, a
                        place where no blessing is denied them.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Then, before
                        the Second Coming, gathered Judah, as directed by Ephraim, shall build
                        up anew the Old Jerusalem and prepare therein a holy temple; and
                        gathered Ephraim, aided by Manasseh, shall build a New Jerusalem in an
                        American Zion and prepare therein a holy temple. It is to these two
                        temples in particular that the Lord shall come at his glorious return,
                        and it is from these two cities - Zion in America and Jerusalem in Old
                        Canaan - that the governance and worship of the world will be directed.
                        Thus Jesus, continuing his preaching to the Nephites, said: "And
                        behold, this people" - this Lehite remnant of Israel - will I establish
                        in this land, unto the fulfilling of the covenant which I made with
                        your father Jacob; and it shall be a New Jerusalem. Jacob is Israel,
                        and Israel is the one with whom the Lord made covenant that his seed
                        should inherit the blessings of Abraham, Isaac, and Israel. "And the
                        powers of heaven shall be in the midst of this people; yea, even I will
                        be in the midst of you." (3 Nephi 20:18, 21-22.) After gathered Israel
                        builds the New Jerusalem, the Lord will come and dwell with his people.</font></big></big></p>
