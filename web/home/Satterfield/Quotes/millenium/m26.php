                        speaks of the New Jerusalem which is to be built. For instance, we
                        read: "And it shall be called the New Jerusalem, a land of peace, a
                        city of refuge, a place of safety for the saints of the Most High God;
                        And the glory of the Lord shall be there, and the terror of the Lord
                        also shall be there, insomuch that the wicked will not come unto it,
                        and it shall be called Zion."</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Also: "Yea,
