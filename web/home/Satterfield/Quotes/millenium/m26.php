                        of these two world capitals will commence before the Second Coming and
                        continue during the Millennium. Classifying scattered Israel as
                        Gentiles, because they are not nationals of the kingdom of Judah (the
                        Jews), the Lord Jesus said: "If they will repent and hearken unto my
                        words, and harden not their hearts, I will establish my church among
                        them, and they shall come in unto the covenant and be numbered among
                        this the remnant of Jacob, unto whom I have given this land for their
                        inheritance." We are the ones here named; the gospel spoken of came
                        through Joseph Smith; we are of the house of Joseph to whom the land of
                        America has been given as an inheritance, even as it was given to the
                        Nephite portion of Joseph's seed. "And they [the Latter-day Saints]
                        shall assist my people, the remnant of Jacob, and also as many of the
                        house of Israel as shall come, that they may build a city, which shall
                        be called the New Jerusalem." Israel - all gathered remnants assisting
                        each other - shall build the New Jerusalem in America.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">"And then
                        shall they [that Israel which builds the New Jerusalem] assist my
                        people that they may be gathered in, who are scattered upon all the
                        face of the land, in unto the New Jerusalem. And then shall the power
                        of heaven come down among them; and I also will be in the midst."
                        Israel gathers; the New Jerusalem is built; Israel continues to gather;
