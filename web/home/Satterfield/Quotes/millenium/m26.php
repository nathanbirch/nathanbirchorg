                        should be established in the mountains, and people from all nations
                        should say: "Come ye, and let us go up to the mountain of the Lord, to
                        the house of the God of Jacob; and he will teach us of his ways, and we
                        will walk in his paths: for out of Zion shall go forth the law, and the
                        word of the Lord from Jerusalem."</font></big></big></p>
