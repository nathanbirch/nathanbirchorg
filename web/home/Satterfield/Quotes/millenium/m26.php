                        an even greater gathering takes place, which includes the restoration
                        of the Ten Tribes to the lands of their inheritance. Then when the Lord
                        is in the midst of his people, "shall the work of the Father commence
                        at that day, even when this gospel shall be preached among the remnant
                        of this people. Verily I say unto you, at that day shall the work of
