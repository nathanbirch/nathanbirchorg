                        whence Lehi should come -- after it should be destroyed it should be
                        built up again, a holy city unto the Lord; wherefore, it could not be a
                        new Jerusalem for it had been in a time of old; but it should be built
                        up again, and become a holy city of the Lord; and it should be built
                        unto the house of Israel."</font></big></big></p>
