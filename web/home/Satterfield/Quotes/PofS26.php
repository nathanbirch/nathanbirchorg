                        the commandments of God and endure to the end (2 Ne. 31:17-21); to gain
                        exaltation after celestial marriage the same continued devotion and
                        righteousness is required. Those who have been married in the temples
                        for
                        eternity know that the ceremony itself expressly conditions the receipt
                        of all promised blessings upon the subsequent faithfulness of the
                        husband and wife. (<em>Mormon Doctrine</em>, p. 117)</font></big></big><br
