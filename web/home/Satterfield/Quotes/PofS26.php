                                    Baptism, Gate to the
                                    Celestial Kingdom</strong></big></big></font></big></big></p>
                <div
                    style="text-align: center; font-style: italic; color: rgb(0, 0, 153);"></div>
                <p
                    style="text-align: center; font-style: italic; color: rgb(0, 0, 153);"><big><big><font
                            face="Arial">D&amp;C 76:51</font></big></big></p>
                <big><big><font face="Arial"><strong>Joseph Fielding Smith</strong></font></big></big>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Baptism is
                        the door into the celestial kingdom. All the ordinances of the gospel
                        pertain to the celestial kingdom, and any person who is faithful to
                        the covenant of baptism will be entitled to enter there, but no person
                        can receive an exaltation in the celestial kingdom without the
                        ordinances of
                        the temple. The endowments are for advancement in that kingdom, and the
                        sealings for our perfection, provided we keep our covenants and
                        obligations. </font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">People
                        baptized, and who are not endowed in the temple of the Lord, may enter
                        the celestial kingdom. But that does not mean that a baptized
                        person is going to get the exaltation in that kingdom. He is not going
                        to pass on to the fulness just by being baptized. He will not pass on
                        to the
                        fulness even after he has been baptized and received an endowment in
                        the temple. He has also to receive the other ordinances so that he can
                        become through his faithfulness and obedience a son of God. . . .</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">The first
                        principles of the gospel are principles that save. By obedience to them
                        we enter the celestial kingdom of God. Then, when we get into
                        that kingdom, if we have received the other covenants, have been true
                        and faithful to other obligations, we will advance until we shall
                        become
                        like God -- his sons, his daughters, receiving a fulness of his
                        kingdom. That is the promise. (<em>Doctrines of Salvation</em>, 2:45-46)</font></big></big><br
                    wp="BR2">
                </p>
                <big><big></big></big>
                <p><big><big><font face="Arial"><strong>Bruce R. McConkie</strong></font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Baptism
                        serves four purposes: 1. It is for the remission of sins. (D. &amp; C.
                        13; 19:31; 20:37; 33:11; 49:13; 68:27; 84:27, 64, 74; 107:20.) 2. It
                        admits the repentant person to membership in the Church and kingdom of
                        God on earth. (D. &amp; C. 20:37, 71-74.) 3. It is the gate to the
                        celestial
                        kingdom of heaven, that is, it starts a person out on the straight and
                        narrow path which leads to eternal life. (2 Ne. 9:23-24; 31:13-21.) 4.
                        It is the
                        means whereby the door to personal sanctification is opened. "Repent,
                        all ye ends of the earth, and come unto me and be baptized in my
                        name," the resurrected Lord proclaimed to the Nephites, "that ye may be
                        sanctified by the reception of the Holy Ghost that ye may stand
                        spotless before me at the last day." (3 Ne. 27:20.) [<em>Mormon Doctrine</em>,
                        p. 70]</font></big></big><br wp="BR2">
                </p>
