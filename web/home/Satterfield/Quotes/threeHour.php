                    1980, 99–101)<br>
                    <br>
                    &nbsp;</font></p>
                <p align="justify"><font face="Arial" size="4">The following materials and 
                    observations are from the presentation on the consolidated meeting schedule: <br>
                    <br>
                    1. Elder Neal A. Maxwell—<font color="#000080">“At the time the new consolidated 
                    schedule was approved by the First Presidency and the Council of the Twelve, it 
                    was made clear that its purposes were to provide ‘more time for giving attention 
                    to family life, individual study, self-improvement, and Christian service.’ We 
                    are mindful that the consolidated schedule would save much in the way of costs 
                    of gasoline, but this was not the basic purpose. It is true that circumstances 
                    in which the saints live vary widely. But the basic purposes of the Church 
                    remain unchanged. In the Book of Mormon we read about how the Sabbath was 
                    basically the teaching and learning day: ‘And there was one day in every week 
                    set apart that they should gather themselves together to teach the people, and 
                    to worship the Lord their God, and also, as often as it was in their power, to 
                    assemble themselves together’ (Mosiah 18:25). In the future, what is in our 
                    power by way of assembling ourselves together may not even be what is feasible 
                    today. But a family-centered Sabbath has been and always will be desired; so 
                    will a scripturally centered Sabbath. We hope that as we now have a fresh 
                    opportunity to study the scriptures that we will do so. We have a chance to sink 
                    our root system in the rich soil of the scriptures and become more firmly 
                    anchored therein under the consolidated schedule. Also, under this new schedule, 
                    it will be important to expedite meetings without making them seem rushed. 
                    Children are still to be blessed in fast meetings. <br>
                    <br>
                    “Space considerations require thoughtful attention. Thousands of dollars have 
                    been saved from purchasing dividers by having the Primary hold split sessions. 
                    Clearly, there are balances to be struck which we think are best determined 
                    locally. One of them, for example, is between the legitimate and competing needs 
                    of having continuity in our Primary teachers, on the one hand, and not having 
                    those sisters go forever without the privileges of participating in Relief 
                    Society, on the other. <br>
                    <br>
                    “A balance also needs to be struck in having active Scouting and exploring 
                    programs which require regular activities, on the one hand, and not badgering 
                    busy high school students with too many activities, on the other. <br>
                    <br>
                    “<i>In sum, the consolidated meeting schedule is an opportunity of major 
                        proportions.</i> I reflect that in the past years we have stretched or pulled or 
                    strained to gain a percentage point here or there in a particular church 
                    program; now we have an inspired consolidated meeting schedule that actually 
                    raises attendance 10 to 15 percent, or higher in some cases. We should welcome 
                    this opportunity with arms reaching out as never before to all who are in need 
                    of our fellowship.”</font> </font></p>
                <p align="justify"><font face="Arial" size="4"><br>
                    <br>
                    2. Elder Dean L. Larsen—“One of the things I have noticed is that there is a 
                    tendency by some to think that the Church is dramatically different now that we 
                    have the consolidated meeting schedule. That simply is not true. We are still 
                    carrying on many of the functions that we have had in the Church. The new 
                    consolidated meeting schedule has not changed the basic Church, and it has not 
                    given license for aberrations in established, prescribed programs.” <br>
                    <br>
                    3. Elder Dean L. Larsen—“There is something about the consolidated meeting 
                    schedule that has an appeal to those who have not been participating with us 
                    before. We don’t know all the reasons for this, but there is something about the 
                    newness and the innovation that has this appeal. Thus, right now during the 
                    so-called honeymoon period, while this new appeal is still there, it is crucial 
                    that we organize to take full advantage of this reactivation opportunity. And 
                    the place to do that on the ward level is in the ward correlation council 
                    meeting. We hope we will all be alerted to this opportunity for reactivation.”
                    <br>
                    <br>
                    4. Does the consolidated meeting schedule allow for special classes for inactive 
                    and prospective elders? “Yes. Ward Temple Preparation seminars and Gospel 
                    Essential classes can be held concurrently with the regular ward Sunday School 
                    classes, or they can be scheduled during other hours not designed for ward 
                    meetings.” <br>
                    <br>
                    5. How does the consolidated meeting schedule affect family home evening? 
                    “Guidelines for family home evening remain unchanged. Families should continue 
                    to hold family home evening on Monday evenings, using the manual and other 
                    approved materials. As in the past, occasional family activities may be 
                    appropriate on Monday evenings in addition to gospel instruction. Family study 
                    of the gospel on Sunday is part of keeping the Sabbath day holy and should be 
                    encouraged in addition to family home evening.” <br>
                    <br>
                    6. How can ward and stake leaders hold the necessary meetings and still maintain 
                    the spirit of the family Sabbath? “Priesthood and other leaders should be 
                    careful to schedule necessary meetings at times that do not interrupt family 
                    Sabbath activities. Careful planning will avoid lengthy meetings. Generally, 
                    times immediately before or after the combined meetings can save on travel and 
                    minimize the disruption of family worship. In areas where travel is not a 
                    significant problem, early morning hours may provide the best time for extra 
                    meetings.” <br>
                    <br>
                    7. Should meetings or other activities be scheduled during the Sunday School 
                    period? “Only the approved Sunday School classes, using the approved curriculum, 
                    are to be conducted during the Sunday School period. Such functions as bishop’s 
                    meetings, executive committee meetings, ward correlation council meetings, choir 
                    practices, etc., should not be scheduled during this period.” <br>
                    <br>
                    8. A bishop reported, “We try to keep our Sundays free from any leadership or 
                    organizational meetings. On the weekday Mutual night, we have found a way to 
                    consolidate other necessary meetings and on that night we hold priesthood 
                    executive committee meeting, ward correlation council, and welfare services 
                    meeting. Our ward officers and teachers come to Church on Sunday and then 
                    generally on our Mutual night once or twice a month. The rest of the time is 
                    left, for other duties relating to Christian service and being together with 
                    their families.” <br>
                    <br>
                    9. “Women are not to be members of Sunday School presidencies. Men are not to be 
                    called to serve in Primary presidencies.” <br>
                    <br>
                    10. What about the basic teacher development course? Elder Dean Larsen—“We are 
                    not using more Sunday School space than before consolidation. There should be no 
                    reason why space cannot continue to be provided for teacher development, and 
                    certainly we do not wish to lose the teacher development basic course which 
                    should be and is implemented under the direction of the Sunday School and should 
                    continue to be a part of that regular curriculum.” <br>
                    <br>
                    11. Barbara Smith, general president of the Relief Society—“Many people call and 
                    ask about the stake Relief Society board—what is it composed of? We want members 
                    to know it should include the executive officers, plus at least eight other 
                    members, so that it is a board of twelve members. We hope Young Adults are 
                    considered for Relief Society presidencies and for teaching positions. They have 
                    much vitality and enthusiasm to give, while the older sisters can give stability 
                    and maturity.” <br>
                    <br>
                    12. “Ward and stake Relief Society choirs may still be organized to provide 
                    music for stake conferences, sacrament meetings, funerals, and other occasions.”
                    <br>
                    <br>
                    13. Elaine Cannon, general president of the Young Women—“Stake advisers for 
                    Young Women are now generally not needed. If they are deemed necessary in a 
                    large stake, their responsibilities would be assigned by the stake Young Women 
                    Presidency. The same is true of ward structure; advisers are needed only when 
                    the young women population demands.” <br>
                    <br>
                    14. The Young Women’s current manuals have twenty-two lessons. What shall they 
                    teach for additional lessons? “Provide weekly spiritual experiences for young 
                    women from the following sources: age group manuals, ‘The Latter-day Saint 
                    Woman,’ Gospel Principles, ‘My Personal Progress,’ Young Women Guidebook (youth 
                    leadership lessons), Church magazines, talks from general women’s meetings, 
                    approved films from meetinghouse library, Teaching, No Greater Call, and guest 
                    speakers, occasionally invited to a class or combination of classes as approved 
                    by the bishopric.” <br>
                    <br>
                    15. Elder Neal A. Maxwell—“A very important role is played by the bishop’s youth 
                    committee and much of the planning for youth on the ward level will occur there. 
                    We see the ward activities committee as becoming more and more crucial as the 
                    bishopric uses them do some significant things.” <br>
                    <br>
                    16. “Those who should attend the bishopric youth committee meeting are the 
                    bishopric, the chairman of the ward activities committee, ward Young Men and 
                    Young Women presidents, the first assistant to the bishop in the priests quorum, 
                    the presidents of the teachers and deacons quorum, and Young Women class 
                    presidents.” <br>
                    <br>
                    17. What is the recommended way to treat youth firesides? Elder Robert L. 
                    Backman—“I don’t think there needs to be much change from the way we have done 
                    this before. A caring bishopric, caring for both the needs of the families and 
                    the needs of the youth, can see the need to continue these important firesides 
                    at an appropriate frequency.” <br>
                    <br>
                    18. Does the new consolidated meeting schedule do away with any ward and stake 
                    activities such as plays and roadshows? “No. There continues to be a need for 
                    appropriate high quality activities. Some events, such as athletic contests, 
                    play rehearsals, etc., may need to be scheduled at other times during the week. 
                    Such scheduling should be approved by the bishopric through the correlation 
                    council.” <br>
                    <br>
                    19. What is the policy regarding competitive athletics and sports? “Competitive 
                    sports programs should continue to receive emphasis and support at all levels of 
                    play, including stake, region, multiregion, etc. The scope of the program for a 
                    given area should be determined by the Executive Administrator, Regional 
                    Representatives, and stake presidents involved. Aaronic Priesthood quorum and 
                    Young Women leaders maintain stewardship for all young men and young women 
                    between the ages of twelve and eighteen. They will determine the extent of the 
                    Young Men and Young Women sports program. It is to be organized, implemented, 
                    and supervised by the ward or stake Activities Committee.” <br>
                    <br>
                    20. How often should priests, teachers, and deacons quorums meet on a week night 
                    to participate in Scouting, Venturing, Exploring, and other activities? Elder 
                    Robert L. Backman—“The Aaronic Priesthood quorums will usually meet weekly, on a 
                    week night other than Monday, in addition to their regular quorum meeting on 
                    Sunday—unless travel or other restrictions require meeting only once a week.”
                    <br>
                    <br>
                    21. What about holding Primary on stake conference days? Elder Dean L. 
                    Larsen—“The initial guidelines did carry a notation that Primary on stake 
                    conference days was to be discontinued, but that was in error. What we have 
                    called junior Sunday School and is now Primary may be held on the same basis as 
                    in the past on stake conference Sundays for the children who have been attending 
                    those services.” <br>
                    <br>
                    22. A bishop—“Our Primary leaders are saying, ‘We never had this kind of 
                    reverence on Tuesday afternoon, our usual Primary time. On Sunday, those 
                    children are prepared to have a spiritual experience, and we don’t have the 
                    kinds of problems we formerly had.’ “ <br>
                    <br>
                    23. If a child turned three years old in January 1980 and has been enrolled in 
                    Sunday School, what Primary class will the child attend? “The child should 
                    attend the nursery until the beginning of the next curriculum year; then he 
                    would be enrolled in the Sunbeam class. Three-year-old children should be 
                    enrolled in Primary according to the entrance and enrollment policy as stated in 
                    the Primary Handbook, page 38.” <br>
                    <br>
                    24. What do we do with the twelve-year-old boy who graduates from Primary in 
                    midyear and enters course 12 “Under the consolidation meeting schedule, a boy 
                    who reaches the age of 12 after the start of the curriculum year will become a 
                    member of the Sunday School and enter course 12 at that time. He will move to 
                    course 13 with the rest of the class at the beginning of a new curriculum year, 
                    regardless of how long he has been in course 12.” <br>
                    <br>
                    25. Will there still be ward choirs? “Yes, ward choirs continue to be an 
                    essential part of the Church program. It is recommended that choirs sing in 
                    sacrament meeting at least twice a month. Special choirs, soloists, or groups 
                    may be used on those Sundays when choirs do not sing. Practices may be held at 
                    any time convenient to the members; however, they should not be held during the 
                    Sunday School hour.” </font></p>
