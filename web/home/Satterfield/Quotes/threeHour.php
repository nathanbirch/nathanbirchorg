                <p align="justify"><font face="Arial" size="6" color="#000080">In March of 1980, 
                    the first of several organizational changes were made.&nbsp; That month, the 
                    consolidated meeting schedule was implemented in the United States and Canada.&nbsp; 
                    The rest of the church began implementation in May.</font></p>
                <p align="justify">&nbsp;</p>
                <p align="justify"><font face="Arial" size="6" color="#000080">Appearing in the
                    <i>Ensign</i> magazine was the following:</font></p>
                <p align="justify">&nbsp;</p>
                <p align="center">&nbsp;</p>
                <p align="center">&nbsp;</p>
                <p align="center"><font face="Arial" size="6"><b>Church Consolidates Meeting 
                        Schedules</b> <br>
                    </font><font face="Arial" size="4"><br>
                    (<i>Ensign</i>, March 1980, pp. 73–78)<br>
                    <br>
                    &nbsp;</font></p>
                <p align="justify"><font face="Arial" size="4">A schedule combining meetings 
                    into a three-hour Sunday block has been introduced throughout the Church. <br>
                    <br>
                    The First Presidency announced the changes in a letter accompanying instructions 
                    for stake presidencies and bishoprics. Wards and branches throughout the United 
                    States and Canada are implementing the program March 2. Those outside the U.S. 
                    and Canada will begin May 4. <br>
                    <br>
                    The Churchwide initiation of the program follows several months of pilot studies 
                    in which selected stakes tried variations of the program—with marked success. 
                    Attendance at sacrament meeting and Relief Society increased an average of 10 to 
                    15 percent in the pilot areas. Priesthood, Young Men, Young Women, and Sunday 
                    School attendance rose 8 to 10 percent. Primary also had an increase, though 
                    slightly lower. The consolidated schedule increased Church attendance among the 
                    less active, with fewer people leaving or skipping meetings. <br>
                    <br>
                    The new schedule groups sacrament, Sunday School, Melchizedek and Aaronic 
                    Priesthood, Relief Society, Young Women, and Primary meetings into a 
                    specifically divided three-hour time block. Ten-minute breaks separate the 
                    meetings. Local leaders are being given two options of how to arrange the 
                    meetings. In the first option, called Option A, all ward members are together 
                    for sacrament meeting. The ward then separates into Sunday School classes (for 
                    those twelve and older), Primary classes (for those under twelve), and nursery 
                    (ages eighteen months to three years). While Primary and nursery children stay 
                    in sessions lasting one hour and forty minutes, those over twelve attend Sunday 
                    School classes and then regroup for quorum, Relief Society, and Young Women 
                    meetings. </font></p>
                <p align="justify">&nbsp;</p>
                <p align="justify">&nbsp;</p>
                <p align="center"><font face="Arial" size="4"><br>
                    </font>
                    <img alt="Image" src="http://www.lds.org/images/Magazines/Ensign/Archive/ensignlp.nfo:o:2e64.jpg" align="center" width="519" height="533"><font face="Arial" size="4"><br>
                    </font><font face="Arial" size="4"><br>
                    Option A<br>
                    <br>
                    <br>
                    <br>
                    <br>
                    <br>
                    <br>
                    <br>
                    </font>
                    <img alt="Image" src="http://www.lds.org/images/Magazines/Ensign/Archive/ensignlp.nfo:o:2e66.jpg" align="center" width="483" height="484"><font face="Arial" size="4"><br>
                    <br>
                    Option B<br>
                    <br>
                    <br>
                    &nbsp;</font></p>
                <p align="justify"><font face="Arial" size="4">In the second option, Option B, 
                    ward members first meet in priesthood, Relief Society, Young Women, and Primary 
                    meetings. Sacrament meeting is held last. <br>
                    <br>
                    Under the new program Junior Sunday School is discontinued, since children are 
                    taught on Sundays in Primary. Prayer meetings for all organizations are also 
                    discontinued. Young Men and Young Women weekday Mutual activities will usually 
                    be held on a ward activity day. One day of the week other than Sunday or Monday 
                    may be designated as ward activity day, on which activities for organizations or 
                    the entire ward, as much as possible, are scheduled. <br>
                    <br>
                    The consolidated meeting schedule reemphasizes personal and family 
                    responsibility for learning, living, and teaching the gospel. It also allows 
                    Church members more time for personal gospel study, service to others, and 
                    meaningful activities. <br>
                    <br>
                    <br>
                    <b>Objectives of the Schedule </b><br>
                    <br>
                    The instructions for stake presidencies and bishoprics indicate the purpose and 
                    major objectives of the new schedule: </font></p>
                <blockquote>
                    <p align="justify"><font face="Arial" size="4"><br>
                        <br>
                        “The purpose of the consolidated meeting schedule is (1) to reemphasize 
                        personal and family responsibility for learning, living, and teaching the 
                        gospel and (2) to allow Church members more time for personal gospel study, 
                        for service to others, and for meaningful activities. The major objectives 
                        of the new schedule are to— <br>
                        <br>
                        “1. Help every Latter-day Saint home become a place where family members 
                        love to be, where they can enrich their lives and find mutual love, support, 
                        appreciation, and encouragement. <br>
                        <br>
                        “2. Emphasize home-centered Sabbath activities. <br>
                        <br>
                        “3. Make more flexible a weekday activity program for all members. <br>
                        <br>
                        “4. Reduce the amount of travel by Church members and provide opportunities 
                        for family members to travel together and participate in Church activities.
                        <br>
                        <br>
                        “5. Conserve energy resources and reduce the nonessential costs required for 
                        members to participate in Church activities.” </font></p>
                </blockquote>
                <p align="justify"><font face="Arial" size="4"><br>
                    <br>
                    The instructions also state: “Local leaders should use their own initiative to 
                    solve specific local problems. Leaders have the responsibility to follow the 
                    guidelines for consolidating meetings provided, but they should rely on 
                    inspiration to find ways of making the schedule work successfully in their 
                    areas.” <br>
                    <br>
                    <br>
                    <b>Suggestions for Individual and Family Sabbath-Day Activities </b><br>
                    <br>
                    Because the new schedule will give families time together on Sundays, parents 
                    will want to plan activities for the Sabbath that will spiritually strengthen 
                    the family. They may wish to spend some time with the family each Sunday in 
                    gospel discussion and instruction, under the direction of the head of the 
                    household. They may use the scriptures, the most recent general conference 
                    talks, family home evening manuals, Church publications, and other publications 
                    as a resource. <br>
                    <br>
                    Other appropriate Sunday activities include (1) writing personal and family 
                    journals, (2) holding family councils, (3) establishing and maintaining family 
                    organizations for the immediate and extended family, (4) personal interviews 
                    between parents and children, (5) writing to relatives and missionaries, (6) 
                    genealogy, (7) visiting relatives and those who are ill or lonely, (8) 
                    missionary work, (9) reading stories to children, and (10) singing Church hymns.
                    <br>
                    <br>
                    Single adults will also wish to spend their time on the Sabbath day in study and 
                    in such other activities as those suggested above. Under the direction of the 
                    ward single adult committee, they may also hold firesides, give service to the 
                    sick and elderly, and participate in other appropriate Sabbath-day activities.
                    <br>
                    <br>
                    Many activities are not appropriate on the Sabbath day, such as gardening, 
                    family parties, and household projects. Families may wish to plan family 
                    household and recreational activities for Saturday or other weekdays when 
                    parents are home with their children. </font></p>
                <p align="justify"><font face="Arial" size="4"><br>
                    <br>
                    <b>Guidelines for Leadership and Other Meetings </b><br>
                    <br>
                    Guidelines for implementing the consolidated schedule give instructions on 
                    holding administrative meetings and special functions. Stake presidency 
                    meetings, bishopric meetings, organization presidency meetings, priesthood 
                    executive committee meetings, correlation councils, choir practices, firesides, 
                    preparation meetings, priesthood leadership meetings, and so forth——all are to 
                    follow these guidelines: <br>
                    <br>
                    “1. High priority should be given to individual and family needs and to those 
                    areas of service and activity that relate to the home. <br>
                    <br>
                    “2. Neither administrative and other meetings nor activities should prevent 
                    individuals and family members from caring for basic individual and family 
                    responsibilities. <br>
                    <br>
                    “3. Emphasis should be upon actual service with a minimum of time used in 
                    preparation for service.” </font></p>
                <p align="justify"><font face="Arial" size="4"><br>
                    <br>
                    <b>Leaders scheduling special meetings are to consider these questions: <br>
                    </b><br>
                    “Does the schedule preserve an adequate block of time for all families and 
                    individuals on Sunday so they can give attention to gospel study, personal 
                    spiritual development, gospel teaching in the home, and appropriate acts of 
                    Christian service?” And, “Are leaders able, as much as possible, to go to and 
                    return from the Sunday block of meetings with their wives and children?” <br>
                    <br>
                    &nbsp;</font></p>
                <p align="justify"><font face="Arial" size="4"><b>Leaders are also instructed to 
                        observe the following guidelines: </b><br>
                    <br>
                    “1. Generally, ward administrative meetings should be held at one of the 
                    following times: (a) a designated weekday, (b) on Sunday, when it would least 
                    interfere with family travel and home-centered activities, (c) during the week 
                    when it would require the least special travel and a minimum of the leaders’ 
                    time away from home. <br>
                    <br>
                    “2. A calendar of activities and meetings should be planned at least three 
                    months in advance by stake and ward correlation councils. The activities 
                    committee would prepare the activity portion of the calendar. The calendar 
                    information should be shared with each family to aid them in planning family 
                    activities. There should be a careful correlation between ward and stake 
                    meetings and activities. <br>
                    <br>
                    “3. Insofar as possible stake and ward meetings should be planned to avoid 
                    conflict with the Sunday meeting schedules in wards. Saturdays should be 
                    reserved for family activities as much as possible. <br>
                    <br>
                    “4. Administrative meetings should begin and end on time. Agendas should be 
                    prepared so that only essential matters are dealt with. <br>
                    <br>
                    “5. Where unusual distances and other special circumstances exist, consideration 
                    should be given to allow flexibility of meeting frequency so that unreasonable 
                    demands are not made upon the time and resources of the members. <br>
                    <br>
                    “6. Where several wards are scheduled in a building, it may be convenient to 
                    designate a day of the week for each ward for activities.” <br>
                    <br>
                    &nbsp;</font></p>
                <p align="justify"><font face="Arial" size="4"><b>“Ready to Move Forward” </b>
                    <br>
                    <br>
                    The changes in the meeting schedules come at a time when Church leaders—notably 
                    President Spencer W. Kimball—have encouraged members to accept change and 
                    continued inspired guidance. In April and October 1979 general conferences, 
                    President Kimball spoke of pending changes that would enable families to live 
                    the gospel more effectively in their homes: <br>
                    <br>
                    “This impression weighs upon me—that the Church is at a point in its growth and 
                    maturity when we are at last ready to move forward in a major way. Some 
                    decisions have been made and others pending, which will clear the way, 
                    organizationally. But the basic decisions needed for us to move forward, as a 
                    people, must be made by the individual members of the Church. The major strides 
                    which must be made by the Church will follow upon the major strides to be made 
                    by us as individuals. …<br>
                    <br>
                    “Only as we see clearly the responsibilities of each individual and the role of 
                    families and the home can we properly understand that the priesthood quorums and 
                    the auxiliary organizations, even wards and stakes, exist primarily to help 
                    members live the gospel in the home. However, church programs should always 
                    support and never detract from gospel-centered family activities” (<i>Ensign</i>, 
                    May 1979, pp. 82–83). <br>
                    <br>
                    Six months later President Kimball told Regional Representatives: <br>
                    <br>
                    “We see ourselves as positioning our people so that the Latter-day Saints can 
                    give greater attention to family life, can focus more on certain simple and 
                    basic things, can render more Christian service, and can have greater 
                    effectiveness in all these things—through the process of simplification, 
                    scheduling, proper priorities, and by honoring the priesthood line” (Address 
                    delivered at Regional Representatives’ Seminar, 5 Oct. 1979, in Salt Lake City).
                    <br>
                    <br>
                    <i>The consolidated meeting schedule has been designed to help fulfill those 
                        purposes. </i></font></p>
