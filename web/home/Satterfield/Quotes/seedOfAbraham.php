                    be the seed of Abraham? Scripturally it has a deeper meaning than being
                    his literal descendants. The Lord made a covenant with Abraham, the
                    great patriarch, that all nations would be blessed through him (see
                    Gen. 18:18; Gal. 3:8; 3 Ne. 20:25, 29). Any man or woman can claim the
                    blessings of Abraham. They become his seed and heirs to the promised
                    blessings by accepting the gospel, being baptized, entering into temple
                    marriage, being faithful in keeping their covenants, and helping to
                    carry the gospel to all the nations of the earth.<br>
