                    To be empowered to “bear this ministry and Priesthood unto all
                    nations,” (Abr. 2:9) a man must receive the Melchizedek Priesthood with
                    its blessings. Then through faithfulness he becomes an heir to the
                    fulness of eternal life. For as Paul said, “And if ye be Christ’s, then
                    are ye Abraham’s seed, and heirs according to the promise” (Gal. 3:29).<br>
                    <br>
                    As the seed of Abraham, we have some obligations. We are commanded to
                    come to Christ by doing “the works of Abraham” (John 8:39; see also
                    John 8:32–50). These works include obeying God, receiving and keeping
                    priesthood and temple ordinances and covenants, preaching the gospel,
                    building a family unit and teaching our children, and being faithful to
                    the end.<br>
                    <br>
                    It is interesting that the Lord used the word seed in His promise to
                    Abraham. It has a fuller meaning than posterity because it means to
                    multiply the blessings of the covenant of Abraham “unto all nations”
                    (Abr. 2:9). The Lord promised Abraham a posterity “as innumerable as
                    the stars” or “the sand upon the seashore” (D&amp;C 132:30). <br>
                    <br>
