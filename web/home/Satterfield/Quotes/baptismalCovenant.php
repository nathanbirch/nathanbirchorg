                <p style="font-family: arial;"><big><strong>Bruce R. McConkie</strong></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>A covenant is a
                    contract in which two or
                    more parties promise and agree to do certain things. Gospel covenants
                    are made between God and men. Baptism is the
                    covenant in which men promise to keep the commandments of God and He
                    promises to give them eternal life. There are many ways in which <u>men's
                        promises</u>
                    might be recounted. Alma, at the waters of Mormon, summarized them by
                    having each repentant soul swear in substance and thought content:</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>(a) to forsake
                    the world, to repent of
                    all his or her sins, to join the true church, "to come into the fold of
                    God."</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>(b) to become a
                    saint, to be numbered
                    with the true believers, to take upon me the name of Christ and become
                    one of his family, "and to be called [one of] his
                    people."</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>(c) to love and
                    serve his fellowmen, to
                    esteem others as himself, and to bear the burdens of his brethren in
                    the church `that they may be light."</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>(d) to visit the
                    fatherless and the
                    widows in their afflictions, to care for the sick, the helpless, and
                    the bereaved, and "to mourn with those that mourn."</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>(e) to proclaim
                    peace, to spread solace,
                    and to "comfort those that stand in need of comfort."</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>(f) to preach
                    the gospel, to raise the
                    warning voice, to call sinners to repentance, and to stand as a witness
                    of Christ "at all times and in all things, and in all
                    places ... even until death."</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>(g) to serve the
                    Lord "and keep his
                    commandments" - a promise that is itself infinite and endless, and is
