                <p style="font-family: arial; margin-left: 40px;"><big>To be baptized
                    is to enter into a
                    covenant of commission. But to fail to be baptized when one is
                    convinced the work is divine is a sin of omission, and penalties
                    will be assessed for failure to meet this requirement. Tens of
                    thousands of people having heard the gospel have failed to be baptized,
                    giving trivial excuses. This
                    is a most serious sin. The Lord told Nicodemus that he and others would
                    not even see the kingdom of God if they rejected the required baptism.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>The covenants we
                    make with God involve
                    promises to do, not merely to refrain from doing, to work righteousness
                    as well as to avoid evil. The children of
                    Israel made such covenants through Moses, saying, "All that the Lord
                    hath spoken we will do" (Exodus 19:8, italics added), though hardly was
                    Moses' back
                    turned until they had broken their promise through wrongdoing. In the
                    baptismal waters we give a similar undertaking and we repledge it in
                    the ordinance of the
                    sacrament. Not to honor these pledges, to refuse to serve or to accept
                    responsibility and do less than one's best at it, is a sin of omission.
