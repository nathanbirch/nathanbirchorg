                    inheritance by learning what I have learned and by living as I have
                    lived. I will give you the laws and principles by
                    which I have acquired my wisdom and stature. Follow my example,
                    mastering as I have mastered, and you will
                    become as I am, and all that I have will be yours.”</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span><br>
                    </span>This
                    parable parallels the pattern of heaven. The gospel of Jesus Christ
                    promises the incomparable inheritance
                    of eternal life, the fulness of the Father, and reveals the laws and
                    principles by which it can be obtained. . . .</font></p>
