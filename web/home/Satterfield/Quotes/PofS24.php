                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span></span>That
                    every soul shall find his place in the hereafter, that he shall be
                    judged and assigned according to what he
                    is, is no less truly scriptural than reasonable. He shall inherit
                    according to his capacity to receive, enjoy, and utilize.
                    This is made sublimely plain by revelation given in 1832, in which we
                    read: “For he who is not able to abide the law
                    of a celestial kingdom cannot abide a celestial glory. And he who
                    cannot abide the law of a terrestrial kingdom cannot
                    abide a terrestrial glory. And he who cannot abide the law of a
                    telestial kingdom cannot abide a telestial glory;
                    therefore he is not meet for a kingdom of glory. Therefore he must
                    abide a kingdom which is not a kingdom of glory.”
                    (<i>Articles of Faith</i>, p.408)</font></p>
                <font style="font-family: arial;" size="+2"><br>
                </font>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span
