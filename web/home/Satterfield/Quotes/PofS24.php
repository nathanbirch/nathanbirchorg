                <p style="text-align: center; font-weight: bold; font-family: arial;"><font
                        size="+2">God
                    Will Judge Men
                    <br>
                    According to Their Works <i>and</i> Desires</font></p>
                <font style="font-family: arial;" size="+2"><br>
                </font>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span
                        style="font-weight: bold;">D&amp;C 137</span></font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>9
                    For I, the Lord, will judge all men according to their works, according
                    to the desire of their hearts.</font></p>
                <font style="font-family: arial;" size="+2"><br>
                </font>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span
                        style="font-weight: bold;">Alma 41</span></font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>3
                    And it is requisite with the justice of God that men should be judged
                    according to their works; and if their
                    works were good in this life, and the desires of their hearts were
                    good, that they should also, at the last day, be
