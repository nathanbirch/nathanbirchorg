                    The one raised to happiness according to his desires of happiness, or
                    good according to his desires of good;
                    and the other to evil according to his desires of evil; for as he has
                    desired to do evil all the day long even so shall he
                    have his reward of evil when the night cometh.</font></p>
                <font style="font-family: arial;" size="+2"><br>
                </font>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span
                        style="font-weight: bold;">Brigham Young</span></font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>This
                    is a subject I have reflected upon a great deal, and I have come to the
                    conclusion that we shall be judged
                    according to the deeds done in the body and according to the thoughts
                    and intents of the heart. (<i>Discourses of
                        Brigham Young</i>, p.382)</font></p>
                <font style="font-family: arial;" size="+2"><br>
                </font>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span
                        style="font-weight: bold;">Neal A. Maxwell</span></font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>God
                    thus takes into merciful account not only our desires and our
                    performance, but also the degrees of difficulty
                    which our varied circumstances impose upon us. No wonder we will not
                    complain at the final judgment, especially
                    since even the telestial kingdom’s glory “surpasses all understanding”
                    (D&amp;C 76:89). God delights in blessing us,
                    especially when we realize “joy in that which [we] have desired”
                    (D&amp;C 7:8). [“According to the Desire of [Our]
                    Hearts,”<i> Ensign</i>, Nov. 1996, p. 21]</font></p>
                <font style="font-family: arial;" size="+2"><br>
                </font>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span
                        style="font-weight: bold;">Dallin H. Oaks</span></font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>God
                    judges us not only for our acts, but also for the desires of our
                    hearts. He has said so again and again. This
                    is a challenging reality, but it is not surprising. Agency and
                    accountability are eternal principles. We exercise our free
                    agency not only by what we do, but also by what we decide, or will, or
                    desire. Restrictions on freedom can deprive
                    us of the power to do, but no one can deprive us of the power to will
                    or desire. Accountability must therefore reach
                    and attach consequences to the desires of our hearts.</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>This
                    principle applies both in a negative way – making us guilty of sin for
                    evil thoughts and desires – and in a
                    positive way – promising us blessings for righteous desires. . . .</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>The
                    desires of our hearts will be an important consideration in the final
                    judgment. Alma taught that God
                    “granteth unto men according to their desire, whether it be unto death
                    or unto life; … according to their wills, whether
                    they be unto salvation or unto destruction. Yea, … he that knoweth good
                    and evil, to him it is given according to his
                    desires.” (Alma 29:4–5.)</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>That
                    is a sobering teaching, but it is also a gratifying one. It means that
                    when we have done all that we can, our
                    desires will carry us the rest of the way. It also means that if our
                    desires are right, we can be forgiven for the mistakes
                    we will inevitably make as we try to carry those desires into effect.
                    What a comfort for our feelings of inadequacy!
                    As Alma said:</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>“It
                    is requisite with the justice of God that … if their works were good in
                    this life, and the desires of their hearts
                    were good, that they should also, at the last day, be restored unto
                    that which is good. … </font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>“If
                    he hath repented of his sins, and desired righteousness until the end
                    of his days, even so he shall be rewarded
                    unto righteousness.” (Alma 41:3, 5–6.)</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>Similarly,
                    in this dispensation the Lord has revealed that he “will judge all men
                    according to their works,
                    according to the desires of their hearts.” (D&amp;C 137:9.) I caution
                    against two possible misunderstandings: First, we
                    must remember that desire is a substitute only when action is truly
                    impossible. If we attempt to use impossibility of
                    action as a cover for our lack of true desire and therefore do not do
                    all that we can to perform the acts that have been
                    commanded, we may deceive ourselves, but we will not deceive the
                    Righteous Judge.</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>In
                    order to serve as a substitute for action, desire cannot be
                    superficial, impulsive, or temporary. It must be
                    heartfelt, through and through. To be efficacious for blessings, the
                    desires of our hearts must be so genuine that they
                    can be called godly.</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>Second,
                    we should not assume that the desires of our hearts can serve as a
                    substitute for an ordinance of the
                    gospel. Consider the words of the Lord in commanding two gospel
                    ordinances: “Verily, verily, I say unto thee, except
                    a man be born of water and of the Spirit, he cannot enter into the
                    kingdom of God.” (John 3:5.) And in respect to the
                    three degrees in the celestial glory, modern revelation states, “In
                    order to obtain the highest, a man must enter into
                    this order of the priesthood [meaning the new and everlasting covenant
                    of marriage].” (D&amp;C 131:2.) No exception
                    is implied in these commands or authorized elsewhere in the scriptures.</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>In
                    the justice and mercy of God, these rigid commands pertaining to
                    essential ordinances are tempered by divine
                    authorization to perform those ordinances by proxy for those who did
                    not have them performed in this life. Thus, a
                    person in the spirit world who so desires is credited with
                    participating in the ordinance just as if he or she had done
                    so personally. In this manner, through the loving service of living
                    proxies, departed spirits are also rewarded for the
                    desires of their hearts.</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>In
                    summary, under the law of God we are accountable for our feelings and
                    desires as well as our acts. Evil
                    thoughts and desires will be punished. Acts that seem to be good bring
                    blessings only when they are done with real
                    and righteous intent. On the positive side, we will be blessed for the
                    righteous desires of our hearts even though some
                    outside circumstance has made it impossible for us to carry those
                    desires into action.</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>To
                    paraphrase Paul’s teaching in Romans 2:29, he is a true Latter-day
                    Saint who is one inwardly, whose
                    conversion is that of the spirit, in the heart, whose praise is not of
                    men for outward acts, but of God, for the inward
                    desires of the heart. (“The Desires of Our Hearts,” <i>Ensign</i>,
                    June 1986, pp.64-67)</font></p>
                <font style="font-family: arial;" size="+2"><br>
                <br>
                </font>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span
                        style="font-weight: bold;">Neal A. Maxwell</span></font></p>
                <p
                    style="text-align: justify; text-indent: -0.275in; margin-left: 0.275in; font-family: arial;"><font
                        size="+2">•<span>
                    </span>Whether
                    in their conception or expression, our desires profoundly affect the
                    use of our moral agency. Desires
                    thus become real determinants, even when, with pitiful naivete, we do
                    not really want the consequences of our
                    desires.</font></p>
                <p
                    style="text-align: justify; margin-left: 0.275in; font-family: arial;"><font
                        size="+2">Desire
                    denotes a real
                    longing or craving. Hence righteous desires are much more than passive
                    preferences
                    or fleeting feelings. Of course our genes, circumstances, and
                    environments matter very much, and they shape
                    us significantly. Yet there remains an inner zone in which we are
                    sovereign, unless we abdicate. In this zone lies
                    the essence of our individuality and our personal accountability.</font></p>
                <p
                    style="text-align: justify; margin-left: 0.275in; font-family: arial;"><font
                        size="+2">Therefore,
                    what we
                    insistently desire, over time, is what we will eventually become and
                    what we will receive
                    in eternity. “For I [said the Lord] will judge all men according to
                    their works, according to the desire of their
                    hearts” (D&amp;C 137:9; see also Jer. 17:10). Alma said, “I know that
                    [God] granteth unto men according to their
                    desire, … I know that he allotteth unto men … according to their wills”
                    (Alma 29:4). To reach this equitable end,
                    God’s canopy of mercy is stretched out, including “all that shall die
                    henceforth without a knowledge of [the
                    gospel], who would have received it with all their hearts, shall be
                    heirs of that kingdom;</font></p>
                <p
                    style="text-align: justify; margin-left: 0.275in; font-family: arial;"><font
                        size="+2">“For
                    I, the Lord, will
                    judge all men according to their works, according to the desire of
                    their hearts” (D&amp;C
                    137:8–9).</font></p>
                <p
                    style="text-align: justify; margin-left: 0.275in; font-family: arial;"><font
                        size="+2">God
                    thus takes into
                    merciful account not only our desires and our performance, but also the
                    degrees of
                    difficulty which our varied circumstances impose upon us. No wonder we
                    will not complain at the final
                    judgment, especially since even the telestial kingdom’s glory
                    “surpasses all understanding” (D&amp;C 76:89). God
                    delights in blessing us, especially when we realize “joy in that which
                    [we] have desired” (D&amp;C 7:8). (
                    “According to the Desire of [Our] Hearts,” <i>Ensign</i>, Nov. 1996,
                    p. 21)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                    <br>
                    </font></p>
                <p
                    style="text-align: justify; text-indent: -0.275in; margin-left: 0.275in; font-family: arial;"><font
                        size="+2">•<span>
                    </span>Actually,
                    everything depends – initially and finally – on our desires. These
                    shape our thought patterns. Our
                    desires thus precede our deeds and lie at the very cores of our souls,
                    tilting us toward or away from God (see
                    D&amp;C 4:3). God can “educate our desires” (see Joseph F. Smith,
                    Gospel Doctrine, 5th ed., Salt Lake City: Deseret
                    Book Co., 1939, p. 297). Others seek to manipulate our desires. But it
                    is we who form the desires, the “thoughts
                    and intents of [our] hearts” (Mosiah 5:13).</font></p>
                <p
                    style="text-align: justify; margin-left: 0.275in; font-family: arial;"><font
                        size="+2">The
                    end rule is
                    “according to [our] desires … shall it be done unto [us]” (D&amp;C
                    11:17), “for I, the Lord, will
                    judge all men according to their works, according to the desire of
                    their hearts” (D&amp;C 137:9; see also Alma 41:5;
                    D&amp;C 6:20, 27). One’s individual will thus remains uniquely his. God
                    will not override it nor overwhelm it.
                    Hence we’d better want the consequences of what we want! (“Swallowed Up
                    in the Will of the Father,” <i>Ensign</i>,
                    Nov. 1995, p. 23)</font></p>
                <font style="font-family: arial;" size="+2"><br>
                </font>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span
                        style="font-weight: bold;">Russell M. Nelson</span></font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>This
                    question reminds us that eventually you (and I) are going to die, be
                    resurrected, be judged, and be awarded
                    a place in eternal realms. (See 1 Cor. 15:22; Alma 12:24; Alma 21:9;
                    Hel. 14:16–17; D&amp;C 138:19.) With each
                    passing sunset, you are closer to that inevitable day of judgment. Then
                    you will be asked to account for your faith,
                    your hopes, and your works. The Lord said:</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>“Every
                    man may act in doctrine and principle … according to the moral agency
                    which I have given unto him,
                    that every man may be accountable for his own sins in the day of
                    judgment.” (D&amp;C 101:78; see also Mosiah 3:24.)</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>As
                    all will be resurrected, your physical body will then be restored to
                    its proper and perfect frame. (See Alma
                    11:43; Alma 40:23.) The day of your resurrection will be a day of
                    judgment that will determine the kind of life you
                    shall have hereafter.</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>That
                    judgment will consider not only your actions, but also your innermost
                    intent and heartfelt desires. Your
                    everyday thoughts have not been lost. Scriptures speak of the “bright
                    recollection” (Alma 11:43) and “perfect
                    remembrance” (Alma 5:18) that your mind will provide in times of divine
                    judgment.</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>The
                    Lord knows the desires of our hearts. At the time of judgment, surely
                    the special yearnings of single sisters
                    and childless couples, for example, will be given compassionate
                    consideration by Him who said:</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>“I,
                    the Lord, will judge all … according to their works, according to the
                    desire of their hearts.” (D&amp;C 137:9; see
                    also Heb. 4:12; Alma 18:32; D&amp;C 6:16; D&amp;C 33:1; D&amp;C
                    88:109.) [“Choices,” <i>Ensign</i>, Nov. 1990, p. 75]</font></p>
                <font style="font-family: arial;" size="+2"><br>
                </font>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span
                        style="font-weight: bold;">Marvin J. Ashton</span></font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>When
                    the Lord measures an individual, He does not take a tape measure around
                    the person’s head to determine
                    his mental capacity, nor his chest to determine his manliness, but He
                    measures the heart as an indicator of the
                    person’s capacity and potential to bless others.</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>Why
                    the heart? Because the heart is a synonym for one’s entire makeup. We
                    often use phrases about the heart
                    to describe the total person. Thus, we describe people as being
                    “big-hearted” or “goodhearted” or having a “heart of
                    gold.” Or we speak of people with faint hearts, wise hearts, pure
                    hearts, willing hearts, deceitful hearts, conniving
                    hearts, courageous hearts, cold hearts, hearts of stone, or selfish
                    hearts.</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>The
                    measure of our hearts is the measure of our total performance. As used
                    by the Lord, the “heart” of a person
                    describes his effort to better self, or others, or the conditions he
                    confronts.</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>A
                    question I suggest to you is this: How do you measure up? Ultimately
                    you and I will be judged not only for
                    our actions, but also for the desires of our hearts. This truth was
                    revealed to the Prophet Joseph Smith at a time when
                    he was shown in vision the celestial kingdom. The revelation is
                    recorded in section 137 of the Doctrine and
                    Covenants. Joseph marveled when he saw his deceased brother Alvin in
                    the celestial kingdom, for Alvin had died
                    before the gospel was restored. Joseph then received this great truth:</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>“All
                    who have died without a knowledge of this gospel, who would have
                    received it if they had been permitted
                    to tarry, shall be heirs of the celestial kingdom of God; … </font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>“For
                    I, the Lord, will judge all men according to their works, according to
                    the desire of their hearts.” (D&amp;C
                    137:7, 9.)</font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>If
                    our works and the desires of our hearts are the ultimate criteria of
                    our character, how do we measure up? What
                    kind of heart should we seek? For what kind of heart should we pray?
                    How should we measure the worth of other
                    people? (“The Measure of Our Hearts,” <i>Ensign</i>, Nov. 1988, p. 15)</font></p>
