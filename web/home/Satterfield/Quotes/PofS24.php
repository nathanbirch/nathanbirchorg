                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span></span><br>
                    In
                    all ages, from Adam to this hour, the holy prophets have taught the
                    true doctrine of the judgment. They have
                    always set forth those concepts and verities that would encourage men
                    to live in such a manner as to gain the glorious
                    reward of eternal life when their day and time came to stand before the
                    Eternal Bar. The hour of judgment is not the
                    same for every man. Some are judged at one time and others at a
                    different hour. <span style="text-decoration: underline;">There are,
                        in fact, many days of
                        judgment available</span>, but always the same Judge sits at the same
                    judgment bar, always the same laws govern the
                    procedures, and always a just and right judgment is imposed.<br>
                    <br>
                    </font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span></span><span style="text-decoration: underline;">Our
                        birth into mortality is a day
                        of judgment</span> in that it signalizes we were found worthy while in
                    the premortal
                    life to undergo a mortal probation and thus to continue on the course
                    leading to eternal life. There are those who press
                    forward along this course during this mortal probation – with a
                    steadfastness in Christ, having a perfect love of God
                    and of all men, keeping the commandments, and doing only those things
                    that please their Lord – until they are
                    translated and taken up into heaven, or until their calling and
                    election is made sure.<span style="text-decoration: underline;"> Either
                        of these glorious
                        eventualities is in itself a day of judgment</span>. Their celestial
                    inheritance is thus assured, though they have not yet gained
                    bodies of immortal glory. <span style="text-decoration: underline;">Death
                        also is a day of judgment</span> when the spirits of men go to either
                    paradise or hell as their
                    deeds warrant.</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span><br>
                    </span><span style="text-decoration: underline;">The second coming of
                        Christ is the
                        great day of judgment for all men</span>, both the living and the dead.
                    In it those
                    who qualify come forth in the resurrection of the just and obtain their
                    rewards in the kingdoms established for them.
                    At that time the decree goes forth that the rest of the dead shall
                    remain in their graves to await the resurrection of the
                    unjust and their consequent telestial inheritance. At that time the
                    wicked among men are consumed as stubble, their
                    bodies become dust again, and their spirits are consigned to an eternal
                    hell to await the day of the resurrection of
                    damnation. At that time those mortals who are worthy escape the
                    burning, abide the day, and remain on the new earth
                    with its new heavens in the presence of earth’s new King.</font></p>
