                <p
                    style="text-align: center; font-family: arial; font-style: italic; color: rgb(0, 0, 153);"><big><font
                        size="+2"><big><span style="font-weight: bold;">Will Be the Moment of
                            Truth</span></big></font></big></p>
                <font style="font-family: arial;" size="+2"><br>
                </font>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span
                        style="font-weight: bold;">Neal A. Maxwell </span></font></p>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span><br>
                    </span></font></p>
                <div style="margin-left: 40px; font-family: arial;"><font size="+2">The
                    judgment day is one of the things that really will be. The “future
                    shock” of that judgment and the events to
                    precede it will be without parallel. The dramatic day described so
                    powerfully by Alma will be a highly compressed
                    and collective moment of truth. This will be the day when every knee
                    shall bow and every tongue confess Jesus is
                    the Christ. (Philippians 2:10-11.) No mortals will be standing that
                    day. Those who were cruelly used by the adversary
                    will see that awful reality. Nephi said the unrepentant guilty would
                    “remember [their] awful guilt in perfectness, and
                    be constrained to exclaim: Holy, holy are thy judgments, O Lord God
                    Almighty – but I know my guilt; I transgressed
                    thy law, and my transgressions are mine; and the devil hath obtained
                    me, that I am a prey to his awful misery.” (2
                    Nephi 9:46.) Jesus, who purchased us and who owns us, will require this
                    owning up. They who transgressed divine
                    law will openly admit that their transgressions are their own and
                    cannot be laid at someone else’s door.</font></div>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span>&nbsp;<br>
                    </span>In
                    writing of this event that really will be, the apostle John described
                    how “the kings of the earth, and the great
                    men, and the rich men, and the chief captains, and the mighty men. . .
                    hid themselves in the dens and in the rocks of
                    the mountains; And said to the mountains and rocks, Fall on us, and
                    hide us from the face of him that sitteth on the
                    throne, and from the wrath of the Lamb.” (Revelation 6:15-16.) This is
                    high drama. This is a final confrontation with
                    the ultimate reality, the living God.</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span><br>
                    </span>On
                    that dramatic day there can be no saving swagger – no panache. On that
                    day there can be no grievance with
                    God that can be advanced at all. In that scene described by John and
                    Alma, those whose grievance with God and his
                    gospel was that his gospel was too plain and too simple will be simply
                    speechless.</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span><br>
                    </span>The
                    living scriptures will have been before us. Living prophets’ words will
                    have previously penetrated every
                    culture and every clime. Goethe said that “architecture is frozen
                    music,” and the scriptures are the preserved moral
                    music of the universe that God kept repeating for mankind over the
                    centuries.</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span><br>
                    </span>The
                    promised day of judgment will come, and all men will be left “without
                    excuse.” (D&amp;C 101:93; Romans
                    1:20.) All individuals will receive “according to their works,
                    according to the desire of their hearts.” (JS-V 1:9.) The
                    justice and mercy of God will have combined so that by then all
                    inhabitants of the earth will have heard the gospel
                    of Jesus Christ sufficiently to be fully accountable for it. Even those
                    who died without that opportunity will by then
                    have heard the gospel “that they might be judged according to men in
                    the flesh, but live according to God in the
                    spirit.” (JFS-V 1:34, 59.) What President Joseph F. Smith saw was just
                    what Peter saw much earlier when he wrote
                    of the preaching of the gospel “to them that are dead, that they might
                    be judged according to men in the flesh, but
                    live according to God in the spirit.” (1 Peter 4:6.)</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span><br>
                    </span>All
                    will have heard from living prophets (through preaching and through the
                    scriptures), so that personal
                    accountability will be just as complete as Nephi indicated: “And you
                    that will not partake of the goodness of God,
                    and respect the words of the Jews, and also my words, and the words
                    which shall proceed forth out of the mouth of
                    the Lamb of God, behold, I bid you an everlasting farewell, for these
                    words shall condemn you at the last day.” (2
                    Nephi 33:14.)</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span><br>
                    </span>Each
                    individual will have had full opportunity to forge his decision, to
                    give his real desires full expression. We
                    will receive what we really chose, and none can or will question the
                    justice or mercy of God. (<i>Things As They Really
                        Are</i>, pp.111-113)</font><br>
