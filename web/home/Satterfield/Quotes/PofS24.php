                    the law a person has received and
                    to judge on the basis of that person’s circumstances, motives, and
                    actions throughout his or her entire life (see Luke
                    12:47–48; John 15:22; 2 Ne. 9:25). [“ ‘Judge Not’ and Judging,”<i>
                        Ensign</i>, Aug. 1999, p. 8]</font></p>
