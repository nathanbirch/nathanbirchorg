                <div style="margin-left: 40px; font-family: arial;"><font size="+2">The
                    Apostle Paul taught that the Lord’s teachings and teachers were given
                    that we may all attain “the measure
                    of the stature of the fulness of Christ” (Eph. 4:13). This process
                    requires far more than acquiring knowledge. It is not
                    even enough for us to be <i>convinced</i> of the gospel; we must act
                    and think so that we are <i>converted</i> by it. In contrast
                    to the institutions of the world, which teach us to <i>know</i>
                    something, the gospel of Jesus Christ challenges us to <i>become</i>
                    something.</font></div>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span>&nbsp;<br>
                    </span>Many
                    Bible and modern scriptures speak of a final judgment at which all
                    persons will be rewarded according to
                    their deeds or works or the desires of their hearts. But other
                    scriptures enlarge upon this by referring to our being
                    judged by the <i>condition</i> we have achieved.</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span><br>
                    </span>The
                    prophet Nephi describes the Final Judgment in terms of what we <i>have
                        become</i>: “And if their works have been
                    filthiness they must needs <i>be</i> filthy; and if they <i>be</i><i>is</i>
                    filthy shall be filthy still; and he that <i>is </i>righteous
                    shall be righteous still” (Morm. 9:14; emphasis added; see also Rev.
                    22:11-12; 2 Ne. 9:16; D&amp;C 88:35). The same
                    would be true of “selfish” or “disobedient” or any other personal
                    attribute inconsistent with the requirements of God.
                    Referring to the “state” of the wicked in the Final Judgment, Alma
                    explains that if we are condemned by our words,
                    our works, and our thoughts, “we shall not be found spotless; … and in
                    this awful state we shall not dare to look up
                    to our God” (Alma 12:14). filthy it must needs be that they cannot
                    dwell in the kingdom of
                    God” (1 Ne. 15:33; emphasis added). Moroni declares, “He that </font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span><br>
                    </span>From
                    such teachings we conclude that the Final Judgment is not just an
                    evaluation of a sum total of good and
                    evil acts – what we have <i>done.</i> It is an acknowledgment of the
                    final effect of our acts and thoughts – what we have
                    <i>become.</i> It is not enough for anyone just to go through the
                    motions. The commandments, ordinances, and covenants
                    of the gospel are not a list of deposits required to be made in some
                    heavenly account. The gospel of Jesus Christ is
                    a plan that shows us how to become what our Heavenly Father desires us
                    to become. </font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span>&nbsp;<br>
                    </span>A parable illustrates
                    this understanding. A wealthy father knew that if he were to bestow his
                    wealth upon a child
                    who had not yet developed the needed wisdom and stature, the
                    inheritance would probably be wasted. The father said
                    to his child:</font></p>
