                        size="+2"><span>&nbsp;<br>
                    </span>The
                    gospel of Jesus Christ is the plan by which we can become what children
                    of God are supposed to become.
                    This spotless and perfected state will result from a steady succession
                    of covenants, ordinances, and actions, an
                    accumulation of right choices, and from continuing repentance. “This
                    life is the time for men to prepare to meet God”
                    (Alma 34:32).</font></p>
