                <p style="text-align: justify; font-family: arial;"><font size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>&nbsp;4
                    And if their works are evil they shall be restored unto them for evil.
