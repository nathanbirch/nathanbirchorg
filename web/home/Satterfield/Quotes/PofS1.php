                <p style="font-style: italic; color: rgb(0, 0, 153);" align="center"><big><big><font
                            face="Arial" size="+1"><big><big>The Terms <em>Free</em> Agency<em> </em>and<em>
                                    Moral Agency</em></big></big></font></big></big></p>
                <big><big><font face="Arial"><strong>Boyd K. Packer</strong></font></big></big>
                <p style="margin-left: 40px;"><big><big><font face="Arial">The phrase
                        "free agency" does not appear in scripture. The only agency spoken of
                        there is moral agency, "which," the Lord said, "I have given
                        unto him, that every man may be accountable for his own sins in the day
                        of judgment." (D&amp;C 101:78; italics added.) ["Our Moral
                        Environment,"
                        <em>Ensign</em>, May 1992, p. 67]</font></big></big><br wp="BR2">
                </p>
                <big><big></big></big>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Life is
                        meant to be a test to see if we will keep the commandments of God. (See
                        2 Ne. 2:5.) We are free to obey or to ignore the spirit and the
                        letter of the law. But the agency granted to man is a moral agency.
                        (See D&amp;C 101:78.) We are not free to break our covenants and escape
                        the
                        consequences. ("Covenants," <em>Ensign</em>, Nov. 1990, p. 84)</font></big></big></p>
                <big><big><br wp="BR2">
                    </big></big>
                <p><big><big><font face="Arial"><strong>Spencer J. Condie</strong></font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">I am
                        indebted to President Boyd K. Packer, who made us aware of the fact
                        that the term free agency appears nowhere in holy writ. Instead, the
                        scriptures generally speak of agency or free will, but when agency is
                        modified, it is referred to as "<em>moral agency</em>" (D&amp;C 101:78;
                        emphasis
                        added). Because the term <em>free agency</em> has been used by various
                        modern prophets, I use the terms <em>free agency</em> and <em>moral
                            agency</em>
                        interchangeably, aware that the latter term is more correct. ("Agency:
                        The Gift of Choices," <em>Ensign</em>, Sept. 1995, p. 18)<strong></strong></font></big></big></p>
