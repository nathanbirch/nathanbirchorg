                <p style="margin-left: 80px;"><big><big><font face="Arial">They may
                        mock and deride, as did a man named Korihor, with these words recorded
                        in the Book of Mormon: "And thus ye lead away this
                        people after the foolish traditions of your fathers, and according to
                        your own desires; and ye keep them down, even as it were in bondage,
                        that
                        ye may glut yourselves with the labors of their hands, that they durst
                        not look up with boldness, and that they durst not enjoy their rights
                        and
                        privileges" (Alma 30:27).</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">Korihor was
                        arguing, as men and women have falsely argued from the beginning of
                        time, that to take counsel from the servants of God is to
                        surrender God-given rights of independence. But the argument is false
                        because it misrepresents reality. When we reject the counsel which
                        comes from God, we do not choose to be independent of outside
                        influence. We choose another influence. We reject the protection of a
                        perfectly
                        loving, all-powerful, all-knowing Father in Heaven, whose whole
                        purpose, as that of His Beloved Son, is to give us eternal life, to
                        give us all that
                        He has, and to bring us home again in families to the arms of His love.
                        In rejecting His counsel, we choose the influence of another power,
                        whose purpose is to make us miserable and whose motive is hatred. We
                        have moral agency as a gift of God. Rather than the right to choose to
                        be free of influence, it is the inalienable right to submit ourselves
                        to whichever of those powers we choose. ("Finding Safety in Counsel," <em>Ensign</em>,
                        May 1997, p. 25)</font></big></big><br wp="BR1">
                </p>
