                        consider
                        immoral? Such an action would wipe out virtually all of the laws
                        against crimes. ("Weightier Matters," <em>Ensign</em>, Jan. 2001, pp.
                        13-15)</big></big><br wp="BR1">
                </p>
                <br>
                <p style="text-align: left; color: rgb(153, 0, 0);"><big><big><font
                            face="Arial" size="+1"><big><big><em>Our Use of Agency Fashions Our
                                    Future</em></big></big></font></big></big><br wp="BR2">
                </p>
                <big><big></big></big>
                <p style="margin-left: 40px;"><big><big><font face="Arial"><strong>Howard
                            W. Hunter</strong></font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">When the
                        children of Israel returned from Egypt and stood on the threshold of
                        the promised land, they faced the clear choice of what was before
                        them. Of the future that was about to be theirs, the Lord said to them:</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">"Behold, I
                        set before you this day a blessing and a curse;</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">"A blessing,
                        if ye obey the commandments of the Lord your God, which I command you
                        this day:</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">"And a
                        curse, if ye will not obey the commandments of the Lord your God."
                        (Deut. 11:26-28.)</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">That is the
                        choice the Lord puts before us as we face our own promised lands and
                        our own bright futures. We are given the knowledge, the
                        help, the enticement, and the freedom to choose the path of eternal
                        safety and salvation. The choice to do so is ours. By divine decree
                        before
                        this world was, the actual choice is and always has been our own.</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">Let us be
                        conscious of the fact that our future is being fashioned by the
                        decisions we make. ("The Golden Thread of Choice," <em>Ensign</em>,
                        Nov.
                        1989, p. 19)</font></big></big></p>
                <big><big><br wp="BR2">
                    </big></big>
                <p style="color: rgb(153, 0, 0);"><big><big><font face="Arial" size="+1"><big><big><em>Agency
                                    Necessary to Inspiration</em></big></big></font></big></big><br wp="BR2">
                </p>
                <big><big></big></big>
                <p style="margin-left: 40px;"><big><big><font face="Arial"><strong>Boyd
                            K. Packer</strong></font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">You have
                        your agency, and inspiration does not--perhaps cannot--flow unless you
                        ask for it, or someone asks for you. No message in scripture
                        is repeated more often than the invitation, even the command, to
                        pray--to ask. Prayer is so essential a part of revelation that without
                        it the veil
                        may remain closed to you. Learn to pray. Pray often. Pray in your mind,
                        in your heart. Pray on your knees. ("Personal Revelation: The Gift, the
                        Test, and the Promise," <em>Ensign</em>, Nov. 1994, p. 59)</font></big></big><br
                    wp="BR1">
                </p>
                <big><big><br wp="BR2">
                    </big></big>
                <p style="color: rgb(153, 0, 0);"><big><big><font face="Arial" size="+1"><big><big><em>Misuse
                                    of Agency Within the Church</em></big></big></font></big></big><br
                    wp="BR2">
                </p>
                <big><big></big></big>
                <div style="margin-left: 40px;"><big><big><font face="Arial"><strong>Ezra
                                Taft Benson</strong></font></big></big></div>
                <p><big><big><font face="Arial"> </font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">Because God
                        has given men their agency, there will always be those who will misuse
                        it. The gospel net draws in the good and the bad, the best
                        and the worst. The worst because the devil, before the final cleansing,
                        will put some of his followers within the kingdom in order to try and
                        destroy it. We have some of them within the kingdom today, and in due
                        course their number shall be known. Time has a way of taking care of
                        all
                        things, of elevating the good and bringing down the bad. If we see
                        things going on within the kingdom that disturb us, we might first
                        resolve, if
                        the matter falls within our stewardship, to go to the person or people
                        involved. If it is of such a nature that we think it should be called
                        to the
                        attention of higher authority, then we can, in a kindly and quiet
                        manner, take the necessary steps at the proper level. (<em>Teachings of
                            Ezra Taft
                            Benson</em>, p.82)<br>
                        <br>
                        </font></big></big></p>
                <big><big><br wp="BR1">
                    </big></big><big style="color: rgb(153, 0, 0);"><big><font face="Arial"
                                                                           size="+1"><big><big><em>We
                                    Have Agency to Believe But Not Teach What We Want</em></big></big></font></big></big><span
                    style="color: rgb(153, 0, 0);">
                </span><big><big><br wp="BR2">
                    </big></big>
                <p style="margin-left: 40px;"><big><big><font face="Arial"><strong>Boyd
                            K. Packer</strong></font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">It is not
                        the belief in a false notion that is the problem, it is the teaching of
                        it to others. In the Church we have the agency to believe whatever we
                        want to believe about whatever we want to believe. But we are not
                        authorized to teach it to others as truth. ("From Such Turn Away," <em>Ensign</em>,
                        May 1985, p. 35) <br>
                        </font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial"><br wp="BR1">
                        </font></big></big></p>
                <big style="color: rgb(153, 0, 0);"><big><font face="Arial" size="+1"><big><big><em>Things
                                    That Aid The Proper Use of Agency</em></big></big></font></big></big><span
                    style="color: rgb(153, 0, 0);">
                </span><big><big><br wp="BR2">
                    </big></big>
                <p style="margin-left: 40px;"><big><big><font face="Arial"><strong>Spencer
                            J. Condie</strong></font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">Our loving
                        Father in Heaven has given us many indispensable resources and means to
                        guide us in the wise exercise of our agency.</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial"><strong>The
                            Gift of the Holy Ghost</strong>. In D&amp;C 33:16, the Lord revealed
                        that "the power of [his] Spirit quickeneth all things." I believe that
                        "all things"
                        means all things. He will not usurp or override our moral agency, but
                        when given an invitation, his Spirit will augment and accelerate our
                        agency.
                        When the Spirit, the gift of the Holy Ghost, is given a chance to
                        influence us, decisions become easier and despair dissipates as
                        solutions to our
                        challenges become clearly evident.</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">The Book of
                        Mormon prophets make it very clear that the Holy Ghost is willing to
                        exert a very powerful influence in our lives when we are
                        responsive to his promptings. Nephi, Mormon, and Ether explained that
                        the Spirit strives with us to guide our lives on righteous paths (see 2
                        Ne.
                        26:11; Morm. 5:16; Ether 2:15). Moroni proclaimed that the Spirit
                        persuades us to do good (see Ether 4:11-12). Amulek taught that the
                        Holy
                        Ghost contends with us to do that which is right (see Alma 34:38), and
                        King Benjamin explained that the Holy Ghost entices us to be righteous
                        (see Mosiah 3:19).</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">The
                        promptings of the Spirit were never intended to supplant our moral
                        agency, but the Spirit will underscore preferable options in our
                        behavior
                        and clarify a certain course of action in our hearts and minds.</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial"><strong>The
                            Savior's Example</strong>. Jesus Christ set the perfect example for us.
                        Without boasting, he acknowledged that "the Father hath not left me
                        alone;
                        for I do always those things that please him" (John 8:29). He has
                        extended to each of us the invitation to "learn of me, and listen to my
                        words;
                        walk in the meekness of my Spirit, and you shall have peace in me"
                        (D&amp;C 19:23).</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial"><strong>Scripture</strong>.
                        Another indispensable resource that assists us in using our agency
                        wisely is holy scripture. The Apostle Paul explained, "All scripture
                        is given by inspiration of God, and is profitable for doctrine, for
                        reproof, for correction, for instruction in righteousness" (2 Tim.
                        3:16). Nephi gave
                        the additional prophetic promise that when we "feast upon the words of
                        Christ, &#8230; the words of Christ will tell [us] all things what [we]
                        should do"
                        (2 Ne. 32:3). In short, the scriptures are our life script, our
                        instruction manual in mortality, if you will. But of course, if the
                        universal teachings in
                        holy writ are to benefit us, we must follow Nephi's additional counsel
                        to "liken all scriptures unto us, that it might be for our profit and
                        learning" (1
                        Ne. 19:23).</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial"><strong>Testimony</strong>.
                        We learn in the book of Revelation that during the war in heaven, those
                        who overcame Satan and his followers did so "by the blood
                        of the Lamb, and by the word of their testimony" (see Rev. 12:7-11). A
                        testimony was an invaluable weapon in the war in heaven, and it is an
                        indispensable weapon here on earth.</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">A testimony
                        that is continually being nourished and is continually growing will
                        help us at every crossroad when important decisions are made.
                        Indeed, a testimony supplants the need to make certain decisions under
                        fire, because we already know well in advance the course of action we
                        will take.</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial"><strong>Power
                            of Prayer</strong>. The Apostle James eloquently observed that "the
                        effectual fervent prayer of a righteous man availeth much" (James
                        5:16).
                        Alma the Elder would certainly be an ardent advocate of this statement
                        by James, for when the angel appeared to Alma's wayward son, he
                        explained to Alma the Younger that "the Lord hath heard the prayers of
                        his people, and also the prayers of his servant, Alma, who is thy
                        father;
                        for he has prayed with much faith concerning thee" (Mosiah 27:14).</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">All prayers
                        are, indeed, answered, and it is well to remember that sometimes the
                        answer is not in the affirmative. As the Son of God prayed in
                        the Garden of Gethsemane that the bitter cup might pass, an affirmative
                        reply would have thwarted the entire plan of salvation. But the Only
                        Begotten Son demonstrated his meekness and humility and obedience as he
                        added: "Nevertheless not my will, but thine, be done" (Luke
                        22:42). The divine answer was no, but "there appeared an angel unto him
                        from heaven, strengthening him" (Luke 22:43). This is certainly a
                        prototype and a promise for each of us as we too are required to drink
                        from bitter cups in our lives. We will not be left comfortless.</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial"><strong>Fasting</strong>.
                        There are great blessings promised to those who fast. Through the
                        prophet Isaiah, the Lord posed an important question, followed by
                        profound promises: "Is not this the fast that I have chosen? to loose
                        the bands of wickedness, to undo the heavy burdens, and to let the
                        oppressed go free, and that ye break every yoke?" (Isa. 58:6.)</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">When we
                        subordinate our physical needs and desires to the dictates of the
                        Spirit, we tap into a spiritual strength beyond our own. If we are in
                        bondage to bad habits or unkind thoughts, we can break the bands of
                        weakness or wickedness through fasting. Our hearts will be filled with
                        love
                        and forgiveness, and we can get on with our lives after having broken
                        "every yoke."</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial"><strong>Ordinances</strong>.
                        Ordinances are outward manifestations of inner covenants, commitments,
                        and promises. Ordinances are not optional on the
                        pathway to perfection. These include baptism and confirmation (see John
                        3:5; 2 Ne. 31:5-12); ordination to the Melchizedek Priesthood, for all
                        males (see D&amp;C 84:33-42); the temple endowment and the sealing
                        ordinance (see D&amp;C 132:15-24).</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">Participation
                        in ordinances helps us use our agency wisely and well, and a constant
                        commitment to covenants spares us the emotional energy
                        required to decide and re-decide what we are going to do each time we
                        face temptation. Each week, through the ordinance of the sacrament,
                        we solemnly covenant to "always remember him" (Moro. 4:3; Moro. 5:2;
                        emphasis added).</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial"><strong>Living
                            Prophets</strong>. A loving Heavenly Father has provided us with living
                        prophets to receive and to help us understand his mind and will. In
                        speaking of his servants, the Savior said: "And whatsoever they shall
                        speak when moved upon by the Holy Ghost shall be scripture, shall be
                        the
                        will of the Lord, shall be the mind of the Lord, shall be the word of
                        the Lord, shall be the voice of the Lord, and the power of God unto
                        salvation"
                        (D&amp;C 68:4). Elsewhere he declared that "whether by mine own voice
                        or by the voice of my servants, it is the same" (D&amp;C 1:38).</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">We sustain
                        the fifteen men serving today in the First Presidency and in the Quorum
                        of the Twelve Apostles as prophets, seers, and revelators.
                        They are watchmen on the tower who point out the course of action we
                        should take and who see beyond the bend in the road and beyond the
                        horizon.</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial"><strong>Patriarchal
                            Blessings</strong>. President Ezra Taft Benson encouraged every youth
                        to receive a patriarchal blessing and admonished, "Study it
                        carefully and regard it as personal scripture for you--for that is what
                        it is. A patriarchal blessing is the inspired and prophetic statement
                        of your
                        life's mission, together with blessings, cautions, and admonitions as
                        the patriarch may be prompted to give" (<em>Ensign</em>, May 1986, p.
                        43).
                        ["Agency: The Gift of Choices," <em>Ensign</em>, Sept. 1995, pp. 21-22]</font></big></big><br
                    wp="BR1">
                </p>
                <big><big><br wp="BR2">
                    </big></big>
                <p><big style="color: rgb(153, 0, 0);"><big><font face="Arial" size="+1"><big><big><em>Enemies
                                    of Agency</em></big></big></font></big></big><br wp="BR2">
                </p>
                <big><big></big></big>
                <p style="margin-left: 40px;"><big><big><font face="Arial"><strong>Spencer
                            J. Condie</strong></font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">Satan would
                        have us waste our time in activities that impede our progress on the
                        pathway to perfection. Following are some of the many
                        enemies of agency.</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial"><strong>Addiction.</strong>
                        Many people lead empty lives completely devoid of purpose, meaning, and
                        direction. Empty lives must be filled with something,
                        anything, so some people fill their empty lives with endless hours of
                        television, while others become addicted to pornography, alcohol,
                        tobacco,
                        and other drugs. Still others develop an unhealthy capacity for
                        overeating. And ever so surely these individuals trade their moral
                        agency for their
                        addiction until they are no longer able to exercise their agency. All
                        of their decisions are now on automatic pilot, with seemingly little
                        hope of
                        changing the direction of their lives. There is little advantage to
                        living in a free country if we are in bondage to personal habits.</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial"><strong>Debt</strong>.
                        The accumulation of financial debt is another dangerous incursion upon
                        our moral agency. A poignant description of the enslaving power
                        of debt was provided by the late President J. Reuben Clark: "Interest
                        never sleeps nor sickens nor dies; it never goes to the hospital; it
                        works on
                        Sundays and holidays; it never takes a vacation &#8230; it has no love, no
                        sympathy; it is as hard and soulless as a granite cliff. Once in debt,
                        interest
                        is your companion every minute of the day and night; you cannot shun it
                        or slip away from it; you cannot dismiss it; it yields neither to
                        entreaties,
                        demands, or orders; and whenever you get in its way or cross its course
                        or fail to meet its demands, it crushes you" (in Conference Report, 6
                        April 1938, p. 103).</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">There are,
                        of course, justifiable occasions when one incurs debt, such as for the
                        purchase of a house or a major business investment. But even
                        then, great wisdom should be used.</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial"><strong>Discouragement</strong>.
                        Discouragement and its fellow travelers of depression, despair, and
                        hopelessness are much like the proverbial rocking chair:
                        they keep us busily occupied, but they do not take us anywhere.</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">I have found
                        through personal experience that whenever I am discouraged and start
                        thinking only of myself and how hard hit I have been, when
                        I kneel down and count my blessings, all of a sudden my personal
                        problems do not seem large at all.</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">President
                        Spencer W. Kimball provided us with excellent counsel in overcoming
                        discouragement and finding meaning to our lives: "When we
                        concern ourselves more with others, there is less time to be concerned
                        with ourselves. In the midst of the miracle of serving, there is the
                        promise of Jesus, that by losing ourselves, we find ourselves (see
                        Matt. 10:39). &#8230; The more we serve our fellowmen in appropriate ways,
                        the
                        more substance there is to our souls. &#8230; Indeed, it is easier to 'find'
                        ourselves because there is so much more of us to find!" (<em>Ensign</em>,
                        Dec. 1974,
                        p. 2.)</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial"><strong>Cultural
                            Traditions</strong>. A recurrent theme throughout the Book of Mormon is
                        the constraining influence of the false "tradition of their fathers"
                        passed down from Laman and Lemuel through subsequent generations (see
                        Mosiah 10:11-12; Alma 37:9; Alma 60:32; Hel. 5:51; Hel. 15:4;
                        Hel. 16:18-20). Tradition can be a double-edged sword. When based upon
                        the perpetuation of righteous principles, tradition can become a
                        marvelous support system in helping us employ our moral agency wisely.
                        On the other hand, many traditions find their origins in the false
                        pride
                        and foibles of mankind. In modern-day revelation, the Lord has taught:
                        "Every spirit of man was innocent in the beginning. &#8230; [But] that wicked
                        one cometh and taketh away light and truth, through disobedience, from
                        the children of men, and because of the tradition of their fathers"
                        (D&amp;C
                        93:38-39; emphasis added).</font></big></big></p>
                <p style="margin-left: 80px;"><big><big><font face="Arial">Cultural
                        customs and traditions often provide a useful map for the members of a
                        given society, but if we are to become members of a celestial
                        culture, we must overcome the natural man reflected in earthly cultures
                        (see Mosiah 3:19; D&amp;C 88:22). Indeed, some cultural
                        enticements--such
                        as following the crowd in matters of fashion and acceptance of worldly
                        standards--are spiritually, and sometimes even physically, destructive.
                        John the Revelator admonished us to "love not the world, neither the
                        things that are in the world. If any man love the world, the love of
                        the
                        Father is not in him" (1 Jn. 2:15; emphasis added; see James 4:4).
                        ["Agency: The Gift of Choices," <em>Ensign</em>, Sept. 1995, pp. 20-21]<br>
                        </font></big></big></p>
