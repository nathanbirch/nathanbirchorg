                <p style="margin-left: 80px; font-family: arial;"><big><big>Few
                        concepts have more potential to mislead us than the idea that choice,
                        or agency, is an ultimate goal. For Latter-day Saints, this potential
                        confusion is partly
                        a product of the fact that moral agency--the right to choose--is a
                        fundamental condition of mortal life. Without this precious gift of
                        God, the purpose of mortal
                        life could not be realized. To secure our agency in mortality we fought
                        a mighty contest the book of Revelation calls a "war in heaven." This
                        premortal contest
                        ended with the devil and his angels being cast out of heaven and being
                        denied the opportunity of having a body in mortal life (see Rev.
                        12:7-9).</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>But our war
                        to secure agency was won. The test in this postwar mortal estate is not
                        to secure choice but to use it--to choose good instead of evil so that
                        we can
                        achieve our eternal goals. In mortality, choice is a method, not a goal.</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>Of course,
                        mortals must still resolve many questions concerning what restrictions
                        or consequences should be placed upon choices. But those questions come
                        under the heading of freedom, not agency. Many do not understand that
                        important fact. We are responsible to use our agency in a world of
                        choices. It will not
                        do to pretend that our agency has been taken away when we are not free
                        to exercise it without unwelcome consequences.</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>Because
                        choice is a method, choices can be exercised either way on any <a
                            name="LPHit23"></a>matter, and our choices can serve any goal.
                        Therefore, those who consider freedom of
                        choice as a goal can easily slip into the position of trying to justify
                        any choice that is made. "Choice" can even become a slogan to justify
                        one particular choice.
                        For example, today one who says "I am pro-choice" is clearly understood
                        as opposing any legal restrictions upon a woman''s choice to abort a
                        fetus.</big></big></p>
