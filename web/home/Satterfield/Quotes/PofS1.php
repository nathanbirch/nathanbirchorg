                        been fascinated with
                        how cleverly those who sought and now defend legalized abortion on
                        demand have moved the issue away from a debate on the moral, ethical,
                        and medical pros
                        and cons of legal restrictions on abortion and focused the debate on
                        the slogan or issue of choice. The slogan or sound bite "pro-choice"
                        has had an almost
                        magical effect in justifying abortion and in neutralizing opposition to
                        it.</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>Pro-choice
