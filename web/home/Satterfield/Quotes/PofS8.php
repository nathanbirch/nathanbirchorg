                <p style="font-family: arial;"><big style="color: rgb(153, 0, 0);"><big><font
                            size="+1"><big><big><em>The Importance of the Veil</em></big></big></font></big></big><br
                    style="font-family: arial;" wp="BR2">
                </p>
                <big><big></big></big>
                <p style="font-family: arial; margin-left: 40px;"><big><big><strong>Neal
                            A. Maxwell</strong></big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>We define
                        the veil as the border between mortality and eternity; it is also a
                        film of forgetting which covers the memories of earlier experiences.
                        This
                        forgetfulness will be lifted one day, and on that day we will see
                        forever--rather than "through a glass darkly" (1 Cor. 13:12).</big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>There are
                        poignant and frequent reminders of the veil, adding to our sense of
                        being close but still outside. In our deepest prayers, when the agency
                        of man
                        encounters the omniscience of God, we sometimes sense, if only
                        momentarily, how very provincial our petitions really are; we perceive
                        that there are more
                        good answers than we have good questions; and we realize that we have
                        been taught more than we can tell, for the language used is not that
                        which tongue can
                        transmit.</big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>We
                        experience this same close separateness when a baby is born, but also
                        as we wait with those who are dying--for then we brush against the
                        veil, as goodbyes
                        and greetings are said--almost within earshot of each other! In such
                        moments, this resonance with realities on the other side of the veil is
                        so real it can he
                        explained in only one way!</big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>No wonder
                        the Savior said that his doctrines would be recognized by his sheep,
                        that we would know his voice, that we would follow him (see John
                        10:14). We
                        do not, therefore, follow strangers. Deep within us, his doctrines do
                        strike the promised chord of familiarity and underscore our true
                        identity. Our sense of
                        belonging grows in spite of our sense of separateness, for his
                        teachings stir our souls awakening feelings within us which have
                        somehow survived underneath
                        the encrusting experiences of mortality.</big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>This inner
                        serenity which the believer knows as he brushes against the veil is
                        cousin to certitude. The peace it brings surpasses our understanding
                        and certainly
                        our capacity to explain. But it requires a patience which stands in
                        stark contrast to the restlessness of the world in which, said Isaiah,
                        the wicked are like the
                        pounding and troubled sea which cannot rest (see Isa. 57:20).</big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>But
                        mercifully the veil is there! It is fixed by the wisdom of God for our
                        good. It is no use our being impatient with the Lord over that reality,
                        for it is clearly a
                        condition to which we agreed so long ago. Even when the veil is parted
                        briefly, it will be on His terms--not ours.</big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>Without the
                        veil, we would lose that precious insulation, thus interfering with our
                        mortal probation and maturation. Without the veil, our brief mortal
                        walk in a
                        darkening world would lose its meaning--for one would scarcely carry
                        the flashlight of faith at noonday and in the presence of the Light of
                        the World!</big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>Without the
                        veil, we could not experience the gospel of work and the sweat of our
                        brow. If we had the security of having already entered into God's rest,
                        certain
                        things would be unneeded; Adam and Eve did not clutch Social Security
                        cards in the Garden of Eden!</big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>And how
                        could we learn about obedience if we were shielded from the
                        consequences of our disobedience? And how could we learn patience under
                        pressure if
                        we did not experience pressure and waiting?</big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>Nor could
                        we choose for ourselves if we were already in His holy presence, for
                        some alternatives do not there exist. Besides, God's court is filled
                        with those
                        who have patiently overcome--whose company we do not yet deserve.</big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>Fortunately,
                        the veil keeps the first, second, and third estates separate, hence our
                        sense of separateness. The veil avoids having things "compound in one"
                        (2 Ne.
                        2:11)--to our everlasting detriment. We are cocooned, as it were, in
                        order that we might truly choose. Once, long ago, we chose to come to
                        this very setting
                        where we could choose. It was an irrevocable choice! And the veil is
                        the guarantor that our ancient choice is honored.</big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>When the
                        veil which now encloses us is no more, time will also be no more (see
                        D&amp;C 84:100). Even now, time is clearly not our natural dimension.
                        Thus it is
                        that we are never really at home in time. Alternately, we find
                        ourselves impatiently wishing to hasten the passage of time or to hold
                        back the dawn. We can do
                        neither, of course. Whereas the bird is at home in the air, we are
                        clearly not at home in time--because we belong to eternity! Time, as
                        much as any one thing,
                        whispers to us that we are strangers here. If time were natural to us,
                        why is it that we have so many clocks and wear wristwatches?</big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>Thus the
                        veil stands--not to forever shut us out--but as a reminder of God's
                        tutoring and patient love for us. Any brush against it produces a
                        feeling of "not
                        yet," but also faint whispers of anticipation of that moment when, in
                        the words of today's choral hymn, "Come, Let Us Anew," those who have
                        prevailed "by
                        the patience of hope and the labor of love" will hear the glorious
                        words, "Well and faithfully done; enter into my joy and sit down on my
                        throne" (Hymns, no.
                        17). [From "Patience," <em>Ensign</em>, Oct. 1980, p. 31]</big></big><br
                    style="font-family: arial;" wp="BR1">
                </p>
