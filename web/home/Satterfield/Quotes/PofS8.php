                        darkness, or forgetfulness, which deprives us of the recollection of
                        our existence in the spirit world before this earth was made and of the
                        acquaintances we had here, will be lifted. Then we will see as we are
                        seen and know as we are known and as we were known before earth life.
                        (A Marvelous
                        Work and a Wonder, pp.281-282)<br>
