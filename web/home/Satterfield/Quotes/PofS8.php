                        born into this
                        world, we only have a vague recollection of our preexistent life. By
                        the inspiration of the Spirit "we see through a glass darkly" and
                        we "know in part." Ultimately our previous knowledge will be restored
                        to us, when that which is perfect comes, and then we shall know even as
                        also we are
                        known. Here we have the reason why the world did not recognize Jesus
                        when he came in the flesh: "He was in the world, and the world was made
                        by him, and
                        the world knew him not." (John 1:10.)</big></big></p>
