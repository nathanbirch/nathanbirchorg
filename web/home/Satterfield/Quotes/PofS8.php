                <p style="font-family: arial; margin-left: 80px;"><big><big>In some
                        ways, our second estate, in relationship to our first estate, is like
                        agreeing in advance to surgery. Then the anesthetic of forgetfulness
                        settles in upon us.
                        Just as doctors do not de-anesthetize a patient in the midst of
                        authorized surgery to ask him again if the surgery should be continued,
                        so, after divine tutoring,
                        we agreed to come here and to submit ourselves to certain experiences;
                        it was an irrevocable decision. ("A More Determined Discipleship," <em>Ensign</em>,
