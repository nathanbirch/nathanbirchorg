                <p style="margin-left: 80px; font-family: arial;"><big><big>The close
                        relationship between partaking of the sacrament and the companionship
                        of the Holy Ghost is explained in the revealed prayer on the sacrament.
                        In partaking of the bread, we witness that we are willing to take upon
                        us the name of Jesus Christ and always remember Him and keep His
                        commandments. When we do so, we have the promise that we will always
                        have His Spirit to be with us (see D&amp;C 20:77).</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>To have the
                        continuous companionship of the Holy Ghost is the most precious
                        possession we can have in mortality. The gift of the Holy Ghost was
                        conferred upon us by the authority of the Melchizedek Priesthood after
                        our baptism. But to realize the blessings of that gift, we must keep
                        ourselves free from sin. When we commit sin, we become unclean and the
                        Spirit of the Lord withdraws from us. The Spirit of the Lord does not
                        dwell in "unholy temples" (see Mosiah 2:36-37; Alma 34:35-36; Hel.
                        4:24), and no unclean thing can dwell in His presence (see Eph. 5:5; 1
                        Ne. 10:21; Alma 7:21; Moses 6:57).</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>A few weeks
                        ago I used a chain saw to cut down a tree in my backyard. It was a
                        dirty job, and when I was done I was splattered with a filthy mixture
                        of sawdust and oil. In that condition I did not want anyone to see me.
                        I just wanted to be cleansed in water so I would again feel comfortable
                        in the presence of other people.</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>Not one of
                        you young men and not one of your leaders has lived without sin since
                        his baptism. Without some provision for further cleansing after our
                        baptism, each of us is lost to things spiritual. We cannot have the
                        companionship of the Holy Ghost, and at the final judgment we would be
                        bound to be "cast off forever" (1 Ne. 10:21). How grateful we are that
                        the Lord has provided a process for each baptized member of His Church
                        to be periodically cleansed from the soil of sin. The sacrament is an
                        essential part of that process.</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>We are
                        commanded to repent of our sins and to come to the Lord with a
                        brokenheart and a contrite spirit and partake of the sacrament in
