                <p style="font-family: arial; margin-left: 40px;"><big><big>We believe
                        in the gift of the Holy Ghost being enjoyed now, as much as it was in
                        the Apostles' days; we believe that it [the gift of the Holy Ghost] is
                        necessary to make and to organize the Priesthood, that no man can be
                        called to fill any office in the ministry without it; we also believe
                        in prophecy, in tongues, in visions, and in revelations, in gifts, and
                        in healings; and that these things cannot be enjoyed without the gift
                        of the Holy Ghost. ...</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>We believe
                        that the Holy Ghost is imparted by the laying on of hands of those in
                        authority, and that the gift of tongues, and also the gift of prophecy
                        are gifts of the Spirit, and are obtained through that medium; ...</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>The Church
                        is a compact body composed of different members, and is strictly
                        analogous to the human system, and Paul, after speaking of the
                        different gifts, says, "Now ye are the body of Christ and members in
                        particular; and God hath set some in the Church, first Apostles,
                        secondarily Prophets, thirdly Teachers, after that miracles, then gifts
                        of healing, helps, governments, diversities of tongues. Are all
                        Teachers? Are all workers of miracles? Do all speak with tongues? Do
                        all interpret?" It is evident that they do not; yet are they all
                        members of one body. All members of the natural body are not the eye,
                        the ear, the head or the hand -- yet the eye cannot say to the ear, I
                        have no need of thee, nor the head to the foot, I have no need of thee;
                        they are all so many component parts in the perfect machines -- the one
                        body; and if one member suffer, the whole of the members suffer with
                        it; and if one member rejoice, all the rest are honored with it.</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>These,
                        then, are all gifts; they come from God; they are of God; they are all
                        the gifts of the Holy Ghost; they are what Christ ascended into heaven
                        to impart; and yet how few of them could be known by the generality of
                        men. ...</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>The gifts
                        of God are all useful in their place, but when they are applied to that
                        which God does not intend, they prove an injury, a snare and a curse
                        instead of a blessing. We may some future time enter more fully into
                        this subject, but shall let this suffice for the present. <small>(<em>Teachings
                                of the Prophet Joseph Smith</em> [1976], pp.243-247)</small></big></big><br>
