                <big><font
                        style="font-weight: bold; color: rgb(102, 0, 0); font-family: arial;"
                        size="+1"><big><em>Preparatory Gifts of the Spirit</em></big></font><br>
                    <big><strong><br>
                        </strong></big></big>
                <div style="margin-left: 40px;"><big><big><strong><span
                                    style="font-family: arial;">Dallin H. Oaks <small
                                        style="color: rgb(153, 153, 153);">Quorum of the Twelve Apostles</small></span>
                            </strong></big></big></div>
                <p style="margin-left: 80px; font-family: arial;"><big><big>We need to
                        distinguish between a manifestation of the Holy Ghost and the gift of
                        the Holy Ghost.</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>As men and
                        women desire to believe, they develop faith in God. (See Alma
                        32:26-43.) When they have enough faith, they can receive a
                        manifestation of the Holy Ghost. In unusual circumstances, to serve the
                        purposes of God, such a manifestation might even include seeing
                        heavenly beings. ...</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>In summary,
                        the Spirit of Christ is given to all men and women that they may know
                        good from evil, and manifestations of the Holy Ghost are given to lead
                        earnest seekers to repentance and baptism. These are preparatory gifts.
                        What we term spiritual gifts come next.<small> ["Spiritual Gifts," <em>Ensign</em>,
                            Sept. 1986, pp. 9]</small><br>
                        <br style="font-family: arial;" wp="BR2">
                    </big></big></p>
                <p
                    style="font-weight: bold; color: rgb(102, 0, 0); font-family: arial; text-align: left;"><big><font
                        size="+1"><big><em>Spiritual Gifts Come to Those Who Have Received the
                            Gift of the Holy Ghost</em></big></font></big><br
                    style="font-family: arial;" wp="BR2">
                </p>
                <p style="margin-left: 40px; font-family: arial;"><big><big><strong>Dallin
                            H. Oaks </strong></big></big><big><big><strong><span
                                style="font-family: arial;"> <small style="color: rgb(153, 153, 153);">Quorum
                                    of the Twelve Apostles</small></span></strong></big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>Spiritual
                        gifts come to those who have received the gift of the Holy Ghost. As
                        the Prophet Joseph Smith taught, the gifts of the Spirit "are obtained
                        through that medium" [the Holy Ghost] and "cannot be enjoyed without
                        the gift of the Holy Ghost. &#8230; The world in general can know nothing
                        about them." (<em>Teachings of the Prophet Joseph Smith</em>, comp.
                        Joseph Fielding Smith, Salt Lake City: Deseret Book Co., 1938, pp. 243,
                        245; see lso Elder Marion G. Romney in Conference Report, April 1956,
                        p. 72.)</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>The gift of
                        the Holy Ghost is conferred on both men and women. So are spiritual
                        gifts. As Elder Bruce R. McConkie declared in Nauvoo at the dedication
                        of the Monument to Women: "Where spiritual things are concerned, as
                        pertaining to all of the gifts of the Spirit, with reference to the
                        receipt of revelation, the gaining of testimonies, and the seeing of
                        visions, in all matters that pertain to godliness and holiness and
                        which are brought to pass as a result of personal righteousness in all
                        these things men and women stand in a position of absolute equality
                        before the Lord. He is no respecter of persons nor of sexes, and he
                        blesses those men and those women who seek him and serve him and keep
                        his commandments." (<em>Ensign</em>, Jan. 1979, p. 61.)</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>Spiritual
                        gifts do not come visibly, automatically, and immediately to all who
                        have received the gift of the Holy Ghost. The Prophet Joseph Smith
                        taught that most such gifts are "not visible to the natural vision, or
                        understanding of man," and that it "require[s] time and circumstances
                        to call these gifts into operation." (<em>Teachings</em>, pp. 244, 246.)</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>The
                        scriptures tell us that we should desire and zealously seek spiritual
                        gifts. (See D&amp;C 46:8; 1 Cor. 12:31; 1 Cor. 14:1, 11.) We are also
                        told that some will receive one gift and some will receive another.
                        (See D&amp;C 46:11; 1 Cor. 12; Moro. 10:8-18.) In every case, the
                        receipt of spiritual gifts is predicated upon faith, obedience, and
                        personal righteousness. ...</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>When we
                        believe and seek spiritual gifts to benefit others "and not for a sign"
                        (D&amp;C 46:9), we are told that signs will follow. "Behold, &#8230; signs
                        follow those that believe. Yea, signs come by faith, not by the will of
                        men, nor as they please, but by the will of God. Yea, signs come by
                        faith, unto mighty works." (D&amp;C 63:9-11.) The Holy Ghost "maketh
                        manifest unto the children of men, according to their faith." (Jarom
                        1:4.) <small>[From "Spiritual Gifts," <em>Ensign</em>, Sept. 1986,
                            pp. 9]</small></big></big></p>
                <big><big><br>
                        <br>
                        <span style="font-family: arial;"><span
                                style="font-weight: bold; font-style: italic; color: rgb(153, 0, 0);">George
                                Q. Cannon</span> <small style="color: rgb(153, 153, 153);">(1827-1901)
                                First Presidency</small></span><br style="font-family: arial;" wp="BR1">
                    </big></big>
                <ul>
                    <li>
                        <p style="font-family: arial;"><big><big>The Prophet Moroni
                            explains more fully even than Paul does concerning these gifts. He said
                            "that if the day cometh that the power and gifts of God shall be done
                            away among you, it shall be because of unbelief."3 Not because God is
                            not willing to bestow gifts upon His children, for as this Prophet
                            says, "God is not a partial God, neither a changeable Being; but he is
                            unchangeable from all eternity to all eternity." His dealings with the
                            children of men are alike. If they come to Him with faith and obey His
                            Gospel, He is bound by the promises that He has made to bestow upon
                            them the same blessings and not to discriminate between His children;
                            for if He did so, He would not be a just Being. But justice is His
                            attribute, and He deals justly with His children. He gives unto them
                            His gifts and blessings according to their faith. To one He gives one
                            gift, to another He gives another gift. </big></big></p>
                    <p><big><span style="font-family: arial;"><big>These gifts are
                                given by Him for the perfection of His people, that in this vale of
                                tears, shut out as we are from His presence, a veil of darkness having
                                been drawn, as it were, between us and Him, those who will exercise
                                faith in His promises and will keep His commandments may receive the
                                aid that is necessary to enable them to walk before Him and to enjoy
                                His power&#8230;.</big> (<span style="font-style: italic;">Gospel Truths</span>,
                            2 Vols., [1957], 1:194) </span></big><br>
                    </p>
                    </li>
                </ul>
                <ul>
                    <li>
                        <p style="font-family: arial;"><big><big>How many of you are
                            seeking for these gifts that God has promised to bestow? How many of
                            you, when you bow before your Heavenly Father in your family circle or
                            in your secret places, contend for these gifts to be bestowed upon you?
                            How many of you ask the Father in the name of Jesus to manifest Himself
                            to you through these powers and these gifts? Or do you go along day by
                            day like a door turning on its hinges, without having any feeling upon
                            the subject, without exercising any faith whatever, content to be
                            baptized and be members of the Church and to rest there, thinking that
                            your salvation is secure because you have done this? </big></big></p>
                    <p style="font-family: arial;"><big><big>I say to you, in the name
                            of the Lord, as one of His servants, that you have need to repent of
                            this. You have need to repent of your hardness of heart, of your
                            indifference and of your carelessness. There is not that diligence,
                            there is not that faith, there is not that seeking for the power of God
                            that there should be among a people who have received the precious
                            promises we have&#8230;. </big></big></p>
                    <p style="font-family: arial;"><big><big>If any of us are
                            imperfect, it is our duty to pray for the gift that will make us
                            perfect. Have I imperfections? I am full of them. What is my duty? To
                            pray to God to give me the gifts that will correct these
                            imperfections&#8230;. They are intended for this purpose. No man ought to
                            say, "Oh, I cannot help this; it is my nature." He is not justified in
                            it, for the reason that God has promised to give strength to correct
                            these things and to give gifts that will eradicate them&#8230;. That is the
                            design of God concerning His children. He wants His Saints to be
                            perfected in the truth. </big></big></p>
                    <p style="font-family: arial;"><big><big>The Lord has said in a
                            revelation to the Church that the Saints should "seek ye earnestly the
                            best gifts, always remembering for what they are given; for verily I
                            say unto you, they are given for the benefit of those who love me and
                            keep all my commandments." How many Latter-day Saints are there who
                            supplicate the Lord for the gifts which they need? &#8230; </big></big></p>
                    <p><big style="font-family: arial;"><big>Every defect in the human
                            character can be corrected through the exercise of faith and pleading
                            with the Lord for the gifts that He has said He will give unto those
                            who believe and obey His commandments. </big></big><big
                        style="font-family: arial;">(<span style="font-style: italic;">Gospel
                            Truths</span>, 2 Vols., [1957], 1:196) </big><big><big> </big></big><br>
                    </p>
                    </li>
                </ul>
