                <p style="font-family: arial; margin-left: 40px;"><big><big>I am
                        disheartened and disturbed when I hear people avoiding the blessings
                        and challenges of life by indicating to themselves, their associates,
                        and their family, "I'm sorry, but that's just the way I am." This is an
                        attitude that brings not only complacency but also a lack of progress.
                        It is a prime cause of unhappiness. </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>How
                        impressive it was in past years to hear President J. Reuben Clark, Jr.,
                        pray at a very advanced age for the Lord's assistance in helping him to
                        continue and remain true and faithful to the end. Yes, to finish life's
                        race with victory, and not resign himself to saying, "I'm in my
                        nineties, and that's just the way I am." </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>How often
                        we hear someone say, "I am just not cut out to be a leader in the
                        Church. It's not my strength. It's not my way. I will attend and go as
                        I feel impressed, but don't expect me to take positions of
                        responsibility and leadership, because I am just not that way." With
                        the gifts that are available from God, every one of us has the
                        opportunity to be the beneficiary of gifts that will make our progress
                        more meaningful and perfect. </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>It is
                        dangerous for anyone to be lulled into lack of progress because "I just
                        wasn't cut out to be a teacher," or "I wasn't cut out to be a good
                        housekeeper," or "It's all right for other people to do these things,
                        but it's impossible for me because it's just not my nature or
                        strength." </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>No one has
                        to be content with status quo. I've heard some of my friends who were
                        having difficulty with drugs and alcohol say, "I guess I'm just
                        supposed to be this way. My father was an alcoholic, and it comes
                        natural for me to be the same." There are no reasons for failure and no
                        traits of continuing downgrading just because an associate or family
                        member has taken these routes in the course of life. We need to make up
                        our minds we are going to take ourselves the way we are and, with God's
                        help, add to our gifts and abilities. </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>It is
                        unfair for us to label ourselves as saddled with this or that because
                        we have failed to realize our potential and the spiritual gifts that
                        are available as we pursue life's trails. It is true that everyone is
                        given a gift. The gifts that are available to us for the benefit of all
                        mankind are for self-improvement, self-development, and self-success so
                        that we may be of value to our associates and those who need our
                        strengths so badly.&nbsp; <small>(<i>The Measure of Our Hearts</i>
                            [1991], pp. 24-25)</small> </big></big></p>
