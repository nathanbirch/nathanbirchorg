                        ordinances of the gospel, it must be necessary for the dead to conform
                        to this plan. If a man cannot enter the kingdom of God without baptism,
                        then the dead must be baptized. But how can they be baptized in water
                        for the remission of their sins?</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>It is easy
                        to understand how they in person could believe in Christ and even
                        obtain the spirit of repentance; but water is an element of this world,
                        and how could spirits be baptized in it, or receive the laying on of
                        hands for the gift of the Holy Ghost? The only way it can be done is
                        vicariously, someone who is living acting as a substitute for the dead.</big></big></p>
