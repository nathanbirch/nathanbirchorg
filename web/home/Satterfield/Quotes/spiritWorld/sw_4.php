                        that message which they rejected from Noah.</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>We read in
                        the scriptures that the Son of God, after his death, went to the
                        spirits in prison and taught them the principles of the gospel, that
                        they might live according to God in the spirit and be judged according
                        to men in the flesh. Those very same spirits, who were disobedient in
                        the days of Noah, heard the voice of the Son of God when he went to
                        them, while his body was in the tomb, and all who were willing to
                        receive his testimony and obey his commandments received relief from
                        their torment.</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>But from
                        the time of their death in the flood until the time of the crucifixion
                        of the Savior, they were shut up in the prison house in torment,
                        suffering the penalty of their transgressions, because they refused to
                        hear a prophet of the Lord - and so it will be with every man who
                        rejects the gospel, whether he lived anciently or whether he lives now;
                        it makes no difference. <em>Every man who rejects the testimony of
                            Jesus, who denies the truth, who refuses to receive the testimony as it
                            is declared unto him by the elders of Israel, shall be punished and
                            shall be placed in the prison house, and there he shall stay until he
                            has paid the penalty of his transgressions</em>. <small>(<em>Doctrines
