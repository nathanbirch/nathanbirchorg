                <p style="margin-left: 80px; font-family: arial;"><big><big>It is the
                        righteous who go to paradise. It is the righteous who cease from those
                        things that trouble. Not so with the wicked. They remain in torment.
                        They have their anguish of soul intensified, if you please, when they
                        get on the other side, because they are constantly recalling to mind
                        their evil deeds. They are aware of their neglected opportunities,
                        privileges in which they might have served the Lord and received a
                        reward of restfulness instead of a reward of punishment. And so they
                        remain in torment until the time comes for their deliverance. . . .</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>The
                        righteous, those who have kept the commandments of the Lord, are not
                        shut up in any such place, but are in happiness in paradise. They cease
                        from all this trouble, and trial, and tribulation, and anguish of soul.
                        They are free from all these torments, because they have been true and
                        faithful to their covenants. <small>(<em>Doctrines of Salvation</em>,
                            2:229-230)</small><br>
