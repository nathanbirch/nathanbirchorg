                    </big></big></p>
                <p
                    style="color: rgb(153, 0, 0); font-style: italic; font-family: arial;"><big><big><strong>The
                            Gulf Bridged Between Paradise and Prison</strong></big></big><br
                    style="font-family: arial;" wp="BR2">
                </p>
                <p style="margin-left: 40px; font-family: arial;"><big><big><strong>Joseph
                            Fielding Smith</strong></big></big> <big><big><br>
                    </big></big></p>
                <div style="margin-left: 80px; font-family: arial;"><big><big>We hear
                            the objection made, from time to time, that Jesus did not come to save
                            the dead, for he most emphatically declared himself that there was an
                            impassable gulf that separated the righteous spirits from the wicked.
                            In defense of their position they quote the words in Luke: "And beside
                            all this, between us and you there is a great gulf fixed: so that they
                            which would pass from hence to you cannot; neither can they pass to us,
                            that would come from thence."</big></big></div>
                <p style="margin-left: 80px; font-family: arial;"><big><big>These
                        words, according to the story, were spoken by Abraham's spirit to the
                        rich man who raised his eyes and asked that Lazarus might go touch his
                        lips and relieve his torment. Abraham replied that it could not be for
                        there was a gulf fixed between them that the spirit of no man could
                        pass. Therefore, say the objectors to the doctrine of universal
                        salvation, "It is quite evident that the righteous and the wicked who
                        are dead cannot visit each other, hence there is no salvation for the
                        dead."</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>This was
                        true before the days that Jesus atoned for sin, which is plainly shown
                        in the passage from the Book of Moses previously quoted. And it was at
                        this period this event occurred. However, Christ came and through his
                        death bridged that gulf, proclaimed liberty to the captives, and the
                        opening of this prison door to those who sat in darkness and captivity.</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big><em>From
                            that time forth this gulf is bridged so that the captives, after they
                            have paid the full penalty of their misdeeds, satisfied justice, and
                            have accepted the gospel of Christ, having the ordinances attended to
                            in their behalf by their living relatives or friends, receive the
                            passport that entitles them to cross the gulf</em>. <small>(<em>Doctrines
                                of Salvation</em>, 2:158; emphasis original)</small></big></big><small>
                </small><br>
                </p>
                <p style="margin-left: 80px; font-family: arial;"><big><big><br>
                        Paradise is not heaven, or the place where God dwells, but a place of
                        departed spirits. Why the belief should be so general that the thief
                        went to heaven with the Savior is rather strange, since Jesus did not
                        go there until after his resurrection. This fact he disclosed to Mary
                        at the tomb. From the time his spirit left his body until he arose from
                        the tomb, Jesus was with the thief in paradise, according to his
                        promise. There the Savior opened the door for the salvation of the
                        dead. Before that time the unworthy dead were shut up in prison and
                        were not visited. (Moses 7:38-39; Isaiah 24:22.) We have good reason to
                        believe that the righteous spirits in paradise did not mingle with the
                        unrighteous spirits before the visit of our Lord to the spirit world.
                        He declared that there was a gulf fixed that could not be crossed which
                        separated the righteous from the unrighteous, therefore there was no
                        sound of the voice of prophets and the Gospel was not declared among
                        the wicked until Christ went into that world before his resurrection.
                        He it was who opened the prison doors. - Isaiah 42:6-7; 61:1.</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>President
                        Brigham Young declared that "Jesus was the first man that ever went to
                        preach to the spirits in prison, holding the keys of the Gospel of
                        salvation to them. Those keys were delivered to him in the day and hour
                        that he went into the spirit world, and with them he opened the door of
                        salvation to the spirits in prison." (J.D. 4:285.) This is in full
                        accord with the scriptures. President Joseph F. Smith, in the vision he
                        beheld of the spirit world, confirmed this view. (<em>Gos. Doc</em>.,
                        pp. 596-601.) In that world Christ taught the righteous spirits and
                        commissioned them to carry his message and sent them forth among the
                        unbaptized dead. In this way he fulfilled his promise made to Isaiah
                        that he would preach to the spirits of the dead and open their prison
                        doors that they might go free. <small>(<em>The Way to Perfection</em>,
                            pp.315-316)</small></big></big></p>
                <div style="margin-left: 40px;"><big style="font-family: arial;"><big><strong>Bruce
                                R. McConkie</strong></big></big></div>
                <ul style="margin-left: 40px; font-family: arial;">
                    <big><big>The spirit prison is hell, that portion of the spirit world
                            where the wicked dwell. (Moses 7:37-39.) Before Christ bridged the gulf
                            between paradise and hell - so that the righteous could mingle with the
                            wicked and preach them the gospel - the wicked in hell were confined to
                            locations which precluded them from contact with the righteous in
                            paradise. Abraham told the rich man in hell that between him and
                            Lazarus (who was in paradise) there was a great gulf fixed so that none
                            could go from paradise to hell or from hell to paradise. (Luke
                            16:19-31.) Now that the righteous spirits in paradise have been
                            commissioned to carry the message of salvation to the wicked spirits in
                            hell, there is a certain amount of mingling together of the good and
                            bad spirits. Repentance opens the prison doors to the spirits in hell;
                            it enables those bound with the chains of hell to free themselves from
                            darkness, unbelief, ignorance, and sin. As rapidly as they can overcome
                            these obstacles - gain light, believe truth, acquire intelligence, cast
                            off sin, and break the chains of hell - they can leave the hell that
                            imprisons them and dwell with the righteous in the peace of paradise. <small>(<em>Mormon
                                    Doctrine</em>, p.755)</small></big></big><br>
                </ul>
                <ul style="margin-left: 40px; font-family: arial;">
                    <big><big><br>
                            By spirit world is meant the abiding place of disembodied spirits,
                            those who have passed from pre-existence to mortality and have also
                            gone on from this temporal world to another sphere to await the day of
                            their resurrection, final redemption, and judgment. This world is
                            divided into two parts: paradise which is the abode of the righteous,
                            and hell which is the abode of the wicked. (Alma 40:11-14.)</big></big>
                </ul>
                <p style="margin-left: 80px; font-family: arial;"><big><big>Until the
                        death of Christ these two spirit abodes were separated by a great gulf,
                        with the intermingling of their respective inhabitants strictly
                        forbidden. (Luke 16:19-31.) After our Lord bridged the gulf between the
                        two (1 Pet. 3:18-21; Moses 7:37-39), the affairs of his kingdom in the
                        spirit world were so arranged that righteous spirits began teaching the
                        gospel to wicked ones. (<em>Gospel Doctrine</em>, 5th ed., pp. 473-476.)</big></big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><big>Thus,
                        although there are two spheres within the one spirit world, there is
                        now some intermingling of the righteous and the wicked who inhabit
                        those spheres; and when the wicked spirits repent, they leave their
                        prison-hell and join the righteous in paradise. Hence, we find Joseph
                        Smith saying: "Hades, sheol, paradise, spirits in prison, are all one:
                        it is a world of spirits. The righteous and the wicked all go to the
                        same world of spirits until the resurrection." (<em>Teachings</em>, p.
                        310.)<small> [<em>Mormon Doctrine</em>, p. 762]</small></big></big></p>
