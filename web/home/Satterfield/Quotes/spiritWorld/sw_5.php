                        different! They move with ease and like lightning. If we want to visit
                        Jerusalem, or this, that, or the other place--and I presume we will be
                        permitted if we desire--there we are, looking at its streets. If we
                        want to behold Jerusalem as it was in the days of the Savior; or if we
                        want to see the Garden of Eden as it was when created, there we are,
                        and we see it as it existed spiritually, for it was created first
                        spiritually and then temporally, and spiritually it still remains. And
                        when there we may behold the earth as at the dawn of creation, or we
                        may visit any city we please that exists upon its surface. If we wish
                        to understand how they are living here on these western islands, or in
                        China, we are there; in fact, we are like the light of the morning, or,
                        I will not say the electric fluid but its operations on the wires. God
                        has revealed some little things, with regard to his movements and
                        power, and the operation and motion of the lightning furnish a fine
                        illustration of the ability of the Almighty. When we pass into the
                        spirit world we shall possess a measure of his power. Here, we are
                        continually troubled with ills and ailments of various kinds. In the
                        spirit world we are free from all this and enjoy life, glory, and
                        intelligence; and we have the Father to speak to us, Jesus to speak to
                        us, and angels to speak to us, and we shall enjoy the society of the
                        just and the pure who are in the spirit world until the resurrection. <small>(<em>Discourses
