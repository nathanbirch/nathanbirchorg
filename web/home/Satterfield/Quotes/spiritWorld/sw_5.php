                <p style="margin-left: 80px; font-family: arial;"><big><big>We read or
                        learn a thing by observation yesterday, and to-day or to-morrow it is
                        gone, unless it be something that impresses us distinctly, that makes a
                        vivid impression upon the mind, that we can remember it perhaps for
                        days, months, and years; but common information and knowledge are
                        constantly coming into our minds, and as constantly being forgotten.
                        And some of the knowledge we receive here at one time becomes so
                        completely obliterated, through the weakness of the animal system, that
                        we cannot call it to mind, no association of ideas will again suggest
                        it to our minds; it is gone, erased, eradicated from the tablet of our
                        memories. This is not owing to the want of capacity in the spirit; no,
                        but the spirit has a full capacity to remember; for do you suppose that
                        God in begetting spirits in the eternal world would beget an imperfect
                        thing, that had no capacities? No. The Being, who is full of
                        intelligence, knowledge, and wisdom, and acting upon the great
                        principles that are ordained for the generation of living beings,
                        spiritual beings, brings them forth with capacities capable of being
                        enlarged or extended wider and wider; consequently it is not the want
                        of capacity in the spirit of man that causes him to forget the
                        knowledge he may have learned yesterday; but it is because of the
                        imperfection of the tabernacle in which the spirit dwells; because
                        there is imperfection in the organization of the flesh and bones, and
                        in things pertaining to the tabernacle; it is this that erases from our
                        memory many things that would be useful; we cannot retain them in our
                        minds, they are gone into oblivion. It is not so with the spirit when
                        it is released from this tabernacle. </big></big></p>
