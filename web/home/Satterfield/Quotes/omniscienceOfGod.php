                <center style="font-family: arial;"><big>Thoughts on
                        <br>
                        <font size="+3"><big>The Omniscience of God</big></font>
                    </big>
                    <p><big>By
                        <br>
                        <big>Elder Neal A. Maxwell</big></big></p>
                </center>
                <p style="font-family: arial;"><big><a href="../PDF/OMNISIC.pdf">PDF
                        Version</a>
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><span style="font-size: 11pt;"></span><span
                        style="font-size: 18pt;"></span><big><big><span
                                style="font-weight: bold;">A.</span></big>&nbsp; God,
                        who knows the beginning from the end, knows, therefore, all that is in
                        between. He could not safely see us through our individual allotments
                        of
                        "all these things" that shall give us experience if He did not first
                        know
                        "all things."</big>
                </big></p>
                <p style="font-family: arial;"><big><big>Below the scripture that
                        declares
                        that God knows "all things" there is no footnote reading "except that
                        God
                        is a little weak in geophysics"! We do not worship a God who simply
                        forecasts
                        a generally greater frequency of earthquakes in the last days before
                        the
                        second coming of His Son; He knows precisely when and where all these
                        will
                        occur. God has even prophesied that the Mount of Olives will cleave in
                        twain at a precise latter-day time as Israel is besieged. (Zechariah
                        14:4.)
                    </big></big></p>
                <p style="font-family: arial;"><big><big>There are no qualifiers, only
                        flat
                        and absolute assertions of the omniscience of God such as these: "The
                        Lord
                        searcheth all hearts, and understandeth all." (1 Chronicles 28:9.) The
                        psalmist said that the Lord's "understanding is infinite." (Psalm
                        147:5.)
                        "Now we are sure that thou knowest all things." (John 16:30.) "The Lord
                        knoweth all things which are to come." (Words of Mormon 1:7.)
                    </big></big></p>
                <p style="font-family: arial;"><big><big>Mortals should not aspire to
                        teach
                        God that He is not omniscient by adding qualifiers that He has never
                        used
                        in the scriptures. Job rightly asked, "Shall any teach God knowledge?"
                        (Job 21:22.)
                    </big></big></p>
                <p style="font-family: arial;"><big><big>The Lord could not know all
                        things
                        that are to come if He did not know all things that are past as well as
                        all things that are present. Alma described God's "foreknowledge" of
                        all
                        things and said also that God "comprehendeth all things." (Alma 13:3;
                        26:35.)
                        Indicating that omniscience is a hallmark of divinity, Helaman wrote,
                        "Except
                        he was a God he could not know of all things." (Helaman 9:41.)
                    </big></big></p>
                <p style="font-family: arial;"><big><big>The Lord Himself said that He
                        "knoweth
                        all things, for all things are present" before Him. (D&amp;C 38:2.) We
                        read, too, that "all things are present with me, for I know them all."
                        (Moses 1:6.)
                    </big></big></p>
                <p style="font-family: arial;"><big><big>Therefore, God's omniscience
                        is
                        not solely a function of prolonged and discerning familiarity with us
                        --
                        but of the stunning reality that the past and present and future are
                        part
                        of an "eternal now" with God! (Joseph Smith, History of the Church
                        4:597.)
                        (<i>All These Things Shall Give Thee Experience,</i> page 7,8)
                        <br>
                        &nbsp;
                    </big></big></p>
                <p style="font-family: arial;"><big><big><b><br>
                            <big>B</big></b>
                        Some find the doctrines of the omniscience and foreknowledge of God
                        troubling
                        because these seem, in some way, to constrict their individual agency.
                        This concern springs out of a failure to distinguish between how it is
                        that God knows with perfection what is to come but that we do not know,
                        thus letting a very clear and simple doctrine get obscured by our own
                        finite
                        view of things. . .&nbsp;
                    </big></big></p>
                <p style="font-family: arial;"><big><big>Ever to be emphasized,
                        however,
                        is the reality that God's "seeing" is not the same thing as His
                        "causing"
                        something to happen. (<i>All These Things Shall Give Thee Experience,</i>
                        page 20)&nbsp;
                        <br>
                        &nbsp;
                    </big></big></p>
                <p style="font-family: arial;"><big><big><big><span
                                style="font-weight: bold;">C</span></big>
                        God is never surprised (fantasy stories to the contrary) by unexpected
                        arrivals in the spirit world because of unforeseen deaths. But we must
                        always distinguish between God's being able to foresee and His causing
                        or desiring something to happen, a very important distinction! God
                        foresaw
                        the fall of His beloved David but did not cause it. (See D&amp;C
                        132:39.)
                        Sending for Bathsheba was David's decision, and even her battle-weary
                        husband
                        Uriah's sleeping loyally by David's door was not enough to bring a by
                        then
                        devious and determined David to his senses. (2 Samuel 11:9.)
                    </big></big></p>
                <p style="font-family: arial;"><big><big>By foreseeing, God can plan
                        and
                        His purposes can be fulfilled, but He does this in a way that does not
                        in the least compromise our individual free agency, any more than an
                        able
                        meteorologist causes the weather rather than forecasts it. Part of the
                        reason for this is our forgetfulness of our earlier experiences and the
                        present inaccessibility of the knowledge and understanding we achieved
                        there. The basic reason, of course, is that, as we decide and act, we
                        do
                        not know what God knows. Our decisions are made in our context, not
                        His.
                        (<i>All These Things Shall Give Thee Experience,</i> page 18-19)
                        <br>
                        &nbsp;
                    </big></big></p>
                <p style="font-family: arial;"><big><big><big><b>D</b></big>
                        Traditional discussions of omniscience ignore the fact that this
                        attribute
                        is much more than God's simply noticing and observing everything as it
                        happens. It is a remarkable thing for God to notice every sparrow that
                        falls. But God could be fully noticing and aware -- and yet still be
                        surprised,
                        along with the rest of us. Yet the living God is aware of all things
                        before
                        they unfold. This supernal dimension of knowledge is a part of
                        omniscience!
                    </big></big></p>
                <p style="font-family: arial;"><big><big>Because of His omniscience and
                        foreknowledge,
                        God is, therefore, able to see His plan unfold safely. If He were less
                        than omniscient and did not, in fact, operate out of perfect
                        foreknowledge,
                        His plan of salvation would by now be in shambles. (<i>All These Things
                            Shall Give Thee Experience,</i> page 18)
                        <br>
                        &nbsp;
                    </big></big></p>
                <p style="font-family: arial;"><big><big><big><b>E</b></big>
                        Did Jesus not understand that we would, at times, feel intimidated by
                        the
                        very perfectness of His life? Of course He knew and knows! His
                        omniscience
                        -- perfect knowledge -- about the long developmental distance we have
                        to
                        travel only makes His entreaties and invitations to us all the more
                        significant
                        and encouraging.
                    </big></big></p>
                <p style="font-family: arial;"><big><big>He understands completely!
                        Hence,
                        His assurances are completely reliable; He will lead us along, giving
                        us
                        here a little, there a little. He knows perfectly our bearing capacity
                        as well as our individual distances yet to be traveled.
                    </big></big></p>
                <p style="font-family: arial;"><big><big>Jesus' kindness and concern
                        for
                        His disciples can be noticed in His efforts to help them establish a
                        wise
                        pace even in their diligence. The wise use of time helps to fit us for
                        eternity. On one occasion, when the Twelve had given their personal
                        reports
                        to the Savior, He could see how harried and wearied they were. When the
                        press of the crowd upon them was so great that they could scarcely eat,
                        Jesus took them apart to rest for awhile in a solitary place. Who more
                        than the Lord of the Sabbath understands about the need for rest?
                    </big></big></p>
                <p style="font-family: arial;"><big><big>Happily, his foreknowledge of
                        our
                        failures does not cause Christ to give up concerning us without
                        providing
                        us with the opportunity to repent and to follow Him. His foreknowledge
                        rests upon His perfect awareness of our weaknesses and capabilities,
                        while
                        His relentlessly redeeming love makes of Him a true and perfect
                        Shepherd.
                    </big></big></p>
                <p style="font-family: arial;"><big><big>The Father's entire plan of
                        salvation
                        has taken into account beforehand our prospective successes and
                        failures,
                        so that His plan will be fully executed and His purposes completely
                        fulfilled
                        through Christ. Unlike us, God can see the end from the beginning, "and
                        thus it is"!
                    </big></big></p>
                <p style="font-family: arial;"><big><big>Thus we are to "confess" God's
                        hand
                        "in all things." This clearly does not mean that He causes or approves
                        of all things that occur; rather, it means that He has taken all things
                        into account from the beginning, and His purposes will still prevail. A
                        failure to confess this is a failure to acknowledge God's attributes of
                        omniscience and omnipotence -- another example of not knowing "what we
                        worship." Such is evidence of defective faith in a defective deity --
                        like
                        saying a god exists but does not preside, cares but cannot deliver, and
                        has identity but not sovereignty.
                    </big></big></p>
                <p style="font-family: arial;"><big><big>One searches in vain for
                        accurate
                        analogies to portray what it is like for us in our own imperfections to
                        ponder His perfections; clearly it is not for finiteness to comprehend
                        infiniteness. For, in addition to believing in God, we are counseled to
                        accept His omniscience, to believe "that man doth not comprehend all
                        things
                        which the Lord can comprehend."
                    </big></big></p>
                <p style="font-family: arial;"><big><big>Likewise, when the Lord asks
                        us
                        to confess His hand in all things, He is not seeking our ritualistic
                        incantation
                        concerning His powers. Again, He is after realism in us, including our
                        acknowledgment that His omniscience has made allowance even for human
                        errors
                        of which He does not approve, but which are not allowed to frustrate
                        His
                        overall plan. We can count on His plans even when He cannot count on
                        us!
                        (<i>Even As I Am</i>, pp. 56-57)
                    </big></big></p>

