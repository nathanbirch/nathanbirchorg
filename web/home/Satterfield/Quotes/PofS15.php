                                    <span style="font-style: italic; color: rgb(153, 0, 0);">What is the
                                        Gospel?</span></big></big></font></big></big></center>
                <p style="font-family: arial;"><big><big><b>Romans 1:16<br>
                        </b></big></big></p>
                <div style="margin-left: 40px;"><big><big><span
                                style="font-family: arial;">For I am not ashamed of the gospel of
                                Christ: <span style="text-decoration: underline;">for it is the power
                                    of God unto salvation</span> to every one that believeth;<br>
                                <br>
                                <br>
