                        lesser priesthood continued, which priesthood
                        holdeth the key of the ministering of angels and the preparatory
                        gospel;
                        Which gospel is the gospel of repentance and of baptism, and the
                        remission
                        of sins, and the law of carnal commandments, which the Lord in his
                        wrath
                        caused to continue with the house of Aaron among the children of Israel
                        until John, whom God raised up, being filled with the Holy Ghost from
                        his
                        mother's womb.<br>
