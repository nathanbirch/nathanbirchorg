                <center style="font-family: arial;"><big><big></big></big>
                    <p style="font-style: italic; color: rgb(153, 0, 0);"><big><big><font
                                size="+2"><big><big>Two Priesthoods</big></big></font></big></big></p>
                </center>
                <p style="font-family: arial;"><big><big><b>Bruce R. McConkie</b>
                    </big></big></p>
                <ul style="font-family: arial;">
                    <big><big></big></big><li><big><big>For our purposes here, the great
                            distinction between the Melchizedek
                            Priesthood
                            and the Aaronic or Levitical Priesthood is this: <u>The Melchizedek
                                Priesthood
                                administers the gospel in its everlasting fulness</u>, but<u> the
                                Aaronic
                                Priesthood administers the preparatory gospel only</u>, which
                            preparatory
                            gospel is the law of Moses and includes the law of carnal commandments.[<i>The
                                Promised Messiah</i>, p.410]</big></big></li>
                    <big><big></big></big>
                </ul>
                <ul style="font-family: arial;">
                    <big><big></big></big><li><big><big>Joseph Smith was asked, "Was the
                            Priesthood of Melchizedek taken away
                            when
                            Moses died?" He replied: "<u>All Priesthood is Melchizedek, but there
                                are
                                different portions or degrees of it</u>. That portion which brought
                            Moses
                            to speak with God face to face was taken away; but that which brought
                            the
                            ministry of angels remained." (Teachings, pp. 180-81.) In other words, <u>there
                                is only one priesthood, but it comes in degrees</u>; it is given
                            partially
                            or it is conferred in its fulness; it comes as the order of Aaron or as
                            the order of Melchizedek. <u>In this same sense, there is only one
                                gospel,
                                one law, one system of salvation, and it comes in degrees</u>. It is
                            all
                            the law of Christ. Salvation comes from no other source. He gives to
                            men
                            as much of his law as they are able to bear. If they can only bear up
                            under
                            the burdens of the lesser system, the schooling system, the preparatory
                            gospel, that is all they receive. Men are given according to their
                            desires
                            and their deeds. [<i>The Mortal Messiah</i>, 1:73]</big></big></li>
                    <big><big></big></big>
                </ul>
                <big><big><b style="font-family: arial;"><br>
                            George Q. Cannon</b><span style="font-family: arial;">
                        </span><br style="font-family: arial;">
                        <span style="font-family: arial;"><br>
                        </span></big></big>
                <div style="margin-left: 40px;"><big style="font-family: arial;"><big>Here
                            is plainly pointed out to us as a people the
                            future glory that God has in store for all those who receive and
                            magnify
                            the Priesthood. They are to receive the Father's Kingdom, and if they
                            receive
                            the Father's Kingdom, they are to receive all that the Father hath, for
                            all that he hath will be given unto them. Can you conceive of this? Can
                            any human being conceive of the immeasurable extent of the glory here
                            promised--the
                            immeasurable extent of exaltation here offered unto all those who
                            receive
                            the Priesthood of the Son of God, and who magnify it? . . . .
                        </big></big><br style="font-family: arial;">
                    <br style="font-family: arial;">
                    <big style="font-family: arial;"><big>God designed when He led Israel
                            out of Egypt to
                            make of that people a royal Priesthood--a kingdom of Kings and Priests.
                            He designed to lead them forward under the guidance of the everlasting
                            Priesthood, the Priesthood after the holy order of the Son of God--to
                            lead
                            them forward until they should behold the face of their God and see Him
                            for themselves. But they would not. They hardened their hearts. They
                            could
                            not endure His presence. Moses, despite all the power which God gave
                            him,
                            and the revelations which He poured out upon him--Moses could not
                            succeed
                            in leading that people forward. They were a stiff-necked race; they
                            were
                            a rebellious race; they were an idolatrous race; they were hard in
                            their
                            hearts; and they would not have God to be their King. They wanted a
                            lower
                            order of affairs; they wanted lower laws, laws that were more in
                            accordance
                            with their fallen and carnal natures. Therefore according to those
                            revelations
                            which God has given unto us, He took Moses out of their midst, and the
                            Holy Priesthood also, and the lesser Priesthood continued, the
                            Priesthood
                            of Aaron, a Priesthood that held the keys of the ministering of angels
                            and the preparatory gospel, the gospel of repentance and baptism for
                            the
                            remission of sins. He left that with them, and the law of carnal
                            commandments
                            which the Lord in His wrath caused to continue with the house of Aaron
                            among the children of Israel until John [see D&amp;C 84:23-27]. They
                            were
                            under this law during this long period. God raised up John as a
                            forerunner
                            to prepare the way for the fullness of the everlasting Gospel and the
                            restoration
                            of the Priesthood in its fullness, that Melchizedek Priesthood which
                            Moses
                            held, and through which he exercised such mighty power among the
                            children
                            of Israel. And it was God's design--if the people would have submitted
                            to it, if they would have received the message that He sent unto them
                            through
                            John and afterwards through His beloved Son--to have restored the
                            Kingdom
                            even to Israel, and to have built up the Kingdom in great power and
                            glory
                            upon the earth. But instead of receiving the Gospel and the Melchizedek
                            Priesthood, they actually destroyed the Son of God. But before they
                            destroyed
                            Him, they destroyed the heir of the Priesthood of Aaron, John the
                            Baptist,
                            who was a direct descendant of Aaron, and by virtue of this descent
                            held
                            the keys of that Priesthood, and exercised the authority and power
                            thereof
                            among the Jews. Not content with rejecting the Son of God, they
                            rejected
                            even John, and the Aaronic Priesthood as well as the Melchizedek
                            Priesthood
                            was taken from the midst of the Jews, and they have remained without
                            Priesthood
                            having been withdrawn from their midst--the authority to officiate in
                            the
                            ordinances pertaining thereunto.
                        </big></big><br style="font-family: arial;">
                    <br style="font-family: arial;">
                    <big style="font-family: arial;"><big>Now, this Priesthood which God
                            has restored in these
                            last days through Peter, James and John, is the Priesthood that
                            continueth
                            in the Church of God in all generations. The Church of God cannot be
                            without
                            it; for without it the power of Godliness is not made manifest to men
                            in
                            the flesh. A people can progress to a certain extent with the Aaronic
                            Priesthood,
                            but there is a limit to their progress. There are bounds beyond which
                            they
                            cannot pass. They cannot attain to the fullness of the glory of God the
                            Eternal Father, without the presence of the Melchizedek Priesthood; for
                            as I have read in your hearing, "This greater Priesthood administereth
                            the Gospel and holdeth the key of the mysteries of the kingdom, even
                            the
                            key of the knowledge of God; therefore in the ordinances thereof,"
                            [that
                            is, in the ordinances of the higher or Melchizedek Priesthood] "the
                            power
                            of Godliness is manifest." This cannot be manifest without that higher
                            Priesthood, without the power which accompanies it. "And without the
                            ordinances
                            thereof," the revelation continues, "and the authority of the
                            Priesthood,
                            the power of godliness is not manifest unto men in the flesh; for
                            without
                            this no man can see the face of God, even the Father, and live."
                            (D&amp;C
                            84:19-22)
                        </big></big><br style="font-family: arial;">
                    <br style="font-family: arial;">
                    <big style="font-family: arial;"><big>Without this Priesthood, without
                            its ordinances,
                            without its powers, without its gifts, "no man can see the face of God,
                            even the Father, and live." Therefore it is essential that, if a people
                            should be exalted unto the presence of God, they should have this
                            Melchizedek
                            or greater Priesthood, and the ordinances thereof, by the means of
                            which
                            they are to be prepared, or they shall be prepared to enter into the
                            presence
                            of God the Father, and endure His presence. [<i>Journal of Discourses</i>,
                            25: 290-294]
                        </big></big><br style="font-family: arial;">
                </div>
