                <big style="font-family: arial;"><big><b>Brigham Young</b>
                        <br>
                        <br>
                    </big></big><big><big></big></big>
                <div style="margin-left: 40px;"><big><big><span
                                style="font-family: arial;">The Gospel of the Son of God that has been
                                revealed
                            </span><u style="font-family: arial;">is
                                a plan or system of laws and ordinances, by strict obedience to which
                                the
                                people who inhabit this earth are assured that they may return again
                                into
                                the presence of the Father and the Son</u><span
                                style="font-family: arial;">. [</span><i style="font-family: arial;">Journal
                                of Discourses</i><span style="font-family: arial;">
                                13:233;
                            </span><i style="font-family: arial;">Discourses of Brigham</i><span
                                style="font-family: arial;"> </span><i style="font-family: arial;">Young</i><span
                                style="font-family: arial;">, p.1]</span>
                        </big></big></div>
                <p style="font-family: arial;"><big><big><b><br>
                            Spencer W. Kimball</b>
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Now, what
                        is the gospel of which we speak? <u>It
                            is the power of God unto salvation; it is the code of laws and
                            commandments
                            which help us to become perfect, and the ordinances which constitute
                            the
                            entrance requirements</u>.
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>The
                        ordinances begin with baptism by immersion by
                        proper authority for the remission of sins and for entrance into the
                        earthly
                        kingdom of God. It is followed by the reception of the Holy Ghost,
                        which
                        is promised to every person who qualifies. The priesthood is given,
                        which
                        opens further doors; the endowment is an indispensable feature in
                        preparation
                        for eternal life; and then, the sealing in the holy temple of a man and
                        a woman for an eternal relationship. These are indispensable! No one
                        can
                        ever reach the heights of exaltation and eternal life without all of
                        them.
                        [<i>The Teachings of Spencer W. Kimball</i>, p.502]
                    </big></big></p>
                <p style="font-family: arial;"><big><big><b><br>
                            Stephen L Richards</b>
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Now if it
                        is essential to receive the gospel in
                        order to receive the Christ, what follows? To answer this we must
                        understand
                        and define the gospel. Is the gospel itself anything more than the sum
                        total of the Savior's divine attributes and qualities? I think that all
                        who give careful consideration to the scriptures and the works of the
                        Master
                        will agree that it is. Perhaps the most famous of all definitions of
                        the
                        gospel is that given by Paul when he said: ". . . I am not ashamed of
                        the
                        gospel of Christ: for it is the power of God unto salvation to every
                        one
                        that believeth;..." (Romans 1:16.)
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>It is
                        certainly not difficult to gather from the
                        words, "power of God unto salvation," a meaning beyond that of a moral
                        code for living based upon the attributes of the Savior. <u>The gospel
                            as a power unto salvation must embrace not only all the Christlike
                            characteristics
                            of living, but also the means essential to salvation</u>. <u>These
                            indispensable
                            means or essentials for salvation have been clearly set forth by the
                            Savior
                            on numerous occasions and in unmistakable terms. He prescribed
                            ordinances
                            and proclaimed their indispensable nature</u>.[Conference Report, April
                        1959, p.52-54]
                    </big></big></p>
                <p style="font-family: arial;"><big><big><b><br>
                            Harold B. Lee</b>
                        <br>
                    </big></big></p>
                <div style="margin-left: 40px;"><big style="font-family: arial;"><big>What
                            is the gospel then? . . . So often I hear my
                            brethren saying something that I wish we would not say quite that way
                            --
                            that the gospel is a way of life. It is not a way of life -- <u>it is
                                the
                                way to eternal life</u>. It is the science of salvation. [Conference
                            Report,
                            April 1959, p.68]
                        </big></big><br>
                </div>
                <p style="font-family: arial;"><big><big>&nbsp;
                        <br>
                        &nbsp;
                        <br>
                        <br>
                    </big></big></p>
                <center style="font-family: arial;"><big><big></big></big>
                    <p style="font-style: italic; color: rgb(153, 0, 0);"><big><big><font
                                size="+2"><big><big>Two Gospels</big></big></font></big></big></p>
                </center>
                <p style="font-family: arial;"><big><big><b>Bruce R. McConkie</b>
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>There are
                        two gospels --<u> the preparatory gospel</u>
                        and <u>the fulness of the everlasting gospel</u>. There are two
                        proclamations,
                        two pronouncements of glad tidings, two messages of light and truth and
                        power, which God has given to his people at one time or another. What
                        the
                        people receive at any given moment in time depends upon them. The Lord
                        gives them all of his word, or only a portion, depending on "the heed
                        and
                        diligence which they give unto him." If all men had open hearts and
                        receptive
                        minds; if they desired righteousness and sought truth in preference to
                        all else; if they conformed to every true principle they received --
                        all
                        would accept the fulness of his gospel and join that church and kingdom
                        which is always administered for the benefit and blessing of mankind.
                        As
                        it is written: "He that will harden his heart, the same receiveth the
                        lesser
                        portion of the word; and he that will not harden his heart, to him is
                        given
                        the greater portion of the word, until it is given unto him to know the
                        mysteries of God until he know them in full." (Alma 12:9-10.)
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>As is
                        evident from the pure meaning of the words
                        themselves, <u>the fulness of the everlasting gospel has always
                            existed
                            and will continue to endure forever</u>; <u>the preparatory gospel, on
                            the other hand, is not eternal in nature, but is something that goes
                            before
                            and makes people ready for the receipt of the fulness of saving truth</u>.
                        [<i>The Promised Messiah</i>, p.404]
                    </big></big></p>
                <p style="font-family: arial;"><big><big><b><br>
                            Delbert L. Stapley</b>
                        <br>
                    </big></big></p>
                <div style="margin-left: 40px;"><big style="font-family: arial;"><big>Among
                            the many choice teachings given by the Savior
                            in that inspiring Sermon on the Mount, is this important instruction:
                            "Enter
                            ye in at the strait gate . . "Because strait is the gate, and narrow is
                            the way, which leadeth unto life, and few there be that find it."
                            (Matthew
                            7:13-14.) You will observe that I have quoted only the positive
                            elements
                            of this scripture. To enter
                            <u>the straight gate implies obedience to gospel
                                requirements</u>, and <u>the narrow way that leads to life connotes
                                additional
                                requirements, rites, and ordinances for all who desire salvation and
                                exaltation</u>.
                            . . .All who have repented and then been baptized and received the Holy
                            Ghost by authorized servants of I God have entered in by the straight
                            gate.
                            The narrow way can only be followed by obedience and faithfulness to
                            all
                            the sacred ordinances and requirements of the higher gospel plan,
                            obtained
                            in the holy temples of God. [Conference Report, April 1955, p.65]
                        </big></big><br>
                </div>
