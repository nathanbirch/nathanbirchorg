                    </big></big></p>
                <p style="font-family: arial;"><big><big><b>Joseph Smith</b>
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>The spirit
                        of Elias is to prepare the way for a
                        greater revelation of God, which is the Priesthood that Aaron was
                        ordained
                        unto. And when God sends a man into the world to prepare for a greater
                        work, holding the keys of the power of Elias, it was called the
                        doctrine
                        of Elias, even from the early ages of the world.
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>John's
                        mission was limited to preaching and baptizing;
                        but what he did was legal; and when Jesus Christ came to any of John's
                        disciples, He baptized them with fire and the Holy Ghost. . . .
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>John did
                        not transcend his bounds, but faithfully
                        performed that part belonging to his office; and every portion of the
                        great
                        building should be prepared right and assigned to its proper place; and
                        it is necessary to know who holds the keys of power, and who does not,
                        or we may be likely to be deceived.
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>That person
                        who holds the keys of Elias hath a preparatory
                        work. [<i>Teachings of the Prophet Joseph Smith</i>, p.336]<br>
                        <br>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><b>Bruce R. McConkie</b>
                    </big></big></p>
                <ul style="font-family: arial;">
                    <big><big></big></big><li><big><big>On the other hand, the
                            preparatory gospel is a lesser portion of the
                            Lord's
                            saving truths, a portion which prepares and schools men for a future
                            day
                            when the fulness of the gospel may be received, a portion which of
                            itself
                            is not sufficient to seal men up unto eternal life or assure them an
                            inheritance
                            in the celestial world. The preparatory gospel "is the gospel of
                            repentance
                            and of baptism, and the remission of sins, and the law of carnal
                            commandments."
                            (D. &amp; C. 84:27.) It is a gospel system administered by the lesser
                            or
                            Aaronic Priesthood. When the power to bestow the Holy Ghost is enjoyed,
                            which power is reserved for holders of the Melchizedek Priesthood, then
                            the fulness of the gospel is manifest. John the Baptist administered
                            the
                            preparatory gospel; Christ came with the fulness of the Melchizedek
                            Priesthood
                            and restored the fulness of the gospel. (John 1:26-27; Acts 19:1-6.) [<i>Mormon
                                Doctrine</i>, p.333]</big></big></li>
                    <big><big></big></big>
                </ul>
                <ul style="font-family: arial;">
                    <big><big></big></big><li><big><big>What does a forerunner or an
                            Elias do to prepare a people for Him who
                            shall
                            come after? He calls people to repentance and baptizes them in water
                            "for
                            the remission of sins," which freedom from sin is actually obtained
                            when
                            the repentant person receives the baptism of fire and of the Holy
                            Ghost.
                            This was the mission of John. He acted in the power and authority of
                            the
                            Aaronic Priesthood. A forerunner preaches "the preparatory gospel"; the
                            One who comes after preaches the fulness of the gospel. "The
                            preparatory
                            gospel . . . is the gospel of repentance and of baptism, and the
                            remission
                            of sins." (D&amp;C 84:26-27.) This is the gospel administered by the
                            law
                            of Moses; it was as far as John's authority went. No one is ever
                            prepared
                            for the Lord while he remains in his sins. The Lord does not save
                            people
                            in their sins, but from their sins. The plait of salvation is designed
                            to enable men to free themselves from sin so they can, as clean and
                            spotless
                            beings, enter the presence of Him who is without sin. No one is ever
                            prepared
                            for the Lord until he confesses and forsakes his sins, until he
                            repents,
                            until he is baptized for the remission of sins. And the fact that John
                            was to prepare "a people" for the Lord means that a people -- composed
                            of a host of individuals -- had to set their houses in order, be
                            baptized
                            by him, and await patiently the coming of Him who would give them the
                            Holy
                            Ghost. When they received the baptism of the Holy Ghost, sin and evil
                            would
                            be burned out of their souls as though by fire, and being thus clean,
                            they
                            would be fit candidates to be with the Lord they would be prepared for
                            the Lord. [<i>The Mortal Messiah</i>, p.386-387]</big></big></li>
                    <big><big><br>
                            &nbsp;</big></big>
                </ul>
                <center
