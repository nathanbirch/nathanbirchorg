                <p><big><font face="Arial" size="+1"><big><strong> Wilford Woodruff</strong></big></font></big></p>
                <p style="margin-left: 40px;"><big><font face="Arial" size="+1"><big>The
                        Lord has said concerning Jesus, that he descended below all things that
                        he might rise above all things, and
                        comprehend all things. No man descended lower than the Savior of the
                        world. Born in a stable, cradled in a manger, he
                        traveled from there to the cross through suffering, mingled with blood,
                        to a throne of grace; and in all his life there was
                        nothing of an earthly nature that seemed to be worth possessing. His
                        whole life was passed in poverty, suffering, pain,
                        affliction, labor, prayer, mourning and sorrow, until he gave up the
                        ghost on the cross. Still he was God's firstborn son and
                        the Redeemer of the world. The question might be asked why the Lord
                        suffered his Son to come here and to live and die
                        as he did. When we get into the spirit world, and the vail is
                        withdrawn, we shall then perhaps understand the whys and
                        wherefore of all these things. In the dispensations and providences of
                        God to man it seems that we are born to suffer pain,
                        affliction, sorrows and trials; this is what God has decreed that the
                        human family shall pass through; and if we make a right
                        use of this probation, the experience it brings will eventually prove a
                        great blessing to us, and when we receive immortality
                        and eternal life, exaltation, kingdoms, thrones, principalities and
                        powers with all the blessings of the fulness of the Gospel
                        of Christ, we shall understand and comprehend why we are called to pass
                        through a continual warfare during the few years
                        we spent in the flesh. (<em>Journal of Discourses</em>, Vol.18, p.33)</big></font></big><br
                    wp="BR1">
                </p>
