                        willing to sacrifice everything for the upbuilding of the
                        Kingdom of God. Any man who will seek to save his life and desert the
                        principles of the Gospel, is not worthy of eternal life. How many have
                        laid down their lives since the creation of the world for the sake of
                        the truth? Jesus Himself descended
                        below all things. He descended, I believe, lower than any other man
                        will be called to descend. Are we greater than Jesus? If we are called
                        upon to-day to lay down our lives, what of it? Is it not as well to die
                        for the Gospel's sake as to die for
                        anything else? A million of men, a few years ago, sacrificed their
                        lives for the honor of this nation. No matter what we may
                        be called to pass through, let us maintain our integrity to God. Where
                        is the man whose mind has been lit up by the
                        inspiration of God to comprehend the celestial kingdom, or the
                        celestial law, or the Gospel of Jesus Christ, who can bear
                        the idea of pursuing a course whereby he will be cut off from
                        inheriting the blessings for which he has hoped in the future? No, I
                        would rather die a thousand deaths than be deprived of these blessings.
                        We have a long time to live when we get
                        through here. There is all eternity before us. It will pay you, it will
                        pay me--no matter what comes, no matter what this
                        nation may do to oppress us--to be true and faithful to our covenants,
                        to our wives and children, to our God and to our
