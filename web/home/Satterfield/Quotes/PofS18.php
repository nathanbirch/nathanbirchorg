                    <p style="color: rgb(0, 0, 153);"><big><font size="+2"><big>Do Not
                            Cling Too</big></font>
                        <br>
                        <font size="+2"><big>Tenaciously To This Life</big></font></big></p>
                    <small></small></center>
                <p style="font-family: arial;"><big><b><font size="+1"><big><br>
                            Harold B. Lee</big></font></b>
