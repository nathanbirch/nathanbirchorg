                    <center style="font-style: italic; color: rgb(0, 0, 153);"><big><big><font
                                    size="+2"><big><big><small>To Die Well<br>
                                            <br>
                                        </small></big></big></font></big></big><big><big><b><font size="+1"><big><big></big></big></font></b></big></big></center>
                    <div style="text-align: left;"><big><b><font size="+1"><big>Sterling
                                    W. Sill</big></font></b>
                        </big></div>
                    <p style="text-align: left; margin-left: 40px;"><big><font size="+1"><big>Two
                            of
                            the most important events in life are birth and
                            death. And what a thrilling thing it is to be well born, to have goodly
                            parents and live in a godly home! But it may be even more important to
                            die well. . . .</big></font>
                    </big></p>
                    <p style="text-align: left; margin-left: 40px;"><big><font size="+1"><big>Sometimes
                            we miss one of our best opportunities--to learn
                            to die well--because we think of death as unpleasant. And because we
                            don't
                            like to think about unpleasant things, we sometimes close our minds and
                            turn away our faces. But death doesn't cease to exist just because it
                            is
                            ignored. The ancient Egyptians had a much more logical way of handling
                            this situation when on their great festive occasions they kept
                            constantly
                            on display before the revelers the skeleton of a dead man. That is,
                            they
                            held up this great image of death before themselves that each one might
                            be continually and constantly reminded that some day he would die.</big></font>
                    </big></p>
                    <p style="text-align: left; margin-left: 40px;"><big><font size="+1"><big>Now
                            I
                            don't want to frighten anyone unduly in this audience
                            this morning, but I would like to point out as gently and as kindly as
                            I can that someday this tremendously important experience of our mortal
                            estate will come to its end. Someone has said that judging by the past
                            there will be very few of us who get out of this world alive. From the
                            very beginning of life, each one of us lives under an irrevocable,
                            unchangeable
                            death sentence, with a guarantee that it will be carried out. The Lord
                            has given us this maximum notice to enable us to adequately prepare for
                            it. And one man indicated this certainty by an inscription on his
                            tombstone
                            saying, "I knew it would happen!" . . .</big></font>
                    </big></p>
                    <p style="text-align: left; margin-left: 40px;"><big><font size="+1"><big>And
                            it
                            has been said that the most important event in
                            life is death. We live to die and then we die to live. Death is a kind
                            of graduation day for life. It is our only means of entrance to our
                            eternal
                            lives. And it seems to me to be a very helpful procedure to spend a
                            little
                            time preliving our death. That is, what kind of person would you like
                            to
                            be when the last hour of your life arrives?</big></font>
                    </big></p>
                    <p style="text-align: left; margin-left: 40px;"><big><font size="+1"><big>The
                            last hour is the key hour. That is the hour that judges
                            all of the other hours. No one can tell whether or not his life has
                            been
                            successful until his last hour. As Sophocles said, "We must wait till
                            evening
                            to know how pleasant the day has been."</big></font>
                    </big></p>
                    <div style="text-align: left; margin-left: 40px;"><big><font size="+1"><big>What
                                a thrilling experience that we may live well, enabling
                                us to die well and then live with God in the celestial kingdom
                                throughout
                                eternity. (<i>Ensign</i>, Nov. 1976, pp. 46-48)</big></font>
