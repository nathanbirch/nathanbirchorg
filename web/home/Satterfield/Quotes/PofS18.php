                <div style="margin-left: 40px;"><big style="font-family: arial;"><font
                            size="+1"><big>No man who is fit to live need
                            fear to die. To one here
                            in this life, death is often the most terrible thing we know, but when
                            we have tasted its reality it will mean to us birth, deliverance, a new
                            creation of ourselves. It will be what health is to a sick man, what
                            home
                            is to an exile, what the loved one given back is to the bereaved. As we
                            draw near to it a solemn gladness should fill our hearts. (<i>The
                                Teachings
                                of Harold B. Lee,</i>&nbsp;&nbsp;&nbsp; p. 44)</big></font></big><big><br>
