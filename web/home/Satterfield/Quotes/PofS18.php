                <ul style="font-family: arial;">
                    <small> </small><li><big> <font size="+1"><big>Now, very clearly
                            what the Lord has told
                            us in those two
                            verses is that there comes a time in the life of every person--no
                            matter
                            how righteous, even the Son of God, His chosen prophets--when, despite
                            the faith of the individual, despite the prayers that have been
                            offered,
                            despite the blessings of those who bear the holy priesthood, it's as
                            though
                            the omnipotent power were saying, "Now, your faith to the contrary,
                            this
                            person has an appointment unto death." (<i>The Teachings of Harold B.
                                Lee</i>,
                            p. 47)</big></font></big></li>
                    <small> </small><small></small>
                </ul>
                <ul style="font-family: arial;">
                    <small> </small><li><big><font size="+1"><big>I know a father who
                            lost his
                            daughter, a mother of four or
                            five children, and it was a long fight. I have never seen a father or
                            mother
                            who had greater devotion to a daughter, and as she sank into the
                            shadows
                            the father said, "I guess I failed. If I had faith, she would have
                            gotten
                            well." The Lord said that "those who have faith in me shall be healed
                            if
                            they are not appointed unto death" (see D&amp;C 42:48). That suggests
                            that
                            we are here filling a mission on earth. We can fail in our appointment,
                            but if we live true, then we fulfill our mission. Now, if the Lord asks
                            us, calls us to no greater calling, I ask you, Would any one of you
                            want
                            to remain here one hour more than the Lord wants you to live in
                            mortality,
                            if He had something else for you to do? Not if you are thinking right.
                            You would want to answer the call of the Master, wouldn't you? (<i>The
                                Teachings of Harold B. Lee</i>, p. 48)</big></font></big><br
