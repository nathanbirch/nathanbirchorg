                    </small><big><font size="+2"><big><span style="color: rgb(0, 0, 153);">The
                                Dead Are More Alive Than
                                Mortals</span><br>
                            <br>
                            <span style="font-style: italic;"></span></big></font><b><font size="+1"><big></big></font></b></big>
                    <div style="text-align: left;"><big><b><font size="+1"><big>Harold B.
                                    Lee<br>
                                </big></font></b></big></div>
                    <br>
                    <small></small></center>
                <div style="margin-left: 40px;"><big style="font-family: arial;"><font
                            size="+1"><big>We speak of the passing of a
                            loved one here as death.
                            What is the meaning of death? If death means insensibility to
                            impressions
                            round about us, then, as a matter of truth, we are more insensible and
                            thus more dead than the deceased; for when we say he died, it meant
                            that
                            his spiritual self took flight from his earthly body. It went "home,"
                            in
                            the language of the scriptures, to that God who gave it life. When we
                            shall
                            place in mother earth this afternoon these mortal remains, we're not
                            placing
                            there that which was the living or spiritual part of the deceased; we
                            shall
                            bury only that which was of the earth. The spirit took its flight, and
                            now has been able to view and see and know things far beyond our
                            understanding.
                            As a matter of truth, he is more alive than we are because he is more
                            sensitive
                            to things round about us. I have a firm conviction that the spirits of
                            those who pass on who are righteous have a great influence upon
                            [funeral]
                            services of this kind, bringing to such services through those who
                            perform
                            on the program, if they are in tune therewith, the kind of sweetness of
                            spirit, kindliness of council, forbearance, unselfishness,
                            devotion--all
                            those attributes all come seemingly back in the spirit of him who has
                            passed
                            through those who have been asked to sing and speak. (<i>The Teachings
                                of Harold B. Lee,</i> p. 45)</big></font>
                    </big><br>
                </div>
