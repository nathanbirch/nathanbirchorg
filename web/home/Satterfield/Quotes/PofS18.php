                            principle. They have work on
                            the other side of the veil; and they want men, and they call them. And
                            that was my view in regards to Brother George A. Smith. When he was
                            almost
                            at death's door, Brother Cannon administered to him, and in thirty
                            minutes
                            he was up and ate breakfast with his family. We labored with him in
                            this
                            way, but ultimately, as you know, he died. But it taught me a lesson. I
                            felt that man was wanted behind the veil. We labored also with Brother
                            Pratt; but he, too, was wanted behind the veil. (<i>The Discourses of
                                Wilford
                                Woodruff</i>, pp.290-291)</big></font> <br>
                        &nbsp; </big></p>
                    <small> </small><li><big><font size="+1"><big>We lost one of our
                            apostles a
                            short time since. He was about
                            the youngest man in the quorum of the apostles. He was suddenly called
                            away from us. There is a meaning to this. Many times things take place
                            with us that we do not comprehend, unless it is given to us by
                            revelation.
                            But there is a meaning in the loss of that young apostle. I had a
                            manifestation
                            of that while in San Francisco recently.</big></font></big></li>
                    <small> </small></blockquote>
                <blockquote style="font-family: arial;"><big><font size="+1"><big>One
                            evening, as I fell
                            asleep, I was very much
                            troubled with evil spirits that tried to afflict me; and while laboring
                            to throw off these spirits and their influence, there was another
                            spirit
                            visited me that seemed to have power over the evil spirits, and they
                            departed
                            from me. Before he left me he told me not to grieve because of the
                            departure
                            of Abraham Hoagland Cannon; for the Lord had called him to fill another
                            important mission in the spirit world, as a pure and holy apostle from
                            Zion in the Rocky Mountains -- a labor which would not only prove a
                            great
                            benefit to his father's household, but to the Church and kingdom of God
                            on the earth. I feel to name this, because it is true. I have become
                            acquainted
                            with many things in our history that I have marveled at. While in the
                            St.
                            George temple I had a son, who was in the north country, drowned. He
                            had
                            a warning of this. In a dream he was notified how he would die. We had
                            testimony of that after his death. I asked the Lord why he was taken
                            from
                            me. The answer to me was, "You are doing a great deal for the
                            redemption
                            of your dead; but the law of redemption requires some of your own seed
                            in the spirit world to attend to work connected with this." That was a
                            new principle to me; but it satisfied me why he was taken away. I name
                            this, because there are a great many instances like it among the
                            Latter-day
                            Saints. This was the case with Brother Abraham Cannon. He was taken
                            away
                            to fulfil that mission. And where we have anything of this kind, we
                            should
                            leave it in the hands of God to reconcile. (<i>The Discourses of
                                Wilford
                                Woodruff</i>, p.292)</big></font></big><br>
