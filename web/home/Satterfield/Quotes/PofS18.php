                            Peter
                            Maughan, who was dead, came to him and said: "Brother Roskelley, we
                            held
                            a council on the other side of the veil. I have had a great deal to do,
                            and I have the privilege of coming here to appoint one man to come and
                            help. I have had three names given to me in council, and you are one of
                            them. I want to inquire into your circumstances.</big></font> </big><small>
                    </small>
                    <p><big><font size="+1"><big>The Bishop told him what he
                            had to do, and they conversed
                            together as one man would converse with another. President Maughan then
                            said to him: "I think I will not call you. I think you are wanted here
                            more than perhaps one of the others."</big></font> </big></p>
                    <small> </small>
