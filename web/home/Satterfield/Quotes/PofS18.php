                        shall speak of a subject which
                        strikes dread--even terror--into
                        the hearts of most men. It is something we fear, of which we are sorely
                        afraid, and from which most of us would flee if we could.</big></font>
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>I
                        shall speak of the passing of
                        the immortal soul into
                        the eternal realms ahead, of that dread day when we shall shuffle off
                        this
                        mortal coil and go back to the dust from whence we came. I shall speak
                        of death--mortal death, the natural death, the death of the body--and
                        of
                        the state of the souls of men when this final consummation is imposed
                        upon
                        them. . . .</big></font>
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>When
                        the faithful saints depart
                        from this life they "are
                        received into a state of happiness, which is called paradise, a state
                        of
                        rest, a state of peace, where they shall rest from all their troubles
                        and
                        from all care, and sorrow" (Alma 40:12), and they remain in this state
                        until the day of their resurrection.</big></font>
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>When
                        the wicked and ungodly
                        depart from this life they
                        continue in their wickedness and rebellion. "That same spirit which
                        doth
                        possess your bodies at the time ye go out of this life," the scripture
                        says, "that same spirit will have power to possess your body in that
                        eternal
                        world." (Alma 34:34.)</big></font>
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>"Ye
                        must press forward with a
                        steadfastness in Christ,"
                        Nephi said to members of the Church, "having a perfect brightness of
                        hope,
                        and a love of God and of all men. Wherefore, if ye shall press forward,
                        feasting upon the word of Christ, and endure to the end, behold, thus
                        saith
                        the Father: Ye shall have eternal life." (2 Ne. 31:20.) That is to
                        say--all
                        the faithful Saints, all of those who have endured to the end, depart
                        this
                        life with the absolute guarantee of eternal life.</big></font>
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>There
                        is no equivocation, no
                        doubt, no uncertainty in
                        our minds. Those who have been true and faithful in this life will not
                        fall by the wayside in the life to come. If they keep their covenants
                        here
                        and now and depart this life firm and true in the testimony of our
                        blessed
                        Lord, they shall come forth with an inheritance of eternal life.</big></font>
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>We
                        do not mean to say that those
                        who die in the Lord,
                        and who are true and faithful in this life, must be perfect in all
                        things
                        when they go into the next sphere of existence. There was only one
                        perfect
                        man--the Lord Jesus whose Father was God.</big></font>
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>There
                        have been many righteous
                        souls who have attained
                        relative degrees of perfection, and there have been great hosts of
                        faithful
                        people who have kept the faith, and lived the law, and departed this
                        life
                        with the full assurance of an eventual inheritance of eternal life.</big></font>
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>There
                        are many things they will
                        do and must do, even beyond
                        the grave, to merit the fulness of the Father's kingdom in that final
                        glorious
                        day when the great King shall say unto them, "Come, ye blessed of my
                        Father,
                        inherit the kingdom prepared for you from the foundation of the world."
                        (Matt. 25:34.)</big></font>
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>But
                        what we are saying is that
                        when the saints of God
                        chart a course of righteousness, when they gain sure testimonies of the
                        truth and divinity of the Lord's work, when they keep the commandments,
                        when they overcome the world, when they put first in their lives the
                        things
                        of God's kingdom: when they do all these things, and then depart this
                        life--though
                        they have not yet become perfect--they shall nonetheless gain eternal
                        life
                        in our Father's kingdom; and eventually they shall be perfect as God
                        their
                        Father and Christ His Son are perfect.</big></font>
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>Is
                        it any wonder that the
                        scriptures say: "Precious in
                        the sight of the Lord is the death of his saints"? (Ps. 116:15.) Truly
                        such is precious, wondrous, and glorious, for when the saints die,
                        added
                        souls have assured themselves of exaltation with Him who provided the
                        way
                        for them to advance and progress and become like Him.</big></font>
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>Is
                        it any wonder that the
                        scriptures say: "Blessed are
                        the dead which die in the Lord," for they shall "rest from their
                        labours;
                        and their works do follow them." (Rev. 14:13.) Truly it is a blessed
                        occasion,
                        for the faithful saints have filled the full measure of their creation,
                        and a gracious God will give them all things in due course.</big></font>
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>Is
                        it any wonder that the Lord
                        says to His saints, "Those
                        that die in me shall not taste of death, for it shall be sweet unto
                        them"?
                        (D&amp;C 42:46.)</big></font>
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>Is
                        it any wonder that the
                        Prophet Joseph Smith said such
                        things as: "When men are prepared, they are better off to go hence"? (<i>Teachings
                            of the Prophet Joseph Smith</i>, p. 326.)</big></font>
                </big></p>
