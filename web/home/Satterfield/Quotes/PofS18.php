                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>On
                        the other side of the veil,
                        there are perhaps seventy
                        billion people. They need the same gospel, and releases occur here to
                        aid
                        the Lord's work there. Each release of a righteous individual from this
                        life is also a call to new labors. Those who have true hope understand
                        this.</big></font>
                </big></p>
                <div style="margin-left: 40px;"><big style="font-family: arial;"><font
                            size="+1"><big>Therefore, though we miss the
                            departed righteous so much
                            here, hundreds may feel their touch there. One day, those hundreds will
                            thank the bereaved for gracefully forgoing the extended association
                            with
                            choice individuals here, in order that they could help hundreds there.
                            In God's ecology, talent and love are never wasted. The hopeful
                            understand
                            this, too. (<i>Notwithstanding My Weakness</i>, p.55)</big></font></big><big>&nbsp;
                    </big></div>
