                            that hour. Very soon after,
                            the second man was taken sick, but not being able to exercise
                            sufficient
                            faith, Brother Roskelley did not go to him. By and by this man
                            recovered,
                            and on meeting Brother Roskelley, he said: "brother Maughan came to me
                            the other night and told me he was sent to call one man from the ward,"
                            and he named two men as had been done to Brother Roskelley. A few days
                            afterwards the third man was taken sick and died.</big></font> </big></p>
