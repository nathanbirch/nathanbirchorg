                <div style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>Sometimes
                            we fret with the
                            things that life brings. One
                            of our leaders said some while ago--he was failing in health,
                            obviously,
                            from the way he wrote--and he said, "It's well that we have headaches,
                            and backaches, and leg aches, and heartaches." It's well that we have
                            these
                            aches and pains to remind us that this isn't the stopping place, and
                            that
                            we won't cling too tenaciously to mortal life. (<i>The Teachings of
                                Harold
                                B. Lee,</i> p. 48)<br>
                            <br>
                        </big></font></big><big></big></div>
