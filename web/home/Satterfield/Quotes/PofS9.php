                                                    may seem very cruel to us without any understanding of all the
                                                    circumstances, to know that the Fall brought upon us all such awful
                                                    consequences, and made it possible for an infinite atonement to be
                                                    offered to amend the broken law. Adam and Eve did not come here in a
                                                    mortal state. They had to come in the manner in which they did and then
                                                    transgress the law. The transgression of that law, contrary to the view
                                                    of many, was not a sin. It was not a sin any more than the
                                                    transgression in the laboratory by a chemist in combining two
                                                    substances and creating another entirely different from the first. It
                                                    was not a sin to bring to pass mortality, a condition which was
                                                    essential to the eternal welfare of man. The Fall changed the nature of
                                                    Adam and Eve to fit them for the condition in which we now are. After
