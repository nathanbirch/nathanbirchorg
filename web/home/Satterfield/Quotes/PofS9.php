                        <div style="margin-left: 40px;"><big><big><span
                                        style="font-family: arial;">The
                                        Fall came by transgression of a law, but there was no sin connected
                                        with it. There is a difference between transgression and sin. Both
                                        always bring consequences. While it may not be a sin to step off a
                                        roof, in doing so one becomes subject to the law of gravity, and
                                        consequences will follow. <small>("The Great Plan of Happiness and
                                            Personal Revelation," </small></span><small><span
                                            style="font-style: italic; font-family: arial;">Things of the Soul</span><span
