                <p
                    style="text-align: center; font-style: italic; color: rgb(153, 0, 0);"><big><big><font
                            face="Arial" size="+1"><big><big>The Importance of Studying the Fall</big></big></font></big></big></p>
                <big><big><font face="Arial"><strong><span
                                style="color: rgb(0, 0, 153);">Ezra Taft Benson</span><small
                                style="font-weight: normal;"> <span style="color: rgb(102, 102, 102);">(1899-1994)
                                    President</span></small></strong></font></big></big>
                <p style="margin-left: 40px;"><big><big><font face="Arial">The Book of
                        Mormon Saints knew that the plan of redemption must start with the
                        account of the fall of Adam. In the words of Moroni, "By Adam
                        came the fall of man. And because of the fall of man came Jesus Christ,
                        . . . and because of Jesus Christ came the redemption of man"
                        (Mormon 9:12).</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Just as a
                        man does not really desire food until he is hungry, so he does not
                        desire the salvation of Christ until he knows why he needs Christ.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">No one
                        adequately and properly knows why he needs Christ until he understands
                        and accepts the doctrine of the Fall and its effect upon all
                        mankind. And no other book in the world explains this vital doctrine
                        nearly as well as the Book of Mormon.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Brethren and
                        sisters, we all need to take a careful inventory of our performance and
                        also the performance of those over whom we preside to be
                        sure that we are teaching the "great plan of the Eternal God" to the
                        Saints.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Are we
                        accepting and teaching what the revelations tell us about the Creation,
                        Adam and the fall of man, and redemption from that fall through
                        the atonement of Christ? Do we frequently review the crucial questions
                        which Alma asks the members of the Church in the fifth chapter of Alma
                        in the Book of Mormon?</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Do we
                        understand and are we effective in teaching and preaching the
                        Atonement? What personal meaning does the Lord's suffering in
                        Gethsemane and on Calvary have for each of us?</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">What does
                        redemption from the Fall mean to us? In the words of Alma, do we "sing
                        the song of redeeming love"? (Alma 5:26). <small>(<em>Ensign</em>, May
                            1987, 85)</small></font></big></big></p>
                <big><big><br wp="BR1">
                    </big></big><br>
                <p
                    style="text-align: center; font-style: italic; color: rgb(153, 0, 0);"><big><big><font
                            face="Arial" size="+1"><big><big>Aspects of the Story of the Fall are
                                Symbolic</big></big></font></big></big></p>
                <br>
                <big><big><font face="Arial"><strong><span
                                style="color: rgb(0, 0, 153);">Bruce R. McConkie</span> <small
                                style="font-weight: normal; color: rgb(102, 102, 102);">(1915-85)
                                Quorum of the Twelve Apostles</small><br>
                            <br>
                        </strong></font></big></big>
                <div style="margin-left: 40px;"><big><big><font face="Arial">As to the
                            Fall itself we are told that the Lord planted "the tree of knowledge of
                            good and evil" in the midst of the garden. (Moses 3:9.) To Adam
                            and Eve the command came: "Of every tree of the garden thou mayest
                            freely eat, But of the tree of knowledge of good and evil, thou shalt
                            not
                            eat of it, nevertheless, thou mayest choose for thyself, for it is
                            given unto thee; but, remember that I forbid it, for in the day thou
                            eatest thereof
                            thou shalt surely die." (Moses 3:16-17.) <em>Again the account is
                                speaking figuratively</em>. What is meant by partaking of the fruit of
                            the tree of the
                            knowledge of good and evil is that our first parents complied with
                            whatever laws were involved so that their bodies would change from
                            their state
                            of paradisiacal immortality to a state of natural mortality.<small><small>
                                    <big>("Christ
                                        and the Creation," <em>Ensign</em>, June 1982, p. 15; emphasis added)</big></small></small></font></big></big><br>
                </div>
                <big><big><br>
                        <br wp="BR2">
                    </big></big>
                <p
                    style="text-align: center; font-style: italic; color: rgb(153, 0, 0);"><big><big><font
                            face="Arial" size="+1"><big><big>The Condition of Adam and Eve in the
                                Garden</big></big></font></big></big></p>
                <br>
                <big><big><font face="Arial"><strong><span
                                style="color: rgb(0, 0, 153);">Joseph Fielding Smith</span> <small
                                style="font-weight: normal; color: rgb(102, 102, 102);">(1876-1972)
                                President</small></strong></font></big></big>
                <p style="margin-left: 40px;"><big><big><font face="Arial">We find Adam
                        in the Garden of Eden with the promise that he can live there, he can
                        stay there, he can enjoy himself as far as is possible under
                        the conditions, as long as he wants to, as long as he does not do
                        something he is told not to do, and that is to partake of the fruit of
                        the tree of
                        knowledge of good and evil. He was told that in the day that he should
                        eat of that fruit he should surely die.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">We find,
                        then, Adam's status before the fall was:</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">1. He was
                        not subject to death.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">2. He was in
                        the presence of God. He saw him just as you see your fathers; was in
                        his presence, and learned his language. Now if any of you
                        are professors from our schools of language, and have an idea that
                        language came as these theorists say, I am going to tell you that Adam
                        had
                        a perfect language, for he was taught the language of God. That was the
                        first language upon this earth. So much for those theories.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">3. He had no
                        posterity.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">4. He was
                        without knowledge of good and evil. He had knowledge, of course. He
                        could speak. He could converse. There were many things he
                        could be taught and was taught; but under the conditions in which he
                        was living at that time it was impossible for him to visualize or
                        understand
                        the power of good and evil. He did not know what pain was. He did not
                        know what sorrow was; and a thousand other things that have come to
                        us in this life that Adam did not know in the Garden of Eden and could
                        not understand and would not have known had he remained there. That
                        was his status before the fall. <small>(<em>Doctrines of Salvation</em>
                            3 Vols. Ed. Bruce R. McConkie [1954-56],
                            1:107-108)</small></font></big></big></p>
                <br>
                <big><big><font face="Arial"><strong><span
                                style="color: rgb(0, 0, 153);">Bruce R. McConkie</span> <small
                                style="font-weight: normal; color: rgb(102, 102, 102);">(1915-85)
                                Quorum of the Twelve Apostles</small></strong></font></big></big>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Thus we
                        learn that the initial creation was paradisiacal; death and mortality
                        had not yet entered the world. There was no mortal flesh upon the
                        earth for any form of life. The Creation was past, but mortality as we
                        know it lay ahead. All things had been created in a state of
                        paradisiacal
                        immortality. It was of this day that Lehi said: "And all things which
                        were created must have remained in the same state in which they were
                        after
                        they were created; and they must have remained forever, and had no
                        end." (2 Ne. 2:22.) If there is no death, all things of necessity must
                        continue to live everlastingly and without end.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Continuing
                        the divine commentary about the Creation, we read: "And I, the Lord
                        God, formed man from the dust of the ground, and breathed
                        into his nostrils the breath of life; and man became a living soul, the
                        first flesh upon the earth, the first man also; nevertheless, all
                        things were
                        before created; but spiritually were they created and made according to
                        my word." (Moses 3:7.) How filled with meaning are these words! The
                        physical body of Adam is made from the dust of this earth, the very
                        earth to which the Gods came down to form him. His "spirit" enters his
                        body,
                        as Abraham expresses it. (See Abr. 5:7.) Man becomes a living, immortal
                        soul; body and spirit are joined together. He has been created
                        "spiritually," as all things were because there is as yet no mortality.
                        Then comes the Fall; Adam falls; mortality and procreation and death
                        commence. Fallen man is mortal; he has mortal flesh; he is "the first
                        flesh upon the earth." And the effects of his fall pass upon all
                        created
                        things. They fall in that they too become mortal. Death enters the
                        world; mortality reigns; procreation commences; and the Lord's great
                        and
                        eternal purposes roll onward.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Thus, "all
                        things" were created as spirit entities in heaven; then "all things"
                        were created in a paradisiacal state upon the earth; that is,
                        "spiritually
                        were they created," for there was as yet no death. They had <em>spiritual
                            bodies </em>made of the elements of the earth as distinguished from
                        the <em>mortal
                            bodies </em>they would receive after the Fall when death would enter
                        the scheme of things. Natural bodies are subject to the natural death;
                        spiritual
                        bodies, being paradisiacal in nature, are not subject to death. Hence
                        the need for a fall and the mortality and death that grows out of it.</font></big></big></p>
                <div style="margin-left: 40px;"><big><big><font face="Arial">Thus, as
                            the interpolative exposition in the divine word explains, "I, the Lord
                            God, planted a garden eastward in Eden, and there I put the man
                            whom I had formed." (Moses 3:8.) Adam, our father, dwelt in the Garden
                            of Eden. He was the <em>first man of all men </em>in the day of his
                            creation, and
                            he became the <em>first flesh of all flesh </em>through the Fall.
                            Because of the Fall "all things" changed from their spiritual state to
                            a natural state. And
                            thus we read: "And out of the ground made I, the Lord God, to grow
                            every tree, <em>naturally, </em>that is pleasant to the sight of man;
                            and man could
                            behold it. And it became also a living soul. For it was <em>spiritual </em>in
                            the day that I created it." (Moses 3:9; italics added). <small>("Christ
                                and
                                the Creation,"
                                <em>Ensign</em>, June 1982, p. 14)</small></font></big></big><br>
                </div>
                <p><big><big><font face="Arial"><br>
                        </font></big></big></p>
                <p><big><big><font face="Arial"><strong><span
                                style="color: rgb(0, 0, 153);">Orson Pratt</span> </strong></font></big></big><big><big><font
                            face="Arial"><strong><small style="font-weight: normal;"><span
                                    style="color: rgb(102, 102, 102);">(1811-1881) Quorum of
                                    the Twelve Apostles</span><br>
                            </small></strong></font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">This tree,
                        of which they both ate, was called the tree of knowledge of good and
                        evil. Why was it thus termed? I will explain a mystery to you,
                        brethren, why this was called so. Adam and Eve, while in the garden of
                        Eden, had not the knowledge you and I have; it is true, they had a
                        degree of intelligence, but they had not the experience, they had not
                        the knowledge by experience, which you and I have: all they knew was
                        barely what they knew when they came there; they knew a commandment had
                        been given to them, and they had sufficient knowledge to name
                        the beasts of the field as they came up before them; but as for the
                        knowledge of good, they had not got it, because they never had anything
                        contrary to good placed before them.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">We will
                        bring up an example. For instance, suppose you had never tasted
                        anything that was sweet--never had the sensation of
                        sweetness--could you have any correct idea of the term sweetness? No.
                        On the other hand, how could you understand bitter if you never had
                        tasted bitterness? Could you define the term to them who had
                        experienced this sensation, or knew it? No. I will bring another
                        example. Take a
                        man who had been perfectly blind from his infancy, and never saw the
                        least gleam of light--could you describe colors to him? No. Would he
                        know anything about red, blue, violet, or yellow? No; you could not
                        describe it to him by any way you might undertake. But by some process
                        let
                        his eyes be opened, and let him gaze upon the sun beams that reflect
                        upon a watery cloud, producing the rainbow, where he would see a
                        variety of colors, he could then appreciate them for himself; but tell
                        him about colors when he is blind, he would not know them from a piece
                        of
                        earthenware. So with Adam previous to partaking of this fruit; good
                        could not be described to him, because he never had experienced the
                        opposite. As to undertaking to explain to him what evil was, you might
                        as well have undertaken to explain, to a being that never had, for one
                        moment, had his eyes closed to the light, what darkness is. The tree of
                        knowledge of good and evil was placed there that man might gain
                        certain information he never could have gained otherwise; by partaking
                        of the forbidden fruit he experienced misery, then he knew that he was
                        once happy, previously he could not comprehend what happiness meant,
                        what good was; but now he knows it by contrast, now he is filled with
                        sorrow and wretchedness, now he sees the difference between his former
                        and present condition, and if by any means he could be restored to
                        his first position, he would be prepared to realize it, like the man
                        that never had seen the light. Let the man to whom all the beauties of
                        light
                        have been displayed, and who has never been in darkness, be in a
                        moment, in the twinkling of an eye, deprived of his natural sight; what
                        a
                        change this would be to him; he never knew anything about darkness
                        before, he never understood the principle at all; it never entered the
                        catalogue of his ideas, until darkness came upon him, and his eye-sight
                        was destroyed: now he can comprehend that the medium he once
                        existed in was light. Now, says he, if I could only regain my sight, I
                        could appreciate it, for I understand the contrast; restore me back
                        again to
                        my sight, and let me enjoy the light I once had; let me gaze upon the
                        works of creation, let me look on the beauties thereof again, and I
                        will be
                        satisfied, and my joy will be full. It was so with Adam; let the way be
                        prepared for his redemption, and the redemption of his posterity, and
                        all
                        creation that groans in pain to be delivered--let them be restored back
                        again to what they lost through the fall, and they will be prepared to
                        appreciate it. <small>(<em>Journal of Discourses </em></small></font></big><big
                        style="font-family: arial;"><big><small><small>26 Vols. [1855-86]</small></small></big></big></big><big><big><font
                            face="Arial"><small><big><em style="font-family: arial;"></em></big>,
                            1:285)</small></font></big></big><br wp="BR1">
                </p>
