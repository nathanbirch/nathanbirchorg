                <p
                    style="text-align: center; color: rgb(153, 0, 0); font-style: italic;"><big><big><font
                            face="Arial" size="+1"><big><big>The Fall</big></big></font></big></big></p>
                <big><big><font face="Arial"><strong><span
                                style="color: rgb(0, 0, 153);">Bruce R. McConkie </span></strong></font></big></big><big><big><font
                            face="Arial"><strong><small
                                style="font-weight: normal; color: rgb(102, 102, 102);">(1915-85)
                                Quorum of the Twelve Apostles</small></strong></font></big></big><big><big><font
                            face="Arial"><strong></strong></font></big></big>
                <p style="margin-left: 40px;"><big><big><font face="Arial">We do not
                        know how the fall was accomplished any more than we know how the Lord
                        caused the earth to come into being and to spin through
                        the heavens in its paradisiacal state. We have been given only enough
                        information about the creation and the fall to enable us to understand
                        the
                        purposes of the Lord, to exercise faith in him, and to gain our
                        salvation.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">As to the
                        fall, the scriptures set forth that there were in the Garden of Eden
                        two trees. One was the tree of life, which figuratively refers to
                        eternal
                        life; the other was the tree of knowledge of good and evil, which
                        figuratively refers to how and why and in what manner mortality and all
                        that
                        appertains to it came into being. "Of every tree of the garden thou
                        mayest freely eat," the Lord told our first parents, "but of the tree
                        of the
                        knowledge of good and evil, thou shalt not eat of it, nevertheless,
                        thou mayest choose for thyself, for it is given unto thee; but,
                        remember that I
                        forbid it, for in the day thou eatest thereof thou shalt surely die."
                        (Moses 3:16-17.)</font></big></big></p>
                <div style="margin-left: 40px;"><big><big><font face="Arial">Eve
                            partook
                            without full understanding; Adam partook knowing that unless he did so
                            he and Eve could not have children and fulfill the
                            commandment they had received to multiply and replenish the earth.
                            After they had thus complied with whatever the law was that brought
                            mortality into being, the Lord said to Eve: "I will greatly multiply
                            thy sorrow and thy conception. In sorrow thou shalt bring
                            forth children, and thy desire shall be to thy husband, and he shall
                            rule over thee." To Adam the decree came: "Cursed shall be the
                            ground for thy sake; in sorrow shalt thou eat of it all the days of thy
                            life. Thorns also and thistles shall it bring forth to thee." Thus the
                            paradisiacal
                            earth was cursed; thus it fell; and thus it became as it now is.</font></big></big></div>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Adam was
                        then told that he would surely die, returning through death to the dust
                        whence his physical body had come. And then the Lord said to
                        his Only Begotten: "Behold, the man is become as one of us to know good
                        and evil; and now lest he put forth his hand and partake also of the
                        tree of life and eat and live forever [in his sins!], therefore I, the
                        Lord God, will send him forth from the Garden of Eden to till the
                        ground from
                        whence he was taken." (Moses 4:22-29.) Such is the ancient account of
                        the fall. <small>(<em>A New Witness for the Articles of Faith</em>
                            [1985], 85-86)</small></font></big></big><br wp="BR2">
                </p>
