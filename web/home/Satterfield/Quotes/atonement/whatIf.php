                            No Atonement?</em></big></font></big><br>
                </p>
                <p style="font-family: arial;"><big><strong>Richard G. Scott</strong></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>I testify that
                    except for the Atonement of the Holy Redeemer, the demands of justice
                    would prevent every soul born on earth from returning to the presence
                    of
                    God, to partake of His glory and exaltation, for all make mistakes for
                    which we cannot personally appease justice. I witness that except for
                    the "infinite
                    atonement" of Christ, we could not return to God at death and, as Jacob
                    solemnly warned, "our spirits [would] become subject to &#8230; the devil, to
                    rise no more.
                    And our spirits [would] become like unto him, and we [would] become
                    devils, angels to a devil, to be shut out from the presence of our God,
                    &#8230; to remain with
                    the father of lies, in misery" [2 Ne. 9:7-9]. ("Jesus Christ, Our
                    Redeemer," <em>Ensign</em>, May 1997, p. 53)<br>
                    <br style="font-family: arial;" wp="BR2">
                </big></p>
                <p style="font-family: arial;"><big><strong>Bruce R. McConkie</strong></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>[<em>In this
                        quotation, Elder McConkie reviews the scriptures regarding the
                        question, "What if there were not Atonement?"</em>] If there were no
                    creation, we would
                    not be, neither the earth, nor any life thereon. All things, in effect,
                    would vanish away. And if there were no atonement, the purposes of
                    creation would be
                    frustrated; man would remain lost and fallen forever; there would be no
                    resurrection nor eternal life; Adam and all his posterity would be as
                    Lucifer, cast out,
                    damned, without hope, lost forever.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>The Book of
                    Mormon prophets have made these things exceedingly clear. In plain
                    words, as they proclaimed the infinite glories of the atonement, they
                    have
                    affirmed such things as: "Were it not for the atonement, which God
                    himself shall make for the sins and iniquities of his people, they must
                    unavoidably perish.
                    For . . . there could not any man be saved except it were through the
                    redemption of God." (Mosiah 13:28, 32; 15:19; 16:4; Jacob 7:12.) "There
                    could be no
                    redemption for mankind save it were through the death and sufferings of
                    Christ, and the atonement of his blood." (Alma 21:9.)</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>Indeed, it is
                    Nephi's brother Jacob to whom we turn for what is probably the clearest
                    explanation found in any scripture now extant for the doctrinal
                    explanation
                    as to why all men would be lost if there were no atonement. "Our flesh
                    must waste away and die," he says, which fact is one of the truisms of
                    life. Then he puts
                    death in its true perspective in the eternal plan with this
                    explanation: "For as death hath passed upon all men, to fulfil the
                    merciful plan of the great Creator,
                    there must needs be a power of resurrection, and the resurrection must
                    needs come unto man by reason of the fall; and the fall came by reason
                    of transgression;
                    and because man became fallen they were cut off from the presence of
                    the Lord." (2 Ne. 9:4-6.) Thus, Adam accomplished his mission to fall
                    and create the
                    need for a Redeemer.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>What then of
                    redemption, of the promised deliverance, of the expiatory sacrifice of
                    Him who did no sin, "who layeth down his life according to the flesh,
                    and
                    taketh it again by the power of the Spirit, that he may bring to pass
                    the resurrection of the dead"? (2 Ne. 2:8.) Of his sinless sacrifice
                    Jacob said: "It must needs
                    be an infinite atonement--save it should be an infinite atonement this
                    corruption could not put on incorruption." (2 Ne. 9:7.) Save for the
                    infinite power of this,
                    the most selfless act ever performed, Paul would never have been able
                    to write of man's body: "It is sown in corruption; it is raised in
                    incorruption: It is sown in
                    dishonour; it is raised in glory; it is sown in weakness; it is raised
                    in power: It is sown a natural body; it is raised a spiritual body." (1
                    Cor. 15:42-44.)</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>Save for this
                    atonement, Jacob continues, "the first judgment which came upon man" -
                    his banishment from the presence of the Lord because he transgressed
                    the law and partook of the forbidden fruit, and also the natural death
                    that attends his newly found mortal state - "must needs have remained
                    to an endless
                    duration. And if so, this flesh must have laid down to rot and to
                    crumble to its mother earth, to rise no more." (2 Ne. 9:7.) There would
                    have been no
                    resurrection, no immortality, no reunion of body and spirit, no victory
                    over the grave - nothing but endless death.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>"But behold, all
                    things have been done in the wisdom of him who knoweth all things." (2
                    Ne. 2:24.) The purposes of the Almighty neither have been nor can be
                    frustrated. The fall was part of his plan; he designed and decreed it
                    from the beginning. Its gloom is to turn into joy and gladness as both
                    temporal and spiritual
                    death are abolished in Gethsemane and on Calvary. And so Jacob
                    exclaims: "O the wisdom of God, his mercy and grace! For behold, if the
                    flesh should rise no
                    more our spirits must become subject to that angel who fell from before
                    the presence of the Eternal God, and became the devil, to rise no
                    more." (2 Ne. 9:8.)
                    Subject to whom? To Lucifer, the traitor and rebel who defied Deity and
                    spread the woes of war in the heavenly courts. Christ is now our King
                    and we worship
                    him because we will it so. Had there been no atonement Lucifer would
                    have been our eternal head and we would have worshipped him because he
                    willed it so.
                    Agency and freedom would have ceased for all those whom God had sired.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>But this is not
                    all. Jacob continues: "And our spirits must have become like unto him,
                    and we become devils, angels to a devil, to be shut out from the
                    presence
                    of our God, and to remain with the father of lies, in misery, like unto
                    himself." (2 Ne. 9:9.) Devils! Angels to a devil! Damned souls, denied
                    a grave, denied a
                    resurrection, purposeless creatures in whose souls the light we once
                    had would become darkness!</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>"God himself
                    shall come down among the children of men, and shall redeem his
                    people." (Mosiah 15:1.) He shall redeem them from that (otherwise)
                    everlasting death which is the grave, and that (otherwise) everlasting
                    death which is eternal, abysmal darkness where none of the light of
                    heaven is found, and
                    where they would have no choice but to grovel before the Angel of
                    Darkness. Ought we not, then, as did our friend Jacob, extol our
                    Redeemer and Savior in
                    such words of doctrine and beauty as these:</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>"O how great the
                    goodness of our God, who prepareth a way for our escape from the grasp
                    of this awful monster; yea, that monster, death and hell, which I call
                    the death of the body, and also the death of the spirit.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>"And because of
                    the way of deliverance of our God, the Holy One of Israel, this death,
                    of which I have spoken, which is the temporal, shall deliver up its
                    dead;
                    which death is the grave.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>"And this death
                    of which I have spoken, which is the spiritual death, shall deliver up
                    its dead; which spiritual death is hell; wherefore, death and hell must
                    deliver up their dead, and hell must deliver up its captive spirits,
                    and the grave must deliver up its captive bodies, and the bodies and
                    the spirits of men will be
                    restored one to the other; and it is by the power of the resurrection
                    of the Holy One of Israel.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>"O how great the
                    plan of our God! For on the other hand, the paradise of God must
                    deliver up the spirits of the righteous, and the grave deliver up the
                    body of
                    the righteous; and the spirit and the body is restored to itself again,
                    and all men become incorruptible, and immortal, and they are living
                    souls, having a perfect
                    knowledge like unto us in the flesh, save it be that our knowledge
                    shall be perfect.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>"O the greatness
                    of the mercy of our God, the Holy One of Israel! For he delivereth his
                    saints from that awful monster the devil, and death, and hell, and that
                    lake of fire and brimstone, which is endless torment." (2 Ne. 9:10-13,
                    19) [From <em>The Promised Messiah</em>, pp. 227-230]</big><br
