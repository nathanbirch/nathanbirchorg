                <p style="font-family: arial; margin-left: 40px;"><big>Now, we often
                    make some common mistakes when applying the Atonement in our lives. I
                    will mention several. God leaves us free to make these very mistakes.
                    Yet each of these mistakes reflects a greater need for our personal
                    submissiveness.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>First, we make a
                    mistake when we think we own ourselves and that we own blocks of time.
                    It's a natural thing to do. But our faith in God includes faith in
                    God's timing, enough to be able to say, in effect, "Thy <em>timing</em>
                    be done" (see D&amp;C 64:32).</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>We make another
                    mistake when we fail to realize how much serious discipleship consists
                    of downsizing our egos and diminishing our selfishness! The bloated
                    natural man will resist any downsizing. But meekness is what has to
                    happen.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>Another mistake
                    we can make is that we maybe don't take life's little quizzes seriously
                    enough. We think we will cram and pass the final exam! The infinite
                    Atonement is there for our finite mistakes, including the daily
                    mistakes we tend to make. It is understandable, of course, that we
                    focus on the crunch times in
                    our lives. We are mindful of these spikes of suffering and stress, and
                    we sometimes, ironically, let ourselves become overcome by relentless
                    routineness-with
                    what might be called the daily dampening of things spiritual.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>We make another
                    mistake. We fail to focus on and to develop <em>patience</em> as well
                    as <em>faith</em> (see Mosiah 23:21). These two attributes are in
                    tandem; they go together.
                    By the way, if we are meek, being <em>tried</em> means being <em>developed.</em>
                    We don't look at impatience in terms of its downside. When we are
                    impatient, in effect, we do
                    not honor what is implied in the words "in process of time." We
                    foolishly wish to have some of life's moments over and done with,
                    seasons to be over with,
                    ignoring the possibilities for service that are inherent in them. We
                    are somewhat like airline passengers in America who fly coast to coast
                    and resent the space in
                    between; but there are <em>souls</em> down there, not just <em>sagebrush!</em>
                    Yet we want to fly over some experiences. It is so likely we will miss
                    the chance to be of service.
                    So it is with life's seeming in-between and routine spaces! Impatience
                    puts us at risk.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>We may feel put
                    upon by events and circumstances-another mistake we can make when not
                    approaching the Atonement properly. Yet many of these things that
                    we feel put upon by actually constitute the customized curricula needed
                    for our personal development. Still, our tendency is to push away the
                    necessary and
                    prescribed courses of spiritual calisthenics. We can't withdraw from
                    all of life's courses and still really stay enrolled in school!</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>Another mistake
                    we make is that we foolishly think we are free to choose, without
                    wanting the consequences of those choices! (see Alma 29:4). Bainville,
                    the
                    French philosopher, said, "One must want the consequences of what one
                    wants."</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>Another mistake
                    commonly made is that we play to the mortal galleries! We listen too
                    much to the wrong peers. There is what I call the mystic "they," who
                    for
                    some become ascendant. In terms of the choices they make, they want to
                    please the mystic "they." We see this politically, in the corporate
                    world, in academic
                    life, and so on.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>Some people are
                    so anxious to be politically correct and to conform to the fashions of
                    the world! What is worse, however, is when we see members of the
                    Church try to conform eternal truths to the ways and thinking of the
                    world. But it won't work! As Paul warned, "The natural man receiveth
                    not the things of the
                    Spirit of God: for they are foolishness unto him: neither can he know
                    them, because they are spiritually discerned" (1 Cor. 2:14). It is a
                    terrible mistake for us to
                    try to please the world by twisting and conforming things that won't
                    fit in the secular matrix.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>Now, these
                    mistakes are but a few we make; they keep us from fully applying the
                    Atonement to our lives. They are not worthy of Jesus and what He
                    accomplished in Gethsemane and on Calvary! ("The Holy Ghost: Glorifying
                    Christ," <em>Ensign,</em> July 2002, pp.56-57)<br>
                </big></p>

