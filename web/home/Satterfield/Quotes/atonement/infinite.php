                <p style="font-family: arial; margin-left: 120px;"><big>The Book of
                    Mormon teaches us of an infinite atonement (see 2 Ne. 9:7; 2 Ne. 25:16;
                    Alma 34:10, 12, 14), an atoning sacrifice by Christ that is unbounded
                    by
                    time, ethnicity, geography, or even kinds of sins, save for the
                    unpardonable sin of denying the Holy Ghost (see Alma 39:6). The
                    Resurrection includes all
                    people "from the days of Adam down" to the end of time (Alma 40:18),
                    those "both old and young, both bond and free, both male and female"
                    (Alma 11:44).
                    The Atonement is also infinite in the sense that the Savior not only
                    overcame death and sin, but he also took upon himself "the pains and
                    the sicknesses" and
                    the "infirmities" of his people (Alma 7:11-12). The Atonement is
                    infinite, too, in that because of the redemption made possible by his
                    beloved Son, our
                    Heavenly Father is able to forgive us "as often as [we] repent" (Mosiah
                    26:30-31; see also Moro. 6:8). ["The Fall and Infinite Atonement," <em>Ensign</em>,
                    Jan. 1996,
                    p. 26]</big><br style="font-family: arial;" wp="BR2">
                </p>
                <p style="font-family: arial; color: rgb(153, 0, 0);"><big
                    style="color: rgb(102, 51, 51);"><font size="+1"><big><em>Summary</em></big></font></big><br
                    style="font-family: arial;" wp="BR2">
                </p>
                <p style="font-family: arial; margin-left: 40px;"><big><strong>Neal A.
                        Maxwell</strong></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Both Jacob and
                    Amulek declared it to be an infinite atonement (2 Nephi 9:7; Alma
                    34:8-12). <strong>First</strong>, the sacrifice of an imperfect and
                    finite human would not
                    have satisfied the requirements of divine justice. A mere mortal could
                    not have performed the Atonement. Divine justice thus required that, if
                    the repentant
                    were not to have to pay for their own sins, the Only Begotten of the
                    Father should lay down His life voluntarily for that purpose ... "For
                    it is expedient that there
                    should be a great and last sacrifice; yea, not a sacrifice of man,
                    neither of beast, neither of any manner of fowl; for it shall not be a
                    human sacrifice; but it must
                    be an infinite and eternal sacrifice. Now there is not any man that can
                    sacrifice his own blood which will atone for the sins of another" (Alma
                    34:10-11).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Why so great a
                    sacrifice? Because all of us mortals need it, along with time and
                    space-- room to repent and grow, so that one day, if submissive, we can
                    be
                    worthy to return to the presence of God: "For behold, if Adam had put
                    forth his hand immediately, and partaken of the tree of life, he would
                    have lived forever,
                    according to the word of God, having <em>no space for repentance</em>;
                    yea, and also the word of God would have been void, and the great plan
                    of salvation would have
                    been frustrated" (Alma 42:5, italics added).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Furthermore,
                    only in an "infinite atonement," would mercy overpower the stern
                    demands of justice (see Alma 34:15). The mercy of God responds to our
                    helpless condition: "Since man had fallen he could not merit anything
                    of himself; but the sufferings and death of Christ atone for their
                    sins, through faith and
                    repentance" (Alma 22:14).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><strong>Second</strong>,
                    the infinite atonement is fully comprehensive in the immortalizing
                    benefits it provides to all of God's children by the grace of God (see
                    Alma
                    11:40-44). "The atonement which is infinite for all mankind" (2 Nephi
                    25:16) provides infinite benefits "according to the great plan of the
                    Eternal God" who
                    foresaw the Fall and who mercifully decreed "there must be an atonement
                    made" (Alma 34:9).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>A <strong>third</strong>
                    dimension may be seen in the infinite intensiveness of Christ's
                    suffering. This intensiveness required a fully atoning and fully
                    comprehending Atoner
                    who would "know according to the flesh" human pain, sorrow, grief, and
                    misery (Alma 7:11-12). "For behold, he suffereth the pains of all men,
                    yea, the pains
                    of every living creature, both men, women, and children, who belong to
                    the family of Adam" (2 Nephi 9:21).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big> Thus, in
                    addition to bearing our sins--the required essence of the
                    Atonement--the "how" of which we surely do not understand, Jesus is
                    further described as
                    having come to know our sicknesses, griefs, pains, and infirmities as
                    well. Another "how" we cannot now comprehend! (See Isaiah 53:4; Matthew
                    8:17;
                    Mosiah 14:4; Alma 7:11-12.) Jesus thus not only satisfied the
                    requirements of divine justice but also, particularly in His Gethsemane
                    and Calvary ordeals,
                    demonstrated and perfected His capacity to succor His people and his
                    empathy for them. He came to know, personally and perfectly, "according
                    to the flesh"
                    how to help us become more like His fully comprehending Father: "Great
                    is our Lord, and of great power: his understanding is infinite" (Psalm
                    147:5).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Jesus' daily
                    mortal experiences and His ministry, to be sure, acquainted Him by
                    observation with a sample of human sicknesses, grief, pains, sorrows,
                    and
                    infirmities which are "common to man' (1 Corinthians 10:13). But the
                    agonies of the Atonement were infinite and first-hand! Since not all
                    human sorrow and
                    pain is connected to sin, the full intensiveness of the Atonement
                    involved bearing our pains, infirmities, and sicknesses, as well as our
                    sins. Whatever our
                    sufferings, we can safely cast our care upon him; for he careth for
                    [us]" (1 Peter 5:7).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Jesus is a fully
                    comprehending Christ.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>The Atonement,
                    then, was infinite in the divineness of the one sacrificed, in the
                    comprehensiveness of its coverage, and in the intensiveness -
                    incomprehensible
                    to us--of the Savior's suffering. (<em>Not My Will, But Thine</em>,
                    pp.49-51; bolding not original)</big></p>

