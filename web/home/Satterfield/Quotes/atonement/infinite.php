                <p style="font-family: arial; margin-left: 120px;"><big>In preparatory
                    times of the Old Testament, the practice of atonement was
                    finite--meaning it had an end. It was a symbolic forecast of the
                    definitive Atonement
                    of Jesus the Christ. His Atonement is infinite--without an end [see 2
                    Ne. 9:7; 2 Ne. 25:16; Alma 34:10, 12, 14]. It was also infinite in that
                    all humankind
                    would be saved from never-ending death. It was infinite in terms of His
                    immense suffering. It was infinite in time, putting an end to the
                    preceding prototype of
                    animal sacrifice. It was infinite in scope - it was to be done once for
                    all [see D&amp;C 76:24; Moses 1:33]. And the mercy of the Atonement
                    extends not only to an
                    infinite number of people, but also to an infinite number of worlds
                    created by Him. It was infinite beyond any human scale of measurement
                    or mortal
                    comprehension.</big></p>
                <p style="font-family: arial; margin-left: 120px;"><big>Jesus was the
                    only one who could offer such an infinite atonement, since He was born
                    of a mortal mother and an immortal Father. Because of that unique
                    birthright, Jesus was an infinite Being. ("The Atonement," <em>Ensign</em>,
                    Nov. 1996, p. 35)</big><br style="font-family: arial;" wp="BR2">
                </p>
