                <p style="font-family: arial; color: rgb(153, 0, 0);"><big><font
                        size="+1"><big><em>Mercy--the Counterbalance to Justice&nbsp; </em></big></font></big></p>
                <p style="font-family: arial;"><big><em></em><em>(Two Discussions
                        Concerning Justice, Mercy, and the Atonement)</em></big><br
                    style="font-family: arial;" wp="BR2">
                </p>
                <p style="font-family: arial; margin-left: 40px;"><big><strong>Dallin
                        H. Oaks</strong></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><em>Justice</em>
                    has many meanings. One is balance. A popular symbol of justice is
                    scales in balance. Thus, when the laws of man have been violated,
                    justice usually
                    requires that a punishment be imposed, a penalty that will restore the
                    balance.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>People generally
                    feel that justice has been done when an offender receives what he
                    deserves-when the punishment fits the crime. The Church's declaration
                    of
                    belief state that "the commission of crime should be punished [under
                    the laws of man] according to the nature of the offense" (D&amp;C
                    134:8). The paramount
                    concern of human law is justice.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Unlike the
                    changeable laws of man, the laws of God are fixed and permanent,
                    "irrevocably decreed in heaven before the foundations of this world"
                    (D&amp;C
                    130:20). These laws of God are likewise concerned with justice. The
                    idea of justice as what one deserves is the fundamental premise of all
                    scriptures that
                    speak of men's being judged according to their works. Alma declared
                    that it was "requisite with the justice of God that men should be
                    judged according to their
                    works" (Alma 41:3). The Savior told the Nephites that all men would
                    stand before Him to be "judged of their works, whether they be good or
                    whether they be
                    evil" (3 Nephi 27:14). In his letter to the Romans, Paul described "the
                    righteous judgment of God" in terms of "render[ing] to every man
                    according to his
                    deeds" (Romans 2:5-6). </big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>According to
                    eternal law, the consequences that follow from the justice of God are
                    severe and permanent. When a commandment is broken, a commensurate
                    penalty is imposed. This happens automatically. Punishments prescribed
                    by the laws of man only follow the judge's action, but under the laws
                    of God the
                    consequences and penalties of sin are inherent in the act. "There is a
                    law given, and a punishment affixed," the prophet Alma taught, and
                    "justice claimeth the
                    creature and executeth the law, and the law inflicteth the punishment."
                    Alma explained, "And thus we see that all mankind were fallen, and they
                    were in the
                    grasp of justice; yea, the justice of God, which consigned them forever
                    to be cut off from his presence" (Alma 42:22, 14). Abinadi added that
                    the Lord "cannot
                    deny justice when it has its claim" (Mosiah 15:27). By itself, justice
                    is uncompromising. </big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>The justice of
                    God holds each of us responsible for our own transgressions and
                    automatically imposes the penalty. This reality should permeate our
                    understanding, and it should influence all our teachings about the
                    commandments of God and the effect of individual transgressions. </big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>In keeping with
                    the legal traditions of man, many seem to want justice. It is true that
                    justice is a friend that will protect us from persecution by the
                    enemies of
                    righteousness. But justice will also see that we receive what we
                    deserve, and this is an outcome I fear. I cannot achieve my eternal
                    goals on the basis of what I
                    deserve. Though I try with all my might, I am still what King Benjamin
                    called an "unprofitable servant" (Mosiah 2:21). To achieve my eternal
                    goals, I need
                    more than I deserve. I need more than justice.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>This realization
                    reminds me of an event that occurred in the law firm where I began
                    practicing law more than forty years ago. A Chicago politician had been
                    indicted for stuffing ballot boxes. A partner in our firm told me how
                    this politician came to his office to ask us to represent him in his
                    criminal trial.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"What can you do
                    for me?" he asked. Our partner replied that if this client retained our
                    firm to conduct his defense, we would investigate the facts, research
                    the
                    law, and present the defense at the trial. "In this way," the lawyer
                    concluded, "we will get you a fair trial."</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>The politician
                    promptly stood up, put on his hat, and stalked out of the office.
                    Pursuing him down the hall, the lawyer asked what he had said to offend
                    him. "Nothing," the politician replied. "Then why are you leaving?" the
                    lawyer asked. "The odds aren't good enough," the politician answered.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>The man would
                    not retain our firm to represent him in court because we would only
                    promise him a fair trial, and he knew he needed more than that. He knew
                    he was guilty, and he could only be saved from prison by something more
                    favorable to him than justice.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Can justice save
                    us? Can man in and of himself overcome the spiritual death all mankind
                    suffers from the Fall, which we bring upon ourselves anew by our
                    own sinful acts? No! Can we "work out our own salvation"? Never! "By
                    the law no flesh is justified," Lehi explained (2 Nephi 2:5).
                    "Salvation doth not
                    come by the law alone,"Abinadi warned (Mosiah 13:28). Shakespeare had
                    one of his characters declare this truth: "In the course of justice,
                    none of us should
                    see salvation: we do pray for mercy" (<em>The Merchant of Venice</em>,
                    act 4, scene 1, lines 196-197).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>We know from
                    numerous scriptures that "no unclean thing" can enter "the kingdom of
                    God" (Moses 6:57; 1 Nephi 10:21; Alma 40:26). If we are to return to
                    the presence of our Heavenly Father, we need the intervention of some
                    powerful influence that transcends justice. That powerful intervention
                    is the atonement
                    of Jesus Christ.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>The good news of
                    the gospel is that because of the atonement of Jesus Christ there is
                    something called <em>mercy</em>. <em>Mercy</em> signifies an
                    advantage greater than what
                    we deserve. This could come by the withholding of a deserved punishment
                    or by the granting of an undeserved benefit.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>If justice is
                    balance, then mercy is counterbalance. If justice is exactly what we
                    deserve, then mercy is <em>more</em> benefit than we deserve. In its
                    relationship to
                    justice and mercy, the Atonement is the means by which justice is
                    served and mercy is extended. In combination, justice and mercy and the
                    Atonement
                    constitute the glorious eternal wholeness of the justice and mercy of
                    God.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Mercy has
                    several different manifestations in connection with our redemption. The
                    universal resurrection from physical death is an unconditional act of
                    mercy
                    made possible by the Atonement. Alma taught Corianton that "mercy
                    cometh because of the atonement; and the atonement bringeth to pass the
                    resurrection of
                    the dead" (Alma 42:23).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>A second effect
                    of the Atonement concerns our redemption from spiritual death. We are
                    redeemed from the fall of Adam without condition. We are redeemed
                    from the effects of our personal sins on condition of our obedience to
                    the laws and ordinances of the gospel.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Justice is
                    served and mercy is extended by the suffering and shed blood of Jesus
                    Christ. The Messiah "offereth himself a sacrifice for sin, to answer
                    the ends of
                    the law" (2 Nephi 2:7; Romans 5:18-19). In this way, "God himself
                    atoneth for the sins of the world, to bring about the plan of mercy, to
                    appease the demands
                    of justice, that God might be a perfect, just God, and a merciful God
                    also" (Alma 42:15).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>We are all
                    dependent upon the mercy of God the Father extended to all mankind
                    through the atoning sacrifice of our Lord and Savior, Jesus Christ.
                    This is the
                    central reality of the gospel. This is why we "talk of Christ, we
                    rejoice in Christ, we preach of Christ . . . that our children may know
                    to what source they may
                    look for a remission of their sins" (2 Nephi 25:26).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>The reality of
                    our total dependence upon Jesus Christ for the attainment of our goals
                    of immortality and eternal life should dominate every teaching and
                    every
                    testimony and every action of every soul touched by the light of the
                    restored gospel. If we teach every other subject and principle with
                    perfection and fall short
                    on this one, we have failed in our most important mission. (From "Sins,
                    Crimes, and Atonement," in <em>With Full Purpose of Heart</em> [Salt
                    Lake City: Deseret
                    Book, 2002], pp. 113-131)<br>
                    <br style="font-family: arial;" wp="BR2">
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><strong>Jeffrey
                        R. Holland</strong></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>This loving,
                    charitable, and merciful generosity of the Savior raises the inevitable
                    question of the place of justice in his working out of the Atonement.
                    The
                    balance between seemingly contradictory principles is examined in the
                    Book of Mormon most skillfully and--because it is a father speaking to
                    his own
                    transgressing son--most sensitively by Alma the Younger when
                    instructing his son Corianton.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Obviously the
                    demands of justice require that penalties must be paid for violation of
                    the law. Adam transgressed and so have all of us; thus the judgment of
                    death (physically) and the consequences of hell (spiritually) is
                    pronounced as a just reward. Furthermore, once guilty, none of us could
                    personally do anything to
                    overcome that fate. We do not have in us the seeds of immortality
                    allowing us to conquer death physically, and we have not been perfect
                    in our behavior, thus
                    forfeiting the purity that would let us return to the presence of God
                    spiritually. Furthermore, God cannot simply turn a blind eye to the
                    breaking of divine law,
                    because in so doing he would dishonor justice and would "cease to be
                    God,"(see Alma 42:13, 22, 25) which thing he would never do. The sorry
                    truth for mortal
                    men and women was, then, that "there was no means to reclaim [them]
                    from this fallen state which man had brought upon himself because of
                    his own
                    disobedience.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"Thus we see
                    that all mankind were fallen, and they were in the grasp of justice;
                    yea, the justice of God, which consigned them forever to be cut off
                    from his
                    presence" (Alma 42:14; see also v. 12).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>The claims of
                    justice must be honored. The absence of law or the lack of any penalty
                    for breaking it would leave the world in amoral chaos. Alma asked
                    rhetorically, "If there was no law given--if a man murdered he should
                    die--would he be afraid he would die if he should murder? And also, if
                    there was no law
                    given against sin men would not be afraid to sin" (Alma 42:19-20).
                    Clearly one of the purposes of law and the firm demands of justice
                    behind it is its
                    preventive impact.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>But what happens
                    when <em>all</em> have sinned and come short of the glory of God? How
                    do we overcome the infinite impact of Adam's transgression? How is the
                    price to be paid for every sin and sorrow and selfish thought that men
                    and women have experienced from Adam to the end of the world? The
                    number of
                    transgressors is as vast as the punishment is ominous. "Do ye suppose
                    that mercy can rob justice?" Alma asked. "I say unto you, Nay; not one
                    whit" (Alma
                    32:25).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>King Benjamin
                    had earlier reminded his people: "After ye have known and have been
                    taught all these things, if ye should transgress and go contrary to
                    that
                    which has been spoken, that ye do withdraw yourselves from the Spirit
                    of the Lord, that it may have no place in you to guide you in wisdom's
                    paths that ye may
                    be blessed, prospered, and preserved--</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"I say unto you,
                    that the man that doeth this, the same cometh out in open rebellion
                    against God. . . . If that man repenteth not, and remaineth and dieth
                    an
                    enemy to God, the demands of divine justice do awaken his immortal soul
                    to a lively sense of his own guilt, which doth cause him to shrink from
                    the presence
                    of the Lord, and doth fill his breast with guilt, and pain, and
                    anguish, which is like an unquenchable fire, whose flame ascendeth up
                    forever and ever.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"And now I say
                    unto you, that mercy hath no claim on that man; therefore his final
                    doom is to endure a never-ending torment" (Mosiah 2:36-39).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Usually when we
                    speak of the atonement of Christ, we emphasize (or find ourselves
                    hoping for) the merciful aspects of that gift. But we must always
                    remember
                    that the Atonement carries with it the unfailing elements of justice as
                    well. Justice <em>is</em> the gulf separating the wicked from the
                    righteous, and "justice cannot be
                    denied" (Jacob 6:10; see also 1 Nephi 2:18). Christ did all that he did
                    so that "a righteous judgment might come upon the children of men." It
                    is a <em>righteous</em>
                    judgment, but it <em>is</em> a judgment.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>To the resistant
                    congregation at Ammonihah, Amulek powerfully taught the justice of the
                    Resurrection--that "the wicked remain as though there had been no
                    redemption made, except it be the loosing of the bands of death," and
                    they shall be "arraigned before the bar of Christ the Son, and God the
                    Father, and the
                    Holy Spirit, which is one Eternal God, to be judged according to their
                    works, whether they be good or whether they be evil" (Mosiah 3:10; Alma
                    11:41, 44). Upon hearing that doctrine, Zeezrom, the chief antagonist
                    in that setting, "began to tremble under a consciousness of his guilt."
                    When Alma stepped in to
                    reinforce the words of Amulek on this doctrine, Zeezrom "began to
                    tremble more exceedingly, for he was convinced more and more of the
                    [justice] of God"
                    (Alma 12:1,7). </big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>An earlier
                    prophet also thought someone in his audience should be alarmed at the
                    justice of God. Abinadi asked King Noah (who displayed much the same
                    spirit Zeezrom had initially shown), "Ought ye not to tremble? For
                    salvation cometh to none such; for the Lord hath redeemed none such;
                    yea, neither can the
                    Lord redeem such; for he cannot deny himself; for he cannot deny
                    justice when it has its claim" (Mosiah 15:27). </big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>It is Book of
                    Mormon doctrine that God must be just (see Alma 42:13, 22, 25).
                    Furthermore, the guilty will, even in everlasting shame, acknowledge
                    that "all
                    his judgments are just; that he is just in all his works" (Alma 12:15).
                    But God is a merciful God also, and so is the Son of God. Therefore,
                    God devised a "plan
                    of mercy" (Alma 42:15) that would satisfy the demands of justice and
                    free those held hostage to sin. It would require that a God himself
                    come to earth to atone
                    for the sins of the world, a point that Abinadi made not once but three
                    times in declaring the Atonement to King Noah and his wicked priests
                    (see Mosiah
                    13:28, 34; 15:1).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>No mere mortal
                    could work such a miracle or bear such a weighty load. As Amulek
                    taught, "There is not any man that can sacrifice his own blood which
                    will
                    atone for the sins of another" (Alma 34:11). In such an earthly
                    accounting, any mortals, however good they might wish to be, would have
                    their own sins to
                    account for, so they would hardly be in a position to take care of
                    another's. And regarding death, no mortals, however strong they might
                    be otherwise, carry in
                    themselves the seeds of life whereby they can raise themselves--let
                    alone others--from the tomb.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>No, only a God
                    (the Son) could meet these demands and thereby help another God (the
                    Father) "be a perfect, just God, and a merciful God also" (Alma 42:15).
                    Only a God himself would bear the seeds of divinity, the seeds of
                    eternal life, that would allow a triumph over death. And only a person
                    qualifying to be a God
                    could live in a world of temptation and be subject to all the ills of
                    the flesh but never yield to them.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>So Christ came
                    to earth, lived his thirty-three years, then fulfilled the ultimate
                    purpose for his birth into mortality. In a spiritual agony that began
                    in Gethsemane
                    and a physical payment that was consummated on the cross of Calvary, he
                    took upon himself every sin and sorrow, every heartache and infirmity,
                    every
                    sickness, sadness, trial, and tribulation experienced by the children
                    of God from Adam to the end of the world. How he did that is a stunning
                    mystery, but he did
                    it. He broke the bands of physical death and gained victory over the
                    grasp of spiritual hell. A God himself came down and made merciful
                    intercession for all the
                    children of men (see Mosiah 15:8).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Through his
                    personal experience Alma came to appreciate that only with such mercy
                    included could the great plan of redemption function. As he later
                    taught
                    his son Corianton, "According to justice, the plan of redemption could
                    not be brought about, [except] on conditions of repentance of men in
                    this probationary
                    state, yea, this preparatory state; for except it were for these
                    conditions, mercy could not take effect except it should destroy the
                    work of justice. Now the work
                    of justice could not be destroyed; if so, God would cease to be God.
                    And thus we see that all mankind were fallen, and they were in the
                    grasp of justice; yea, the
                    justice of God, which consigned them forever to be cut off from his
                    presence. . . . </big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"But there is a
                    law given, and a punishment affixed, and a repentance granted; which
                    repentance, mercy claimeth; otherwise, justice claimeth the creature
                    and
                    executeth the law, and the law inflicteth the punishment; if not so,
                    the works of justice would be destroyed, and God would cease to be God.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"But God ceaseth
                    not to be God, and mercy claimeth the penitent, and mercy cometh
                    because of the atonement; and the atonement bringeth to pass the
                    resurrection of the dead; and the resurrection of the dead bringeth
                    back men into the presence of God; and thus they are restored into his
                    presence, to be judged
                    according to their works, according to the law and justice.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"For behold,
                    justice exerciseth all his demands, and also mercy claimeth all which
                    is her own; and thus, none but the truly penitent are saved" (Alma
                    42:13-14,
                    22-24). </big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>As he ascended
                    into heaven, Christ did so "having the bowels of mercy; being filled
                    with compassion towards the children of men; standing betwixt them and
                    justice; having broken the bands of death, taken upon himself their
                    iniquity and their transgressions, having redeemed them, and satisfied
                    the demands of
                    justice" (Mosiah 15:9). It is a matter of surpassing wonder that the
                    voluntary and merciful sacrifice of a single being could satisfy the
                    infinite and eternal
                    demands of justice, atone for every human transgression and misdeed
                    ever committed in the history of the world, and provide for the
                    sweeping of all mankind
                    into the encompassing arms of God's compassionate embrace--but that is
                    what happened. (From <em>Christ and the New Covenant: The Messianic
                        Message of the Book
                        of Mormon</em> [Salt Lake City: Deseret Book Co., 1997], pp. 225-229)<br>
                    <br style="font-family: arial;" wp="BR2">
                </big></p>
                <p style="font-family: arial; color: rgb(153, 0, 0);"><big><font
                        size="+1"><big><em>Justice, Mercy, and Atonement-A Parable</em></big></font></big><br
                    style="font-family: arial;" wp="BR2">
                </p>
                <p style="font-family: arial; margin-left: 40px;"><big><strong>Boyd K.
                        Packer</strong></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Let me tell you
                    a story--a parable.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>There once was a
                    man who wanted something very much. It seemed more important than
                    anything else in his life. In order for him to have his desire, he
                    incurred a great debt.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>He had been
                    warned about going into that much debt, and particularly about his
                    creditor. But it seemed so important for him to do what he wanted to do
                    and to
                    have what he wanted right now. He was sure he could pay for it later.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>So he signed a
                    contract. He would pay it off some time along the way. He didn't worry
                    too much about it, for the due date seemed such a long time away. He
                    had what he wanted now, and that was what seemed important.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>The creditor was
                    always somewhere in the back of his mind, and he made token payments
                    now and again, thinking somehow that the day of reckoning really
                    would never come.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>But as it always
                    does, the day came, and the contract fell due. The debt had not been
                    fully paid. His creditor appeared and demanded payment in full.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Only then did he
                    realize that his creditor not only had the power to repossess all that
                    he owned, but the power to cast him into prison as well.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"I cannot pay
                    you, for I have not the power to do so," he confessed.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"Then," said the
                    creditor, "we will exercise the contract, take your possessions, and
                    you shall go to prison. You agreed to that. It was your choice. You
                    signed
                    the contract, and now it must be enforced."</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"Can you not
                    extend the time or forgive the debt?" the debtor begged. "Arrange some
                    way for me to keep what I have and not go to prison. Surely you believe
                    in mercy? Will you not show mercy?"</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>The creditor
                    replied, "Mercy is always so one-sided. It would serve only you. If I
                    show mercy to you, it will leave me unpaid. It is justice I demand. Do
                    you
                    believe in justice?"</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"I believed in
                    justice when I signed the contract," the debtor said. "It was on my
                    side then, for I thought it would protect me. I did not need mercy
                    then, nor
                    think I should need it ever. Justice, I thought, would serve both of us
                    equally as well."</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"It is justice
                    that demands that you pay the contract or suffer the penalty," the
                    creditor replied. "That is the law. You have agreed to it and that is
                    the way it must
                    be. Mercy cannot rob justice."</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>There they were:
                    One meting out justice, the other pleading for mercy. Neither could
                    prevail except at the expense of the other.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"If you do not
                    forgive the debt there will be no mercy," the debtor pleaded.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"If I do, there
                    will be no justice," was the reply.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Both laws, it
                    seemed, could not be served. They are two eternal ideals that appear to
                    contradict one another. Is there no way for justice to be fully served,
                    and
                    mercy also?</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>There is a way!
                    The law of justice can be fully satisfied and mercy can be fully
                    extended--but it takes someone else. And so it happened this time.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>The debtor had a
                    friend. He came to help. He knew the debtor well. He knew him to be
                    shortsighted. He thought him foolish to have gotten himself into such a
                    predicament. Nevertheless, he wanted to help because he loved him. He
                    stepped between them, faced the creditor, and made this offer.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"I will pay the
                    debt if you will free the debtor from his contract so that he may keep
                    his possessions and not go to prison."</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>As the creditor
                    was pondering the offer, the mediator added, "You demanded justice.
                    Though he cannot pay you, I will do so. You will have been justly dealt
                    with and can ask no more. It would not be just."</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>And so the
                    creditor agreed.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>The mediator
                    turned then to the debtor. "If I pay your debt, will you accept me as
                    your creditor?"</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"Oh yes, yes,"
                    cried the debtor. "You save me from prison and show mercy to me."</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"Then," said the
                    benefactor, "you will pay the debt to me and I will set the terms. It
                    will not be easy, but it will be possible. I will provide a way. You
                    need not
                    go to prison."</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>And so it was
                    that the creditor was paid in full. He had been justly dealt with. No
                    contract had been broken. The debtor, in turn, had been extended mercy.
                    Both
                    laws stood fulfilled. Because there was a mediator, justice had claimed
                    its full share, and mercy was fully satisfied.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Each of us lives
                    on a kind of spiritual credit. One day the account will be closed, a
                    settlement demanded. However casually we may view it now, when that day
                    comes and the foreclosure is imminent, we will look around in restless
                    agony for someone, anyone, to help us.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>And, by eternal
                    law, mercy cannot be extended save there be one who is both willing and
                    able to assume our debt and pay the price and arrange the terms for
                    our redemption.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Unless there is
                    a mediator, unless we have a friend, the full weight of justice
                    untempered, unsympathetic, must, positively must fall on us. The full
                    recompense
                    for every transgression, however minor or however deep, will be exacted
                    from us to the uttermost farthing.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>But know this:
                    Truth, glorious truth, proclaims there is such a Mediator.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"For there is
                    one God, and one mediator between God and men, the man Christ Jesus."
                    (1 Tim. 2:5.)</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Through Him
                    mercy can be fully extended to each of us without offending the eternal
                    law of justice. (From "The Mediator," <em>Ensign</em>, May 1977, pp.
                    54-56)</big></p>

