                <p style="font-family: arial;"><big><strong>Neal A. Maxwell</strong></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>The entirety of
                    Jesus' unique role as advocate cannot be understood if approached only
                    legalistically and adversarily, such as when one mortal lawyer jousts
                    with another lawyer before an impassive judge. In the sense here
                    intended, an advocate seeks to plead, to persuade, to intercede, and to
                    mediate in order to aid
                    another's cause. This is surely part of what Jesus does for us. But He
                    is, once again, both the advocate and the judge! No mortal lawyer has
                    such a dual role.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>When commencing
                    the agonies of the Atonement, Jesus declared of His atoning purpose,
                    "For this cause came I into the world" (John 18:37). </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>In calling upon
                    the mercy available in Father's plan, Jesus thereby truly "advocateth
                    the cause of the children of men" (Moro. 7:28). It is significant that
                    He so
                    pleads for us out of His full, personal knowledge and understanding of
                    each of us, including our individual experiences and shortfalls set
                    amid the shared
                    general human condition. "Behold, and hearken, . . . saith the Lord
                    your God, even Jesus Christ, your advocate, who knoweth the weakness of
                    man and how to
                    succor them who are tempted" (D&amp;C 62:1). </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>He can succor us
                    in any form of the human condition because, as He reminded Joseph in
                    the Liberty jail: "The Son of Man hath descended below them all. Art
                    thou greater than he?" (D&amp;C 122:8.) No person, therefore, who comes
                    before Him can exclaim, "You don't understand what I have been through!"</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>How like the
                    tender words cited earlier (D&amp;C 45:3-5) are the following and
                    amplifying words recorded in Alma, attesting to Jesus' personal
                    knowledge of our
                    individual and personal sufferings and sicknesses:</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>"And he shall go
                    forth, suffering pains and afflictions and temptations of every kind;
                    and this that the word might be fulfilled which saith he will take upon
                    him
                    the pains and the sicknesses of his people.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>"And he will
                    take upon him death, that he may loose the bands of death which bind
                    his people; and he will take upon him their infirmities, that his
                    bowels may
                    be filled with mercy, according to the flesh, that he may know
                    according to the flesh how to succor his people according to their
                    infirmities." (Alma 7:11-12.
                    See also Matt. 8:17.) </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big> Hence Jesus not
                    only bore our sins personally in order to atone for them, but He also
                    bore our pains, infirmities, and afflictions. Thereby ensured is the
                    precious
                    fact that Jesus' mercy would be full, because He knows how to succor us
                    in a unique, merciful, and personal way--amid all of that through which
                    we mortals
                    individually pass. Having so purchased us once, His glad and great
                    investment in us continues (see Acts 20:28). ...</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>Therefore,
                    beyond ensuring general immortality if, as to our sins, we are
                    adequately and truly repentant, He likewise specifically succors and
                    pleads for us as
                    only He can. He does this out of His perfect love, a love which is
                    fully informed by His perfect, personal familiarity with our individual
                    situations. No wonder
                    He is the wondrous Keeper of the entering gate to eternal life, and in
                    His loving empathy He "employeth no servant there" (see 2 Ne. 9:41). ...</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>Hence we see how
                    Jesus is our advocate in the most unique way. After all, if all this
                    were analogous to mere mortal advocacy, how many advocates know their
                    clients perfectly? For example, how many mortal advocates have actually
                    suffered deeply, physically and mentally, for their clients? Besides
                    all this, we are not
                    mere clients. We are Christ's spirit brothers and sisters! ...</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>However, unlike
                    the "finished" act of the Atonement achieved in that axis of atoning
                    agony--Gethsemane and on Calvary--across an additional time frame
                    Jesus' continuing advocacy for us mortals is spread. It continues in a
                    special, ongoing process as we, individually, access and apply His
                    atoning blood. (See
                    Mosiah 4:1-4.) Having paid the price for us, the Grand Atoner is also
                    the apparent judge as to our entitlement to access further His
                    atonement, as we repent and
                    become "the children of Christ" (Mosiah 5:7). </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>Jesus did not
                    retire to some corner of the universe for a well-earned vacation after
                    performing the great Atonement, but He has remained constantly vigilant
                    and
                    vigorous, personally mindful of all of us.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big> While the
                    scriptures relevant to Jesus' advocacy are abundant, verb tenses vary,
                    as already noted, suggesting something significant. For further
                    examples
                    consider the <em>post-atonement</em> scriptures, such as Heb. 7:25,
                    which declares that "Jesus <em>ever liveth</em> to make intercession,"
                    suggesting a continuousness. Heb. 9:24
                    speaks of how He <em>is to appear</em> to make intercession for us,
                    perhaps at the day of judgment. The Apostle John wrote that <em>if we
                        sin</em>, <em>we have an advocate</em> with the
                    Father, likewise suggesting an ongoingness to Jesus' advocacy and to
                    our accessing and applying His atonement (1 Jn. 2:1). Moro. 7:28 notes
                    Christ's
                    atonement and declares that He "<em>advocateth</em> the cause of the
                    children of men." D&amp;C 45:3-5, as noted earlier, indicates that He "<em>is
                        pleading</em>" in our behalf, again
                    the indication of an ongoingness.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>Hence His
                    pleading apparently continues for all specific and worthy individuals
                    until the final judgment. ... Hence Jesus' advocacy appears to be a
                    continuing
                    process, as we struggle individually to overcome our weakness and
                    thereby to "apply the atoning blood of Christ" by becoming more like
                    Him (Mosiah 4:2).
                    (<em>One More Strain of Praise </em>[Salt Lake City: Deseret Book Co.,
                    1999], pp. 34-48)<br>
                </big></p>
