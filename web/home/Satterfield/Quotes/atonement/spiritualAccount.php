                        J. Condie (of the Seventy)<br>
                    </strong></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>The Atonement
                    began in Gethsemane and continued on Golgotha until the Savior himself
                    declared on the cross, "It is finished" (John 19:30). ["The Fall and
                    Infinite Atonement," <em>Ensign</em>, Jan. 1996, p. 24]</big><br
                    style="font-family: arial;" wp="BR2">
                </p>
                <p style="font-family: arial; margin-left: 40px;"><big><strong>James E.
                        Talmage</strong></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>At the ninth
                    hour, or about three in the afternoon, a loud voice, surpassing the
                    most anguished cry of physical suffering issued from the central cross,
                    rending
                    the dreadful darkness. It was the voice of the Christ: "Eloi, Eloi,
                    lama sabachthani? which is, being interpreted, My God, my God, why hast
                    thou forsaken me?"
                    What mind of man can fathom the significance of that awful cry? It
                    seems, that in addition to the fearful suffering incident to
                    crucifixion, the agony of
                    Gethsemane had recurred, intensified beyond human power to endure. In
                    that bitterest hour the dying Christ was alone, alone in most terrible
                    reality. That the
                    supreme sacrifice of the Son might be consummated in all its fulness,
                    the Father seems to have withdrawn the support of His immediate
                    Presence, leaving to the
                    Savior of men the glory of complete victory over the forces of sin and
                    death. (<em>Jesus the Christ</em>, pp.660-661)</big><br
                    style="font-family: arial;" wp="BR2">
                </p>
                <p style="font-family: arial; margin-left: 40px;"><big><strong>Bruce R.
                        McConkie</strong></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Finally, on a
                    hill called Calvary--again, it was outside Jerusalem's walls--while
                    helpless disciples looked on and felt the agonies of near death in
                    their own
                    bodies, the Roman soldiers laid him upon the cross.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>With great
                    mallets they drove spikes of iron through his feet and hands and
                    wrists. Truly he was wounded for our transgressions and bruised for our
                    iniquities.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Then the cross
                    was raised that all might see and gape and curse and deride. This they
                    did, with evil venom, for three hours from 9:00 A.M. to noon.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Then the heavens
                    grew black. Darkness covered the land for the space of three hours, as
                    it did among the Nephites. There was a mighty storm, as though the
                    very God of Nature was in agony.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>And truly he
                    was, for while he was hanging on the cross for another three hours,
                    from noon to 3:00 P.M., all the infinite agonies and merciless pains of
                    Gethsemane recurred. ("The Purifying Power of Gethsemane," <em>Ensign</em>,
                    May 1985, p. 10)</big><br style="font-family: arial;" wp="BR2">
                </p>
                <p style="font-family: arial; margin-left: 40px;"><big><strong>Joseph
                        B. Wirthlin</strong></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Jesus Christ
                    suffered in the Garden of Gethsemane more than you can comprehend.
                    Willingly and lovingly, He took upon Himself not only our sins but the
                    pains, sicknesses, and sufferings of all mankind. He suffered similarly
                    on the cross, where He gave His life to pay the penalty for our sins if
                    we will repent.
                    And then in His ultimate triumph, He was resurrected and broke the
                    bands of death, making the Resurrection available to all. ("Growing
