                <p style="font-family: arial; text-align: left;"><big><font size="+1"><big><em><span
                                style="color: rgb(153, 0, 0);">The Ordeal of the Atonement as Recorded
                                in the Scriptures</span></em></big></font></big><br
                    style="font-family: arial;" wp="BR2">
                </p>
                <p style="font-family: arial; margin-left: 40px;"><big>[The following
                    is a discussion of the ordeal of the atonement as found in the
                    scriptures.]</big><br style="font-family: arial;" wp="BR2">
                </p>
                <p style="font-family: arial; margin-left: 40px;"><big><strong>Neal A.
                        Maxwell</strong></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>The Atonement is
                    the chief expression of Christ's loving-kindness. He endured so many
                    things. For instance, as prophesied, He was spat upon (see 1 Ne. 19:9).
                    As foretold, He was struck and scourged (see Mosiah 3:9). Likewise, He
                    was offered vinegar and gall while aflame with thirst (see Ps. 69:21).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Yet in His later
                    description of His agonies, Jesus does not speak of those things.
                    Instead, after the Atonement, there is no mention about His being spat
                    upon,
                    struck, or proffered vinegar and gall. Instead, Christ confides in us
                    His chief anxiety, namely, that He "would that [He] might not drink the
                    bitter cup, and
                    shrink" (D&amp;C 19:18)--especially desiring not to get partway through
                    the Atonement and then pull back. Mercifully for all of us, He
                    "finished [His] preparations
                    unto the children of men" (D&amp;C 19:19). Jesus partook of history's
                    bitterest cup without becoming bitter! Significantly, when He comes
                    again in majesty and
                    power, He will cite His aloneness, saying, "I have trodden the
                    wine-press alone" (D&amp;C 133:50).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>The Book of
                    Mormon describes Jesus' Atonement as the "infinite atonement" (Alma
                    34:12); it certainly required infinite suffering. When suffering and
                    burdened Jesus entered Gethsemane, He "fell on the ground" (Mark
                    14:35). He did not merely kneel down, pray intensely and briefly, and
                    leave. His agonies
                    were so great that He began to bleed at every one of thousands of His
                    pores (see D&amp;C 19:18). An angel, whose identity we do not know,
                    came to strengthen
                    Him (see Luke 22:43). Mark wrote that Jesus became "sore amazed" and
                    "very heavy" (Mark 14:33), meaning in the Greek, respectively,
                    "astonished and
                    awestruck" and "depressed and dejected." None of us can tell Christ
                    anything about depression!</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>In the course of
                    that great prayer, He pled with the Father in the most intimate and
                    familial of terms, "Abba, Father, all things are possible unto thee;
                    take away
                    this cup from me: nevertheless not what I will, but what thou wilt"
                    (Mark 14:36). This was not theater but real pleading to a loving Father
                    from a suffering Son
                    in the deepest possible distress!</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>In the Atonement
                    Jesus experienced what He later described as "the fierceness of the
                    wrath of Almighty God" (D&amp;C 76:107; D&amp;C 88:106). We can't even
                    begin to imagine what it would have been like as He stood in our places
                    and paid the price for our sins.</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>Though sinless
                    Himself, He bore the sins of billions. Thus His empathy and mercy
                    became fully perfected and personalized. Indeed, He thus "descended
                    below
                    all things, in that he comprehended all things" (D&amp;C 88:6; see also
                    122:8).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>He was scourged,
                    most likely with a Roman flagellum of several thongs; at the end of
                    each were sharp objects designed to tear the flesh. His tensed back
                    muscles would have been torn. If he was struck with the usual number of
                    blows, 39, the first blows would have bruised and the last blows would
                    have shredded
                    His flesh. Believing Christian physicians wrote that, medically
                    speaking, Jesus would have been in serious, if not critical, medical
                    condition because of the loss
                    of blood; and, as we know by revelation, He had previously bled from
                    every pore in the Garden of Gethsemane (see William D. Edwards, Wesley
                    J. Gabel,
                    Floyd E. Hosmer, "On the Physical Death of Jesus Christ," <em>Journal
                        of the American Medical Association</em>, 21 Mar. 1986, vol. 255, no.
                    11, 1458).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>The divine
                    reproach Jesus felt so exquisitely, because of His meekly standing in
                    for us, fulfilled yet another prophecy: "Reproach hath broken my heart;
                    and I
                    am full of heaviness: and I looked for some to take pity, but there was
                    none; and for comforters, but I found none" (Ps. 69:20). His heart was
                    broken, as He did
                    "suffer both body and spirit" (D&amp;C 19:18). He trembled because of
                    pain, and yet He, amidst profound aloneness, finished His preparations,
                    bringing to pass the
                    unconditional immortality of all mankind and "eternal life" for all
                    those who would keep His commandments (Moses 1:39).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>At the apogee of
                    His agony, Jesus uttered on the cross the great soul cry of
                    foresakenness: "My God, my God, why hast thou forsaken me?" (Matt.
                    27:46).
                    President Young's insight helps us understand His aloneness, which was
                    a unique dimension of His agony:</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>"At the very
                    moment, at the hour when the crisis came for him to offer up his life,
                    the Father withdrew Himself, withdrew His Spirit, and cast a vail over
                    [Jesus]. That is what made him sweat blood. If he had had the power of
                    God upon him, he would not have sweat blood; but all was withdrawn from
                    him, and a
                    veil was cast over him, and he then plead with the Father not to
                    forsake him" (in <em>Journal of Discourses</em>, 3:206).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>When Jesus comes
                    in overwhelming majesty and power, in at least one of His appearances
                    He will come in red attire, reminding us that He shed His blood to
                    atone for our sins (see D&amp;C 133:48; Isa. 63:1). His voice will be
                    heard to declare, again, how alone He once was: "I have trodden the
                    wine-press alone &#8230; and
                    none were with me" (D&amp;C 133:50).</big></p>
                <p style="font-family: arial; margin-left: 80px;"><big>The more we know
                    of Jesus' Atonement, the more we will humbly and gladly glorify Him,
                    His Atonement, and His character. We will never tire of paying
                    tribute to His goodness and loving-kindness. How long will we so speak
                    of our gratitude for His Atonement? The scriptures advise "forever and
                    ever"! (See
                    D&amp;C 133:52.) ["Enduring Well," <em>Ensign</em>, Apr. 1997, p. 10]<br>
                    <br style="font-family: arial;" wp="BR2">
                </big></p>
                <p style="font-family: arial; text-align: left; color: rgb(153, 0, 0);"><big><font
                        size="+1"><big><em>Atonement Began in Gethsemane and Continued to the
                            Cross</em></big></font></big><br style="font-family: arial;" wp="BR2">
                </p>
