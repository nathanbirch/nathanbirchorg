                <p style="font-family: arial; margin-left: 40px;"><big>But a universal
                    redemption from the effects of original sin, has nothing to do with
                    redemption from our personal sins; for the original sin of Adam, and
                    the
                    personal sins of his children, are two different things. The first was
                    committed by man in his immortal state; the second was committed by man
                    in a mortal
                    state; the former was committed in a state of ignorance of good or
                    evil; the latter was committed by man, having a knowledge of both good
                    and evil. As the
                    sins are different, and committed entirely under different
                    circumstances, so the penalties are different also. The penalty of the
                    first transgression was an eternal
                    separation of body and spirit, and eternal banishment from the presence
                    of Jehovah; while the penalty of our own transgressions does not
                    involve a disunion of
                    body and spirit, but only eternal banishment. The first penalty not
                    only shut man out from the presence of God, but deprived him eternally
                    of a body; the
                    second penalty permits him to retain his body, though in a banished
                    condition. As the penalties are different, so also is the redemption.
                    Redemption from the
                    first penalty is unconditional on the part of man; redemption from the
                    second penalty is conditional. Unconditional redemption is universal;
                    it takes within its
                    scope all mankind; it is as unlimited as the fall; it redeems men from
                    all its effects; it restores to them their bodies; it restores them to
                    the presence of God.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>The children of
                    Adam had no agency in the transgression of their first parents, and
                    therefore they are not required to exercise any agency in their
                    redemption
                    from its penalty. They are redeemed from it without faith, repentance,
                    baptism, or and other act, either of the mind or body.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>Conditional
                    redemption is also universal in its nature; it is offered to all, but
                    not received by all; it is a universal gift, though not universally
                    accepted; its
                    benefits can be obtained only through faith, repentance, baptism, the
                    laying on of the hands, and obedience to all other requirements of the
                    Gospel.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>Unconditional
                    redemption is a gift forced upon mankind, which they cannot reject,
                    though they were disposed. Not so with conditional redemption; it can
                    be
                    received or rejected according to the will of the creature.</big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>Redemption from
                    the original sin is without faith or works; redemption from our own
                    sins is given through faith and works. Both are the gifts of free
                    grace; but
                    while one is a gift forced upon us unconditionally, the other is a gift
                    merely offered to us conditionally. The reception of the one is
                    compulsory; the reception of
                    the other is voluntary. ...</big></p>
