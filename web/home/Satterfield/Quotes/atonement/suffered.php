                    for His
                    Father.</big></p>
                <div style="margin-left: 80px;"><big style="font-family: arial;">As
                        Jehovah, Jesus had said to Abraham: "Is any thing too hard for the
                        Lord?" (Gen. 18:14.) Jesus had taught this very truth about what was
                        feasible for
                        believers in His mortal ministry. Had not an angel told a perplexed
                        Mary about her own impending miracle, saying, "For with God nothing
                        shall be
                        impossible"? (Luke 1:37.) And so in His anguish, Jesus actually pled
