                    weight of all
                    mortal sins, somehow, past, present, and future, pressed upon that
                    perfect, sinless, and sensitive soul! All infirmities and sicknesses
                    were part, too, of the awful
                    arithmetic of the Atonement. (See Alma 7:11, 12; Isa. 53:3-5; Matt.
                    8:17.)</big></p>
                <p style="margin-left: 80px; font-family: arial;"><big>"And he said,
                    Abba, Father, all things are possible unto thee; take away this cup
