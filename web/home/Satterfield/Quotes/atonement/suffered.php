                        quoted back to the
                        Father those special, significant words--"All things are possible unto
                        thee; take away this cup from me." (See Matt. 16:26; 26:39.) This was
                        not theater--this
                        was shuddering reality! Did the Lamb, in this extremity, hope for a ram
                        in the thicket? I do not know, but the suffering was enormously
                        multiplied by infinity.
