                <p style="font-family: arial; text-align: center;"><big><big><big><strong
                                style="color: rgb(153, 0, 0);"><em><br>
                                    Atonement Enables Us to Both <br>
                                    Overcome and Become</em></strong></big><em></em></big></big></p>
                <big style="color: rgb(0, 0, 153);"><big style="font-family: arial;"><big><font
                                size="-1"><big><big><big><strong>David A. Bednar</strong> <span
                                            style="color: rgb(153, 153, 153);">(Quorum of the Twelve Apostles)</span></big></big></big></font></big></big></big>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>The gospel of Jesus Christ encompasses much more
                                than avoiding, overcoming, and being cleansed from sin and the bad
                                influences in our lives; it also essentially entails doing good, being
                                good, and becoming better. Repenting of our sins and seeking
                                forgiveness are spiritually necessary, and we must always do so. But
                                remission of sin is not the only or even the ultimate purpose
                                of the gospel. To have our hearts changed by the Holy Spirit such that
                                "we have no more disposition to do evil, but to do good continually"
                                (Mosiah 5:2), as did King Benjamin's people, is the
                                covenant responsibility we have accepted. This mighty change is not
                                simply the result of working harder or developing greater individual
                                discipline. Rather, it is the consequence of a
                                fundamental change in our desires, our motives, and our natures made
                                possible through the Atonement of Christ the Lord. Our spiritual
                                purpose is to overcome both sin and the desire to sin,
                                both the taint and the tyranny of sin.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><u>Prophets throughout the ages have emphasized
                                    the dual requirements of (1) avoiding and overcoming bad and (2) doing
                                    good and becoming better</u>. Consider the penetrating questions posed
                                by the Psalmist:</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"Who shall ascend into the hill of the Lord? or
                                who shall stand in his holy place?</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"He that hath clean hands, and a pure heart; who
                                hath not lifted up his soul unto vanity, nor sworn deceitfully" (Psalm
                                24:3-4).</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Brothers and sisters, it is possible for us to
                                have clean hands but not have a pure heart. Please notice that both
                                clean hands and a pure heart are required to ascend into the hill of
                                the Lord and
                                to stand in His holy place.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Let me suggest that hands are made clean through
                                the process of putting off the natural man and by overcoming sin and
                                the evil influences in our lives through the Savior's Atonement. Hearts
                                are purified as we receive His strengthening power to do good and
                                become better. All of our worthy desires and good works, as necessary
                                as they are, can never produce clean hands and a
                                pure heart. <u>It is the Atonement of Jesus Christ that provides both
                                    a <em>cleansing and redeeming power</em> that helps us to overcome sin
                                    and a<em> sanctifying and strengthening power</em> that helps us to
                                    become better than we ever could by relying only upon our own strength</u></big></big></font><font
                            size="-1"><big><big>. <u>The infinite Atonement is for both the
                                    sinner and for the saint in each of us</u>.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>In the Book of Mormon, we find the masterful
                                teachings of King Benjamin concerning the mission and Atonement of
                                Jesus Christ. The simple doctrine he taught caused the congregation to
                                fall to the earth, for the fear of the Lord had come upon them. "And
                                they had viewed themselves in their own carnal state, even less than
                                the dust of the earth. And they all cried aloud with one
                                voice, saying: O have mercy, and apply the atoning blood of Christ that
                                we may <em>receive forgiveness of our sins</em>, and our <em>hearts
                                    may be purified</em>; for we believe in Jesus Christ, the Son of God,
                                who created heaven and earth, and all things; who shall come down among
                                the children of men" (Mosiah 4:2; emphasis added).</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Again in this verse we find the twofold blessing
                                of both forgiveness of sin, suggesting clean hands, and the
                                transformation of our nature, signifying pure hearts.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>As King Benjamin concluded his instruction, he
                                reiterated the importance of these two basic aspects of spiritual
                                development.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"And now, for the sake of these things which I
                                have spoken unto you-that is, for the sake of<em> retaining a remission
                                    of your sins from day to day, that ye may walk guiltless before God</em>
                                - I
                                would that ye should impart of your substance to the poor" (Mosiah
                                4:26; emphasis added).</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Our sincere desire should be to have both clean
                                hands <em>and</em> a pure heart-both a remission of sins from day to
                                day and to walk guiltless before God. Clean hands alone will not be
                                enough when
                                we stand before Him who is pure and who, as "a lamb without blemish and
                                without spot" (1 Peter 1:19), freely spilled His precious blood for us.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Some who hear or read this message may think the
                                spiritual progress I am describing is not attainable in their lives. We
                                may believe these truths apply to others but not to us.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big> We will not attain a state of perfection in this
                                life, but we can and should press forward with faith in Christ along
                                the strait and narrow path and make steady progress toward our eternal
                                destiny. The Lord's pattern for spiritual development is "line upon
                                line, precept upon precept, here a little and there a little" (2 Nephi
                                28:30). Small, steady, incremental spiritual improvements
                                are the steps the Lord would have us take. Preparing to walk guiltless
                                before God is one of the primary purposes of mortality and the pursuit
                                of a lifetime; it does not result from sporadic
                                spurts of intense spiritual activity.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big> I witness that the Savior will strengthen and
                                assist us to make sustained, paced progress. The example in the Book of
                                Mormon of "many, exceedingly great many" (Alma 13:12) in the ancient
                                Church who were pure and spotless before God is a source of
                                encouragement and comfort to me. I suspect those members of the ancient
                                Church were ordinary men and women just like you
                                and me. These individuals could not look upon sin save it were with
                                abhorrence, and they "were made pure and entered into the rest of the
                                Lord their God" (v. 12). And these principles and
                                this process of spiritual progress apply to each of us equally and
                                always. </big></big></font><font size="-2"><big><big>("Clean Hands and
                                a Pure Heart," <em>Ensign</em>, Nov 2007, pp. 80-83; underlining added)</big></big></font></big></big></p>
                <big><big><br style="font-family: arial;" wp="BR2">
                    </big></big>
                <p
                    style="font-family: arial; text-align: center; color: rgb(153, 0, 0);"><big><big><big><strong><em>Atonement
                                    Enables Us to Change Our Lives</em></strong></big></big></big><br
                    style="font-family: arial;" wp="BR2">
                </p>
                <big><big></big></big>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big><strong><br>
                                    <big style="color: rgb(0, 0, 153);">Ezra Taft Benson</big></strong><big>
                                    <span style="color: rgb(153, 153, 153);">(1899-1994) President</span></big></big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>The next principle I would like to discuss is
                                this: No one is more anxious to see us change our lives than the Father
                                and the Savior. In the book of Revelation is a powerful and profound
                                invitation from the Savior. He says, "I stand at the door, and knock:
                                if any man hear my voice, and open the door, I will come in to him."
                                (Rev. 3:20.) Note that He does not say, "I stand at the
                                door and wait for you to knock." He is calling, beckoning, asking that
                                we simply open our hearts and let Him in.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>In Moroni's great sermon on faith, the principle
                                is even more clearly taught. He was told by the Lord, "If men come unto
                                me I will show unto them their weakness. I give unto men weakness
                                that they may be humble; and my grace is sufficient for all men." It
                                matters not what is our lack or our weakness or our insufficiency. His
                                gifts and powers are sufficient to overcome them all.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Moroni continues with the words of the Lord: "My
                                grace is sufficient for all men that humble themselves before me; for
                                if they humble themselves before me, and have faith in me, <span
                                    style="font-style: italic;">then will I
                                    make weak things become strong unto them</span>." (Ether 12:27; italics
                                added.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>What a promise from the Lord! The very source of
                                our troubles can be changed, molded, and formed into a strength and a
                                source of power. This promise is repeated in one form or another in
                                many other scriptures. Isaiah said, "He giveth power to the faint; and
                                to them that have no might he increaseth strength." (Isa. 40:29.) Paul
                                was told by the Lord, "My grace is sufficient for
                                thee: for my strength is made perfect in weakness." (2 Cor. 12:9.) In
                                the Doctrine and Covenants we read, "He that trembleth under my power
                                shall be made strong, and shall bring forth fruits
                                of praise and wisdom." (D&amp;C 52:17; see also 1 Ne. 17:3; 2 Ne. 3:13;
                                D&amp;C 1:28; 133:58-59.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Brothers and sisters, we must take our sins to the
                                Lord in humble and sorrowful repentance. We must plead with Him for
                                power to overcome them. The promises are sure. He will come to our
                                aid. We will find the power to change our lives.</big></big></font><font
                            size="-2"><big><big> ( "A Mighty Change of Heart," <em>Ensign</em>,
                                Oct. 1989, pp. 4-5)</big></big></font></big></big><br
                    style="font-family: arial;" wp="BR1">
                </p>
