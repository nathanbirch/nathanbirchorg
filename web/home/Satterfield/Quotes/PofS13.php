                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>I have pondered the instruction of Jacob as
                                contained in the Book of Mormon:</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"Wherefore, we search the prophets, and we have
                                many revelations and the spirit of prophecy; and having all these
                                witnesses we obtain a hope, and our faith becometh unshaken, insomuch
                                that we truly can command in the name of Jesus and the very trees obey
                                us, or the mountains, or the waves of the sea.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"Nevertheless, the Lord God showeth us our
                                weakness <u>that we may know that it is by his grace, and his great
                                    condescensions unto the children of men, that we have power to do these
                                    things</u>"
                                (Jacob 4:6-7).</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Brothers and sisters, please pay particular
                                attention to the word <em>grace</em> as it is used in the verse I just
                                read. In the Bible Dictionary we learn that the word <em>grace</em>
                                frequently is used in the
                                scriptures to connote a strengthening or enabling power:</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"The main idea of the word is divine means of help
                                or strength, given through the bounteous mercy and love of Jesus Christ.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"&#8230; It is likewise through the grace of the Lord
                                that individuals, through faith in the atonement of Jesus Christ and
                                repentance of their sins, receive strength and assistance to do good
                                works
                                that they otherwise would not be able to maintain if left to their own
                                means. This grace is an enabling power that allows men and women to lay
                                hold on eternal life and exaltation after they
                                have expended their own best efforts" (p. 697).</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Thus, <u>the enabling and strengthening aspect of
                                    the Atonement helps us to see and to do and to become good in ways that
                                    we could never recognize or accomplish with our limited mortal
                                    capacity</u>. I testify and witness that the enabling power of the
                                Savior's Atonement is real. Without that strengthening power of the
                                Atonement, I could not stand before you this morning.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Can we sense the grace and strengthening power of
                                Christ in the testimony of Ammon? "Yea, I know that I am nothing; as to
                                my strength I am weak; therefore I will not boast of myself, but I
                                will boast of my God, <u>for in his strength I can do all things</u>;
                                yea, behold, many mighty miracles we have wrought in this land, for
                                which we will praise his name forever" (Alma 26:12). <u>Truly,
                                    brothers and sisters, in the strength of the Lord we can do and endure
                                    and overcome all things.</u></big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>As I walked out of the Church Administration
                                Building after my interview with President Hinckley on Friday
                                afternoon, I recalled the words of Enoch:</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"And when Enoch had heard these words, he bowed
                                himself to the earth, before the Lord, and spake before the Lord,
                                saying: Why is it that I have found favor in thy sight, and am but a
                                lad,
                                and all the people hate me; for I am slow of speech; wherefore am I thy
                                servant?</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"And the Lord said unto Enoch: Go forth and do as
                                I have commanded thee, and no man shall pierce thee. Open thy mouth,
                                and it shall be filled, and I will give thee utterance, for all flesh
                                is
                                in my hands, and I will do as seemeth me good" (Moses 6:31-32).</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>For all of us who feel unprepared and overwhelmed
                                and unequal to a new calling or responsibility, the promise of the Lord
                                to Enoch is equally applicable. The promise was true in Enoch's
                                day, and it is true today. </big></big></font><font size="-2"><big><big>("In
                                the Strength of the Lord," <em>Ensign</em>, Nov 2004, p. 76)</big></big></font></big></big><br
                    style="font-family: arial;" wp="BR1">
                </p>
                <big><big><br style="font-family: arial;" wp="BR2">
                    </big></big>
                <p
                    style="font-family: arial; text-align: center; color: rgb(153, 0, 0);"><big><big><big><strong><em>The
                                    Atonement Enables Us to <br>
                                    Overcome Our Weaknesses</em></strong></big></big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big><strong><big><span
                                            style="color: rgb(0, 0, 153);">M. Russell Ballard</span>&nbsp; <span
                                            style="color: rgb(153, 153, 153); font-weight: normal;">(Quorum of the
                                            Twelve Apostles)</span></big><br>
                                </strong></big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>The Lord promised us through the prophet Moroni:
                                "If men come unto me I will show unto them their weakness. I give unto
                                men weakness that they may be humble; and my grace is
                                sufficient for all men that humble themselves before me; for if they
                                humble themselves before me, and have faith in me, then will I make
                                weak things become strong unto them" (Ether 12:27).</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>There are several interesting things about this
                                scripture. First is that the Lord gives us weaknesses--not sin, but
                                weaknesses--so that we may be humble. Think about that for a moment. If
                                we
                                were perfect in every respect, it would be hard to be humble. Even in
                                specific things, humility comes harder to those who are very strong in
                                one area or another. The woman or man who is
                                remarkably beautiful or handsome can easily become proud of her or his
                                appearance. A brilliant scholar may look down in condescension on those
                                less intellectually blessed. Our weaknesses
                                help us to be humble.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Then comes the promise. If we are willing to
                                humble ourselves, then, as it says, "my grace is sufficient." In the
                                Bible Dictionary, <em>grace</em> is defined as an "enabling power"
                                (697). Can you see
                                the significance of that promise?</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>One of the signs of our day is how frequently we
                                use the word <em>addiction</em> to describe destructive behavior. We
                                talk about being addicted to alcohol, to drugs, to pornography. These
                                are all
                                insidious and powerful evils. Jesus warned His disciples that
                                "whosoever committeth sin is the servant of sin" (John 8:34). Alma used
                                a similar metaphor when he warned us about the "chains
                                of hell" (Alma 12:11).</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>One of the most devastating effects of sin is that
                                it weakens you, binds you, brings you down to slavery. The grace of God
                                and of His Son, the Lord Jesus Christ, is the answer to that
                                predicament. <u>If you will but humble yourselves and turn to Them,
                                    then Their grace, Their enabling power, can not only help you throw off
                                    the chains of sin but actually turn your weaknesses
                                    into strengths</u>.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Brothers and sisters, how I long to have the
                                reality of that promise sink into your hearts. <u>Are you struggling
                                    with some sin or weakness</u>? It can be something as simple as not
                                having the
                                willpower to rise in the morning early enough to have time for
                                scripture study and prayer. It can be something so powerful, such as
                                Internet pornography or lack of moral self-control, that you
                                feel you have been pulled down into an abyss and there is no hope for
                                you. <u>Do you find yourself hating what you are doing but not able to
                                    find the willpower to turn away from it?</u> <u>Then reach
                                    out and humble yourself</u>. <u>The Lord's enabling power is
                                    sufficient to change your heart, to turn your life, to purge your soul</u>.
                                <u>But you must make the first move, which is to humble yourself and
                                    realize that only in God can you find deliverance</u>. ("Be Strong in
                                the Lord," <em>Ensign</em>, Jul 2004, p. 11-12)</big></big></font></big></big><br
                    style="font-family: arial;" wp="BR1">
                </p>
                <big><big><br style="font-family: arial;" wp="BR2">
                    </big></big>
                <p
                    style="font-family: arial; text-align: center; color: rgb(153, 0, 0);"><big><big><big><strong><em>How
                                    to Receive the Lord's Enabling Power?</em></strong></big></big></big><br
                    style="font-family: arial;" wp="BR2">
                </p>
                <big><big></big></big>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big><strong><br>
                                    <big style="font-weight: normal;"><span
                                            style="color: rgb(0, 0, 153); font-weight: bold;">Gene R. Cook</span> <small
                                            style="color: rgb(102, 102, 102);">(Of the Seventy)</small></big></strong></big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Perhaps some of us have not received or known how
                                to use the great gift of grace the Father has given to us through the
                                Atonement of His Son, Jesus Christ. "For what doth it profit a man if a
                                gift is bestowed upon him, and he receive not the gift?" (D&amp;C
                                88:33.) The prophet Zenock even said, "Thou art angry, O Lord, with
                                this people, because they will not understand thy mercies
                                which thou hast bestowed upon them because of thy Son." (Alma 33:16.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><u>How many of us, at times, try to resolve life's
                                    challenges ourselves, without seeking the intervention of the Lord in
                                    our lives</u>? <u>We try to carry the burden alone</u>.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>As some are faced with trials and afflictions,
                                they say, "Why won't God help me?" Some have even struggled with doubts
                                about their prayers and their personal worthiness and say, "Perhaps
                                prayer doesn't work."</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="8"></a>Others who have suffered with
                                sickness, discouragement, financial crisis, rejection, disappointment,
                                and even loss of loved ones may say, "Why won't the Lord heal me or
                                help me with my
                                son? Why didn't He prevent her death? Does life have to be this
                                unhappy?"</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Yes, one might even cry out, "O God, where art
                                thou? &#8230; How long shall thy hand be stayed?" (D&amp;C 121:1-2.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="10"></a>Jesus taught that we pass through
                                all these trials to refine us "in the furnace of affliction" (1 Ne.
                                20:10), and that we should not bear them unaided, but "in [the]
                                Redeemer's name" (D&amp;C
                                138:13). In spite of our feeling, at times, that He has forgotten us,
                                He testifies, "Yea, they may forget, yet will I not forget thee &#8230;</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"Behold, I have graven thee upon the palms of my
                                hands." (1 Ne. 21:15-16.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>I testify that the Lord, through His grace, can
                                continually assist us in our daily lives and in our physical and mental
                                sickness, pain, transgressions, and even in all of our infirmities.
                                (See
                                Mosiah 14:5; Alma 7:11-13; Alma 34:31.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>However, to pass successfully through the trials
                                we encounter, we must keep our eyes and our hearts centered on the Lord
                                Jesus Christ. Because "since man had fallen he could not merit
                                anything of himself" (Alma 22:14); therefore, we needed an advocate, an
                                intercessor, a mediator to assist us. "And it is <em>because of thy Son</em>
                                that thou hast been thus merciful unto [us]." (Alma
                                33:11; emphasis added.) </big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>We should have great hope in knowing, however
                                unworthy we may feel or weak we may be,<u> <em>that if we will do all
                                        we can,</em> He will come to our aid and provide for us whatever we may
                                    lack.</u></big></big></font><font size="-1"><big><big>
                                (See 2 Cor. 12:9.) That statement, to some degree, defines grace.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Grace is a "divine means of help or strength,
                                given through the bounteous mercy and love of Jesus Christ." It is "an
                                enabling power." (Bible Dictionary, p. 697.) The doctrine of the grace
                                of
                                the Father and the Son and how it affects us is so significant that it
                                is mentioned more than two hundred times in the standard works.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="18"></a>If we can obtain the grace of the
                                Lord Jesus Christ, that divine enabling power to assist us, we will
                                triumph in this life and be exalted in the life to come.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="19"></a><u>Let me share with you five
                                    principles that may help us obtain that divine intervention in our own
                                    life or perhaps vicariously assist in the life of another</u>. These
                                principles are simple to
                                understand but most challenging to apply. You already know all of them.
                                However, you may not have considered how directly related they are to
                                obtaining grace.<a name="20"></a></big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><strong>The first principle is <em>faith.</em></strong></big></big></font><font
                            size="-1"><big><big><em></em> "Therefore being justified by faith, we
                                have peace with God through our Lord Jesus Christ:</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="21"></a>"By whom also we have access by
                                faith into this grace." (Rom. 5:1-2.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="22"></a>It is evident that this grace, or
                                enabling power, is accessed by faith. No wonder faith in the Lord Jesus
                                Christ is the first principle of the gospel.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="23"></a>How clear Christ's question was
                                to a sinking Peter, after he had walked on the water: "O thou of little
                                faith, wherefore didst thou doubt?" (Matt. 14:31.) The moment Peter
                                doubted and took
                                his eyes off the Savior, he severed himself from the power of Jesus
                                Christ that had sustained him on the water.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="24"></a>How many times, likewise, as we
                                have prayed for assistance or help with our problems, have we severed
                                ourselves from the power of God because of doubt or fear, and thus
                                could not obtain
                                this enabling power of God? (See D&amp;C 6:36; D&amp;C 67:3.)<a
                                    name="25"></a></big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><em><strong>Repentance</strong></em></big></big></font><font
                            size="-1"><big><big><strong> is the second principle</strong>. The
                                grace of the Lord through the Atonement can both cleanse us of sin and
                                assist us in perfecting ourselves through our trials, sicknesses, and
                                even
                                "character defects." We are both sanctified and justified through the
                                grace of the Lord. (See D&amp;C 20:30-31.) Truly, "as a man his sins
                                confess, Christ, in mercy, manifests." (Gene R. Cook
                                and Holly Cook, "I Am a Healthy Man," unpublished hymn; see Alma
                                24:10.) Remember, Christ can repair our flaws and failings that
                                otherwise are not repairable. (See Gen. 18:14; Mark
                                9:23-24.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="26"></a>That great truth ought to fill us
                                all with hope, as long as we are quick to remember that the effect of
                                grace in our lives is conditioned upon repenting of our sins.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="27"></a>"Therefore, blessed are they who
                                will repent. &#8230;</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="28"></a>"And may God grant &#8230; that men
                                might be brought unto repentance and good works, that they might be
                                restored unto grace for grace, according to their works." (Hel.
                                12:23-24.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="29"></a>A repentant heart and good works
                                are the very conditions required to have grace restored to us. When
                                someone pleads fervently in prayer for an answer, the answer may be
                                more conditioned
                                on repentance of personal sins than on any other factor. (See D&amp;C
                                101:7-8; Mosiah 11:23-24.)<a name="30"></a></big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><strong>The third principle is <em>humility</em></strong></big></big></font><font
                            size="-1"><big><big><em>.</em> "But he giveth more grace. Wherefore he
                                saith, God resisteth the proud, but giveth grace unto the humble."
                                (James 4:6.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="31"></a>"And if men come unto me I will
                                show unto them their weakness. I give unto men weakness that they may
                                be humble; and my grace is sufficient for all men that humble
                                themselves before
                                me." (Ether 12:27.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="32"></a>Humility is an essential
                                condition to obtaining this divine assistance.<a name="33"></a></big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><em><strong>Doing all in your own power</strong></em></big></big></font><font
                            size="-1"><big><big><strong> is the fourth principle</strong>. Truly
                                did Paul teach, "By grace are ye saved through faith; and that not of
                                yourselves: it is the gift of God:</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="34"></a>"Not of works, lest any man
                                should boast." (Eph. 2:8-9.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="35"></a>Yes, works alone cannot bring
                                that divine gift, but they are a key condition upon which the gift is
                                received. (See 2 Ne. 10:23-25.) "For we know that it is by grace that
                                we are saved, after all
                                we can do." (2 Ne. 25:23.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Thus, unless one has done all in his own power, he
                                cannot expect the grace of God to be manifest. What a glorious
                                principle to understand: the Lord's assistance to us--whether we have
                                strong faith or weak faith; whether a man, a woman, or a child--is not
                                based just on what we know, how strong we are, or who we are, but more
                                upon our <em>giving all that we can give</em> and<em>
                                    doing all that we can do</em> in our present circumstance. Once one has
                                given all he can, then the Lord, through His grace, may assist him.
                                (See D&amp;C 123:17.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="37"></a>Clearly, the Lord's role and our
                                role in our receiving divine help come into clear perspective in these
                                inspired words: "I can do all things through Christ which strengtheneth
                                me." (Philip.
                                4:13.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><strong>The fifth principle, <em>keeping the
                                        commandments</em></strong></big></big></font><font size="-1"><big><big><em>,</em>
                                surely is a condition for receiving the grace of the Lord. "If you keep
                                my commandments you shall receive of his fulness &#8230; ; therefore, &#8230;
                                you shall receive grace for grace." (D&amp;C 93:20; see also D&amp;C
                                93:28.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="39"></a>To obtain grace, one does not
                                have to be perfect but he does have to be trying to keep the
                                commandments the best that he can. Then the Lord may allow him to
                                receive that power.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>Moroni sums up the doctrine of grace succinctly:
                                "If ye shall deny yourselves of all ungodliness, and love God with all
                                your might, mind and strength, then is his grace sufficient for you,
                                that
                                by his grace ye may be perfect in Christ; &#8230;</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="41"></a>"Then are ye sanctified in Christ
                                by the grace of God, through the shedding of the blood of Christ."
                                (Moro. 10:32-33.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>What glorious gospel news is an understanding of
                                this doctrine of grace, which persuades us to more fully center our
                                faith and hope upon Jesus Christ. Through the grace of the Father, we
                                will better know how to come unto the Son. (1 Ne. 15:14-15.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="44"></a>Let us be submissive to the
                                Father's will, recognizing that His will is preeminent. How thankful we
                                ought to be to submit to His will, because He and His Son will never do
                                anything "save it
                                be for the benefit of the world." (2 Ne. 26:24.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="45"></a>By seeking the intercession of
                                the Lord more fully in our lives:</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="46"></a></big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>&#8226; We will "grow in grace and in the knowledge of
                                the truth." (D&amp;C 50:40.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="47"></a>&#8226; We will "teach &#8230; diligently and
                                [His] grace shall attend [us]." (D&amp;C 88:78.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="48"></a>&#8226; For our labor, we will "receive
                                the grace of God, that [we] might wax strong in the Spirit, &#8230;&#8230; that
                                [we] might teach with power and authority from God." (Mosiah 18:26.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="49"></a>&#8226; We will not "fall from grace."
                                (D&amp;C 20:32.)</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="50"></a>&#8226; We will "receive grace for
                                grace" (D&amp;C 93:20).</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big><a name="51"></a></big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"Let us therefore come boldly unto the throne of
                                grace, that we may obtain mercy, and find grace to help in time of
                                need." (Heb. 4:16.)</big></big></font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font
                            style="font-family: arial;" size="-1"><big><big><a name="52"></a>I
                                bear witness that if we will seek the grace of God, He will come to our
                                aid and the aid of our loved ones in times of need. Let us obey the
                                Lord in all things and offer to Him the ultimate
                                sacrifice of "a broken heart and a contrite spirit." (3 Ne. 9:20; see
                                also 3 Ne. 12:19.) </big></big></font><font style="font-family: arial;"
                                                                    size="-2"><big><big>("Receiving Divine Assistance through the Grace of
                                the Lord," <em>Ensign</em>, May 1993, pp. 79-80; bolding and
