                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>The healing power of the Lord Jesus
                                Christ--whether it removes our burdens or strengthens us to endure and
                                live with them like the Apostle Paul--is available for every affliction
                                in
                                mortality.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>After I gave a general conference talk on the
                                evils of pornography (see "Pornography," <em></em><em>Liahona</em> and
                                <em>Ensign,</em> May 2005, 87-90), I received many letters from persons
                                burdened with this
                                addiction. Some of these letters were from men who had overcome
                                pornography. One man wrote:</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"There are several lessons I've gleaned from my
                                experience coming out of the darkness of a sin that so thoroughly
                                dominates the lives of the people it ensnares: (1) This is a major
                                problem
                                that is unbelievably difficult to overcome. &#8230; (2) The most important
                                source of support and strength in the repentance process is the Savior.
                                &#8230; (3) Intense, daily scripture study, regular temple
                                worship, and serious, contemplative participation in the ordinance of
                                the sacrament are all indispensable parts of a true repentance process.
                                This, I assume, is because all of these activities
                                serve to deepen and strengthen one's relationship with the Savior,
                                one's understanding of His atoning sacrifice, and one''s faith in His
                                healing power" (letter dated Oct. 24, 2005).</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"Come unto me," the Savior said, "and ye shall
                                find rest unto your souls" (Matthew 11:28</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>-29). That heavy-laden man turned to the Savior,
                                and so can each of us.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>A woman whose marriage was threatened by her
                                husband's addiction to pornography wrote how she stood beside him for
                                five pain-filled years until, as she said, "through the gift of our
                                precious Savior's glorious Atonement and what He taught me about
                                forgiveness, [my husband] finally is free--and so am I." As one who
                                needed no cleansing from sin, but only sought a
                                loved one''s deliverance from captivity, she wrote this advice:</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"Commune with the Lord. &#8230; He is your best friend!
                                He knows your pain because He has felt it for you already. He is ready
                                to carry that burden. Trust Him enough to place it at His feet and
                                allow Him to carry it for you. Then you can have your anguish replaced
                                with His peace, in the very depths of your soul" (letter dated Apr. 18,
                                2005).</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>A man wrote a General Authority about how the
                                power of the Atonement helped him with his problem of same-gender
                                attraction. He had been excommunicated for serious transgressions that
                                violated his temple covenants and his responsibilities to his children.
                                He had to choose whether to attempt to live the gospel or whether to
                                continue a course contrary to its teachings.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"I knew it would be difficult," he wrote, "but I
                                didn't realize what I would have to go through." His letter describes
                                the emptiness and loneliness and the incredible pain he experienced
                                from
                                deep within his soul as he sought to return. He prayed mightily for
                                forgiveness, sometimes for hours at a time. He was sustained by reading
                                the scriptures, by the companionship of a loving
                                bishop, and by priesthood blessings. But what finally made the
                                difference was the help of the Savior. He explained:</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"It [was] only through Him and His Atonement. &#8230; I
                                now feel an overwhelming gratitude. My pains have been almost more than
                                I could bear at times, and yet they were so small compared to
                                what He suffered. Where there once was darkness in my life, there is
                                now love and gratitude."</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>He continues: "Some profess that change is
                                possible and therapy is the only answer. They are very learned on the
                                subject and have so much to offer those who struggle &#8230; , but I worry
                                that
                                they forget to involve Heavenly Father in the process. If change is to
                                happen, it will happen according to the will of God. I also worry that
                                many people focus on the causes of [same-gender
                                attraction]. &#8230; There is no need to determine why I have [this
                                challenge]. I don't know if I was born with it, or if environmental
                                factors contributed to it. The fact of the matter is that I have
                                this struggle in my life and what I do with it from this point forward
                                is what matters" (letter dated Mar. 25, 2006).</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>The persons who wrote these letters know that the
                                Atonement of Jesus Christ and the healing it offers do much more than
                                provide the opportunity for repentance from sins. The Atonement
                                also gives us the strength to endure "pains and afflictions and
                                temptations of every kind," because our Savior also took upon Him "the
                                pains and the sicknesses of his people" (Alma 7:11).
                                Brothers and sisters, if your faith and prayers and the power of the
                                priesthood do not heal you from an affliction, the power of the
                                Atonement will surely give you the strength to bear the
                                burden.</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>"Come unto me, all ye that labour and are heavy
                                laden," the Savior said, "and I will give you rest &#8230; unto your souls"
                                (Matthew 11:28-29).</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>As we struggle with the challenges of mortality, I
                                pray for each of us, as the prophet Mormon prayed for his son, Moroni:
                                "May Christ lift thee up, and may his sufferings and death, &#8230; and
                                his mercy and long-suffering, and the hope of his glory and of eternal
                                life, rest in your mind forever"Moroni 9:25).</big></big></font></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><font
                            size="-1"><big><big>I testify of Jesus Christ, our Savior, who invites
                                us all to come unto Him and be perfected in Him. He will bind up our
                                wounds and He will heal the heavy laden. </big></big></font><font
                            size="-2"><big><big>("He Heals the Heavy Laden,"
