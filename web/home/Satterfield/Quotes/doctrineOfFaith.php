                <p style="font-family: arial;"><big><big><strong>Bruce R. McConkie</strong></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><strong>Queries</strong>:
                        Which comes first faith or
                        knowledge? Is faith a vague and uncertain hope in something unseen
                        that, as a sprouting seed, may grow into knowledge?
                        Or is faith based on truth and knowledge? Is faith something that grows
                        out of and comes because of a prior knowledge of the truth?</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><strong>Answers</strong>:
                        Faith is the child of knowledge.
                        It is reserved for those only who first have knowledge; there neither
                        is nor can be any faith until there is knowledge.
                        No one can have faith in a God of whom he knows nothing. Faith is
                        founded on truth; it is the offspring of truth; it can never exist
                        alone and apart from the
                        truth. No one can have faith unto life and salvation in a false god; no
                        idol ever had power to raise the dead or stop the sun. And faith is
                        power. It is true that
                        faith in some doctrine or on some theological point may be imperfect;
                        it is true that sproutings of either faith or knowledge can become
                        perfect relative to that
                        doctrine or concept. But faith itself - the great and eternal power
                        that creates and governs and saves, faith unto life and salvation -
                        saving faith grows out of
                        knowledge and cannot come in any other way.</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>"Faith
                        cometh by hearing," Paul says, "and hearing the
                        word of God" taught by the power of the Holy Ghost. (Romans 10:17.)
                        Faith comes to those only who
                        receive the word of truth. Hence the gospel truism that "it pleased God
                        by the foolishness of preaching to save them that believe." (1
                        Corinthians 1:21.) The
                        gospel embraces all truth, and truth - nothing else -- can bring
                        salvation. Thus Alma says, "If ye have faith ye hope for things which
                        are not seen, which are
                        true." (Alma 32:21.) Indeed, faith is a hope in that which is not seen
                        that is true, and there can be no faith in an unseen thing that is
                        false. There is no power in
                        falsehood. No one can have faith unto life and salvation in a god who
                        is believed to be a spirit nothingness, or in a doctrine that denies
                        the resurrection, or in a
                        philosophy that postulates man's evolutionary evolvement from lower
                        forms of life, or in anything that is not true.</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Truth,
                        diamond truth, is the rock foundation upon which
                        faith rests, and no one can have faith in God or in any gospel truth
                        unless and until he comes to a
                        knowledge of whatever truth is involved. (<em>A New Witness for the
                            Articles of Faith</em>, p.166-167)</big></big></p>
                <div style="margin-left: 40px;"><br wp="BR2">
                    <big style="font-family: arial;"><big></big></big></div>
                <p style="font-family: arial; margin-left: 40px;"><big><big>How do men
                        exercise faith? If they have an occasion to
                        heal the sick, raise the dead or move mountains, how is it done? Faith
                        is power, but what causes the
                        power to flow forth and accomplish the desired result? As an
                        introductory explanation, the account we are studying asks: "What are
                        we to understand by a
                        man's working by faith?"</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>By way of
                        answer, the account says: "We understand that
                        when a man works by faith he works by mental exertion instead of
                        physical force. It is by words,
                        instead of exerting his physical powers, with which every being works
                        when he works by faith. God said, `Let there be light: and there was
                        light.' Joshua spake
                        and the great lights which God had created stood still. Elijah
                        commanded, and the heavens were stayed for the space of three years and
                        six months, so that it did
                        not rain: he again commanded and the heavens gave forth rain. All this
                        was done by faith. And the Saviour says: `If you have faith as a grain
                        of mustard seed,
                        say to this mountain, "Remove," and it will remove; or say to that
                        sycamine tree, "Be ye plucked up, and planted in the midst of the sea,"
                        and it shall obey you.'
                        Faith, then, works by words; and with these its mightiest works have
                        been, and will be performed." (Lectures on Faith 7:3.)</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>But working
                        by faith is not the mere speaking of a few
                        well-chosen words; anyone with the power of speech could have commanded
                        the rotting corpse of
                        Lazarus to come forth, but only one whose power was greater than death
                        could bring life again to the brother of Mary and Martha. Nor is
                        working by faith
                        merely a mental desire, however strong, that some eventuality should
                        occur. There may be those whose mental powers and thought processes are
                        greater than
                        any of the saints, but only persons who are in tune with the Infinite
                        can exercise the spiritual forces and powers that come from him.</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Those who
                        work by faith must first have faith; no one can
                        use a power that he does not possess, and the faith or power must be
                        gained by obedience to those
                        laws upon which its receipt is predicated. These we have set forth.
                        Those who work by faith must believe in the Lord Jesus Christ and in
                        his Father. They must
                        accept at face value what the revealed word teaches as to the
                        character, attributes, and perfections of the Father and the Son. They
                        must then work the works of
                        righteousness until they know within themselves that their way of life
                        conforms to the divine will, and they must be willing to lay their all
                        on the altar of the
                        Almighty.</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>And then --
                        when the day is at hand and the hour has
                        arrived for the miracle to be wrought -- then they must be in tune with
                        the Holy Spirit of God. He who is
                        the Author of faith, he whose power faith is, he whose works are the
                        embodiment of justice and judgment and wisdom and all good things, even
                        he must
                        approve the use of his power in the case at hand. Faith cannot be
                        exercised contrary to the order of heaven or contrary to the will and
                        purposes of him whose
                        power it is. Men work by faith when they are in tune with the Spirit
                        and when what they seek to do by mental exertion and by the spoken word
                        is the mind and
                        will of the Lord. (Bruce R. McConkie, <em>A New Witness for the
                            Articles of Faith</em>, pp.191-192)</big></big></p>
                <big style="font-family: arial;"><big><br wp="BR2">
                    </big></big>
                <p style="font-family: arial;"><big><big><strong>Marion G. Romney</strong></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>It is also
                        clear from the scriptures that one must pray in
                        faith. It is apparent that there is a direct relationship between the
                        strength of one's faith and the
                        effectiveness of his prayer. There is, however, a principle associated
                        with this matter of faith that we should all understand. I used to feel
                        that if I could
                        develop enough faith, I could receive in every instance exactly what I
                        prayed for. This belief was based upon such scriptures as Matthew
                        17:20, in which Jesus
                        said to his disciples, "If ye have faith as a grain of mustard seed ye
                        shall say unto this mountain, Remove hence to yonder place; and it
                        shall remove; and
                        nothing shall be impossible unto you." But at that time I had not
                        learned that this promise was made upon the implied condition that
                        one's request be made in
                        harmony with the <em>will</em> <em>of God.</em></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>We learned
                        this principle in our home through a rather
                        trying experience. During the early years of our married life, my wife
                        and I intensely desired a particular
                        blessing that we thought would be a great blessing, and we set about to
                        obtain it by faith. We asked, we believed, we thought we had faith, but
                        though we
                        fasted often and prayed fervently, the years rolled by without bringing
                        us the object of our prayers.</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Finally we
                        concluded that there must be something that we
                        had not fully understood. Further research and prayerful study of the
                        scriptures revealed the fact that
                        we were not giving proper consideration to the will of the Lord, that
                        we were concentrating our faith and prayers solely upon receiving the
                        particular thing
                        which by predetermination we had set our hearts upon. We had to
                        reconsider the conditions of the promise. We found that Jesus had
                        stated them in full in
                        several places. For example, to the Nephites he said, ". . . whatsoever
                        ye shall ask the Father in my name, <em>which is right, </em>believing
                        that ye shall receive, behold
                        it shall be given you." (3 Nephi 18:20. Italics added.) In the light of
                        this and other scriptures, we came to realize that pending the time we
                        could know what is
                        right and what is expedient, we had to learn to be as earnest in
                        praying "if it be thy will" as we were in presenting our request.</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>We further
                        learned that the time will come when we shall
                        know the will of God before we ask, and then everything for which we
                        pray will be right and
                        expedient. And that will be when, as a result of righteous living, we
                        shall so enjoy the companionship of the Holy Spirit that he will
                        dictate what we ask. On
                        this point the Lord has said:</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>"And if ye
                        are purified and cleansed from all sin, ye
                        shall ask whatsoever you will in the name of Jesus and it shall be done.</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>"But know
                        this, it shall be given you what you shall ask."
                        (D&amp;C 50:29-30; also D&amp;C 46:30 and Helaman 10:5.) (Marion G.
                        Romney, <em>Improvement Era</em>, April,
                        1966, pp. 274-276, 301, 302, 304)</big></big></p>
