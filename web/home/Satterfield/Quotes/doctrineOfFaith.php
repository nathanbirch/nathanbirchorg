                <p style="font-family: arial;"><big><big><strong>Joseph B. Wirthlin</strong></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>"The just
                        shall live by faith"(Romans 1:7), we are told in
                        holy writ. I ask again, What is faith? </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Faith
                        exists when absolute confidence in that which we
                        cannot see combines with action that is in absolute conformity to the
                        will of our Heavenly Father.
                        Without all three--first, absolute confidence; second, action; and
                        third, absolute conformity--without these three all we have is a
                        counterfeit, a weak and
                        watered-down faith. Let me discuss each of these three imperatives of
                        faith. </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>First, we
                        must have confidence in that which we cannot
                        see. . . .</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Second, for
                        our faith to make a difference, we must act.
                        We must do all that is in our power to change passive belief into
                        active faith, for truly, "faith, if it hath
                        not works, is dead" (James 2:17). </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>In 1998,
                        President Gordon B. Hinckley raised a voice of
                        warning to the Saints of this Church as well as to the world at large.
                        He uttered that same warning last
                        night at priesthood meeting. He said: "I am suggesting that the time
                        has come to get our houses in order. So many people are living on the
                        very edge of their
                        incomes. In fact, some are living on borrowings. . . . I am troubled by
                        the huge consumer installment debt which hangs over the people of the
                        nation, including
                        our own people" ("To the Boys and to the Men," <em>Ensign</em>, Nov.
                        1998, 53; <em>Liahona,</em>
                        Jan. 1999, 65). </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Brothers
                        and sisters, when these prophetic words were
                        uttered, some faithful members of the Church mustered their faith and
                        heeded the counsel of the prophet.
                        They are profoundly grateful today that they did. Others perhaps
                        believed that what the prophet said was true but lacked faith, even as
                        small as a grain of
                        mustard seed. Consequently, some have suffered financial, personal, and
                        family distress. </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Third,
                        one's faith should be consistent with the will of
                        our Heavenly Father, including His laws of nature. The sparrow flying
                        into a hurricane may believe that
                        he can successfully navigate the storm, but the unforgiving natural law
                        will convince him otherwise in the end. </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Are we
                        wiser than the sparrow? Often what passes for faith
                        in this world is little more than gullibility. It is distressing to see
                        how eager some people are to
                        embrace fads and theories while rejecting or giving less credence and
                        attention to the everlasting principles of the gospel of Jesus Christ.
                        It is distressing how
                        eagerly some rush into foolish or unethical behavior, believing that
                        God will somehow deliver them from the inevitable tragic consequences
                        of their actions.
                        They even go so far as to ask for the blessings of heaven, knowing in
                        their hearts that what they do is contrary to the will of our Father in
                        Heaven. </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>How do we
                        know when our faith conforms to the will of our
                        Heavenly Father and He approves of that which we seek? We must know the
                        word of God. One of
                        the reasons we immerse ourselves in the scriptures is to know of
                        Heavenly Father's dealings with man from the beginning. If the desires
                        of our heart are
                        contrary to scripture, then we should not pursue them further. </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Next, we
                        must heed the counsel of latter-day prophets as
                        they give inspired instruction. </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Additionally,
                        we must ponder and pray and seek the
                        guidance of the Spirit. If we do so, the Lord has promised, "I will
                        tell you in your mind and in your heart,
                        by the Holy Ghost, which shall come upon you and which shall dwell in
                        your heart" (D&amp;C 8:2). </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Only when
                        our faith is aligned with the will of our
                        Heavenly Father will we be empowered to receive the blessings we seek.
                        ("Shall He Find Faith On the
                        Earth," Ensign, Nov. 2002, pp. 82-84)</big></big></p>
                <br style="font-family: arial;">
                <p style="font-family: arial;"><big><big><strong>Dallin H. Oaks</strong></big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>My first
                        point on the subject of timing is that the Lord
                        has His own timetable. "My words are sure and shall not fail," the Lord
                        taught the early elders of this
                        dispensation. "But," He continued, "all things must come to pass in
                        their time" (D&amp;C 64:31--32). </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>The first
                        principle of the gospel is faith in the Lord
                        Jesus Christ. Faith means trust--trust in God's will, trust in His way
                        of doing things, and trust in His
                        timetable. We should not try to impose our timetable on His. . . .
                        Indeed, we cannot have true faith in the Lord without also having
                        complete trust in His will
                        and in His timing. . . . </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>The Lord's
                        timing also applies to the important events of
                        our personal lives. A great scripture in the Doctrine and Covenants
                        declares that a particular spiritual
                        experience will come to us "in his own time, and in his own way, and
                        according to his own will" (D&amp;C 88:68). This principle applies to
                        revelation (see Oaks,
                        "Teaching and Learning by the Spirit,"<em> Ensign</em>, March 1997, 11)
                        and to all of the most important events in our lives: birth, marriage,
                        death, and even our moves
                        from place to place. . . .</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>It is not
                        enough that we are under call, or even that we
                        are going in the right direction. The timing must be right, and if the
                        time is not right, our actions should
                        be adjusted to the Lord's timetable as revealed by His servants. . . .</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Someone has
                        said that life is what happens to us while we
                        are making other plans. Because of things over which we have no
                        control, we cannot plan and bring
                        to pass everything we desire in our lives. Many important things will
                        occur in our lives that we have not planned, and not all of them will
                        be welcome. The
                        tragic events of September 11th and their revolutionary consequences
                        provide an obvious example. Even our most righteous desires may elude
                        us, or come in
                        different ways or at different times than we have sought to plan. </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>For
                        example, we cannot be sure that we will marry as soon
                        as we desire. A marriage that is timely in our view may be our blessing
                        or it may not. . . </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>The timing
                        of marriage is perhaps the best example of an
                        extremely important event in our lives that is almost impossible to
                        plan. Like other important mortal
                        events that depend on the agency of others or the will and timing of
                        the Lord, marriage cannot be anticipated or planned with certainty. We
                        can and should
                        work for and pray for our righteous desires, but, despite this, many
                        will remain single well beyond their desired time for marriage. </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>So what
                        should be done in the meantime? Faith in the Lord
                        Jesus Christ prepares us for whatever life brings. This kind of faith
                        prepares us to deal with life's
                        opportunities--to take advantage of those that are received and to
                        persist through the disappointments of those that are lost. In the
                        exercise of that faith we
                        should commit ourselves to the priorities and standards we will follow
                        on matters we do not control and persist faithfully in those
                        commitments whatever
                        happens to us because of the agency of others or the timing of the
                        Lord. When we do this, we will have a constancy in our lives that will
                        give us direction and
                        peace. Whatever the circumstances beyond our control, our commitments
                        and standards can be constant. . . .</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Wise are
                        those who make this commitment: <em>I will put
                            the Lord first in my life and I will keep His commandments.</em> The
                        performance of that commitment is
                        within everyone's control. We can fulfill that commitment without
                        regard to what others decide to do, and that commitment will anchor us
                        no matter what
                        timing the Lord directs for the most important events in our lives. </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Do you see
                        the difference between committing to what <em>you
                            will do,</em> in contrast to trying to plan that you will be married by
                        the time you graduate or that you
                        will earn at least X amount of dollars on your first job?</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>If we have
                        faith in God and if we are committed to the
                        fundamentals of keeping His commandments and putting Him first in our
                        lives, we do not need to plan
                        every single event--even every important event--and we should not feel
                        rejected or depressed if some things--even some very important
                        things--do not happen at
                        the time we had planned or hoped or prayed. </big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Commit
                        yourself to put the Lord first in your life, keep
                        His commandments, and do what the Lord's servants ask you to do. Then
                        your feet are on the pathway
                        to eternal life. Then it does not matter whether you are called to be a
                        bishop or a Relief Society president, whether you are married or
                        single, or whether you die
                        tomorrow. You do not know what will happen. Do your best on what is
                        fundamental and personal and then trust in the Lord and His timing.
                        (Dallin H. Oaks,
                        "Timing," Ensign, October
                        2003, pp. 10 ff).</big></big></p>
                <big style="font-family: arial;"><big><br wp="BR2">
