                <p><font face="Arial" size="+1"><strong><big>From A Vision Given to
                            Benjamin F. Johnson</big> </strong></font></p>
                <p><font face="Arial" size="+1">[While serving a mission for the Church
                    in 1840, Benjamin F. Johnson received the following vision while in
                    Erie,
                    Pennsylvania. What he saw is interesting in light of man's descending
                    below all things.]</font></p>
                <p style="margin-left: 40px;"><big><font face="Arial" size="+1"><big>The
                        word at once went out that a Mormon elder had come, and all appeared
                        anxious that I should preach. I did so the next
                        day, and the day after I was taken by others of the kindred to Union
                        district, where I commenced to preach to a large
                        congregation, and from there to a larger still. Here now a wide door
                        for preaching was opening to me . . . .Here lived
                        Washington Walker, a Universalist, who took me to his house and made it
                        my home while I remained in that country, often
                        taking me in his sleigh or carriage to my appointments. He was a
                        gentleman of culture, but of few words. At this time his
                        sister, an eminent Presbyterian, came from Erie City to visit them.
                        They took her to my appointments to hear Mormonism in
                        which she seemed to take a lively interest, and on one occasion said
                        there was one subject that greatly interested her, on
                        which she wanted light, and wished I would make it the subject of my
                        next discourse. This was "Foreordination" or "Election
                        and Reprobation." If she had struck me with a club I could not have
                        felt more stunned, dazed and foolish. I felt that I must
                        comply with her request, but how? In preaching the first principles of
                        the Gospel, the Second Coming of Christ, the
                        gathering of Israel, Book of Mormon, etc., I was perfect, both in the
                        letter and in the Spirit, but what did I know about
                        Predestination? I did not know its definition, or meaning, nor of
                        Election and Reprobation. And I was expected to preach
                        upon that subject. How could I without one gleam of light or some key
                        of knowledge to inspire me? I searched the
                        scriptures and prayed, but no light came to me. The subject occupied my
                        thoughts, and "foreordination" rang in my ear like
                        a funeral knell. I wished to make excuse to the lady, but how dare I
                        shrink from my calling? Had I not professed that my
                        capability to teach was from the Lord, and could I say I was not
                        prepared? But oh, how dark it all was to me!</big></font></big></p>
                <p style="margin-left: 40px;"><big><font face="Arial" size="+1"><big>The
                        day of meeting came, the hour was fast approaching, and the thought
                        almost took my breath. I had not eaten, I had not
                        slept, for Predestination had occupied my thoughts night and day. I did
                        not fear for myself, but for the great cause to be
                        dishonored, perhaps by me. But the hours would not wait, the
                        congregation had assembled, the house was full, and my
                        feelings almost as dark as suicide. I opened the meeting, arose, and
                        mechanically, without a thought as to what I would
                        read, opened the Bible and saw the first chapter to the Ephesians and
                        read, "We were chosen from before the foundation
                        of the world, to the adoption of children by Jesus Christ to the praise
                        and glory of God." Here now was the key of
                        knowledge, and with it came the light of the Lord to fill my whole
                        being.<em> The visions of heaven were opened before me.</em> I
                        saw that all intelligence moved to the accomplishment of objects for
                        their own greatness and glory, and to that end the
                        earth was made, not upon the principle that nothing had put forth to
                        beget something, but from matter as coexistent with
                        spirit. I saw that the spirits of all men had been begotten and that
                        they were the morning stars that sang together and
                        shouted for joy when the foundations of the earth were laid, <em>for
                            they saw that upon the earth they would receive
                            tabernacles, through which they, like the Elder Brother would "descend
                            below all things to arise above all things."</em> <em>And that
                            as He was foreordained a Lamb slain for sin, also was it foreordained
                            that man should sin; for if sin had not come there
                            would have been no death, and without death no pain, sorrow and
                            suffering; and without these there could be no joy and
                            happiness; for as light is comprehended through darkness, so pleasure
                            is bought by pain, its opposite. I saw that there is
                            opposition to all things, and had there been no element of death there
                            could have been no increase of life, "that man sinned
                            that man might be, and that men might have joy," after tribulation.</em></big></font></big></p>
                <p style="margin-left: 40px;"><big><font face="Arial" size="+1"><big>All
                        these ideas, and many others I presented and elucidated in a discourse
                        delivered in a vehement and powerful manner,
                        of over three hours' length, while the congregation sat as if riveted
                        to their seats, and not a move did I notice from the time I
                        arose, until I took my seat, and even yet all sat still as if in a maze.</big></font></big></p>
                <p style="margin-left: 40px;"><big><font face="Arial" size="+1"><big>But
                        though all seemed to wonder, the marvel of no one could equal my own.
                        To me it was as though from Egyptian
                        darkness I had been suddenly brought into the light of the sun. The
                        heavens had seemed opened to me, and of all I was
                        the one most instructed. I knew it was all of the Lord because I had
                        not desired the light for my own praise and glory.</big></font></big></p>
                <p style="margin-left: 40px;"><font face="Arial" size="+1"><big>An old
                    Methodist preacher came to me at the close and said, "My young friend,
                    you have taken us beyond all of my
                    comprehension but I cannot gainsay one word." The lady left the next
                    day, seemed very thoughtful, and treated me with the
                    greatest respect, but she was of wealth and position, and I thought was
                    sorrowful that all the great things she had learned
                    were through so low and poor a people as the Mormons. (<em>Benjamin
                        Johnson My Life's Review</em> [1947], pp.79-82)</big><br>
                </font></p>

