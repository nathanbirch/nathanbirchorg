                        children that certain ordinances may be received and performed. For
                        this purpose temples were built, and into those temples those who
                        desire a place in the Celestial Kingdom have the authority to go and
                        receive their blessings, to enrich their lives and prepare them for
                        that kingdom. This is the kingdom where God and Christ will dwell. This
                        is the place that those will live, who have earned the right to inherit
                        it. <small>(<i>Sharing the Gospel With Others</i>, p.47-48)</small><br>
