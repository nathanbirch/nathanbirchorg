                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>But
                        the time is to come, away in the distant future, when the inhabitants
                        of the earth, all who have dwelt upon it, with the exception of a few
                        who are unredeemable, will be brought to bow the knee willingly; not by
                        compulsion, not by coercion, not by constraint, but willingly bow the
                        knee to King Immanuel and worship God, the true God, the Father, in
                        Christ's holy name, and be willing, perfectly willing, to render
                        obedience to his commandments. Of course, the condition in which they
                        will dwell will be consequent upon their doings when they were in the
                        flesh. They cannot all be in the same class; that is impossible. There
                        is a difference in the degrees of intelligence. Every one of them has
                        his own identity. Every son or daughter of God has a special identity,
                        and that will always be had. We will not all be of the same height or
                        breadth, nor be exactly of the same outward appearance or of features,
                        but every one will be himself, and we will be cultivated and advanced
                        and developed on our own lines. If sinners are only fit, after
                        punishment, to enter into the Telestial world, there will be no end to
                        the progress along telestial lines; but those who thus come forth will
                        be "bodies telestial and not bodies celestial," and there will be
                        progress on every line and kind of being, for that is the order of the
                        universe. Light and truth will come to us as fast as we are willing to
                        receive it and obey it and put it into practice. <small>(Conference
                            Report, October 1913, p.19)</small><br>
