                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>And
                        may I say that there are comparatively few people in all the world who
                        understand that there will be a resurrection. The Lord has again
                        revealed this in our day. He has impressed it upon us and has given us
                        to understand that when the time comes for that resurrection, if we are
                        worthy that we will be quickened celestial bodies, and from then on, we
                        will dwell in the celestial kingdom, the highest of all kingdoms. But
                        he has taught us also that there are other places where we may go. If
                        we don't want to go to the celestial kingdom, by being less careful and
                        particular about keeping the commandments of God, we may go into the
                        terrestrial kingdom, and if we are still more careless, we may find our
                        way into the telestial kingdom, which is the least of the kingdoms of
                        glory.&nbsp; There are some people who have supposed that if we are
                        quickened telestial bodies that eventually, throughout the ages of
                        eternity, we will continue to progress until we will find our place in
                        the celestial kingdom, but the scriptures and revelations of God have
                        said that those who are quickened telestial bodies cannot come where
                        God and Christ dwell, worlds without end. <small>(Conference Report,
                            October 1945, p.172)<br>
                            <br>
                        </small></big></font></big></p>
                <div style="margin-left: 80px; font-family: arial;"><br>
                    <big><font size="+1"><big>The Lord has informed us that there are three
                            degrees of glory. One is known as the Telestial Kingdom. Into this
                            kingdom go those who have lived and have not earned more than the
                            privileges of that kingdom -- a kingdom of glory, it is true, but it is
                            a lesser kingdom. Those who are satisfied with an inheritance in that
                            kingdom, and who live for it, will be resurrected, and they will be
                            quickened, telestial bodies, and will inherit that kingdom. Referring
                            to these the Lord informs us that where God and Christ dwell they can
                            never be.</big></font></big><big> </big><br>
                    <br>
                    <big><font size="+1"><big>Then there is another kingdom known as the
                            Terrestrial Kingdom. The Lord says that those who desire greater
                            blessings and privileges may inherit that kingdom, provided they live
                            for it, and in the resurrection they will be quickened terrestrial
                            bodies, and will inherit the Terrestrial Kingdom.</big></font></big></div>
                <div style="margin-left: 80px; font-family: arial;"><big><big>But to
                            those who desire the highest and best of all, our Heavenly Father has
                            made known that there is another Kingdom, known as the Celestial
                            Kingdom. Those who desire to inherit that kingdom must conform to the
                            rules that are laid down by the Redeemer of mankind while here upon the
                            earth. The virtues that are attained by those in the other kingdoms,
                            will be achieved by those who inherit the Celestial Kingdom, and
                            additional virtues that the others have not acquired will be possessed
                            by those who inherit the Celestial Kingdom.</big></big></div>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>In
