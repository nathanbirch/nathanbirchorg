                <p style="margin-left: 80px; font-family: arial;">&nbsp;<big><font
                        size="+1"><big>The Apostle Paul goes on to reason that if Christ was
                        not risen, then we will not rise from the dead; but that if He has been
                        raised from the dead then we also shall be raised; and he goes on to
                        show how universal that resurrection shall be--some to come forth in
                        the resurrection of the just, and some in the resurrection of the
                        unjust, and that there are to be different grades of glory among those
                        that are resurrected. If you want to learn a little more about that, in
                        greater plainness, read the 88th section of the D&amp;C and you will
                        find there something that may be called philosophical as well as
                        theological. Those that obey celestial laws will so improve and purify
                        and sanctify their bodies that those bodies will be fit to come forth
                        in "the first resurrection" to celestial glory, and that they will then
                        be "bodies celestial;" while those who would not receive the laws of
                        God which are celestial, that is, receiving every word that comes from
                        the month of God, but will obey a terrestrial law, will be quickened by
                        a portion of the terrestrial glory and receive of the same in a
                        fullness; and, as we learn also in The Vision, they will not be bodies
                        celestial but "bodies terrestrial," a different class, but raised from
                        the dead and quickened by the power of that vital spirit which
                        quickeneth all things. And they who do not receive the terrestrial laws
                        but only the telestial, will come forth in the resurrection, raised
                        with a telestial body and be quickened by the telestial glory. In the
                        revelation that I referred to, in the 88th section, we learn that they
                        will improve, as all things will have to, for progress is the law of
                        the universe, and all beings, all intelligences will have an
                        opportunity of progressing along certain lines. Those who are of a
                        celestial body shall come forth and have a body like unto the glorious
                        body of the Son of God, and will dwell in His presence and be with Him
                        in glory in the presence of the Father, while those who only obey the
                        terrestrial or the telestial laws, after they are redeemed will come
                        forth in the way that is described, "but where God and Christ
                        dwell"--so it is said of the telestial--"they never can come worlds
                        without end." That may answer some queries that are made in some of our
                        theological classes. <small>(Conference Report, April 1915, p.36)</small></big></font></big></p>
