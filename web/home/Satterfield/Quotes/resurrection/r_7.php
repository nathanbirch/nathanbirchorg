                    <div style="margin-left: 40px;"><big><font size="+1"><big>Telestial law
                                is the law of evil, carnality, and corruption. Those who so live
                                develop telestial bodies, which can stand telestial glory, which is
                                found in a telestial kingdom. Terrestrial law is the law of decency and
                                uprightness from a worldly standpoint. Those who conform to this higher
                                order thereby create for themselves terrestrial bodies, which in turn
                                can stand terrestrial glory and go to a terrestrial kingdom. Celestial
                                law is the law of the gospel; it is the law of Christ. It calls upon
                                men to forsake the world and rise above every carnal and evil thing. It
                                calls upon men to repent and be baptized and receive the sanctifying
                                power of the Holy Spirit of God. It requires that they become new
                                creatures of the Holy Ghost. Only those who so live acquire thereby
                                celestial bodies; only such bodies can stand celestial glory, and this
                                glory is found only in a celestial kingdom. Since the final destiny of
                                this earth is to become a celestial globe, it thereby becomes the
                                ultimate and highest heaven for all the faithful who have lived on its
                                surface. <small>(<i>The Millennial Messiah</i>, pp. 697-698)</small></big></font></big><big><b><font
