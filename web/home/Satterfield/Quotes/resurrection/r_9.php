                            does not love her husband in this state she cannot love him in the
                            next. This is not so. Those who attain to the blessing of the first or
                            celestial resurrection will be pure and holy, and perfect in body.
                            Every man and woman that reaches to this unspeakable attainment will be
                            as beautiful as the angels that surround the throne of God. If you can,
                            by faithfulness in this life, obtain the right to come up in the
                            morning of the resurrection, you need entertain no fears that the wife
                            will be dissatisfied with her husband, or the husband with the wife;
                            for those of the first resurrection will be free from sin and from the
                            consequences and power of sin. This body "is sown in corruption, it is
                            raised in incorruption; it is sown in dishonor, it is raised in glory;
                            it is sown in weakness, it is raised in power; it is sown a natural
                            body, it is raised a spiritual body." "And, as we have borne the image
                            of the earthly, we shall also bear the image of the heavenly." <small>(<i>Journal
                                    of Discourses</i>, 10:24)</small></big></font><small> </small></big><br>
