                <p style="margin-left: 40px; font-family: arial;"><big><b><font
                            size="+1"><big>Spencer W. Kimball</big></font></b> </big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>When
                        we take precautions to protect ourselves from hazards, accidents,
                        death, we are thinking not only of saving ourselves from suffering,
                        from pain, from expense, but to preserve our bodies for their eternal
                        destiny.</big></font> </big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>The
                        body goes through many changes. Cells divide and growth follows. We
                        grow from pudgy infancy, through fast-growing childhood, through
                        gangling youth to full maturity, and finally into the shrinking,
                        furrowing, stiffening old age.</big></font> </big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>A
                        soul can continue to develop mentally and spiritually through these
                        changes, but the body reaches a summit from which it traverses a
                        declining path. The body resurrected will be neither the unbalanced
                        body of immature youth, nor the creaking, wrinkling one of many years,
                        but when it is restored and resurrected it will undoubtedly return in
                        the bloom of its greatest mortal perfection.</big></font> </big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>Some
                        sectarian peoples minimize the body and look forward to freedom from
                        it. Some flail and beat and torture the body, but the gospel of Jesus
                        Christ magnifies the importance of the body and the dignity of man.
                        This body will come forth in the resurrection. It will be free from all
                        imperfections and scars and infirmities which came to it in mortality
                        which were not self-inflicted. Would we have a right to expect a
                        perfect body if we carelessly or intentionally damaged it?</big></font>
                </big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>We
                        shall have our resurrected, perfected bodies through the eternities.
                        They were given to us -- we had little to do with getting them.</big></font>
                </big></p>
                <div style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>It
                            then becomes our duty to protect them from hazards, from mutilation or
                            disfigurement. We should treat them well, building them with proper
                            foods, proper rest, proper exercise and keep them strong, robust,
                            beautiful, and undamaged and live on and on till called home by our
                            Lord. <small>(<i>The Teachings of Spencer W. Kimball</i>, pp. 36-37)</small></big></font><small>
                        </small></big><br>
