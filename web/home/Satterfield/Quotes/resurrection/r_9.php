                        will gain celestial bodies with all the powers of exaltation and
                        eternal increase. These bodies will shine like the sun as our Savior's
                        does, as described by John. Those who enter the terrestrial kingdom
                        will have terrestrial bodies, and they will not shine like the sun, but
                        they will be more glorious than the bodies of those who receive the
                        telestial glory.</big></font> </big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>In
