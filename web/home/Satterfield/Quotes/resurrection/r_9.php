                <p style="margin-left: 40px; font-family: arial;"><big><b><font
                            size="+1"><big>Joseph Fielding Smith</big></font></b> </big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>There
                        is no reason for any person to be concerned as to the appearance of
                        individuals in the resurrection. Death is a purifying process as far as
                        the body is concerned. We have reason to believe that the appearance of
                        old age will disappear and the body will be restored with the full
                        vigor of manhood and womanhood. Children will arise as children, for
                        there is no growth in the grave. Children will continue to grow until
                        they reach the full stature of their spirits. Anything contrary to this
                        would be inconsistent. When our bodies are restored, they will appear
                        to be in the full vigor of manhood and womanhood, for the condition of
                        physical weakness will all be left behind in the grave. . . .</big></font>
                </big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>President
                        Joseph F. Smith when speaking at the funeral of Sister Rachel Grant the
                        mother of President Heber J. Grant had the following to say in relation
                        to deformities in the resurrection:</big></font> </big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>"Deformity
                        will be removed; defects will be eliminated, and men and women shall
                        again to the perfection of their spirits, to the perfection that God
                        designed in the beginning. It is his purpose that men and women, his
                        children, born to become heirs of God, and joint heirs with Jesus
                        Christ shall be made perfect, physically as well as spiritually through
                        obedience to the law by which he has provided the means that perfection
                        shall come to all his children. Therefore, I look for the time to come
                        when our dear Brother William C. Staines, whom we all knew so well, and
                        with whom we were familiar for years--I was familiar with him all my
                        life, just as I was familiar with Aunt Rachel here all my life, and do
                        not remember the time when I did not know her--I look for the time when
                        Brother Staines will be restored. He will not remain the crippled and
                        deformed William C. Staines that we knew, but he will be restored to
                        his perfect frame--every limb, every joint, every part of his physical
                        being will be restored to its perfect frame. This is the law and the
                        word of God to us, as it is contained in the revelations that have come
                        to us through the Prophet Joseph Smith. The point in my mind which I
                        desire to speak of particularly is this: When we shall have the
                        privilege to meet our mother, our aunt, our sister, this noble woman
                        whose mortal remains lie here now, but whose immortal spirit has
                        ascended to God from whence she came, when that spirit shall return to
                        take up this tabernacle again, here will be Aunt Rachel in her
                        perfection. . . . Under the law of restoration that God has provided,
                        she will regain her perfection, the perfection of her youth, the
                        perfection of her glory and of her being, until her resurrected body
                        shall assume the exact stature of the spirit that possessed it here in
                        its perfection, and thus we shall see the glorified, redeemed, exalted,
                        perfected Aunt Rachel, mother, sister, saint and daughter of the living
                        God, her identity being unchanged, as a child may grow to manhood or
                        womanhood and still be the same being." (<i>Gospel Doctrine</i>, p.
                        23-24).</big></font> </big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>Salvation
                        would be incomplete if individuals should arise in the resurrection
                        with all the deformities, weaknesses, and imperfections that are found
                        in so many of the human family in this moral existence. We have every
                        reason to believe that the spirits of mankind and all other creatures
                        were in a perfect form in the spirit world. It would be an awful
                        stretch of the imagination to think that the imperfections found so
                        frequently in mortality were defects which were designed in the
                        creation. Moreover, as the Lord made it dear in relation to the man who
                        was born blind, it was not an immortal condition.</big></font> </big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>By
                        the great power and faith of the Son of God, he was able to correct
                        deformity, blindness, and give to the deaf the gift of speech, by the
                        word of his power. The question has frequently been asked when a child
                        has been born with some physical defect or deformity, was this a
                        punishment or a condition which was his before he was born? No! All of
                        these ills are ills of the flesh or defects that are due to mortal
                        conditions which may have come upon the body even before birth, but we
                        may be assured that these defects were not conditions which existed in
                        the world of spirits.</big></font> </big></p>
                <div style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>It
                            is the will of the Lord that in the restoration of all things there
                            shall come perfection. The physical defects, some of which may have
                            resulted before birth, are defects which are due to some physical and
                            mortal condition and not an inheritance from the spirit world. <small>(<i>Answers
                                    to Gospel Questions</i>, 4:185-189)</small><br>
                            <br>
                        </big></font></big><small>&nbsp;</small><big><b><font size="+1"><big><br>
                            </big></font></b></big></div>
                <center style="font-family: arial;">
                    <div style="text-align: left;"><big
                            style="color: rgb(153, 0, 0); font-style: italic;"><b><font size="+1"><big>Resurrected
                                    Bodies are Spiritual Bodies</big></font></b></big><br>
                    </div>
                </center>
                <p style="margin-left: 40px; font-family: arial;"><big><b><font
                            size="+1"><big>Joseph Fielding Smith</big></font></b> </big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>In
                        the resurrection from the dead, the bodies which were laid down natural
                        bodies shall come forth spiritual bodies. That is to say, in mortality
                        the life of the body is in the blood, but the body when raised to
                        immortality shall be quickened by the spirit and not the blood. Hence,
                        it becomes spiritual, but it will be composed of flesh and bones, just
                        as the body of Jesus was, who is the prototype.</big></font> </big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>These
                        modern blind teachers of the blind who deny the literal resurrection
                        have a very false understanding of what is meant by a spiritual body.
                        They have based their conclusion on the statement that Paul makes that
                        the body is raised a spiritual body and that flesh and blood cannot
                        inherit the kingdom of God. They cannot conceive in their minds of a
                        body raised from the dead, being composed of flesh and bones, quickened
                        by spirit and not by blood.</big></font> </big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>When
                        Paul spoke of the spiritual body, he had no reference at all to the
                        spirit body, and there they have made their mistake. They have confused
                        the spiritual body, or, in other words, the body quickened by the
                        spirit, with the body of the spirit alone. They think that those who
                        believe in the resurrection of the literal body believe that it shall
                        be raised again, quickened by blood, which is not the case&#8230;.</big></font>
                </big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>After
                        the resurrection from the dead our bodies will be spiritual bodies, but
                        they will be bodies that are tangible, bodies that have been purified,
                        but they will nevertheless be bodies of flesh and bones. They will not
                        be blood bodies. They will no longer be quickened by blood but
                        quickened by the spirit which is eternal, and they shall become
                        immortal and shall never die.</big></font> </big></p>
                <p style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>Now
                        if our good friends understood this, they would not fall into this
                        error of thinking that Paul's doctrine was in conflict with that of the
                        Lord and Savior Jesus Christ, when Paul declared that the body that
                        would be raised would be a spiritual body. You read in the Book of
                        Genesis, where the Lord said to Noah after the flood, that the blood
                        was the life of the body; the blood is the life thereof, he says.
                        Therefore, "Whoso sheddeth man's blood, by man shall his blood be
                        shed," because blood is the life of the mortal body.</big></font> </big></p>
                <div style="margin-left: 80px; font-family: arial;"><big><font size="+1"><big>But
                            with the body brought forth in the resurrection, which is the immortal
                            body, that is not the case. In it blood does not exist, but the spirit
                            is the life giving power. Hence, they are no longer bodies quickened by
                            blood but bodies quickened by spirit, and hence they are spiritual
                            bodies, but tangible bodies of flesh and bones, just as was the body of
                            the Son of God. Now this is the doctrine of the Lord and Savior of the
                            world. <small>(<i>Doctrines of Salvation</i>, 2:.285-286)</small></big></font></big><small>&nbsp;
                    </small><br>
                </div>
