                            bear the image of our earthly parents in their fallen state, but by
                            obedience to the Gospel of salvation and the renovating influences of
                            the Holy Ghost, and the holy resurrection, we shall put on the image of
                            the heavenly, in beauty, glory, power and goodness. <small>(<i>Discourses
