                            know that in the future, after we have passed through this life, we
                            will then have our wives and our children with us. We will have our
                            bodies glorified, made free from every sickness and distress, and
                            rendered most beautiful. There is nothing more beautiful to look upon
                            than a resurrected man or woman. There is nothing grander that I can
                            imagine that a man can possess than a resurrected body. There is no
                            Latter-day Saint within the sound of my voice but that certainly has
                            this prospect of coming forth in the morning of the First Resurrection
                            and being glorified, exalted in the presence of God, having the
                            privilege of talking with our Father as we talk with our earthly
