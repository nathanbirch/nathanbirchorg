                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>"So
                        also is the resurrection of the dead. It is sown in corruption; it is
                        raised in incorruption:</big></font> <br>
                    <font size="+1"><big>"It is sown in dishonour; it is raised in glory:
                        it is sown in weakness; it is raised in power:</big></font> <br>
                    <font size="+1"><big>"It is sown a natural body; it is raised a
                        spiritual body. There is a natural body, and there is a spiritual
                        body." (1 Corinthians 15:42-44.)</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>Because
                        Paul distinguishes between a natural body and a spiritual body, and had
                        previously referred to the seed planted in the ground, a false
                        conclusion is reached from the analogy. It is argued that the seed
                        itself is not harvested--it dies in the ground and there comes forth a
                        new plant; therefore, this is so with the body which is buried in the
                        ground--it comes forth a spiritual body, something new and different.
                        This appears to be strengthened by the fact that Paul adds: "Now this I
                        say, brethren, that flesh and blood cannot inherit the kingdom of God.
                        " (1 Corinthians 15:50.)</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>Now
                        let me point up the fallacy of this reasoning by this statement from
                        scripture:</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>"And
                        the spirit and the body are the soul of man.</big></font> <br>
                    <font size="+1"><big>"And the resurrection from the dead is the
                        redemption of the soul." (D&amp;C 88:15-16.)</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>There
                        is a separation of the spirit and the body at the time of death. The
                        resurrection will again unite the spirit with the body, and the body
                        becomes a spiritual body, one of flesh and bones but quickened by the
                        spirit instead of blood. Thus, our bodies after the resurrection,
                        quickened by the spirit, shall become immortal and never die. This is
                        the meaning of the statements of Paul that "there is a natural body,
                        and there is a spiritual body" and "that flesh and blood cannot inherit
                        the kingdom of God." The natural body is flesh and blood, but quickened
                        by the spirit instead of blood, it can and will enter the kingdom.</big></font>
                </big></p>
