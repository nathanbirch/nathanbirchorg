                <big><b><font size="+1"><big><br>
                        </big></font></b></big><big style="font-family: arial;"><b><font
                            size="+1"><big>James E. Talmage</big></font></b></big>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>[Footnotes
                        have been added in brackets]</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big><i>Two
                            General Resurrections</i> are mentioned in the scriptures, and these
                        may be specified as first and final, or as the resurrection of the just
                        and the resurrection of the unjust. The first was inaugurated by the
                        resurrection of Jesus Christ; immediately following which many of the
                        saints came forth from their graves. A continuation of this, the
                        resurrection of the just, has been in operation since [Note the fact
                        that Moroni, the last of the Nephite prophets, who died in the first
                        quarter of the fifth century A.D., appeared as a resurrected being to
                        Joseph Smith in 1823], and will be greatly extended, or brought to pass
                        in a general way, in connection with the coming of Christ in His glory.
                        The final resurrection will be deferred until the end of the thousand
                        years of peace, and will be in connection with the last judgment.</big></font>
                </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big><i>The
                            First Resurrection</i> -- -Christ's Resurrection, and That Immediately
                        Following -- The facts of Christ's resurrection from the dead are
                        attested by such an array of scriptural proofs that no doubt of the
                        reality finds place in the mind of any believer in the inspired
                        records. . . .</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>Christ,
                        "the firstfruits of them that slept," [1 Cor. 15:20, 23; see also Acts
                        26:23; Col. 1:18; Rev. 1:5; see<i> Vitality of "Mormonism</i>, " pp.
                        288-294] "the firstborn from the dead" and "the first begotten of the
                        dead" was the first among men to come forth from the grave in an
                        immortalized body; but soon after His resurrection many of the saints
                        were brought from their tombs: "And the graves were opened; and many
                        bodies of the saints which slept arose, And came out of the graves
                        after his resurrection, and went into the holy city, and appeared unto
                        many." [Matt. 27:52-53.]</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>Alma,
                        the Nephite prophet, whose writings antedate by many decades the birth
                        of Christ, clearly understood that there would be no resurrection prior
                        to that of the Redeemer, for he said: "Behold, I say unto you, that
                        there is no resurrection -- or, I would say, in other words, that this
                        mortal does not put on immortality, this corruption does not put on
                        incorruption -- until after the coming of Christ." Furthermore, Alma
                        foresaw a general resurrection in connection with Christ's coming forth
                        from the dead, as the context of the foregoing quotation shows. [Alma
                        40:2, 16] Inspired men among the Nephites spoke of the death and
                        resurrection of Christ [See D&amp;C 63:20, 21] even during the time of
                        His actual ministry in the flesh; and their teachings were speedily
                        confirmed by the appearance of the risen Lord among them [D&amp;C
                        88:25, 26], as had been foretold by earlier prophets. [D&amp;C 76:107]
                        . . .</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big><i>Resurrection
                            at the Time of Christ's Second Coming</i> -- Soon after the bodily
                        departure of Christ from earth, the apostles, upon whom then devolved
                        the direct responsibilities of the Church, were found preaching the
                        doctrine of a future and universal resurrection. . . .</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>The
                        resurrection was a favorite theme with Paul; in his epistles to the
                        saints, he gave it frequent and prominent attention. [See Rom. 6:5;
                        8:11; 1 Cor., chap. 15; 2 Cor. 4:14; Philip. 3:21; Col. 3:4; 1 Thess.
                        4:14; Heb. 6:2] From him also we learn that an order of precedence is
                        to be observed in the resurrection: "But now is Christ risen from the
                        dead, and become the firstfruits of them that slept. For since by man
                        came death, by man came also the resurrection of the dead. For as in
                        Adam all die, even so in Christ shall all be made alive. But every man
                        in his own order: Christ the firstfruits; afterward they that are
                        Christ's at his coming." [1 Cor. 15:20-23; the entire chapter should be
                        studied]</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>It
                        is expressly asserted that many graves shall yield up their dead at the
                        time of Christ's advent in glory, and the just who have slept, together
                        with many who have not died, will be caught up to meet the Lord. Paul
                        thus wrote to the saints in Thessalonica: "Even so them also which
                        sleep in Jesus will God bring with him. * * * For the Lord himself
                        shall descend from heaven with a shout, with the voice of the
                        archangel, and with the trump of God: and the dead in Christ shall rise
                        first. Then we which are alive and remain shall be caught up together
                        with them in the clouds to meet the Lord in the air." [1 Thess. 4:14-17]</big></font>
                </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>To
                        the three Nephite disciples, who had asked the blessing of John the
                        beloved apostle, Christ promised: "And ye shall never endure the pains
                        of death; but when I shall come in my glory, ye shall be changed in the
                        twinkling of an eye from mortality to immortality." [3 Nephi 28:8]</big></font>
                </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>Through
                        the medium of latter-day revelation the Lord has said: "Behold, I will
                        come; and they shall see me in the clouds of heaven, clothed with power
                        and great glory, with all the holy angels; and he that watches not for
                        me shall be cut off. But before the arm of the Lord shall fall, an
                        angel shall sound his trump, and the saints that have slept shall come
                        forth to meet me in the cloud." [D&amp;C 45:44, 45] Of the many signs
                        and wonders which shall attend the Lord's glorious coming we have this
                        partial description: "And the face of the Lord shall be unveiled; And
                        the saints that are upon the earth, who are alive, shall be quickened
                        and be caught up to meet him. And they who have slept in their graves
                        shall come forth, for their graves shall be opened; and they also shall
                        be caught up to meet him in the midst of the pillar of heaven -- They
                        are Christ's, the firstfruits; they who shall descend with him first,
                        and they who are on the earth and in their graves, who are first caught
                        up to meet him." [D&amp;C 88:95-98]</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>Such
                        are some of the glories to attend the resurrection of the just. And the
                        company of the righteous will include all who have lived faithfully
                        according to the laws of God as made known to them; children who have
                        died in their innocence; and even the just among the heathen nations
                        who have lived in comparative darkness while groping for light, and who
                        have died in ignorance. This doctrine is made plain by modern
                        revelation: "And then shall the heathen nations be redeemed, and they
                        that knew no law shall have part in the first resurrection." [D&amp;C
                        45:54; see also Ezek. 36:23, 24; 37:28; 39:7, 21, 23] The Millennium
                        then is to be inaugurated by a glorious deliverance of the just from
                        the power of death; and of this company of the redeemed it is written:
                        "Blessed and holy is he that hath part in the first resurrection: on
                        such the second death hath no power, but they shall be priests of God
                        and of Christ, and shall reign with him a thousand years." [Rev. 20:6.]</big></font>
                </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big><i>The
                            Final Resurrection</i> -- "But the rest of the dead lived not again
                        until the thousand years were finished." [Rev. 20:5] So testified the
                        Revelator after having described the glorious blessings of the
                        righteous, who are given part in the first resurrection. The unworthy
                        will be called to the judgment of condemnation, when the regenerated
                        world is ready to be presented to the Father.</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>The
                        contrast between those whose part in the first resurrection is assured,
                        and those whose doom it is to wait until the time of final judgment, is
                        a strong one, and in no case do the scriptures lighten it. We are told
                        that it is right for us to weep over bereavement by death, "and more
                        especially for those that have not hope of a glorious resurrection."
                        [D&amp;C 42:45] In the present day, the voice of Jesus Christ is heard
                        in solemn warning: "Hearken ye, for, behold, the great day of the Lord
                        is nigh at hand. For the day cometh that the Lord shall utter his voice
                        out of heaven; the heavens shall shake and the earth shall tremble, and
                        the trump of God shall sound both long and loud, and shall say to the
                        sleeping nations: Ye saints arise and live; ye sinners stay and sleep
                        until I shall call again." [D&amp;C 43:17, 18]</big></font> </big></p>
                <div style="margin-left: 40px;"><big style="font-family: arial;"><font
                            size="+1"><big>The vision of the final scene is thus described by
                            John:. "And I saw the dead, small and great, stand before God; and the
                            books were opened: and another book was opened, which is the book of
                            life: and the dead were judged out of those things which were written
                            in the books, according to their works. And the sea gave up the dead
                            which were in it; and death and hell delivered up the dead which were
                            in them: and they were judged every man according to their works."
                            [Rev. 20:12, 13] As the scriptures conclusively prove, the resurrection
                            is to be universal. While it is true that the dead shall be brought
                            forth in order, each as he is prepared for the first or a later stage,
                            yet everyone who has tabernacled in the flesh shall again assume his
                            body; and, with spirit and body reunited, he shall be judged. <small>(<i>Articles
                                    of Faith</i>, p.385)</small></big></font><small> </small><br>

