                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>There
                        are three general resurrections revealed to man on the earth; one of
                        these is past, and the other two are future.</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>The
                        first general resurrection took place in connection with the
                        resurrection of Jesus Christ. This included the Saints and Prophets of
                        both hemispheres, from Adam down to John the Baptist; or, in other
                        words, those who died in Christ before his resurrection.</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>The
                        second will take place in a few years from the present time, and will
                        be immediately succeeded by the coming of Jesus Christ, in power and
                        great glory, with all his Saints and Angels. This resurrection will
                        include the Former and Latter-day Saints -- all those who have received
                        the Gospel since the former resurrection.</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>The
                        third and last resurrection will take place more than a thousand years
                        afterwards, and will embrace all the human family not included in the
                        former resurrections or translations.</big></font> </big></p>
                <div style="margin-left: 40px;"><big style="font-family: arial;"><font
                            size="+1"><big>After man is raised from the dead he will be judged
