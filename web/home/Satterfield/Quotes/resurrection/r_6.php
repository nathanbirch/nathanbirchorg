                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>Two
                        great resurrections await the inhabitants of the earth: one is the
                        first resurrection, the resurrection of life, the resurrection of the
                        just; the other is the second resurrection, the resurrection of
                        damnation, the resurrection of the unjust. (John 5:28-29; Rev. 20; D.
                        &amp; C. 76.) But even within these two separate resurrections, there
                        is an order in which the dead will come forth. Those being resurrected
                        with celestial bodies, whose destiny is to inherit a celestial kingdom,
                        will come forth in the morning of the first resurrection. Their graves
                        shall be opened and they shall be caught up to meet the Lord at his
                        Second Coming. They are Christ's, the firstfruits, and they shall
                        descend with him to reign as kings and priests during the millennial
                        era. (D. &amp; C. 29:13; 43:18; 76:50-70; 88:97-98; 1 Thess. 4:16-17;
                        Rev. 20:3-7.)</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>"And
                        after this another angel shall sound, which is the second trump; and
                        then cometh the redemption of those who are Christ's at his coming; who
                        have received their part in that prison which is prepared for them,
                        that they might receive the gospel, and be judged according to men in
                        the flesh." (D. &amp; C. 88:99.) This is the afternoon of the first
                        resurrection; it takes place after our Lord has ushered in the
                        millennium. Those coming forth at that time do so with terrestrial
                        bodies and are thus destined to inherit a terrestrial glory in
                        eternity. (D. &amp; C. 76:71-80.)</big></font> </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>At
                        the end of the millennium, the second resurrection begins. In the
                        forepart of this resurrection of the unjust those destined to come
                        forth will be "the spirits of men who are to be judged, and are found
                        under condemnation; And these are the rest of the dead; and they live
                        not again until the thousand years are ended, neither again, until the
                        end of the earth." (D. &amp; C. 88:100-101.) These are the ones who
                        have earned telestial bodies, who were wicked and carnal in mortality,
                        and who have suffered the wrath of God in hell "until the last
                        resurrection, until the Lord, even Christ the Lamb, shall have finished
                        his work." (D. &amp; C. 76:85.) Their final destiny is to inherit a
                        telestial glory. (D. &amp; C. 76:81-112.)</big></font> </big></p>
