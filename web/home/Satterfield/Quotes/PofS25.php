                <big><big></big></big>
                <p><big><big><font face="Arial"><strong>D&amp;C 131:1</strong></font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">In the
                        celestial glory there are three heavens or degrees.</font></big></big><br
                    wp="BR2">
                </p>
                <big><big></big></big>
                <p><big><big><font face="Arial"><strong>James E. Talmage</strong></font></big></big></p>
                <ul>
                    <big><big> </big></big><li><big><big><font face="Arial">The three
                            kingdoms of widely differing glories are severally organized on a plan
                            of gradation. The Telestial kingdom comprises subdivisions;
                            this also is the case, we are told, with the Celestial; and, by
                            analogy, we conclude that a similar condition prevails in the
                            Terrestrial. Thus the
                            innumerable degrees of merit amongst mankind are provided for in an
                            infinity of graded glories. The Celestial kingdom is supremely honored
                            by the personal ministrations of the Father and the Son. The
                            Terrestrial kingdom will be administered through the higher, without a
                            fulness of
                            glory. The Telestial is governed through the ministrations of the
                            Terrestrial, by "angels who are appointed to minister for them." (<em>Articles
                                of
                                Faith</em>, p. 409)</font></big></big></li>
                    <big><big> </big></big><li><big><big><font face="Arial">There is no
                            claim of universal forgiveness; no unwarranted glorification of Mercy
                            to the degrading or neglect of Justice; no thought that a
                            single sin of omission or of commission shall fail to leave its wound
                            or scar. In the great future there shall be found a place for every
                            soul,
                            whatever his grade of spiritual intelligence may be. "In my Father's
                            house are many mansions," (John 14:2), declared the Savior to his
                            apostles; and Paul adds, "There are also celestial bodies, and bodies
                            terrestrial; but the glory of the celestial is one, and the glory of
                            the
                            terrestrial is another. There is one glory of the sun and another glory
                            of the moon and another glory of the stars; for one star differeth from
                            another star in glory. So also is the resurrection of the dead," (I
                            Corinthians 15:40-42). The Latter-day Saints claim a revelation of the
                            present
                            dispensation as supplementing the scripture just quoted. From this
                            later scripture, (see D&amp;C, Sec. 76), we learn that there are three
                            well-defined degrees in the future state, with numerous, perhaps
                            numberless, gradations. . . . We hold that there is a wide difference
                            between
                            salvation and exaltation; that there are infinite gradations beyond the
                            grave as there are here, and as there were in the state preceding this.
                            (The Story and Philosophy of "Mormonism,", p.131)</font></big></big><big><big><br
                                wp="BR2">
                        </big></big></li>
                </ul>
                <p><big><big><font face="Arial"><strong>John A. Widtsoe</strong></font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">These
                        gradations in salvation may be innumerable, since all members of the
                        human family are different. The many gradations are however
                        reduced to three classes: (1) the celestial, the highest, as of the sun
                        in glory; (2) the terrestrial, the next, as of the moon; (3) the
                        telestial, the
                        lowest, as of the stars. (<em>Evidences and Reconciliations</em>, p.199)</font></big></big><br
                    wp="BR2">
                </p>
                <big><big></big></big>
                <p><big><big><font face="Arial"><strong>Bruce R. McConkie</strong></font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Rewards
                        granted individuals in eternity will vary between and within kingdoms.
                        Only those who are sealed in the new and everlasting covenant
                        of marriage and who thereafter keep the terms and conditions of that
                        covenant will attain the highest of three heavens within the celestial
                        kingdom. (D. &amp; C. 131:1-4.) Inhabitants of the telestial kingdom
                        will differ in glory among themselves "as one star differs from another
                        star in
                        glory." (D. &amp; C. 76:98; 1 Cor. 15:41.) Similar variations will
                        exist among inheritors of the terrestrial kingdom. (D. &amp; C.
                        76:71-79.) [<em>Mormon
                            Doctrine</em>, p.420]</font></big></big><br wp="BR2">
                </p>
