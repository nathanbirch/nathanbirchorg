                </p>
                <p><big><big><font face="Arial"><strong>James E. Talmage</strong></font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial"><em>Graded
                            Glories</em> -- That the privileges and glories of heaven are graded to
                        suit the various capacities of the blessed, is indicated in Christ's
                        teachings. To the apostles He said: "In my Father's house are many
                        mansions: if it were not so, I would have told you. I go to prepare a
                        place
                        for you. And if I go and prepare a place for you, I will come again,
                        and receive you unto myself; that where I am, there ye may be also."
                        [John
                        14:2-3]</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">This
                        utterance is supplemented by that of Paul, who speaks of graded
                        conditions in the resurrection as follows: "There are also celestial
                        bodies,
                        and bodies terrestrial: but the glory of the celestial is one, and the
                        glory of the terrestrial is another. There is one glory of the sun, and
                        another
                        glory of the moon, and another glory of the stars: for one star
                        differeth from another star in glory. So also is the resurrection of
                        the dead." [1 Cor.
                        15:40-42]</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">A fuller
                        knowledge of this subject has been imparted in the present
                        dispensation. From a revelation given in 1832 [D&amp;C 76] we learn
                        that three
                        great kingdoms or degrees of glory are established, known as the
                        Celestial, the Terrestrial, and the Telestial. Far below the last and
                        least of
                        these, is the state of eternal punishment prepared for the sons of
                        Perdition.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial"><em>The
                            Celestial Glory is provided for those who merit the highest honors of
                            heaven. In the revelation referred to, we read of them</em>: "They are
                        they
                        who received the testimony of Jesus, and believed on his name and were
                        baptized after the manner of his burial, being buried in the water in
                        his
                        name, and this according to the commandment which he has given -- That
                        by keeping the commandments they might be washed and cleansed
                        from all their sins, and receive the Holy Spirit by the laying on of
                        the hands of him who is ordained and sealed unto this power; And who
                        overcome by faith, and are sealed by the Holy Spirit of promise, which
                        the Father sheds forth upon all those who are just and true. They are
                        they
                        who are the church of the Firstborn. They are they into whose hands the
                        Father has given all things -- -They are they who are priests and
                        kings,
                        who have received of his fulness, and of his glory; And are priests of
                        the Most High, after the order of Melchizedek, which was after the
                        order of
                        Enoch, which was after the order of the Only Begotten Son. Wherefore,
                        as it is written, they are gods, even the sons of God -- Wherefore, all
                        things are theirs, whether life or death, or things present, or things
                        to come, all are theirs and they are Christ's, and Christ is God's. * *
                        * These
                        shall dwell in the presence of God and his Christ forever and ever.
                        These are they whom he shall bring with him, when he shall come in the
                        clouds of heaven to reign on the earth over his people. These are they
                        who shall have part in the first resurrection. These are they who shall
                        come forth in the resurrection of the just. * * * These are they who
                        are just men made perfect through Jesus the mediator of the new
                        covenant,
                        who wrought out this perfect atonement through the shedding of his own
                        blood. These are they whose bodies are celestial, whose glory is that
                        of the sun, even the glory of God, the highest of all, whose glory the
                        sun of the firmament is written of as being typical." [D&amp;C 76:50-70]</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial"><em>The
                            Terrestrial Glory</em> -- <em>This, the next lowest degree, will be
                            attained by many whose works do not merit the highest reward</em>. We
                        read of them:
                        "These are they who are of the terrestrial, whose glory differs from
                        that of the church of the Firstborn who have received the fulness of
                        the
                        Father, even as that of the moon differs from the sun in the firmament.
                        Behold, these are they who died without law; And also they who are the
                        spirits of men kept in prison, whom the Son visited, and preached the
                        gospel unto them, that they might be judged according to men in the
                        flesh;
                        Who received not the testimony of Jesus in the flesh, but afterwards
                        received it. These are they who are honorable men of the earth, who
                        were
                        blinded by the craftiness of men. These are they who receive of his
                        glory, but not of his fulness. These are they who receive of the
                        presence of
                        the Son, but not of the fulness of the Father. Wherefore they are
                        bodies terrestrial, and not bodies celestial, and differ in glory as
                        the moon
                        differs from the sun. These are they who are not valiant in the
                        testimony of Jesus; wherefore, they obtain not the crown over the
                        kingdom of our
                        God." [D&amp;C 76:71-79]</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial"><em>The
                            Telestial Glory</em> -- The revelation continues: "And again, we saw
                        the glory of the telestial, which glory is that of the lesser, even as
                        the glory of
                        the stars differs from that of the glory of the moon in the firmament.
                        These are they who received not the gospel of Christ, neither the
                        testimony
                        of Jesus. These are they who deny not the Holy Spirit. These are they
                        who are thrust down to hell. These are they who shall not be redeemed
                        from the devil until the last resurrection, until the Lord, even Christ
                        the Lamb, shall have finished his work." [D&amp;C 76:81-85] <em>We
                            learn further that
                            the inhabitants of this kingdom are to be graded among themselves,
                            comprising as they do the unenlightened among the varied opposing sects
                            and divisions of men, and sinners of many types, whose offenses are not
                            those of utter perdition</em>: "For as one star differs from another
                        star in
                        glory, even so differs one from another in glory in the telestial
                        world; For these are they who are of Paul, and of Apollos, and of
                        Cephas. These
                        are they who say they are some of one and some of another -- some of
                        Christ, and some of John, and some of Moses, and some of Elias, and
                        some of Esaias, and some of Isaiah, and some of Enoch; But received not
                        the gospel, neither the testimony of Jesus, neither the prophets,
                        neither the everlasting covenant." [D&amp;C 76:98-101] <em>Evidently a
                            considerable part of the human family will fail of all glory beyond
                            that of the
                            telestial kingdom, for we are told</em>: "But behold, and lo, we saw
                        the glory and the inhabitants of the telestial world, that they were as
                        innumerable
                        as the stars in the firmament of heaven, or as the sand upon the
                        seashore." [D&amp;C 76:109] <em>They are thus not wholly rejected;
                            their every merit
                            will be respected</em>. "For they shall be judged according to their
                        works, and every man shall receive according to his own works, his own
                        dominion,
                        in the mansions which are prepared; And they shall be servants of the
                        Most High, but where God and Christ dwell they cannot come, worlds
                        without end." [D&amp;C 76:111-112]</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial"><em>That
                            every soul shall find his place in the hereafter, that he shall be
                            judged and assigned according to what he is, is no less truly
                            scriptural than
                            reasonable. He shall inherit according to his capacity to receive,
                            enjoy, and utilize. This is made sublimely plain by revelation given in
                            1832, in
                            which we read</em>: "For he who is not able to abide the law of a
                        celestial kingdom cannot abide a celestial glory. And he who cannot
                        abide the law
                        of a terrestrial kingdom cannot abide a terrestrial glory. And he who
                        cannot abide the law of a telestial kingdom cannot abide a telestial
                        glory;
                        therefore he is not meet for a kingdom of glory. Therefore he must
                        abide a kingdom which is not a kingdom of glory." [D&amp;C 88:22-24] (<em>Articles
                            of
                            Faith</em>, pp.405; italics added not for emphasis but clarity in
                        reading)<strong></strong></font></big></big></p>
