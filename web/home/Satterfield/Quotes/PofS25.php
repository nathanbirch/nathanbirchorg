                <p style="margin-left: 40px;"><big><big><font face="Arial">This is the
                        plan of salvation. Jesus will never cease his work until all are
                        brought up to the enjoyment of a kingdom in the mansions of his Father,
                        where there are many kingdoms and many glories, to suit the works and
                        faithfulness of all men that have lived on the earth. Some will obey
                        the
                        celestial law and receive of its glory, some will abide the terrestrial
                        and some the telestial, and others will receive no glory. (<em>Discourses
                            of
                            Brigham Young</em>, p.56)</font></big></big><br wp="BR2">
                </p>
                <big><big></big></big>
                <p><big><big><font face="Arial"><strong>John Taylor</strong></font></big></big></p>
                <ul>
                    <big><big> </big></big><li><big><big><font face="Arial">God is
                            interested in the whole of the human family. He cannot take them all
                            into the celestial kingdom, for they are not all prepared to go
                            there, and you cannot prepare them and He cannot, because they have to
                            be governed by certain laws and certain principles and certain
                            feelings, and if they are not governed by these and will not be
                            governed by a celestial law, they are not prepared for a celestial
                            glory. There
                            are some that may be governed by a terrestrial law, and may be prepared
                            for a terrestrial glory, but not for a celestial glory. Still, they are
                            God's children, and He is doing the best by them He can. (<em>Journal
                                of Discourses</em>, 24:194-195)</font></big></big></li>
                    <big><big> </big></big><li><big><big><font face="Arial">We are told
                            that if we cannot abide the law of the celestial kingdom we cannot
                            inherit a celestial glory. Is not that doctrine? Yes. "But," says
                            one, "are not we all going into the celestial kingdom?" I think not,
                            unless we turn round and mend our ways very materially. It is only
                            those
                            who can abide a celestial glory and obey a celestial law that will be
                            prepared to enter a celestial kingdom. "Well," says another, "are the
                            others going to be burned up?" No. "Do you expect everybody to walk
                            according to this higher law?" No, I do not. And do I expect those that
                            do not are going into the celestial kingdom? No, I do not. "Well, where
                            will they go?" If they are tolerably good men and do not do anything
                            very bad, they will get into a terrestrial kingdom, and if there are
                            some that cannot abide a terrestrial law, they may get into a telestial
                            kingdom, or otherwise, as the case may be. (<em>The Gospel Kingdom</em>,
                            p.20)</font></big></big></li>
                    <big><big> </big></big><li><big><big><font face="Arial">As eternal
                            beings we all have to stand before him to be judged; and he has
                            provided different degrees of glory -- the celestial, the terrestrial,
                            and the telestial glories -- which are provide according to certain
                            unchangeable laws which cannot be controverted. What will he do with
                            them? For those who are ready to listen to him and be brought under the
                            influence of the Spirit of God, and be lead by the principles of
                            revelation and the light of heaven, and who are willing to yield
                            obedience to his commands at all times and carry out his purposed upon
                            the
                            earth and who are willing to abide a celestial law, he has prepared for
                            them a celestial glory, that they may be with him for ever and ever.
                            And what about the others? They are not prepared to go there any more
                            that lead is prepared to stand the same test as gold or silver; and
                            there they cannot go. And there is a great gulf between them. But he
                            [God] will do with them just as well as he can. A great many of these
                            people in the world, thousand and hundreds of millions of them, will be
                            a great deal better off through the interposition of the Almighty than
                            they have any id (<em>Journal of Discourses</em>, 20:116)</font></big></big><br
