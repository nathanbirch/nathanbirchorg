                <p><big><big><font face="Arial"><strong>Joseph Fielding Smith</strong></font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">It has been
                        asked if it is possible for one who inherits the telestial glory to
                        advance in time to the celestial glory?</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">The answer
                        to this question is, No!</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">The
                        scriptures are clear on this point. Speaking of those who go to the
                        telestial kingdom, the revelation says: "And they shall be servants of
                        the
                        Most High; but where God and Christ dwell they cannot come, worlds
                        without end."</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Notwithstanding
                        this statement, those who do not comprehend the word of the Lord argue
                        that while this is true, that they cannot go where God
                        is "worlds without end," yet in time they will get where God was, but
                        he will have gone on to other heights.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">This is
                        false reasoning, illogical, and creates mischief in making people think
                        they may procrastinate their repentance, but in course of time they
                        will reach exaltation in celestial glory.</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">Now let us
                        see how faulty this reasoning is. If in time those who enter the
                        telestial glory may progress till they reach the stage in which the
                        celestial is in now -- then they are in celestial glory, are they not,
                        even if the celestial has advanced? That being the case (I state this
                        for the
                        argument only, for it is not true), then they partake of all the
                        blessings which are now celestial. That means that they become gods,
                        have
                        exaltation, gain the fulness of the Father, and receive a continuation
                        of the "seeds forever." The Lord, however, has said that these
                        blessings,
                        which are celestial blessings, they may never have; they are barred
                        forever!</font></big></big></p>
                <p style="margin-left: 40px;"><big><big><font face="Arial">The
                        celestial and terrestrial and telestial glories, I have heard compared
                        to the wheels on a train. The second and third may, and will, reach the
                        place where the first was, but the first will have moved on and will
                        still be just the same distance in advance of them. This illustration
                        is not true!
                        The wheels do not run on the same track, and do not go in the same
                        direction. The terrestrial and the telestial are limited in their
                        powers of
                        advancement, worlds without end. (<em>Doctrines of Salvation</em>,
                        2:31-32)</font></big></big><br wp="BR2">
