                <p style="margin-left: 40px;"><big><big><font face="Arial">Paul
                        ascended into the third heavens, and he could understand the three
                        principal rounds of Jacob's ladder -- the telestial, the terrestrial,
                        and
                        the celestial glories or kingdoms, where Paul saw and heard things
                        which were not lawful for him to utter. I could explain a hundred fold
                        more
                        than I ever have of the glories of the kingdoms manifested to me in the
                        vision, were I permitted, and were the people prepared to receive them.
                        (<em>Teachings of the Prophet Joseph Smith</em>, p. 304)</font></big></big><br
                    wp="BR1">
                </p>
                <big><big><br wp="BR2">
                    </big></big>
                <p align="center"><big
                    style="color: rgb(0, 0, 153); font-style: italic;"><big><font
                            face="Arial" size="+1"><big><big><strong>In My Father's House <br>
                                    are Many Mansions<br wp="BR2">
                                </strong></big></big></font></big></big></p>
                <big><big></big></big>
                <p><big><big><font face="Arial"><strong>Brigham Young</strong></font></big></big></p>
                <ul>
                    <big><big> </big></big><li><big><big><font face="Arial">These words
                            set forth the fact to which Jesus referred when he said, "In my
                            Father's house are many mansions." How many I am not
                            prepared to say; but here are three distinctly spoken of: the
                            celestial, the highest; the terrestrial, the next below it; and the
                            telestial, the third.
                            (<em>Discourses of Brigham Young</em>, p.391)</font></big></big></li>
                    <big><big> </big></big><li><big><big><font face="Arial">We read in
                            the Bible that there is one glory of the sun, another glory of the
                            moon, and another glory of the stars. In the book of Doctrine and
                            Covenants, these glories are called telestial, terrestrial and
                            celestial, which is the highest. These are worlds, different
                            departments, or
                            mansions, in our Father's house. (<em>Discourses of Brigham Young</em>,
                            p.382) </font></big></big></li>
                    <big><big> </big></big><li><big><big><font face="Arial">How many
                            kingdoms there are has not been told to us; they are innumerable. (<em>Discourses
                                of Brigham Young</em>, p.382)</font></big></big></li>
                    <big><big> </big></big><li><big><big><font face="Arial">How many
                            glories and kingdoms will there be in eternity? You will see the same
                            variety in eternity as you see in the world. (<em>Discourses of
                                Brigham Young</em>, p.382)</font></big></big><br wp="BR2">
                    </li>
                    <big><big></big></big>
                </ul>
