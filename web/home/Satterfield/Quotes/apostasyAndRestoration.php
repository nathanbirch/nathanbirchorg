                            size="+2"><big>[After Bro. Marsh sat
                            down, Briham Young stood and said:</big></font><br>
                        <br>
                        <br>
                    </big>
                    <div style="text-align: left;"><big><font size="+1"><big><span
                                    style="font-weight: bold;">Brigham Young</span>: A portion of the
                                congregation have heard what brother Marsh has said; but he spoke so
                                low that you could not all hear.&nbsp; He wants to know whether this
