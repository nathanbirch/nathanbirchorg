                        <i><font size="+2"><big><small>The following are the <br>
                                    REMARKS BY THOMAS B. MARSH <br>
                                    he made to the Church in Salt Lake City in asking for <br>
                                    forgiveness of his sins against the Church.</small><br>
                            </big></font></i>
                    </big>
                    <p><big><span style="font-style: italic;">The Bowery, Salt Lake City</span><br>
                        Sunday, September 6th, 1857</big></p>
                    <big></big>
                    <p style="text-align: left; font-weight: bold;"><big><font
                            style="font-style: italic;" size="+1"><big>[In September of 1857,
                            Thomas B.
                            Marsh came to Salt Lake City seeking&nbsp; forgiveness from Church
                            leaders.&nbsp; President Brigham Young invited him to speak to those of
                            the Church.&nbsp; The following is a record of those proceedings.]</big></font><br>
                    </big></p>
                    <big></big>
                    <p style="text-align: left;"><big><font size="+1"><big><br>
                        </big></font></big></p>
                    <big></big>
                    <div style="text-align: center;"><big><font size="+2"><big>[Brigham
                                Young
                                introduced Thomas B. Marsh in the meeting with these words:]</big></font><br>
                        </big></div>
                    <big></big>
                    <p style="text-align: left;"><big><font size="+1"><big>Brother Thomas
                            B. Marsh,
                            formerly the President of the Quorum of the Twelve Apostles, has now
                            come to us, after an absence of nearly nineteen years.&nbsp; He is on
                            the stand to-day, and wishes to make a few remarks to the congregation.<br>
                            <br>
                            You will comprehend the purport of the remarks he wishes to make, by my
                            relating a part of his conversation with me yesterday.&nbsp; He came
                            into my office and wished to know whether I could be reconciled to him,
                            and whether there could be a reconciliation between himself and the
                            Church of the living God.&nbsp; He reflected for a moment and said, I
                            am reconciled to the Church, but I want to know whether the Church can
                            be reconciled to me.<br>
                            <br>
                            He is here, and I want him to say what he may wish to.&nbsp; [Brother
                            Marsh then arose, and the President continued.]&nbsp; Brethren and
                            sisters, I now introduce to you brother Thomas B. Marsh.&nbsp; When the
                            Quorum of the Twelve was first organized, he was appointed to be their
                            President.&nbsp; (</big></font><font size="+1"><big><span
                                style="font-style: italic;">Journal of Discourses</span>, Vol.5, p.207</big></font><font
                            size="+1"><big>)<br>
                        </big></font></big></p>
                    <big></big>
                    <p style="text-align: center;"><big><font size="+2"><big><br>
                            [The Remarks of Thomas B. Marsh:]</big></font><br>
                    </big></p>
                    <big></big></center>
                <p style="font-family: arial;"><big><font size="+1"><big>I do not know
                        that I can make all this vast
                        congregation
                        hear and understand me. My voice never was very strong, but it has been
                        very much weakened of late years by the afflicting rod of Jehovah. He
                        loved
                        me too much to let me go without whipping. I have seen the hand of the
                        Lord in the chastisement which I have received. I have seen and known
                        that
                        it has proved he loved me; for if he had not cared anything about me,
                        he
                        would not have taken me by the arm and given me such a shaking.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="+1"><big>If there are
                        any among this people who should ever
                        apostatize
                        and do as I have done, prepare your backs for a good whipping, if you
                        are
                        such as the Lord loves. But if you will take my advice, you will stand
                        by the authorities; but if you go away and the Lord loves you as much
                        as
                        he did me, he will whip you back again.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="+1"><big>Many have said
                        to me, "How is it that a man like
                        you,
                        who understood so much of the revelations of God as recorded in the
                        Book
                        of Doctrine and Covenants, should fall away?" I told them not to feel
                        too
                        secure, but to take heed lest they also should fall; for I had no
                        scruples
                        in my mind as to the possibility of men falling away.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="+1"><big>I can say, in
                        reference to the Quorum of the Twelve,
                        to
                        which I belonged, that I did not consider myself a whit behind any of
                        them,
                        and I suppose that others had the same opinion; but, let no one feel
                        too
                        secure: for, before you think of it, your steps will slide. You will
                        not
                        then think nor feel for a moment as you did before you lost the Spirit
                        of Christ; for when men apostatize, they are left to grovel in the dark.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="+1"><big>I have sought
                        diligently to know the Spirit of
                        Christ
                        since I turned my face Zionward, and I believe I have obtained it. I
                        have
                        frequently wanted to know how my apostacy began, and I have come to the
                        conclusion that I must have lost the Spirit of the Lord out of my heart.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="+1"><big>The next
                        question is, "How and when did you lose the
                        Spirit?"
                        I became jealous of the Prophet, and then I saw double, and overlooked
                        everything that was right, and spent all my time in looking for the
                        evil;
                        and then, when the Devil began to lead me, it was easy for the carnal
                        mind
                        to rise up, which is anger, jealousy, and wrath. I could feel it within
                        me; I felt angry and wrathful; and the Spirit of the Lord being gone,
                        as
                        the Scriptures say, I was blinded, and I thought I saw a beam in
                        brother
                        Joseph's eye, but it was nothing but a mote, and my own eye was filled
                        with the beam; but I thought I saw a beam in his, and I wanted to get
                        it
                        out; and, as brother Heber says, I got mad, and I wanted everybody else
                        to be mad. I talked with Brother Brigham and Brother Heber, and I
                        wanted
                        them to be mad like myself; and I saw they were not mad, and I got
                        madder
                        still because they were not. Brother Brigham, with a cautious look,
                        said,
                        "Are you the leader of the Church, brother Thomas?" I answered, "No."
                        "Well
                        then," said he, "Why do you not let that alone?"</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="+1"><big>Well, this is
                        about the amount of my hypocrisy--I
                        meddled
                        with that which was not my business. But let me tell you, my brethren
                        and
                        friends, if you do not want to suffer in body and mind, as I have
                        done,--if
                        there are any of you that have the seeds of apostacy in you, do not let
                        them make their appearance, but nip that spirit in the bud; for it is
                        misery
                        and affliction in this world, and destruction in the world to come. I
                        know
                        that I was a very stiff-necked man, and I felt, for the first four or
                        five
                        years especially, that I would never return to the Church; but towards
                        the latter part of the time, I began to wake up and to be sensible that
                        I was being chastised by the Almighty; and I felt to realize the
                        language
                        of Jeremiah concerning Ephraim in the last days, where he says, "Is
                        Ephraim
                        my dear son? Is he a pleasant child? For since I spake against him, I
                        do
                        earnestly remember him still; therefore my bowels are troubled for him;
                        I will surely have mercy on him, saith the Lord."</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="+1"><big>Thinks I, this
                        language suits my condition. I then
                        thought,
                        I will go back and see if the Lord will heal me, for I am of the seed
                        of
                        Ephraim, and I felt troubled from that day, and my soul was vexed with
                        the filthy conversation of those Sodomites. . . .</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="+1"><big>I have given
                        you some items of my apostasy. I will
                        now
                        relate some of my recent experience.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="+1"><big>When I got to
                        Florence, or Winter Quarters, where I
                        had
                        to stay, waiting for an opportunity to cross the Plains, I read many of
                        the publications and works of the Church, and became strengthened and
                        informed
                        in regard to the Priesthood of the Son of God. Although I knew
                        something
                        about the Priesthood before, so far as the theory was concerned, yet I
                        discovered that I had never properly understood it; and hence I feel
                        that
                        my faith is greatly strengthened. I wanted to get posted up and see
                        what
                        the "Mormons" had learned since I left them; and I learned very much by
                        reading the discourses that had been preached here.&nbsp; </big></font><font
                        size="+1"><big>(<i>Journal of Discourses</i>, Vol.5, p.207</big></font>
                    <font size="+1"><big>)<br>
                    </big></font></big></p>
                <p style="font-family: arial;"><big><font size="+1"><big><br>
                    </big></font></big></p>
