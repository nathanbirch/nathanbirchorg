                                requisite number of cows, they agreed to exchange milk.<br>
                                <br>
                                The wife of Thomas B. Marsh, who was then President of the Twelve
                                Apostles, and sister Harris concluded they would exchange milk, in
                                order to make a little larger cheese than they otherwise could.&nbsp;
                                To be sure to have justice done, it was agreed that they should not
                                save the strippings, but that the milk and strippings should all go
                                together.&nbsp; Small matters to talk about here, to be sure, two
                                women's exchanging milk to make cheese.<br>
                                <br>
                                Mrs. Harris, it appeared, was faithful to the agreement and carried to
                                Mrs. Marsh the milk and strippings, but Mrs. Marsh, wishing to make
                                some extra good cheese, saved a pint of strippings from each cow and
                                sent Mrs. Harris the milk without the strippings.<br>
                                <br>
                                Finally it leaked out that Mrs. Marsh had saved strippings, and it
                                became a matter to be settled by the Teachers.&nbsp; They began to
                                examine the matter, and it was proved that Mrs. Marsh had saved the
                                strippings, and consequently had wronged Mrs. harris out of that amount.<br>
                                <br>
                                An appeal was taken from the Teacher to the Bishop, and a regular
                                Church trial was had.&nbsp; President Marsh did not consider that the
                                Bishop had done him and his lady justice, for they decided that the
                                strippings were wrongfully saved, and that the woman had violated her
                                covenant.<br>
                                <br>
                                Marsh immediately took an appeal to the High Council, who investigated
                                the question with much patience, and I assure you they were a grave
                                body. Marsh being extremely anxious to maintain the character of his
