                                received into full fellowship, and to be again baptised here.<br>
                                &nbsp;<br>
                                There are many here who have formerly been acquainted with him--with
                                his moral character, and they can judge as well as myself.&nbsp; Those
                                who are not acquainted with him will be willing to coincide with the
                                judgment of those who once knew him.<br>
                                <br>
                                I shall call a vote, to ascertain whether the people are willing that
                                he should be baptised into the Church of Jesus Christ of Latter-day
                                Saints, and be acknowledged a member in full fellowship.&nbsp; I wish
                                those who are willing to receive brother Marsh into full fellowship as
                                a member in this Church and kingdom to manifest it by the uplifted
                                hand.&nbsp; [All hands appeared to be raised.]&nbsp; If there are any
                                who are not willing, they now have the privilege of manifesting it by
                                the uplifted hand. [Not a hand was raised.]<br>
                                <br>
                                Brother Marsh, I think that will be satisfactory to you.<br>
                                <br>
                                <span style="font-weight: bold;">T. B. Marsh</span>:&nbsp; "It is, and
                                I thank God for it."</big></font><font size="+1"><big>&nbsp;&nbsp;&nbsp;
                                (<span style="font-style: italic;">Journal of Discourses</span>, Vol.5,
                                p.210)<br>
                            </big></font></big></div>
                    <big></big></div>
                <p style="text-align: center; font-family: arial;"><big><font size="+1"><big><br>
                    </big></font></big></p>
                <div style="text-align: left; font-family: arial;"><big><font
                            style="font-weight: bold; font-style: italic;" size="+1"><big>[Though
                            Bro.
                            Marsh had been rebaptized in Florence, Nebraska, he was rebabptized
                            again in Salt Lake on the same day the Church members sustained him.]</big></font><br>
                    </big></div>
                <p style="text-align: center; font-family: arial;"><big><font size="+1"><big>___________________________________________<br>
                    </big></font></big></p>
                <big><br style="font-family: arial;">
                    <br style="font-family: arial;">
                </big>
                <div style="text-align: center; font-family: arial;"><big><small><font
                                size="+1"><font style="font-style: italic;" size="+2">The day after he
                            was voted back
                            into the Church <br>
                            Brother Marsh formally dedicated himself to the Lord:</font></font></small><br>
                    </big></div>
                <p style="font-family: arial;"><big><font size="+1"><big>I, Thomas B.
                        Marsh, do hereby, this day, Sept. 7th
                        AD 1857, consecrate and dedicate myself soul, body and spirit with all
                        I possess on earth, to the Lord praying to the God of Abraham Isaac and
                        Jacob to set me apart or sanctify me to be exclusively his to do
                        whatsoever he should require of me and to give me Grace to sanctify the
                        Lord of hosts in my heart that I might Love him with all my heart soul
                        mind, strength and understanding Amen.<br>
                    </big></font></big></p>
                <p style="text-align: right; font-family: arial;"><big>(Dedication of
                    Thomas B. Marsh 7
                    September 1857, Brigham Young Collection, Church Archives)<font
                        size="+1"><big><br>
                    </big></font></big></p>
                <p style="font-family: arial;"><big><font size="+1"><big><br>
                    </big></font></big></p>
                <p style="text-align: center; font-family: arial;"><big><font size="+1"><big>_____________________________________________<br>
                    </big></font></big></p>
                <p style="text-align: left; font-family: arial;"><big><font size="+1"><big><br>
                    </big></font></big></p>
                <div style="text-align: center; font-family: arial;"><big><font
                            style="font-style: italic;" size="+2"><big>The Conclusion to Life of
                            Thomas B. Marsh</big></font><br>
                        <br>
                    </big></div>
                <p style="text-align: left; font-family: arial;"><big><font size="+1"><big>One
                        month after his
                        arrival in Salt Lake Valley, Thomas was married to Hannah Adams (4
                        October 1857), and the couple soon settled in Spainish Fork, where they
                        acquired a small adobe house. Here Brother Marsh attempted, without
                        success, to establish a school. Though having received some financial
                        help from Bishop John L. Butler, the Marshes were scarcely able to
                        sustain life. By late fall of 1859, meager circumstances prompted
                        Thomas to request further assistance. His petition reflected his
                        penitent spirit:</big></font></big></p>
                <p style="text-align: justify; font-family: arial;"><big><font size="+1"><big>"[I
                        write] not in a
                        spirit of complaining of any person neither of murmuring against the
                        providence of my Heavenly Father; no! It is good enough for me for I
                        have sin[n]ed and made many crooked paths and I would rather kiss the
                        chastning hand and thank Him that it is as well with me as it is for He
                        in his providence has brought me to the Valies of these mountains! fed
                        and clothed me and kept me alive untill now, given me a name and place
                        among his people and restored me, vile as I have been, [to] His
                        Everlasting Priesthood, notwithstanding I so foolishly and so
                        ignominiously once threw it away and cast it behind my back."</big></font></big></p>
                <p style="text-align: left; font-family: arial;"><big><font size="+1"><big>His
                        request did not go
                        unheeded.<br>
                    </big></font></big></p>
                <p style="text-align: left; font-family: arial;"><big><font size="+1"><big>On
                        11 March 1859, Thomas
                        B. Marsh was re-ordained an elder, and by November 1861 he had been
                        ordained a high priest. In the Endowment House on 1 November 1862, he
                        received his endowment and was sealed to his wife, Hannah. It was about
                        this same time that the couple opted to settle near Ogden. Thomas was
                        placed in the care of David M. Stuart, Ogden First Ward. Though almost
                        wholly supported by the Church until his death in January 1866 at
                        Ogden, Thomas Baldwin Marsh "died in good faith," having once again
                        accepted the principles he had espoused nearly thirty-six years earlier
                        in Fayette, New York. He had learned by sad experience the hazards of
                        aspiring to the honors of men, the dangers of exercising unrighteous
                        dominion, and the consequences of uncontrolled criticism of those in
                        authority<br>
                    </big></font></big></p>
                <p style="font-family: arial;"><big>(Lyndon W. Cook, " 'I Have Sinned
                    Against Heaven, and Am Unworthy of
                    Your Confidence, But I Cannot Live without a Reconciliation' ": Thomas
                    B. Marsh Returns to the Church," <span style="font-style: italic;">BYU
                        Studies</span>,&nbsp; Vol. 20, No. 4<font size="+1"><big>, </big></font>p.400)<br>
                </big></p>
                <hr style="width: 100%; height: 2px;">
                <p style="font-family: arial;"><big><br>
                </big></p>
                <p style="text-align: center;"><big><big><font face="Arial,Helvetica"><font
                            size="+1"><big><big>President Gordon B. Hinckley</big></big></font></font></big><br>
                    <font face="Arial,Helvetica"><font size="+1"><big>&#8220;Small Acts Lead to
                        Great
                        Consequences,&#8221;</big></font></font>
                    <br>
                    <font face="Arial,Helvetica"><font size="+1"><big><i>Ensign</i>, May
                        1984,
                        pp. 81-83</big></font></font></big></p>
                <p><br>
                </p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>Now we are
                        ready to return
                        to our homes. We have been counseled by the Brethren and have been
                        strengthened
                        in our faith. As we are about to separate, I should like to emphasize
                        the
                        importance of watching the little things in our lives. Have you ever
                        noticed
                        a large gate in a farm fence? As you open it or close it there appears
                        to be very little movement at the hinge. But there is great movement at
                        the perimeter.</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>Speaking to
                        the Prophet Joseph
                        Smith in 1831, the Lord said: &#8220;Out of small things proceedeth that
                        which
                        is great.&#8221; (D&amp;C 64:33.) It is so with good or evil, my brothers and
                        sisters. Small, kind acts can grow into mammoth good institutions. The
                        Boy Scout movement is an example of this as is known by anyone
                        acquainted
                        with the history of this great institution. It is so likewise with evil
                        things. Small acts of dishonesty, small acts of an immoral nature,
                        small
                        outbursts of anger can grow into great and terrible things.</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>There stood
                        once on the grounds
                        right here, before ever this building was constructed, a bowery&#8212;a
                        rather
                        crude structure in which the Saints met in those days of their poverty.
                        In September of 1857, there was presented in that old bowery on a
                        Sunday
                        afternoon, what was really the concluding act of a drama of great
                        tragedy.</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>On that
                        Sunday Brigham Young
                        was conducting a meeting and introduced to the congregation a man who
                        appeared
                        to be old and infirm and weary of life.</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>Said
                        President Brigham Young
                        to the congregation:</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>&#8220;Brother
                        Thomas B. Marsh,
                        formerly the President of the Quorum of the Twelve Apostles, has now
                        come
                        to us, after an absence of nearly nineteen years. He is on the stand
                        to-day,
                        and wishes to make a few remarks to the congregation. &#8230;</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>&#8220;He came into
                        my office and
                        wished to know whether I could be reconciled to him, and whether there
                        could be a reconciliation between himself and the Church of the living
                        God. He reflected for a moment and said, I am reconciled to the Church,
                        but I want to know whether the Church can be reconciled to me.</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>&#8220;He is here,&#8221;
                        said President
                        Young, &#8220;and I want him to say what he may wish to. &#8230; Brethren and
                        sisters,
                        I now introduce to you Brother Thomas B. Marsh. When the Quorum of the
                        Twelve was first organized, he was appointed to be their President.&#8221;</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>Brother Marsh
                        rose to the
                        pulpit. This man, who was named the first President of the Council of
                        the
                        Twelve Apostles and to whom the Lord had spoken in so marvelous a
                        manner,
                        as recorded in section 112 of the Doctrine and Covenants&#8212;which I wish
                        you
                        would read&#8212;said to the people:</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>&#8220;I do not
                        know that I can
                        make all this vast congregation hear and understand me. My voice never
                        was very strong, but it has been very much weakened of late years by
                        the
                        afflicting rod of Jehovah. He loved me too much to let me go without
                        whipping.
                        I have seen the hand of the Lord in the chastisement which I have
                        received.
                        I have seen and known that it has [page 82] proved he loved me; for if
                        he had not cared anything about me, he would not have taken me by the
                        arm
                        and given me such a shaking.</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>&#8220;If there are
                        any among this
                        people who should ever apostatize and do as I have done, prepare your
                        backs
                        for a good whipping, if you are such as the Lord loves. But if you will
                        take my advice, you will stand by the authorities; but if you go away
                        and
                        the Lord loves you as much as he did me, he will whip you back again.</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>&#8220;Many have
                        said to me,&#8221; he
                        continued, &#8220; &#8216;How is it that a man like you, who understood so much of
                        the revelations of God as recorded in the Book of Doctrine and
                        Covenants,
                        should fall away?&#8217; I told them not to feel too secure, but to take heed
                        lest they also should fall; for I had no scruples in my mind as to the
                        possibility of men falling away.&#8221;</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>He continued,
                        &#8220;I can say,
                        in reference to the Quorum of the Twelve, to which I belonged, that I
                        did
                        not consider myself a whit behind any of them, and I suppose that
                        others
                        had the same opinion; but, let no one feel too secure; for, before you
                        think of it, your steps will slide. You will not then think nor feel
                        for
                        a moment as you did before you lost the Spirit of Christ; for when men
                        apostatize, they are left to grovel in the dark.&#8221; (<i>Journal of
                            Discourses</i>,
                        5:206.)</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>Speaking in a
                        voice that
                        was difficult to hear, and appearing as an old man when he was actually
                        only fifty-seven years of age, he spoke of the travails through which
                        he
                        had passed before he had finally made his way to the valley of the
                        Great
                        Salt Lake and asked that he might be baptized again into the Church.</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>I wondered,
                        as I read that
                        story so filled with pathos, what had brought him to this sorry state.
                        I discovered it, in the Journal of Discourses, in a talk given to the
                        [page
                        83] Saints in this same bowery the year before by George A. Smith. I
                        think,
                        if you&#8217;ll bear with me for a minute or two, it is worth the telling to
                        illustrate to all of us the need to be careful in dealing with small
                        matters
                        which can lead to great consequences.</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>According to
                        the account
                        given by George A. Smith, while the Saints were in Far West, Missouri,
                        &#8220;the wife of Thomas B. Marsh, who was then President of the Twelve
                        Apostles,
                        and Sister Harris concluded they would exchange milk, in order to make
                        a little larger cheese than they otherwise could. To be sure to have
                        justice
                        done, it was agreed that they should not save the strippings (to
                        themselves),
                        but that the milk and strippings should all go together.</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>Now for you
                        who have never
                        been around a cow, I should say that the strippings came at the end of
                        the milking and were richer in cream.</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>&#8220;Mrs. Harris,
                        it appeared,
                        was faithful to the agreement and carried to Mrs. Marsh the milk and
                        strippings,
                        but Mrs. Marsh, wishing to make some extra good cheese, saved a pint of
                        strippings from each cow and sent Mrs. Harris the milk without the
                        strippings.&#8221;</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>A quarrel
                        arose, and the
                        matter was referred to the home teachers. They found Mrs. Marsh guilty
                        of failure to keep her agreement. She and her husband were upset and,
                        &#8220;an
                        appeal was taken from the teacher to the bishop, and a regular Church
                        trial
                        was had.&#8221; President Marsh did not consider that the bishop had done him
                        and his lady justice for they (that is, the bishop&#8217;s court) decided
                        that
                        the strippings were wrongfully saved, and that the woman had violated
                        her
                        covenant.</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>&#8220;Marsh
                        immediately took an
                        appeal to the High Council, who investigated the question with much
                        patience,
                        and,&#8221; says George A. Smith, &#8220;I assure you they were a grave body. Marsh
                        being extremely anxious to maintain the character of his wife, &#8230; made a
                        desperate defence, but the High Council finally confirmed the bishop&#8217;s
                        decision.</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>&#8220;Marsh, not
                        being satisfied,
                        took an appeal to the First Presidency of the Church, and Joseph and
                        his
                        counselors had to sit upon the case, and they approved the decision of
                        the High Council.</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>&#8220;This little
                        affair,&#8221; Brother
                        Smith continues, &#8220;kicked up a considerable breeze, and Thomas B. Marsh
                        then declared that he would sustain the character of his wife even if
                        he
                        had to go to hell for it.</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>&#8220;The then
                        President of the
                        Twelve Apostles, the man who should have been the first to do justice
                        and
                        cause reparation to be made for wrong, committed by any member of the
                        family,
                        took that position, and what next? He went before a magistrate and
                        swore
                        that the &#8216;Mormons&#8217; were hostile towards the state of Missouri.</big></font></font>
                </big></p>
                <p><big><font face="Arial,Helvetica"><font size="+1"><big>&#8220;That
                        affidavit brought from
                        the government of Missouri an exterminating order, which drove some
                        15,000
                        Saints from their homes and habitations, and some thousands perished
                        through
                        suffering the exposure consequent on this state of affairs.&#8221; (<i>Journal
                            of Discourses</i>, 3:283&#8211;84.) Such is George A. Smith&#8217;s account.</big></font></font>
                </big></p>
                <big><font face="Arial,Helvetica"><font size="+1"><big>What a very
                        small and trivial
                        thing&#8212;a little cream over which two women quarreled. But it led to, or
                        at least was a factor in, Governor Boggs&#8217; cruel exterminating order
                        which
                        drove the Saints from the state of Missouri, with all of the terrible
                        suffering
                        and consequent death that followed. The man who should have settled
                        this
                        little quarrel, but who, rather, pursued it, troubling the officers of
                        the Church, right up to the Presidency, literally went through hell for
                        it. He lost his standing in the Church. He lost his testimony of the
                        gospel.
                        For nineteen years he walked in poverty and darkness and bitterness,
                        experiencing
                        illness, and loneliness. He grew old before his time. Finally, like the
                        prodigal son in the parable of the Savior (see Luke 15:11&#8211;32), he
                        recognized
                        his foolishness and painfully made his way to this valley, and asked
                        Brigham
                        Young to forgive him and permit his rebaptism into the Church. He had
                        been
                        the first President of the Council of the Twelve, loved, respected, and
                        honored in the days of Kirtland, and the early days of Far West. Now he
                        asked only that he might be ordained a deacon and become a doorkeeper
                        in
                        the house of the Lord.</big></font></font></big>
                <p><font size="+1"><br>
