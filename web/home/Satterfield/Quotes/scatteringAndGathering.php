                            of Jesus Christ, and yields obedience to it, in such a case I will give
                            you the words of the Prophet Joseph-- "When the Lord pours out the Holy
                            Ghost upon that individual he will have spasms, and you would think
                            that
                            he was going into fits."
                        </big></big><br style="font-family: arial;">
