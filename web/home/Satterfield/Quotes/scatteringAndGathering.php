                            you may find nine of them purely of the Gentile stock, and one son or
                            one
                            daughter in that family who is purely of the Blood of Ephraim. It was
                            in
                            the veins of the father or mother, and was reproduced in the son or
                            daughter,
                            while all the rest of the family are Gentiles. You may think that is
                            singular,
                            but it is true. It is the house of Israel we are after, and we care not
                            whether they come from the east, the west, the north, or the south;
                            from
                            China, Russia, England, California, North or South America, or some
                            other
                            locality; and it is the very lad on whom father Jacob laid his hands,
