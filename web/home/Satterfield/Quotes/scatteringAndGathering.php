                </div>
                <p style="font-family: arial; margin-left: 80px;"><big><big>When the
                        Lord called Abraham out of Ur, the land
                        of his fathers, he made certain covenants with him because of his
                        faithfulness.
                        One promise was that through him and his seed after him all nations of
                        the earth should be blessed. This blessing is accomplished in several
                        ways.
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 120px;"><big><big>1. Through
                        Jesus Christ who came through the lineage
                        of Abraham;
                        <br>
                        &nbsp;<br>
                        2. Through the priesthood which was conferred upon
                        Abraham and his descendants;
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 120px;"><big><big>3. <u>Through
                            the scattering of Israel among all
                            nations by which the blood of Israel was sprinkled among the nations,
                            and
                            thus the nations partake of the leaven of righteousness, on condition
                            of
                            their repentance, and are entitled to the promises made to the children
                            of Abraham</u>; and
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 120px;"><big><big>4. In the
                        fact that the Lord covenanted with Abraham
                        that after his time all who embraced the gospel should be called by his
                        name, or, should be numbered among his seed, and should receive the
                        Holy
                        Ghost. (<i>Doctrines of Salvation</i>, 3:246-247)
                    </big></big></p>
                <div style="text-align: left; margin-left: 40px;"><big
                        style="font-family: arial; color: rgb(51, 51, 153); font-weight: bold;"><big><i>Again,
                                he said:</i>
                        </big></big><br style="font-family: arial;">
                </div>
                <div style="margin-left: 80px;"><big style="font-family: arial;"><big>&nbsp;<br>
                            Let us consider the fulfillment of this great promise,
                            and see how the nations of the earth, through Abraham's seed have been
                            blessed.
                        </big></big><br style="font-family: arial;">
                    <big style="font-family: arial;"><big><br>
                            First, <u>the Lord always turns punishments to the
                                accomplishment of his purposes. The scattering of the Israelites among
                                all nations was a punishment inflicted upon them, but a great blessing
                                extended to the nations among whom they were scattered</u>. The word of
                            the Lord to Abraham in this regard, is much clearer as it has come to
                            us
                            in the writings of Abraham wherein he states:
                        </big></big><br style="font-family: arial;">
                    <big style="font-family: arial;"><big><br>
                            "And I will bless them that bless thee, and curse
                            them that curse thee; and in thee (that is, in thy Priesthood) and in
                            thy
                            seed (that is, thy Priesthood), for I give unto thee a promise that
                            this
                            right shall continue in thee, and in thy seed after thee (that is to
                            say,
                            the literal seed, or the seed of the body) shall all the families of
                            the
                            earth be blessed, even with the blessings of the gospel, which are the
                            blessings of salvation, even of life eternal." (Abraham 2:11)
                        </big></big><br style="font-family: arial;">
                    <big style="font-family: arial;"><big><br>
                            Again the Lord said:
                        </big></big><br style="font-family: arial;">
                    <big style="font-family: arial;"><big><br>
                            "And I will bless them through thy name; for, as
                            many as receive this gospel shall be called after thy name, and shall
                            be
                            accounted thy seed, and shall rise up and bless thee, as their father."
                            (Abraham 2:9)
                        </big></big><br style="font-family: arial;">
                    <big><big><span style="font-family: arial;"><br>
                            </span><u style="font-family: arial;">We see, then, how through the
                                scattering of Israel,
                                especially the descendants of the ten tribes who mingled with the
                                Gentile
                                nations, the blood of Abraham had been mixed with the blood of the
                                Gentiles,
                                and in this way the Gentiles have been brought into the seed of
                                Abraham,
                                and are therefore entitled to receive, on conditions of their
                                repentance,
                                all the blessings promised to the seed of Abraham</u><span
                                style="font-family: arial;">. The children of
                                Israel, even in their greatest number, never fulfilled the promise of
                                the
                                Lord concerning their magnitude when dwelling in the land of Palestine.
                                The prediction was that their number should be as countless as the
                                stars
                                or the sand upon the seashore. In Palestine they never reached
                                proportions
                                too great to be numbered nor have they reached this number in their
                                scattered
                                condition although they had become absorbed into the body of the
                                Gentile
                                nations.
                            </span><u style="font-family: arial;">Moreover, they, through this
                                scattering, planted in the hearts
                                of the Gentiles to some degree a desire to worship the God of Abraham
                                and
                                to accept of his teachings and the teachings of the prophets who came
                                through
                                his seed. Because the Jews rejected Jesus Christ they were scattered as
                                the Savior predicted; but the Lord has kept them, for his own purpose,
                                as a distinct people. They have not mixed to any great extent with the
                                Gentiles by marriage, but have maintained their racial identity</u><span
                                style="font-family: arial;">. And
                                when Christ comes, he will appear to the gathered Jews as predicted by
                                Zechariah. (</span><i style="font-family: arial;">The Restoration of
                                All Things</i><span style="font-family: arial;">, p.129-137)</span>
                        </big></big></div>
