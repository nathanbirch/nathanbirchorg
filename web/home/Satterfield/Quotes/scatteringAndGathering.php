                <p style="font-family: arial;"><big><big><br>
                        <i style="font-weight: bold; color: rgb(51, 51, 153);">Proper
                            understanding of the scattering and gathering
                            of Israel begins with the Abrahamic covenant. In the covenant, God
                            promises
                            that all the nations of the world will be blessed through Abraham's
                            posterity.
                            This will be accomplished through the scattering of Israel among the
                            nations
                            and then gathering them again. The following quotes will help in
                            understanding
                            this.</i>
                    </big></big></p>
