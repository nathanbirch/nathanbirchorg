                <p style="font-family: arial; margin-left: 80px;"><big><big>The
                        descendants of Abraham, the tribes of Israel,
                        became the chosen people of the Lord according to the promise. The Lord
                        honored
                        them, nourished them, watched over them with a jealous care, until they
                        became a great nation in the land the Lord had given to their fathers.
                        Notwithstanding this tender care and the instructions and warnings this
                        people received from time to time through their prophets, they failed
                        to
                        comprehend the goodness of the Lord and departed from him. Because of
                        their
                        rebellion they were driven out of their land and eventually were
                        scattered
                        among the nations. Their priesthood was lost and they were left in
                        spiritual
