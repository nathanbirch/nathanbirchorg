                                    they
                                    are lost from the knowledge of the world; they are not seen and
                                    recognized
                                    as the kingdom they once were; but in general terms, their whereabouts
                                    is known. They are scattered in all the nations of the earth, primarily
                                    in the nations north of the lands of their first inheritance. (</span><i
                                    style="font-family: arial;">A New
                                    Witness for the Articles of Faith</i><span style="font-family: arial;">,
