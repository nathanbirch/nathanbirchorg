                            </span></big><br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big><span
                            style="font-weight: bold;">Abraham 2:6-11</span></big></big><br>
                <big><big><br>
                    </big></big></p>
                <div style="margin-left: 80px;"><big style="font-family: arial;"><big>6
                            But I, Abraham, and Lot, my brother's son, prayed
                            unto the Lord, and the Lord appeared unto me, and said unto me: Arise,
                            and take Lot with thee; for I have purposed to take thee away out of
                            Haran,
                            and to make of thee a minister to bear my name in a strange land which
                            I will give unto thy seed after thee for an everlasting possession,
                            when
                            they hearken to my voice.
                        </big></big><br style="font-family: arial;">
                    <big style="font-family: arial;"><big>&nbsp;</big></big><br
                        style="font-family: arial;">
                    <big style="font-family: arial;"><big>7 For I am the Lord thy God; I
                            dwell in heaven;
                            the earth is my footstool; I stretch my hand over the sea, and it obeys
                            my voice; I cause the wind and the fire to be my chariot; I say to the
                            mountains--Depart hence--and behold, they are taken away by a
                            whirlwind,
                            in an instant, suddenly.
                        </big></big><br>
                </div>
