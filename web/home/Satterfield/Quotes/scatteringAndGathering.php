                            Will we go to the Gentile nations to preach the
                            Gospel? Yes, and gather out the Israelites, wherever they are mixed
                            among
                            the nations of the earth. What part or portion of them? The same part
                            or
                            portion that redeemed the house of Jacob, and saved them from perishing
                            with famine in Egypt. When Jacob blessed the two sons of Joseph,
                            "guiding
                            his hands wittingly," he placed his right hand upon Ephraim, "and he
                            blessed
                            Joseph, and said, God, before whom my fathers Abraham and Isaac did
                            walk,
                            the God which fed me all my life long unto this day, the Angel which
