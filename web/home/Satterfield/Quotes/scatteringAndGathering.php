                            the days of my preaching, and that is the blood which ran in my veins
                            when
                            I embraced the Gospel. If there are any of the other tribes of Israel
                            mixed
                            with the Gentiles we are also searching for them. Though the Gentiles
                            are
                            cut off, do not suppose that we are not going to preach the Gospel
                            among
                            the Gentile nations, for they are mingled with the house of Israel, and
                            when we send to the nations we do not seek for the Gentiles, because
                            they
                            are disobedient and rebellious. We want the blood of Jacob, and that of
                            his father Isaac and Abraham, which runs in the veins of the people.
                            There
