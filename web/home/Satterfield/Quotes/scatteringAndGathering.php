                            our hands upon them that they may receive the Holy Ghost, and the Lord
                            will make them of the house of Israel. They will be broken off from the
                            wild olive tree, and be grafted into the good and tame olive tree, and
                            will partake of its sap and fatness. If you take a bud and inoculate it
                            into another tree it ceases to receive nourishment from its original
                            stock;
                            it must, however, receive nourishment, or it will die. Where must it
                            receive
                            its nourishment from? From the tree into which it has been introduced;
                            it is supported by it, and becomes incorporated with it.
                        </big></big><br style="font-family: arial;">
                    <big style="font-family: arial;"><big><br>
                            It is so with the House of Israel and the Gentile
                            nations; if the Gentiles are grafted into the good olive tree they will
                            partake of its root and fatness.
