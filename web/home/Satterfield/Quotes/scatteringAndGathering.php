                        stakes
                        of Zion in their own lands and among their own people. But with the Ten
                        Tribes, in part at least, it will be another thing. They are destined
                        to
                        return (at least in large and representative numbers) to the same soil
                        where the feet of their forebears walked during the days of their
                        mortal
                        pilgrimage. They are to return to Palestine. At least a constituent
                        assembly
                        will congregate there in the very land given of God to Abraham their
                        father.
                        Others will, of course, be in America and in all lands, but the formal
                        return, the return from the north countries, will be to the land of
                        their
                        ancient inheritance. . . .
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>It is
                        clear; it is plain; it is certain: God gave
                        ancient Canaan to Abraham, Isaac, and Jacob, and the twelve tribes of
                        Israel,
                        of whom the Ten Tribes are the dominant part. It is their land, in time
                        and in eternity. It is their land now whenever they are worthy to tread
                        its blessed surface. And it shall be theirs again in that everlasting
                        eternity
                        that lies ahead. "It is decreed that the poor and the meek of the earth
                        shall inherit it," in that celestial day when it shall be crowned with
                        the presence of God, even the Father. (D&amp;C 88:17-19.) Where else,
                        then,
                        would we expect to see the Ten Tribes return? Where else would we
                        expect
                        them to assemble to worship the God of their fathers and to be
                        inheritors
                        of the promises made to the ancient ones whose seed they are?
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>Thus it is
                        that we see why the revelations speak
                        of the gathering of Israel from the four parts of the earth into the
                        Church
                        and kingdom of God on earth, and also of the leading of the Ten Tribes
                        from the land of the north back again to their promised Canaan. The
                        gathering
                        of Israel is one thing, the return of the Ten Tribes to a specified
                        place
                        is another; and Moses gave to men in our day the keys and power to
                        perform
                        both labors. This means that Israel is gathered at the direction and
                        pursuant
                        to the power and authority vested in the legal administrators who
                        preside
                        over The Church of Jesus Christ of Latter-day Saints. And it also means
                        that the Ten Tribes -- scattered, lost, unknown, and now in all the
                        nations
                        of the earth -- these Ten Tribes, with their prophets, with their
                        scriptures,
                        in faith and desiring righteousness, shall return to blessed Canaan at
                        the direction of these same legal administrators. The President of the
                        Church is the only person on earth at any given time who does or can
                        exercise
                        these or any other priesthood keys in their eternal fulness. He will
                        direct
                        the return of the Ten Tribes. It will not come to pass in any other
                        way.
                        . . .
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>We do not
                        say that occasional blood descendants
                        of Reuben or Naphtali or others of the other tribal heads shall not
                        return
                        to their Palestinian Zion, or assemble in an American Zion, or find
                        their
                        way into the stakes of Zion in all nations, all before the Second
                        Coming
                        of Christ. Some shall no doubt return to Canaan as true believers and
                        members
                        of the true Church, with the intent and purpose of fulfilling the
                        scriptures
                        and building up the ancient cities of Israel. This may well happen in
                        some
                        small measure, and to it there can be no objection. Great movements
                        have
                        small beginnings, and floods that sweep forth from bursting dams are
                        first
                        forecast when small rivulets trickle from the pent-up reservoirs. But
                        we
                        do say that the great day of the return of the Ten Tribes, the day when
                        the assembling hosts shall fulfill the prophetic promises, shall come
                        after
                        our Lord's return. . . .
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>In that
                        day, "They who are in the north countries
                        shall come in remembrance before the Lord." The lost tribes of Israel,
                        the Ten Tribes whose members are scattered among the northern nations,
                        shall be remembered. The set time to favor them will have dawned. The
                        work
                        of the Father will then commence among them; converts will be made;
                        they
                        will believe the everlasting gospel tan to them by the elders of
                        Ephraim;
                        and they will be baptized and receive the gift of the Holy Ghost. They
                        will become again the Lord's people. "And their prophets shall hear his
                        voice, and shall no longer stay themselves."
                        <br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big>Their
                        prophets! Who are they? Are they to be holy
                        men called from some unknown place and people? Are they prophets
                        unbeknown
                        to the presiding officers of "the only true and living church upon the
                        face of the whole earth"? (D&amp;C 1:30.) Perish the thought! The
                        President
                        of the Church, who holds the keys to lead the Ten Tribes from the
                        nations
                        of the north wherein they now reside, holds also the keys of salvation
                        for all men. There are not two true churches on earth, only one; there
                        are not two gospels or two be plans of salvation, only one; there are
                        not
                        two competing organizations, both having divine approval, only one. "Is
                        Christ divided?" (1 Cor. 1:13.) God forbid. Their prophets are members
                        of The Church of Jesus Christ of Latter-day Saints. They are stake
                        presidents
                        and bishops and quorum presidents who are appointed to guide and direct
                        the destinies of their stakes and wards and quorums. (Bruce R.
                        McConkie,
                        <i>The
                            Millennial Messiah</i>, pp. 319-326)<br>
                    </big></big></p>
                <p style="font-family: arial; margin-left: 80px;"><big><big><br>
                    </big></big></p>
