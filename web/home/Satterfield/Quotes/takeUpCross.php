                                emerge from a consciously chosen way of life, one in which we deny
                                ourselves of all ungodliness and we take up the cross daily&#8212;not
                                occasionally, not weekly, not monthly. If we are thus determined, then
                                we are emulating yet another quality of our Lord, of whom we read, &#8220;And
