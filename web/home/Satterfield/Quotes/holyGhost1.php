                <p style="font-family: arial; margin-left: 40px;"><big><big><strong></strong>In
                        our day
                        the Book of Mormon is the primary source to which we should turn for
                        help in learning how to invite the constant companionship of the Holy
                        Ghost.
                        The description in the Book of Mormon of the Liahona, the director or
                        compass used by Lehi and his family in their journey in the wilderness,
                        specifically was
                        included in the record as a type and a shadow for our day and as an
                        essential lesson about what we should do to enjoy the blessings of the
                        Holy Ghost.</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>As we
                        strive to align our
                        attitudes and actions with righteousness, then the Holy Ghost becomes
                        for us today what the Liahona was for Lehi and his family in
                        their day. The very factors that caused the Liahona to work for Lehi
                        will likewise invite the Holy Ghost into our lives. And the very
                        factors that caused the
                        Liahona not to work anciently will likewise cause us to withdraw
                        ourselves from the Holy Ghost today.</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big><strong></strong>As
                        we study
                        and ponder the purposes of the Liahona and the principles by which it
                        operated, I testify that we will receive inspiration suited to our
                        individual
                        and family circumstances and needs. We can and will be blessed with
                        ongoing direction from the Holy Ghost.</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>The Liahona
                        was prepared by
                        the Lord and given to Lehi and his family after they left Jerusalem and
                        were traveling in the wilderness (see Alma 37:38; D&amp;C
                        17:1). This compass or director pointed the way that Lehi and his
                        caravan should go (see 1 Nephi 16:10), even "a straight course to the
                        promised land" (Alma
                        37:44). The pointers in the Liahona operated "according to the faith
                        and diligence and heed" (1 Nephi 16:28) of the travelers and failed to
                        work when family
                        members were contentious, rude, slothful, or forgetful (see 1 Nephi
                        18:12, 21; Alma 37:41, 43).</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>The compass
                        also provided a
                        means whereby Lehi and his family could obtain greater "understanding
                        concerning the ways of the Lord" (1 Nephi 16:29). Thus,
                        the primary purposes of the Liahona were to provide both direction and
                        instruction during a long and demanding journey. The director was a
                        physical
                        instrument that served as an outward indicator of their inner spiritual
                        standing before God. It worked according to the principles of faith and
                        diligence.</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>Just as
                        Lehi was blessed in
                        ancient times, each of usin this day has been given a spiritual compass
                        that can direct and instruct us during our mortal journey. The
                        Holy Ghost was conferred upon you and me as we came out of the world
                        and into the Savior's Church through baptism and confirmation. By the
                        authority of
                        the holy priesthood we were confirmed as members of the Church and
                        admonished to seek for the constant companionship of "the Spirit of
                        truth; whom the
                        world cannot receive, because it seeth him not, neither knoweth him:
                        but ye know him; for he dwelleth with you, and shall be in you" (John
                        14:17).</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>As we each
                        press forward along
                        the pathway of life, we receive direction from the Holy Ghost just as
                        Lehi was directed through the Liahona. "For behold, again
                        I say unto you that if ye will enter in by the way, and receive the
                        Holy Ghost, it will show unto you all things what ye should do" (2
                        Nephi 32:5).</big></big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><big>The Holy
                        Ghost operates in our
                        lives precisely as the Liahona did for Lehi and his family, according
                        to our faith and diligence and heed.</big></big></p>
