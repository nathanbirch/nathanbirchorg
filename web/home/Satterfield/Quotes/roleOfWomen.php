                                has restored to earth the authority for entering into that covenant, and
                                has declared that it is the only due and proper way of joining husband
                                and wife</span>, and the only means by which the sacred family relationship
                            may be carried beyond the grave and through eternity.<span STYLE="text-decoration: underline">He
