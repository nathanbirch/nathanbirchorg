                        <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>Alma
                                also taught that in the resurrection "all things shall be restored to
                                their proper and perfect frame" (Alma 40:23).</big></font> </big></p>
                        <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>Many
                                living witnesses can testify to the literal fulfillment of these
                                scriptural assurances of the resurrection. Many, including some in my
                                own extended family, have seen a departed loved one in vision or
                                personal appearance and have witnessed their restoration in "proper and
                                perfect frame" in the prime of life. Whether these were manifestations
                                of persons already resurrected or of righteous spirits awaiting an
                                assured resurrection, the reality and nature of the resurrection of
                                mortals is evident. <span style="font-style: italic;">What a comfort
                                    to know that all who have been disadvantaged in life from birth
                                    defects, from mortal injuries, from disease, or from the natural
                                    deterioration of old age will be resurrected in "proper and perfect
                                    frame."</span></big></font>&nbsp; ...</big></p>
                        <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>The
                                "lively hope" we are given by the resurrection is our conviction that
                                death is not the conclusion of our identity but merely a necessary step
                                in the destined transition from mortality to immortality. This hope
                                changes the whole perspective of mortal life. <span
                                    style="font-style: italic;">The assurance of resurrection and
                                    immortality affects how we look on the physical challenges of
                                    mortality, how we live our mortal lives, and how we relate to those
                                    around us.</span></big></font> </big></p>
                        <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>The
                                assurance of resurrection gives us the strength and perspective to
                                endure the mortal challenges faced by each of us and by those we love, <span
                                    style="font-style: italic;">such things as the physical, mental, or
                                    emotional deficiencies we bring with us at birth or acquire during
                                    mortal life. Because of the resurrection, we know that these mortal
                                    deficiencies are only temporary!</span></big></font> </big></p>
                        <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big><span
                                    style="font-style: italic;">The assurance of resurrection also gives
                                    us a powerful incentive to keep the commandments of God during our
                                    mortal lives</span>. Resurrection is much more than merely reuniting a
                                spirit to a body held captive by the grave. <i
                                    style="font-style: italic;">We know from the Book of Mormon that the
                                    resurrection is a restoration</i><span style="font-style: italic;">
                                    that brings back "carnal for carnal" and "good for that which is good" </span>(Alma
                                41:13; see also Alma 41:2-4 and Hel. 14:31). The prophet Amulek taught,
                                "That same spirit which doth possess your bodies at the time that ye go
                                out of this life, that same spirit will have power to possess your body
                                in that eternal world" (Alma 34:34). As a result, when persons leave
                                this life and go on to the next, "they who are righteous shall be
                                righteous still" (2 Ne. 9:16), and "whatever principle of intelligence
                                we attain unto in this life &#8230; will rise with us in the resurrection"
                                (D&amp;C 130:18).</big></font> </big></p>
                        <p style="font-family: arial; margin-left: 40px;"><big><font size="+1"><big>The
                                principle of restoration also means that persons who are not righteous
                                in mortal life will not rise up righteous in the resurrection (see 2
                                Ne. 9:16; 1 Cor. 15:35-44; D&amp;C 88:27-32). <span
                                    style="font-style: italic;">Moreover, unless our mortal sins have been
                                    cleansed and blotted out by repentance and forgiveness</span> (see Alma
                                5:21; 2 Ne. 9:45-46; D&amp;C 58:42), <span style="font-style: italic;">we
                                    will be resurrected with a "bright recollection"</span> (Alma 11:43) <span
                                    style="font-style: italic;">and a "perfect knowledge of all of our
                                    guilt, and our uncleanness"</span> (2 Ne. 9:14; see also Alma 5:18).
                                The seriousness of that reality is emphasized by the many scriptures
                                suggesting that the resurrection is followed immediately by the Final
                                Judgment (see 2 Ne. 9:15, 22; Mosiah 26:25; Alma 11:43-44; Alma 42:23;
                                Morm. 7:6; Morm. 9:13-14). Truly, "this life is the time for men to
                                prepare to meet God" (Alma 34:32).&nbsp;&nbsp;<small> ["Resurrection," </small></big></font><i>Ensign</i>,
                            May 2000, pp. 14-16; emphasis added]</big></p>
