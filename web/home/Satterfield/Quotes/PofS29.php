                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>TWO
                    CLASSES OF SONS OF PERDITION. The great punishment received by these
                    rebellious spirits is that
                    they are to remain without bodies eternally and are denied the
                    redemption through the atonement of Jesus Christ.
                    They are banished forever from the presence of God because they have
                    lost the power of repentance, for they chose
                    evil by choice after having had the light. While dwelling in the
                    presence of God they knowingly entered into their
                    rebellion. Their mission on earth is to attempt to destroy the souls of
                    men and make them miserable as they
                    themselves are miserable. These spirits are known as sons of perdition.</font></p>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>There
                    is another class of sons of perdition. This class is composed of all
                    those who have known the power of God
                    in this mortal life and then, having full knowledge of the power and
                    purposes of God, rebel against him, putting Jesus
                    Christ to open shame.</font></p>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>The
                    Lord has defined this class as follows: “Thus saith the Lord concerning
                    all those who know my power, and
                    have been made partakers thereof, and suffered themselves through the
                    power of the devil to be overcome, and to
                    deny the truth and defy my power -- They are they who are the sons of
                    perdition, of whom I say that it had been better
                    for them never to have been born; For they are vessels of wrath, doomed
                    to suffer the wrath of God, with the devil
                    and his angels in eternity; Concerning whom I have said there is no
                    forgiveness in this world nor in the world to come
                    -- Having denied the Holy Spirit after having received it, and having
                    denied the Only Begotten Son of the Father,
                    having crucified him unto themselves and put him to an open shame.</font></p>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>“These
                    are they who shall go away into the lake of fire and brimstone, with
                    the devil and his angels -- And the
                    only ones on whom the second death shall have any power; Yea, verily,
                    the only ones who shall not be redeemed in
                    the due time of the Lord, after the sufferings of his wrath.”</font></p>
                <div style="margin-left: 40px; font-family: arial;"><br>
                    <font style="font-family: arial;" size="+2"></font></div>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>PUNISHMENT
                    OF SONS OF PERDITION. The extent of this punishment none will ever know
                    except those
                    who partake of it. That it is the most severe punishment that can be
                    meted out to man is apparent. Outer darkness is
                    something which cannot be described, except that we know that it is to
                    be placed beyond the benign and comforting
                    influence of the Spirit of God -- banished entirely from his presence.</font></p>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>This
                    extreme punishment will not be given to any but the sons of perdition.
                    Even the wicked of the earth who
                    never knew the power of God, after they have paid the price of their
                    sinning -- for they must suffer the excruciating
                    torment which sin will bring -- shall at last come forth from the
                    prison house, repentant and willing to bow the knee
                    and acknowledge Christ, to receive some influence of the Spirit of God
                    in the telestial kingdom.</font></p>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>“For
                    they shall be judged according to their works, and every man shall
                    receive according to his own works, his
                    own dominion, in the mansions which are prepared; And they shall be
                    servants of the Most High; but where God and
                    Christ dwell they cannot come, worlds without end.”</font></p>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>With
                    the sons of perdition, however, even this blessing is denied. They have
                    wilfully made themselves servants
                    of Satan and servants to him shall they remain forever. They place
                    themselves beyond the power of repentance and
                    beyond the mercies of God. (<i>Doctrines of Salvation</i>, 2:219-221)</font></p>
                <font style="font-family: arial;" size="+2"><br>
                <br>
                </font>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span
                        style="font-weight: bold;">Spencer W. Kimball<br>
                        <br>
                    </span></font></p>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>Those
                    who followed Lucifer in his rebellion in the premortal life and those
                    who in mortality sin against the Holy
                    Ghost are sons of perdition. The ex-mortal sons of perdition will be
                    resurrected, as will everyone else; but they will
                    finally suffer the second death, the spiritual death, for “the are cut
                    off again as to things pertaining to righteousness.”
                    (Hel. 14:18.)</font></p>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>In
                    the days of the restoration there apparently were those who taught that
                    the devil and his angels and the sons
                    of perdition should sometime be restored. The Prophet Joseph Smith
                    would not countenance the teaching of this
                    doctrine, and sanctioned the decision of the bishop that any who taught
                    it should be barred from communion. </font></p>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>In
                    the realms of perdition or the kingdom of darkness, where there is no
                    light, Satan and the unembodied spirits
                    of the pre-existence shall dwell together with those of mortality who
                    retrogress to the level of perdition. These have
                    lost the power of regeneration. They have sunk so low as to have lost
                    the inclinations and ability to repent,
                    consequently the gospel plan is useless to them as an agent of growth
                    and development.</font></p>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>[Quotes
                    D&amp;C 88:24]</font></p>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>[Quotes
                    D&amp;C 76:31-38]</font></p>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>The
                    Prophet Joseph Smith gives us this further picture:</font></p>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>“.
                    . . Those who commit the unpardonable sin are doomed to Gnolom to dwell
                    in hell, worlds without end. As
                    they concocted scenes of bloodshed in this world, so they shall rise to
                    that resurrection which is as the lake of fire
                    and brimstone. Some shall rise to the everlasting burnings of God; for
                    God dwells in everlasting burnings and some
                    shall rise to the damnation of their own filthiness, which is as
                    exquisite a torment as the lake of fire and brimstone.” </font></p>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>Speculation
                    as to individual sons of perdition is at best unprofitable. Some have
                    consigned Judas Iscariot to this
                    doom, based on certain scriptural passages. (See John 12:6; 6:70;
                    17:12; Acts 1:20.) President Joseph F. Smith
                    questions this interpretation:</font></p>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>“To
                    my mind it strongly appears that not one of the disciples possessed
                    sufficient light, knowledge nor wisdom,
                    at the time of the crucifixion, for either exaltation or condemnation;
                    for it was afterward that their minds were opened
                    to understand the scriptures, and that they were endowed with power
                    from on high; without which they were only
                    children in knowledge, in comparison to what they afterwards became
                    under the influence of the Spirit.” (<i>The Miracle
                        of Forgiveness</i>, pp.125-127)</font></p>
                <font style="font-family: arial;" size="+2"><br>
                </font>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span
                        style="font-weight: bold;">James E. Talmage</span></font></p>
                <p
                    style="text-align: justify; text-indent: -0.25in; font-family: arial; margin-left: 0.6634in;"><font
                        size="+2">•<span>&nbsp; </span><span style="font-family: arial;">Far
                        below the
                        lowest of these kingdoms of glory is the fate or state decreed for the
                        souls who have sinned in the
                        full light of knowledge and with conscious guilt, those who having
                        received the testimony of Christ have
                        ruthlessly and wantonly denied it in the interest of temporary gain or
                        gratification, who have fallen so far in
                        transgression as to be known by the awful name “sons of perdition,” for
                        whom no forgiveness is promised. (See
                        Doctrine &amp; Covenants, 76:32-38). [</span><i
                        style="font-family: arial;">The Vitality of Mormonism</i><span
                        style="font-family: arial;">,
                        p.68]</span></font></p>
                <p
                    style="text-indent: -0.25in; margin-left: 0.6634in; font-family: arial;"><font
                        size="+2">&nbsp;</font></p>
                <p
                    style="text-align: justify; text-indent: -0.25in; margin-left: 0.6634in; font-family: arial;"><font
                        size="+2">•<span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>The Sons of
                    Perdition – We learn of another class of souls whose sins are such as
                    to place them beyond the
                    present possibility of repentance and salvation. These are called sons
                    of Perdition, children of the fallen angel
                    who was once a Son of the Morning, Lucifer, now Satan, or Perdition.
                    These are they who have violated truth
                    in the light of knowledge; who, having received the testimony of
                    Christ, and having been endowed by the Holy
                    Spirit, then deny the same and defy the power of God, crucifying the
                    Lord afresh and putting Him to an open
                    shame. This, the unpardonable sin, can be committed by those only who
                    have received knowledge and conviction
                    of the truth, against which they then rebel. Their sin is comparable to
                    the treason of Lucifer, by which he sought
                    to usurp the power and glory of his God. Concerning them and their
                    dreadful fate, the Lord has said: “They are
                    they who are the sons of perdition, of whom I say that it had been
                    better for them never to have been born; For
                    they are vessels of wrath, doomed to suffer the wrath of God, with the
                    devil and his angels in eternity;
                    Concerning whom I have said there is no forgiveness in this world nor
                    in the world to come * * * And the only
                    ones on whom the second death shall have any power; * * * they shall go
                    away into everlasting punishment,
                    which is endless punishment, which is eternal punishment, to reign with
                    the devil and his angels in eternity,
                    where their worm dieth not, and the fire is not quenched, which is
                    their torment -- And the end thereof, neither
                    the place thereof, nor their torment, no man knows; Neither was it
                    revealed, neither is, neither will be revealed
                    unto man, except to them who are made partakers thereof; Nevertheless
                    I, the Lord, show it by vision unto many,
                    but straightway shut it up again; Wherefore, the end, the width, the
                    height, the depth, and the misery thereof, they
                    understand not, neither any man except those who are ordained unto this
                    condemnation.”</font></p>
                <p
                    style="text-align: justify; margin-left: 0.6634in; font-family: arial;"><font
                        size="+2">The
                    doctrines of the Church are explicit in defining the relationship
                    between the mortal probation and the
                    future state, and in teaching individual accountability and the free
                    agency of man. The Church affirms that in
                    view of the responsibility under which every man rests, as the director
                    of his own course, he must be and is free
                    to choose in all things, from the life that leads to the celestial home
                    to the career that is but the introduction to
                    the miseries of perdition. Freedom to worship, or to refuse to worship,
                    is a God-given right, and every soul must
                    abide the result of his choice. (<i>Articles of Faith</i>, pp.410-411)</font></p>
                <font style="font-family: arial;" size="+2"><br>
                </font>
                <p style="text-align: justify; font-family: arial;"><font size="+2"><span
                        style="font-weight: bold;">John A. Widstoe<br>
                        <br>
                    </span></font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span></span><span style="font-weight: bold;">Who Are The
                        Sons Of Perdition? </span>The
                    name Perdition was given to Lucifer, a son of the morning. He refused
                    to accept the plan proposed by God the Father, for the salvation of His
                    spirit children. For this defiant rebellion he
                    was “thrust down from the presence of God and the Son,” and became
                    Satan or the devil who “maketh war with the
                    saints of God.” Those who do likewise, who follow Satan are called sons
                    of perdition. (Pearl of Great Price, Moses
                    4:1-4). They are they who have known my power, and have been made
                    partakers thereof, and suffered themselves
                    through the power of the devil to be overcome, and to deny the truth
                    and defy my power.” (D. &amp; C. 76:31)</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>However,
                    Lucifer was “an angel of God who was in authority in the presence of
                    God.” He had risen high in
                    knowledge, understanding, and power. He was Lucifer, a son of the
                    morning (of light). For his rebellion there was
                    no excuse. He committed the unpardonable sin, in denying that of which
                    he had full and complete knowledge. He
                    became thereby the father of lies (See D. &amp; C. 76:26, 32-48).</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>It
                    is probable that only personages who have acquired similar full
                    knowledge, who willfully and deliberately deny
                    the truth, when they know it to be the truth, can commit the
                    unpardonable sin and become sons of perdition. They
                    are sons of perdition because, “Having denied the Holy Spirit after
                    having received it, and having denied the Only
                    Begotten Son of the Father, having crucified him unto themselves and
                    put him to open shame” (D. &amp; C. 76:35). They
                    must have had a fullness of knowledge; a testimony which cannot be
                    destroyed. One must be on a high eminence to
                    fall so low; and few in world’s history have attained such a height. It
                    is doubtful if even Judas, who betrayed Jesus,
                    was sufficiently enlightened to become a son of perdition (Joseph F.
                    Smith, <i>Gospel Doctrine</i>, p. 545). Cain was called
                    Perdition because of his sin, but it is added “for thou wast also
                    before the world,” implying a reason from out of the
                    pre-existent world, for this heavy punishment (Pearl of Great Price,
                    Moses 5:24).</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>Moreover,
                    the expression, sons of perdition, is often used in the scriptures to
                    describe disciples of Satan, all who
                    defy God and teach untruth, and who delight in lies, without
                    necessarily committing the unpardonable sin. The many
                    brethren and sisters who have propounded questions about the sons of
                    perdition may rest secure that with their
                    present knowledge they cannot become sons of perdition.</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>According
                    to Mormon doctrine, the bodies of all who have had a mortal existence
                    upon earth will be resurrected
                    from the grave. The atonement of Jesus Christ knows no exceptions (Book
                    of Mormon, 3 Nephi 19:22). Yet, after
                    the resurrection comes the judgment. The acts on earth may forfeit many
                    of the possible gifts following earth
                    existence (Ibid., 3 Nephi 26:4, 5). The spiritual redemption, which is
                    part of the redemption from the grave, will
                    apparently be denied the sons of perdition. That appears to be the
                    meaning of the statements that “he [the Lord] saves
                    all except them”; and that they are, “the only ones on whom the second
                    death shall have power (D. &amp; C. 76:38, 43,
                    44). They who will be judged to be sons of perdition will arise from
                    the grave with their bodies, but their bodies will
                    be of no use to them, as the “second death” is meted out to them in the
                    final judgment.</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>The
                    destiny of the sons of perdition is not known. They shall suffer the
                    “second death”; they shall be subject to
                    “everlasting punishment”; they shall “reign with the devil and his
                    angels in eternity.” What this means has not been
                    revealed. The Lord has declared:</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>“And
                    the end thereof, neither the place thereof, nor their torment, no man
                    knows;</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>“Neither
                    was it revealed neither is neither will be revealed unto man, except to
                    them who are made partakers
                    thereof; . . .</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>“Wherefore,
                    the end, the width, the height, the depth, and the misery thereof, they
                    understand not, neither any
                    man except those who are ordained unto this condemnation” (D. &amp; C.
                    76:45-46, 48).</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>It
                    must be a terrible punishment beyond human comprehension, the greatest
                    conceivable, yet a justified
                    punishment. Since the greatest sin is the unpardonable sin, it would
                    appear that they will forfeit all the gains of the
                    ages of pre-existence and the years on earth. It is no wonder that the
                    heavens wept over Lucifer’s rebellion (D. &amp; C.
                    76:26).</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>President
                    Brigham Young has suggested that the ultimate punishment of the sons of
                    perdition may be that they,
                    having their spiritual bodies disorganized, must start over again must
                    begin anew the long journey of existence,
                    repeating the steps that they took in the eternities before the Great
                    Council was held. That would be punishment,
                    indeed! “They will be decomposed, both soul and body, and return to
                    their native element. I do not say that they will
                    be annihilated; but they will be disorganized, and will be as if they
                    had never been; while we live and retain our
                    identity and contend against those principles which tend to death or
                    dissolution” (<i>Journal of Discourses</i>, 7:57). “The
                    clay that marred in the potter’s hands was thrown back into the
                    unprepared portion to be prepared over again” (Ibid.,
                    2:124).</font></p>
                <p style="text-align: justify; font-family: arial; margin-left: 40px;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>Little
                    is known of the sons of perdition and their destiny, yet that little
                    known stands as a warning to all men. To
                    deal carelessly with truth, to deny it when once gained, to defy the
                    laws of truth which are the laws of God must be
                    counted among the greatest sins. Those who deal lightly with truth in
                    their lives, though they may not become sons
                    of perdition, must expect a heavy punishment, which often begins in
                    mortality. (<i>Evidences and Reconciliations</i>,
                    pp.212-214)<br>
                    </font></p>
