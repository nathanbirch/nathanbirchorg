                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>ALL
                    SAVED EXCEPT SONS OF PERDITION. It is a very pleasing and consoling
                    thing to know that the Lord
                    will save all of his children, excepting the very few who wilfully
                    rebel against him. When his children have paid the
                    penalty of their transgressions, they shall come forth from the
                    clutches of the second death to receive a place
                    somewhere in the great heavenly kingdoms, which are prepared for them
                    with their several glories and degrees of
                    salvation.</font></p>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>It
                    is the purpose of the Almighty to save all mankind, and all will enter
                    into his kingdoms in some degree of glory,
                    except sons of perdition who sin beyond the power of repentance and
                    redemption, and therefore cannot receive
                    forgiveness of sins. All the rest shall be saved, but not all with the
                    same degree of glory or exaltation. Every man will
                    be judged according to his works, his opportunities for receiving the
                    truth, and the intent of his heart. (<i>Doctrines of
                        Salvation</i>, 2:20-21)</font></p>
                <div style="margin-left: 40px; font-family: arial;"><br>
                    <font style="font-family: arial;" size="+2"></font></div>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>DIFFERENCES
                    IN KIND OF RESURRECTED BODIES. In section 88 of the Doctrine and
                    Covenants, we are
                    taught that there is a difference in the kinds of resurrection. Some
                    will be raised with celestial bodies; some with
                    terrestrial bodies, and some with telestial bodies; and yet others will
                    be raised with bodies without any qualification
                    or power of glory, and these will be sons of perdition. (<i>Doctrines
                        of Salvation</i>, 2:33)</font></p>
                <div style="margin-left: 40px; font-family: arial;"><br>
                    <font style="font-family: arial;" size="+2"></font></div>
                <p style="text-align: justify; margin-left: 40px; font-family: arial;"><font
                        size="+2"><span>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</span>SONS
                    OF PERDITION WILLFULLY REJECT ATONEMENT. With the Latter-day Saints,
                    this is not so. While
                    it is true we teach that a man must comply with these principles of the
                    gospel in order to receive salvation and
                    exaltation in the kingdom of heaven -- which is proved by many passages
                    of scripture -- nevertheless, we hold out
                    the hope that all may be saved, excepting the sons of perdition, a
                    class that wilfully rejects the atonement of the
                    Savior, for the Lord intends to save all the workmanship of his hands,
                    save these few who will not receive salvation.
                    Our doctrine consigns none others to perdition, but holds forth the
                    hope that all will eventually be saved in some
                    degree of glory. (<i>Doctrines of Salvation</i>, 2:140)</font></p>
                <div style="margin-left: 40px; font-family: arial;"><br>
