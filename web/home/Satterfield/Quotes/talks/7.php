<p><font size=+1>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Also by way
of having all things in perspective, we should be aware that there are
approved and inspired writings that are not in the standard works. These
writings also are true and should be used along with the scriptures themselves
in learning and teaching the gospel. Next to the standard works five of
the greatest documents in our literature are--</font>
<br><font size=+1>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 1. The "Wentworth
Letter." (See <i>History of the Church</i>, 4:535-41.) Written by the Prophet
Joseph Smith, it contains an account of the coming forth of the Book of
Mormon, of the ancient inhabitants of the Americas, of the organization
of the Church in this dispensation, and of the persecutions suffered by
the early Latter-day Saints. The thirteen Articles of Faith are part of
this letter.</font>
<br><font size=+1>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 2. <i>Lectures
on Faith.</i> These lectures were prepared by and under the direction of
the Prophet Joseph Smith and were taught by him and by others in the School
of the Prophets. The Prophet said they embraced "the important doctrine[s]
of salvation" (Preface to D&amp;C 1835 ed.; reprint, Independence, Mo.:
Herald House, 1971).</font>
<br><font size=+1>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 3. <i>The
Father and the Son: A Doctrinal Exposition by the First Presidency and
the Twelve</i>. (See James R. Clark, comp., <i>Messages of the First Presidency
of the The Church of Jesus Christ of Latter-day Saints</i>, 6 vols. [Salt
ake City: Bookcraft, 1965-75], 5:26-34; see also 5:23-25.) This exposition
sets forth the tatus and relationship of the Father and the Son, shows
those ways in which Christ is the Father, and through its various recitations
lays to rest the false and heretical view that Adam is our Father and our
God.</font>
<br><font size=+1>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 4. The "King
Follett Sermon" and the "Sermon in the Grove." (See <i>History of the Church</i>,
6:302-17; 6:473-79.) These two sermons, one in thought and content, set
forth the doctrine of the plurality of Gods and of becoming joint heirs
with Christ. They show that man may become as his Maker and reign in celestial
exaltation forever.</font>
<br><font size=+1>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 5. "The Origin
of Man," by the First Presidency of the Church. (See Clark, <i>Messages
of the First Presidency</i>, 4:200-206; see also 4:199.) This inspired
writing sets forth the official position of the Church on the origin of
man and therefore impinges on the evolutionary fantasies of biologists
and their fellow travelers. As might be expected, it arouses great animosity
among intellectuals whose testimonies are more ethereal than real.</font>
