               <center style="font-family: arial;"><big><i><font size="+2"><big>THE
ORIGIN OF MAN</big></font></i>
</big>
<p><big>By The First Presidency of the Church
<br>
<i>Messages of the First Presidency, [6 vol.s, 1965-75]</i>, 4:200-206</big></p><p><big>Reprinted in <span style="font-style: italic;">Ensign</span>, May, Feb. 2002, pp. 26-30</big></p>
</center>
<p style="font-family: arial;"><big><a href="../PDF/OriginofMan.pdf">Click
here for PDF Version</a>
</big></p>
<p style="font-family: arial;"><big>"God created man in his own image."
</big></p>
<p style="font-family: arial;"><big>Inquiries arise from time to time
respecting the attitude of the Church
of Jesus Christ of Latter-day Saints upon questions which, though not
vital
from a doctrinal standpoint, are closely connected with the fundamental
principles of salvation. The latest inquiry of this kind that has
reached
us is in relation to the origin of man. It is believed that a statement
of the position held by the Church upon this important subject will be
timely and productive of good.
</big></p>
<p style="font-family: arial;"><big>In presenting the statement that
follows we are not conscious of putting
forth anything essentially new; neither is it our desire so to do.
Truth
is what we wish to present, and truth -- eternal truth -- is
fundamentally
old. A restatement of the original attitude of the Church relative to
this
matter is all that will be attempted here. To tell the truth as God has
revealed it, and commend it to the acceptance of those who need to
conform
their opinions thereto, is the sole purpose of this presentation.
</big></p>
<p style="font-family: arial;"><big>"God created man in his own image,
in the image of God created he him;
male and female created he them." In these plain and pointed words the
inspired author of the book of Genesis made known to the world the
truth
concerning the origin of the human family. Moses, the
prophet-historian,
"learned," as we are told, "in all the wisdom of the Egyptians," when
making
this important announcement, was not voicing a mere opinion, a theory
derived
from his researches into the occult lore of that ancient people. He was
speaking as the mouthpiece of God, and his solemn declaration was for
all
time and for all people. No subsequent revelator of the truth has
contradicted
the great leader and lawgiver of Israel. All who have since spoken by
divine
authority upon this theme have confirmed his simple and sublime
proclamation.
Nor could it be otherwise. Truth has but one source, and all
revelations
from heaven are harmonious with each other. The omnipotent Creator, the
maker of heaven and earth -- had shown unto Moses everything pertaining
to this planet, including the facts relating to man's origin, and the
authoritative
pronouncement of that mighty prophet and seer to the house of Israel,
and
through Israel to the whole world, is couched in the simple clause:
"God
created man in his own image" (Genesis 1:27; Pearl of Great Price --
Book
of Moses, 1: 27-41.)
</big></p>
<p style="font-family: arial;"><big>The creation was two-fold firstly
spiritual, secondly temporal. This
truth, also, Moses plainly taught much more plainly than it has come
down
to us in the imperfect translations of the Bible that are now in use.
Therein
the fact of a spiritual creation, antedating the temporal creation, is
strongly implied, but the proof of it is not so clear and conclusive as
in other records held by the Latter-day Saints to be of equal authority
with the Jewish scriptures. The partial obscurity of the latter upon
the
point in question is owing, no doubt, to the loss of those "plain and
precious"
parts of sacred writ, which, as the Book of Mormon informs us, have
been
taken away from the Bible during its passage down the centuries (I
Nephi
13:24-29). Some of these missing parts the Prophet Joseph Smith
undertook
to restore when he revised those scriptures by the spirit of
revelation,
the result being that more complete account of the creation which is
found
in the book of Moses, previously cited. Note the following passages:
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>And now, behold,
I say unto you, that these are the generations of the
heaven and of the earth, when they were created, in the day that I, the
Lord God, made the heaven and the earth;
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>And every plant
of the field before it was in the earth, and every herb
of the field before it grew.
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>For I, the Lord
God, created all things of which I have spoken, spiritually,
before they were naturally upon the face of the earth. For I, the Lord
God, had not caused it to rain upon the face of the earth.
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>And I, the Lord
God, had created all the children of men, and not yet
a man to till the ground; for in heaven created I them, and there was
not
yet flesh upon the earth, neither in the water, neither in the air.
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>But, I, the Lord
God, spake, and there went up a mist from the earth,
and watered the whole face of the ground.
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>And I, the Lord
God, formed man from the dust of the ground, and breathed
into his nostrils the breath of life; and man became a living soul, the
first flesh upon the earth, the first man also.
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>Nevertheless,
all things were before created, but spiritually were they
created and made, according to my word (Pearl of Great Price -- Book of
Moses, 3: 4-7. See also chapters 1 and 2, and compare with Genesis 1
and
2).
</big></p>
<p style="font-family: arial;"><big>These two points being established,
namely, the creation of man in the
image of God, and the two-fold character of the creation, let us now
inquire:
What was the form of man, in the spirit and in the body, as originally
created? In a general way the answer is given in the words chosen as
the
text of this treatise. "God created man in his own image." It is more
explicitly
rendered in the Book of Mormon thus: "All men were created in the
beginning
after mine own image: (Ether 3:15). It is the Father who is speaking.
If,
therefore, we can ascertain the form of the "Father of spirits," "The
God
of the spirits of all flesh," we shall be able to discover the form of
the original man.
</big></p>
<p style="font-family: arial;"><big>Jesus Christ, the Son of God, is
"the express image" of His Father's
person (Hebrews 1:3). He walked the earth as a human being, as a
perfect
man, and said, in answer to a question put to Him: "He that hath seen
me
hath seen the Father" (John 14:9). This alone ought to solve the
problem
to the satisfaction of every thoughtful, reverent mind. The conclusion
is irresistible, that if the Son of God be the express image (that is,
likeness) of His Father's person, then His Father is in the form of
man;
for that was the form of the Son of God, not only during His mortal
life,
but before His mortal birth, and after His resurrection. It was in this
form that the Father and the Son, as two personages, appeared to Joseph
Smith, when, as a boy of fourteen years, he received his first vision.
Then if God made man--the first man --in His own image and likeness, he
must have made him like unto Christ, and consequently like unto men of
Christ's time and of the present day. That man was made in the image of
Christ, is positively stated in the Book of Moses: "And I, God, said
unto
mine Only Begotten, which was with me from the beginning, Let us make
man
in our image, after our likeness; and it was so. * * * And I, God,
created
man in mine own image, in the image of mine Only Begotten created I
him,
male and female created I them" (2:26, 27).
</big></p>
<p style="font-family: arial;"><big>The Father of Jesus is our Father
also. Jesus Himself taught this truth,
when He instructed His disciples how to pray: "Our Father which art in
heaven," etc. Jesus, however, is the firstborn among all the sons of
God
the first begotten in the spirit, and the only begotten in the flesh.
He
is our elder brother, and we, like Him, are in the image of God. All
men
and women are in the similitude of the universal Father and Mother, and
are literally the sons and daughters of Deity.
</big></p>
<p style="font-family: arial;"><big>"God created man in His own image."
This is just as true of the spirit
as it is of the body, which is only the clothing of the spirit, its
complement;
the two together constituting the soul. The spirit of man is in the
form
of man, and the spirits of all creatures are in the likeness of their
bodies.
This was plainly taught by the Prophet Joseph Smith (Doctrine and
Covenants,
77: 2).
</big></p>
<p style="font-family: arial;"><big>Here is further evidence of the
fact. More than seven hundred years
before Moses was shown the things pertaining to this earth, another
great
prophet, known to us as the brother of Jared, was similarly favored by
the Lord. He was even permitted to behold the spirit-body of the
foreordained
Savior, prior to His incarnation; and so like the body of a man was
gazing
upon a being of flesh and blood. He first saw the finger and then the
entire
body of the Lord -- all in the spirit. The Book of Mormon says of this
wonderful manifestation:
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>And it came to
pass that when the brother of Jared had said these words,
behold, the Lord stretched forth His hand and touched the stones one by
one with His finger; and the veil was taken from off the eyes of the
brother
of Jared, and he saw the finger of the Lord; and it was as the finger
of
a man, like unto flesh and blood; and the brother of Jared fell down
before
the Lord, for he was struck with fear.
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>And the Lord saw
that the brother of Jared had fallen to the earth;
and the Lord said unto him, Arise, why hast thou fallen?
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>And he saith
unto the Lord, I saw the finger of the Lord, and I feared
lest he should smite me; for I knew not that the Lord had flesh and
blood.
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>And the Lord
said unto him, Because of thy faith thou hast seen that
I shall take upon me flesh and blood; and never has man come before me
with such exceeding faith as thou hast; for were it not so, ye could
not
have seen my finger. Sawest thou more than this?
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>And he answered,
Nay, Lord, show thyself unto me.
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>And the Lord
said unto him, Believest thou the words which I shall speak?
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>And he answered,
Yea, Lord, I know that thou speakest the truth, for
thou art a God of truth and canst not lie.
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>And when he had
said these words, behold, the Lord showed himself unto
him, and said, Because thou knowest these things ye are redeemed from
the
fall; therefore ye are brought back into my presence; therefore I show
myself unto you.
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>Behold, I am He
who was prepared from the foundation of the world to
redeem my people. Behold, I am Jesus Christ, I am the Father and the
Son.
In me shall all mankind have light, and that eternally, even they who
shall
believe on my name; and they shall become my sons and my daughters.
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>And never have I
shewed myself unto man whom I have created, for never
hath man believed in me as thou hast. Seest thou that ye are created
after
mine own image?, Yea, even all men were created in the beginning after
mine own image.
</big></p>
<p style="font-family: arial; margin-left: 40px;"><big>Behold, this
body, which ye now behold, is the body of my spirit, and
man have I created after the body of my spirit; and even as I appear
unto
thee to be in the spirit, will I appear unto my people in the flesh.
(Ether
3:6-16.)
</big></p>
<p style="font-family: arial;"><big>What more is needed to convince us
that man, both in spirit and in body,
is the image and likeness of God, and that God Himself is in the form
of
man?
</big></p>
<p style="font-family: arial;"><big>When the divine Being whose
spirit-body the brother of Jared beheld,
took upon Him flesh and blood, He appeared as a man, having "body,
parts
and passions," like other men, though vastly superior to all others,
because
He was God, even the Son of God, the Word made flesh: in Him "dwelt the
fulness of the Godhead bodily." And why should He not appear as a man?
That was the form of His spirit, and it must needs have an appropriate
covering, a suitable tabernacle. He came into the world as He had
promised
to come (III Nephi, 1: 13), taking an infant tabernacle, and developing
it gradually to the fulness of His spirit stature. He came as man had
been
coming for ages, and as man has continued to come ever since. Jesus,
however,
as shown, was the only begotten of God in the flesh.
</big></p>
<p style="font-family: arial;"><big>Adam, our progenitor, "the first
man," was, like Christ, a pre-existent
spirit, and like Christ he took upon him an appropriate body, the body
of a man, and so became a "living soul." The doctrine of the
pre-existence,
-- revealed so plainly, particularly in latter days, pours a wonderful
flood of light upon the otherwise mysterious problem of man's origin.
It
shows that man, as a spirit, was begotten and born of heavenly parents,
and reared to maturity in the eternal mansions of the Father, prior to
coming upon the earth in a temporal body to undergo an experience in
mortality.
It teaches that all men existed in the spirit before any man existed in
the flesh, and that all who have inhabited the earth since Adam have
taken
bodies and become souls in like manner.
</big></p>
<p style="font-family: arial; font-weight: bold;"><big>It is held by
some that Adam was not the first man upon this earth,
and that the original human being was a development from lower orders
of
the animal creation. These, however, are the theories of men. The word
of the Lord declares that Adam was "the first man of all men" (Moses
1:34),
and we are therefore in duty bound to regard him as the primal parent
of
our race. It was shown to the brother of Jared that all men were
created
in the beginning after the image of God; and whether we take this to
mean
the spirit or the body, or both, it commits us to the same conclusion:
Man began life as a human being, in the likeness of our heavenly
Father.
</big></p>
<p style="font-family: arial; font-weight: bold;"><big>True it is that
the body of man enters upon its career as a tiny germ
embryo, which becomes an infant, quickened at a certain stage by the
spirit
whose tabernacle it is, and the child, after being born, develops into
a man. There is nothing in this, however, to indicate that the original
man, the first of our race, began life as anything less than a man, or
less than the human germ or embryo that becomes a man.
</big></p>
<p style="font-family: arial; font-weight: bold;"><big>Man, by
searching, cannot find out God. Never, unaided, will he discover
the truth about the beginning of human life. The Lord must reveal
Himself,
or remain unrevealed; and the same is true of the facts relating to the
origin of Adam's race --God alone can reveal them. Some of these facts,
however, are already known, and what has been made known it is our duty
to receive and retain.
</big></p>
<p style="font-family: arial; font-weight: bold;"><big>The Church of
Jesus Christ of Latter-day Saints, basing its belief on
divine revelation, ancient and modern, proclaims man to be the direct
and
lineal offspring of Deity. God Himself is an exalted man, perfected,
enthroned,
and supreme. By His almighty power He organized the earth, and all that
it contains, from spirit and element, which exist co-eternally with
Himself.
He formed every plant that grows, and every animal that breathes, each
after its own kind, spiritually and temporally -- "that which is
spiritual
being in the likeness of that which is temporal, and that which is
temporal
in the likeness of that which is spiritual." He made the tadpole and
the
ape, the lion and the elephant but He did not make them in His own
image,
nor endow them with Godlike reason and intelligence. Nevertheless, the
whole animal creation will be perfected and perpetuated in the
Hereafter,
each class in its "distinct order or sphere," and will enjoy "eternal
felicity."
That fact has been made plain in this dispensation (Doctrine and
Covenants,
77: 3).
</big></p>
<p style="font-family: arial;"><big><span style="font-weight: bold;">Man
is the child of God, formed in the divine image and endowed with
divine attributes, and even as the infant son of an earthly father and
mother is capable in due time of becoming a man, so the undeveloped
offspring
of celestial parentage is capable, by experience through ages and
aeons,
of evolving into a God.
</span><br>
&nbsp;
</big></p>
