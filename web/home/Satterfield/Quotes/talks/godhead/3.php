                <center><i><font size=+3>Only One God to Worship, April, 1912</font></i>
                    <p>1912 -- April -- Improvement Era 15:483-485 (April, 1912).
                        <br><i>Messages of the First Presidency</i>, 4:269-271</center>
                <p>This signed article is an official interpretation by the First Presidency
                    of a single verse of scripture, namely, Moses 1:6 in the Pearl of Great
                    Price, with supporting scriptures from the other three Standard Works or
                    scriptures of the Church.
                    <br>&nbsp;
                <p><br>
                <center>
                    <p>ONLY ONE GOD TO WORSHIP
                    <p>By the First Presidency of the Church</center>

                <p><br>
                <p><font size=+1>Discussions frequently arise over the meaning of isolated
                    texts of scriptures, most of which would find interpretation in other passages
                    of holy writ. Some of them are needless and unimportant, and these should
                    be avoided; but it is proper to study and arrive at correct conclusions
                    as to the meaning of that which is written for our learning and edification.
                    The published standards of the Church, as has been frequently announced,
                    are the Bible, "so far as it is translated correctly," the Book of Mormon,
                    the Doctrine and Covenants, and the Pearl of Great Price. They have been
                    so accepted by the Church in conference assembled. Thus we have something
                    at hand which can be appealed to as authority in doctrine.</font><font size=+1></font>
                <p><font size=+1>The Lord has also appointed one man at a time on the earth
                    to hold the keys of revelation to the entire body of the Church in all
                    its organizations, authorities, ordinances and doctrines. The spirit of
                    revelation is bestowed upon all its members for the benefit and enlightenment
                    of each individual receiving its inspiration, and according to the sphere
                    in which he or she is called to labor. But for the entire Church, he who
                    stands at the head is alone appointed to receive revelations by way of
                    commandment and as the end of controversy. Assisted by his counselors,
                    he presides over the whole Church in all the world; thus the First Presidency
                    hold the right to give authoritative direction in all matters that pertain
                    to the building up and government and regulation of the body.</font><font size=+1></font>
                <p><font size=+1>A question has been discussed in some of the auxiliary
                    societies, and finally has been submitted to us, in reference to a saying
                    to be found in the Pearl of Great Price, as follows:</font><font size=+1></font>
                <p><font size=+1>"And I have a work for thee, Moses, my son; and thou art
                    in the similitude of my Only Begotten; and mine Only Begotten is and shall
                    be the Savior, for He is full of grace and truth; but there is no God beside
                    me, and all things are present with me, for I know them all." (Moses 1:6).</font><font size=+1></font>
                <p><font size=+1>The words particularly debated are; "but there is no God
                    beside me." They appear to be in conflict with many other statements in
                    scripture, both ancient and modern, but are not out of harmony with them
                    when properly understood.</font><font size=+1></font>
                <p><font size=+1>Moses was reared in an atmosphere of idolatry. There were
                    numerous deities among the Egyptians. In commencing the work which the
                    Lord said he had for Moses to do, it was necessary to center his mind and
                    faith upon God the Eternal Father as the only Being to worship. Therefore,
                    the words now under consideration, or rather those that were actually spoken
                    to Moses of which these are a translation, were made emphatic, not only
                    as to the false gods of the times but delusive spirits, of whom Satan was
                    the chief and who tried to pass himself off to Moses as a divine object
                    of worship, as narrated in the same chapter. (Verses 12-25).</font><font size=+1></font>
                <p><font size=+1>This was repeated in substance, and for the same reasons,
                    in the first of the Ten Commandments: "Thou shalt have no other gods before
                    me," - that is, beside me, above me, or equal to me, or to be an object
                    of worship, (Exodus 20:2-5). Or, as Paul put it: "For though there be [many]
                    that are called gods whether in heaven or in earth, (as there are gods
                    many and lords many) yet to us there is but one God, the Father, of whom
                    are all things, and one Lord, Jesus Christ, by whom are all things and
                    we by Him." (1 Cor. 8:5-6).</font><font size=+1></font>
                <p><font size=+1>It should be remembered that it was Christ before he was
                    in the flesh who gave the law and the commandments to Moses, and who spoke
                    for the Father, as He explained to the Nephites when he appeared to them
                    after his resurrection. (3 Nephi 15:5.) He "was in the beginning with God
                    and was God," according to John 1:1. The Father was represented by Him
                    and He acted and spoke for the Father, in the creation and from that time
                    forward in all the divine dispensations. Angels also, under Him, have been
                    appointed to speak for God, being so authorized and empowered. (See Ex.
                    23:20, 21). But the sole object of worship, God the Eternal Father, stands
                    supreme and alone, and it is in the name of the Only Begotten that we thus
                    approach Him, as Christ taught always. "God standeth in the congregation
                    of the mighty; He judgeth among the gods." (Psalms 82:1.) Jesus quoted
                    this and did not dispute it (John 10:34-6). All the perfected beings who
                    are rightly called gods, being, like the Savior, possessed of "the fullness
                    of the Godhead bodily," are ONE, just as the Father and the Son and the
                    Holy Ghost are one.</font><font size=+1></font>
                <p><font size=+1>There are questions relating to doctrine and principle
                    that are proper subjects for class discussion, when that is conducted for
                    the purpose of gaining information. There are topics, however, that are
                    of no particular moment, or on which no definite conclusion can be authoritatively
                    reached, and these ought to be avoided, as a waste of time and a cause
                    of endless dispute. Let the light shine and be sought for in faith, but
                    let contention have no place among the Latter-day Saints!</font>
