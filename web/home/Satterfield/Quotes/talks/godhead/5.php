                <center style="font-family: arial;"><i><font size="+2">Sermon in The
                        Grove</font></i>
                    <p>Joseph Smith
                        <br>
                        <font size="-1">Meeting in the Grove, east of the Temple, June 16, 1844</font>
                        <br>
                        <font size="-1"><i>History of the Church</i> 6:473-479; <i>Teachings
                            of the Prophet Joseph Smith</i>, pp. 369-376</font></p>
                </center>
                <p style="font-family: arial;"><big><font size="-1"><big>President
                        Joseph Smith read the 3rd chapter of Revelation, and took for his text
                        1st chapter, 6th verse -- "And hath made us kings and priests unto God
                        and His Father: to Him be glory and dominion forever and ever. Amen."</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>It
                        is altogether correct in the translation. Now, you know that of late
                        some
                        malicious and corrupt men have sprung up and apostatized from the
                        Church
                        of Jesus Christ of Latter-day Saints, and they declare that the Prophet
                        believes in a plurality of Gods, and, lo and behold! we have discovered
                        a very great secret, they cry -- "The Prophet says there are many Gods,
                        and this proves that he has fallen."</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>It
                        has been my intention for a long time to take up this subject and lay
                        it
                        clearly before the people, and show what my faith is in relation to
                        this
                        interesting matter. I have contemplated the saying of Jesus (Luke 17th
                        chapter, 26th verse) -- "And as it was in the days of Noah, so shall it
                        be also in the days of the Son of Man." And if it does rain, I'll
                        preach
                        this doctrine, for the truth shall be preached.</big></font>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b><font size="-1"><big><br>
                                Plurality
                                of Gods</big></font></b></big></p>
                </center>
                <p style="font-family: arial;"><big><font size="-1"><big>I
                        will preach on the plurality of Gods. I have selected this text for
                        that
                        express purpose. I wish to declare I have always and in all
                        congregations
                        when I have preach on the subject of the Deity, it has been the
                        plurality
                        of Gods. I has been preached by the Elders for fifteen years.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>I
                        have always declared God to be a distinct personage, Jesus Christ a
                        separate
                        and distinct personage from God the Father, and the Holy Ghost was a
                        distinct
                        personage and a Spirit: and these three constitute three distinct
                        personages
                        and three Gods. If this is in accordance with the New Testament, lo and
                        behold! we have three Gods anyhow, and they are plural; and who can
                        contradict
                        it?</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>Our
                        text says, "And hath made us kings and priests unto God and His
                        Father."
                        The Apostles have discovered that there were Gods above, for John says
                        God was the Father of our Lord Jesus Christ. My object was to preach
                        the
                        scriptures, and preach the doctrine they contain, there being a God
                        above,
                        the Father of our Lord Jesus Christ. I am bold to declare I have taught
                        all the stronger doctrines in public than in private.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>John
                        was one of the men, and apostles declare they were made kings and
                        priests
                        unto God, the Father of our Lord Jesus Christ. It reads just so in the
                        Revelation, Hence the doctrine of a plurality of Gods is as prominent
                        in
                        the Bible as any other doctrine. It is all over the face of the Bible.
                        It stands beyond the power of controversy. A wayfaring man, though a
                        fool,
                        need not err therein.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>Paul
                        says there are Gods many and Lords many. I want to set it forth in a
                        plain
                        and simple manner; but to us there is but one God -- that is</big></font><font
                        size="-1"><big><i>pertaining
                            to us</i>; and he is in all and through all. But if Joseph Smith says
                        there
                        are Gods many and Lords many, they cry, "Away with him! Crucify him!
                        Crucify
                        him!"</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>Mankind
                        verily say that the Scriptures are with them. Search the Scriptures,
                        for
                        they testify of things that these apostates would gravely pronounce
                        blasphemy.
                        Paul, if Joseph Smith is a blasphemer, you are. I say there are Gods
                        many
                        and Lords many, but to us only one, and we are to be in subjection to
                        that
                        one, and no man can limit the bounds or the eternal existence of
                        eternal
                        time. Hath he beheld the eternal world, and is he authorized to say
                        that
                        there is only one God? He makes himself a fool if he thinks or says so,
                        and there is and end of his career or progress in knowledge. He cannot
                        obtain all knowledge, for he has sealed up the gate to it.</big></font>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b><font size="-1"><big><br>
                                Scriptural
                                Interpretation</big></font></b></big></p>
                </center>
                <p style="font-family: arial;"><big><font size="-1"><big>Some
                        say I do not interpret the Scripture the same as they do. They say it
                        means
                        the heathen's gods. Paul says there are Gods many and Lords many; and
                        that
                        makes a plurality of Gods, in spite of the whims of all men. Without a
                        revelation, I am no going to give them the knowledge of the God of
                        heaven.
                        You know and I testify that Paul had no allusion to the heathen gods. I
                        have it from God, and get over it if you can. I have a witness of the
                        Holy
                        Ghost, and a testimony that Paul had no allusion to the heathen gods in
                        the text. I will show from the Hebrew Bible that I am correct, and the
                        first word shows a plurality of Gods; and I want the apostates and
                        learned
                        men to come here and prove to the contrary, if they can. An unlearned
                        boy
                        must give you a little Hebrew.&nbsp;</big></font><font size="-1"><big><i>Berosheit
                            baurau Eloheim ait aushamayeen vehau auraits</i>, rendered by King
                        James'
                        translators, "In the beginning God created the heaven and the earth." I
                        want to analyze the word&nbsp;</big></font><font size="-1"><big><i>Berosheit</i>.&nbsp;</big></font><font
                        size="-1"><big><i>Rosh</i>,
                        the head;&nbsp;</big></font><font size="-1"><big><i>Sheit</i>,
                        a grammatical termination; the&nbsp;</big></font><font size="-1"><big><i>Baith</i>
                        was not originally put there when the inspired man wrote it, but it has
                        been since added by an old Jew.&nbsp;</big></font><font size="-1"><big><i>Baurau</i>
                        signifies to bring forth;</big></font><font size="-1"><big><i>Eloheim</i>
                        is from the word&nbsp;</big></font><font size="-1"><big><i>Eloi</i>,
                        God, in the singular number; and by adding the word&nbsp;</big></font><font
                        size="-1"><big><i>heim</i>,
                        it renders it Gods. It read first, "In the beginning the head of the
                        Gods
                        brought forth the Gods," or, as other have translated it, "The head of
                        the Gods called the Gods together." I want to show a little learning as
                        well as other fools.&nbsp;</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>The
                        head God organized the heavens and the earth. I defy all the world to
                        refute
                        me. In the beginning the heads of the Gods organized the heavens and
                        the
                        earth. Now the learned priests and the people rage, and the heathen
                        imagine
                        a vain thing. If we pursue the Hebrew text further, it reads,&nbsp;</big></font><font
                        size="-1"><big><i>"Berosheit
                            baurau Eloheim ait aushamayeen vehau auraits."</i> -- "The head one of
                        the Gods said, Let us make a man in our own image," I once asked a
                        learned
                        Jew, "If the Hebrew language compels us to render all words ending
                        in&nbsp;</big></font><font size="-1"><big><i>heim</i>
                        in the plural, why not render the first&nbsp;</big></font><font
                        size="-1"><big><i>Eloheim</i>
                        plural?" He replied, "That is the rule with few exceptions; but in this
                        case it would ruin the Bible." He acknowledged I was right. I came here
                        to investigate these things precisely as I believe them. Hear and judge
                        for yourselves; and if you go away satisfied, well and good.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>In
                        the very beginning the Bible shows there is a plurality of Gods beyond
                        the power of refutation. It is a great subject I am dwelling on. The
                        word</big></font><font size="-1"><big><i> Eloheim</i>
                        ought to be in the plural all the way through -- Gods. The heads of the
                        Gods appointed one God for us; and when you take [that] view of the
                        subject,
                        its sets one free to see all the beauty, holiness and perfection of the
                        Gods. All I want is to get the simple, naked truth, and the whole truth.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>Many
                        men say there is one God; the Father, the Son and the Holy Ghost are
                        only
                        one God. I say that is a strange God anyhow -- three in one, and one in
                        three! It is a curious organization. "Father, I pray not for the world,
                        but I pray for them which thou hast given me." "Holy Father, keep
                        through
                        Thine own name those whom thou hast given me, that they may be one as
                        we
                        are." All are to be crammed into one God, according to sectarianism. It
                        would make the biggest God in all the world. He would be a wonderfully
                        big God -- he would be a giant or a monster. I want to read the text to
                        you myself -- "I am agreed with the Father and the Father is agreed
                        with
                        me, and we are agreed as one." The Greek shows that it should be
                        agreed.
                        "Father, I pray for them which Thou hast given me out of the world, and
                        not for those alone, but for them also which shall believe on me
                        through
                        their word, that they all may be agreed, as Thou, Father, are with me,
                        and I with Thee, that they also may be agreed with us," and all come to
                        dwell in unity, and in all the glory and everlasting burnings of the
                        Gods;
                        and then we shall see as we are seen, and be as our God and He as His
                        Father.
                        I want to reason a little on this subject. I learned it by translating
                        the papyrus which is now in my house.</big></font>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b><font size="-1"><big><br>
                                Abraham's
                                Reasoning</big></font></b></big></p>
                </center>
                <p style="font-family: arial;"><big><font size="-1"><big>I
                        learned a testimony concerning Abraham, and he reasoned concerning the
                        God of heaven. "In order to do that," said he, "suppose we have two
                        facts:
                        that supposes another fact may exist -- two men on the earth, one wise
                        than the other, would logically show that another who is wiser than the
                        wisest may exist. Intelligences exist one above another, so that there
                        is no end to them."</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>If
                        Abraham reasoned thus -- If Jesus Christ was the Son of God, and John
                        discovered
                        that God the Father of Jesus Christ had a Father, you may suppose that
                        He had a Father also. Where was there ever a son without a father? And
                        where was there ever a father without first being a son? Whenever did a
                        tree or anything spring into existence without a progenitor? And
                        everything
                        comes in this way. Paul says that which is earthly is in the likeness
                        of
                        that which is heavenly, Hence if Jesus had a Father, can we not believe
                        that He had a Father also? I despise the idea of being scared to death
                        at such a doctrine, for the Bible is full of it.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>I
                        want you to pay particular attention to what I am saying. Jesus said
                        that
                        the Father wrought precisely in the same way as His Father had done
                        before
                        Him. As the Father had done before? He laid down His life, and took it
                        up the same as His Father had done before. He did as He was sent, to
                        lay
                        down His life and take it up again; and then was committed unto Him the
                        keys. I know it is good reasoning.</big></font>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b><font size="-1"><big><br>
                                The
                                Church Being Purged</big></font></b></big></p>
                </center>
                <p style="font-family: arial;"><big><font size="-1"><big>I
                        have reason to think that the Church is being purged. I saw Satan fall
                        from heaven, and the way they ran was a caution. All these are wonders
                        and marvels in our eyes in these last days. So long as men are under
                        the
                        law of God, they have no fears -- they do not scare themselves.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>I
                        want to stick to my text, to show that when men open their lips against
                        these truths they do not injure me, but injure themselves. To the law
                        and
                        to the testimony, for these principles are poured out all over the
                        Scriptures.
                        When things that are of the greatest importance are passed over by the
                        weak-minded men without even a thought, I want to see truth in all its
                        bearings and hug it to my bosom. I believe all that God ever revealed,
                        and I never hear of a man being damned for believing too much; but they
                        are damned for unbelief.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>They
                        found fault with Jesus Christ because He said He was the Son of God,
                        and
                        made Himself equal with God. They say of me, like they did of the
                        Apostles
                        of old, that I must be put down. What did Jesus say? "Is it not written
                        in your law, I said, Ye are Gods? If He called them Gods unto whom the
                        word of God came, and the Scriptures cannot be broken, say ye of Him
                        whom
                        the Father hath sanctified and sent into the world, Thou blasphemest;
                        because
                        I said I am the Son of God?" It was through Him that they drank of the
                        spiritual rock. Of course He would take the honor to Himself. Jesus, if
                        they were called Gods unto whom the word of God came, why should it be
                        thought blasphemy that I should say I am the Son of God?</big></font>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b><font size="-1"><big>Eternal
                                Glories</big></font></b></big></p>
                </center>
                <p style="font-family: arial;"><big><font size="-1"><big>Go
                        and read the vision in the Book of Covenants. There is clearly
                        illustrated
                        glory upon glory -- one glory of the sun, another glory of the moon,
                        and
                        a glory of the stars; and as one star differeth from another star in
                        glory,
                        even so do they of the telestial world differ in glory, and every man
                        who
                        reigns in celestial glory is a God to his dominions. By the apostates
                        admitting
                        the testimony of the Doctrine and Covenants they damn themselves. Paul,
                        what do you say? They impeached Paul and all went and left him. Paul
                        had
                        seven churches, and they drove him off from among them; and yet they
                        cannot
                        do it by me. I rejoice in that. My testimony is good.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>Paul
                        says, "There is one glory of the sun, and another glory of the moon,
                        and
                        another glory of the stars; for one star differeth from another star in
                        glory. So also is the resurrection of the dead." They who obtain a
                        glorious
                        resurrection from the dead, are exalted far above principalities,
                        powers,
                        thrones, dominions and angels, and are expressly declared to be heirs
                        of
                        God and joint heirs with Jesus Christ, all having eternal power.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>These
                        Scriptures are a mixture of very strange doctrines to the Christian
                        world,
                        who are blindly led by the blind. I will refer to another Scripture.
                        "Now,"
                        says God, when He visited Moses in the bush, (Moses was a stammering
                        sort
                        of a boy like me) God said, "Thou shalt be a God unto the children of
                        Israel."
                        God said, "Thou shalt be a God unto Aaron, and he shall be thy
                        spokesman."
                        I believe those Gods that God reveals as Gods to be sons of God, and
                        all
                        can cry, "Abba, Father!" Sons of God who exalt themselves to be Gods,
                        even
                        from before the foundation of the world, and are the only Gods I have a
                        reverence for.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>John
                        said he was a king. "And from Jesus Christ, who is the faithful
                        witness,
                        and the first begotten of the dead, and the Prince of the kings of the
                        earth. Unto Him that loved us, and washed us from our sins in His own
                        blood,
                        and hath made us kings and priests unto God, and His Father; to him be
                        glory and dominion forever and ever, Amen." Oh, Thou God who art King
                        of
                        kings and Lord of lords, the sectarian world, by their actions,
                        declare,
                        "We cannot believe Thee."</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>The
                        old Catholic church traditions are worth more than all you have said.
                        Here
                        is a principle of logic that most men have no more sense than to adopt.
                        I will illustrate it by an old apple tree. Here jumps off a branch and
                        says, I am the true tree, and you are corrupt. If the whole tree is
                        corrupt,
                        are not its branches corrupt? If the Catholic religion is a false
                        religion,
                        how can any true religion come out of it? If the Catholic church is
                        bad,
                        how can any good thing come out of it? The character of the old
                        churches
                        have always been slandered by all apostates since the world began.</big></font>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b><font size="-1"><big><br>
                                The
                                Lord Will Not Acknowledge Traitors</big></font></b></big></p>
                </center>
                <p style="font-family: arial;"><big><font size="-1"><big>I
                        testify again, as the Lord lives, God never will acknowledge any
                        traitors
                        or apostates. Any man who will betray the Catholics will betray you;
                        and
                        if he will betray me, he will betray you. All men are liars who say
                        they
                        are of the true Church without the revelations of Jesus Christ and the
                        Priesthood of Melchizedek, which is after the order of the Son of God.</big></font>
                </big></p>
                <p style="font-family: arial;"><big><font size="-1"><big>It
                        is in the order of heavenly things that God should always send a new
                        dispensation
                        into the world when men have apostatized from the truth and lost the
                        priesthood,
                        but when men come out and build upon other men's foundations, they do
                        it
                        on their own responsibility, without authority from God; and when the
                        floods
                        come and the winds blow, their foundations will be found to be sand,
                        and
                        their whole fabric will crumble to dust.</big></font>
                </big></p>
                <p style="font-family: arial;"><font size="-1"><big>Did
                    I build on any other man's foundation? I have got all the truth which
                    the
                    Christian world possessed, and an independent revelation in the
                    bargain,
                    and God will bear me off triumphant. I will drop this subject. I wish I
                    could speak for three or four hours; but it is not expedient on account
                    of the rain; I would still go on, and show you proof upon proofs; all
                    the
                    Bible is equal in support of this doctrine, one part as another.</big>&nbsp;</font>
                </p>

