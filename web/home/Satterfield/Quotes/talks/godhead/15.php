                <center style="font-family: arial;"><big>
                    </big>
                    <h1><big><i>The Light of Christ</i></big></h1>
                </center>
                <center style="font-family: arial;"><big><big>President Marion G. Romney</big>
                        <br>
                        <br>
                        Conference Report, Apr. 1977, p. 59-63;
                        <br>
                        or <i>Ensign,</i> May 1977, p. 43-45</big></center>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>My
                                brethren, I pray, and ask you to join in that prayer,
                                that while I speak we will enjoy the Spirit of Christ. If we don't
                                enjoy
                                it, we won't enjoy these remarks, because my topic is "The Light of
                                Christ."
                                <span style="text-decoration: underline;">There are three phases of the
                                    light of Christ that I want to mention</span>.</big></big></font>
                    </big></big></p>
                <div style="margin-left: 40px; font-family: arial;"><big><big><font
                                size="-1"><big><big>The <span style="text-decoration: underline;">first</span>
                                    one is the light which enlighteneth every man
                                    that cometh into the world;</big></big></font>
                        </big></big><br>
                    <big><big><font size="-1"><big><big>The <span
                                        style="text-decoration: underline;">second</span> phase is the gift of
                                    the Holy Ghost;</big></big></font>
                        </big></big><br>
                    <big><big><font size="-1"><big><big>And the<span
                                        style="text-decoration: underline;"> third</span> is the more sure
                                    word of prophecy.</big></big></font></big></big></div>
                <p style="font-family: arial;"><big><big></big></big></p>
                <p style="font-family: arial;"><big><big><b><font size="-1"><big><big><br>
                                    The Light of Christ</big></big></font></b>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>In
                                the eighty-eighth section of the Doctrine and Covenants,
                                the Lord says, "The light of Christ . . . proceedeth forth from the
                                presence
                                of God to fill the immensity of space." (Vv. 7, 12 [D&amp;C 88:7, 12] .)</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>In
                                another revelation, it is written that this light,
                                which is "the Spirit of Jesus Christ . . . giveth light to every man
                                that
                                cometh into the world; and the Spirit enlighteneth every man through
                                the
                                world, that hearkeneth to the voice of the Spirit." (D&amp;C
                                84:45&shy;46.)</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>This
                                Spirit is, no doubt, the source of one's conscience,
                                which Webster defines as "a knowledge or feeling of right and wrong
                                with
                                a compulsion to do right."</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>Mormon
                                was alluding to this Spirit when he wrote to his
                                son Moroni that "every thing which inviteth and enticeth [CR, p. 60] to
                                do good, and to love God, and to serve him, is inspired of God.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"Wherefore,
                                take heed, my beloved brethren, that ye do
                                not judge that which is evil to be of God, or that which is good and of
                                God to be of the devil.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"For
                                behold, my brethren, it is given unto you to judge,
                                [and that gift is because the light of Christ enlighteneth every man
                                that
                                cometh into the world], that ye may know good from evil; and the way to
                                judge is as plain, that ye may know with a perfect knowledge, as the
                                daylight
                                is from the dark night.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"For
                                behold, the Spirit of Christ is given to every man,
                                that he may know good from evil." (Moro. 7:13&shy;16 [Moroni
                                7:13&shy;16]
                                .)</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>President
                                Joseph F. Smith says that this Spirit of Christ
                                "strives with . . . men, and will continue to strive with them [if they
                                will resist the enticings of Satan], until it brings them to a
                                knowledge
                                of the truth and the possession of the greater light and testimony of
                                the
                                Holy Ghost." (Gospel Doctrine, Deseret Book Co., 1973, pp. 67&shy;68.)</big></big></font>
                        <br>
                        &nbsp;
                    </big></big></p>
                <p style="font-family: arial;"><big><big><b><font size="-1"><big><big>Gift
                                    of the Holy Ghost</big></big></font></b>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>Now,
                                this statement of President Smith's brings us to
                                a consideration of the second phase of our subject: the gift of the
                                Holy
                                Ghost.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>The
                                Holy Ghost is a person, a [Ensign, p. 44] spirit,
                                the third member of the Godhead. He is a messenger and a witness of the
                                Father and the Son. He brings to men testimony, witness, and knowledge
                                of God the Father, Jesus Christ His Son, and the truths of the gospel.
                                He vitalizes truth in the hearts and souls of men.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"There
                                is a difference," said the Prophet Joseph Smith,
                                "between the Holy Ghost and the gift of the Holy Ghost. Cornelius
                                received
                                the Holy Ghost before he was baptized, which was the convincing power
                                of
                                God unto him of the truth of the Gospel, but he could not receive the
                                gift
                                of the Holy Ghost until after he was baptized. Had he not [been
                                baptized],
                                the Holy Ghost which convinced him of the truth of God, would have left
                                him." (Teachings of the Prophet Joseph Smith, p. 199.) That's not my
                                statement;
                                that's the statement of the Prophet Joseph Smith. But I know it's true.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>The
                                gift of the Holy Ghost confers upon one, as long as
                                he is worthy, the right to receive light and truth.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>Obtaining
                                the gift of the Holy Ghost is preceded by faith,
                                repentance, and baptism. Retaining the spirit, power, and guidance of
                                the
                                Holy Ghost requires a righteous life--a dedicated effort to constantly
                                comply with the laws and ordinances of the gospel.</big></big></font>
                        <br>
                        &nbsp;
                    </big></big></p>
                <p style="font-family: arial;"><big><big><b><font size="-1"><big><big>Member
                                    of the Godhead</big></big></font></b>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>The
                                Holy Ghost is, as we have said, the third member of
                                the Godhead. Of Him the Prophet Joseph said:</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"The
                                Holy Ghost has not a body of flesh and bones [as
                                we know that God and Jesus Christ have], but is a personage of Spirit."
                                (D&amp;C 130:22.)</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>The
                                Holy Ghost is the great witness of, the messenger
                                for, and testifier of the Father and the Son. The Savior, speaking of
                                Him
                                as the "Spirit of truth," said:</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"When
                                he, the Spirit of truth, is come, he will guide
                                you into all truth: for he shall not speak of himself; but whatsoever
                                he
                                shall hear, that shall he speak: and he will shew you things to come.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"He
                                shall glorify me: for he shall receive of mine, and
                                shall shew it unto you." (John 16:13&shy;14.)</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>By
                                the witness and power of the Holy Ghost we receive
                                personal testimonies of the truths of the gospel, including knowledge
                                of
                                God the Father and His beloved Son, Jesus Christ, our Savior and
                                Redeemer.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>Notwithstanding
                                the availability of the gifts of the Holy
                                Ghost, there are many people who live within reach of them who fail to
                                see them. Concerning such tragedy, the Lord said:</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"Behold,
                                I am Jesus Christ, the Son [CR, p. 61] of God.
                                I am the same that came unto mine own, and mine own received me not. I
                                am the light which shineth in darkness, and the darkness comprehendeth
                                it not." (D&amp;C 6:21.)</big></big></font>
                        <br>
                        &nbsp;
                    </big></big></p>
                <p style="font-family: arial;"><big><big><b><font size="-1"><big><big>Comprehending
                                    the light</big></big></font></b>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>All
                                three of the synoptic Gospel writers record the following
                                classic illustration of the difficulty one in darkness has in
                                comprehending
                                the light. Matthew's version reads:</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"When
                                Jesus came into the coasts of Caesarea Philippi,
                                he asked his disciples, saying, Whom do men say that I the Son of man
                                am?</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"And
                                they said, Some say that thou art John the Baptist:
                                some, Elias; and others, Jeremias, or one of the prophets." (Matt. 16:
                                13&shy;14.)</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>Now,
                                the people who expressed these opinions were Christ's
                                contemporaries. Their conclusions evidenced the fact that they knew
                                something
                                about His mighty works. No doubt they were aware of His claim that He
                                was
                                the Son of God. Their minds, however, were opaque to the light of His
                                true
                                identity. Although the light was shining brightly about them, they
                                "comprehended
                                it not."</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>Having
                                heard their answer as to who men said He was, Jesus
                                directed to His disciples the question, "But whom say ye that I am?"
                                (Matt.
                                16:15 [Matthew 16:15] .)</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>Then
                                Peter, speaking for himself and presumptively for
                                the others, answered, "Thou art the Christ, the Son of the living God."
                                (Matt. 16:16 [Matthew 16:16] .) In this declaration, Peter evidenced
                                the
                                fact that he and his fellow disciples did comprehend the light shining
                                in the world of spiritual darkness around them.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>In
                                His response to Peter's answer, Jesus declared a truth
                                understood only by those who comprehend the light by and through the
                                gift
                                and power of the Holy Ghost, for Jesus' answer was:</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"Flesh
                                and blood hath not revealed it unto thee," He said,
                                "but my Father which is in heaven, . . . and upon this rock"--meaning,
                                upon the rock of revelation, which comes by means of the Holy Ghost--"I
                                will build my church; and the gates of hell shall not prevail against
                                it."
                                (Matt. 16:17&shy;18 [Matthew 16:17&shy;18] .)</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>How
                                difficult it is to get one in darkness to comprehend
                                the light or to believe that there is such light is illustrated by
                                John's
                                account of the interview between Jesus and Nicodemus.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"There
                                was," says John, "a man of the Pharisees, named
                                Nicodemus, a ruler of the Jews:</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"[Who]
                                came to Jesus by night, and said unto him, Rabbi,
                                we know that thou art a teacher come from God: for no man can do these
                                miracles that thou doest, except God be with him.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"Jesus
                                answered and said . . . Verily, verily, I say unto
                                thee, Except a man be born again, he cannot see the kingdom of God.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"Nicodemus
                                saith unto him, How can a man be born when
                                he is old? can he enter the second time into his mother's womb, and be
                                born?</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"Jesus
                                answered, Verily, verily, I say unto thee, Except
                                a man be born of water and of the Spirit, he cannot enter into the
                                kingdom
                                of God." (John 3:1&shy;5; see also 3:6&shy;10.)</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>One
                                is born again by actually receiving and experiencing
                                the light and power inherent in the gift of the Holy Ghost.</big></big></font>
                        <br>
                        &nbsp;
                    </big></big></p>
                <p style="font-family: arial;"><big><big><b><font size="-1"><big><big>"More
                                    sure word of prophecy"</big></big></font></b>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>Now,
                                concerning the third phase of our theme, "the more
                                sure word of prophecy" (D&amp;C 131:5), which is obtained by making
                                one's
                                "calling and election sure" (2 Pet. 1:10 [2 Peter 1:10] ), the Prophet
                                Joseph said:</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"After
                                a person has faith in Christ, repents of his sins,
                                and is baptized for the remission of his sins and receives the Holy
                                Ghost,
                                (by the laying on of hands) . . . then let him continue to humble
                                himself
                                before God, hungering and thirsting after righteousness, and [CR, p.
                                62]
                                living by every word of God, and the Lord will soon say unto him, Son,
                                thou shalt be exalted. When the Lord has thoroughly proved him, and
                                finds
                                that the man is determined to serve Him at all hazards, then the man
                                will
                                find his calling and his election made sure, then it will be his
                                privilege
                                to receive the other Comforter, which the Lord hath promised the
                                Saints,
                                as is recorded in the testimony of St. John." (Teachings of the Prophet
                                Joseph Smith, p. 150.)</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>In
                                the eighty-eighth section of the Doctrine and Covenants
                                is recorded a revelation in which the Lord, addressing some of the
                                early
                                Saints in Ohio, said:</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"I
                                now send upon you another Comforter, even upon you
                                my friends, that it may abide in your hearts, even the Holy Spirit of
                                promise;
                                which other Comforter is the same that I promised unto my disciples, as
                                is recorded in the testimony of John.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"This
                                Comforter is the promise which I give unto you of
                                eternal life, even the glory of the celestial kingdom." (D&amp;C
                                88:3&shy;4.)</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>I
                                should think that all faithful Latter-day Saints "would
                                want that more sure word of prophecy, that they were sealed in the
                                heavens
                                and had the promise of eternal life in the kingdom of God." (History of
                                the Church of Jesus [Ensign, p. 45] Christ of Latter-day Saints, 5:388.)</big></big></font>
                        <br>
                        &nbsp;
                    </big></big></p>
                <p style="font-family: arial;"><big><big><b><font size="-1"><big><big>Anchor
                                    to men's souls</big></big></font></b>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>As I
                                read the sacred records, I find recorded experiences
                                of men in all dispensations who have had this more sure anchor to their
                                souls, this peace in their hearts.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>Lehi's
                                grandson Enos so hungered after righteousness that
                                he cried unto the Lord until "there came a voice unto [him], saying:
                                Enos,
                                thy sins are forgiven thee, and thou shalt be blessed." (Enos 5 [Enos
                                1:5]
                                .) Years later Enos revealed the nature of this promised blessing when
                                he wrote:</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"I
                                soon go to the place of my rest, which is with my Redeemer;
                                for I know that in him I shall rest. And I rejoice in the day when my
                                mortal
                                shall put on immortality, and shall stand before him; then shall I see
                                his face with pleasure, and he will say unto me: Come unto me, ye
                                blessed,
                                there is a place prepared for you in the mansions of my Father." (Enos
                                27 [Enos 1:27] .)</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>To
                                Alma the Lord said, "Thou art my servant; and I covenant
                                with thee that thou shalt have eternal life." (Mosiah 26:20.)</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>To
                                His twelve Nephite disciples, the Master said:</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"What
                                is it that ye desire of me, after that I am gone
                                to the Father?</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"And
                                they all spake, save it were three, saying: We desire
                                that after we have lived unto the age of man, that our ministry,
                                wherein
                                thou hast called us, may have an end, that we may speedily come unto
                                thee
                                in thy kingdom.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"And
                                he said unto them: Blessed are ye because ye desired
                                this thing of me; therefore, after that ye are seventy and two years
                                old
                                ye shall come unto me in my kingdom; and with me ye shall find rest."
                                (3
                                Ne. 28:1&shy;3 [3 Nephi 28:1&shy;3] .)</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>As
                                Moroni labored in solitude abridging the Jaredite record,
                                he received from the Lord this comforting assurance:</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"Thou
                                hast been faithful; wherefore, thy garments shall
                                be made clean. And because thou hast seen thy weakness thou shalt be
                                made
                                strong, even unto the sitting down in the place which I have prepared
                                in
                                the mansions of my Father." (Eth. 12:37 [Ether 12:37] .)</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>Paul,
                                in his second epistle to Timothy, wrote:</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"I
                                am now ready to be offered, and the time of my departure
                                is at hand.</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"I
                                have fought a good fight, I have finished my course,
                                I have kept the faith:</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"Henceforth
                                there is laid up for me a crown of righteousness,
                                which the Lord, the righteous judge, shall give me at that day." (2
                                Tim.
                                4:6&shy;8 [2 Timothy 4:6&shy;8] .)</big></big></font>
                        <br>
                        &nbsp;
                    </big></big></p>
                <p style="font-family: arial;"><big><big><b><font size="-1"><big><big>Assurances
                                    in our day</big></big></font></b>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>In
                                this dispensation many have received like assurances.
                                In the spring of 1839, while the Prophet Joseph and his associates were
                                languishing in Liberty Jail, Heber C. Kimball, our president's
                                grandfather,
                                labored against great odds caring for the Saints and striving to free
                                the
                                brethren who were in jail. On the sixth of April he wrote:</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"My
                                family having been gone about two months, during which
                                time I heard nothing from them; our brethren being in prison; death and
                                destruction following us everywhere we went; I felt very sorrowful and
                                lonely. The following words came to mind, and the Spirit said unto me,
                                'write,' which I did by taking a piece of paper and writing on my knee
                                as follows: . . .</big></big></font>
                    </big></big></p>
                <p style="font-family: arial;"><big><big><font size="-1"><big><big>"Verily
