                <center style="font-family: arial;"><big>
                    </big>
                    <h2><big><i>The King Follett Discourse</i></big></h2>
                </center>
                <center style="font-family: arial;"><i>The Being and Kind of Being God
                        Is;</i>
                    <br>
                    <i>The Immortality of the Intelligence of Man</i>
                    <p><font size="+1">Joseph Smith</font>
                        <br>
                        April 7, 1844
                        <br>
                        <i>History of the Church</i>, 6:302-317</p>
                </center>
                <p style="font-family: arial;"><br>
                </p>
                <p style="font-family: arial;"><big><i>President Joseph Smith delivered
                        the following discourse before about
                        twenty thousand Saints at the April General Conference of the Church,
                        1844,
                        being the funeral sermon of Elder King Follett. Reported by Willard
                        Richards,
                        Wilford Woodruff, Thomas Bullock and William Clayton.</i>
                </big></p>
                <p style="font-family: arial;"><big>Beloved Saints, I will call the
                    attention of this congregation while
                    I address you on the subject of the dead. The decease of our beloved
                    brother,
                    Elder King Follett, who was crushed in a well by the falling of a tub
                    of
                    rock, has more immediately led me to that subject. I have been
                    requested
                    to speak by his friends and relatives, but inasmuch as there are a
                    great
                    many in this congregation who live in this city as well as elsewhere,
                    who
                    have lost friends, I feel disposed to speak on the subject in general,
                    and offer you my ideas, so far as I have ability, and so far as I shall
                    be inspired by the Holy Spirit to dwell on this subject.
                </big></p>
                <p style="font-family: arial;"><big>I want your prayers and faith that
                    I may have the instruction of Almighty
                    God and the gift of the Holy Ghost, so that I may set forth things that
                    are true and which can be easily comprehended by you, and that the
                    testimony
                    may carry conviction to your hearts and minds of the truth of what I
                    shall
                    say. Pray that the Lord may strengthen my lungs, stay the winds, and
                    let
                    the prayers of the Saints to heaven appear, that they may enter into
                    the
                    ears of the Lord of Sabbaoth, for the effectual prayers of the
                    righteous
                    avail much. There is strength here, and I verily believe that your
                    prayers
                    will be heard.
                </big></p>
                <p style="font-family: arial;"><big>Before I enter fully into the
                    investigation of the subject which is
                    lying before me, I wish to pave the way and bring up the subject from
                    the
                    beginning, that you may understand it. I will make a few preliminaries,
                    in order that you may understand the subject when I come to it. I do
                    not
                    intend to please your ears with superfluity of words or oratory, or
                    with
                    much learning; but I intend to edify you with the simple truths from
                    heaven.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>The Character of God</b></big></p>
                </center>
                <p style="font-family: arial;"><big>In the first place, I wish to go
                    back to the beginning -- to the morn
                    of creation. There is the starting point for us to look to, in order to
                    understand and be fully acquainted with the mind, purposes and decrees
                    of the Great Elohim, who sits in yonder heavens as he did at the
                    creation
                    of this world. It is necessary for us to have an understanding of God
                    himself
                    in the beginning. If we start right, it is easy to go right all the
                    time;
                    but if we start wrong, we may go wrong, and it be a hard matter to get
                    right.
                </big></p>
                <p style="font-family: arial;"><big>There are but a very few beings in
                    the world who understand rightly
                    the character of God. The great majority of mankind do not comprehend
                    anything,
                    either that which is past, or that which is to come, as it respects
                    their
                    relationship to God. They do not know, neither do they understand the
                    nature
                    of that relationship; and consequently they know but little above the
                    brute
                    beast, or more than to eat, drink and sleep. This is all man knows
                    about
                    God or his existence, unless it is given by the inspiration of the
                    Almighty.
                </big></p>
                <p style="font-family: arial;"><big>If a man learns nothing more than
                    to eat, drink and sleep, and does
                    not comprehend any of the designs of God, the beast comprehends the
                    same
                    things. It eats, drinks, sleeps, and knows nothing more about God; yet
                    it knows as much as we, unless we are able to comprehend by the
                    inspiration
                    of Almighty God. If men do not comprehend the character of God, they do
                    not comprehend themselves. I want to go back to the beginning, and so
                    lift
                    you minds into a more lofty sphere and a more exalted understanding
                    than
                    what the human mind generally aspires to.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>What Kind of Being Is God?</b></big></p>
                </center>
                <p style="font-family: arial;"><big>I want to ask this congregation,
                    every man, woman and child, to answer
                    the question in their own heart, what kind of a being God is? Ask
                    yourselves;
                    turn your thought into your hearts, and say if any of you have seen,
                    heard,
                    or communed with him. This is a question the may occupy your attention
                    for a long time. I again repeat the question -- What kind of a being is
                    God? Does any man or woman know? Have any of you seen him, heard him,
                    or
                    communed with him? Here is the question that will, peradventure, from
                    this
                    time henceforth occupy your attention. The Scriptures inform us that
                    "This
                    is life eternal that they might know thee, the only true God, and Jesus
                    Christ whom thou hast sent."
                </big></p>
                <p style="font-family: arial;"><big>If any man does not know God, and
                    inquires what kind of a being he is,
                    -- if he will search diligently his own heart -- if the declaration of
                    Jesus and the apostles be true, he will realize that he has not eternal
                    life; for there can be eternal life on no other principle.
                </big></p>
                <p style="font-family: arial;"><big>My first object is to find out the
                    character of the only wise and true
                    God, and what kind of a being he is; and if I am so fortunate as to be
                    the man to comprehend God, and explain or convey the principles to your
                    hearts, so that the Spirit seals them upon you, then let every man and
                    woman henceforth sit in silence, put their hands on their mouths, and
                    never
                    lift their hands or voices, or say anything against the man of God or
                    the
                    servants of God again. But if I fail to do it, it becomes my duty to
                    renounce
                    all further pretensions to revelations and inspirations, or to be a
                    prophet;
                    and I should be like the rest of the world -- a false teacher, be
                    hailed
                    as a friend, and no man would seek my life. But if all religious
                    teachers
                    were honest enough to renounce their pretensions to godliness when
                    their
                    ignorance of the knowledge of God is made manifest, they will all be as
                    badly off as I am, at any rate; and you might as well take the lives of
                    other false teachers as that of mine, if I am false. If any man is
                    authorized
                    to take away my life because he thinks and says I am a false teacher,
                    then,
                    upon the same principle, we should be justified in taking away the life
                    of every false teacher, and where would be the end of blood? And who
                    would
                    not be the sufferer?
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>The Privilege of Religious Freedom</b></big></p>
                </center>
                <p style="font-family: arial;"><big>But meddle not with any man for his
                    religion: and all governments ought
                    to permit every man to enjoy his religion unmolested. No man is
                    authorized
                    to take away life in consequence of difference of religion, which all
                    laws
                    and governments ought to tolerate and protect, right or wrong. Every
                    man
                    has a natural, and, in our country, a constitutional right to be a
                    false
                    prophet, as well as a true prophet. If I show, verily, that I have the
                    truth of God, and show that ninety-nine out of every hundred professing
                    religious ministers are false teachers, having no authority, while they
                    pretend to hold the keys of God's kingdom on earth, and was to kill
                    them
                    because they are false teachers, it would deluge the whole world with
                    blood.
                </big></p>
                <p style="font-family: arial;"><big>I will prove that the world is
                    wrong, by showing what God is. I am going
                    to enquire after God; for I want you all to know him, and to be
                    familiar
                    with him; and if I am bringing you to a knowledge of him, all
                    persecutions
                    against me ought to cease. You will then know that I am his servant;
                    for
                    I speak as one having authority.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>God an Exalted Man</b></big></p>
                </center>
                <p style="font-family: arial;"><big>I will go back to the beginning
                    before the world was, to show what kind
                    of being God is. What sort of a being was God in the beginning? Open
                    your
                    ears and hear, all ye ends of the earth, for I am going to prove it to
                    you by the Bible, and to tell you the designs of God in relation to the
                    human race, and why He interferes with the affairs of man.
                </big></p>
                <p style="font-family: arial;"><big><i>God himself was once as we are
                        now, and is an exalted man, and sits
                        enthroned in yonder heavens! That is the great secret. If the veil were
                        rent today, and the great God who holds this world in its orbit, and
                        who
                        upholds all worlds and all things by his power, was to make himself
                        visible,
                        -- I say, if you were to see him today, you would see him like a man in
                        form -- like yourselves in all the person, image, and very form as a
                        man;
                        for Adam was created in the very fashion, image and likeness of God,
                        and
                        received instruction from, and walked, talked and conversed with him,
                        as
                        one man talks and communes with another.</i>
                </big></p>
                <p style="font-family: arial;"><big>In order to understand the subject
                    of the dead, for consolation of those
                    who mourn for the loss of their friends, it is necessary we should
                    understand
                    the character and being of God and how he came to be so; for I am going
                    to tell you how God came to be God. We have imagined and supposed that
                    God was God from all eternity. I will refute that idea, and take away
                    the
                    veil, so that you may see.
                </big></p>
                <p style="font-family: arial;"><big>These are incomprehensible ideas to
                    some, but they are simple. <i>It
                        is the first principle of the Gospel to know for a certainty the
                        Character
                        of God, and to know that we may converse with him as one man converses
                        with another, and that he was once a man like us; yea, that God
                        himself,
                        the Father of us all, dwelt on an earth, the same as Jesus Christ
                        himself
                        did; and I will show it from the Bible.</i>
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>Power of the Father and the Son</b></big></p>
                </center>
                <p style="font-family: arial;"><big>I wish I was in a suitable place to
                    tell it, and that I had the trump
                    of an archangel, so that I could tell the story in such a manner that
                    persecution
                    would cease for ever. What did Jesus say? (Mark it, Elder Rigdon!) The
                    Scriptures inform us that Jesus said, As the Father hath power in
                    Himself,
                    even so hath the Son power -- to do what? Why, what the Father did. The
                    answer is obvious -- in a manner to lay down His body and take it up
                    again.
                    Jesus, what are you going to do? To lay down my life as my Father did,
                    and take it up again. Do we believe it? I you do not believe it, you do
                    not believe the Bible. The Scriptures say it, and I defy all the
                    learning
                    and wisdom and all the combined powers of earth and hell together to
                    refute
                    it.
                </big></p>
                <p style="font-family: arial;"><big>Here, then, is eternal life -- to
                    know the only wise and true God; and
                    you have got to learn how to be Gods yourselves, and to be kings and
                    priests
                    to God, the same as all Gods have done before you, namely, by going
                    from
                    one small degree to another, and from a small capacity to a great one;
                    from grace to grace, from exaltation to exaltation, until you attain to
                    the resurrection of the dead, and are able to dwell in everlasting
                    burnings,
                    and to sit in glory, as do those who sit enthroned in everlasting
                    power.
                    And I want you to know that God, in the last days, while certain
                    individuals
                    are proclaiming his name, is not trifling with you or me.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>The Righteous to Dwell in Everlasting Burnings</b></big></p>
                </center>
                <p style="font-family: arial;"><big>These are the first principles of
                    consolation. How consoling to the
                    mourners when they are called to part with a husband, wife, father,
                    mother,
                    child, or dear relative, to know that, although the earthly tabernacle
                    is laid down and dissolved, they shall rise again to dwell in
                    everlasting
                    burnings in immortal glory, not to sorrow, suffer, or die any more; but
                    they shall be heirs of God and joint heirs with Jesus Christ. What is
                    it?
                    To inherit the same power, the same glory and the same exaltation,
                    until
                    you arrive at the station of a God, and ascend the throne of eternal
                    power,
                    the same as those who have gone before. What did Jesus do? Why; I do
                    the
                    things I saw my Father do when worlds come rolling into existence. My
                    Father
                    worked out his kingdom with fear and trembling, and I must do the same;
                    and when I get my kingdom, I shall present it to my Father, so that he
                    may obtain kingdom upon kingdom, and it will exalt him in glory. He
                    will
                    then take a higher exaltation, and I will take his place, and thereby
                    become
                    exalted myself. So that Jesus treads in the tracks of his Father, and
                    inherits
                    what God did before; and God is thus glorified and exalted in the
                    salvation
                    and exaltation of all his children. It is plain beyond disputation, and
                    you thus learn some of the first principles of the Gospel, about which
                    so much hath been said.
                </big></p>
                <p style="font-family: arial;"><big><i>When you climb up a ladder, you
                        must begin at the bottom, and ascend
                        step by step, until you arrive at the top; and so it is with the
                        principles
                        of the Gospel -- you must begin with the first, and go on until you
                        learn
                        all the principles of exaltation. But it will be a great while after
                        you
                        have passed through the veil before you will have learned them.</i> <i>It
                        is not all</i> to be comprehended in this world; it will be a great
                    work
                    to learn our salvation <i>and exaltation even beyond the grave.</i> I
                    suppose
                    I am not allowed to go into an investigation of anything that is not
                    contained
                    in the Bible. If I do, I think there are so many over-wise men here,
                    that
                    they would cry "treason" and put me to death. So I will go to the old
                    Bible
                    and turn commentator today.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>Meaning of the Hebrew Scriptures</b></big></p>
                </center>
                <p style="font-family: arial;"><big>I shall comment on the very first
                    Hebrew word in the Bible; I will make
                    a comment on the very first sentence of the history of the creation in
                    the Bible -- <i>Berosheit</i>. I want to analyze the word. <i>Baith</i>
                    -- in, by through, and everything else. <i>Rosh</i> -- the head. <i>Sheit</i>
                    -- grammatical termination. When the inspired man wrote it, he did not
                    put the baith there. An old Jew without any authority added the word;
                    he
                    thought it too bad to begin to talk about the head! It read first, "The
                    head one of the Gods brought forth the Gods." That is the true meaning
                    of the words. <i>Baurau</i> signifies to bring forth. If you do not
                    believe
                    it, you do not believe the learned man of God. Learned men can teach
                    you
                    no more than what I have told you. <i>Thus the head God brought forth
                        the
                        Gods in the grand council.</i>
                </big></p>
                <p style="font-family: arial;"><big>I will transpose and simplify it in
                    the English language. Oh, ye lawyers,
                    ye doctors, and ye priests, who have persecuted me, I want to let you
                    know
                    that the Holy Ghost knows something as well as you do. The head God
                    called
                    together the Gods and sat in grand council to bring forth the world.
                    The
                    grand councilors sat at the head in yonder heavens and contemplated the
                    creation of the worlds which were created at the time. When I say
                    doctors
                    and lawyers, I mean the doctors and lawyers of the Scriptures. I have
                    done
                    so hitherto without explanation, to let the lawyers flutter and
                    everybody
                    laugh at them. Some learned doctors might take a notion to say the
                    Scriptures
                    say thus and so; and we might believe the Scriptures; they are not to
                    be
                    altered. But I am going to show you an error in them.
                </big></p>
                <p style="font-family: arial;"><big>I have an old edition of the New
                    Testament in the Latin, Hebrew, German
                    and Greek languages. I have been reading the German, and find it to be
                    the most [nearly] correct translation, and to correspond nearest to the
                    revelations which God has given to me for the last fourteen years. It
                    tells
                    about Jacobus, the son of Zebedee. It means Jacob. In the English New
                    Testament
                    it is translated James. Now, if Jacob had the keys, you might talk
                    about
                    James through all eternity and never get the keys. In the 21st of the
                    fourth
                    chapter of Matthew, my old German edition gives the word Jacob instead
                    of James.
                </big></p>
                <p style="font-family: arial;"><big>The doctors (I mean doctors of law,
                    no physic) say, "If you preach anything
                    no according to the Bible, we will cry treason." How can we escape the
                    damnation of hell, except God be with us and reveal to us? Men bind us
                    with chains. The Latin says Jacob and the german says Jacob; here we
                    have
                    the testimony of four against one. I thank God that I have got this old
                    book; but I thank him more for the gift of the Holy Ghost. I have got
                    the
                    oldest book in the world; but I [also] have the oldest book in my
                    heart,
                    even the gift of the Holy Ghost. I have all the four Testaments. Come
                    here,
                    ye learned men, and read, if you can. I should not have introduced this
                    testimony, were it not to back up the word rosh -- the head, the Father
                    of the Gods. I should not have brought it up, only to show that I am
                    right.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>A Council of the Gods</b></big></p>
                </center>
                <p style="font-family: arial;"><big>In the beginning, the head of the
                    Gods called a council of the Gods;
                    and they came together and concocted a plan to create the world and
                    people
                    it. When we begin to learn this way, we begin to learn the only true
                    God,
                    and what kind of a being we have got to worship. Having a knowledge of
                    God, we begin to know how to approach him, and how to ask so as to
                    receive
                    an answer. When we understand the character of God, and how to come to
                    him, he begins to unfold the heavens to us, and to tell us all about
                    it.
                    When we are ready to come to him, he is ready to come to us.
                </big></p>
                <p style="font-family: arial;"><big>Now, I ask all who hear me, why the
                    learned men who are preaching salvation,
                    say that God created the heavens and the earth out of nothing? The
                    reason
                    is, that they are unlearned in the things of God, and have not the gift
                    of the Holy Ghost; they account it blasphemy in any one to contradict
                    their
                    idea. If you tell them that God made the world out of something, they
                    will
                    call you a fool. But I am learned, and know more than all the world put
                    together. The Holy Ghost does, anyhow, and He is within me, and
                    comprehends
                    more than all the world: and I will associate myself with Him.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>Meaning of the Word Create</b></big></p>
                </center>
                <p style="font-family: arial;"><big>You ask the learned doctors why
                    they say the world was made out of nothing;
                    and they will answer, "Doesn't the Bible say He <i>created</i> the
                    world?"
                    And they infer, from the word create, that it must have been made out
                    of
                    nothing. Now, the word create came from the <i>baurau</i> which does
                    not
                    mean to create out of nothing; it means to organize; the same as a man
                    would organize materials and build a ship. Hence, we infer that God had
                    materials to organize the world out of chaos -- chaotic matter, which
                    is
                    element, and in which dwells all the glory. Element had an existence
                    from
                    the time he had. The pure principles of element are principles which
                    can
                    never be destroyed; they may be organized and re-organized, but not
                    destroyed.
                    They had no beginning, and can have no end.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>The Immortal Spirit</b></big></p>
                </center>
                <p style="font-family: arial;"><big>I have another subject to dwell
                    upon, which is calculated to exalt man;
                    but it is impossible for me to say much on this subject. I shall
                    therefore
                    just touch upon it, for time will not permit me to say all. It is
                    associated
                    with the subject of the resurrection of the dead, -- namely, the soul
                    --
                    the mind of man -- the immortal spirit. Where did it come from? All
                    learned
                    men and doctors of divinity say that God created it in the beginning;
                    but
                    it is not so: the very idea lessens man in my estimation. I do not
                    believe
                    the doctrine; I know better. Hear it, all ye ends of the world; for God
                    has told me so; and if you don't believe me, it will not make the truth
                    without effect. I will make a man appear a fool before I get through;
                    if
                    he does not believe it. I am going to tell of things more noble.
                </big></p>
                <p style="font-family: arial;"><big>We say that God himself is a
                    self-existent being. Who told you so? It
                    is correct enough; but how did it get into you heads? Who told you that
                    man did not exist in like manner upon the same principles? Man does
                    exist
                    upon the same principles. God made a tabernacle and put a spirit into
                    it,
                    and it became a living soul. (Refers to the old Bible.) How does it
                    read
                    in the Hebrew? It does not say in the Hebrew that God created the
                    spirit
                    of man. It says "God made man out of the earth and put into him Adam's
                    spirit, and so became a living body."
                </big></p>
                <p style="font-family: arial;"><big>The mind or the intelligence which
                    man possesses is co-equal with God
                    himself. I know that my testimony is true; hence, when I talk to these
                    mourners, what have they lost? Their relatives and friends are only
                    separated
                    from their bodies for a short season: their spirits which existed with
                    God have left the tabernacle of clay only for a little moment, as it
                    were;
                    and they now exist in a place where they converse together the same as
                    we do on the earth.
                </big></p>
                <p style="font-family: arial;"><big>I am dwelling on the immortality of
                    the spirit of man. Is it logical
                    to say that the intelligence of spirits is immortal, and yet that it
                    had
                    a beginning? The intelligence of spirits had not beginning, neither
                    will
                    it have an end. That is good logic. That which has a beginning may have
                    an end. There never was a time when there were not spirits; for they
                    are
                    co-equal [co-eternal] with our Father in heaven.
                </big></p>
                <p style="font-family: arial;"><big>I want to reason more on the spirit
                    of man; for I am dwelling on the
                    body and spirit of man -- on the subject of the dead. I take my ring
                    from
                    my finger and liken it unto the mind of man -- the immortal part,
                    because
                    it has no beginning. Suppose you cut it in two; then it has a beginning
                    and an end; but join it again, and it continues one eternal round. So
                    with
                    the spirit of man. As the Lord liveth, if it had a beginning, it will
                    have
                    an end. All the fools and learned and wise men from the beginning of
                    creation,
                    who say that the spirit of man had a beginning, prove that it must have
                    an end; and if that doctrine is true, then the doctrine of annihilation
                    would be true. But if I am right, I might with boldness proclaim from
                    the
                    house-tops that God never had the power to create the spirit of man at
                    all. God himself could not create himself.
                </big></p>
                <p style="font-family: arial;"><big>Intelligence is eternal and exists
                    upon a self-existent principle. It
                    is a spirit from age to age, and there is no creation about it. All the
                    minds and spirits that God ever sent into the world are susceptible of
                    enlargement.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>The Power to Advance in Knowledge</b></big></p>
                </center>
                <p style="font-family: arial;"><big>The first principles of man are
                    self-existent with God. God himself,
                    finding he was in the midst of spirits and glory, because he was more
                    intelligent,
                    saw proper to institute laws whereby the rest could have a privilege to
                    advance like himself. The relationship we have with God places us in a
                    situation to advance in knowledge. He has power to institute laws to
                    instruct
                    the weaker intelligences, that they may be exalted with himself, so
                    that
                    they might have one glory upon another, and all that knowledge, power,
                    glory, and intelligence, which is requisite in order to save them in
                    the
                    world of spirits.
                </big></p>
                <p style="font-family: arial;"><big>This is good doctrine. It tastes
                    good. I can taste the principles of
                    eternal life, and so can you. They are given to my by the revelations
                    of
                    Jesus Christ; and I know that when I tell you these words of eternal
                    life
                    as they are given to me, you taste them, and I know that you believe
                    them.
                    You say honey is sweet, and so do I. I can also taste the spirit of
                    eternal
                    life. I know it is good; and when I tell you of these things which were
                    given my be inspiration of the Holy Spirit, you are bound to receive
                    them
                    as sweet, and rejoice more and more.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>The Relation of Man to God</b></big></p>
                </center>
                <p style="font-family: arial;"><big>I want to talk more of the relation
                    of man to God. I will open your
                    eyes in relation to your dead. All things whatsoever God in his
                    infinite
                    wisdom has seen fit and proper to reveal to us, while we are dwelling
                    in
                    mortality, in regard to our mortal bodies, are revealed to us in the
                    abstract,
                    and independent of affinity of this mortal tabernacle, but are revealed
                    to our spirits precisely as though we had no bodies at all; and those
                    revelations
                    which will save our spirits will save our bodies. God reveals them to
                    us
                    in view of no eternal dissolution of the body, or tabernacle. Hence the
                    responsibility, the awful responsibility, that rests upon us in
                    relation
                    to our dead; for all the spirits who have not obeyed the Gospel in the
                    flesh must either obey it in the spirit or be damned. Solemn thought!
                    --
                    dreadful thought! Is there nothing to be done? -- no preparation -- no
                    salvation for our fathers and friends who have died without having had
                    the opportunity to obey the decrees of the Son of Man? Would to God
                    that
                    I had forty days and nights in which to tell you all! I would let you
                    know
                    that I am not a "fallen prophet."
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>Our Greatest Responsibility</b></big></p>
                </center>
                <p style="font-family: arial;"><big>What promises are made in relation
                    to the subject of the salvation of
                    the dead? and what kind of characters are those who can be saved,
                    although
                    their bodies are mouldering and decaying in the grave? When his
                    commandments
                    teach us, it is in view of eternity; for we are looked upon by God as
                    though
                    we were in eternity. God dwells in eternity, and does not view things
                    as
                    we do.
                </big></p>
                <p style="font-family: arial;"><big>The greatest responsibility in this
                    world that God has laid upon us
                    is to seek after our dead. The Apostle says, "They without us cannot be
                    made perfect;" (Hebrews 11:40) for it is necessary that the sealing
                    power
                    should be in our hands to seal our children and our dead for the
                    fulness
                    of the dispensation of times -- a dispensation to meet the promises
                    made
                    by Jesus Christ before the foundation of the world for the salvation of
                    man.
                </big></p>
                <p style="font-family: arial;"><big>Now, I will speak of them. I will
                    meet Paul half way. I say to you,
                    Paul, you cannot be perfect without us. It is necessary that those who
                    are going before and those who come after us should have salvation in
                    common
                    with us; and thus hath God made it obligatory upon man. Hence, God
                    said,
                    "I will send you Elijah the prophet before the coming of the great and
                    dreadful day of the Lord; and he shall turn the heart of the fathers to
                    the children, and the heart of the children to their fathers, lest I
                    come
                    and smite the earth with a curse." (Malachi 4:5-6.)
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>A Salvation for Men</b></big></p>
                </center>
                <p style="font-family: arial;"><big>I have a declaration to make as to
                    the provisions which God hath made
                    to suit the conditions of man -- made for before the foundation of the
                    world. What has Jesus said? All sin, and all blasphemies, and every
                    transgression,
                    except one, that man can be guilty of, may be forgiven; and there is a
                    salvation for all men, either in this world or the world to come, who
                    have
                    not committed the unpardonable sin, there being a provision either in
                    this
                    world or the world of spirits. Hence God hath made a provision that
                    every
                    spirit in the eternal world can be ferreted out and saved unless he has
                    committed that unpardonable sin which cannot be remitted to him either
                    in this world or the world of spirits. God has wrought out a salvation
                    for all men, unless they have committed a certain sin; and every man
                    who
                    has a friend in the eternal world can save him, unless he has committed
                    the unpardonable sin. And so you can see how far you can be a savior.
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>The Unpardonable Sin</b></big></p>
                </center>
                <p style="font-family: arial;"><big>A man cannot commit the
                    unpardonable sin after the dissolution of the
                    body, and there is a way possible for escape. Knowledge saves a man;
                    and
                    in the world of spirits no man can be exalted but by knowledge. So long
                    as a man will not give heed to the commandments, he must abide without
                    salvation. If a man has knowledge, he can be saved; although, if he has
                    been guilty of great sins, he will be punished for them. But when he
                    consents
                    to obey the Gospel, whether here or in the world of spirits, he is
                    saved.
                </big></p>
                <p style="font-family: arial;"><big>A man is his own tormenter and his
                    own condemner. Hence the saying,
                    They shall go into the lake that burns with fire and brimstone. The
                    torment
                    of disappointment in the mind of man is as exquisite as a lake burning
                    with fire and brimstone. I say, so is the torment of man.
                </big></p>
                <p style="font-family: arial;"><big>I know the Scriptures and
                    understand them. I said, no man can commit
                    the unpardonable sin after the dissolution of the body, nor in this
                    life,
                    until he receives the Holy Ghost but they must do it in this world.
                    Hence
                    the salvation of Jesus Christ was wrought out for all men, in order to
                    triumph over the devil; for if it did not catch him in one place, it
                    would
                    in another; for he stood up as a Savior. All will suffer until they
                    obey
                    Christ himself.
                </big></p>
                <p style="font-family: arial;"><big>The contention in heaven was --
                    Jesus said there would be certain souls
                    that would not be saved; and the devil said he could save them all, and
                    laid his plans before the grand council, who gave their vote in favor
                    of
                    Jesus Christ. So the devil rose up in rebellion against God, and was
                    cast
                    down, with all who put up their heads for him. (Book of Moses -- Pearl
                    of Great Price, Chap. 4:1-4; Book of Abraham, Chap. 3:23-28.)
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>The Forgiveness of Sins</b></big></p>
                </center>
                <p style="font-family: arial;"><big>All sins shall be forgiven, except
                    the sin against the Holy Ghost; for
                    Jesus will save all except the sons of perdition. What must a man do to
                    commit the unpardonable sin? He must receive the Holy Ghost, have the
                    heavens
                    opened unto him, and know God, and then sin against Him. After a man
                    has
                    sinned against the Holy Ghost, there is no repentance for him. He has
                    got
                    to say that the sun does not shine while he sees it; he has got to deny
                    Jesus Christ when the heavens have been opened unto him, and to deny
                    the
                    plan of salvation with his eyes open to the truth of it; and from that
                    time he begins to be an enemy. This is the case with many apostates of
                    the Church of Jesus Christ of Latter-day Saints.
                </big></p>
                <p style="font-family: arial;"><big>When a man begins to be an enemy to
                    this work, he hunts me, he seeks
                    to kill me, and never ceases to thirst for my blood. He gets the spirit
                    of the devil -- the same spirit that they had who crucified the Lord of
                    Life -- the same spirit that sins against the Holy Ghost. You cannot
                    save
                    such persons; you cannot bring them to repentance; they make open war,
                    like the devil, and awful is the consequence.
                </big></p>
                <p style="font-family: arial;"><big>I advise all of you to be careful
                    what you do, or you may by-and-by
                    find out that you have been deceived. Stay yourselves; do not give way;
                    don't make any hasty moves, you may be saved. If a spirit of bitterness
                    is in you, don't be in haste. You may say, that man is a sinner. Well,
                    if he repents, he shall be forgiven. Be cautious: await. When you find
                    a spirit that wants bloodshed -- murder, the same is not of God, but is
                    of the devil. Out of the abundance of the heart of man the mouth
                    speaketh.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>"In My Father's House"</b></big></p>
                </center>
                <p style="font-family: arial;"><big>The best men bring forth the best
                    works. The man who tells you words
                    of life is the man who can save you. I warn you against all evil
                    characters
                    who sin against the Holy Ghost; for there is no redemption for them in
                    this world nor in the world to come.
                </big></p>
                <p style="font-family: arial;"><big>I could go back and trace every
                    subject of interest concerning the relationship
                    of man to God, if I had time. I can enter into the mysteries; I can
                    enter
                    largely into the eternal worlds; for Jesus said, "In my Father's house
                    are many mansions; if it were not so, I would have told you. I go to
                    prepare
                    a place for you" (John 14:2). Paul says, "There is one glory of the
                    sun,
                    and another glory of the moon, and another glory of the stars; for one
                    star differeth from another star in glory. So also is the resurrection
                    of the dead" (1 Cor. 15:41). What have we to console us in relation to
                    the dead? We have reason to have the greatest hope and consolations for
                    our dead of any people on the earth; for we have seen them walk
                    worthily
                    in our midst, and seen them sink asleep in the arms of Jesus; and those
                    who have died in the faith are now in the celestial kingdom of God. And
                    hence is the glory of the sun.
                    <br>
                    &nbsp;
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>Righteous Mourners Rejoice</b></big></p>
                </center>
                <p style="font-family: arial;"><big>You mourners have occasion to
                    rejoice, speaking of the death of Elder
                    King Follett; for your husband and father is gone to wait until the
                    resurrection
                    of the dead -- until the perfection of the remainder; for at the
                    resurrection
                    your friend will rise in perfect felicity and go to celestial glory,
                    while
                    many must wait myriads of years before they can receive the like
                    blessings;
                    and your expectations and hopes are far above what man can conceive;
                    for
                    why has God revealed it to us?
                </big></p>
                <p style="font-family: arial;"><big>I am authorized to say, by the
                    authority of the Holy Ghost, that you
                    have no occasion to fear; for he is gone to the home of the just. Don't
                    mourn, don't weep. I know it by the testimony of the Holy Ghost that is
                    within me; and you may wait for your friends to come forth to meet you
                    in the morn of the celestial world.
                </big></p>
                <p style="font-family: arial;"><big>Rejoice, O Israel! Your friends who
                    have been murdered for the truth's
                    sake in the persecutions shall triumph gloriously in the celestial
                    world,
                    while their murderers shall welter for ages in torment, even until they
                    shall have paid the uttermost farthing. I say this for the benefit of
                    strangers.
                </big></p>
                <p style="font-family: arial;"><big>I have a father, brothers,
                    children, and friends who have gone to a
                    world of spirits. They are only absent for a moment. They are in the
                    spirit,
                    and we shall soon meet again. The time will soon arrive when the
                    trumpet
                    shall sound. When we depart, we shall hail our mothers, fathers,
                    friends,
                    and all whom we love, who have fallen asleep in Jesus. There will be no
                    fear of mobs, persecutions, or malicious lawsuits and arrests; but it
                    will
                    be an eternity of felicity.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big>* * *
                    </big></p>
                    <p><big><b>Baptism</b>I will leave this subject here, and make a few
                        remarks
                        on the subject of baptism. The baptism of water, without the baptism of
                        fire and the Holy Ghost attending it, is of no use; they are
                        necessarily
                        and inseparably connected. An individual must be born of water and the
                        Spirit in order to get into the kingdom of God. In the German, the text
                        bears me out the same as the revelations which I have given and taught
                        for the last fourteen years on that subject. I have the testimony to
                        put
                        in their teeth. My testimony has been true all the time. You will find
                        it in the declaration of John the Baptist. (Reads from the German.)
                        John
                        says, "I baptize you with water, but when Jesus comes, who has the
                        power
                        (or keys), he shall administer the baptism of fire and the Holy Ghost."
                        Where is now all the sectarian world? And if this testimony is true,
                        they
                        are all damned as clearly as anathema can do it. I know the text is
                        true.
                        I call upon all you Germans who know that it is true to say, Aye. (Loud
                        shouts of "Aye.")</big></p>
                </center>
                <p style="font-family: arial;"><big>Alexander Campbell, how are you
                    going to save people with water alone?
                    For John said his baptism was good for nothing without the baptism of
                    Jesus
                    Christ. "Therefore, not leaving the principles of the doctrine of
                    Christ,
                    let us go on unto perfection; not laying again the foundation of
                    repentance
                    from dead works, and of faith toward God, of the doctrine of baptisms,
                    and of laying on of hands, and of resurrection of the dead, and of
                    eternal
                    judgment. And this will we do, if God permit." (Heb. 6:1-3).
                </big></p>
                <p style="font-family: arial;"><big>There is one God, one Father, one
                    Jesus, one hope of our calling, one
                    baptism. * * * Many talk of baptism not being essential to salvation;
                    but
                    this kind of teaching would lay the foundation of their damnation. I
                    have
                    the truth, and am at the defiance of the world to contradict me, if
                    they
                    can.
                </big></p>
                <p style="font-family: arial;"><big>I have now preached a little Latin,
                    a little Hebrew, Greek, and German;
                    and I have fulfilled all. I am not so big a fool as many have taken me
                    to be. The Germans know that I read the German correctly.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <center style="font-family: arial;"><big></big>
                    <p><big><b>A Call to Repentance</b></big></p>
                </center>
                <p style="font-family: arial;"><big>Hear it, all ye ends of the earth
                    -- all ye priests, all ye sinners,
                    and all men. Repent! repent! Obey the Gospel. Turn to God; for your
                    religion
                    won't save you, and you will be damned. I do not say how long. There
                    have
                    been remarks made concerning all men being redeemed from hell; but I
                    say
                    that those who sin against the Holy Ghost cannot be forgiven in this
                    world
                    or in the world to come; they shall die the second death. Those who
                    commit
                    the unpardonable sin are doomed to
                    <i>Gnolom</i> -- to dwell in hell, worlds
                    without end. As they concoct scenes of bloodshed in this world, so they
                    shall rise to that resurrection which is as the lake of fire and
                    brimstone.
                    Some shall rise to the everlasting burnings of God; for God dwells in
                    everlasting
                    burnings, and some shall rise to the damnation of their own filthiness,
                    which is as exquisite a torment as the lake of fire and brimstone.
                </big></p>
                <p style="font-family: arial;"><big>I have intended my remarks for all,
                    both rich and poor, bond and free,
                    great and small. I have no enmity against any man. I love you all; but
                    I hate some of your deeds. I am your best friend, and if persons miss
                    their
                    mark it is their own fault. If I reprove a man, and he hates me, he is
                    a fool; for I love all men, especially these my brethren and sisters. I
                    rejoice in hearing the testimony of my aged friends. You don't know me;
                    you never knew my heart. No man knows my history. I cannot tell it: I
                    shall
                    never undertake it. I don't blame any one for not believing my history.
                    If I had not experienced what I have, I could not have believed it
                    myself.
                    I never did harm any man since I was born in the world. My voice is
                    always
                    for peace.
                </big></p>
                <p><big><span style="font-family: arial;">I cannot lie down until all
                        my work is finished. I never think any evil,
                        nor do anything to the harm of my fellowman. When I am called by the
                        trump
                        of the archangel and weighed in the balance, you will all know me then.
                        I add no more. God bless you all. Amen.</span>
                </big></p>

