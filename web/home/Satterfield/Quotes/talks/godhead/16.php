            <main class="container">
                <center style="font-family: arial;"><big><span style="font-size: 18pt;"><i>The
                                Light of Christ: What
                                Everyone
                                Called to Preach the Gospel,&nbsp;</i></span>
                    </big>
                    <p><big><span style="font-size: 18pt;"><i>Teach the Gospel, or Live the
                                Gospel
                                Should Know</i></span>
                    </big></p>
                    <big></big>
                    <p><big>President Boyd K. Packer
                        <br>
                        <i>Acting President of the Quorum of the Twelve Apostles<br>
                            Ensign</i>, April 2005, pp. 8-14<br>
                    </big></p>
                    <big></big>
                    <p><big><b>From an address given
                            on June 22, 2004, at a seminar for new mission presidents,&nbsp;</b>
                        <br>
                        <b>Missionary Training
                            Center,
                            Provo, Utah.&nbsp;</b></big></p>
                    <big></big></center>
                <p style="font-family: arial;"><big><br>
                </big></p>
                <p style="font-family: arial;"><big>Most members of the
                    Church
                    have a basic understanding of the Holy Ghost. Most have experienced its
                    promptings and understand why the Holy Ghost is called the
                    Comforter.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>They know "the Holy
                    Ghost
                    . . . is a personage of Spirit" (D&amp;C 130:22) and a member of the
                    Godhead
                    (see Articles of Faith 1:1).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>But many do not know
                    that
                    there is another Spirit - "the light of Christ" (D&amp;C 88:7) -
                    another
                    source of inspiration, which each of us possesses in common with all
                    other
                    members of the human family. If we know about the Light of Christ, we
                    will
                    understand that there is something inside all of us, and we can appeal
                    to that in our desire to share truth.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>The Holy Ghost and the
                    Light
                    of Christ are different from each other. While they are sometimes
                    described
                    in the scriptures with the same words, they are two different and
                    distinct
                    entities. It is important for you to know about both of them.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>The more we know about
                    the
                    Light of Christ, the more we will understand about life and the more we
                    will have a deep love for all mankind. We will be better teachers and
                    missionaries
                    and parents, and better men and women and children. We will have deeper
                    regard for our brothers and sisters in the Church and for those who do
                    not believe and have not yet had conferred upon them the gift of the
                    Holy
                    Ghost.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>The Light of Christ is
                    defined
                    in the scriptures as "the Spirit [which] giveth light to <i>every </i>man
                    that cometh into the world" (D&amp;C 84:46; emphasis added); "the light
                    which is in all things, which giveth life to all things, which is the
                    law
                    by which all things are governed" (D&amp;C 88:13; see also John 1:4-9;
                    D&amp;C 84:45-47; 88:6; 93:9).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>And the Light of Christ
                    is
                    also described in the scriptures as "the Spirit of Jesus Christ"
                    (D&amp;C
                    84:45), "the Spirit of the Lord" (2 Corinthians 3:18; see also Mosiah
                    25:24),
                    "the Spirit of truth" (D&amp;C 93:26), "the light of truth" (D&amp;C
                    88:6),
                    "the Spirit of God" (D&amp;C 46:17), and "the Holy Spirit" (D&amp;C
                    45:57).
                    Some of these terms are also used to refer to the Holy Ghost.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>The First Presidency has
                    written, "There is a universally diffused essence which is the light
                    and
                    the life of the world, which lighteth every man that cometh into the
                    world,
                    which proceedeth forth from the presence of God throughout the
                    immensity
                    of space, the light and power of which God bestows in different degrees
                    to 'them that ask him,' according to their faith and
                    obedience."["Receiving
                    the Holy Ghost," <i>Improvement Era, </i>Mar. 1916, 460. ]
                </big></p>
                <p style="font-family: arial;"><big>Regardless of whether
                    this
                    inner light, this knowledge of right and wrong, is called the Light of
                    Christ, moral sense, or conscience, it can direct us to moderate our
                    actions
                    - unless, that is, we subdue it or silence it.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Every spirit child of
                    our
                    Heavenly Father enters into mortality to receive a physical body and to
                    be tested.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>"The Lord said . . .
                    they
                    are the workmanship of mine own hands, and I gave unto them their
                    knowledge,
                    in the day I created them; and in the Garden of Eden, gave I unto man
                    his
                    agency" (Moses 7:32).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>"Wherefore, men are free
                    according to the flesh; and all things are given them which are
                    expedient
                    unto man. And they are free to choose liberty and eternal life, through
                    the great Mediator of all men, or to choose captivity and death,
                    according
                    to the captivity and power of the devil" (2 Nephi 2:27).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Therefore, we know that
                    "every
                    man may act in doctrine and principle pertaining to futurity, according
                    to the <i>moral agency </i>[the words <i>free agency </i>do not
                    appear
                    in the revelations] which I have given unto him, that every man may be
                    accountable for his own sins in the day of judgment" (D&amp;C 101:78;
                    emphasis
                    added).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>We are admonished to
                    "quench
                    not the Spirit" (1 Thessalonians 5:19). Thus we can see that "[all] are
                    instructed sufficiently that they know good from evil" (2 Nephi 2:5;
                    see
                    also 2 Nephi 2:27). They have their agency, and they are
                    accountable.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>This Spirit of Christ
                    fosters
                    everything that is good, every virtue (see Moroni 7:16). It stands in
                    brilliant,
                    indestructible opposition to anything that is coarse or ugly or profane
                    or evil or wicked (see Moroni 7:17).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Conscience affirms the
                    reality
                    of the Spirit of Christ in man. It affirms, as well, the reality of
                    good
                    and evil, of justice, mercy, honor, courage, faith, love, and virtue,
                    as
                    well as the necessary opposites - hatred, greed, brutality, jealousy
                    (see
                    2 Nephi 2:11, 16). Such values, though physically intangible, respond
                    to
                    laws with cause-and-effect relationships as certain as any resulting
                    from
                    physical laws (see Galatians 6:7-9). The Spirit of Christ can be
                    likened
                    unto a "guardian angel" for every person.[See Joseph Fielding Smith,
                    <i>Doctrines
                        of Salvation </i>, comp. Bruce R. McConkie, 3 vols. (1954-56), 1:54.
                    ]&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>The Spirit of Christ can
                    enlighten the inventor, the scientist, the painter, the sculptor, the
                    composer,
                    the performer, the architect, the author to produce great, even
                    inspired
                    things for the blessing and good of all mankind.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>This Spirit can prompt
                    the
                    farmer in his field and the fisherman on his boat. It can inspire the
                    teacher
                    in the classroom, the missionary in presenting his discussion. It can
                    inspire
                    the student who listens. And of enormous importance, it can inspire
                    husband
                    and wife, and father and mother.
                </big></p>
                <p style="font-family: arial;"><big>This inner Light can
                    warn
                    and guard and guide. But it can be repulsed by anything that is ugly or
                    unworthy or wicked or immoral or selfish.
                </big></p>
                <p style="font-family: arial;"><big>The Light of Christ
                    existed
                    in you before you were born (see D&amp;C 93:23, 29-30), and it will be
                    with you every moment that you live and will not perish when the mortal
                    part of you has turned to dust. It is ever there.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Every man, woman, and
                    child
                    of every nation, creed, or color - everyone, no matter where they live
                    or what they believe or what they do - has within them the imperishable
                    Light of Christ. In this respect, all men are created equally. The
                    Light
                    of Christ in everyone is a testimony that God is no respecter of
                    persons
                    (see D&amp;C 1:35). He treats everyone equally in that endowment with
                    the
                    Light of Christ.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>It is important for a
                    teacher
                    or a missionary or a parent to know that the Holy Ghost can work
                    through
                    the Light of Christ. A teacher of gospel truths is not planting
                    something
                    foreign or even new into an adult or a child. Rather, the missionary or
                    teacher is making contact with the Spirit of Christ already there. The
                    gospel will have a familiar "ring" to them. Then the teaching will come
                    "to the convincing of [those who will listen] that Jesus is the Christ,
                    the Eternal God, manifesting himself unto all nations" (Book of Mormon
                    title page).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>During His mortal
                    ministry,
                    Jesus taught His gospel and put in place the foundation upon which His
                    Church would be built. The foundation was built of stones of doctrine
                    which
                    can neither be seen with mortal eyes nor felt by touch; they are
                    invisible
                    and intangible. They will not weather away or crumble. They cannot be
                    broken
                    or dissolved or destroyed. These stones of doctrine are imperishable
                    and
                    indestructible.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>These stones of doctrine
                    existed "before the world was" (D&amp;C 124:38), "from before the
                    foundation
                    of the world" (D&amp;C 124:41). Christ built His Church upon them.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Jesus spoke of "the
                    stone
                    which the builders rejected" (Matthew 21:42). Then the shadow of
                    apostasy
                    settled over the earth. The line of priesthood authority was broken.
                    But
                    mankind was not left in total darkness or completely without revelation
                    or inspiration. The idea that with the Crucifixion of Christ the
                    heavens
                    were closed and that they opened in the First Vision is not true. The
                    Light
                    of Christ would be everywhere present to attend the children of God;
                    the
                    Holy Ghost would visit seeking souls. The prayers of the righteous
                    would
                    not go unanswered.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>The conferring of the <i>gift
                    </i>of the Holy Ghost must await the restoration of the priesthood and
                    the dispensation of the fulness of times, when all things would be
                    revealed.
                    Temple work - ordinance work - would then be revealed. Then those who
                    lived
                    during the many generations when essential ordinances were unavailable,
                    when baptism was not available, would be redeemed. God never abandons
                    His
                    children. He never has abandoned this earth.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>When the fulness of His
                    gospel
                    was restored, The Church of Jesus Christ of Latter-day Saints was built
                    upon the same foundation stones of doctrine.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Because we learn most
                    everything
                    through physical senses, teaching intangible doctrines which cannot be
                    seen or felt becomes very difficult. Jesus, the Master Teacher, taught
                    these doctrines, and they can be taught in the same way today. It is my
                    purpose to show you how He, the Master Teacher, taught them.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>You can come to
                    understand
                    spiritual truths as clearly as if these stones of doctrine were as
                    tangible
                    as granite or flint or marble. Marble will yield to the hands of the
                    sculptor
                    so that others can see what he sees hidden within the shapeless stone.
                    In like manner, you can teach others to see - that is, to understand -
                    these intangible, invisible stones of doctrine.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>The way the Savior
                    taught,
                    and the way you can teach, is both simple and very profound. If you
                    choose
                    a tangible object as a symbol for a doctrine, you can teach just as He
                    did. A teacher can associate the doctrine with an object already known,
                    which <i>can </i>be seen with physical eyes.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Jesus compared faith to
                    a
                    seed, the tiny mustard seed, which can be seen and touched. He told how
                    if the seed is nurtured, it can grow and flourish and become a tree.
                    (See
                    Luke 13:19.)&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>He compared the kingdom
                    of
                    heaven to an everyday object that can be seen. "The kingdom of heaven,"
                    He said, "is like unto a net, that was cast into the sea, and gathered
                    of every kind" (Matthew 13:47); and He said, "The kingdom of heaven is
                    like unto treasure hid in a field; the which when a man hath found, he
                    hideth, and for joy thereof goeth and selleth all that he hath, and
                    buyeth
                    that field" (Matthew 13:44).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Christ used as examples,
                    as symbols, such ordinary things as salt (see Matthew 5:13; Mark
                    9:49-50;
                    Luke 14:34) and candles (see Matthew 5:15; Mark 4:21; Luke 8:16;
                    11:33-36;
                    Revelation 18:23), as rain (see Matthew 7:25-27) and rainbows (see
                    Revelation
                    4:3; 10:1). The four Gospels are full of such examples. Likewise the
                    Book
                    of Mormon, the Doctrine and Covenants, and the Pearl of Great Price
                    have
                    dozens of similar references. They are everywhere. That is what a story
                    or a parable is - a true-to-life example used to teach a principle or a
                    doctrine that is invisible or intangible.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>One time in Matthew, one
                    time in Luke, three times in the Book of Mormon, and three times in the
                    Doctrine and Covenants, the Savior spoke of a hen with her chickens
                    (see
                    Matthew 23:37; Luke 13:34;3 Nephi 10:4-6; D&amp;C 10:65; 29:2; 43:24).
                    Everyone knows about hens and chickens, even little children.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Now faith is not really
                    exactly
                    like a seed, nor is the kingdom of heaven exactly like a net or a
                    treasure
                    or leaven (see Luke 13:21) or "a merchant man, seeking goodly pearls"
                    (Matthew
                    13:45). But with these illustrations, Jesus was able to open the eyes
                    of
                    His disciples - not their natural eyes but the eyes of their
                    understanding
                    (see Matthew 13:15; John 12:40; Acts 28:27; Ephesians 1:18; 2 Nephi
                    16:10;
                    D&amp;C 76:12, 19; 88:11; 110:1).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>With the eyes of our
                    understanding,
                    we see things that are spiritual. With our spirits reaching out, we can
                    touch things that are spiritual and <i>feel </i>them. Then we can <i>see
                    </i>and we can <i>feel </i>things that are invisible to the physical
                    senses.
                    Remember, Nephi told his rebellious brothers, who had rejected a
                    message
                    from an angel, "Ye were past feeling, that ye could not <i>feel </i>his
                    words" (1 Nephi 17:45; emphasis added).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Paul wrote to the
                    Corinthians
                    that "God hath revealed them unto us by his Spirit: for the Spirit
                    searcheth
                    all things, yea, the deep things of God. . . .&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>"Which things also we
                    speak,
                    not in the words which man's wisdom teacheth, but which the Holy Ghost
                    teacheth; comparing spiritual things with spiritual.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>"But the natural man
                    receiveth
                    not the things of the Spirit of God: for they are foolishness unto him:
                    neither can he know them, because they are spiritually discerned" (1
                    Corinthians
                    2:10, 13-14).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>In modern revelation,
                    Christ
                    spoke of "the light which shineth, which giveth you light [and]
                    enlighteneth
                    your eyes, which is the same light that quickeneth your understandings"
                    (D&amp;C 88:11).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>I do not know how to
                    teach
                    about the Spirit of Christ except to follow what the Lord did when He
                    taught
                    invisible, intangible truths to His disciples.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>To describe the Light of
                    Christ, I will compare or liken it to the light of the sun. Sunlight is
                    familiar to everyone; it is everywhere present and can be seen and can
                    be felt. Life itself depends upon sunlight.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>The Light of Christ <i>is
                    </i>like sunlight. It, too, is everywhere present and given to everyone
                    equally.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Just as darkness must
                    vanish
                    when the light of the sun appears, so is evil sent fleeing by the Light
                    of Christ.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>There is no darkness in
                    sunlight.
                    Darkness is subject unto it. The sun can be hidden by clouds or by the
                    rotation of the earth, but the clouds will disappear, and the earth
                    will
                    complete its turning.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>According to the plan,
                    we
                    are told that "it must needs be, that there is an opposition in all
                    things"
                    (2 Nephi 2:11).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Mormon warned that "the
                    devil
                    . . . persuadeth no man to do good, no, not one; neither do his angels;
                    neither do they who subject themselves unto him.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>"[Now] seeing that ye
                    know
                    the light by which ye may judge, which light is the light of Christ,
                    see
                    that ye do not judge wrongfully" (Moroni 7:17-18).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>This Light of Christ,
                    which
                    gives life, is within you. The evil one will attempt to obscure it. It
                    can be so clouded with confusion so far as to convince you that it does
                    not even exist.
                </big></p>
                <p style="font-family: arial;"><big>Just as sunlight is a
                    natural
                    disinfectant, the Spirit of Christ can cleanse the spirit.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Every soul, no matter
                    who
                    or where or when, is a child of God. Our responsibility is to teach
                    that
                    "there is a spirit in man: and the inspiration of the Almighty giveth
                    them
                    understanding" (Job 32:8).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>President Joseph
                    Fielding
                    Smith spoke of the teachings of the Holy Ghost and of the Spirit of
                    Christ:
                    "Every man can receive a manifestation of the Holy Ghost, even when he
                    is out of the Church, if he is earnestly seeking for the light and for
                    the truth. The Holy Ghost will come and give the man the testimony he
                    is
                    seeking, and then withdraw; and the man does not have a claim upon
                    another
                    visit or constant visits and manifestations from him. He may have the
                    constant
                    guidance of that other Spirit, the Spirit of Christ." [<i>Doctrines of
                        Salvation, </i>1:42; see also Joseph Smith, <i>Teachings of the
                        Prophet
                        Joseph Smith, </i>sel. Joseph Fielding Smith (1976), 149. ]&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>The Spirit of Christ is
                    always
                    there. It never leaves. It cannot leave.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Everyone everywhere
                    already
                    has the Spirit of Christ, and while the Spirit of the Holy Ghost can
                    visit
                    anyone, the <i>gift </i>of the Holy Ghost is obtained "by obedience
                    to
                    the laws and ordinances of the Gospel" (Articles of Faith 1:3), by
                    submitting
                    to "baptism by immersion for the remission of sins; [and the] laying on
                    of hands for the gift of the Holy Ghost" (Articles of Faith 1:4). It is
                    not automatically present like the Spirit of Christ is present. This
                    gift
                    must be conferred by one holding authority (see Articles of Faith
                    1:5).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>That is what we are
                    commissioned
                    to do, to foster the Light of Christ, which is within every soul we
                    meet,
                    and bring souls to the point where the Holy Ghost may visit them. And
                    then,
                    in due time, they can receive, through the ordinance, the gift of the
                    Holy
                    Ghost, which is conferred upon every member of the Church.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Once a person has
                    received
                    that gift of the Holy Ghost and can cultivate it together with the
                    Light
                    of Christ, which they already have, then the fulness of the gospel is
                    open
                    to their understanding. The Holy Ghost can even work through the Light
                    of Christ. [See
                    <i>Doctrines of Salvation, </i>1:54. ]&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>The Light of Christ is
                    as
                    universal as sunlight itself. Wherever there is human life, there is
                    the
                    Spirit of Christ. Every living soul is possessed of it. It is the
                    sponsor
                    of everything that is good. It is the inspirer of everything that will
                    bless and benefit mankind. It nourishes goodness itself.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Mormon taught: "Search
                    diligently
                    in the light of Christ that ye may know good from evil; and if ye will
                    lay hold upon every good thing, and condemn it not, ye certainly will
                    be
                    a child of Christ" (Moroni 7:19).&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Everyone knows about
                    sunlight.
                    When you liken the Spirit of Christ to sunlight, ordinary examples from
                    your own experiences may come to your mind. These examples are almost
                    endless.
                    These examples can be understood by little children or by adults, as
                    the
                    parables of Christ can be understood. It should not be difficult to
                    teach
                    how revelation can come through Light, even though we do not know
                    exactly
                    how inspiration works.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Man himself, with all
                    his
                    limitations, can convey messages through fiber-optic cables. A single
                    tiny
                    fiber of glass, smaller than a human hair, can carry 40,000 messages at
                    the same time. These can then be decoded and turned into sight and
                    sound
                    and color, even motion. Man can do that.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>A laser beam, where
                    there
                    is no wire or fiber at all, can carry 100 billion bits of information
                    in
                    a second.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>If man can do that, why
                    should
                    we marvel at the promise that the Light of Christ is in all of us and
                    that
                    the Holy Ghost can visit any of us?&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>It should not be
                    difficult,
                    therefore, to understand how revelation from God to His children on
                    earth
                    can come to all mankind through both the Spirit of Christ and the Holy
                    Ghost.&nbsp;
                </big></p>
                <p style="font-family: arial;"><big>This Light of Christ is
                    everywhere
                    in the scriptures. The Doctrine and Covenants is a very rich source of
                    teaching on the Light of Christ. For example, it speaks of "the light
                    of
                    truth; which truth shineth. This is the light of Christ. . . . He is in
                    the sun, and the light of the sun, and the power thereof by which it
                    was
