            <main class="container">
                <center><i><font size=+3>The Father, Son, and Holy Ghost</font></i>
                    <p>President Gordon B. Hinckley
                        <br><i>Ensign</i>, Mar. 1998, pp. 2-7</center>

                <p>The first article of faith is familiar to all members of the Church.
                    It is the pivotal position of our religion. It is significant that in setting
                    forth the primary elements of our doctrine, the Prophet Joseph put this
                    number one:
                <p>"We believe in God, the Eternal Father, and in His Son, Jesus Christ,
                    and in the Holy Ghost" (A of F 1:1).
                <p>The preeminence given that declaration is in accord with another statement
                    the Prophet made. Said he: "It is the first principle of the gospel to
                    know for a certainty the character of God" (<i>History of the Church</i>,
                    6:305).
                <p>These tremendously significant and overarching declarations are in harmony
                    with the words of the Lord in His great intercessory prayer: "And this
                    is life eternal, that they might know thee the only true God, and Jesus
                    Christ, whom thou hast sent" (John 17:3).
                <p>I would like to discuss this most important of all theological subjects,
                    that which is the subject of the first article of faith.
                <p>I believe without equivocation or reservation in God, the Eternal Father.
                    He is my Father, the Father of my spirit, and the Father of the spirits
                    of all men. He is the great Creator, the Ruler of the Universe. He directed
                    the creation of this earth on which we live. In His image man was created.
                    He is personal. He is real. He is individual. He has "a body of flesh and
                    bones as tangible as man's" (D&amp;C 130:22).
                <p>In the account of the creation of the earth, "God said, Let us make
                    man in our image, after our likeness" (Gen. 1:26).
                <p>Could any language be more explicit? Does it demean God, as some would
                    have us believe, that man was created in His express image? Rather, it
                    should stir within the heart of every man and woman a greater appreciation
                    for himself or herself as a son or daughter of God. Paul's words to the
                    Corinthian Saints are as applicable to us today as they were to those to
                    whom he wrote. Said he:
                <p>"Know ye not that ye are the temple of God, and that the Spirit of God
                    dwelleth in you?
                <p>"If any man defile the temple of God, him shall God destroy; for the
                    temple of God is holy, which temple ye are" (1 Cor. 3:16-17).
                    <br>&nbsp;
                <p><b>God Has a Body</b>
                <p>I remember the occasion of more than 50 years ago when, as a missionary,
                    I was speaking in an open-air meeting in Hyde Park, London. As I was presenting
                    my message, a heckler interrupted to say, "Why don't you stay with the
                    doctrine of the Bible which says in John [4:24], 'God is a Spirit'?"
                <p>I opened my Bible to the verse he had quoted and read to him the entire
                    verse: "God is a Spirit: and they that worship him must worship him in
                    spirit and in truth."
                <p>I said, "Of course God is a spirit, and so are you, in the combination
                    of spirit and body that makes of you a living being, and so am I."
                <p>Each of us is a dual being of spiritual entity and physical entity.
                    All know of the reality of death when the body dies, and each of us also
                    knows that the spirit lives on as an individual entity and that at some
                    time, under the divine plan made possible by the sacrifice of the Son of
                    God, there will be a reunion of spirit and body. Jesus' declaration that
                    God is a spirit no more denies that he has a body than does the statement
                    that I am a spirit while also having a body.
                <p>I do not equate my body with His in its refinement, in its capacity,
                    in its beauty and radiance. His is eternal. Mine is mortal. But that only
                    increases my reverence for Him. I worship Him "in spirit and in truth."
                    I look to Him as my strength. I pray to Him for wisdom beyond my own. I
                    seek to love Him with all my heart, might, mind, and strength. His wisdom
                    is greater than the wisdom of all men. His power is greater than the power
                    of nature, for He is the Creator Omnipotent. His love is greater than the
                    love of any other, for His love encompasses all of His children, and it
                    is His work and His glory to bring to pass the immortality and eternal
                    life of His sons and daughters of all generations (see Moses 1:39).
                <p>He "so loved the world, that he gave his only begotten Son, that whosoever
                    believeth in him should not perish, but have everlasting life" (John 3:16).
                <p>This is the Almighty of whom I stand in awe and reverence. It is He
                    to whom I look in fear and trembling. It is He whom I worship and unto
                    whom I give honor and praise and glory. He is my Heavenly Father, who has
                    invited me to come unto Him in prayer, to speak with Him, with the promised
                    assurance that He will hear and respond.
                    <br>&nbsp;
                <p><b>Thanks Be to God</b>
                <p>I thank Him for the light and knowledge and understanding He has bestowed
                    upon His children. I thank Him for His voice, which has spoken eternal
                    truth with power and promise. I thank Him for His revelation of Himself
                    as set forth in the Old Testament, for His declaration, as set forth in
                    the New Testament, at the baptism of His Beloved Son in the waters of Jordan
                    when His voice was heard saying, "This is my beloved Son, in whom I am
                    well pleased" (Matt. 3:17).
                <p>I thank Him for His similar declaration on the Mount of Transfiguration
                    when He spoke again to Jesus and His Apostles, and angels also, when "after
                    six days Jesus taketh Peter, James, and John his brother, and bringeth
                    them up into an high mountain apart,
                <p>"And was transfigured before them: and his face did shine as the sun,
                    and his raiment was white as the light.
                <p>"And, behold, there appeared unto them Moses and Elias talking with
                    him.
                <p>"Then answered Peter, and said unto Jesus, Lord, it is good for us to
                    be here: if thou wilt, let us make here three tabernacles; one for thee,
                    and one for Moses, and one for Elias.
                <p>"While he yet spake, behold, a bright cloud overshadowed them: and behold
                    a voice out of the cloud, which said, This is my beloved Son, in whom I
                    am well pleased; hear ye him" (Matt. 17:1-5).
                <p>I thank Him for that voice again heard when the risen Lord was introduced
                    to the people of the Western Hemisphere with the voice of God declaring,
                    "Behold my Beloved Son, in whom I am well pleased, in whom I have glorified
                    my name" (3 Ne. 11:7).
                <p>I stand in awe and reverence and gratitude for His appearance in this
                    dispensation when, as He introduced the risen Lord to one who had sought
                    Him in prayer, the Father declared, "This is My Beloved Son. Hear Him!"
                    (JS--H 1:17).
                    <br>&nbsp;
                <p><b>Testimony of the Lord Jesus Christ</b>
                <p>I believe in the Lord Jesus Christ, the Son of the eternal, living God.
                    I believe in Him as the Firstborn of the Father and the Only Begotten of
                    the Father in the flesh. I believe in Him as an individual, separate and
                    distinct from His Father. I believe in the declaration of John, who opened
                    his gospel with this majestic utterance:
                <p>"In the beginning was the Word, and the Word was with God, and the Word
                    was God.
                <p>"The same was in the beginning with God. …
                <p>"And the Word was made flesh, and dwelt among us, (and we beheld his
                    glory, the glory as of the only begotten of the Father,) full of grace
                    and truth" (John 1:1-2, 14).
                <p>I believe that He was born of Mary of the lineage of David as the promised
                    Messiah, that He was in very deed begotten of the Father, and that in His
                    birth was the fulfillment of the great prophetic declaration of Isaiah:
                <p>"For unto us a child is born, unto us a son is given: and the government
                    shall be upon his shoulder: and his name shall be called Wonderful, Counsellor,
                    The mighty God, The everlasting Father, The Prince of Peace" (Isa. 9:6).
                <p>I believe that in His mortal life He was the one perfect man to walk
                    the earth. I believe that in His words are to be found that light and truth
                    which, if observed, would save the world and bring exaltation to mankind.
                    I believe that in His priesthood rests divine authority--the power to bless,
                    the power to heal, the power to govern in the earthly affairs of God, the
                    power to bind in the heavens that which is bound upon the earth.
                    <br>&nbsp;
                <p><b>Christ's Atoning Sacrifice</b>
                <p>I believe that through His atoning sacrifice, the offering of His life
                    on Calvary's hill, He expiated the sins of mankind, relieving us from the
                    burden of sin if we will forsake evil and follow Him. I believe in the
                    reality and the power of His Resurrection which we commemorate at this
                    Easter season. I believe in the grace of God made manifest through His
                    sacrifice and redemption, and I believe that through His Atonement, without
                    any price on our part, each of us is offered the gift of resurrection from
                    the dead. I believe further that through that sacrifice there is extended
                    to every man and woman, every son and daughter of God, the opportunity
                    for eternal life and exaltation in our Father's kingdom, as we hearken
                    and obey His commandments.
                <p>None so great has ever walked the earth. None other has made a comparable
                    sacrifice or granted a comparable blessing. He is the Savior and the Redeemer
                    of the world. I believe in Him. I declare His divinity without equivocation
                    or compromise. I love Him. I speak His name in reverence and wonder. I
                    worship Him as I worship His Father, in spirit and in truth. I thank Him
                    and kneel before His wounded feet and hands and side, amazed at the love
                    He offers me.
                <p>God be thanked for His Beloved Son, who reached out long ago and said
                    to each of us:
                <p>"Come unto me, all ye that labour and are heavy laden, and I will give
                    you rest.
                <p>"Take my yoke upon you, and learn of me; for I am meek and lowly in
                    heart: and ye shall find rest unto your souls.
                <p>"For my yoke is easy, and my burden is light" (Matt. 11:28-30).
                    <br>&nbsp;
                <p><b>Christ Is the Living, Resurrected Son of God</b>
                <p>He lives, the firstfruits of the Resurrection. I know He lives today
                    as really, as certainly, as individually, as He lived when, as the risen
                    Lord, He beckoned His discouraged disciples to "come and dine. … And [He]
                    taketh bread, and giveth them, and fish likewise" (John 21:12-13).
                <p>Scripture tells of others to whom He showed Himself and with whom He
                    spoke as the living, resurrected Son of God. Likewise in this dispensation
                    He has appeared, and those who saw Him declared:
                <p>"And now, after the many testimonies which have been given of him, this
                    is the testimony, last of all, which we give of him: That he lives!
                <p>"For we saw him, even on the right hand of God; and we heard the voice
                    bearing record that he is the Only Begotten of the Father--
                <p>"That by him, and through him, and of him, the worlds are and were created,
                    and the inhabitants thereof are begotten sons and daughters unto God" (D&amp;C
                    76:22-24).
                <p>This is the Christ in whom I believe and of whom I testify.
                    <br>&nbsp;
                <p><b>The Holy Ghost Is the Third Member of the Godhead</b>
                <p>That knowledge comes from the word of scripture, and that testimony
                    comes by the power of the Holy Ghost. It is a gift, sacred and wonderful,
                    borne by revelation from the third member of the Godhead. I believe in
                    the Holy Ghost as a personage of spirit who occupies a place with the Father
                    and the Son, these three composing the divine Godhead.
                <p>The importance of that place is made clear from the words of the Lord,
                    who said:
                <p>"All manner of sin and blasphemy shall be forgiven unto men: but the
                    blasphemy against the Holy Ghost shall not be forgiven unto men.
                <p>"And whosoever speaketh a word against the Son of man, it shall be forgiven
                    him: but whosoever [page 7] speaketh against the Holy Ghost, it shall not
                    be forgiven him, neither in this world, neither in the world to come" (Matt.
                    12:31-32).
                <p>That the Holy Ghost was recognized in ancient times as a member of the
                    Godhead is evident from the conversation between Peter and Ananias when
                    the latter held back a part of the price received from the sale of a piece
                    of land.
                <p>"But Peter said, Ananias, why hath Satan filled thine heart to lie to
                    the Holy Ghost … ?
                <p>"… Thou hast not lied unto men, but unto God" (Acts 5:3-4).
                <p>The Holy Ghost stands as the third member of the Godhead, the Comforter
                    promised by the Savior who would teach His followers all things and bring
                    all things to their remembrance, whatsoever He had said unto them (see
                    John 14:26).
                <p>The Holy Ghost is the Testifier of Truth, who can teach men things they
                    cannot teach one another. In those great and challenging words of Moroni,
                    a knowledge of the truth of the Book of Mormon is promised "by the power
                    of the Holy Ghost." Moroni then declares, "And by the power of the Holy
                    Ghost ye may know the truth of all things" (Moro. 10:4-5).
                <p>I believe this power, this gift, is available to us today.
                    <br>&nbsp;
                <p><b>Members of the Godhead Are Real and Individual</b>
                <p>And so, my beloved brethren and sisters, I believe in God, the Eternal
                    Father, and in His Son, Jesus Christ, and in the Holy Ghost.
                <p>I was baptized in the name of these three. I was married in the name
                    of these three. I have no question concerning their reality and their individuality.
                    That individuality was made apparent when Jesus was baptized by John in
                    Jordan. There in the water stood the Son of God. His Father's voice was
                    heard declaring His divine sonship, and the Holy Ghost was manifest in
                    the form of a dove (see Matt. 3:16-17).
                <p>I am aware that Jesus said that they who had seen Him had seen the Father
                    (see John 14:9). Could not the same be said by many a son who resembles
                    his parent?
                <p>When Jesus prayed to the Father, certainly He was not praying to Himself!
                    <br>&nbsp;
                <p><b>Members of the Godhead Are Perfectly United</b>
                <p>They are distinct beings, but they are one in purpose and effort. They
                    are united as one in bringing to pass the grand, divine plan for the salvation
                    and exaltation of the children of God.
                <p>In His great, moving prayer in the garden before His betrayal, Christ
                    pleaded with His Father concerning the Apostles, whom He loved, saying:
                <p>"Neither pray I for these alone, but for them also which shall believe
                    on me through their word;
                <p>"That they all may be one; as thou, Father, art in me, and I in thee,
                    that they also may be one in us" (John 17:20-21).
                <p>It is that perfect unity between the Father, the Son, and the Holy Ghost
                    that binds these three into the oneness of the divine Godhead.
                <p>Miracle of miracles and wonder of wonders, they are interested in us,
                    and we are the substance of their great concern. They are available to
                    each of us. We approach the Father through the Son. He is our intercessor
                    at the throne of God. How marvelous it is that we may so speak to the Father
                    in the name of the Son.
                <p>I bear witness of these great, transcendent truths. And I do so by the
                    gift and power of the Holy Ghost.

