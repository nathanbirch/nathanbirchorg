                <center style="font-family: arial;"><big><i><font size="+3"><big>The
                                Pattern of Our Parentage</big></font></i>
                    </big>
                    <p><big>Elder Boyd K. Packer
                        <br>
                        <small>Of the Quorum of the Twelve Apostles
                            <br>
                            <i>Ensign</i>, Nov. 1984, pp. 66-69</small></big></p>
                </center>
                <p style="font-family: arial;"><big>It is the Sabbath day, and outside
                    is such a glorious day that all nature
                    seems to bespeak the works of God. I can't refrain from saying with the
                    poet:
                </big></p>
                <blockquote style="font-family: arial;"><big>O suns and skies and
                        clouds of June,
                        <br>
                        And flowers of June together,
                        <br>
                        Ye cannot rival for one hour
                        <br>
                        October's bright blue weather.
                        <br>
                        (Helen Hunt Jackson, "October's Bright Blue Weather.")</big></blockquote>
                <big><span style="font-family: arial;">I desire to share a few thoughts
                        about a basic doctrine of the Church.
                    </span></big>
                <p style="font-family: arial;"><big>What I say is based on these
                    convictions:
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><i>First</i>:
                    instruction vital to our salvation is not hidden in an
                    obscure verse or phrase in the scriptures. To the contrary, essential
                    truths
                    are repeated over and over again.
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><i>Second</i>:
                    every verse, whether oft-quoted or obscure, must be measured
                    against other verses. There are complementary and tempering teachings
                    in
                    the scriptures which bring a balanced knowledge of truth.
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><i>Next</i>:
                    there is a consistency in what the Lord says and what He
                    does, that is evident in all creation. Nature can teach valuable
                    lessons
                    about spiritual and doctrinal matters. The Lord drew lessons from
                    flowers
                    and foxes, from seeds and salt, and sparrows and sunsets.
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><i>Fourth</i>:
                    not all that God has said is in the Bible. Other scriptures--the
                    Book of Mormon, the Doctrine and Covenants, and the Pearl of Great
                    Price--have
                    equal validity, and they sustain one another.
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big><i>Fifth</i>:
                    while much must be taken on faith alone, there is individual
                    revelation through which we may know the truth. "There is a spirit in
                    man:
                    and the inspiration of the Almighty giveth them understanding." (Job
                    32:8.)
                    What may be obscure in the scriptures can be made plain through the
                    gift
                    of the Holy Ghost. We can have as full an understanding of spiritual
                    things
                    as we are willing to earn.
                </big></p>
                <p style="font-family: arial; margin-left: 40px;"><big>And I add one
                    more conviction: there is an adversary who has his own
                    channels of spiritual communication. He confuses the careless and
                    prompts
                    those who serve him to devise deceptive, counterfeit doctrine,
                    carefully
                    contrived to appear genuine.
                </big></p>
                <p style="font-family: arial;"><big>I mention this because now, as
                    always, there are self-appointed spokesmen
                    who scoff at what we believe and misrepresent what we teach.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><b>Go to His Friends</b>
                </big></p>
                <p style="font-family: arial;"><big>As a young seminary teacher, I
                    learned a valuable lesson from our principal,
                    Able S. Rich. He told me, "If you really want to know what a man is,
                    and
                    what he believes, do not go to his enemies. Go to the man himself or to
                    his friends. He does not confide the thoughts of his heart to his
                    enemies.
                    His friends know him best; they know his strengths and his weaknesses.
                    They will represent him fairly. His enemies will misrepresent him."
                </big></p>
                <p style="font-family: arial;"><big>The doctrine I wish to discuss
                    concerns the nature of man and of God.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><b>The Question</b>
                </big></p>
                <p style="font-family: arial;"><big>There is a question in both the Old
                    and the New Testaments: "What is
                    man, that thou art mindful of him? and the son of man, that thou
                    visitest
                    him?" (Ps. 8:4; see also Heb. 2:5-7.)
                </big></p>
                <p style="font-family: arial;"><big>The answer is taught most simply in
                    the song we sang together at the
                    intermission of the meeting:
                </big></p>
                <blockquote style="font-family: arial;"><big>I am a child of God,
                        <br>
                        And He has sent me here,
                        <br>
                        Has given me an earthly home
                        <br>
                        With parents kind and dear. &#8230;
                        <br>
                        If am a child of God,
                        <br>
                        Rich blessings are in store;
                        <br>
                        If I but learn to do his will
                        <br>
                        I'll live with him once more.
                        <br>
                        (<i>Sing with Me</i>, B-76.)</big></blockquote>
                <big><b style="font-family: arial;">Children of God</b></big>
                <p style="font-family: arial;"><big>Those lyrics teach a basic doctrine
                    of the Church. We are the children
                    of God. That doctrine is not hidden away in an obscure verse. It is
                    taught
                    over and over again in scripture. These clear examples are from the
                    Bible:
                </big></p>
                <p style="font-family: arial;"><big>"All of you are children of the
                    most High." (Ps. 82:6.)
                </big></p>
                <p style="font-family: arial;"><big>And: "We are the offspring of God."
                    (Acts 17:29.)
                </big></p>
                <p style="font-family: arial;"><big>Doctrinal truths are interrelated.
                    There is an old saying that if you
                    pick up one end of a stick, you pick up the other end as well.
                </big></p>
                <p style="font-family: arial;"><big>If you concede that we are
                    His&nbsp; children, you must allow that
                    God is our Father.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><b>God, Our Father</b>
                </big></p>
                <p style="font-family: arial;"><big>That, too, is repeated over and
                    over again in the scriptures. There
                    are so many references that I could not even begin to read them to you.
                </big></p>
                <p style="font-family: arial;"><big>But I make this point: Christ did
                    not speak only of the Father, or my
                    Father; He spoke of your Father, and our Father. He even put them
                    together
                    in one sentence, saying, "Your Father, and your God, and my God."
                    (D&amp;C
                    88:75; italics added.) God is addressed universally in the Christian
                    world
                    as Father. Were we not commanded to pray "Our Father which art in
                    heaven"?
                    (Matt. 6:9.)
                </big></p>
                <p style="font-family: arial;"><big>You may respond, "Every Christian
                    knows that." Perhaps every Christian
                    does, but so-called Christians, with the help of clergymen, belittle in
                    most unchristian ways our teaching that we are the literal sons and
                    daughters
                    of God.
                </big></p>
                <p style="font-family: arial;"><big>Other ideals flow from that great
                    truth. Once you know that, you know
                    that all men are brothers. That realization changes you. Thereafter you
                    cannot willingly injure another. You could not transgress against them
                    in any way.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><b>Self-respect</b>
                </big></p>
                <p style="font-family: arial;"><big>That simple, profound doctrine is
                    worth knowing for another reason as
                    well. It brings a feeling of self-worth, dignity, self-respect. Then
                    self-pity
                    and depression fade away. We then can yield to the discipline of a
                    loving
                    Father and accept even the very hard lessons of life.
                </big></p>
                <p style="font-family: arial;"><big>Christ taught us to be "perfect,
                    even as your Father which is in heaven
                    is perfect" (Matt. 5:48), to take on His attributes, to follow the
                    pattern
                    of our parentage.
                </big></p>
                <p style="font-family: arial;"><big>A little girl taught me a profound
                    lesson on this subject. Surely you
                    are not above learning from little children. Much of what I know that
                    really
                    matters I have learned from being a father.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><b>The Chicks</b>
                </big></p>
                <p style="font-family: arial;"><big>Some years ago I returned home to
                    find our little children were waiting
                    in the driveway. They had discovered some newly hatched chicks under
                    the
                    manger in the barn. When they reached for them, a protective hen
                    rebuffed
                    them. So they came for reinforcements.
                </big></p>
                <p style="font-family: arial;"><big>I soon gathered a handful of little
                    chicks for them to see and touch.
                </big></p>
                <p style="font-family: arial;"><big>As our little girl held one of
                    them, I said in a teasing way, "That
                    will make a nice watchdog when it grows up, won't it?" She looked at me
                    quizzically, as if I didn't know much.
                </big></p>
                <p style="font-family: arial;"><big>So I changed my approach: "It won't
                    be a watchdog, will it?" She shook
                    her head, "No, Daddy." Then I added, "It will be a nice riding horse."
                </big></p>
                <p style="font-family: arial;"><big>She wrinkled up her nose and gave
                    me that "Oh, Dad!" look. For even
                    a four-year-old knows that a chick will not be a dog, nor a horse, nor
                    even a turkey. It will be a chicken. It will follow the pattern of its
                    parentage. She knew that without having had a course in genetics,
                    without
                    a lesson or a lecture.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><b>After Their Own Kind</b>
                </big></p>
                <p style="font-family: arial;"><big>No lesson is more manifest in
                    nature than that all living things do
                    as the Lord commanded in the Creation. They reproduce "after their own
                    kind." (See Moses 2:12, 24.) They follow the pattern of their
                    parentage.
                    Everyone knows that; every four-year-old knows that! A bird will not
                    become
                    an animal nor a fish. A mammal will not beget reptiles, nor "do men
                    gather
                    &#8230; figs of thistles." (Matt. 7:16.)
                </big></p>
                <p style="font-family: arial;"><big>In the countless billions of
                    opportunities in the reproduction of living
                    things, one kind does not beget another. If a species ever does cross,
                    the offspring cannot reproduce. The pattern for all life is the pattern
                    of the parentage.
                </big></p>
                <p style="font-family: arial;"><big>This is demonstrated in so many
                    obvious ways, even an ordinary mind
                    should understand it. Surely no one with reverence for God could
                    believe
                    that His children evolved from slime or from reptiles. (Although one
                    can
                    easily imagine that those who accept the theory of evolution don't show
                    much enthusiasm for genealogical research!) The theory of evolution,
                    and
                    it is a theory, will have an entirely different dimension when the
                    workings
                    of God in creation are fully revealed.
                </big></p>
                <p style="font-family: arial;"><big>Since every living thing follows
                    the pattern of its parentage, are we
                    to suppose that God had some other [page 68] strange pattern in mind
                    for
                    His offspring? Surely we, His children, are not, in the language of
                    science,
                    a different species than He is?
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><b>Like God</b>
                </big></p>
                <p style="font-family: arial;"><big>What is in error, then, when we use
                    the term Godhood to describe the
                    ultimate destiny of mankind? We may now be young in our
                    progression--juvenile,
                    even infantile, compared with Him. Nevertheless, in the eternities to
                    come,
                    if we are worthy, we may be like unto Him, enter His presence, "see as
                    [we] are seen, and know as [we] are known," and receive a "fulness."
                    (D&amp;C
                    76:94.)
                </big></p>
                <p style="font-family: arial;"><big>This doctrine is not at variance
                    with the scriptures. Nevertheless,
                    it is easy to understand why some Christians reject it, because it
                    introduces
                    the possibility that man may achieve Godhood.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><b>One God</b>
                </big></p>
                <p style="font-family: arial;"><big>Their concern centers on certain
                    verses of scripture, for there are
                    many references (at least twenty in the Bible alone) which speak of one
                    God. For example, Eph. 4:6: There is "one God and Father of all."
                </big></p>
                <p style="font-family: arial;"><big>But if you hold strictly to a too
                    rigid interpretation of those verses,
                    you create serious theological problems for yourself.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><b>Plural Terms</b>
                </big></p>
                <p style="font-family: arial;"><big>There are many other verses of
                    scripture, at least an equal number in
                    the Bible, that speak in plural terms of "lords" and "gods." The first
                    chapter of Genesis states:
                </big></p>
                <p style="font-family: arial;"><big>"And God said, Let us make man in
                    our image, after our likeness." (Gen.
                    1:26; italics added.)
                </big></p>
                <p style="font-family: arial;"><big>Such references are found from
                    Genesis to Revelation. (See Rev. 1:6.)
                </big></p>
                <p style="font-family: arial;"><big>The strongest one was given by
                    Christ Himself when He quoted that very
                    clear verse from the Eighty-second Psalm:
                </big></p>
                <p style="font-family: arial;"><big>"Is it not written in your law, I
                    said, Ye are gods? [See Ps. 82:6.]
                </big></p>
                <p style="font-family: arial;"><big>"If he called them gods, unto whom
                    the word of God came, and the scripture
                    cannot be broken;
                </big></p>
                <p style="font-family: arial;"><big>"Say ye of him, whom the Father
                    hath sanctified, and sent into the world,
                    Thou blasphemest; because I said, I am the Son of God?" (John 10:34-36;
                    italics added.)
                </big></p>
                <p style="font-family: arial;"><big>The acceptance of this truth does
                    not mean accepting the multiple gods
                    of mythology nor the polytheism of the pagans, which was so roundly
                    condemned
                    by Isaiah and the other prophets.
                </big></p>
                <p style="font-family: arial;"><big>There is one God, the Father of
                    all. This we accept as fundamental doctrine.
                </big></p>
                <p style="font-family: arial;"><big>There is only one Redeemer,
                    Mediator, Savior. This we know.
                </big></p>
                <p style="font-family: arial;"><big>There is one Holy Ghost, a
                    personage of spirit, who completes the Godhead.
                </big></p>
                <p style="font-family: arial;"><big>I have emphasized the word one, in
                    each sentence, but I have used it
                    three times. Three is plural.
                </big></p>
                <p style="font-family: arial;"><big>Paul used the plural many and the
                    singular one in the same verse:
                </big></p>
                <p style="font-family: arial;"><big>"For though there be that are
                    called gods, whether in heaven or in earth,
                    (as there be gods many, and lords many,)
                </big></p>
                <p style="font-family: arial;"><big>"But to us there is but one God,
                    the Father." (1 Cor. 8:5-6.)
                </big></p>
                <p style="font-family: arial;"><big>Anyone who believes and teaches of
                    God the Father, and accepts the divinity
                    of Christ, and of the Holy Ghost, teaches a plurality of Gods.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big><b>Relying on Reason</b>
                </big></p>
                <p style="font-family: arial;"><big>When the early Apostles were gone,
                    those who assumed the leadership
                    of the Church forsook revelation and relied on reason. The idea of
                    three
                    separate Gods offended them, for it appeared to contravene those
                    scriptures
                    which refer to one God.
                </big></p>
                <p style="font-family: arial;"><big>To reconcile that problem, they
                    took verses here and there and ignored
                    all else that bears on the subject. They tried to stir the three ones
                    together
                    into some mysterious kind of a composite one. They came up with creeds
                    which cannot be squared with the scriptures. And they were left with a
                    philosophy which opposes all we know of creation, of the laws of
                    nature.
                    And that, interestingly enough, defies the very reason upon which they
                    came to depend.
                </big></p>
                <p style="font-family: arial;"><big>The Apostle Paul understood this
                    doctrine and wrote to the Philippians:
                </big></p>
                <p style="font-family: arial;"><big>"Let this mind be in you, which was
                    also in Christ Jesus:
                </big></p>
                <p style="font-family: arial;"><big>"Who, being in the form of God,
                    thought it not robbery to be equal with
                    God." (Philip. 2:5-6.)
                </big></p>
                <p style="font-family: arial;"><big>Lorenzo Snow, a modern Apostle,
                    wrote a poem to his ancient counterpart
                    Paul, from which I quote only one verse:
                </big></p>
                <blockquote style="font-family: arial;"><big>A Son of God, like God to
                        be,
                        <br>
                        Would not be robbing Deity,
                        <br>
                        And he who has this hope within,
                        <br>
                        Will purify himself from sin.
                        <br>
                        (<i>Improvement Era</i>, June 1919, p. 661.)</big></blockquote>
                <big><b style="font-family: arial;">Purity</b></big>
                <p style="font-family: arial;"><big>What could inspire one to purity
                    and worthiness more than to possess
                    a spiritual confirmation that we are the children of God? What could
                    inspire
                    a more lofty regard for oneself, or engender more love for mankind?
                </big></p>
                <p style="font-family: arial;"><big>This thought does not fill me with
                    arrogance. It fills me with overwhelming
                    humility. Nor does it sponsor any inclination to worship oneself or any
                    man.
                </big></p>
                <p style="font-family: arial;"><big>The doctrine we teach has no
                    provision for lying or stealing, for pornography,
                    immoralities, for child [page 69] abuse, for abortion, or murder. We
                    are
                    bound by the laws of His church, as sons and daughters of God, to avoid
                    all of these and every other unholy or impure practice.
                </big></p>
                <p style="font-family: arial;"><big>We did not invent this doctrine.
                    Much of it was preserved in the Bible
                    as it was revealed to prophets in ancient times. And as they foretold,
                    further light and knowledge was revealed.
                </big></p>
                <p style="font-family: arial;"><big>With the restoration of the fulness
                    of the gospel came the Book of Mormon,
                    Another Testament of Jesus Christ. Other revelations were given and
                    continue
                    to be given, and verses which seemed to oppose one another have
                    harmony.
                </big></p>
                <p style="font-family: arial;"><big>The Prophet Joseph Smith said, "It
                    is the first principle of the Gospel
                    to know for a certainty the Character of God." (<i>Teachings of the
                        Prophet
                        Joseph Smith</i>, sel. Joseph Fielding Smith, Salt Lake City: Deseret
                    Book
                    Co. 1938, p. 345.)
                </big></p>
                <p style="font-family: arial;"><big>And that knowledge is given us.
                </big></p>
                <p style="font-family: arial;"><big>The Father is the one true God.
                    This thing is certain: no one will ever
                    ascend above Him; no one will ever replace Him. Nor will anything ever
                    change the relationship that we, His literal offspring, have with Him.
                    He is Eloheim, the Father. He is God. Of Him there is only one. We
                    revere
                    our Father and our God; we worship Him.
                </big></p>
                <p style="font-family: arial;"><big>There is only one Christ, one
                    Redeemer. We accept the divinity of the
                    Only Begotten Son of God in the flesh. We accept the promise that we
                    may
                    become joint heirs with Him. Paul wrote to the Romans:
                </big></p>
                <p style="font-family: arial;"><big>"The spirit itself beareth witness
                    with our spirit, that we are the
                    children of God:
                </big></p>
                <p style="font-family: arial;"><big>"And if children, then heirs; heirs
                    of God, and joint-heirs with Christ"
                    (Rom. 8:16-17.)
                </big></p>
                <p style="font-family: arial;"><big>There are those who mock our
                    beliefs in the most uncharitable ways.
                    And we will bear what they do with long-suffering, for it does not
                    change
                    truth. And in their own way they move our work along a little faster.
                    We
                    will send our missionaries abroad to teach that we are the literal sons
                    and daughters of God.
                </big></p>
                <p style="font-family: arial;"><big>We will strive with every exertion
                    to teach what Christ taught, to live
                    as He lived, to endure as He endured.
                </big></p>
                <p style="font-family: arial;"><big>We began with this question: "What
                    is man that thou art mindful of him?"
                    Christ, our Redeemer, our Elder Brother, asked, "What manner of men
                    ought
                    ye to be?" And then He answered, "Verily I say unto you, even as I am."
                    (3 Ne. 27:27.)
                </big></p>
                <p><big><span style="font-family: arial;">I bear solemn witness that
                        Jesus is the Christ, the Only Begotten of
                        the Father in the flesh; that He is our Redeemer, our Savior; that God
                        is our Father. This we know through the gift of the Holy Ghost. And I
                        humbly
                        but resolutely affirm that we will not, we cannot, stray from this
                        doctrine.
                        On this fundamental truth we will never yield! In the name of Jesus
                        Christ,
                        amen.<br>
                    </span></big></p>
