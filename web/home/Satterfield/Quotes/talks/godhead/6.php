                <center><span STYLE="font-size: 20pt"><i>The Grandeur of God</i></span>
                    <p>Elder Jeffery R. Holland
                        <br>Of the Quorum of the Twelve Apostles
                        <br><i>Ensign</i>, Nov. 2003, pp. ??
                    <p>[Note: Original footnotes have been placed within the text.]</span></center>

                <p>
                <p>Of the many magnificent purposes served in the life and ministry of
                    the Lord Jesus Christ, one great aspect of that mission often goes uncelebrated.
                    His followers did not understand it fully at the time, and many in modern
                    Christianity do not grasp it now, but the Savior Himself spoke of it repeatedly
                    and emphatically. It is the grand truth that in all that Jesus came to
                    say and do, including and especially in His atoning suffering and sacrifice,
                    He was showing us who and what God our Eternal Father is like, how completely
                    devoted He is to His children in every age and nation. In word and in deed
                    Jesus was trying to reveal and make personal to us the true nature of His
                    Father, our Father in Heaven.
                <p>He did this at least in part because then and now all of us need to
                    know God more fully in order to love Him more deeply and obey Him more
                    completely. As both Old and New Testaments declare, "The first of all the
                    commandments is . . . thou shalt love the Lord thy God with all thy heart,
                    and with all thy soul, and with all thy mind, and with all thy strength:
                    this is the first [and great] commandment" (Mark 12:29-30; see also Matthew
                    22:37-38; Deuteronomy 6:5).
                <p>Little wonder then that the Prophet Joseph Smith taught: "It is the
                    first principle of the gospel to know for a certainty the character of
                    God." "I want you all to know Him," he said, "and to be familiar with Him"
                    (<i>History of the Church,</i> 6:305.). We must have "a
                    <i>correct</i>
                    idea of his . . . perfections, and attributes," an admiration for "the
                    excellency of [His] character" (<i>Lectures on Faith</i> (1985), 38, 42.).
                    Thus the first phrase we utter in the declaration of our faith is, "We
                    believe in God, the Eternal Father" (Articles of Faith 1:1.). So, emphatically,
                    did Jesus. Even as He acknowledged His own singular role in the divine
                    plan, the Savior nevertheless insisted on this prayerful preamble: "And
                    this is life eternal, that they might know thee the only true God" (John
                    17:3).
                <p>After generations of prophets had tried to teach the family of man the
                    will and the way of the Father, usually with little success, God in His
                    ultimate effort to have us know Him, sent to earth His Only Begotten and
                    perfect Son, created in His very likeness and image, to live and serve
                    among mortals in the everyday rigors of life.
                <p>To come to earth with such a responsibility, to stand in place of Elohim-speaking
                    as He would speak, judging and serving, loving and warning, forbearing
                    and forgiving as He would do-this is a duty of such staggering proportions
                    that you and I cannot comprehend such a thing. But in the loyalty and determination
                    that would be characteristic of a divine child, Jesus could comprehend
                    it and He did it. Then, when the praise and honor began to come, He humbly
                    directed all adulation to the Father.
                <p>"The Father . . . doeth the works," He said in earnest. "The Son can
                    do nothing of himself, but what he seeth the Father do: for what things
                    soever [the Father] doeth, these also doeth the Son likewise" (John 14:10;
                    5:19). On another occasion He said: "I speak that which I have seen with
                    my Father." "I do nothing of myself; but as my Father hath taught me."
                    "I came down from heaven, not to do mine own will, but the will of him
                    that sent me" (John 8:38, 28; 6:38).
                <p>I make my own heartfelt declaration of God our Eternal Father this morning
                    because some in the contemporary world suffer from a distressing misconception
                    of Him. Among these there is a tendency to feel distant from the Father,
                    even estranged from Him, if they believe in Him at all. And if they do
                    believe, many moderns say they might feel comfortable in the arms of Jesus,
                    but they are uneasy contemplating the stern encounter of God (see William
                    Barclay, <i>The Mind of Jesus</i> (1961), especially the chapter "Looking
                    at the Cross" for a discussion of this modern tendency). Through a misreading
                    (and surely, in some cases, a mistranslation) of the Bible, these see God
                    the Father and Jesus Christ His Son as operating very differently, this
                    in spite of the fact that in both the Old Testament and the New, the Son
                    of God is one and the same, acting as He always does under the direction
                    of the Father, who is Himself the same "yesterday, today, and forever"
                    (for example, 1 Ne. 10:18; 2 Nephi 27:23; Moroni 10:19; D&amp;C 20:12).
                <p>In reflecting on these misconceptions we realize that one of the remarkable
                    contributions of the Book of Mormon is its seamless, perfectly consistent
                    view of divinity throughout that majestic book. Here there is no Malachi-to-Matthew
                    gap, no pause while we shift theological gears, no misreading the God who
                    is urgently, lovingly, faithfully at work on every page of that record
                    from its Old Testament beginning to its New Testament end. Yes, in an effort
                    to give the world back its Bible and a correct view of Deity with it, what
                    we have in the Book of Mormon is a uniform view of God in all His glory
                    and goodness, all His richness and complexity-including and especially
                    as again demonstrated through a personal appearance of His Only Begotten
                    Son, Jesus Christ.
                <p>How grateful we are for <i>all</i> the scriptures, especially the scriptures
                    of the Restoration, that teach us the majesty of each member of the Godhead.
                    How we would thrill, for example, if all the world would receive and embrace
                    the view of the Father so movingly described in the Pearl of Great Price.
                <p>There, in the midst of a grand vision of humankind which heaven opened
                    to his view, Enoch, observing both the blessings and challenges of mortality,
                    turns his gaze toward the Father and is stunned to see Him weeping. He
                    says in wonder and amazement to this most powerful Being in the universe:
                    "How is it that thou canst weep? . . . Thou art just [and] merciful and
                    kind forever; . . . Peace . . . is the habitation of thy throne; and mercy
                    shall go before thy face and have no end; how is it thou canst weep?"
                <p>Looking out on the events of almost any day, God replies: "Behold these
                    thy brethren; they are the workmanship of mine own hands. . . . I gave
                    unto them . . . [a] commandment, that they should love one another, and
                    that they should choose me, their Father; but behold, they are without
                    affection, and they hate their own blood. . . . Wherefore should not the
                    heavens weep, seeing these shall suffer?" (Moses 7:29-33, 37)
                <p>That single, riveting scene does more to teach the true nature of God
                    than any theological treatise could ever convey. It also helps us understand
                    much more emphatically that vivid moment in the Book of Mormon allegory
                    of the olive tree, when after digging and dunging, watering and weeding,
                    trimming, pruning, transplanting, and grafting, the great Lord of the vineyard
                    throws down his spade and his pruning shears and weeps, crying out to any
                    who would listen, "What could I have done more for my vineyard?" (Jacob
                    5:41; see also vv. 47, 49)
                <p>What an indelible image of God's engagement in our lives! What anguish
                    in a parent when His children do not choose Him nor "the gospel of God"
                    He sent! (Romans 1:1) How easy to love someone who so singularly loves
                    us!
                <p>Of course the centuries-long drift away from belief in such a perfect
                    and caring Father hasn't been helped any by the man-made creeds of erring
                    generations which describe God variously as unknown and unknowable----formless,
                    passionless, elusive, ethereal, simultaneously everywhere and nowhere at
                    all. Certainly that does not describe the Being we behold through the eyes
                    of these prophets. Nor does it match the living, breathing, embodied Jesus
                    of Nazareth who was and is in "the brightness of his glory, and the express
                    image of his [Father]" (Hebrews 1:3; see also 2 Corinthians 4:4; Colossians
                    1:15).
                <p>In that sense Jesus did not come to improve God's view of man nearly
                    so much as He came to improve man's view of God and to plead with them
                    to love their Heavenly Father as He has always and will always love them.
                    The plan of God, the power of God, the holiness of God, yes, even the anger
                    and the judgment of God they had occasion to understand. But the love of
                    God, the profound depth of His devotion to His children, they still did
                    not fully know-until Christ came.
                <p>So feeding the hungry, healing the sick, rebuking hypocrisy, pleading
                    for faith-this was Christ showing us the way of the Father, He who is "merciful
                    and gracious, slow to anger, long-suffering and full of goodness" (<i>Lectures
                        on Faith</i>, 42). In His life and especially in His death, Christ was
                    declaring, "This is <i>God's</i> compassion I am showing you, as well as
                    that of my own." In the perfect Son's manifestation of the perfect Father's
                    care, in Their mutual suffering and shared sorrow for the sins and heartaches
                    of the rest of us, we see ultimate meaning in the declaration: "For God
                    so loved the world, that he gave his only begotten Son, that whosoever
                    believeth in him should not perish, but have everlasting life. For God
                    sent not his Son into the world to condemn the world; but that the world
                    through him might be saved" (John 3:16-17).
                <p>I bear personal witness this day of a personal, living God, who knows
                    our names, hears and answers prayers, and cherishes us eternally as children
                    of His spirit. I testify that amidst the wondrously complex tasks inherent
                    in the universe, He seeks our individual happiness and safety above all
                    other godly concerns. We are created in His very image and likeness (see
                    Genesis 1:26-27; Moses 2:26-27), and Jesus of Nazareth, His Only Begotten
                    Son in the flesh, came to earth as the perfect mortal manifestation of
                    His grandeur. In addition to the witness of the ancients we also have the
                    modern miracle of Palmyra, the appearance of God the Father and His Beloved
                    Son, the Savior of the world, to the boy prophet Joseph Smith. I testify
                    of that appearance, and in the words of that prophet I, too, declare: "Our
                    heavenly Father is more liberal in His views, and boundless in His mercies
                    and blessings, than we are ready to believe or receive. . . . God does
                    not look on sin with [the least degree of] allowance, but . . . the nearer
                    we get to our heavenly Father, the more we are disposed to look with compassion
                    on perishing souls; we feel that we want to take them upon our shoulders,
                    and cast their sins behind our backs" (<i>Teachings of the Prophet Joseph
                        Smith</i>, Sel Joseph Fielding Smith [1976], 257, 240-41).
                <p>I bear witness of a God who has such shoulders. And in the spirit of
                    the holy apostleship, I say as did one who held this office anciently:
                    "Herein [then] is love, not that we loved God, but that he loved us, and
                    sent his Son to be the propitiation for our sins. Beloved, if God so loved
                    us, we ought also to love one another"(1 John 4:10)-and to love Him forever,
                    I pray. In the sacred name of Jesus Christ, amen.

