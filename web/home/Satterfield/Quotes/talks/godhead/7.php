                <center><span STYLE="font-size: 24pt"><i>God the Father</i></span>
                    <p>Elder Bruce R. McConkie
                        <br>Quorum of the Twelve Apostles
                    <p>From <i>A New Witness for the Articles of Faith</i>
                        <br>(Salt Lake City: Deseret Book, 1985), pp.58-65
                    <p><b>Nature of the Godhead</b></center>

                <p><br>
                <p>Three glorious persons comprise the Godhead or supreme presidency of
                    the universe. They are the Father, the Son, and the Holy Ghost. Each one
                    possesses the same divine nature, knows all things, and has all power.
                    Each one has the same character, the same perfections and the same attributes.
                    In Christ, Paul says, "dwelleth all the fulness of the Godhead bodily."
                    (Colossians 2:9.) Because of this perfect unity, they are spoken of as
                    being one God. "Hear, O Israel," Moses proclaimed, "the Lord our God is
                    one Lord: And thou shalt love the Lord thy God with all thine heart, and
                    with all thy soul, and with all thy might." (Deuteronomy 6:4-5.) This unity
                    and oneness is the perfect pattern for all who seek salvation for salvation
                    consists in being one with the Lord our God. Thus Jesus prayed for all
                    true believers, "that they all may be one; as thou, Father, art in me,
                    and I in thee, that they also may be one in us." (John 17:21.)
                <p>This divine doctrine that the three persons in the Godhead are one is
                    as clear and as plain and as easy to understand as any of the basic doctrines
                    of the gospel. There is little excuse for the vagaries and contradictions
                    found in the creeds of Christendom. These three divine beings comprise
                    what has come to be called, in the sectarian world, the Holy Trinity. The
                    sacred name -- titles they bear are not simply different manifestations
                    of the same divine essence or power or being. The living Gods are individuals
                    who can and do come and go throughout the universe as the needs of their
                    eternal ministries require. Men cannot worship the true God with that faith
                    and devotion which brings salvation unless they distinguish between the
                    members of the Godhead and know the place and functions of each.
                <p>Our faith is centered in the Son of God, and through him in the Father,
                    and the Holy Ghost is their minister. It is of their diverse yet coordinated
                    functions that Joseph Smith said: "Everlasting covenant was made between
                    three personages before the organization of this earth, and relates to
                    their dispensation of things to men on the earth; these personages, according
                    to Abraham's record, are called God the first, the Creator; God the second,
                    the Redeemer; and God the third the witness or Testator." (<i>Teachings
                        of the Prophet Joseph Smith</i>, p. 190.) Creation, redemption, and testification
                    are thus the chief and paramount labors of the respective members of the
                    Godhead. These are separate and identifiable pursuits, each as distinct
                    from the others as the Gods of heaven are separate in being from each other.
                    And yet the three are inseparably woven together as the essential parts
                    of one eternal plan of salvation; they unite as one, as do those Gods whose
                    work they are. We shall consider these separable yet united labors along
                    with others that relate to them, each in due order and in its proper place.
                    <br>&nbsp;
                <p><br>
                <center>
                    <p><b>Ministry of the Father</b></center>

                <p>There are four ways in which the labors and ministry and status of "God
                    the first," who is the Father, are distinct and separate from the other
                    members of the Godhead. These are:
                <p>1. <i>The Father is a Holy Man; he is Ahman, the Almighty Elohim.</i>
                <p>The profound truth concerning God is that he is a Holy Man, a personage
                    of tabernacle, a being in whose image and likeness mortal man was made.
                    His work and his glory are to bring to pass the immortality and eternal
                    life of man. Eternal life is the name of the kind of life he lives and
                    until men know that he is a Holy Man, they will never have the desire and
                    the incentive to become like him and to be inheritors of eternal life.
                <p>The whole body of revealed writ attests to the eternal verity that the
                    Supreme God is a Holy Man. "In the language of Adam," the Mosaic account
                    recites, "Man of Holiness is his name, and the name of his Only Begotten
                    is the Son of Man even Jesus Christ." (Moses 6:57.) Thus, when Jesus asked
                    the ancient disciples, "Whom do men say that I the Son of man am?" (Matthew
                    16:13), it was as though he asked: "Who do men say that I am? I testify
                    that I am the Son of Man of Holiness, which is to say, the Son of that
                    Holy Man who is God, but who do men say that I am?" In this same vein,
                    one of the early revelations given in this dispensation asks: "What is
                    the name of God in the pure language?" The answer: "Ahman." Question: "What
                    is the name of the Son of God?" Answer: "Son Ahman." (Orson Pratt, cited
                    in <i>Mormon Doctrine</i>, 2nd ed., p. 29.) And to Enoch the Lord identified
                    himself by saying: "Behold, I am God; Man of Holiness is my name; Man of
                    Counsel is my name; and Endless and Eternal is my name, also." (Moses 7:35.)
                <p>Properly understood, the four Gospels (Matthew, Mark, Luke, and John)
                    contain the purest and most expansive accounts of the nature and kind of
                    being that God is that can be found anywhere in the revealed word. All
                    Christians are assumed to know that God, in the meridian day, was in Christ
                    manifesting himself to the world. We know what Christ was like as a mortal,
                    and we know the kind of body he now possesses in glorious immortality.
                    Thus, we are not left in darkness nor in doubt as to the character, perfections,
                    and attributes of the Father, nor as to the kind of body now possessed
                    by the Father of the Son of Man.
                <p>The resurrected Lord Jesus invited great hosts of Nephites to feel the
                    nail marks in his hands and feet and to thrust their hands into his riven
                    side. His disciples in Jerusalem, assembled in the Upper Room on the very
                    day of his resurrection had a similar privilege. To them he said: "Behold
                    my hands and my feet, that it is I myself: handle me, and see; for a spirit
                    hath not flesh and bones, as ye see me have." And then in their presence
                    he ate "a piece of a broiled fish, and of an honeycomb," that they might
                    know and bear witness that his body was real and tangible and personal,
                    one that could eat and digest food even as is the case with mortal bodies.
                    (Luke 24:39-43.) These solemn scenes give meaning and light and life to
                    Paul's words: "God, who at sundry times and in divers manners spake in
                    time past unto the fathers by the prophets, hath in these last days spoken
                    unto us by his Son, whom he hath appointed heir of all things, by whom
                    also he made the worlds; who being the brightness of his glory, and the
                    express image of his person, and upholding all things by the word of his
                    power, when he had by himself purged our sins, sat down on the right hand
                    of the Majesty on high." (Hebrews 1:1-3.) And these scenes, coupled with
                    Paul's words, are but the foundation for Joseph Smith's revealed pronouncement:
                    "The Father has a body of flesh and bones as tangible as man's; the Son
                    also; but the Holy Ghost has not a body of flesh and bones, but is a personage
                    of Spirit." (D&amp;C 130:22.)
                <p>Perhaps no single verse of scripture has spread greater spiritual confusion
                    or led more people to build their houses of worship on the sands of false
                    doctrine than John 4:24. It contains the words "God is a Spirit," which
                    are interpreted by the Christian world to mean that God is a spirit essence
                    that fills all space, has no form or substance, and dwells in human hearts.
                    It might properly be said that "God is a Spirit" if by that is meant that
                    he has a spiritual or resurrected body in harmony with Paul's statement
                    relative to the resurrection that the body "is sown a natural body; it
                    is raised a spiritual body. There is a natural [mortal] body, and there
                    is a spiritual [resurrected] body." (1 Corinthians 15:44.) But the fact
                    is that John 4:24 is mistranslated. It is part of a passage in which Jesus
                    is teaching that the Father seeks true worshippers who will worship him
                    in spirit and in truth. "For unto such," he says, "hath God promised his
                    Spirit." (JST, John 4:26.)
                <p>2. <i>Elohim is the Father of spirits.</i>
                <p>As we view the starry heavens with their endless orbs, all spinning
                    in assigned spheres; as we marvel at the wonders of life in all its varieties,
                    as it is found on this and endless other earths; as we think of the miracles
                    of creation, of redemption, and of everlasting life, all operating by the
                    will and at the word of the Creator of the universe, we are led to exclaim:
                    How can we conceive of such an Omnipotent God? how can mere man ever hope
                    to know him whom it is eternal life to know? Can man comprehend God?
                <p>Strangely, marvelously, wondrously, the answer is found in the simple
                    declaration that God is our Father. Our finite minds cannot comprehend
                    his infinite laws. But we can envision him as a Father, as a personal,
                    loving being filled with tenderness and compassion. He is more than the
                    Father of the
                <p>Firstborn; more than the Father of the Only Begotten in the flesh; more
                    than the Father in the sense that he created the first mortal man. He is,
                    in deed and in fact, the Father of the spirits of all men in the literal
                    and full sense of the word. Each of us was begotten by him in the premortal
                    life. We are his spirit children.
                <p>Holy writ abounds in statements about the preexistent life of man. To
                    Adam the divine voice said: "I am God; I made the world, and men before
                    they were in the flesh." (Moses 6:51.) Enoch, in vision, "beheld the spirits
                    that God had created." (Moses 6:36.) To Moses, the great Creator said:
                    "I, the Lord God, had created all the children of men; and not yet a man
                    to till the ground; for in heaven created I them." (Moses 3:5.) Abraham
                    saw all the hosts of spirits in their preexistent home. They were identified
                    as "the intelligences that were organized before the world was." They were
                    called "souls" and "spirits," and the "noble and great ones" among them
                    were foreordained to perform prophetic labors in mortality. Abraham saw
                    that "there stood one among them that was like unto God." This one, under
                    the Father, became the Creator, and was chosen and foreordained to be the
                    Redeemer. Those spirits who kept their first estate were then assured of
                    a mortal probation, while those who rebelled were cast out of heaven. (Abraham
                    3:22-28.) Isaiah and John and Jude, in their biblical accounts, tell us
                    of the casting out of the rebels from their heavenly home. (Isaiah 14:12-20;
                    Revelation 12:7-9; Jude 1:6.) Lehi records the same event in the Book of
                    Mormon. (2 Nephi 2:17.) All of Paul's teachings about election and the
                    special blessings given the house of Israel presuppose an understanding
                    of the premortal life and the foreordination of the faithful spirits. And
                    one of Paul's most compelling exhortations to keep the commandments is
                    couched in these words: "We have had fathers of our flesh which corrected
                    us, and we gave them reverence: shall we not much rather be in subjection
                    unto the Father of spirits, and live?" (Hebrews 12:9.)
                <p>There are hundreds of scriptural passages that speak of the Father and
                    his relationship to mortals. Implicit in all of them is the eternal verity
                    that we are his children. After his resurrection, Jesus said to Mary Magdalene:
                    "I ascend unto my Father, and your Father" (John 20:17), thus attesting
                    that God both had a Son in mortality and was the Father of all men in the
                    spirit. Ancient Israel prayed: "O God, the God of the spirits of all flesh."
                    (Numbers 16:22.) Jesus taught the people to pray: "Our Father which art
                    in heaven." (Matthew 6:9.) How better could he have taught us our relationship
                    to the Father of us all?
                <p>3. <i>The Father is the Creator</i>.
                <p>In the ultimate and final sense of the word, the Father is the Creator
                    of all things. That he used the Son and others to perform many of the creative
                    acts, delegating to them his creative powers, does not make these others
                    creators in their own right, independent of him. He is the source of all
                    creative power, and he simply chooses others to act for him in many of
                    his creative enterprises. But there are two creative events that are his
                    and his alone. First, he is the Father of all spirits, Christ's included;
                    none were fathered or created by anyone else. Second, he is the Creator
                    of the physical body of man. Though Jehovah and Michael and many of the
                    noble and great ones played their assigned roles in the various creative
                    events, yet when it came time to place man on earth, the Lord God himself
                    performed the creative acts. "I, God, created man in mine own image, in
                    the image of mine Only Begotten created I him; male and female created
                    I them." (Moses 2:27.)
                <p>4. <i>The Father is the author of the plan of salvation.</i>
                <p>Only the Father is or could be the author of the plan of salvation.
                    He alone was in a position to ordain the laws and establish the system
                    whereby his spirit children (Christ included!) could be saved. Others might
                    perform labors for a portion or for all of the family of God, but only
                    he could create salvation and specify how it might be gained. This concept
                    becomes clear when we learn how God came to be God, how he gained his exaltation,
                    and how he made provision for his spirit children to go and do likewise.
                <p>As to God's high status as an exalted man, as to how he attained this
                    position of supreme eminence, and as to how we may pursue the same course
                    to the same eternal destiny, the Prophet Joseph Smith proclaimed: "God
                    himself was once as we are now, and is an exalted man, and sits enthroned
                    in yonder heavens! That is the great secret." Let all the saints ponder
                    these words in their hearts; let them plead for enlightenment from the
                    Holy Spirit; let them know that the concept involved opens the door to
                    an understanding of the plan of salvation. "If the veil were rent today,
                    and the great God who holds this world in its orbit, and who upholds all
                    worlds and all things by His power, was to make himself visible, -- I say,
                    if you were to see him today, you would see him like a man in form -- like
                    yourselves in all the person, image, and very form as a man; for Adam was
                    created in the very fashion, image and likeness of God, and received instruction
                    from, and walked, talked and conversed with Him, as one man talks and communes
                    with another." Oh, how great the importance to make these things known
                    unto all men so that they no longer worship gods of their own creating!
                <p>"It is the first principle of the gospel to know for a certainty the
                    character of God," the inspired word continues, "and to know that we may
                    converse with Him as one man converses with another, and that He was once
                    a man like us; yea, that God himself, the Father of us all, dwelt on an
                    earth, the same as Jesus Christ Himself did." The Father is a glorified,
                    perfected, resurrected, exalted man who worked out his salvation by obedience
                    to the same laws he has given to us so that we may do the same.
                <p>"Here, then, is eternal life -- to know the only wise and true God;
                    and you have got to learn how to be gods yourselves and to be kings and
                    priests to God, the same as all gods have done before you, namely, by going
                    from one small degree to another, and from a small capacity to a great
                    one; from grace to grace, from exaltation to exaltation, until you attain
                    to the resurrection of the dead, and are able to dwell in everlasting burnings,
                    and to sit in glory, as do those who sit enthroned in everlasting power."
                    (King Follett Address in <i>History of the Church</i> 6:305-6.)
                <p>In the light of this infinite and eternal perspective of the everlasting
                    system of salvation, how could anyone but the Father create a plan of salvation
                    for his spirit children? And so we read in the inspired word: "God himself,
                    finding he was in the midst of spirits and glory, because he was more intelligent,
                    saw proper to institute laws whereby the rest could have a privilege to
                    advance like himself. The relationship we have with God places us in a
                    situation to advance in knowledge. He has power to institute laws to instruct
                    the weaker intelligences, that they may be exalted with Himself, so that
                    they might have one glory upon another, and all that knowledge, power,
                    glory, and intelligence, which is requisite in order to save them in the
                    world of spirits." (Ibid., p. 312.)
                <p>After the plan of salvation had been taught to all the spirit children
                    of the Father; after the nature of this mortal probation had been explained;
                    after the need for a Redeemer had been set forth -- then the Lord God who
                    is the Father sent forth a great cry in the grand council. "Whom shall
                    I send?" he asked. `Whom shall I send to be my Son, to be the Savior and
                    Redeemer, to work out the infinite and eternal atonement? Whom shall I
                    send to put into full operation all the terms and conditions of my plan
                    of salvation?'
                <p>There were two volunteers. The Lord Jehovah said: "Here am I, send me."
                    (Abraham 3:27.) And also: "Father, thy will be done, and the glory be thine
                    forever." That is to say: `Send me and I will follow thy plan, Father;
                    men shall have their agency, and all things will be done as thou wilt.'
                    The second volunteer, who was Lucifer, said: "Behold, here am I, send me,
                    I will be thy son, and I will redeem all mankind, that one soul shall not
                    be lost, and surely I will do it; wherefore give me thine honor." (Moses
                    4:1-2.) Thus Satan sought to amend the plan of the Father and to take the
                    glory in all things unto himself. And then "the Lord," who is the Father,
                    said, "I will send the first." (Abraham 3:27.)
                <p>The plan of salvation is known to us as the gospel of Jesus Christ.
                    In the ultimate sense, as Paul says, it is "the gospel of God ... concerning
                    his Son Jesus Christ our Lord." (Romans 1:1-3.) We, properly, call it after
                    the Son of God to show that all of its terms and conditions are put into
                    operation because of his atoning sacrifice. But the plan is the Father's;
                    he is its author. It is Christ's by adoption, even as it is ours by adoption
                    if we believe and obey its laws and ordinances.

