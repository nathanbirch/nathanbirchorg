            <main class="container">
                <center style="font-family: arial;"><big><i><font size="+2"><big>Our
                                Relationship With The Lord</big></font></i></big>
                    <p><font size="+1">Elder Bruce R. McConkie</font> <br>
                        Quorum of the Twelve Aposltes<br>
                    </p>
                    <p>(BYU Devotional--Mar 2, 1982)
                    </p>
                    <p><i>Brigham Young University 1981-82 Fireside and Devotional Speeches</i>
                        <br>
                        Provo: Brigham Young Unversity, 1982, pp. 97-103.</p>
                </center>
                <p style="font-family: arial;"><big>I shall speak about our
                    relationship with the Lord and of the true fellowship
                    all saints should have with the Father. I shall set forth what we must
                    believe relative to the Father and the Son in order to gain eternal
                    life.
                </big></p>
                <p style="font-family: arial;"><big>I shall expound the doctrine of the
                    Church relative to what our relationship
                    should be to all members of the Godhead, and do so in plainness and
                    simplicity
                    so that none need mis-understand or be led astray by other voices.
                </big></p>
                <p style="font-family: arial;"><big>I shall express the view of the
                    Brethren, of the prophets and apostles
                    of old, and of all those who understand the scriptures and are in tune
                    with the Holy Spirit.
                </big></p>
                <p style="font-family: arial;"><big>These matters lie at the very
                    foundation of revealed religion. In presenting
                    them I am on my own ground and am at home with my subject. I shall not
                    stoop to petty wranglings about semantics, but shall stay with matters
                    of substance. I shall simply go back to basics and set forth
                    fundamental
                    doctrines of the kingdom, knowing that everyone who is sound
                    spiritually
                    and who has the guidance of the Holy Spirit will believe my words and
                    follow
                    my counsel.
                </big></p>
                <p style="font-family: arial;"><big>Please do not put too much stock in
                    some of the current views and vagaries
                    that are afloat, but rather, turn to the revealed word, get a sound
                    understanding
                    of the doctrines, and keep yourselves in the mainstream of the Church.
                </big></p>
                <p style="font-family: arial;"><big>Now, it is no secret that many
                    false and vain and foolish things are
                    being taught in the sectarian world and even among us about our need to
                    gain a special relationship with the Lord Jesus. I shall summarize the
                    true doctrine in this field and invite erring teachers and beguiled
                    students
                    to repent and believe the accepted gospel verities as I shall set them
                    forth.
                </big></p>
                <p style="font-family: arial;"><big>There is no salvation in believing
                    any false doctrine, particularly
                    a false or unwise view about the Godhead or any of its members. Eternal
                    Life is reserved for those who know God and the one whom he sent to
                    work
                    out the infinite and eternal atonement.
                </big></p>
                <p style="font-family: arial;"><big>True and saving worship is found
                    only among those who know the truth
                    about God and the Godhead and who understand the true relationship men
                    should have with each member of that Eternal Presidency.
                </big></p>
                <p style="font-family: arial;"><big>It follows that the devil would
                    rather spread false doctrine about God
                    and the Godhead, and induce false feelings with reference to any one of
                    them that almost any other thing he could do. The creeds of Christendom
                    illustrate perfectly what Lucifer wants so- called Christian people to
                    believe about deity, in order to be damned.
                </big></p>
                <p style="font-family: arial;"><big>These creeds codify what Jeremiah
                    calls the lies about God. They say
                    he is unknown, uncreated, and incomprehensible. They say he is a spirit
                    without body, parts or passions. They say he is everywhere and nowhere
                    in particular present. That he fills the immensity of space and yet
                    dwells
                    in the hearts of men, and that he is an immaterial, incorporeal
                    nothingness.
                    They say he is one-God- in-three and three-Gods-in-one who neither
                    hears,
                    nor sees, nor speaks. Some even say he is dead, which he might as well
                    be if their descriptions identify his being.
                </big></p>
                <p style="font-family: arial;"><big>These concepts summarize the chief
                    and greatest herracy of Christendom.
                    Truly the most grievous and evil herracy ever imposed on an erring and
                    wayward Christianity is their creedal concept about God and the
                    Godhead!
                    But none of this troubles us very much. God has revealed himself to us
                    in this day even as he did to the prophets of old.
                </big></p>
                <p style="font-family: arial;"><big>We know thereby, that he a personal
                    being in whose image man was made.
                    We know that he has a body of flesh and bones as tangible as man's.
                    That
                    he is a resurrected, glorified, and perfected being; and that he lives
                    in the family unit. We know that we are his spirit children; that he
                    endowed
                    us with the divine gift of agency; and that he ordained the laws
                    whereby
                    we might advance and progress and become like him.
                </big></p>
                <p style="font-family: arial;"><big>We know that God is the only
                    supreme and independent being in whom all
                    fullness and perfection dwell and that he is omnipotent, omniscient,
                    and
                    by the power of his spirit, omnipresent.
                </big></p>
                <p style="font-family: arial;"><big>We know "the Almighty God gave his
                    Only Begotten Son" (D&amp;C 20:21),
                    as the scriptures attest, to ransom man from the temporal and spiritual
                    death brought into the world by the fall of Adam, and to put into
                    operation
                    all of the terms and conditions of the Father's plan.
                </big></p>
                <p style="font-family: arial;"><big>We know that the Holy Ghost, as a
                    personage of Spirit, is both a revelator
                    and a sanctifier and that his chief mission is to bear record of the
                    Father
                    and of the Son.
                </big></p>
                <p style="font-family: arial;"><big>Thus there are, in the eternal
                    Godhead, three persons--God the first,
                    the Creator; God the second, the Redeemer; and God the third, the
                    Testator.
                    These three are one--one God if you will--in purposes, in powers, and
                    in
                    perfections. But each has his own separable work to perform, and
                    mankind
                    has a defined and known and specific relationship to each one of them.
                    It is of these relationships we shall now speak.
                </big></p>
                <p style="font-family: arial;"><big>Let us set forth those doctrines
                    and concepts that a gracious God has
                    given to us in this day and which must be understood in order to gain
                    eternal
                    life. They are:
                </big></p>
                <p style="font-family: arial;"><big><b>1. We worship the Father and him
                        only and no one else.</b>
                </big></p>
                <p style="font-family: arial;"><big>We do not worship the Son and we do
                    not worship the Holy Ghost. I know
                    perfectly well what the scriptures say about worshipping Christ and
                    Jehovah,
                    but they are speaking in an entirely different sense--the sense of
                    standing
                    in awe and being reverentially grateful to Him who has redeemed us.
                    Worship
                    in the true and saving sense is reserved for God the first, the
                    Creator.
                </big></p>
                <p style="font-family: arial;"><big>Our revelations say that the Father
                    "is infinite and eternal," that
                    he created mankind, "and gave unto them commandments that they should
                    love
                    and serve him, the only living and true God, and that he should be the
                    only being whom they should worship." (D&amp;C 20:17-19)
                </big></p>
                <p style="font-family: arial;"><big>Jesus said "True worshippers
                    shall"--note that this is mandatory--"shall
                    worship the Father in spirit and in truth; for the Father seeketh such
                    to worship him. For unto such hath God promised his spirit. And they
                    who
                    worship him in spirit and in truth." (JST, John 4:25-26) There is no
                    other
                    way, no other approved system of worship.
                </big></p>
                <p style="font-family: arial;"><big><b>2. We love and serve both the
                        Father and the Son.</b>
                </big></p>
                <p style="font-family: arial;"><big>In the full, final, and ultimate
                    sense of the word the define decree
                    is: "Thou shalt love the Lord thy God with all thy heart, with all thy
                    might, mind, and strength; and in the name of Jesus Christ thou shalt
                    serve
                    him." (D&amp;C 59:5) And Jesus also said, "If ye love me keep my
                    commandments."
                    (John 14:15)
                </big></p>
                <p style="font-family: arial;"><big>These, then, are the commandments
                    of commandments. They tie the Father
                    and the Son together, as one, so that both receive our love and
                    service.
                </big></p>
                <p style="font-family: arial;"><big><b>3. Christ himself loves, serves,
                        and worships the Father.</b>
                </big></p>
                <p style="font-family: arial;"><big>Though Christ is God, yet there is
                    a Deity above him, a Deity whom he
                    worships. That God is the Father. To Mary Magdalene, the first mortal
                    to
                    see a resurrected person, Jesus said, "I ascend unto my Father, and
                    your
                    Father; and to my God, and your God." (John 20:17)
                </big></p>
                <p style="font-family: arial;"><big>All of us, Christ included, are the
                    spirit children of the Father; all
                    of us, Christ included, seek to become like the Father. In this sense,
                    the Firstborn, our elder brother, goes forward as we do.
                </big></p>
                <p style="font-family: arial;"><big><b>4. The plan of salvation is the
                        gospel of the Father.</b>
                </big></p>
                <p style="font-family: arial;"><big>The plan of salvation originated
                    with the Father; he is the author and
                    finisher of our faith in the final sense; he ordained the laws by
                    obedience
                    to which both we and Christ become like him.
                </big></p>
                <p style="font-family: arial;"><big>The Father did <i>not</i> ask for
                    volunteers to propose a plan whereby
                    man might be saved. What he did was to ask whom he should send to be
                    the
                    redeemer in the plan he devised. Christ and Lucifer both volunteered
                    and
                    the Lord chose his Firstborn and rejected the amendatory offer of the
                    Son
                    of the Morning.
                </big></p>
                <p style="font-family: arial;"><big>Thus Paul speaks of "the gospel of
                    God,... concerning his Son Jesus
                    Christ, our Lord, which was made of the seed of David, according to the
                    flesh." (Rom 1:1-3) It is the Father's gospel, it became the gospel of
                    the Son by adoption, and we call it after Christ's name because his
                    atoning
                    sacrifice put all of it's terms and conditions into operation.
                </big></p>
                <p style="font-family: arial;"><big><b>5. Christ worked out his own
                        salvation by worshiping the Father.</b>
                </big></p>
                <p style="font-family: arial;"><big>After the Firstborn of the Father,
                    while yet a spirit being, had gained
                    power and intelligence that made him like unto God; after he had
                    become,
                    under the Father, the Creator of worlds without number; after he had
                    reigned
                    on the throne of eternal power as the Lord Omnipotent-- after all this
                    he yet had to gain a mortal and then an immortal body.
                </big></p>
                <p style="font-family: arial;"><big>After the Son of God "made flesh"
                    his "tabernacle," and while he yet
                    "dwelt among the sons of men"; after he left his preexistent glory as
                    we
                    all do at birth; after he was born of Mary in Bethlehem of Judea--after
                    all this he was called upon to work out his own salvation.
                </big></p>
                <p style="font-family: arial;"><big>Of our Lord's life while in this
                    mortal probation the scripture says,
                    "He received not of the fullness at the first, but received grace for
                    grace;
                    and he received not of the fullness at first, but continued from grace
                    to grace, until he received a fullness." Finally, after his
                    resurrection,
                    "he received a fullness of the glory of the Father; and he received all
                    power both in heaven and on earth, and the glory of the Father was with
                    him, for he dwelt in him." (D&amp;C 93:12-17)
                </big></p>
                <p style="font-family: arial;"><big>Note it please, the Lord Jesus
                    worked out his own salvation while in
                    this mortal probation by going from grace to grace, until having
                    overcome
                    the world and being raised in immortal glory, he became like the Father
                    in the full, complete, and eternal sense.
                </big></p>
                <p style="font-family: arial;"><big><b>6. All men must worship the
                        Father in the same way Christ did in
                        order to gain salvation.</b>
                </big></p>
                <p style="font-family: arial;"><big>Thus saith the Lord: "I give unto
                    you these sayings"-- those we have
                    just quoted which tell how Christ gained his salvation by worshipping
                    the
                    Father--"I give unto you these sayings," saith the Lord, "that you may
                    understand and know <i>how</i> to worship, and know <i>what</i> you
                    worship,
                    that you may come unto the Father in my name, and in due course receive
                    of his fullness."
                </big></p>
                <p style="font-family: arial;"><big>What a wondrous concept this is. We
                    too can become like the Father "For
                    if you keep my commandments," the Lord continues, "you shall receive of
                    his fullness, and be glorified in me as I am in the Father; therefore I
                    say unto you, you shall receive grace for grace." (D&amp;C 93:19-20)
                </big></p>
                <p style="font-family: arial;"><big><b>7. The Father sent the Son to
                        work out the infinite and eternal atonement.</b>
                </big></p>
                <p style="font-family: arial;"><big>As temporal and spiritual death
                    came by the fall of Adam, so immortality
                    and eternal life come by the atonement of Christ. Such was and is and
                    ever
                    shall be the plan of the Father. Adam was sent to earth to fall and
                    Christ
                    came to ransom men from the fall.
                </big></p>
                <p style="font-family: arial;"><big>Thus the Father sent this call
                    forth in the councils of eternity: 'Whom
                    shall I send to be my Son, to ransom men from temporal and spiritual
                    death,
                    to bring to pass the immortality and eternal life of man, to put into
                    full
                    operation all of the terms and conditions of my eternal plan of
                    redemption
                    and salvation?'
                </big></p>
                <p style="font-family: arial;"><big>Christ is the Redeemer of men and
                    the Savior of the world because his
                    Father sent him and gave him power to do the assigned work. He said he
                    had power to lay down his life and to take it again because he had been
                    so commanded by the Father. Lehi says he rose from the dead "by the
                    power
                    of the Spirit." (2 Ne. 2:8)
                </big></p>
                <p style="font-family: arial;"><big>The great and eternal redemption,
                    in all its phases, was wrought by
                    Christ using the power of the Father.
                </big></p>
                <p style="font-family: arial;"><big><b>8. The Son came to do the will
                        of the Father in all things.</b>
                </big></p>
                <p style="font-family: arial;"><big>Jesus said: "I came down from
                    heaven, not to do mine own will, but the
                    will of him that sent me." Also I came into the world to do the will of
                    my Father because my Father sent me. And my Father sent me that I might
                    be lifted up upon the cross." (3 Ne. 27:13-14)
                </big></p>
                <p style="font-family: arial;"><big>And Paul said of him: He "made
                    himself of no reputation, and took upon
                    him the form of a servant, and was made in the likeness of men: And
                    being
                    found in fashion as a man, he humbled himself, and became obedient unto
                    death, even the death of the cross." (Philip. 2:7-8)
                </big></p>
                <p style="font-family: arial;"><big>How better could his relationship
                    that he and his Father have be stated?
                </big></p>
                <p style="font-family: arial;"><big><b>9. God, through Christ, is
                        reconciling men to himself.</b>
                </big></p>
                <p style="font-family: arial;"><big>Fallen man is carnal, sensual, and
                    devilish by nature; he is spiritually
                    dead; he is out of harmony with the Father.
                </big></p>
                <p style="font-family: arial;"><big>Thus, as Paul says, "All things are
                    of God, who hath reconciled us to
                    himself by Jesus Christ, and hath given to us the ministry of
                    reconciliation;
                    To wit, that God was in Christ reconciling the world unto himself." We
                    have "the word of reconciliation." which is the gospel, and our
                    preaching
                    is: "Be ye reconciled to God," that is, to the Father. (2Cor. 5:18-20)
                </big></p>
                <p style="font-family: arial;"><big><b>10. Christ is the Mediator
                        between God and man</b>.
                </big></p>
                <p style="font-family: arial;"><big>Because all men must be reconciled
                    to God in order to be saved, he,
                    in his goodness and grace, has provided a Mediator for them.
                </big></p>
                <p style="font-family: arial;"><big>Paul tells us: "There is one God,
                    and one mediator between God and men,
                    the man Christ Jesus: Who gave himself a ransom for all." (1 Tim.
                    2:5-6)
                    To this we add: If there were no mediator we could never be reconciled
                    to the Father and hence there would be no salvation.
                </big></p>
                <p style="font-family: arial;"><big><b>11. Christ is our Intercessor
                        with the Father, our Advocate in the
                        courts above.</b>
                </big></p>
                <p style="font-family: arial;"><big>In the process of mediating between
                    us and our Maker, in the process
                    of reconciling sin-ridden men with a sin-free God, Christ makes
                    intercession
                    for all who repent. He advocates the cause of those who believe in him.
                    "Father," he pleads, "spare these my brethren that believe on my name,
                    that they may come unto me and have everlasting life." (D&amp;C 45:5)
                </big></p>
                <p style="font-family: arial;"><big><b>12. Our eternal fellowship is
                        with the Father and the Son</b>.
                </big></p>
                <p style="font-family: arial;"><big>John says: "Our fellowship is with
                    the Father, and with his Son Jesus
                    Christ." If we keep the commandments, "We have fellowship" with the
                    Father--which
                    is the object and end of our existence. And in the very nature of
                    things
                    we also have eternal fellowship with Christ, because he walked in the
                    light
                    and became and is one with the Father. (1 John 1:3-7)
                </big></p>
                <p style="font-family: arial;"><big><b>13. God was in Christ
                        manifesting himself to the world.</b>
                </big></p>
                <p style="font-family: arial;"><big>The Son, Paul tells us, is in "the
                    express image of his (Father's) person."
                    (Heb 1:3) "I and my Father are one," Jesus said. (John 10:30) Thus in
                    his
                    appearance, in his person, and in his attributes, the Son is the image
                    and likeness of the Father. "He that hath seen me hath seen the Father,
                    Jesus said, the four gospels are a treasure house of knowledge because
                    they set forth what the son is like, and he is like his Father.
                </big></p>
                <p style="font-family: arial;"><big><b>14. Christ is the revealer of
                        the Father.</b>
                </big></p>
                <p style="font-family: arial;"><big>God is and can be known only by
                    revelation; he stands revealed or he
                    remains forever unknown. Jesus said: "No man knoweth...who the Father
                    is,
                    but his Son, and he to whom the Son will reveal him." (Luke 10:22)
                </big></p>
                <p style="font-family: arial;"><big><b>15. Christ is the way to the
                        Father.</b>
                </big></p>
                <p style="font-family: arial;"><big>"I am the way," he said. "No man
                    cometh unto the Father, but by me."
                    (John 14:6)
                </big></p>
                <p style="font-family: arial;"><big>Who can doubt that Christ's mission
                    is to reveal the Father, to lead
                    men to the Father, to teach them how to worship the Father, to
                    reconcile
                    them to the Father.
                </big></p>
                <p style="font-family: arial;"><big><b>16. Christ proclaims the gospel
                        of the Father.</b>
                </big></p>
                <p style="font-family: arial;"><big>In the ultimate sense the world of
                    salvation comes from the Father.
                    "God, who at sundry times and in divers manners spake in time past unto
                    the fathers by the prophets," Paul says, "Hath in these last days
                    spoken
                    unto us by his Son." (Heb. 1:1)
                </big></p>
                <p style="font-family: arial;"><big>The Father sent the prophets; they
                    represented him; and they spoke his
                    word. When Jesus quoted the Old Testament prophets to the Nephites, he
                    attributed their words to the Father.
                </big></p>
                <p style="font-family: arial;"><big>Through the revelations came from
                    the Son, yet in the ultimate sense
                    the truths taught were those of the Father. We are also aware of many
                    instances
                    in which Jesus, acting by divine investiture of authority, speaks in
                    the
                    first person as though he were the Father.
                </big></p>
                <p style="font-family: arial;"><big>Thus Jesus said: "My doctrine is
                    not mine, but his that sent me. If
                    any man will do his will, he shall know of the doctrine, whether it be
                    of God, or whether I speak of myself." (John 7:16-17)
                </big></p>
                <p style="font-family: arial;"><big><b>17. Christ glorifies the Father
                        and so must we.</b>
                </big></p>
                <p style="font-family: arial;"><big>"Glorify thy Son." Jesus prayed to
                    the Father, "that thy Son also may
                    glorify thee...I have glorified thee on the earth: I have finished the
                    work which thou gavest me to do." (John 17:1,4)
                </big></p>
                <p style="font-family: arial;"><big>As Christ our Pattern, by obedience
                    and by doing his appointed labors,
                    glorified the Father, so must we. Acting in his Father's name, Jesus
                    ascribed
                    the honor and glory in all things to the Father. The very pattern of
                    prayer
                    that he gave us directs that we go and do likewise.
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big>Now, we might continue on and add
                    to this list, all of which would bear
                    the same witness and accord with what we have said. Let us instead,
                    based
                    on these present concept, discuss the problem at hand and draw some
                    conclusions.
                </big></p>
                <p style="font-family: arial;"><big><b>What is and should be our
                        relationship to the member of the Godhead?</b>
                </big></p>
                <p style="font-family: arial;"><big>First, be it remembered, that most
                    scriptures that speak of God or of
                    the Lord do not even bother to distinguish between the Father and the
                    Son,
                    simply because it doesn't make any difference which God is involved.
                    They
                    are one. The words or deeds of either of them would be the words and
                    deeds
                    of the other in the same circumstance.
                </big></p>
                <p style="font-family: arial;"><big>Further, if a revelation comes
                    from, or by the power of the Holy Ghost,
                    ordinarily the words will be those of the Son, though what the Son says
                    will be what the Father would say, and the words may thus be considered
                    as the Father's.
                </big></p>
                <p style="font-family: arial;"><big>And thus any feelings of love,
                    praise, awe, or worship that may fill
                    our hearts when we receive the divine word will be the same no matter
                    who
                    is thought or known to be the author of them.
                </big></p>
                <p style="font-family: arial;"><big>And yet we do have a proper
                    relationship to each member of the Godhead,
                    in part at least because they are separate and severable functions
                    which
                    each performs, and also because of what they as one Godhead have done
                    for
                    us.
                </big></p>
                <p style="font-family: arial;"><big>Our relationship with the Father is
                    supreme, paramount, and pre-eminent
                    over all others. He is the God we worship. It is his gospel that saves
                    and exalts. He ordained and established the plan of salvation. He is
                    the
                    one who was once as we are now. The life he lives is eternal life, and
                    if we are to gain this greatest of all the gifts of God, it will be
                    because
                    we become like him.
                </big></p>
                <p style="font-family: arial;"><big>Our relationship with the Father is
                    one of parent and child. He is the
                    one who gave us our agency. It was his plan that provided for a fall
                    and
                    an atonement. And it is to him that we must be reconciled if we are to
                    gain salvation. He is the one to whom we have direct access by prayer,
                    and if there were some need--which there is not!--to single out one
                    member
                    of the Godhead for a special relationship, the Father, not the Son,
                    would
                    be the one to choose.
                </big></p>
                <p style="font-family: arial;"><big>Our relationship with the Son is
                    one of brother or sister in the pre-mortal
                    life and one of being led to the Father by him while in this mortal
                    sphere.
                    He is the Lord Jehovah who championed our cause before the foundations
                    of the earth were laid. He is the God of Israel, the Promised Messiah,
                    and the Redeemer of the world.
                </big></p>
                <p style="font-family: arial;"><big>By faith we are adopted into his
                    family and become his children. We
                    take upon ourselves his name, keep his commandments, and rejoice in the
                    cleansing power of his blood. Salvation comes by him. From creations
                    dawn,
                    as long as eternity endures, there neither has been nor will be any act
                    of such transcendent power and import as his atoning sacrifice.
                </big></p>
                <p style="font-family: arial;"><big>We do not have a fraction of the
                    power we need to properly praise his
                    holy name and ascribe unto him the honor and power and might and glory
                    and dominion that is his. He is our Lord, our God, and our King.
                </big></p>
                <p style="font-family: arial;"><big>Our relationship with the Holy
                    Spirit is quite another thing. This holy
                    personage is a revelator and a sanctifier. He bears record of the
                    Father
                    and the Son. He dispenses spiritual gifts to the faithful. Those of us
                    who have received the gift of the Holy Ghost have the right to his
                    constant
                    companionship.
                </big></p>
                <p style="font-family: arial;"><big>And again, if it were proper--and I
                    repeat, it is not!--to single out
                    one member of the Godhead for some special attention, we might well
                    conclude
                    that member should be the Holy Ghost. We might well adopt as a slogan:
                    Seek the Spirit. The reason of course is that the sanctifying power of
                    the Spirit would assure us of reconciliation with the Father. And any
                    person
                    who enjoys the constant companionship of the Holy Spirit will be in
                    complete
                    harmony with the divine will in all things.
                </big></p>
                <p style="font-family: arial;"><big><b>Heresies</b>
                </big></p>
                <p style="font-family: arial;"><big>Now, in spite of all these truths,
                    which ought to be obvious to every
                    spiritually enlightened person, heresies rear their ugly heads among us
                    from time to time.
                </big></p>
                <p style="font-family: arial;"><big>There are those deluded cultists,
                    and others who, unless they repent,
                    are on the road to becoming cultists, who choose to believe we should
                    worship
                    Adam. These have or should find their way out of the Church.
                </big></p>
                <p style="font-family: arial;"><big>There are others--in the main they
                    are intellectuals without strong
                    testimonies--who postulate that God does not know all things but is
                    progressing
                    in truth and knowledge and will do so everlastingly. These, unless they
                    repent, will live and die weak in the faith and will fall short of
                    inheriting
                    what might have been theirs in eternity.
                </big></p>
                <p style="font-family: arial;"><big>There are yet others who have an
                    excessive zeal which causes them to
                    go beyond the mark. Their desire for excellence is inordinate. In an
                    effort
                    to be truer than true they devote themselves to gaining a special,
                    personal
                    relationship with Christ that is both improper and perilous.
                </big></p>
                <p style="font-family: arial;"><big>I say perilous because this course,
                    particularly in the lives of some
                    who are spiritually immature, is a gospel hobby which creates an
                    unwholesome
                    holier-than-thou attitude. In other instances it leads to despondency
                    because
                    the seeker after perfection knows he is not living the way he supposed
                    he should.
                </big></p>
                <p style="font-family: arial;"><big>Another peril is that those so
                    involved often begin to pray directly
                    to Christ because of some special friendship they feel has been
                    developed.
                    In this connection a current and unwise book, which advocates gaining a
                    special relationship with Jesus, contains this sentence--quote:
                    "Because
                    the Savior is our mediator, our prayers go through Christ to the
                    Father,
                    and the Father answers our prayers through his Son." Unquote. This is
                    plain,
                    sectarian non-sense. Our prayers are addressed to the Father and to him
                    only. They do not go through Christ or the Blessed Virgin or St.
                    Genevieve
                    or along the beads of a rosary. We are entitled to come boldly unto the
                    throne of grace, that we may obtain mercy and find grace to help in
                    time
                    of need.
                </big></p>
                <p style="font-family: arial;"><big>And I rather suppose that He who
                    sitteth upon the throne will chose
                    his own ways to answer his children, and that they are numerous.
                    Perfect
                    prayer is addressed to the Father, in the name of the Son; it is
                    uttered
                    by the power of the Holy Ghost; and it is answered in whatever way
                    seems
                    proper by Him whose ear is attuned to the needs of his children.
                </big></p>
                <p style="font-family: arial;"><big>Now I know that some may be
                    offended at the counsel that they should
                    not strive for a special and personal relationship with Christ. It will
                    seem to them as though I am speaking out against mother love, or
                    Americanism,
                    or the little red school house. But I am not. There is a fine line here
                    over which true worshippers will not step.
                </big></p>
                <p style="font-family: arial;"><big>It is true that there may, with
                    propriety, be a special relationship
                    with a wife, with children, with friends, with teachers, with the
                    beasts
                    of the field and the fowls of the sky and the lilies of the valley. But
                    the very moment anyone singles out one member of the Godhead as the
                    almost
                    sole recipient of his devotion, to the exclusion of the others, that is
                    the moment when spiritual instability begins to replace sense and
                    reason.
                </big></p>
                <p style="font-family: arial;"><big><b>Stay in the Mainstream of the
                        Church</b>
                </big></p>
                <p style="font-family: arial;"><big>The proper course for all of us is
                    to stay in the mainstream of the
                    Church. This is the Lord's Church, and it is led by the spirit of
                    inspiration,
                    and the practice of the Church constitutes the interpretation of the
                    scripture.
                </big></p>
                <p style="font-family: arial;"><big>And you have never heard one of the
                    First Presidency or the Twelve,
                    who hold the keys of the kingdom, and who are appointed to see that we
                    are not "tossed to and fro, and carried about with every wind of
                    doctrine"
                    (Eph 4:14), you have never heard one of them advocate this excessive
                    zeal
                    that calls for gaining a so-called special and personal relationship
                    with
                    the Lord.
                </big></p>
                <p style="font-family: arial;"><big>You have heard them teach and
                    testify of the ministry and mission of
                    the Lord Jesus, using the most persuasive and powerful language at
                    their
                    command. But never, never at any time have they taught or endorsed the
                    inordinate and intemperate zeal that encourages endless, sometimes
                    day-long
                    prayers, in order to gain a personal relationship with the Savior.
                </big></p>
                <p style="font-family: arial;"><big>Those who truly love the Lord and
                    who worship the Father in the name
                    of the Son by the power of the Spirit, according to the approved
                    patterns,
                    maintain a reverential barrier between themselves and all the members
                    of
                    the Godhead.
                </big></p>
                <p style="font-family: arial;"><big>I am well aware that some who have
                    prayed for endless hours feel they
                    have a special and personal relationship with Christ that they never
                    had
                    before. I wonder if this is any or much different, however, from the
                    feelings
                    of fanatical sectarians who with glassy eyes and fiery tongues assure
                    us
                    they have been saved by grace and are assured of a place with the Lord
                    in a heavenly abode, when in fact they have never even received the
                    fullness
                    of the gospel.
                </big></p>
                <p style="font-family: arial;"><big>I wonder if it is not part of
                    Lucifer's system to make people feel they
                    are special friends of Jesus when in fact they are not following the
                    normal
                    and usual pattern of worship found in the true Church.
                </big></p>
                <p style="font-family: arial;"><big>Let me remind you to stay in the
                    course charted by the Church. It is
                    the Lord's Church, and he will not permit it to be led astray. If we
                    take
                    the counsel that comes from the prophets and seers we will pursue the
                    course
                    that is pleasing to the Lord.
                </big></p>
                <p style="font-family: arial;"><big>Would it be amiss if I reminded you
                    that Jesus maintained a reserve
                    between him and his disciples and that he did not allow them the same
                    intimacy
                    with him that they had with each other. This was particularly true
                    after
                    his resurrection.
                </big></p>
                <p style="font-family: arial;"><big>For instance, when Mary Magdalene,
                    in a great outpouring of love and
                    devotion, sought to embrace the Risen Lord, her hands were stayed.
                    "Touch
                    me not," he said. Between her and him, no matter the degree of their
                    love,
                    there was a line over which she could not pass.
                </big></p>
                <p style="font-family: arial;"><big>And yet, almost immediately
                    thereafter, a whole group of faithful women
                    held that same Lord by the feet, and we cannot doubt bathed his wounded
                    feet with their tears.
                </big></p>
                <p style="font-family: arial;"><big>It is a fine and sacred line, but
                    clearly there is a difference between
                    a personal and intimate relationship with the Lord, which is improper,
                    and one of worshipful adoration, which yet maintains the required
                    reserve
                    between us and Him who has bought us with his blood.
                </big></p>
                <p style="font-family: arial;"><big>Now I sincerely hope that no one
                    will imagine that I have in the slightest
                    degree downgraded the Lord Jesus in the scheme of things. I have not
                    done
                    so. As far as I know there is not a man on earth who thinks more highly
                    of him than I do. It just may be that I have preached more sermons,
                    taught
                    more doctrine, and written more words about the Lord Jesus Christ than
                    any man now living. I have ten large volumes in print, seven of which
                    deal
                    almost entirely with Christ, and the other three with him and his
                    doctrines.
                </big></p>
                <p style="font-family: arial;"><big>I do not suppose that what I have
                    here said will be an end to controversy
                    or to the spread of false views and doctrines. The devil is not dead
                    and
                    he delights in controversy. But you have been warned, and you have
                    heard
                    the true doctrine taught. Those who need to study the matter further
                    would
                    do well to get and study a copy of what I have said when it is
                    published
                    by the Brigham Young University.
                </big></p>
                <p style="font-family: arial;"><big>Let us then end on the note of
                    testimony and of prayer. I bear record
                    of the divine Sonship of Him of whom we have this day spoken. He is or
                    should be our best friend through whom we can be reconciled to God.
                </big></p>
                <p style="font-family: arial;"><big>And I pray that the true doctrine
                    of the Father and of the Son and of
                    the Holy Ghost, which as the Book of Mormon says is one God, may be
                    found
                    in the hearts and souls of all of us.
                </big></p>
                <p><big><span style="font-family: arial;">In the name of the Lord Jesus
                        Christ, Amen.
                    </span></big><br style="font-family: arial;">
