                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>The
                                        Lord said, "Be still, and know that I am God" (Psalm 46:10). The
                                        process
                                        of knowing is personal revelation.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>I
                                        base what I will say about personal revelation on two gospel
                                        principles.
                                        The first principle is this: We are dual beings, a spirit son or
                                        daughter
                                        of God, alive and intelligent in the first estate, confined now to a
                                        body
                                        of flesh and bone. "The spirit and the body are the soul of man"
                                        (D&amp;C
                                        88:15). The spirit is eternal; the body will become so. There are
                                        languages
                                        we can speak and hear with the body. There are languages of the spirit,
                                        one being revelation.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>The
                                        second principle: There are two sources of revelation-the one
                                        consistent
                                        with the great plan of happiness, the other growing from "the cunning
                                        plan
                                        of the evil one" (2 Nephi 9:28). So that we will not be deceived, there
                                        is order in the Church and there are established channels through which
                                        revelation is given to the Church and to us, its members, as
                                        individuals.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>It
                                        should not be as difficult as it is to teach the reality of the spirit
                                        to some adults, especially those who "when they are learned they think
                                        they are wise" (2 Nephi 9:28). But the Lord taught that "except ye &#8230;
                                        become
                                        as little children, ye shall not enter into the kingdom of heaven"
                                        (Matthew
                                        18:3; see also Luke 18:17; 3 Nephi 11:37-38).</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>Perhaps
                                        that is because little children learn quickly things that adults are
                                        slow
                                        to comprehend. A friend told me that his young son knew all about
                                        computers,
                                        and said, "I have no idea where he gets it." I said, "Probably from his
                                        younger brother or sister."</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>The
                                        computer is a good illustration of our dual nature. Computers are made
                                        of metal, plastic, and glass, and can hold an astonishing amount of
                                        information-all
                                        the standard works, sets of encyclopedias, dictionaries, whole
                                        libraries,
                                        even illustrations. Press the keys, and you can select any part of what
                                        is stored and see it instantly on a screen. You can rearrange, add to,
                                        or subtract from what is stored in the computer, and can print on paper
                                        whatever you desire, even in full color. You then can hold in your hand
                                        tangible, absolute proof of what is inside that box of metal and glass
                                        and plastic.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>However,
                                        should you take the computer completely apart or melt it down, you
                                        could
                                        find not one word, not one illustration-no tangible evidence that there
                                        were volumes, verses, or illustrations inside the computer. You could
                                        no
                                        more find words in the ashes of a computer than you could find the
                                        spirit
                                        in the ashes of a cremated human body.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>Notwithstanding
                                        that the spirit is invisible and intangible, it is the very essence of
                                        reality. "Ye are the temple of God, and &#8230; the Spirit of God dwelleth in
                                        you" (1 Corinthians 3:16).</big></big></big></font>
                        </big></big></big></p>
