                                        can imagine some young persons reading this and thinking: "Too late!
                                        I've
                                        completely ruined my life already." I understand! You may think that a
                                        dozen times or more in the next few years. But it is not so. In fact,
                                        whatever
                                        your age, if you follow the right plan, you will be all right. Come
                                        what
                                        may, wherever you go, whatever happens to the world, whatever trials
                                        you
                                        face, you need not fear the future. "If ye are prepared ye shall not
                                        fear"
                                        (D&amp;C 38:30). I assure you, you need not fear the future. You will
                                        be
                                        happy-if, that is, you follow "the great plan of happiness."</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>The
                                        plan of happiness comes with many choices and options to suit
                                        individual
                                        personalities, abilities, desires, and needs.</big></big></big></font>
                            <br>
                            <font size="-1"><big><big><big>The
                                        plan is found in the scriptures, and spoken of as:</big></big></big></font>
                            <br>
                            <font size="-1"><big><big><big>--The
                                        Merciful Plan of the Great Creator (2 Nephi 9:6; Alma 42:15, 31).</big></big></big></font>
                            <br>
                            <font size="-1"><big><big><big>--The
                                        Great Plan of Redemption (Alma 12:25, 26, 30, 32; 17:16; 18:39;
                                        22:13-14;
                                        29:2; 34:16, 31; 39:18; 42:11; Jacob 6:8).</big></big></big></font>
                            <br>
                            <font size="-1"><big><big><big>--The
                                        Plan of Salvation (Jarom 1:2; Alma 24:14; 42:5; Moses 6:62).</big></big></big></font>
                            <br>
                            <font size="-1"><big><big><big>--The
                                        Great Plan of the Eternal God (2 Nephi 9:13; Alma 34:9; Abraham 4:21).</big></big></big></font>
                            <br>
                            <font size="-1"><big><big><big>--The
                                        Eternal Plan of Deliverance (2 Nephi 11:5).</big></big></big></font>
                            <br>
                            <font size="-1"><big><big><big>--The
                                        Plan of Restoration (Alma 41:2).</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>And
                                        the title I favor:</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>--The&nbsp;</big></big></big></font><font
                                size="-1"><big><big><big><i>Great
                                        </i>Plan
                                        of Happiness (Alma 42:8, 16).</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>Once
                                        a person has even the outline of the plan, he knows which way to go.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>Following
                                        are the essential components of the great plan of happiness, of
                                        redemption,
                                        of salvation.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><b><font size="-1"><big><big><big>Premortal
                                            Life</big></big></big></font></b>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>First,
                                        our premortal existence. I don't know of any idea that helps us make
                                        sense
                                        out of life as much as this fundamental truth: We lived as spirit
                                        children-individual,
                                        intelligent sons and daughters of God-before our mortal birth. So many
                                        things in life can be understood only if we know there was a premortal
                                        life, and so many things can never be understood without that knowledge.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>Why
                                        is it that two children born of the same earthly parents, so alike in
                                        physical
                                        appearance, frequently are so different in personality, temperament,
                                        and
                                        disposition-which seem inborn with them? Further, did you ever ponder
                                        on
                                        the expression "all men are created equal" (which incidentally is not a
                                        scriptural statement) and wonder, if this is true, why there is so much
                                        inequity and seeming unfairness in life? Why the unequal distribution
                                        of
                                        talents and gifts, of advantages and disadvantages?</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>I
                                        don't know how to answer such questions as these without the doctrine
                                        of
                                        a premortal life.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>This
                                        great plan of happiness was first presented in a grand council in our
                                        premortal
                                        life. Each of us was there, a son or a daughter of God. Each had a
                                        choice
                                        to accept or to reject it. There was a rebellion, and a war erupted
                                        over
                                        the plan. Our being here with a mortal body tells us we were on the
                                        right
                                        side.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>The
                                        leader of the rebellion formulated a plan of his own. It is described
                                        in
                                        the revelations as the "cunning plan of the evil one" or the "very
                                        subtle
                                        plan of the adversary" (2 Nephi 9:28; Alma 12:4-5) or the "great plan
                                        of
                                        destruction" (3 Nephi 1:16; see also Helaman 2:8; D&amp;C 10:12).</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>Each
                                        of us chose the great plan of happiness. We know this because otherwise
                                        we would not be here in mortality. So far, so good.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>But
                                        in mortal life, with both good and evil present, we must decide again.
                                        We must undergo the test. We have been given our agency and cannot
                                        escape
                                        confirming or rejecting the decision we made in the first estate. It is
                                        "choose you&nbsp;</big></big></big></font><font size="-1"><big><big><big><i>this
                                        </i>day
                                        whom ye will serve" (Joshua 24:15; Alma 30:8; see also D&amp;C 127:2;
                                        Moses
                                        6:33).</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>Following
                                        are the other components of the plan of happiness, in outline form.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>After
                                        the spirit creation, the physical creation of the world and all living
                                        things took place, with a separate creation of physical bodies for the
                                        sons and daughters of God.</big></big></big></font>
                            <br>
                            &nbsp;
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><b><font size="-1"><big><big><big>The
                                            Fall of Adam and Eve</big></big></big></font></b>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>The
                                        transfer from the first estate in heaven to the second estate in
                                        mortality
                                        is in effect the fall of man. Even though we may not fully understand
                                        it
                                        now, we need to accept the Fall as truth or we will not understand the
                                        ministry of Christ as our Redeemer, the Atonement, the ordinances and
                                        covenants,
                                        or the purposes of life itself.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>The
                                        Fall came by transgression of a law, but there was no sin connected
                                        with
                                        it. There is a difference between transgression and sin. Both always
                                        bring
                                        consequences. While it may not be a sin to step off a roof, in doing so
                                        one becomes subject to the law of gravity, and consequences will follow.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>The
                                        fall of man was made from the presence of God to this mortal life,
                                        where,
                                        now in the presence of both good and evil, we face the test.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>The
                                        plan provides that we experience all that is good in life. It is
                                        intended
                                        that we be happy.</big></big></big></font>
                            <br>
                            &nbsp;
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><b><font size="-1"><big><big><big>The
                                            Atoning Savior</big></big></big></font></b>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>Essential
                                        to the plan is the Redeemer, who compensated for the transgression
                                        which
                                        brought about the Fall and guaranteed that, regardless of who we are or
                                        what we do, we will rise from the dead in the resurrection. However, we
                                        will not return to the presence of God unless we are clean.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>The
                                        plan presupposes mistakes. Under the plan, penalties connected with bad
                                        choices, our sins, may be cancelled on condition that we keep the
                                        commandments
                                        which activate the influence of the Atonement.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>We
                                        are commanded to&nbsp;</big></big></big></font><font size="-1"><big><big><big><i>do
                                        </i>some
                                        things, and we are commanded&nbsp;</big></big></big></font><font
                                size="-1"><big><big><big><i>not
                                        </i>to
                                        do others in order to merit the redeeming power of that sacrifice, the
                                        atonement of Christ. The choice is ours. Alma said, "God gave unto them
                                        commandments, after having made known unto them the plan of redemption"
                                        (Alma 12:32).</big></big></big></font>
                            <br>
                            &nbsp;
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><b><font size="-1"><big><big><big>A
                                            Plan and a Creator</big></big></big></font></b>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>The
                                        plan is not based on chance, nor on accident. It is based on purpose,
                                        on
                                        agency, on choice. It accords with laws which were in force long before
                                        the plan was ever laid down. All of it has order; all of it was planned
                                        for us.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>The
                                        beauty and precision of the universe, the endless variety of plant and
                                        animal life, all testify of a plan, and of a Creator.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>--The
                                        bats, who invented radar.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>--The
                                        beaver with his contracting license.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>--The
                                        hummingbird, a tenth of an ounce, navigating with more proficiency than
                                        could Magellan or Columbus, or any who have followed them.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>--The
                                        swallows, who keep a calendar.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>--The
                                        monarch butterfly, who summers in Canada but owns shares in a
                                        condominium
                                        in Mexico.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>--The
                                        trapdoor spider, who invented camouflage.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>--The
                                        bees, who pioneered the year's supply then taught it to the squirrels.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>--The
                                        birds, who designed hollow bones so that they could fly.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>--The
                                        duck, choosing a broad, flat breast so that Archimedes wouldn't be
                                        ridiculed
                                        when he expounded his principles of flotation.&nbsp;</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>--Water,
                                        volunteering to&nbsp;</big></big></big></font><font size="-1"><big><big><big><i>expand
                                        </i>when
                                        it gets cold (rather than to&nbsp;</big></big></big></font><font
