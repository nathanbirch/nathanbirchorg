                                        is the process of communication to the spiritual eyes and to the
                                        spiritual
                                        ears that were ours before our mortal birth. The scriptures speak of
                                        "the
                                        eyes of our understanding" (see Jeremiah 5:21; Ephesians 1:18; 2 Nephi
                                        21:3; D&amp;C 110:1; 138:11, 29), and of "blindness of mind" (Ether
                                        4:15)
                                        and of heart (Deuteronomy 28:28; Ephesians 4:18; D&amp;C 58:15). They
                                        speak
                                        of "feeling" words, rather than hearing them (1 Nephi 17:45), and of
                                        the
                                        still, small voice (1 Kings 19:12; 1 Nephi 17:45).</big></big></big></font>
