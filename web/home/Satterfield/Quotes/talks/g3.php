                                        scriptures tell us "it must needs be that there is an opposition in all
                                        things" (2 Nephi 2:11). Therefore, we will be confronted with
                                        philosophies
                                        of men which conflict with the plan of redemption. We must be sure we
                                        don't
                                        "buy into" them. Any philosophy that claims to exempt us from moral or
                                        spiritual accountability will bring trouble.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>Do
                                        not be ashamed if, especially in your youth, you lack the knowledge,
                                        the
                                        experience, or even the courage to challenge those theories and
                                        philosophies
                                        or to confront those who teach them. Sometimes wisdom or common sense
                                        may
                                        tell you not even to try.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>We
                                        may safely study and learn about the theories and philosophies of men,
                                        but if they contradict the plan of redemption, the great plan of
                                        happiness,
                                        do not "buy into" them as truth. If you do, you may be putting a
                                        mortgage
                                        on your testimony, on your knowledge of premortal life, on the creation
                                        of man, on the Fall and the Atonement, on your Redeemer, the
                                        Resurrection,
                                        and exaltation; for "every plant, which my heavenly Father hath not
                                        planted,
                                        shall be rooted up" (Matthew 15:13).</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>If
                                        you "buy into" the philosophies of men, you may have your testimony
                                        repossessed.
                                        Your respect for moral law may go with it, and you will end up with
                                        nothing.
                                        "Everything that is in the world, whether it be ordained of men, by
                                        thrones,
                                        or principalities, or powers, or things of name, whatsoever they may
                                        be,
                                        that are not by me or by my word, saith the Lord, shall be thrown down,
                                        and shall not remain after men are dead" (D&amp;C 132:13).</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>The
                                        prophet Jacob urged us not to spend our labor for "that which is of no
                                        worth &#8230; which cannot satisfy" (2 Nephi 9:51).</big></big></big></font>
                            <br>
                            &nbsp;
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><b><font size="-1"><big><big><big>Testimony
                                            Comes from Personal Revelation</big></big></big></font></b>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>Now,
                                        although a testimony of this plan is of crucial importance to us, we
                                        must
                                        not count on winning many debates on the plan of redemption versus the
                                        prevailing theories and philosophies of men. I learned a long time ago
                                        that spiritual knowledge is described in a different language than is
                                        secular
                                        knowledge.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>On
                                        this I had a valuable experience before I was a General Authority. It
                                        affected
                                        me profoundly. I sat on a plane next to a professed atheist who
                                        ridiculed
                                        my belief in God. I bore my testimony to him: "There is a God. I&nbsp;</big></big></big></font><font
                                size="-1"><big><big><big><i>know
                                        </i>He
                                        lives!"</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>He
                                        said: "You don't&nbsp;</big></big></big></font><font size="-1"><big><big><big><i>know.
                                        </i>Nobody&nbsp;</big></big></big></font><font size="-1"><big><big><big><i>knows
                                        </i>that.
                                        You can't&nbsp;</big></big></big></font><font size="-1"><big><big><big><i>know
                                        </i>it."
                                        When I would not yield, the atheist posed perhaps the ultimate
                                        challenge
                                        to testimony. "All right," he said in a sneering, condescending way,
                                        "you
                                        say you know." Then, "Tell me&nbsp;</big></big></big></font><font
                                size="-1"><big><big><big><i>how
                                        </i>you
                                        know."</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>I
                                        could not do it. I was helpless to communicate. When I used the
                                        words&nbsp;</big></big></big></font><font size="-1"><big><big><big><i>spirit
                                        </i>and&nbsp;</big></big></big></font><font size="-1"><big><big><big><i>witness,
                                        </i>the
                                        atheist responded, "I don't know what you are talking about." The
                                        words&nbsp;</big></big></big></font><font size="-1"><big><big><big><i>prayer,
                                            discernment, </i>and&nbsp;</big></big></big></font><font size="-1"><big><big><big><i>faith
                                        </i>also
                                        were meaningless to him.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>"You
                                        see," he said, "you don't really know. If you did, you would be able to
                                        tell me&nbsp;</big></big></big></font><font size="-1"><big><big><big><i>how
                                            you know.</i>"</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>Perhaps,
                                        I thought, I had borne my testimony to him unwisely, and I was at a
                                        loss
                                        as to what to do. Then came the experience. A thought, a revelation,
                                        came
                                        into my mind, and I said to the atheist: "Let me ask you a question. Do
                                        you know what salt tastes like?"</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>"Of
                                        course I do," was his reply.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>"When
                                        did you taste salt last?"</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>"I
                                        just had dinner on the plane."</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>"You
                                        just think you know what salt tastes like," I said.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>He
                                        insisted, "I know what salt tastes like as well as I know anything."</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>"If
                                        I gave you a cup of salt and a cup of sugar, could you tell the salt
                                        from
                                        the sugar if I let you taste them both?"</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>"Now
                                        you are getting juvenile," he said. "Of course I could tell the
                                        difference.
                                        I know what salt tastes like. I know it as well as I know anything."</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>"Then,"
                                        I said, "assuming that I have never tasted salt, explain to me just
                                        what
                                        it tastes like."</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>After
                                        some thought, he ventured, "Well-I-uh, it is not sweet, and it is not
                                        sour."</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>"You've
                                        told me what it isn't, not what it is."</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>After
                                        several attempts, of course he could not do it. He could not convey, in
                                        words alone, so ordinary an experience as tasting salt.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>I
                                        bore testimony to him once again and said: "I know there is a God. You
                                        ridiculed that testimony and said that if I&nbsp;</big></big></big></font><font
                                size="-1"><big><big><big><i>did
                                        </i>know,
                                        I would be able to tell you exactly&nbsp;</big></big></big></font><font
                                size="-1"><big><big><big><i>how
                                        </i>I
                                        know. My friend, spiritually speaking, I have tasted salt. I am no more
                                        able to convey to you in words alone how this knowledge has come than
                                        you
                                        are able to tell me what salt tastes like. But I say to you again,
                                        there
                                        is a God! He lives! And just because you don't know, don't try to tell
                                        me that I don't know, for I do!"</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>As
                                        we parted, I heard him mutter: "I don't need your religion for a
                                        crutch.
                                        I don't need it."</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>That
                                        to me was a great lesson on personal revelation. From it I learned
                                        about
                                        prompting and the truth of the scripture which says, "Treasure up in
                                        your
                                        minds continually the words of life, and it shall be given you in the
                                        very
                                        hour that portion that shall be meted unto every man" (D&amp;C 84:85).</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>Since
                                        then I have never been embarrassed or ashamed that I could not explain
                                        in words alone everything I know spiritually, or tell just how I
                                        received
                                        it. From such experiences we will surely suffer some humiliation, but
                                        that
                                        is good for our faith. And we have an ever-present guide. We will be
                                        tested,
                                        but we will never be left without help.</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>Someone
                                        wrote:</big></big></big></font>
                        </big></big></big></p>
                <p style="font-family: arial;"><big><big><big><font size="-1"><big><big><big>With
                                        thoughtless and impatient hands</big></big></big></font>
