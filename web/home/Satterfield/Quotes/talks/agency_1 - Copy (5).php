                        <big><span style="font-family: arial;">&nbsp;&nbsp; <br>
                            </span></big>
                        <div style="margin-left: 40px;"><big><span style="font-family: arial;">&nbsp;Where
                                    and when did all of this happen? Well, it happened long before man&#8217;s
                                    mortal birth. It happened in a great premortal existence where we
                                    developed our identities and increased our spiritual capabilities by
                                    exercising our agency and making important choices. We developed our
                                    intelligence and learned to love the truth, and we prepared to come to
                                    earth to continue our progress.</span></big><br
