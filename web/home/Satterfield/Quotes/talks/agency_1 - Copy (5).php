                                    would be able to predict or choose a particular outcome by a given
                                    action. Our existence and the creation around us are convincing
                                    evidence that God, the Creator, exists and that our mortal world
                                    consists of &#8220;both things to act and things to be acted upon&#8221; (2 Nephi
                                    2:14)&#8211;or, in other words, choices.</span></big><br
                                style="font-family: arial;">
