                                    things must needs be a compound in one; &#8230; no life neither death, nor
                                    corruption nor incorruption, happiness nor misery, neither sense nor
                                    insensibility&#8221; (2 Nephi 2:11).</span></big><br
                                style="font-family: arial;">
