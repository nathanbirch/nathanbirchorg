                                    ordained thee a prophet unto the nations.&#8221; (Jer. 1:5.) At another time
                                    God reminded Job that &#8220;all the sons of God shouted for joy&#8221; (Job 38:7)
                                    before there was yet any man or woman on the earth God was creating.
                                    The Apostle Paul taught that God the Father chose us &#8220;before the
                                    foundation of the world.&#8221; (Eph. 1:4.)</span></big><br
                                style="font-family: arial;">
