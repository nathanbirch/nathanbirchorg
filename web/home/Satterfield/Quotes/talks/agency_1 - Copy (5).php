                                    He further explained that for these opposites or alternatives to exist,
                                    there must be law. Law provides us the options. It is by the operation
                                    of laws that things happen. By using or obeying a law, one can bring
                                    about a particular result&#8211;and by disobedience, the opposite result.
                                    Without law there could be no God, for He would be powerless to cause
