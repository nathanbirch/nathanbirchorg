                        <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            </span></big>
                        <div style="margin-left: 40px;"><big><span style="font-family: arial;">Our
                                    Father in Heaven wanted our growth to continue in mortality and to be
                                    enhanced by our freedom to choose and learn. He also wanted us to
                                    exercise our faith and our will, especially with a new physical body to
                                    master and control. But we know from both ancient and modern revelation
                                    that Satan wished to deny us our independence and agency in that
                                    now-forgotten moment long ago, even as he wishes to deny them this very
                                    hour. Indeed, Satan violently opposed the freedom of choice offered by
                                    the Father, so violently that John in the Revelation described &#8220;war in
                                    heaven&#8221; (Rev. 12:7) over the matter. Satan would have coerced us, and
                                    he would have robbed us of that most precious of gifts if he could: our
                                    freedom to choose a divine future and the exaltation we all hope to
                                    obtain.</span></big><br style="font-family: arial;">
