                        <big><br style="font-family: arial;">
                        </big>
                        <div style="text-align: center;"><big style="color: rgb(0, 0, 153);"><big><span
                                        style="font-family: arial;">Agency Is Not the Ultimate Goal &#8211; </span><span
                                        style="font-family: arial;">It Is a Means to an End!</span></big></big><br
                                style="font-family: arial;">
                        </div>
                        <big><br style="font-family: arial;">
                            <span style="font-family: arial;"><span style="font-weight: bold;">Dallin
                                    H. Oaks</span> <span style="color: rgb(153, 153, 153);">- Quorum of
                                    the Twelve Apostles</span></span><br style="font-family: arial;">
                        </big>
                        <div style="margin-left: 40px;"><big><span style="font-family: arial;">&nbsp;
                                    My next example in this message on weightier matters is the role of
                                    choice, or agency.</span></big><br style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp; <br>
                                    &nbsp;Few concepts have more potential to mislead us than the idea that
                                    choice, or agency, is an ultimate goal. For Latter-day Saints, this
                                    potential confusion is partly a product of the fact that moral
                                    agency&#8212;the right to choose&#8212;is a fundamental condition of mortal life.
                                    Without this precious gift of God, the purpose of mortal life could not
                                    be realized. To secure our agency in mortality we fought a mighty
                                    contest the book of Revelation calls a &#8220;war in heaven.&#8221; This premortal
                                    contest ended with the devil and his angels being cast out of heaven
                                    and being denied the opportunity of having a body in mortal life (see
                                    Rev. 12:7-9).</span></big><br style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp; <br>
                                    &nbsp;But our war to secure agency was won. The test in this postwar
                                    mortal estate is not to secure choice but to use it&#8212;to choose good
                                    instead of evil so that we can achieve our eternal goals. In mortality,
                                    choice is a method, not a goal.</span></big><br
                                style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                                    Of course, mortals must still resolve many questions concerning what
                                    restrictions or consequences should be placed upon choices. But those
                                    questions come under the heading of freedom, not agency. Many do not
                                    understand that important fact. We are responsible to use our agency in
                                    a world of choices. It will not do to pretend that our agency has been
                                    taken away when we are not free to exercise it without unwelcome
                                    consequences.</span></big><br style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                                    Because choice is a method, choices can be exercised either way on any
                                    matter, and our choices can serve any goal. Therefore, those who
                                    consider freedom of choice as a goal can easily slip into the position
                                    of trying to justify any choice that is made. &#8220;Choice&#8221; can even become
                                    a slogan to justify one particular choice. For example, today one who
                                    says &#8220;I am pro-choice&#8221; is clearly understood as opposing any legal
                                    restrictions upon a woman&#8217;&#8216;s choice to abort a fetus.</span></big><br
                                style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                                    More than 30 years ago, as a young law professor, I published one of
                                    the earliest articles on the legal consequences of abortion. Since that
                                    time I have been a knowledgeable observer of the national debate and
                                    the unfortunate Supreme Court decisions on the so-called &#8220;right to
                                    abortion.&#8221; I have been fascinated with how cleverly those who sought
                                    and now defend legalized abortion on demand have moved the issue away
                                    from a debate on the moral, ethical, and medical pros and cons of legal
                                    restrictions on abortion and focused the debate on the slogan or issue
                                    of choice. The slogan or sound bite &#8220;pro-choice&#8221; has had an almost
                                    magical effect in justifying abortion and in neutralizing opposition to
                                    it.</span></big><br style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                                    Pro-choice slogans have been particularly seductive to Latter-day
                                    Saints because we know that moral agency, which can be described as the
                                    power of choice, is a fundamental necessity in the gospel plan. All
                                    Latter-day Saints are pro-choice according to that theological
                                    definition. But being pro-choice on the need for moral agency does not
                                    end the matter for us. Choice is a method, not the ultimate goal. We
                                    are accountable for our choices, and only righteous choices will move
                                    us toward our eternal goals.</span></big><br style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                                    In this effort, Latter-day Saints follow the teachings of the prophets.
                                    On this subject our prophetic guidance is clear. The Lord commanded,
                                    &#8220;Thou shalt not &#8230; kill, nor do anything like unto it&#8221; (D&amp;C 59:6).
                                    The Church opposes elective abortion for personal or social
                                    convenience. Our members are taught that, subject only to some very
                                    rare exceptions, they must not submit to, perform, encourage, pay for,
                                    or arrange for an abortion. That direction tells us what we need to do
                                    on the weightier matters of the law, the choices that will move us
                                    toward eternal life.</span></big><br style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                                    In today&#8217;s world we are not true to our teachings if we are merely
                                    pro-choice. We must stand up for the right choice. Those who persist in
                                    refusing to think beyond slogans and sound bites like pro-choice wander
                                    from the goals they pretend to espouse and wind up giving their support
                                    to results they might not support if those results were presented
                                    without disguise.</span></big><br style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                                    For example, consider the uses some have made of the possible
                                    exceptions to our firm teachings against abortion. Our leaders have
                                    taught that the only possible exceptions are when the pregnancy
                                    resulted from rape or incest, or when a competent physician has
                                    determined that the life or health of the mother is in serious jeopardy
                                    or that the fetus has severe defects that will not allow the baby to
                                    survive beyond birth. But even these exceptions do not justify abortion
                                    automatically. Because abortion is a most serious matter, we are
                                    counseled that it should be considered only after the persons
                                    responsible have consulted with their bishops and received divine
                                    confirmation through prayer.</span></big><br style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                                    Some Latter-day Saints say they deplore abortion, but they give these
                                    exceptional circumstances as a basis for their pro-choice position that
                                    the law should allow abortion on demand in all circumstances. Such
                                    persons should face the reality that the circumstances described in
                                    these three exceptions are extremely rare. For example, conception by
                                    incest or rape&#8212;the circumstance most commonly cited by those who use
                                    exceptions to argue for abortion on demand&#8212;is involved in only a tiny
                                    minority of abortions. More than 95 percent of the millions of
                                    abortions performed each year extinguish the life of a fetus conceived
                                    by consensual relations. Thus the effect in over 95 percent of
                                    abortions is not to vindicate choice but to avoid its consequences.
                                    Using arguments of &#8220;choice&#8221; to try to justify altering the consequences
                                    of choice is a classic case of omitting what the Savior called &#8220;the
                                    weightier matters of the law.&#8221;</span></big><br
                                style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                                    A prominent basis for the secular or philosophical arguments for
                                    abortion on demand is the argument that a woman should have control
                                    over her own body. Not long ago I received a letter from a thoughtful
                                    Latter-day Saint outside the United States who analyzed that argument
                                    in secular terms. Since his analysis reaches the same conclusion I have
                                    urged on religious grounds, I quote it here for the benefit of those
                                    most subject to persuasion on this basis:</span></big><br
                                style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                                    &#8220;Every woman has, within the limits of nature, the right to choose what
                                    will or will not happen to her body. Every woman has, at the same time,
                                    the responsibility for the way she uses her body. If by her choice she
                                    behaves in such a way that a human fetus is conceived, she has not only
                                    the right to but also the responsibility for that fetus. If it is an
                                    unwanted pregnancy, she is not justified in ending it with the claim
                                    that it interferes with her right to choose. She herself chose what
                                    would happen to her body by risking pregnancy. She had her choice. If
                                    she has no better reason, her conscience should tell her that abortion
                                    would be a highly irresponsible choice.</span></big><br
                                style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                                    &#8220;What constitutes a good reason? Since a human fetus has intrinsic and
                                    infinite human value, the only good reason for an abortion would be the
                                    violation or deprivation of or the threat to the woman&#8217;s right to
                                    choose what will or will not happen to her body. Social, educational,
                                    financial, and personal considerations alone do not outweigh the value
                                    of the life that is in the fetus. These considerations by themselves
                                    may properly lead to the decision to place the baby for adoption after
                                    its birth, but not to end its existence in utero.</span></big><br
                                style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                                    &#8220;The woman&#8217;s right to choose what will or will not happen to her body
                                    is obviously violated by rape or incest. When conception results in
                                    such a case, the woman has the moral as well as the legal right to an
                                    abortion because the condition of pregnancy is the result of someone
                                    else&#8217;s irresponsibility, not hers. She does not have to take
                                    responsibility for it. To force her by law to carry the fetus to term
                                    would be a further violation of her right. She also has the right to
                                    refuse an abortion. This would give her the right to the fetus and also
                                    the responsibility for it. She could later relinquish this right and
                                    this responsibility through the process of placing the baby for
                                    adoption after it is born. Whichever way is a responsible choice.&#8221;</span></big><br
                                style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                                    The man who wrote those words also applied the same reasoning to the
                                    other exceptions allowed by our doctrine&#8212;life of the mother and a baby
                                    that will not survive birth.</span></big><br style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp; <br>
                                    I conclude this discussion of choice with two more short points.</span></big><br
                                style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp; <br>
                                    &nbsp;If we say we are anti-abortion in our personal life but
                                    pro-choice in public policy, we are saying that we will not use our
                                    influence to establish public policies that encourage righteous choices
                                    on matters God&#8217;s servants have defined as serious sins. I urge
                                    Latter-day Saints who have taken that position to ask themselves which
                                    other grievous sins should be decriminalized or smiled on by the law
                                    due to this theory that persons should not be hampered in their
                                    choices. Should we decriminalize or lighten the legal consequences of
                                    child abuse? of cruelty to animals? of pollution? of fraud? of fathers
                                    who choose to abandon their families for greater freedom or convenience?</span></big><br
                                style="font-family: arial;">
                            <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                                    Similarly, some reach the pro-choice position by saying we should not
                                    legislate morality. Those who take this position should realize that
                                    the law of crimes legislates nothing but morality. Should we repeal all
                                    laws with a moral basis so that our government will not punish any
                                    choices some persons consider immoral? Such an action would wipe out
                                    virtually all of the laws against crimes. <small>(&#8220;Weightier Matters,&#8221;
                                        <span style="font-style: italic;">Ensign</span>, Jan. 2001, pp. 13ff)</small></span></big><br
                                style="font-family: arial;">
