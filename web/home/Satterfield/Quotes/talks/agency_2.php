                <p align="center"><big><font face="Arial" size="+2"><big><em>Agency or
                            Inspiration?</em></big></font></big></p>
                <div style="text-align: center;"><big><font face="Arial">Elder Bruce R.
                        McConkie</font></big><br>
                    <font face="Arial">of the Council of the Twelve</font><br>
                    <font face="Arial"><em>New Era</em>, Jan. 1975, pp. 38-43</font>
                    <br wp="BR1">
                </div><br>
                <br wp="BR2">
                <p><big><big><font face="Arial">My wife and I were having a serious
                        discussion recently in which we were counting our many blessings. We
                        named a host of things that have
                        come to us, because of the Church, because of our family, because of
                        the glorious restoration of eternal truth that has taken place in this
                        day;
                        and then she climaxed the discussion by asking this question: "What's
                        the greatest blessing that has ever come into your life?"</font></big></big></p>
                <p><big><big><font face="Arial">Without a moment's hesitation I said,
                        "The greatest blessing that has ever come to me was on the thirteenth
                        day of October in 1937 at 11:20
                        A.M. when I was privileged to kneel in the Salt Lake Temple at the
                        Lord's altar and receive you as an eternal companion."</font></big></big></p>
                <p><big><big><font face="Arial">She said, "Well, you passed that test."</font></big></big></p>
                <p><big><big><font face="Arial">I believe that the most important
                        single thing that any Latter-day Saint ever does in this world is to
                        marry the right person, in the right place, by
                        the right authority; and that then--when they have been so sealed by
                        the power and authority that Elijah the prophet restored--the most
                        important remaining thing that any Latter-day Saint can ever do is so
                        to live that the terms and conditions of the covenant thus made will be
                        binding and efficacious now and forever. And so I'd like, if properly
                        guided, to make some suggestions that apply in all fields of choice--in
                        all
                        fields, at least all major fields, of activity--but that apply
                        particularly to the matter of eternal marriage, singling that out as
                        the one thing
                        paramount above all others.</font></big></big></p>
                <p><big><big><font face="Arial">When we dwelt in the presence of God
                        our Heavenly Father, we were endowed with agency. This gave us the
                        opportunity, the privilege, to
                        choose what we would do--to make a free, untrammeled choice. When
                        father Adam was placed in the Garden of Eden he was given this same
                        power, and we now possess it. We're expected to use the gifts and
                        talents and abilities, the sense and judgment and agency with which we
                        are
                        endowed.</font></big></big></p>
                <p><big><big><font face="Arial">But on the other hand we're commanded
                        to seek the Lord, to desire his Spirit, to get the spirit of revelation
                        and inspiration in our lives. We come
                        into the Church and a legal administrator places his hands upon our
                        head and says, "Receive the Holy Ghost." This gives us the gift of the
                        Holy
                        Ghost, which is the right to the constant companionship of that member
                        of theGodhead, based on faithfulness.</font></big></big></p>
                <p><big><big><font face="Arial">And so we're faced with two
                        propositions. One is that we ought to be guided by the spirit of
                        inspiration, the spirit of revelation. The other is that
                        we're here under a direction to use our agency, to determine what we
                        ought to do on our own; and we need to strike a fine balance between
                        these two, if we're going to pursue a course that will give us joy,
                        satisfaction, and peace in this life and lead to eternal reward in our
                        Father's
                        kingdom.</font></big></big></p>
                <p><big><big><font face="Arial">When we were with our Father in the
                        preexistent sphere, he observed and studied us and he knew how we would
                        respond to his laws when we
                        were in his presence, when we had the knowledge that he was our Father
                        and that the teachings presented to us came from him. We walked by
                        sight. Now he's finding out how we'll respond when we walk by faith,
                        when we're outside his presence and we have to rely on things other
                        than
                        the personal counsel that we once received from him.</font></big></big></p>
                <p><big><big><font face="Arial">I'd like to present three case studies,
                        out of which, perhaps, we can draw some very realistic and sound
                        conclusions as to what ought to be in
                        our lives. I'll take these illustrations out of the revelations that
                        the Lord has given us.</font></big></big></p>
                <big><big><br wp="BR1">
                        <br wp="BR2">
                    </big></big>
                <p><big><big><font face="Arial"><strong><em>"You have not understood"</em></strong></font></big></big></p>
                <p><big><big><font face="Arial">Case study number one: There was a man
                        named Oliver Cowdery. In the early days he operated as an amanuensis to
                        the Prophet. He was the
                        scribe. He wrote down the words that the Prophet dictated while the
                        Spirit rested upon him in the translation processes (the Book of Mormon
                        was then being translated). Brother Cowdery was relatively spiritually
                        immature at that time, and he sought and desired to do something beyond
                        his then present spiritual capacity. He wanted to translate. And so he
                        importuned the Prophet, the Prophet took the matter up with the Lord,
                        and
                        they got a revelation. The Lord said, "Oliver Cowdery, verily, verily,
                        I say unto you, that assuredly as the Lord liveth, who is your God and
                        your
                        Redeemer, even so surely shall you receive a knowledge of whatsoever
                        things you shall ask in faith, with an honest heart, believing that you
                        shall receive. &#8230;" And then one thing he might receive is defined as, "a
                        knowledge concerning the engravings of old records, which are ancient,
                        which contain those parts of my scripture of which has been spoken by
                        the manifestation of my Spirit."</font></big></big></p>
                <p><big><big><font face="Arial">Having thus dealt with the specific
                        problem, then the Lord revealed a principle that applies to it and all
                        other like situations: "Yea, behold, I will
                        tell you in your mind and in your heart, by the Holy Ghost, which shall
                        come upon you and which shall dwell in your heart. Now, behold, this is
                        the spirit of revelation." (D&amp;C 8:1-3.)</font></big></big></p>
                <p><big><big><font face="Arial">Oliver did what a good many of us would
                        have done. He had the instructions I have read, and he assumed that
                        they meant what they seemed
                        on the surface to say, which was that if in faith he asked God, he'd
                        have power to translate. But in his condition of relative spiritual
                        immaturity,
                        he hadn't yet learned what was involved in asking of God, or how to
                        generate the kind of faith or do the specific thing that has to be done
                        in
                        order to get an answer to a prayer. And so he asked. And as you know,
                        he failed; he was totally unable to translate. This caused some
                        concern,
                        I suppose, to him and the Prophet. The matter was referred back to the]
                        Lord, whose promise they had been attempting to conform to; and the
                        answer came, the reason came, why he couldn't translate: "Behold, you,
                        have not understood; you have supposed that I would give it unto you,
                        when you took no thought save it was to ask me." (D&amp;C 9:7.)</font></big></big></p>
                <p><big><big><font face="Arial">Now, seemingly, that's all he'd been
                        instructed to do, to ask in faith; but implicit in asking in faith is
                        the precedent requirement that we do
                        everything in our power to accomplish the goal that we seek. We use the
                        agency with which we have been endowed. We use every faculty and
                        capacity and ability that we possess to bring about the eventuality
                        that may be involved. This is translating the Book of Mormon, it's
                        choosing a
                        wife, it's choosing employment, it's doing any one of ten thousand
                        important things that arise in our lives.</font></big></big></p>
                <p><big><big><font face="Arial">The Lord continued:</font></big></big></p>
                <p><big><big><font face="Arial">"&#8230; I say unto you, that you must study
                        it out in your mind; then you must ask me if it be right, and if it is
                        right I will cause that your bosom shall
                        burn within you; therefore, you shall feel that it is right.</font></big></big></p>
                <p><big><big><font face="Arial">"But if it be not right you shall have
                        no such feelings, but you shall have a stupor of thought that shall
                        cause you to forget the thing which is
                        wrong; therefore, you cannot write that which is sacred save it be
                        given you from me." (D&amp;C 9:8-9.)</font></big></big></p>
                <p><big><big><font face="Arial">How do you choose a wife? I've heard a
                        lot of young people from Brigham Young University and elsewhere say,
                        "I've got to get a feeling of
                        inspiration. I've got to get some revelation. I've got to fast and pray
                        and get the Lord to manifest to me whom I should marry." Well, maybe it
                        will
                        be a little shock to you, but never in my life did I ever ask the Lord
                        whom I ought to marry. It never occurred to me to ask him. I went out
                        and
                        found the girl I wanted; she suited me; I evaluated and weighed the
                        proposition, and it just seemed a hundred percent to me as though this
                        ought to be. Now, if I'd done things perfectly, I'd have done some
                        counseling with the Lord, which I didn't do; but all I did was pray to
                        the Lord
                        and ask for some guidance and direction in connection with the decision
                        that I'd reached. A more perfect thing to have done would have been to
                        counsel with him relative to the decision and get a spiritual
                        confirmation that the conclusion, which I by my agency and faculties
                        had arrived at,
                        was the right one.</font></big></big></p>
                <big><big><br wp="BR1">
                        <br wp="BR2">
                    </big></big>
                <p><big><big><font face="Arial"><strong><em>"Why are you asking me?"</em></strong></font></big></big></p>
                <p><big><big><font face="Arial">Now, case study number two: There was a
                        man whose name is not preserved to us in the ancient record. He's known
                        as the brother of Jared.
                        From other sources we know his name was Moriancumer. He was the
                        spiritual leader, initially, of the Jaredite people. As they started
                        their
                        progress from the tower of Babel to their American promised land, he
                        was the one who communed with the Lord to get the direction, the
                        spiritual
                        guidance that they as a people needed.</font></big></big></p>
                <p><big><big><font face="Arial">And some very interesting things
                        occurred. They got to the waters that they were going to cross, and the
                        Lord said to him, "Build some barges."
                        But interestingly, the Lord didn't tell him how to build the barges.
                        The brother of Jared had done it on a previous occasion; he didn't need
                        instruction; he didn't need revelation to guide him. So he built the
                        barges.</font></big></big></p>
                <p><big><big><font face="Arial">But this time they were going to be
                        used under some peculiar and difficult circumstances, and he needed
                        something more than was now
                        present in them: he needed some air. And this was a problem that was
                        beyond him. So he took that matter up with the Lord, and because it was
                        totally beyond his capacity to solve, the Lord solved it for him and
                        said, "Do thus and so and you'll have air."</font></big></big></p>
                <p><big><big><font face="Arial">But then the brother of Jared--having
                        confidence because he was talking to the Lord, because he was communing
                        and getting answers--asked
                        another question: he asked for a solution to a problem that he should
                        have figured out by himself and not taken up with the Lord. He said,
                        "What
                        will we do for light in the vessels?"</font></big></big></p>
                <p><big><big><font face="Arial">And the Lord talked to him about it a
                        little and then he said this: "What will ye that I should do that ye
                        may have light in your vessels?" (Ether
                        2:23.) In effect, "What are you asking me for? This is something you
                        should have solved." And he talked a little more, and he repeated in
                        essence the question: "What will ye that I should prepare for you that
                        ye may have light when ye are swallowed up in the depths of the sea?"
                        (Ether 2:25.) In other words, "Moriancumer, this is your problem. Why
                        are you troubling me? I've given you your agency; you are endowed with
                        capacity and ability. Get out and solve the problem."</font></big></big></p>
                <p><big><big><font face="Arial">Well, the brother of Jared got the
                        message. He went up into a mount called Shelem, and the record says he
                        "did molten out of a rock sixteen
                        small stones; and they were white and clear, even as transparent
                        glass." (Ether 3:1.)</font></big></big></p>
                <p><big><big><font face="Arial">Well, the brother of Jared took sixteen
                        little crystals of some sort (he could hold all of them in his hands)
                        up on the mount. The record says, "He
                        did carry them in his hands upon the top of the mount" (Ether 3:1), and
                        then he said in effect to the Lord, "Now this is what I hope you will
                        do."
                        You really don't tell the Lord what to do, but you get some
                        inspiration, and you use your judgment, and then you talk the matter
                        over with him.
                        And so Moriancumer said to the Lord: "Touch these stones, O Lord, with
                        thy finger, and prepare them that they may shine forth in darkness; and
                        they shall shine forth unto us in the vessels which we have prepared,
                        that we may have light while we shall cross the sea." (Ether 3:4.)</font></big></big></p>
                <p><big><big><font face="Arial">And the Lord did what the brother of
                        Jared asked, and this is the occasion when he then saw the finger of
                        the Lord; and, while he was in tune,
                        he received revelation that exceeded anything that any prophet had ever
                        gained up to that moment. The Lord revealed more to him about his
                        nature and personality than ever theretofore had come forth, and it all
                        came about because he'd done everything that he could do and because
                        he counseled with the Lord.</font></big></big></p>
                <p><big><big><font face="Arial">There's a fine balance between agency
                        and inspiration. We're expected to do everything in our power and then
                        to seek an answer from the
                        Lord, a confirming seal that we've reached the right conclusion; and
                        sometimes, happily, in addition, we get added truths and knowledge that
                        we
                        hadn't even supposed.</font></big></big></p>
                <big><big><br wp="BR1">
                        <br wp="BR2">
                    </big></big>
                <p><big><big><font face="Arial"><strong><em>"They shall counsel between
                                themselves and me"</em></strong></font><font face="Arial"><em></em></font></big></big></p>
                <p><big><big><font face="Arial">Now, case study number three. In the
                        early history of the Church, the Lord commanded the Saints to assemble
                        in a certain place in Missouri.
                        The decree went forth: "Assemble." Specifically, the decree went forth,
                        "Let the Presiding Bishop come here and do such and such." Now notice
                        what happened. The Lord is talking:</font></big></big></p>
                <p><big><big><font face="Arial">"&#8230; as I spake concerning my servant
                        Edward Partridge, this land is the land of his residence, and those
                        whom he has appointed for his
                        counselors; and also the land of the residence of him whom I have
                        appointed to keep my storehouse;</font></big></big></p>
                <p><big><big><font face="Arial">"Wherefore, let them bring their
                        families to this land, [and here's the point] as they shall counsel
                        between themselves and me." (D&amp;C 58:24-25.)</font></big></big></p>
                <p><big><big><font face="Arial">You see, the Lord said "assemble" to
                        Zion. The details and the arrangements, however, the how and the when
                        and the circumstances, are to be
                        determined by the agency of those who are called to assemble, but they
                        are to counsel with the Lord. Now, when you counsel with the Lord, you
                        talk something over. I bring my children in and we counsel on a
                        problem. I don't tell them what ought to be; I say, "What do you think?
                        What's
                        your evaluation? What do you want to do in this situation? What's the
                        best thing to do?" And they tell me what they think, and if I happen to
                        have
                        any wisdom or judgment on the matter, I express my views. The Lord has
                        all wisdom, all knowledge, and all power; he knows how to govern and
                        control and direct us in a perfect manner. He lets us determine what we
                        should do, but he expects us to counsel with him.</font></big></big></p>
                <p><big><big><font face="Arial">Now, after the Lord had said this to
                        the Presiding Bishopric of the Church, he gave the principle that
                        governed in that situation, and it governs in
                        all situations. And this is one of our glorious revealed truths. He
                        said:</font></big></big></p>
                <p><big><big><font face="Arial">"For behold, it is not meet that I
                        should command in all things; for he that is compelled in all things,
                        the same is a slothful and not a wise servant;
                        wherefore he receiveth no reward.</font></big></big></p>
                <p><big><big><font face="Arial">"Verily I say, men should be anxiously
                        engaged in a good cause, and do many things of their own free will, and
                        bring to pass much
                        righteousness;</font></big></big></p>
                <p><big><big><font face="Arial">"For the power is in them, wherein they
                        are agents unto themselves. And inasmuch as men do good they shall in
                        nowise lose their reward.</font></big></big></p>
                <p><big><big><font face="Arial">"But he that doeth not anything until
                        he is commanded, and receiveth a commandment with doubtful heart, and
                        keepeth it with slothfulness, the
                        same is damned." (D&amp;C 58:26-29.)</font></big></big></p>
                <p><big><big><font face="Arial">The Prophet Joseph Smith was asked,
                        "How do you govern so great and diverse a people as the Latter-day
                        Saints?"</font></big></big></p>
                <p><big><big><font face="Arial">He replied, "I teach them correct
                        principles and they govern themselves."</font></big></big></p>
                <big><big><br wp="BR1">
                        <br wp="BR2">
                    </big></big>
                <p><big><big><font face="Arial">Now, that's the order of heaven. That's
                        how the Almighty operates. That's how the Church is supposed to
                        operate. We're supposed to learn
                        correct principles and then govern ourselves. We make our own choices,
                        and then we present the matter to the Lord and get his approving,
                        ratifying seal.</font></big></big></p>
                <p><big><big><font face="Arial">"Counsel with the Lord in all thy
                        doings" </font></big></big></p>
                <p><big><big><font face="Arial">Those are the three case studies; let
                        us come to the revealed conclusion. There was a man named Alma, a
                        mighty and a great prophet. He had
                        a son named Helaman, who was a holy and righteous man, following the
                        pattern that his father had set. And to Helaman, Alma said this: "O,
                        remember, my son, and learn wisdom in thy youth; yea, learn in thy
                        youth to keep the commandments of God. Yea, and cry unto God for all
                        thy
                        support." (Alma 37:35-36.) Do you think that if you're counseled to
                        pray to the Lord for support, both temporal and spiritual, that that's
                        all you
                        have to do? The Lord's prayer says, "Give us this day our daily bread."
                        Do you go out and sit down in the desert or on the mountain and pray
                        with all the fervor you can possess, "Give us this day our daily
                        bread," or do you go out and plant crops and raise herds and do
                        everything that
                        you can in your situation to accomplish the end result?</font></big></big></p>
                <p><big><big><font face="Arial">Continuing: "yea, let all thy doings be
                        unto the Lord, and whithersoever thou goest let it be in the Lord; yea,
                        let thy thoughts be directed unto the
                        Lord; yea, let the affections of thy heart be placed upon the Lord
                        forever." (Alma 37:36.) Now note: "Counsel with the Lord in all thy
                        doings, and
                        he will direct thee for good." (Alma 37:37.)</font></big></big></p>
                <p><big><big><font face="Arial">What was Oliver Cowdery's problem? "You
                        took no thought save it was to ask &#8230; you must study it out in your
                        mind." (D&amp;C 9:7-8.)</font></big></big></p>
                <p><big><big><font face="Arial">Well, do you want a wife? Do you want
                        anything that's right and proper? You go to work, and you use the
                        agency and power and ability that God
                        has given you. You use every faculty, you get all the judgment that you
                        can centered on the problem, you make up your own mind, and then, to
                        be sure that you don't err, you counsel with the Lord. You talk it
                        over. You say, "This is what I think; what do you think?" And if you
                        get the calm,
                        sweet surety that comes only from the Holy Spirit, you know you've
                        reached the right conclusion; but if there's anxiety and uncertainty in
                        your
                        heart, then you'd better start over, because the Lord's hand is not in
                        it, and you're not getting the ratifying seal that, as a member of the
                        Church
                        who has the gift of the Holy Ghost, you are entitled to receive.</font></big></big></p>
                <p><big><big><font face="Arial">"&#8230; yea, when thou liest down at night
                        lie down unto the Lord, that he may watch over you in your sleep, and
                        when thou risest in the morning let
                        thy heart be full of thanks unto God; and if ye do these things, ye
                        shall be lifted up at the last day." (Alma 37:37.) If you learn how to
                        use the
                        agency that God has given you, and if you try to make your own
                        decisions, and if you reach conclusions that are sound and right, and
                        if you
                        counsel with the Lord and get his ratifying seal of approval upon the
                        conclusions you've reached, then you've received revelation, for one
                        thing;
                        and for another thing, you're going to have the great reward of eternal
                        life and be lifted up at the last day. We're not all equal by any
                        means;
                        some have one talent and capacity and some another. But if we use the
                        talents we have, somehow we'll come out all right.</font></big></big></p>
                <p><big><big><font face="Arial">One Monday when we were celebrating
                        Washington's birthday, I was down at my mother's sawing a log in the
                        backyard. She came out to give
                        me some direction and see how I was doing it, and she wasn't very
                        pleased. She thought I ought to do it differently. She went back into
                        the
                        house and in a few minutes my younger brother arrived. She said to him,
                        "I think you'd better go out in the backyard and give Bruce some help
                        and see that he does this thing right." And then she said to him,
                        "Bruce isn't very bright." Well, so I'm not. So I start where I am, and
                        I go forward
                        from there. I start using such talent as I have, and I begin to apply
                        the principles of eternal truth to my life. I consult and counsel with
                        the Lord in
                        the process, and no matter where I am, the gospel takes me forward and
                        onward and upward, and blessings flow to me that will ennoble and
                        sanctify and improve me in this life and eventually give me glory and
                        honor and dignity in the life to come.</font></big></big></p>
                <p><big><big><font face="Arial">We have the spirit of revelation</font></big></big></p>
                <p><big><big><font face="Arial">I think we've said enough; the
                        principles are before us. Let me just do one thing more. Let me do, in
                        effect, what my friend Alma would do. After
                        he'd preached a sermon, he said, "And this is not all. Do ye not
                        suppose that I know of these things myself?" (Alma 5:45.) That is he'd
                        given
                        them the case studies, he'd quoted the revelations, he'd told them what
                        was involved, and then he bore personal testimony. This is what we
                        ought to do in the Church. We ought to learn how to teach by the power
                        of the Spirit, so that when we get through talking about the gospel
                        subjects we'll know whether what we've said is right, and we'll be in a
                        position to bear testimony, not alone of the truth and the divinity of
                        the
                        work, but also that the doctrine we proclaim and the everlasting truths
                        we expound are right, that they are the mind and voice and will of the
                        Lord. The glorious, wondrous thing about this work and about these
                        doctrines is that they are true. There isn't anything in this world, no
                        truth that
                        we can conceive of, to compare with the truth that the work we're
                        engaged in is true, that the Lord's hand is here. It's a literal fact
                        that we have
                        the gift and power of the Holy Ghost. We have the spirit of revelation,
                        the spirit of testimony, the spirit of prophecy. These things must be,
                        or
                        we're not in the Church and kingdom of God; we're not the Lord's people.</font></big></big></p>
                <p><big><big><font face="Arial">The fact is that we do have them;
                        revelation works. Don't shy away from getting revelation. Joseph Smith
                        said, "God hath not revealed anything
                        to Joseph, but what he will make known unto the Twelve, and even the
                        least Saint may know all things as fast as he is able to bear them."
                        (Teachings of the Prophet Joseph Smith, p. 149.) We're entitled to the
                        spirit of revelation, but what I'm attempting to teach is that there's
                        a how
                        and a procedure, and there are conditions precedent. It is our
                        obligation to go to work on our problems and then counsel with the Lord
                        and get
                        the ratifying seal of the Holy Spirit on the conclusions that we've
                        reached; and that ratifying seal is the spirit of revelation.</font></big></big></p>
                <p><big><big><font face="Arial">God grant us wisdom in these things.
                        God grant us the courage and the ability to stand on our own feet and
                        use our agency and the abilities and
                        capacities we possess; then let's be sufficiently humble and amenable
                        to the Spirit to bow our will to his will, to get his ratifying,
                        confirming seal
                        of approval, to get in our lives in that way the spirit of revelation.
                        And if we so do, there's no question about the result; it's peace in
