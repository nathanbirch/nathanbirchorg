                <center style="font-family: arial;"><big><i><font size="+3"><big>The
                                Great Plan of the Eternal God</big></font></i>
                    </big>
                    <p><big>Elder Neal A. Maxwell
                        <br>
                        Of the Quorum of the Twelve Apostles
                        <br>
                        <i>Ensign</i>, May 1984, pp. 21-23
                    </big></p>
                    <p><big>(Underlining is NOT original.)</big></p>
                </center>
                <p style="font-family: arial;"><font size="+1">One of the great
                    blessings flowing from amplifying, latter-day
                    revelations is the crucial, doctrinal framework known as the marvelous
                    plan of salvation, the plan of happiness, or the plan of mercy. (See
                    Alma
                    42:5, 8, 15.) However designated, it represents what Amulek called the
                    "great plan of the Eternal God" without which mankind would unavoidably
                    perish. (Alma 34:9.)</font>
                </p>
                <p style="font-family: arial;"><font size="+1">The plan is a most
                    stunning example of the precious perspective
                    of the gospel of Jesus Christ. Furthermore, full faith in the Lord
                    Jesus
                    Christ includes and requires full faith in His Father's plan of
                    salvation.</font>
                </p>
                <p style="font-family: arial;"><font size="+1">President Brigham Young
                    declared: "The Spirit of revelation
                    must be in each and every individual, to know the plan of salvation and
                    keep in the path that leads them to the presence of God." (<i>Journal
                        of
                        Discourses</i>, 9:279.)</font>
                </p>
                <p style="font-family: arial;"><font size="+1">So vital is this
                    framework that if one stays or strays
                    outside it, he risks provinciality and misery. In fact,&nbsp;<span
                        style="text-decoration: underline;">most
                        human misery represents ignorance of or noncompliance with the plan</span>.&nbsp;<span
                        style="text-decoration: underline;">A
                        cessation of such mortal suffering will not come without compliance to
                        it.</span> Hence, the Lord, who has freely shared this vital knowledge
                    with us, has urged us to teach the fundamentals of this plan "freely."
                    (Moses 6:58.)</font>
                </p>
                <p style="font-family: arial;"><font size="+1">At the center of the
                    Father's plan is Jesus Christ, mankind's
                    Redeemer. Yet, as foreseen, many judge Jesus "to be a thing of naught"
                    (1 Ne. 19:9), or "consider him" merely "a man." (Mosiah 3:9.) Whether
                    others
                    deny or delimit Jesus, for us He is our Lord and Savior! Comparatively,
                    brothers and sisters, it matters very little what people think of us,
                    but
                    it matters very much what we think of Him. It matters very little, too,
                    who others say we are; what matters is who we say Jesus is. (See Matt.
                    16:13-17.)</font>
                </p>
                <p style="font-family: arial;"><font size="+1">For instance, we
                    appreciate not only the towering divinity
                    of Jesus Christ, but His breathtaking mobility and the scope of His
                    shepherding.
                    The resurrected Jesus revisited the Middle East scene of His mortal
                    messiahship.
                    Then some souls in the Americas. (See 3 Ne 11.) And then His other lost
                    sheep. (See 3 Ne. 17:4.)</font>
                </p>
                <p style="font-family: arial;"><font size="+1">In His selfless
                    plan,&nbsp;<span style="text-decoration: underline;">the
                        Lord doeth nothing save it be for the benefit of the children of men.</span>
                    (See 2 Ne. 26:24.)&nbsp;<span style="text-decoration: underline;">He
                        labors,
                        lovingly and constantly, as Moses and Jeremiah declared, "for our good
                        always</span>." (Deut. 6:24; see also Jer. 32: 38-40.) In His grand
                    design,
                    His "work" and "glory" are "to bring to pass the immortality and
                    eternal
                    life of man." (Moses 1:39.) Thus, even when we truly learn to love God,
                    we must humbly acknowledge that He loved us first. (See 1 Jn. 4:19.)</font>
                </p>
                <p style="font-family: arial;"><font size="+1">Meanwhile, Shakespeare
                    was not very wide of the mark in
                    writing, "All the world's a stage." (<span style="font-style: italic;">As
                        You Like It</span>, act 2, scene 7.)<span
                        style="text-decoration: underline;">
                        But not for playacting</span>!</font>
                </p>
                <p style="font-family: arial;"><font size="+1">The very word plan
                    confirms God's paternal purpose, a
                    realization so desperately needed by the confused and despairing on the
                    world's stage.</font>
                </p>
                <p style="font-family: arial;"><font size="+1">The "plan of happiness"
                    not only ensures the immortalization
                    of our individual identities, but can yield bettered and reborn
                    individuals.
                    Fittingly, one appreciative prophet declared, "O how great the plan of
                    our God!" (2 Ne. 9:13.) Enoch wept when he saw the sweep of history and
                    the unnecessary human misery. (See Moses 7:41.) But he also saw the
                    triumph
                    of God's plan. Another prophet exclaimed: "God &#8230; made these things
                    known
                    unto us that we might not perish &#8230; because he loveth our souls &#8230; ;
                    therefore,
                    in his mercy he doth visit us by his angels, that the plan of salvation
                    might be made known unto us." (Alma 24:14.)</font>
                </p>
                <p style="font-family: arial;"><font size="+1">It took visits by angels
                    in our day, too, in order to
                    instruct us anew regarding God's plan of salvation and to reassure us
                    that
                    mortality is not a conclusive and massive mausoleum, and that death is
                    not extinction.</font>
                </p>
                <p style="font-family: arial;"><font size="+1">Alma faced an awful and
                    anguished moment when he felt
                    that he might actually "become extinct both soul and body." (Alma
                    36:15.)
                    Then he remembered his father's prophecies "concerning the coming of
                    one
                    Jesus Christ, a Son of God, to atone for the sins of the world." (Alma
                    36:17.) In a moment of deep intellectual humility, his "mind caught
                    hold
                    upon this thought." Out came Alma's great soul cry: "O Jesus, thou Son
                    of God, have mercy on me"! (Alma 36:18.)</font>
                </p>
                <p style="font-family: arial;"><font size="+1">Purpose replaced pain.
                    Joy swallowed up despair as Alma
                    apparently viewed God upon His throne, and he longed to join God! (See
                    Alma 36:22.)</font>
                </p>
                <p style="font-family: arial;"><span style="text-decoration: underline;"><font
                            size="+1">Such longing
                        for a heavenly home is real</font></span><font size="+1">, especially
                    in view of how this life
                    is designed. After all, brothers and sisters, when we rejoice in
                    beautiful
                    scenery, great art, and great music, it is but the flexing of instincts
                    acquired in another place and another time.</font>
                </p>
                <p style="font-family: arial;"><font size="+1">Life turns out, however,
                    to be just what one would expect
                    of&nbsp;<span style="text-decoration: underline;">a deliberately
                        constructed
                        proving and tutoring experience which features opportunities, choices,
                        and deprivations</span>.&nbsp;<span style="text-decoration: underline;">Furthermore,
                        there is no way around--the only way to go is through!</span></font>
                </p>
                <p style="font-family: arial;"><font size="+1">And what a "through" it
                    is!</font>
                </p>
                <p style="font-family: arial;"><font size="+1">Even so, for us
                    mistake-prone mortals,&nbsp;<span style="text-decoration: underline;">this
                        plan of mercy provides for recognition and redress of error and for the
                        resumption of interrupted individual development</span>.</font>
                </p>
                <p style="font-family: arial;"><span style="text-decoration: underline;"><font
                            size="+1">Redemptive and
                        refining provisions are made</font></span><font size="+1">: For a brave
                    Peter faltering and sinking
                    on the churning waves--and yet knowing to whom to look to live, and
                    crying
                    out, "Lord, save me." (Matt. 14:30.) For a meek Moses struggling with
                    people,
                    fatigued and with all the burdens of leadership. (See Num. 11:11, 14,
                    29.)
                    For a Jonah seeking to substitute Tarshish, but still reaching Nineveh
                    and, thereby, receiving a great lesson in compassion. For erring Oliver
                    Cowdery, Martin Harris, and Thomas B. Marsh to recover their spiritual
                    poise and vote with their feet by traveling westward to rejoin, in
                    reconciliation,
                    and to sustain the plan and its enunciating latter-day prophets.</font>
                </p>
                <p style="font-family: arial;"><font size="+1">Hence, brothers and
                    sisters, for the faithful, our finest
                    hours are sometimes during or just following our darkest hours.</font>
                </p>
                <p style="font-family: arial;"><font size="+1">It is an incredible
                    irony, therefore, that some complainingly
                    attempt to use the very tutoring process of the Lord against Him. Or
                    resent
                    the reality that we are to walk by faith during this mortal experience.
                    Yet, as practical and spiritual President Brigham Young said, "There is
                    no saving faith merely upon &#8230; acknowledging a fact." (<i>Discourses of
                        Brigham Young</i>, sel. John A. Widtsoe, Salt Lake City: Deseret Book
                    Co.,
                    1941, p. 154.)</font>
                </p>
                <p style="font-family: arial;"><span style="text-decoration: underline;"><font
                            size="+1">Furthermore,
                        since this life is such a brief experience, there must be regular exit
                        routes</font></span><font size="+1">.&nbsp;<span
                        style="text-decoration: underline;">Some easy</span>.&nbsp;<span
                        style="text-decoration: underline;">Some
                        hard</span>.&nbsp;<span style="text-decoration: underline;">Some sudden</span>.&nbsp;<span
                        style="text-decoration: underline;">Others
                        lingering</span>. Therefore, we cannot presume, even by faith, to block
                    all these exits, all the time, and for all people. Nor, if possessed of
                    full, eternal perspective, would we desire so to do.</font>
                </p>
                <p style="font-family: arial;"><font size="+1">Since certain
                    recollections are withheld, we do not now
                    see the end from the beginning. But God does. Meanwhile, we are in what
                    might be called "the murky middle." Therein, however, we can still
                    truly
                    know that God loves us, individually and perfectly, even though we
                    cannot
                    always explain the meaning of all things happening to us or around us.
                    (See 1 Ne. 11:17.)</font>
                </p>
                <p style="font-family: arial;"><font size="+1">Enclosed in this mortal
                    cocoon, or classroom, we would
                    be totally provincial in outlook except for faith in, and knowledge of,
                    the "great plan of the Eternal God."</font>
                </p>
                <p style="font-family: arial;"><font size="+1">Hence, Christ's
                    doctrines pertaining to the plan of salvation
                    stand like sentinel scriptures to mark and light the way. His gospel
                    guardrails
                    line the strait and narrow path to steady us, nudge us, and even jar us
                    for the sake of our spiritual safety!</font>
                </p>
                <p style="font-family: arial;"><font size="+1">So much more than a
                    matter of abstract theology,&nbsp;<span
                        style="text-decoration: underline;">this
                        great plan can focus daily life</span>. Its truths are crucial to how
                    we
                    see ourselves, others, life, the Lord, and even the universe. Or how we
                    view a baby. Or death. Or the praise and honors of the world.&nbsp;<span
                        style="text-decoration: underline;">This
                        plan constitutes the mother lode of meaning and can cradle us,
                        conceptually,
                        amid any concern</span>.</font>
                </p>
                <p style="font-family: arial;"><span style="text-decoration: underline;"><font
                            size="+1">Its truths and
                        perspectives permit us to distinguish between a great book and mere
                        want
                        ads</font></span><font size="+1">,<span
                        style="text-decoration: underline;"> between vengeance
                        and justice</span>,&nbsp;<span style="text-decoration: underline;">rage
                        and righteous indignation</span>, and<span
                        style="text-decoration: underline;">
                        pleasure and happiness</span>.</font>
                </p>
                <p style="font-family: arial;"><font size="+1">With an understanding of
                    God's plan of salvation, we know
                    that the rejoicing, the striving, the suffering, the tutoring, and the
                    enduring experiences of life&nbsp;<span
                        style="text-decoration: underline;">all
                        play their part</span> in an intelligible process of helping us, if we
                    will, to become, as the Savior beckoningly invited, "even as I am." (3
                    Ne. 27:27.)</font>
                </p>
                <p style="font-family: arial;"><font size="+1">This individual
                    development sometimes requires the march
                    of a Zion's Camp, or an arduous Hole in the Rock trek, or special
                    classrooms
                    like the settlements in northern Mexico, wherein special individuals
                    were
                    fashioned. Those episodes, however, had nothing to do with real estate
                    but everything to do with our second estate!</font>
                </p>
                <p style="font-family: arial;"><font size="+1">Hence,&nbsp;<span
                        style="text-decoration: underline;">in
                        submitting knowingly and meekly to this plan, we cannot say to the Lord
                        that we are willing to surrender but only on our terms</span>.&nbsp;<span
                        style="text-decoration: underline;">There
                        are no conditions in unconditional surrender</span>!</font>
                </p>
                <p style="font-family: arial;"><font size="+1">Even with all of its
                    interior consistency, however, the
                    plan cannot bring true happiness to anyone whose life is grossly
                    inconsistent
                    with its standards. It cannot fully enfold him who is too worried about
                    being taken in. It has no place of honor for one too concerned with
                    losing
                    his place in the secular synagogue. (See John 12:42-43.)</font>
                </p>
                <p style="font-family: arial;"><font size="+1">Though the plan reflects
                    a caring Father and a Savior,
                    believers in the plan are not automatically immune to the consuming
                    cares
                    of the world.</font>
                </p>
                <p style="font-family: arial;"><span style="text-decoration: underline;"><font
                            size="+1">The plan places
                        a striking emphasis on present human freedom to choose</font></span><font
                        size="+1">. (See 2 Ne.
                    2:27.)&nbsp;<span style="text-decoration: underline;">Yet some of our
                        present
                        circumstances may reflect previous agreements, now forgotten, but once
                        freely made</span>.</font>
                </p>
                <p style="font-family: arial;"><font size="+1">The plan always points
                    the way, but&nbsp;<span style="text-decoration: underline;">does
                        not always smooth the way</span>, since individual development requires
                    an "opposition in all things." (2 Ne. 2:11.)</font>
                </p>
                <p style="font-family: arial;"><font size="+1">The Lord will give us
                    the needed intellectual and spiritual
                    confirmation concerning His plan, but on His terms and in His own way.</font>
                </p>
                <p style="font-family: arial;"><font size="+1">"If any man will do his
                    will, he shall know of the doctrine,
                    whether it be of God, or whether I speak of myself." (John 7:17.)</font>
                </p>
                <p style="font-family: arial;"><span style="text-decoration: underline;"><font
                            size="+1">Truly, of all
                        the errors mortals could make, God's plan of salvation is the wrong
                        thing
                        to be wrong about</font></span><font size="+1">!</font>
                </p>
                <p style="font-family: arial;"><font size="+1">No error could be more
                    enormous or more everlasting in
                    its consequences!</font>
                </p>
                <p style="font-family: arial;"><font size="+1">No wonder this Church
                    and its people go to such great
                    lengths and expense to share the fulness of the gospel concerning this
                    plan.</font>
                </p>
                <p style="font-family: arial;"><font size="+1">No wonder the Lord wants
                    the plan taught plainly and repetitively.</font>
                </p>
                <p style="font-family: arial;"><font size="+1">And why not? It is God's
                    plan--not ours! And, given the
                    unimpressive outcomes of man's plans to solve the world's problems,
                    aren't
                    we glad! Furthermore, of all the things about which we might converse,
                    as Jacob wrote, "Why not speak of the atonement of Christ?" (Jacob
                    4:12.)
                    Why not, brothers and sisters? This event arches over all of human
                    history,
                    as a Redeeming God and Savior Son pressed onward with the great plan of
                    happiness. Yes, in the plan, God would have us be happy, but first we
                    had
                    to be free to choose.</font>
                </p>
                <p style="font-family: arial;"><font size="+1">God's gift to us of this
                    moral agency tells us wonderful
                    things about His beneficial and developmental purposes. Our abuse of
                    that
                    agency tells us awful things about ourselves!</font>
                </p>
                <p style="font-family: arial;"><font size="+1">Yet, on this mortal
                    stage, we see great moments of mercy,
                    stirring celebrations of compassion, striking selflessness, and quiet
                    and
                    constant heroism among people of all creeds and colors and cultures.</font>
                </p>
                <p style="font-family: arial;"><font size="+1">This should not surprise
                    us. After all, whose spirit children
                    are we? (See Heb. 12:9.)</font>
                </p>
                <p style="font-family: arial;"><font size="+1">Unsurprisingly,
                    therefore, this mortal school produces
                    some soaring triumphs but also a history filled with individual
                    mistakes.
                    But we should not blame the school, nor the curriculum! Least of all,
                    the
                    Schoolmaster! Furthermore, we dare not lecture Him on the plight of His
                    students!</font>
                </p>
                <p style="font-family: arial;"><font size="+1">Meanwhile, brothers and
                    sisters, no one ever promised
                    us that discipleship in the last days would be a picnic in the park.</font>
                </p>
                <p style="font-family: arial;"><font size="+1">Former periods of stress
                    can guide us. When the earlier
                    coming of Jesus was imminent, signs abounded. Still, for some, there
                    were
                    "doubtings." (3 Ne. 8:4.) But the faithful prevailed and were
                    vindicated.</font>
                </p>
                <p style="font-family: arial;"><font size="+1">There were determined
                    detractors then, mocking the faith
                    of believers, briefly creating "a great uproar," even rejoicing over
                    the
                    seeming prospect that the faith of Christ's followers would be in vain.
                    (See 3 Ne. 1:5-7.) It was not.&nbsp;<span
                        style="text-decoration: underline;">Members
                        kept the faith, and the faith kept them</span>!</font>
                </p>
                <p style="font-family: arial;"><font size="+1">For today's spiritually
                    attuned, the reassurances will
                    be there, as with Elisha's young servant. Encircled by an outnumbering
                    enemy, the young man rightly sought reassurance from the prophet and
                    seer,
                    who told him, "Fear not: for they that be with us are more than they
                    that
                    be with them." (2 Kgs. 6:16.) But the young man could count, and,
                    clearly,
                    it did not look that way to him--until after the prophet's prayer in
                    his
                    behalf. Then, the young man's eyes were opened, and he saw the mountain
                    filled with horses and chariots of fire. (See 2 Kgs. 6:17.)</font>
                </p>
                <p style="font-family: arial;"><font size="+1">All will be well now, as
                    anciently, because the Lord's
                    covenant keepers have His echoing assurance:</font>
                </p>
                <p style="font-family: arial;"><font size="+1">"And they shall be my
                    people, and I will be their God:</font>
                </p>
                <p style="font-family: arial;"><font size="+1">"And I will give them
                    one heart, and one way, that they
                    may fear me for ever, for the good of them, and of their children after
                    them:</font>
                </p>
                <p style="font-family: arial;"><font size="+1">"And I will make an
                    everlasting covenant with them, that
                    I will not turn away from them, to do them good." (Jer. 32:38-40.)</font>
                </p>
                <p style="font-family: arial;"><font size="+1">I so testify and assure
                    in Apostolic authority and in
                    the holy name of Jesus Christ, amen.<br>
