<p style="font-family: arial; text-align: center;"><big><big
 style="font-family: century gothic; color: rgb(0, 0, 153);">First
Presidency Message</big><b><big> </big><br>
</b>(Heber J. Grant, J. Reuben Clark, Jr.,
David O. McKay)
</big></p>
<p style="font-family: arial; text-align: center;"><big><span
 style="font-family: arial;">Conference Report, October 1942,
p.12-13</span></big></p>
<p style="font-family: arial;"><big><i><br>
In the October, 1942 General Conference, J. Reuben Clark, Jr. read
a message from the First Presidency to the general Church membership.
Under
the title of PARENTHOOD, the following was said:</i>
<br>
&nbsp;
</big></p>
<p style="font-family: arial;"><big>Amongst His earliest commands
to Adam and Eve, the Lord said: "Multiply
and replenish the earth." He has repeated that command in our day. He
has
again revealed in this, the last dispensation, the principle of the
eternity
of the marriage covenant. He has restored to earth the authority for
entering
into that covenant, and has declared that it is the only due and proper
way of joining husband and wife, and the only means by which the sacred
family relationship may be carried beyond the grave and through
eternity.
He has declared that this eternal relationship may be created only by
the
ordinances which are administered in the holy temples of the Lord, and
therefore that His people should marry only in His temple in accordance
with such ordinances.
</big></p>
<p style="font-family: arial;"><big>The Lord has told us that it
is the duty of every husband and wife to
obey the command given to Adam to multiply and replenish the earth, so
that the legions of choice spirits waiting for their tabernacles of
flesh
may come here and move forward under God's great design to become
perfect
souls, for without these fleshly tabernacles they cannot progress to
their
God-planned destiny. Thus, every husband and wife should become a
father
and mother in Israel to children born under the holy, eternal covenant.
</big></p>
<p style="font-family: arial;"><big>By bringing these choice
spirits to earth, each father and each mother
assume towards the tabernacled spirit and towards the Lord Himself by
having
taken advantage of the opportunity He offered, an obligation of the
most
sacred kind, because the fate of that spirit in the eternities to come,
the blessings or punishments which shall await it in the hereafter,
depend,
in great part, upon the care, the teachings, the training which the
parents
shall give to that spirit.
</big></p>
<p style="font-family: arial;"><big>No parent can escape that
obligation and that responsibility, and for
the proper meeting thereof, the Lord will hold us to a strict
accountability.
No loftier duty than this can be assumed by mortals.
</big></p>
<p style="font-family: arial;"><big>Motherhood thus becomes a holy
calling, a sacred dedication for carrying
out the Lord's plans, a consecration of devotion to the uprearing and
fostering,
the nurturing in body, mind, and spirit, of those who kept their first
estate and who come to this earth for their second estate "to see if
they
will do all things whatsoever the Lord their God shall command them."
(Abraham
3:25 ) To lead them to keep their second estate is the work of
motherhood
and "they who keep their second estate shall have glory added upon
their
heads for ever and ever." (Abr. 3:25)
</big></p>
<p style="font-family: arial;"><big>This divine service of
motherhood can be rendered only by mothers. It
may not be passed to others. Nurses cannot do it; public nurseries
cannot
do it; hired help cannot do it--only mother, aided as much as may be by
the loving hands of father, brothers, and sisters, can give the full
needed
measure of watchful care. The mother who entrusts her child to the care
of others, that she may do non-motherly work, whether for gold, for
fame,
or for civic service, should remember that "a child left to himself
bringeth
his mother to shame." (Prov. 29: 15) In our day the Lord has said that
unless parents teach their children the doctrines of the Church "the
sin
be upon the heads of the parents." (D. &amp; C 68:25)
</big></p>
<p><big><span style="font-family: arial;">Motherhood is near to
divinity. It is the highest, holiest service to
be assumed by mankind. It places her who honors its holy calling and
service
next to the angels. To you mothers in Israel we say God bless and
protect
you, and give you the strength and courage, the faith and knowledge,
the
holy love and consecration to duty, that shall enable you to fill to
the
fullest measure the sacred calling which is yours. To you mothers and
mothers-to-be
we say: Be chaste, keep pure, live righteously, that your posterity to
the last generation may call you blessed. <br>
</span></big></p>
