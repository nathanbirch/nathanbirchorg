                        style="font-family: arial;">
                    <span style="font-family: arial;"><span style="font-weight: bold;">Spencer
                            W. Kimball</span> <span style="color: rgb(153, 153, 153);">(1895-1985)
                            - President</span></span><br style="font-family: arial;">
                </big>
                <div style="margin-left: 40px;"><big><span style="font-family: arial;">Sin
                            becomes a habit. Sin is intensely habit-forming and sometimes moves men
                            to the tragic point of no return. Without repentance there can be no
                            forgiveness, and without forgiveness all the blessings of eternity hang
                            in jeopardy. As the transgressor moves deeper and deeper in his sin,
                            and the error is entrenched more deeply and the will to change is
                            weakened, it becomes increasingly nearer hopeless and he skids down and
                            down until either he does not want to climb back up or he has lost the
                            power to do so. <small>(<span style="font-style: italic;">The
                                    Teachings of Spencer W. Kimball</span> [1982], p.83)</small></span></big><br
                        style="font-family: arial;">
                </div>
                <big><br style="font-family: arial;">
                    <span style="font-family: arial;"><span style="font-weight: bold;">Neal
                            A. Maxwell</span> <span style="color: rgb(153, 153, 153);">(1926-2004)
                            - Quorum of the Twelve Apostles</span></span><br
                        style="font-family: arial;">
                </big>
                <div style="margin-left: 40px;"><big><span style="font-family: arial;">Of
                            course, as individuals, we are free to choose! But wrong choices will
                            make us less free. Furthermore, erosive error gradually makes one less
                            and less of an individual. God and His prophets would spare us that
                            shrinkage.&nbsp; <small>(&#8220;Answer Me,&#8221; <span
                                    style="font-style: italic;">Ensign</span>, Nov. 1988, p. 32)</small></span></big><br
                        style="font-family: arial;">
                </div>
                <big><br style="font-family: arial;">
                    <span style="font-family: arial;"><span style="font-weight: bold;">Richard
                            G. Scott </span><span style="color: rgb(153, 153, 153);">- Quorum of
                            the Twelve Apostles</span></span><br style="font-family: arial;">
                </big>
                <div style="margin-left: 40px;"><big><span style="font-family: arial;">Why
                            has your moral agency been given to you? Only to live a pleasurable
                            life and to make choices to do the things you want to do? Or is there a
                            more fundamental reason&#8212;to be able to make the choices that will lead
                            you to fully implement your purpose for being here on earth and to
                            establish priorities in your life that will assure the development and
                            happiness the Lord wants you to receive.</span></big><br
                        style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            Recently I met an intelligent young man with great potential. He was
                            undecided about a mission. He has decided not to attend a university
                            now. In his free time he only does what he likes to do. He doesn&#8217;t work
                            because he doesn&#8217;t have to, and it would take time from pleasure. He
                            passed seminary classes without much thought of personally applying the
                            knowledge gained. I noted: &#8220;You are making choices today that appear to
                            give you what you want: an easy life, abundant enjoyment, and not much
                            sacrifice. You can do that for a while, yet every decision you make
                            narrows your future. You are eliminating possibilities and options.
                            There will come a time, and it won&#8217;t be too distant, where you are
                            going to spend the rest of your life doing things you don&#8217;t want to do,
                            in places you don&#8217;t want to be, because you have not prepared yourself.
                            You are not taking advantage of your opportunities.&#8221; <small>(&#8220;First
                                Things First,&#8221; <span style="font-style: italic;">Ensign</span>, May
                                2001, p. 6)</small></span></big><br style="font-family: arial;">
                </div>
                <big><br style="font-family: arial;">
                    <span style="font-family: arial;"><span style="font-weight: bold;">Robert
                            D. Hales</span> <span style="color: rgb(153, 153, 153);">- Quorum of
                            the Twelve Apostles</span></span><br style="font-family: arial;">
                </big>
                <div style="margin-left: 40px;"><big><span style="font-family: arial;">In
                            these latter days, as in the times of old, we must avoid being acted
                            upon by acting for ourselves to avoid evil. The Holy Ghost will prompt
                            us. Joseph was told to flee from Potiphar&#8217;s wife. Abraham obeyed the
                            commandment to flee out of the land of Ur. Lehi was instructed to flee
                            Jerusalem before it was destroyed. And to protect the Savior&#8217;s life,
                            Mary and Joseph were prompted to flee into Egypt.</span></big><br
                        style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            The promptings that come to us to flee evil reflect our Heavenly
                            Father&#8217;s understanding of our particular strengths and weaknesses and
                            His awareness of the unforeseen circumstances of our lives. When these
                            promptings come, they will not generally stop us in our tracks, for the
                            Spirit of God does not speak with a voice of thunder. The voice will be
                            as soft as a whisper, coming as a thought to our minds or a feeling in
                            our hearts. By heeding its gentle promptings, we will be protected from
                            the destructive consequences of sin.</span></big><br
                        style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            But if we ignore those promptings, the light of the Spirit will fade.
                            Our agency will be limited or lost, and we will lose the confidence and
                            ability to act. We will be &#8220;walking in [spiritual] darkness at
                            noon-day.&#8221; [D&amp;C 95:6] Then how easy it is to wander into strange
                            paths and become lost! How quickly we are bound in the chains of sin
                            spoken of by Lehi to his rebellious sons [see 2 Ne. 1:13].&nbsp; For
                            example, if we make choices that put us deeply in debt, we will lose
                            our agency to meet our wants and needs or to save for that inevitable
                            rainy day. If we choose to break the law, we may be put in prison,
                            where our agency is so limited that we cannot choose where we go, who
                            we see, or what we do. Spirit prison is very much like that. Therefore,
                            to retain our agency we must daily walk in the light of our Lord and
                            Savior and follow the path of obedience. It is the only path that leads
                            to our Father in Heaven. <small>(&#8220;To Act for Ourselves: The Gift and
                                Blessings of Agency,&#8221; <span style="font-style: italic;">Ensign</span>,
                                May 2006, 4&#8211;8)</small></span></big><br style="font-family: arial;">
                </div>
                <big><br>
                    <br style="font-family: arial;">
                </big>
                <div style="text-align: center;"><big style="color: rgb(0, 0, 153);"><big><span
                                style="font-family: arial;">Submission to God Brings </span><span
                                style="font-family: arial;">Greater Freedom and Agency</span></big></big><br
                        style="font-family: arial;">
                </div>
                <big><br style="font-family: arial;">
                    <span style="font-family: arial;"><span style="font-weight: bold;">D.
                            Todd Chirstofferson</span> <span style="color: rgb(153, 153, 153);">-
                            Quorum of the Twelve Apostles</span></span><br
                        style="font-family: arial;">
                </big>
                <div style="margin-left: 40px;"><big><span style="font-family: arial;">To
                            the secular world it seems a paradox that greater submission to God
                            yields greater freedom. The world looks at things through Korihor&#8217;s
                            lens, considering obedience to God&#8217;&#8217;s laws and ordinances to be
                            &#8220;bondage&#8221; (Alma 30:24, 27). So how do obedience and truth make us free?
                            We can easily think of some practical ways in which truth gives us the
                            ability to do things we otherwise could not do or to avoid disasters we
                            might otherwise suffer.</span></big><br style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            A young British girl learned in school about the characteristics of
                            water along a shoreline that signal the approach of a tsunami. Two
                            weeks later, on vacation with her family in Thailand, she observed
                            those phenomena and insistently warned her parents and the people
                            around her. They escaped to higher ground just in time when the
                            December 26, 2004, tsunami hit south Asia. More than a hundred people
                            owe their lives to that girl&#8217;s knowledge of certain truths of the
                            natural world. [See &#8220;Girl Honored for Saving Lives with Pre-Tsunami
                            Warning,&#8221; Deseret Morning News, Dec. 27, 2005, A2.]</span></big><br
                        style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            But the Lord&#8217;s statement that the truth will make us free has broader
                            significance. &#8220;Truth,&#8221; He tells us, &#8220;is knowledge of things as they
                            are, and as they were, and as they are to come&#8221; (D&amp;C 93:24).
                            Possession of this knowledge of things past, present, and future is a
                            critical element of God&#8217;s glory: &#8220;The glory of God is intelligence, or,
                            in other words, light and truth&#8221; (D&amp;C 93:36). Does anyone doubt
                            that, as a consequence of possessing all light and truth, God possesses
                            ultimate freedom to be and to do?</span></big><br
                        style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            Likewise, as our understanding of gospel doctrine and principles grows,
                            our agency expands. First, we have more choices and can achieve more
                            and receive greater blessings because we have more laws that we can
                            obey. Think of a ladder&#8211;each new law or commandment we learn is like
                            one more rung on the ladder that enables us to climb higher. Second,
                            with added understanding we can make more intelligent choices because
                            we see more clearly not only the alternatives but also their potential
                            outcomes. As Professor Daniel H. Ludlow once expressed it, &#8220;The extent
                            of our individual &#8230; agency &#8230; is in direct proportion to the number and
                            kind of laws we know and keep.&#8221; [Daniel H. Ludlow, &#8220;Moral Free Agency,&#8221;
                            in BYU Speeches of the Year 1974 (1975), 182]</span></big><br
                        style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            The Lord promises that if, in the exercise of our agency, we follow His
                            example and always do those things that please Him and the Father, then
                            we will come to know and understand all things:</span></big><br
                        style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            &#8220;And if your eye be single to my glory, your whole bodies shall be
                            filled with light, and there shall be no darkness in you; and that body
                            which is filled with light comprehendeth all things&#8221; (D&amp;C 88:67).</span></big><br
                        style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            &#8220;That which is of God is light; and he that receiveth light, and
                            continueth in God, receiveth more light; and that light groweth
                            brighter and brighter until the perfect day&#8221; (D&amp;C 50:24).</span></big><br
                        style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            &#8220;He that keepeth [God&#8217;s] commandments receiveth truth and light, until
                            he is glorified in truth and knoweth all things&#8221; (D&amp;C 93:28).</span></big><br
                        style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            &#8220;And the Spirit giveth light to every man that cometh into the world;
                            and the Spirit enlighteneth every man through the world, that
                            hearkeneth to the voice of the Spirit.</span></big><br
                        style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            &#8220;And every one that hearkeneth to the voice of the Spirit cometh unto
                            God, even the Father&#8221; (D&amp;C 84:46&#8211;47).</span></big><br
                        style="font-family: arial;">
                    <big><span style="font-family: arial;">&nbsp;<br>
                            These are magnificent promises: to be filled with light and truth, to
                            comprehend all things, to be glorified in truth and know all things,
                            and to come even unto the Father. I have no doubt regarding the literal
                            fulfillment of these promises in those who exercise their agency to
                            choose obedience, but along with you, I recognize that they are not
                            realized in a day. Much obedience and experience are required before we
                            enjoy a fulness. We should, however, be encouraged by what John said of
                            the Savior:</span></big><br style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            &#8220;And I, John, saw that he received not of the fulness at the first, but
                            received grace for grace;</span></big><br style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            &#8220;And he received not of the fulness at first, but continued from grace
                            to grace, until he received a fulness&#8221; (D&amp;C 93:12&#8211;13). (&#8220;Moral
                            Agency,&#8221; <span style="font-style: italic;">Ensign</span>, Jun 2009, p.
                            46)<br>
