                                    style="font-family: arial;">Difference Between Agency and Liberty</span><br>
                            </big></big></big></div>
                <br>
                <div style="text-align: center;"><big style="color: rgb(0, 0, 153);"><big><span
                                style="font-family: arial;">There is a Difference Between&nbsp; </span><span
                                style="font-family: arial;">Agency and Freedom/Liberty</span></big></big><br
                        style="font-family: arial;">
                </div>
                <br style="font-family: arial;">
                <big><span style="font-family: arial;"><span style="font-weight: bold;">Marion
                            G. Romney</span> <span style="color: rgb(153, 153, 153);">(1897-1988)
                            - First Presidency</span></span><br style="font-family: arial;">
                </big>
                <div style="margin-left: 40px;"><big><span style="font-family: arial;">While
                            perhaps it is seldom, if ever, contended that either political
                            independence or economic freedom alone brings perfect liberty, it is
                            not, however, uncommon for free agency to be considered as synonymous
                            with freedom of the soul. And it is true that the God-given right to
                            choose one&#8217;s course of action is an indispensable prerequisite to such
                            freedom. Without it we can scarcely enjoy any type of
                            liberty&#8212;political, economic, or personal. It is one of our greatest
                            heritages. ...</span><br style="font-family: arial;">
                        <span style="font-family: arial;"><br>
                            Free agency, however, precious as it is, is not of itself the perfect
                            liberty we seek, nor does it necessarily lead thereto. As a matter of
                            fact, through the exercise of their agency more people have come to
                            political, economic, and personal bondage than to liberty.</span><br
                            style="font-family: arial;">
                        <span style="font-family: arial;"><br>
                            The Nephites, for example, at one time, by the exercise of their
                            agency, brought themselves to such a state of affairs that their only
                            course led to political bondage. This they did while living under a
                            government providing for the freest exercise of agency. &#8220;Their laws and
                            their governments,&#8221; says the record, &#8220;were established by the voice of
                            the people, and they who chose evil were more numerous than they who
                            chose good.&#8221; Therefore, &#8220;they could not be governed by the law nor
                            justice, save it were to their destruction.&#8221; (Hel. 5:2&#8211;3.) Under these
                            circumstances, they chose as rulers wicked men, who would certainly
                            destroy their political liberties, to replace righteous men who had in
                            the past protected and preserved those liberties and would have
                            continued to do so in the future. ...</span><br
                            style="font-family: arial;">
                        <span style="font-family: arial;"><br>
                            With respect to the loss of personal liberty through the misuse of free
                            agency, our daily lives are filled with tragic evidence. We see the
                            alcoholic with his craving for drink, the dope fiend in his frenzy, and
                            worse, the pervert with his irretrievable loss of manhood. Who will say
                            that such persons enjoy liberty?</span><br style="font-family: arial;">
                        <span style="font-family: arial;"><br>
                            Notwithstanding the fact that through its misuse, political, economic,
                            and personal liberty are lost, free agency will always endure because
                            it is an eternal principle. However, the free agency possessed by any
                            one person is increased or diminished by the use to which he puts it.
                            Every wrong decision one makes restricts the area in which he can
                            thereafter exercise his agency. The further one goes in the making of
                            wrong decisions in the exercise of free agency, the more difficult it
                            is for him to recover the lost ground. One can, by persisting long
                            enough, reach the point of no return. He then becomes an abject slave.
                            By the exercise of his free agency, he has decreased the area in which
                            he can act, almost to the vanishing point. ...</span><br
                            style="font-family: arial;">
                        <span style="font-family: arial;"><br>
                            These poor souls have placed themselves in the power of Lucifer and his
                            followers, who, as you remember, became Perdition. (See D&amp;C 76:26.)
                            Their final fate is to be cast out into outer darkness, such punishment
                            being the natural consequence of the alternatives they elected in the
                            exercise of their agency. The fact that they were originally endowed by
                            their Creator with free agency does not save them from the most awful
                            bondage, the bondage of sin.</span><br style="font-family: arial;">
                        <span style="font-family: arial;"><br>
                            Just as following wrong alternatives restricts free agency and leads to
                            slavery, so pursuing correct alternatives widens the scope of one&#8217;s
                            agency and leads to perfect liberty. As a matter of fact, one may, by
                            this process, obtain freedom of the soul while at the same time being
                            denied political, economic, and personal liberty.&nbsp; For example,
                            consider the Prophet Joseph Smith. Here was a man enjoying freedom of
                            the soul while suffering the deprivation of almost every other liberty.
                            <small>(&#8220;The Perfect Law of Liberty,&#8221; <span style="font-style: italic;">Ensign</span>,
                                Nov. 1981, 43&#8211;45)</small></span><br style="font-family: arial;">
                    </big></div>
                <big><br>
                    <br style="font-family: arial;">
                    <span style="font-family: arial;"><span style="font-weight: bold;">Dallin
                            H. Oaks</span> <span style="color: rgb(153, 153, 153);">- Quorum of
                            the Twelve Apostles</span></span><br style="font-family: arial;">
                </big>
                <div style="margin-left: 40px;"><big><span style="font-family: arial;">First,
                            because free agency is a God-given precondition to the purpose of
                            mortal life, no person or organization can take away our free agency in
                            mortality.</span></big><br style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            Second, what can be taken away or reduced by the conditions of
                            mortality is our freedom, the power to act upon our choices.&nbsp; Free
                            agency is absolute, but in circumstances of mortality freedom is always
                            qualified.</span></big><br style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            Freedom may be qualified or taken away (1) by physical laws, including
                            the physical limitations with which we are born, (2) by our own
                            actions, and (3) by the actions of others, including governments.</span></big><br
                        style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            Lehi taught his son Jacob that &#8220;men are free [have freedom] according
                            to the flesh&#8221; (2 Nephi 2:27).&nbsp; For example, in the flesh we are
                            subject to the physical law of gravity.&nbsp; If I should hang from the
                            catwalk in the Marriott Center and release my grip, I would not be free
                            to will myself into a soft landing.&nbsp; And I cannot choose to run
                            through a brick wall.</span></big><br style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            A loss of freedom reduces the extent to which we can act upon our
                            choices, but it does not deprive us of our God-given free agency. ...</span></big><br
                        style="font-family: arial;">
                    <big><span style="font-family: arial;"><br>
                            Interferences with our freedom do not deprive us of our free
                            agency.&nbsp; When Pharaoh put Joseph in prison, he restricted Joseph&#8217;s
                            freedom, but he did not take away his free agency.&nbsp; When Jesus
                            drove the money changers out of the temple, he interfered with their
                            freedom to engage in a particular activity at a particular time in a
                            particular place, but he did not take away their free agency.&nbsp; <small>(&#8220;Free
                                Agency and Freedom,&#8221; in The Book of Mormon: Second Nephi, The Doctrinal
                                Structure [Eds. Monte S. Nyman and Charles D. Tate, Jr. Religious
                                Studies Center, BYU, Provo Utah, 1989] pp. 9-11.)</small></span></big><br
                        style="font-family: arial;">
                </div>
                <big><br style="font-family: arial;">
                </big>
                <div style="text-align: center;"><big style="color: rgb(0, 0, 153);"><big><span
                                style="font-family: arial;"><br>
                                The Exercise of Agency Brings </span><span style="font-family: arial;">Greater
                                or Lesser Freedom</span></big></big><br style="font-family: arial;">
                </div>
                <big><br style="font-family: arial;">
                    <span style="font-family: arial;"><span style="font-weight: bold;">Marion
                            G. Romney</span> <span style="color: rgb(153, 153, 153);">(1897-1988)
                            - First Presidency</span></span><br style="font-family: arial;">
                </big>
                <div style="margin-left: 40px;"><big><span style="font-family: arial;">&#8226;&nbsp;&nbsp;&nbsp;
                            Every choice one makes either expands or contracts the area in which he
                            can make and implement future decisions. When one makes a choice, he
                            irrevocably binds himself to accept the consequences of that choice. <small>(Conference
                                Report, October 1968, p.65)</small></span></big><br
                        style="font-family: arial;">
                    <br style="font-family: arial;">
                    <big><span style="font-family: arial;">&#8226;&nbsp;&nbsp;&nbsp; [T]he free
                            agency possessed by any one person is increased or diminished by the
                            use to which he puts it. Every wrong decision one makes restricts the
                            area in which he can thereafter exercise his agency. The further one
                            goes in the making of wrong decisions in the exercise of free agency,
                            the more difficult it is for him to recover the lost ground. One can,
                            by persisting long enough, reach the point of no return. He then
                            becomes an abject slave. By the exercise of his free agency, he has
                            decreased the area in which he can act, almost to the vanishing point.
                            . . . Just as following wrong alternatives restricts free agency and
                            leads to slavery, so pursuing correct alternatives widens the scope of
                            one&#8217;s agency and leads to perfect liberty. As a matter of fact, one
                            may, by this process, obtain freedom of the soul while at the same time
                            being denied political, economic, and personal liberty. <small>(&#8220;The
                                Perfect Law of Liberty,&#8221; Ensign, Nov. 1981, p. 45)</small></span></big><br
                        style="font-family: arial;">
                </div>
