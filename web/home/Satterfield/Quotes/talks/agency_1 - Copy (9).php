                <div style="margin-left: 40px;"><big><span style="font-family: arial;">The
                            promptings that come to us to flee evil reflect our Heavenly Father&#8217;&#8217;s
                            understanding of our particular strengths and weaknesses and His
                            awareness of the unforeseen circumstances of our lives. When these
                            promptings come, they will not generally stop us in our tracks, for the
                            Spirit of God does not speak with a voice of thunder. The voice will be
                            as soft as a whisper, coming as a thought to our minds or a feeling in
                            our hearts. By heeding its gentle promptings, we will be protected from
                            the destructive consequences of sin. ...<br>
                            <br>
                        </span></big><big><span style="font-family: arial;"></span></big></div>
                <div style="margin-left: 40px;"><big><span style="font-family: arial;">To
                            all who desire to enjoy the supernal blessings of agency, I testify
                            that agency is strengthened by our faith and obedience. Agency leads us
                            to act: to seek that we may find, to ask that we may receive guidance
                            from the Spirit, to knock on that door that leads to spiritual light
                            and ultimately salvation. I bear special witness that our Savior Jesus
                            Christ is the source of that light, even the Light and Life of the
                            World. As we use our agency to follow Him, His light will grow within
                            us brighter and brighter until that perfect day [D&amp;C 50:24] when we
                            are welcomed into the presence of our Father in Heaven for all
                            eternity. <small>(&#8220;To Act for Ourselves: The Gift and Blessings of
                                Agency,&#8221; <span style="font-style: italic;">Ensign</span>, May 2006,
