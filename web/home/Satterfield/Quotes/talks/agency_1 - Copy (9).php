                <div style="margin-left: 40px;"><big><span style="font-family: arial;">Narcotic
                            addiction serves the design of the prince of darkness, for it disrupts
                            the channel to the holy spirit of truth. At present, the adversary has
                            an unfair advantage. Addiction has the capacity to disconnect the human
                            will and nullify moral agency. It can rob one of the power to decide.
                            Agency is too fundamental a doctrine to be left in such jeopardy. <small>(&#8220;Revelation
                                in a Changing World,&#8221; <span style="font-style: italic;">Ensign</span>,
                                Nov. 1989, p.14)</small></span></big><br>
                    <big><span style="font-family: arial;"></span></big></div>
                <big><span style="font-family: arial;"><br>
                        <span style="font-weight: bold;">Robert D. Hales</span> <span
                            style="color: rgb(153, 153, 153);">- Quorum of the Twelve Apostles</span><br>
                    </span></big>
                <div style="margin-left: 40px;"><big><span style="font-family: arial;">Warning
                            lights of a personal nature are activated for many reasons. For
                            example, the use of alcohol, tobacco, drugs, or pornography would turn
                            on warning lights, because when we choose to use these substances, we
                            become slaves and our moral agency is limited. We must be prepared with
                            preconditioned responses to reject these things when they are offered
                            to us, or we will jeopardize our right to have the Spirit to guide us
                            and direct us and our ability to return to our Heavenly Father with
                            honor. <small>(&#8220;Return with Honor,&#8221; <span style="font-style: italic;">Ensign</span>,
                                June 1999, p. 10)</small></span></big><br>
                    <big><span style="font-family: arial;"></span></big></div>
                <big><span style="font-family: arial;"><br>
                        <br>
                    </span></big>
                <div style="text-align: center;"><big style="color: rgb(0, 0, 153);"><big><span
                                style="font-family: arial;">Resources to Guide Us in the Wise Use of
                                Agency</span></big></big><br>
                    <big><span style="font-family: arial;"></span></big></div>
                <big><span style="font-family: arial;"><br style="font-weight: bold;">
                        <span style="font-weight: bold;">Spencer J. Condie</span><span
                            style="color: rgb(153, 153, 153);"> - of the Seventy</span><br>
                    </span></big>
                <div style="margin-left: 40px;"><big><span style="font-family: arial;">Our
                            loving Father in Heaven has given us many indispensable resources and
                            means to guide us in the wise exercise of our agency.</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            <span style="font-weight: bold;">The Gift of the Holy Ghost.</span> In
                            D&amp;C 33:16, the Lord revealed that &#8220;the power of [his] Spirit
                            quickeneth all things.&#8221; I believe that &#8220;all things&#8221; means all things.
                            He will not usurp or override our moral agency, but when given an
                            invitation, his Spirit will augment and accelerate our agency. When the
                            Spirit, the gift of the Holy Ghost, is given a chance to influence us,
                            decisions become easier and despair dissipates as solutions to our
                            challenges become clearly evident.</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            The Book of Mormon prophets make it very clear that the Holy Ghost is
                            willing to exert a very powerful influence in our lives when we are
                            responsive to his promptings. Nephi, Mormon, and Ether explained that
                            the Spirit strives with us to guide our lives on righteous paths (see 2
                            Ne. 26:11; Morm. 5:16; Ether 2:15). Moroni proclaimed that the Spirit
                            persuades us to do good (see Ether 4:11&#8211;12). Amulek taught that the
                            Holy Ghost contends with us to do that which is right (see Alma 34:38),
                            and King Benjamin explained that the Holy Ghost entices us to be
                            righteous (see Mosiah 3:19).</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            The promptings of the Spirit were never intended to supplant our moral
                            agency, but the Spirit will underscore preferable options in our
                            behavior and clarify a certain course of action in our hearts and minds.</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            <span style="font-weight: bold;">The Savior&#8217;s Example.</span> Jesus
                            Christ set the perfect example for us. Without boasting, he
                            acknowledged that &#8220;the Father hath not left me alone; for I do always
                            those things that please him&#8221; (John 8:29). He has extended to each of
                            us the invitation to &#8220;learn of me, and listen to my words; walk in the
                            meekness of my Spirit, and you shall have peace in me&#8221; (D&amp;C 19:23).</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            <span style="font-weight: bold;">Scripture.</span> Another
                            indispensable resource that assists us in using our agency wisely is
                            holy scripture. The Apostle Paul explained, &#8220;All scripture is given by
                            inspiration of God, and is profitable for doctrine, for reproof, for
                            correction, for instruction in righteousness&#8221; (2 Tim. 3:16). Nephi gave
                            the additional prophetic promise that when we &#8220;feast upon the words of
                            Christ, &#8230; the words of Christ will tell [us] all things what [we]
                            should do&#8221; (2 Ne. 32:3). In short, the scriptures are our life script,
                            our instruction manual in mortality, if you will. But of course, if the
                            universal teachings in holy writ are to benefit us, we must follow
                            Nephi&#8217;s additional counsel to &#8220;liken all scriptures unto us, that it
                            might be for our profit and learning&#8221; (1 Ne. 19:23).</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            <span style="font-weight: bold;">Testimony. </span>We learn in the
                            book of Revelation that during the war in heaven, those who overcame
                            Satan and his followers did so &#8220;by the blood of the Lamb, and by the
                            word of their testimony&#8221; (see Rev. 12:7&#8211;11). A testimony was an
                            invaluable weapon in the war in heaven, and it is an indispensable
                            weapon here on earth.</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            A testimony that is continually being nourished and is continually
                            growing will help us at every crossroad when important decisions are
                            made. Indeed, a testimony supplants the need to make certain decisions
                            under fire, because we already know well in advance the course of
                            action we will take.</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp; <br>
                            <span style="font-weight: bold;">&nbsp;Power of Prayer.</span> The
                            Apostle James eloquently observed that &#8220;the effectual fervent prayer of
                            a righteous man availeth much&#8221; (James 5:16). Alma the Elder would
                            certainly be an ardent advocate of this statement by James, for when
                            the angel appeared to Alma&#8217;s wayward son, he explained to Alma the
                            Younger that &#8220;the Lord hath heard the prayers of his people, and also
                            the prayers of his servant, Alma, who is thy father; for he has prayed
                            with much faith concerning thee&#8221; (Mosiah 27:14).</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp; <br>
                            &nbsp;All prayers are, indeed, answered, and it is well to remember
                            that sometimes the answer is not in the affirmative. As the Son of God
                            prayed in the Garden of Gethsemane that the bitter cup might pass, an
                            affirmative reply would have thwarted the entire plan of salvation. But
                            the Only Begotten Son demonstrated his meekness and humility and
                            obedience as he added: &#8220;Nevertheless not my will, but thine, be done&#8221;
                            (Luke 22:42). The divine answer was no, but &#8220;there appeared an angel
                            unto him from heaven, strengthening him&#8221; (Luke 22:43). This is
                            certainly a prototype and a promise for each of us as we too are
                            required to drink from bitter cups in our lives. We will not be left
                            comfortless.</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            <span style="font-weight: bold;">Fasting. </span>There are great
                            blessings promised to those who fast. Through the prophet Isaiah, the
                            Lord posed an important question, followed by profound promises: &#8220;Is
                            not this the fast that I have chosen? to loose the bands of wickedness,
                            to undo the heavy burdens, and to let the oppressed go free, and that
                            ye break every yoke?&#8221; (Isa. 58:6.)</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            When we subordinate our physical needs and desires to the dictates of
                            the Spirit, we tap into a spiritual strength beyond our own. If we are
                            in bondage to bad habits or unkind thoughts, we can break the bands of
                            weakness or wickedness through fasting. Our hearts will be filled with
                            love and forgiveness, and we can get on with our lives after having
                            broken &#8220;every yoke.&#8221;</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            <span style="font-weight: bold;">Ordinances.</span> Ordinances are
                            outward manifestations of inner covenants, commitments, and promises.
                            Ordinances are not optional on the pathway to perfection. These include
                            baptism and confirmation (see John 3:5; 2 Ne. 31:5&#8211;12); ordination to
                            the Melchizedek Priesthood, for all males (see D&amp;C 84:33&#8211;42); the
                            temple endowment and the sealing ordinance (see D&amp;C 132:15&#8211;24).</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            Participation in ordinances helps us use our agency wisely and well,
                            and a constant commitment to covenants spares us the emotional energy
                            required to decide and re-decide what we are going to do each time we
                            face temptation. Each week, through the ordinance of the sacrament, we
                            solemnly covenant to &#8220;always remember him&#8221; (Moro. 4:3; Moro. 5:2;
                            emphasis added).</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            <span style="font-weight: bold;">Living Prophets. </span>A loving
                            Heavenly Father has provided us with living prophets to receive and to
                            help us understand his mind and will. In speaking of his servants, the
                            Savior said: &#8220;And whatsoever they shall speak when moved upon by the
                            Holy Ghost shall be scripture, shall be the will of the Lord, shall be
                            the mind of the Lord, shall be the word of the Lord, shall be the voice
                            of the Lord, and the power of God unto salvation&#8221; (D&amp;C 68:4).
                            Elsewhere he declared that &#8220;whether by mine own voice or by the voice
                            of my servants, it is the same&#8221; (D&amp;C 1:38).</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            We sustain the fifteen men serving today in the First Presidency and in
                            the Quorum of the Twelve Apostles as prophets, seers, and revelators.
                            They are watchmen on the tower who point out the course of action we
                            should take and who see beyond the bend in the road and beyond the
                            horizon.</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            <span style="font-weight: bold;">Patriarchal Blessings</span>.
                            President Ezra Taft Benson encouraged every youth to receive a
                            patriarchal blessing and admonished, &#8220;Study it carefully and regard it
                            as personal scripture for you&#8212;for that is what it is. A patriarchal
                            blessing is the inspired and prophetic statement of your life&#8217;s
                            mission, together with blessings, cautions, and admonitions as the
                            patriarch may be prompted to give&#8221; (<span style="font-style: italic;">Ensign</span>,
                            May 1986, p. 43).<small> [From &#8220;Agency: The Gift of Choices,&#8221; <span
                                    style="font-style: italic;">Ensign</span>, Sept. 1995, pp.
                                21-22]&nbsp;&nbsp;&nbsp; </small></span></big><br>
