                <div style="margin-left: 40px;"><big><span style="font-family: arial;">What
                            is it that will enable one man to govern his fellows aright? It is just
                            as Joseph Smith said to a certain man who asked him,&#8221;How do you govern
                            such a vast people as this?&#8221; &#8220;Oh,&#8221; says Joseph, &#8220;it is very easy.&#8221;
                            &#8220;Why,&#8221; says the man &#8220;but we find it very difficult.&#8221; &#8220;But,&#8221; said
                            Joseph, &#8220;it is very easy, for I teach the people correct principles,
