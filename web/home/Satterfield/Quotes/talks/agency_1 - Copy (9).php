                    </span></big>
                <div style="margin-left: 40px;"><big><span style="font-family: arial;">I
                            am convinced that of all governments the greatest and the best is that
                            government in which the people govern themselves. The Prophet Joseph
                            Smith was asked by a stranger visitor at Nauvoo, &#8220;How do you govern
                            these people, these Americans, these Britons, these Scandinavians,
                            these men from all parts of the world, all nationalities, speaking
                            different languages, having different customs and traditions,&#8211;how do
                            you govern them, that they live together in peace, with a common
                            purpose, and in the spirit of unity?&#8221; The Prophet sagely answered--and
                            he never said a wiser thing--&#8220;I teach them correct principles, and they
                            govern themselves.&#8221; That government in which the people can and do
                            govern themselves by obedience to correct principles, is manifestly
                            superior to any government that depends upon one man&#8217;s will. The word
                            of God declares, &#8220;It is a slothful servant who waits to be commanded in
                            all things.&#8221; Even in a government where God might command and direct in
                            everything, the condition would be inferior to what it would be in a
                            community of enlightened freemen, enjoying the fullness of the Gospel,
                            filled with the knowledge of the heavens, doing good of their own
                            accord, governing themselves, loving their neighbors and doing all
                            things with an eye single to the glory of God. <small>(Conference
                                Report, April 1909, p.76)</small></span></big><small><br>
                    </small><span style="font-family: arial;"></span></div>
                <big><span style="font-family: arial;"><br>
                        <span style="font-weight: bold;">Boyd K. Packer</span> <span
                            style="color: rgb(153, 153, 153);">- Quorum of the Twelve Apostles</span><br>
                    </span></big>
                <div style="margin-left: 40px;"><big><span style="font-family: arial;">Several
                            weeks ago I had in my office a four-star general and his wife; they
                            were very impressive people. They admire the Church because of the
                            conduct of our youth. The general's wife mentioned her children, of
                            whom she is justly proud. But she expressed a deep concern. "Tell me,"
                            she said, "how you are able to control your youth and build such
                            character as we have seen in your young men?&#8221;</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp; <br>
                            I was interested in her use of the word 'control'. The answer, I told
                            them, centered in the doctrines of the gospel. They were interested; so
                            I spoke briefly of the doctrine of agency. I said we develop control by
                            teaching freedom. Perhaps at first they thought we start at the wrong
                            end of the subject. A four-star general is nothing if not a
                            disciplinarian. But when one understands the gospel, it becomes very
                            clear that the best control is self-control.</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            It may seem unusual at first to foster self-control by centering on
                            freedom of choice, but it is a very sound doctrinal approach.</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            While either subject may be taught separately, and though they may
                            appear at first to be opposites, they are in fact parts of the same
                            subject.</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            Some who do not understand the doctrinal part do not readily see the
                            relationship between obedience and agency. And they miss one vital
                            connection and see obedience only as restraint. They then resist the
                            very thing that will give them true freedom. There is no true freedom
                            without responsibility, and there is no enduring freedom without a
                            knowledge of the truth. The Lord said, "If ye continue in my word, then
                            are ye my disciples indeed; and ye shall know the truth, and the truth
                            shall make you free." (John 8:31-32.)</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            The general quickly understood a truth that is missed even by some in
                            the Church. Latter-day Saints are not obedient because they are
                            compelled to be obedient. They are obedient because they know certain
                            spiritual truths and have decided, as an expression of their own
                            individual agency, to obey the commandments of God.</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            We are the sons and daughters of God, willing followers, disciples of
                            the Lord Jesus Christ, and "under this head are [we] made free."
                            (Mosiah 5:8.)</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            Those who talk of blind obedience may appear to know many things, but
                            they do not understand the doctrines of the gospel. There is an
                            obedience that comes from a knowledge of the truth that transcends any
                            external form of control. We are not obedient because we are blind, we
                            are obedient because we can see. The best control, I repeat, is
                            self-control.</span></big><br>
                    <big><span style="font-family: arial;">&nbsp;&nbsp;&nbsp; <br>
                            The general knew then why we teach our children the doctrines of the
                            gospel of Jesus Christ and where they get the resolute determination to
                            protect individual freedom. <small>(&#8220;Agency and Control,&#8221; <span
                                    style="font-style: italic;">Ensign</span>, May 1983, p.66)</small></span></big><small><br>
