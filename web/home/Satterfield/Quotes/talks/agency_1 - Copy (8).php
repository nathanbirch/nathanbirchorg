                                    began, "Elder Scott, this is my father. I love him. He's concerned
                                    about some things that happened in my early childhood. They are no
                                    longer a problem for me. Could you help him?" What a powerful
                                    confirmation of the Savior's capacity to heal! She no longer suffered
                                    from the consequences of abuse, because she had adequate understanding
                                    of His Atonement, sufficient faith, and was obedient to His law. As you
                                    conscientiously study the Atonement and exercise your faith that Jesus
                                    Christ has the power to heal, you can receive the same blessed relief.
