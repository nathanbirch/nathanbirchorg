                                    recognizes that healing comes through the unwavering love of Heavenly
                                    Father for each of His children. He also understands that the power of
                                    healing is inherent in the Atonement of Jesus Christ. Therefore, his
                                    strategy is to do all possible to separate you from your Father and His
                                    Son. Do not let Satan convince you that you are beyond help.</span></big><br
