                                    that agency improperly, causing serious problems to others. Some would
                                    even violate sacred trust, such as a father or family member abusing an
                                    innocent child. Since our Heavenly Father is completely just, there has
                                    to be a way of overcoming the tragic consequences of such damaging use
                                    of agency for both the victim and the perpetrator. That secure healing
                                    comes through the power of the Atonement of His Beloved Son, Jesus
                                    Christ, to rectify that which is unjust. Faith in Jesus Christ and in
                                    His power to heal provides the abused with the means to overcome the
                                    terrible consequences of another's unrighteous acts. When combined with
                                    complete repentance, the Atonement also affords the perpetrator a way
                                    to avoid the severe punishment the Lord has decreed for such acts.</span></big><br
