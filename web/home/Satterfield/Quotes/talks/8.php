               <center><i><font size=+3>The Wentworth Letter</font></i>
<p>Joseph Smith
<br><i>History of the Church</i>, Vol.4: pp.536-541</center>

<p><i>1 March 1, 1842.--At the request of Mr. John Wentworth, Editor and
Proprietor of the Chicago Democrat, I have written the following sketch
of the rise, progress, persecution, and faith of the Latter-day Saints,
of which I have the honor, under God, of being the founder. Mr. Wentworth
says that he wishes to furnish Mr. Bastow, a friend of his, who is writing
the history of New Hampshire, with this document. As Mr. Bastow has taken
the proper steps to obtain correct information, all that I shall ask at
his hands, is, that he publish the account entire, ungarnished, and without
misrepresentation</i>.
<p>I was born in the town of Sharon, Windsor County, Vermont, on the 23rd
of December, A.D. 1805. When ten years old, my parents removed to Palmyra,
New York, where we resided about four years, and from thence we removed
to the town of Manchester. My father was a farmer and taught me the art
of husbandry. When about fourteen years of age, I began to reflect upon
the importance of being prepared for a future state, and upon inquiring
[about] the plan of salvation, I find that there was a great clash in religious
sentiment; if I went to one society they referred me to one plan, and another
to another; each one pointing to his own particular creed as the summum
bonum of perfection. Considering that all could not be right, and that
God could no be the author of so much confusion, I determined to investigate
the subject more fully, believing that if God had a Church it would not
be split up into factions, and that if He taught one society to worship
one way, and administer in one set of ordinances, He would not teach another,
principles which were diametrically opposed.
<p>Believing the word of God, I had confidence in the declaration of James
-- "If any of you lack wisdom, let him ask of God, that giveth to all men
liberally, and upbraideth not; and it shall be given him." I retire to
a secret place in a grove, and began to call upon the Lord; while fervently
engaged in supplication, my mind was taken away from the objects with which
I was surrounded, and I was enwrapped in a heavenly vision, and saw two
glorious personages, who exactly resembled each other in features and likeness,
surrounded with a brilliant light which eclipsed the sun at noon day. They
told me that all religious denominations were believing in incorrect doctrines,
and that none of them was acknowledged of God as His Church and kingdom:
and was expressly commanded "to go not after them," at the same time receiving
a promise that the fullness of the Gospel should at some future time be
made known unto me.
<p>On the evening on the 21st of September, A.D. 1823, while I was praying
unto God, and endeavoring to exercise faith in the precious promises of
Scripture, on a sudden a light like that of day, only of a far purer and
more glorious appearance and brightness, burst into the room indeed the
first sight was as though the house was filled with consuming fire; the
appearance produced a shock that affected the whole body; in a moment a
personage stood before me surrounded with a glory yet greater than that
with which I was already surrounded. This messenger proclaimed himself
to be an angel of God, sent to bring the joyful tidings that the covenant
which God made with ancient Israel was at hand to be fulfilled, that the
preparatory work for the second coming of the Messiah was speedily to commence;
that the time was at hand for the Gospel in all its fullness to be preached
in power, unto all nations that a people might be prepared for the Millennial
reign. I was informed that I was chosen to be an instrument in the hands
of God to bring about some of His purposes in this glorious dispensation.
<p>I was also informed concerning the aboriginal inhabitants of this country
and shown who they were, and from whence they came; a brief sketch of their
origin, progress, civilization, laws, governments, of their righteousness
and iniquity, and the blessings of God being finally withdrawn from them
as a people, was made known unto me; I was also told where were deposited
some plates on which were engraven an abridgment of the records of the
ancient Prophets that had existed on this continent. The angel appeared
to me three times the same night and unfolded the same things. After having
received many visits from the angels of God unfolding the majesty and glory
of the events that should transpire in the last days, on the morning of
the 22nd of September, A.D. 1827, the angel of the Lord delivered the records
into my hands.
<p>These records were engraven on plates which had the appearance of gold,
each plate was six inches wide and eight inches long, and not quite so
thick as common tin. They were filled with engravings, in Egyptian characters,
and bound together in a volume as the leaves of a book, with three rings
running through the whole. The volume was something near six inches in
thickness, a part of which was sealed. The characters on the unsealed part
were small, and beautifully engraved. The whole book exhibited many marks
of antiquity in its construction, and much skill in the art of engraving.
With the records was found a curious instrument, which the ancients called
"Urim and Thummim," which consisted of two transparent stones set in the
rim of a bow fastened to a breast plate. Through the medium of the Urim
and Thummim I translated the record by the gift and power of God.
<p>In this important and interesting book the history of ancient America
is unfolded, from its first settlement by a colony that came from the Tower
of Babel, at the confusion of languages to the beginning of the fifth century
of the Christian Era. We are informed by these records that America in
ancient times has been inhabited by two distinct races of people. The first
were called Jaredites, and came directly from the Tower of Babel. The second
race came directly from the city of Jerusalem, about six hundred years
before Christ. They were principally Israelites, of the descendants of
Joseph. The Jaredites were destroyed about the time that the Israelites
came from Jerusalem, who succeeded them in the inheritance of the country.
The principal nation of the second race fell in battle towards the close
of the fourth century. The remnant are the Indians that now inhabit this
country. This book also tells us that our Savior made His appearance upon
this continent after His resurrection; that He planted the Gospel here
in all its fulness, and richness, and power, and blessing; that they had
Apostles, Prophets, Pastors, Teachers, and Evangelists; the same order,
the same priesthood, the same ordinances, gifts, powers, and blessings,
as were enjoyed on the eastern continent, that the people were cut off
in consequence of their transgressions, that the last of their prophets
who existed among them was commanded to write an abridgment of their prophecies,
history,&amp;c, and to hide it up in the earth, and that it should come
forth and be united with the Bible for the accomplishment of the purposes
of God in the last days. For a more particular account I would refer to
the Book of Mormon, which can be purchased at Nauvoo, or from any of our
Traveling Elders.
<p>As soon as the news of this discovery was made known, false reports,
misrepresentation and slander flew, as on the wings of the wind, in every
direction; the house was frequently beset by mobs and evil designing persons.
Several times I was shot at, and very narrowly escaped, and every device
was made use of to get the plates away from me; but the power and blessing
of God attended me, and several began to believe my testimony.
<p>On the 6th of April, 1830, the "Church of Jesus Christ of Latter-day
Saints" was first organized in the town of Fayette, Seneca county, state
of New York. Some few were called and ordained by the Spirit of revelation
and prophecy, and began to preach as the Spirit gave them utterance, and
though weak, yet were they strengthened by the power of God, and many were
brought to repentance, were immersed in the water, and were filled with
the Holy Ghost by the laying on of hands. They saw visions and prophesied,
devils were cast out, and the sick healed by the laying on of hands. From
that time the work roiled forth with astonishing rapidity, and churches
were soon formed in the states of New York, Pennsylvania Ohio, Indiana,
Illinois, and Missouri; in the last named state a considerable settlement
was formed in Jackson county, numbers joined the Church and we were increasing
rapidly; we made large purchases of land, our farms teemed with plenty,
and peace and happiness were enjoyed in our domestic circle, and through,
out our neighborhood; but as we could not associate with our neighbors
(who were, many of them, of the basest of men, and had fled from the face
of civilized society, to the frontier country to escape the hand of justice,)
in their midnight revels, their Sabbath breaking, horse racing and gambling;
they commenced at first to ridicule, then to persecute and finally an organized
mob assembled and burned our houses, tarred and feathered and whipped many
of our brethren, and finally, contrary to law, justice and humanity, drove
them from their habitations; who, houseless and homeless, had to wander
on the bleak prairies till the children left the tracks of their blood
on the prairie. This took place in the month of November, and they had
no other covering but the canopy of heaven, in this inclement season of
the year; this proceeding was winked at by the government, and although
we had warrantee deeds for our land, and had violated no law, we could
obtain no redress.
<p>There were many sick, who were thus inhumanly driven from their houses,
and had to endure all this abuse and to seek homes where they could be
found. The result was, that a great many of them being deprived of the
comforts of life, and the necessary attendances, died; many children were
left orphans, wives, widows and husbands, widowers; our farms were taken
possession of by the mob, many thousands of cattle, sheep, horses and hogs
were taken, and our household goods, store goods, and printing press and
type were broken, taken, or otherwise destroyed,
<p>Many of our brethren removed to Clay county, where they continued until
1836, three years; there was no violence offered, but there were threatenings
of violence. But in the summer of 1836 these threatenings began to assume
a more serious form, from threats, public meetings were called, resolutions
were passed, vengeance and destruction were threatened, and affairs again
assumed a fearful attitude, Jackson county was a sufficient precedent,
and as the authorities in that county did not interfere they boasted that
they would not in this; which on application to the authorities we found
to be too true, and after much privation and loss of property, we were
again driven from our homes.
<p>We next settled in Caldwell and Daviess counties, where we made large
and extensive settlements, thinking to free ourselves from the power of
oppression, by settling in new counties, with very few inhabitants in them;
but here we were not allowed to live in peace, but in 1838 we were again
attacked by mobs, an exterminating order was issued by Governor Boggs,
and under the sanction of law, an organized banditti ranged through the
country, robbed us of our cattle, sheep hogs, &amp;c., many of our people
were murdered in cold blood, the chastity of our women was violated, and
we were forced to sign away our property at the point of the sword; and
after enduring every indignity that could be heaped upon us by an inhuman,
ungodly band of marauders, from twelve to fifteen thousand souls, men women,
and children were driven from their own firesides, and from lands to which
they had warrantee deeds, houseless, friendless, and homeless (in the depths
of winter) to wander as exiles on the earth, or to seek an asylum in a
more genial clime, and among a less barbarous people. Many sickened and
died in consequence of the cold and hardships they had to endure; many
wives were left widows, and children, orphans, and destitute. It would
take more time than is allotted me here to describe the injustice, the
wrongs, the murders the bloodshed, the theft, misery and woe that have
been caused by the barbarous, inhuman, and lawless proceedings of the state
of Missouri.
<p>In the situation before alluded to, we arrived in the state of Illinois
in 1839, where we found a hospitable people and a friendly home; a people
who were willing to be governed by the principles of law and humanity.
We have commenced to build a city called "Nauvoo," in Hancock county. We
number from six to eight thousand here, besides vast numbers in the county
around, and in almost every county of the state. We have a city charter
granted us, and charter for a Legion, the troops of which now number 1,500.
We have also a charter for a University, for an Agricultural and Manufacturing
Society, have our own laws and administrators, and possess all the privileges
that other free and enlightened citizens enjoy.
<p>Persecution has not stopped the progress of truth, but has only added
fuel to the flame, it has spread with increasing rapidity. Proud of the
cause which they have espoused, and conscious of our innocence, and of
the truth of their system, amidst calumny and reproach, have the Elders
of this Church gone forth, and planted the Gospel in almost every state
in the Union; it has penetrated our cities, it has spread over our villages,
and has caused thousands of our intelligent, noble, and patriotic citizens
to obey its divine mandates, and be governed by its sacred truths. It has
also spread into England. Ireland, Scotland, and Wales, where, in the year
1840, a few of our missionaries were sent, and over five thousand joined
the Standard of Truth; there are numbers now joining in every land.
<p>Our missionaries are going forth to different nations, and in Germany,
Palestine, New Holland, Australia, the East Indies, and other places, the
Standard of Truth has been erected; no unhallowed hand can stop the work
from progressing; persecutions may rage, mobs may combine, armies may assemble,
calumny may defame, but the truth of God will go forth boldly, nobly, and
independent, till it has penetrated every continent, visited every clime,
swept every country, and sounded in every ear, till the purposes of God
shall be accomplished, and the Great Jehovah shall say the work is done.
<br>&nbsp;
<p>We believe in God the eternal Father, and in His Son Jesus Christ, and
in the Holy Ghost.
<p>We believe that men will be punished for their own sins, and not for
Adam's transgression.
<p>We believe that through the atonement of Christ all mankind may be saved
by obedience to the laws and ordinances of the Gospel.
<p>We believe that the first principle and ordinances of the Gospel are:
(1) Faith in the Lord Jesus Christ; (2) Repentance; (3) Baptism by immersion
for the remission of sins; (4) Laying on of hands for the gift of the Holy
Ghost.
<p>We believe that a man must be called of God by prophecy and by the laying
on hands, by those who are in authority, to preach the Gospel and administer
in the ordinances thereof.
<p>We believe in the same organization that existed in the primitive Church,
viz: apostles, prophets, pastors, teachers, evangelists, etc.
<p>We believe in the gift of tongues, prophecy, revelation, visions, healing,
interpretation of tongues,etc.
<br>&nbsp;
<p>We believe the Bible to be the word of God, as far as it is translated
correctly; we also believe the Book of Mormon to be the word of God.
<p>We believe all that God has revealed, all that He does now reveal, and
we believe that He will yet reveal many great and important things pertaining
to the kingdom of God.
<p>We believe in the literal gathering of Israel and in the restoration
of the Ten Tribes: that Zion will be built upon this [the American] continent;
that Christ will reign personally upon the earth; and that the earth will
be renewed and receive its paradisiacal glory.
<p>We claim the privilege of worshiping Almighty God according to the dictates
of our own conscience, and allow all men the same privilege, let them worship
how, where, or what they may.
<p>We believe in being subject to kings, presidents, rulers and magistrates,
in obeying honoring, and sustaining the law.
<p>We believe in being honest, true, chaste, benevolent, virtuous, and
in doing good to all men; indeed we may say that we follow the admonition
of Paul, "We believe all thing, we hope all things, we have endured many
things, and hope to be able to endure all things. If there is anything
virtuous, lovely, or of good report, or praiseworthy, we seek after these
things.
<br>&nbsp;
<p>Respectfully, &amp;c., JOSEPH SMITH

