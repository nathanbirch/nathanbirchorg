<p style="margin-left: 40px;"><big><font face="Arial,Helvetica"><font size="+1"><big>Many years ago, large packs
of wolves roamed the countryside in Ukraine, making travel in that part
of the world very dangerous. These wolf packs were fearless. They were
not intimidated by people nor by any of the weapons available at that
time.
The only thing that seemed to frighten them was fire. Consequently,
travelers
who found themselves away from cities developed the common practice of
building a large bonfire and keeping it burning through the night. As
long
as the fire burned brightly, the wolves stayed away. But if it were
allowed
to burn out and die, the wolves would move in for an attack. Travelers
understood that building and maintaining a roaring bonfire was not just
a matter of convenience or comfort; it was a matter of survival. (See
Mary
Pratt Parrish,
<i>Ensign</i>, May 1972, p. 25.)</big></font></font>
</big></p>
<p style="margin-left: 40px;"><big><font face="Arial,Helvetica"><font size="+1"><big>We do not have to protect
ourselves from wolf packs as we travel the road of life today, but, in
a spiritual sense, we do face the devious wolves of Satan in the forms
of temptation, evil, and sin. We live in dangerous times when these
ravenous
wolves roam the spiritual countryside in search of those who may be
weak
in faith or feeble in their conviction. In his first epistle, Peter
described
our "adversary the devil, as a roaring lion [that] walketh about,
seeking
whom he may devour." (1 Pet. 5:8.) The Lord told the Prophet Joseph
Smith
that "enemies prowl around thee like wolves for the blood of the lamb."
(D&amp;C 122:6.) We are all vulnerable to attack. However, we can
fortify
ourselves with the protection provided by a burning testimony that,
like
a bonfire, has been built adequately and maintained carefully.</big></font></font>
</big></p>
<div style="margin-left: 40px;"><big><font face="Arial,Helvetica"><font size="+1"><big><u>Unfortunately, some in
the Church may believe sincerely that their testimony is a raging
bonfire
when it really is little more than the faint flickering of a candle</u>.
Their faithfulness has more to do with habit than holiness, and their
pursuit
of personal righteousness almost always takes a back seat to their
pursuit
of personal interests and pleasure. With such a feeble light of
testimony
for protection, these travelers on life's highways are easy prey for
the
wolves of the adversary. <small>("Spiritual Bonfires of Testimony,"
<i>Ensign</i>,
Nov. 1992, p. 34; underlining added)</small></big></font></font><small>
</small></big><br>
</div>
