<p style="margin-left: 40px;"><big><b><font face="Arial,Helvetica"><font size="+1"><big>From <i>Men and Women
of Christ</i>:</big></font></font></b>
</big></p>
<p style="margin-left: 80px;"><big><font face="Arial,Helvetica"><font size="+1"><big>If we enlist and take the
Savior's yoke upon us we "shall find rest unto [our] souls" (Matthew
11:29).
If we are only part-time soldiers, though, partially yoked, we
experience
quite the opposite: frustration, irritation, and the absence of His
full
grace and spiritual rest. In that case weaknesses persist and
satisfactions
are intermittent. . . Actually the partially yoked experience little
spiritual
satisfaction, because they are burdened by carrying the awful weight of
the natural man -- without any of the joys that come from progressing
toward
becoming "the man of Christ." They have scarcely "[begun] to be
enlightened"
(Alma 32:34). The meek and fully yoked, on the other hand, find God's
reassuring
grace and see their weakness yielding to strength (see Ether 12:27).</big></font></font>
</big></p>
<p style="margin-left: 80px;"><big><font face="Arial,Helvetica"><font size="+1"><big>Strange as it seems, a few
of the partially yoked, undeservedly wearing the colors of the kingdom,
are just close enough to the prescribed path and process to be able to
observe in others some of the visible costs of discipleship. Sobered by
that observation, they want victory without battle and expect campaign
ribbons merely for watching; but there is no witness until after the
trial
of their faith (see Ether 12:6).</big></font></font>
</big></p>
<div style="margin-left: 80px;"><big><font face="Arial,Helvetica"><font size="+1"><big><u>These same Church members
know just enough about the doctrines to converse superficially on them,
but their scant knowledge about the deep doctrines is inadequate for
deep
discipleship</u> (see 1 Corinthians 2:10). <u>Thus uninformed about
the
deep doctrines, they make no deep change in their lives</u>. They lack
the faith to "give place" (Alma 32:27) consistently for real
discipleship.
Such members move out a few hundred yards from the entrance to the
straight
and narrow path and repose on the first little rise, thinking, "Well,
this
is all there is to it"; and they end up living far below their
possibilities.
While not as distant as those King Benjamin described "For how knoweth
a man the master whom he has not served, and who is a stranger unto
him,
and is far from the thoughts and intents of his heart?" (Mosiah 5:13)
--
these people are not drawing closer either. <small>(<i>Men and Women of Christ</i> [1991], pp. 2-3)</small></big></font></font><small>
</small></big><br>
</div>
<p><big>&nbsp;
</big></p>
<div style="margin-left: 40px;"><big><font face="Arial,Helvetica"><font size="+1"><big><b>From "Overcome ... Even
As I Also Overcame"</b>:</big></font></font></big></div>

<p style="margin-left: 80px;"><big><font face="Arial,Helvetica"><font size="+1"><big>Events and circumstances
in the last days make it imperative for us as members of the Church to
become more grounded, rooted, established, and settled (see Col. 1:23;
Col. 2:7; 2 Pet. 1:12). Jesus said to His disciples, "settle this in
your
hearts, that ye will do the things which I shall teach, and command
you"
(JST Luke 14:28). If not so settled, the turbulence will be severe. If
settled, we will not be "tossed to and fro," whether by rumors, false
doctrines,
or by the behavioral and intellectual fashions of the world. Nor will
we
get caught up in the "talk show" mentality, spending our time like
ancient
Athenians "in nothing else, but either to tell, or to hear some new
thing"
(Acts 17:21). Why be concerned with the passing preferences of the
world
anyway? "For the fashion of this world passeth away" (1 Cor. 7:31). . .
.</big></font></font>
</big></p>
<p style="margin-left: 80px;"><big><font face="Arial,Helvetica"><font size="+1"><big>Some Church members, alas,
are neither reconciled to the will of God nor are they sufficiently
settled
as to their covenants. . . .</big></font></font>
</big></p>
<p style="margin-left: 80px;"><big><font face="Arial,Helvetica"><font size="+1"><big><u>Some give of their time
yet withhold themselves, being present without giving of their presence
and going through the superficial motions of membership instead of the
deep emotions of consecrated discipleship</u>.</big></font></font>
</big></p>
<p style="margin-left: 80px;"><big><font face="Arial,Helvetica"><font size="+1"><big><u>Some try to get by with
knowing only the headlines of the gospel</u>, not really talking much
of
Christ or rejoicing in Christ and esteeming lightly His books of
scripture
which contain and explain His covenants (see 2 Ne. 25:26).</big></font></font>
</big></p>
<p style="margin-left: 80px;"><big><font face="Arial,Helvetica"><font size="+1"><big>Some are so proud they never
learn of obedience and spiritual submissiveness. They will have very
arthritic
knees on the day when every knee shall bend. There will be no gallery
then
to play to; all will be participants!</big></font></font>
</big></p>
<p style="margin-left: 80px;"><big><font face="Arial,Helvetica"><font size="+1"><big>Maintaining Church membership
on our own terms, therefore, is not true discipleship.</big></font></font>
</big></p>
<p style="margin-left: 80px;"><big><font face="Arial,Helvetica"><font size="+1"><big>Real disciples absorb the
fiery darts of the adversary by holding aloft the quenching shield of
faith
with one hand, while holding to the iron rod with the other (see Eph.
6:16;
1 Ne. 15:24; D&amp;C 27:17). There should be no mistaking; it will take
both hands! <small>("Overcome ... Even As I Also Overcame," <i>Ensign</i>,
May
1987, p. 70; underlining added)</small></big></font></font><small>
</small><br>
