<p><big><font style="font-family: Candara; color: rgb(102, 51, 51);" face="Arial,Helvetica"><font size="+2"><big>Joseph B. Wirthlin</big></font></font><small style="color: rgb(102, 102, 102);"><font face="Arial,Helvetica"><font size="+2"> (1917-2008) Quorum of the Twelves Apostles</font></font></small>
