                <center><i><font size=+3>Our Moral Agency</font></i>
                    <p>Elder L. Lionel Kendrick
                        <br><font size=-1>Of the Seventy</font>
                        <br><font size=-1><i>Ensign</i>, Mar. 1996, pp. 28-32</font></center>
                <font size=-1></font>
                <p>
                <p><font size=-1>One of the marvelous blessings of the Book of Mormon is
                    that it contains, in clarity, revelations reserved to come forth in this
                    dispensation of time. Much of the knowledge that we have relating to the
                    principle of moral agency is found in these modern revelations.</font><font size=-1></font>
                <p><font size=-1>The Lord gave a brief but detailed definition of this
                    divine doctrine when He said, "Every man may act in doctrine and principle
                    pertaining to futurity, according to the moral agency which I have given
                    unto him, that every man may be accountable for his own sins in the day
                    of judgment" (D&amp;C 101:78; emphasis added). We are accountable for our
                    actions as we exercise our moral agency. If we understand this principle
                    and make righteous choices, our lives will be blessed. Wise choices can
                    put us in control of situations where we might otherwise be tempted to
                    compromise our principles. We cannot control all that happens to us; however,
                    we can choose to be in control of our responses.</font><font size=-1></font>
                <p><font size=-1>While I was serving as a mission president, my two assistants
                    were tracting one day and introduced themselves to a man who responded
                    in a rage. He repeatedly took the Lord's name in vain and called them profane
                    names. Then he ordered them to leave his property. When they shared this
                    incident, I asked how they had responded. They indicated that they told
                    him they had an important message that would bless his life. I asked what
                    they felt as they were being cursed and rejected. One told me that he had
                    an overwhelming feeling of compassion for the man. The impression came
                    to him to ask Heavenly Father to forgive the man, for they realized that
                    he did not know what he was doing. These elders were not in control of
                    the situation, but they exercised their moral agency to control how they
                    responded.</font><font size=-1></font>
                <p><font size=-1>We may be born into less-than-desirable circumstances.
                    We can choose to let those circumstances keep us from becoming successful,
                    or we can respond with a divine determination to achieve in spite of adverse
                    conditions. At times our circumstances call for us to make critical choices
                    to keep our covenants or to compromise them. Covenants should never be
                    compromised, even when at the moment some circumstances might seem to justify
                    it.</font><font size=-1></font>
                <p><font size=-1>The Book of Mormon teaches us of a group of converted
                    Lamanites, known as Ammonites, who had repented and felt the Lord had forgiven
                    them. They entered into a covenant that "they never would use weapons again
                    for the shedding of man's blood; … covenanting with God, that rather than
                    shed the blood of their brethren they would give up their own lives"(Alma
                    24:18). Later, in a time of war when others were bearing the brunt of their
                    defense, these Ammonites "were about to break the covenant which they had
                    made and take up their weapons of war"(Alma 56:7). Helaman, who realized
                    the importance of keeping covenants, later wrote: "I would not suffer them
                    that they should break this covenant which they had made, supposing that
                    God would strengthen us" (Alma 56:8). The two thousand sons of the Ammonites
                    went to war in their place and, under Helaman's direction, were successful
                    in the defense of the city of Judea. The Lord always stands ready to strengthen
                    us when we use our moral agency to hold true to our covenants, even under
                    extremely adverse circumstances.</font><font size=-1></font>
                <p><font size=-1>I once coached a high school basketball player named Jim
                    who chose not to compromise his principles for the sake of success. During
                    a state quarterfinal game, we played a team with an all-state center whom
                    Jim was assigned to guard. We knew that if Jim was successful, we could
                    win the game and advance to the semifinal. The lead changed thirteen times.
                    With two minutes left in the game, I called our last time-out. As Jim came
                    to the bench, he said, "Coach, I know how we can win this game. The center
                    bruised his big toe on his right foot during practice this week. The next
                    time down court, I can accidentally step on his toe and this game will
                    be ours." Then, without hesitation, he said: "It wouldn't be right, would
                    it? It would take all the fun from the victory." This young man could have
                    taken action without seeking consent. His desire to obey a principle of
                    correct conduct was greater than his desire to win by compromising his
                    principles. Those principles sustained him later when a work-related accident
                    left him using a wheelchair for the remainder of his life. We lost that
                    basketball game during an overtime, but I still consider this the greatest
                    victory I had as a coach--to see a young man make a righteous choice under
                    pressured conditions.</font><font size=-1></font>
                <p><font size=-1>It is important that we understand this powerful principle
                    of moral agency and its application in our [page 29] lives, since we are
                    making decisions each day that determine our divine destinies. To more
                    fully understand moral agency, it is needful to review its role in premortality
                    and in mortality.</font>
                    <br><font size=-1></font>&nbsp;<font size=-1></font>
                <p><b><font size=-1>Premortality</font></b><font size=-1></font>
                <p><font size=-1>Moral agency is an inherent part of our existence. The
                    Lord revealed to Joseph Smith: "Intelligence, or the light of truth, was
                    not created or made, neither indeed can be.</font><font size=-1></font>
                <p><font size=-1>"All truth is independent in that sphere in which God
                    has placed it, to act for itself, as all intelligence also; otherwise there
                    is no existence" (D&amp;C 93:29-30; emphasis added).</font><font size=-1></font>
                <p><font size=-1>Heavenly Father is indeed the Father of our spirits (see
                    Rom. 8:16; Heb. 12:9), and we are created in His image (see Mosiah 7:27;
                    Ether 3:15-16; Moses 6:8-9). We continued to have our moral agency following
                    our spiritual birth. Our spirits were born innocent (see D&amp;C 93:38;
                    Moro. 8:8, 12), but some became more intelligent, noble, and great than
                    others (see Abr. 3:18-19, 22-23; D&amp;C 138:55-56) because they exercised
                    more faith and performed greater works in premortal life (see Alma 13:3-4).
                    During this period, Satan exercised his agency in attempting to destroy
                    ours, (see Moses 4:3), and many chose to follow him (see Abr. 3:28; D&amp;C
                    29:36). In the Grand Council during premortal life, we each exercised our
                    agency; there were not then, nor will there ever be, any neutrals (see
                    2 Ne. 10:16).</font><font size=-1></font>
                <p><font size=-1>A number of significant events took place during our [page
                    30] premortal life that give light to the role of moral agency. Among these
                    were the Council in Heaven, the Creation, the War in Heaven, and the Fall.</font><font size=-1></font>
                <p><font size=-1><b><i>The Council in Heaven</i></b>. References in the
                    revelations give evidence that a Grand Council was convened during our
                    premortal life. All of Heavenly Father's spiritual children were there.
                    The purpose of the council was to prepare us for our earthly experiences.
                    We were taught all that we would need to know to return to Heavenly Father's
                    presence one day. We did indeed receive "[our] first lessons in the world
                    of spirits and were prepared to come forth in the due time of the Lord"
                    (D&amp;C 138:56).</font><font size=-1></font>
                <p><font size=-1>After the plan of salvation was presented for a sustaining
                    vote, Heavenly Father inquired as to whom He should send to put the plan
                    into effect in mortality. Satan responded with an alteration of the plan,
                    a version conceived with an evil intent. He said: "I will redeem all mankind,
                    that one soul shall not be lost … ; wherefore give me thine honor" (Moses
                    4:1). Satan's plan would indeed "destroy the agency of man" (Moses 4:3).
                    Selfish and senseless, his plan defied eternal law. It was an impossible
                    plan, for without moral agency no one could become exalted. Today he continues
                    to activate parts of his evil plan, appealing to the carnal tendencies
                    of man through temptations. If we are not aware, we may find ourselves
                    being lured by Satan's temptations, and if we follow his plan we will become
                    like him (see Mosiah 16:3).</font><font size=-1></font>
                <p><font size=-1>After Satan presented his evil version of the Father's
                    plan, the Savior responded to Heavenly Father: "Here am I, send me" (Abr.
                    3:27). He added: "Father, thy will be done, and the glory be thine forever"
                    (Moses 4:2). In those few humble words, the Savior, exercising His moral
                    agency, accepted the sacred assignment as the atoning sacrifice; in this
                    role He would suffer sorely for our sins. We, too, exercised our moral
                    agency in the premortal council. We sustained Heavenly Father's plan with
                    its laws, ordinances, and covenants. We also sustained Jesus Christ as
                    our Savior, who was, indeed, foreordained (see Ether 3:14; 1 Pet. 1:19-20).</font><font size=-1></font>
                <p><font size=-1><b><i>Creation</i></b>. Abraham records that the premortal
                    Jesus Christ directed, "We will go down, for there is space there, and
                    we will take of these materials, and we will make an earth whereon these
                    may dwell (Abr. 3:21). It was determined that this earth would be a place
                    of probation (see Alma 12:24; D&amp;C 29:43).</font><font size=-1></font>
                <p><font size=-1>One of the purposes of the probation was stated by the
                    Savior: "We will prove them … to see if they will do all things whatsoever
                    the Lord their God shall command them" (Abr. 3:25). This means that in
                    mortality our "natural man" tendencies must be brought into subjection
                    by the "spiritual man" through our moral agency. With our mortal bodies,
                    we have the power of procreation in this life. We have the choice and capacity
                    to control our physical desire, or to have it control us. This is one of
                    the most important challenges we face in using our moral agency. Unrighteous
                    use of the procreative power carries with it the most condemning consequences,
                    but if we use it righteously, we have potential to "receive a fulness of
                    joy" (D&amp;C 93:33-34; D&amp;C 138:17).</font><font size=-1></font>
                <p><font size=-1><b><i>The War in Heaven</i></b>. Angrily, Lucifer used
                    his divine gift of agency to make a decision that would lead to his eternal
                    damnation. In bold opposition, he rebelled [page 31] against God (see Rev.
                    12:7-8) and "kept not his first estate" (Abr. 3:28). "A third part of the
                    hosts of heaven turned he away from me [the Lord God] because of their
                    agency" (D&amp;C 29:36; emphasis added). Even with the possibility of their
                    eternal damnation, Heavenly Father would not take their agency from them.
                    To do so would be counter to eternal law. As a result of their rebelliousness,
                    Lucifer and his followers were cast out of heaven (see Rev. 12:9; D&amp;C
                    29:36-37) and forfeited the blessings of eternal life.</font><font size=-1></font>
                <p><font size=-1><b><i>The Fall</i></b>. The plan of salvation was activated
                    when Adam and Eve were placed in the Garden of Eden and given the opportunity
                    to exercise their agency. Their Father commanded them to "be fruitful,
                    and multiply, and replenish the earth," (Moses 2:28) but Adam and Eve were
                    unable to have children, for their bodies had not yet become mortal. However,
                    the Creator also provided the means by which they could choose to obey
                    His commandment. He planted two special trees in the Garden of Eden, the
                    tree of life and the tree of knowledge of good and evil. These two trees
                    in the garden offered completely different choices, with different consequences.</font><font size=-1></font>
                <p><font size=-1>Adam and Eve were commanded, "But of the tree of the knowledge
                    of good and evil, thou shalt not eat of it, nevertheless, thou mayest choose
                    for thyself, for it is given unto thee; but, remember that I forbid it,
                    for in the day thou eatest thereof thou shalt surely die" (Moses 3:17;
                    emphasis added). By partaking, Adam and Eve experienced a spiritual death,
                    being separated from Deity as they were cast out of the garden. Their bodies
                    became mortal, subject to temptations of the flesh and, eventually, to
                    death (see D&amp;C 29:40-43).</font><font size=-1></font>
                <p><font size=-1>Had they not partaken of the fruit of the tree of knowledge
                    of good and evil, they "would have remained in the garden of Eden. …</font><font size=-1></font>
                <p><font size=-1>"And they would have had no children; … they would have
                    remained in a state of innocence, having no joy, for they knew no misery;
                    doing no good, for they knew no sin" ().2 Ne. 2:22-23). We, too, would
                    have remained in our premortal condition without the possibility of mortal
                    bodies or earthly experiences. We would have had no possibility of godhood.</font><font size=-1></font>
                <p><font size=-1>We are blessed as a result of our first parents' partaking
                    of the fruit of this tree. Our eternity depended on their act of agency.
                    We now have the power of procreation, providing bodies for the spirit children
                    of Heavenly Father. Indeed, "Adam fell that men might be; and men are,
                    that they might have joy" (2 Ne. 2:25). Adam and Eve made a conscious decision
                    based upon the two choices that were placed before them. They chose mortality,
                    which gave them the opportunity for eternal life, and, as a result, they
                    were cast out of the Garden of Eden, "cut off both temporally and spiritually
                    from the presence of the Lord; and thus … they became subjects to follow
                    after their own will" (Alma 42:7). Moral agency was now in full force in
                    mortality.</font>
                    <br><font size=-1></font>&nbsp;<font size=-1></font>
                <p><b><font size=-1>Mortality</font></b><font size=-1></font>
                <p><font size=-1>Although Adam and Eve did not forget there was a God,
                    they had no memory of their premortal instructions (see Joseph Smith, comp.,
                    <i>Lectures on Faith</i> [1985], 14.). A loving Lord gave them counsel
                    concerning the plan of salvation, and their transgression in the Garden
                    of Eden was forgiven (see Alma 12:30-37; Moses 6:50-53). As they called
                    upon him, He gave them commandments to keep (see Moses 5:5). From these
                    early [page 32] instructions given to Adam and Eve, as well as from additional
                    revelations down to the most recent, we have abundant direction about the
                    use of our moral agency.</font><font size=-1></font>
                <p><font size=-1>For agency to be part of our lives four principles are
                    essential: (1) eternal laws ordained by God; (2) opposition; (3) knowledge
                    of good and evil; (4) freedom to choose.</font><font size=-1></font>
                <p><font size=-1><b><i>Laws</i></b>. Laws are given as a standard by which
                    we exercise our moral agency and by which we will be judged.</font><font size=-1></font>
                <p><font size=-1>God is the author of the eternal laws that govern all
                    existence (see D&amp;C 88:13, 36-38, 41-42). All blessings come from obedience
                    to a law (see D&amp;C 130:20-21; D&amp;C 132:5). Where there is no law
                    given, there is no punishment (see 2 Ne. 9:25; Alma 42:17), for the Atonement
                    covers all who are not under the law (see Moro. 8:22).</font><font size=-1></font>
                <p><font size=-1>We are commanded to keep the laws of God (see Mosiah 2:22),
                    and a way is provided for us to be obedient to each of them (see Alma 41:8;
                    1 Ne. 3:7). While we may use our agency to obey or to disobey, we are not
                    free to choose the consequences of our disobedience (see 2 Ne. 2:26-29;
                    Alma 5:41-42).</font><font size=-1></font>
                <p><font size=-1>At death, our first judgment will be based upon our obedience
                    to laws given in mortality (see Alma 40:11-14); and in our final judgment,
                    we will inherit the kingdom whose laws we prepared ourselves to receive
                    (see D&amp;C 88:21-24; D&amp;C 76:111; D&amp;C 98:18). We cannot enter
                    the celestial kingdom unless we have lived the celestial law (see D&amp;C
                    132:21).</font><font size=-1></font>
                <p><font size=-1><b><i>Opposition</i></b>. Agency could not exist without
                    the opportunity to face opposition. Lehi explained: "Wherefore, man could
                    not act for himself save it should be that he was enticed by the one or
                    the other," righteousness or wickedness (2 Ne. 2:16; see also 2 Ne. 2:11).
                    The Lord counseled: "And it must needs be that the devil should tempt the
                    children of men, or they could not be agents unto themselves; for if they
                    never should have bitter they could not know the sweet" (D&amp;C 29:39).
                    Thus by partaking of the forbidden fruit, Adam and Eve gained for themselves
                    and all their posterity the opportunity to experience opposition and to
                    exercise moral agency.</font><font size=-1></font>
                <p><font size=-1><b><i>Knowledge of good and evil</i></b>. Knowledge is
                    essential for agency to exist. When Adam and Eve partook of the fruit,
                    they made it possible for all to gain knowledge. Lehi said, "Men are instructed
                    sufficiently that they know good from evil," (2 Ne. 2:5) so that they may
                    "act for themselves and not … be acted upon" (2 Ne. 2:26).</font><font size=-1></font>
                <p><font size=-1>Alma taught: "He that knoweth not good from evil is blameless;
                    but he that knoweth good and evil, to him it is given according to his
                    desires, whether he desireth good or evil, life or death, joy or remorse
                    of conscience" (Alma 29:5). With knowledge comes accountability. Mormon
                    explained the unhappy consequences in unwise exercise of moral agency.
                    Once people have been enlightened by the Spirit, gaining knowledge of the
                    laws that govern righteousness, and then disobey the commandments, "their
                    state becomes worse than though they had never known these things" (Alma
                    24:30).</font><font size=-1></font>
                <p><font size=-1><b><i>Freedom to choose</i></b>. Laws, opposition, and
                    knowledge were given to the children of God. We are free to choose between
                    contrasting paths with their clear consequences (see 2 Ne. 2:27; 10:23).
                    In thus exercising our moral agency, we are given the power to be our own
                    judges (see Alma 41:3-7).</font>
                    <br><font size=-1></font>&nbsp;<font size=-1></font>
                <p><b><font size=-1>Principles</font></b><font size=-1></font>
                <p><font size=-1>Satan will attempt to gain control of our agency through
                    temptations, but there are principles that will assist us in resisting
                    his enticements.</font><font size=-1></font>
                <p><font size=-1>While our physical bodies might be basically "carnal,
                    sensual and devilish, by nature," (see Alma 42:10) our eternal spirit has
                    the power to rule over the body (1 Cor. 9:27). We can be in control of
                    our thoughts, feelings, and actions if we properly exercise our moral agency.</font><font size=-1></font>
                <p><font size=-1>In early September 1994, Marc Bennett faced a difficult
                    decision. The eighteen-year-old football star from Salisbury High School
                    in Sherwood Park, Alberta, Canada, had just won a starting position on
                    the University of Alberta Golden Bear football team.</font><font size=-1></font>
                <p><font size=-1>He was the youngest player on the team and the first Latter-day
                    Saint to play for Alberta in over a decade. Coming up was "Rookie Night,"
                    a time for the team veterans to initiate the first-year players. Alcohol
                    consumption would be expected.</font><font size=-1></font>
                <p><font size=-1>As practice broke that Friday afternoon, John Price, a
                    veteran fullback, took Marc aside and asked if he were planning to attend
                    the initiation. Before Marc could reply, John explained the importance
                    of being together as a team. Then he added that he knew what Marc's standards
                    were. If anyone asked Marc to do something contrary to his beliefs, John
                    said, they would have to go through him first. As Marc entered the hall
                    that night, John greeted him with a soft drink and made sure that he was
                    able to maintain his standards throughout the event.</font><font size=-1></font>
                <p><font size=-1>At the year-end banquet, Marc received the Hector Creighton
                    Award as the most outstanding rookie player. It was then announced that
                    he would not be returning for the 1995 season because he would be serving
                    a two-year mission for The Church of Jesus Christ of Latter-day Saints.
                    As the evening progressed, many team members stopped at Marc's table to
                    wish him well on his mission. Marc is now serving in the France Marseilles
                    Mission.</font><font size=-1></font>
                <p><font size=-1>When we are obedient and set the proper example, the Lord,
                    even by inspiring the actions of others, can strengthen our power to resist.
                    John Price was an answer to Marc's prayers. When we let our lights shine
                    before men, we need not fear.</font><font size=-1></font>
                <p><font size=-1>Satan has no power over us except that which we allow
                    him to take. The Prophet Joseph Smith taught the principle that those with
                    bodies have power over those without bodies (see<i> Scriptural Teachings
                        of the Prophet Joseph Smith</i>, comp. Joseph Fielding Smith, annotated
                    by Richard C. Galbraith, Salt Lake City: Deseret Book Co., 1993, p. 206).
                    It is only through the misuse of our agency that we give Satan influence
                    in our lives.</font><font size=-1></font>
                <p><font size=-1>We will not be tempted beyond our capacity to resist (see
                    1 Cor. 10:13). This principle comes as a promise to those who are striving
                    to resist and are not reckless in their response to temptations (see Alma
                    13:28).</font><font size=-1></font>
                <p><font size=-1>Moral agency is an eternal principle of power and potential.
                    A loving Heavenly Father has given us this great gift, and through it the
                    opportunity to determine our divine destiny.</font><font size=-1></font>
                <p><font size=-1>May we always be grateful for the trust that He has given
                    us, and may we be true to Him day by day as we exercise our priceless agency.</font>

