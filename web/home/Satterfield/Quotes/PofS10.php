                <p align="center"><b><i><font size="6">Elder Jeffrey R. Holland </font></i></b>
                </p>
                <p align="center"><i><font size="6">on the Fall of Adam and the Natural Man</font></i></p>
                <p>&nbsp;</p>
                <p>
                    <img border="0" src="../Rel341/Pictures/People%20in%20Church%20History/Jeffrey%20R%20Holland.jpg" width="315" height="395" align="left"></p>
                <p><font size="5">Elder Jeffrey R. Holland has written: </font></p>
                <p align="center"><b><font size="5">The Fall</font></b></p>
                <p><font size="5">So Adam and Eve willingly made a choice, choosing the path 
                    toward growth and godhood inherent in the fruit of the tree of knowledge of good 
                    and evil over the potentially meaningless (at least at that point in their 
                    development) tree of life. With the enticement of Lucifer, &quot;<font color="#000099">that 
                    old serpent that did beguile our first parents, which was the cause of their 
                    fall</font>,&quot; as Abinadi phrased it, they consciously chose to step out of the 
                    garden of Eden-a magnificent, terrestrial-like, paradisiacal world-into a 
                    fallen, telestial one, a world filled with very unparadisiacal thistles and 
                    thorns, sorrow and sin, disease and death. <br>
                    <br>
                    In doing so, Adam and Eve answered forever the plaintive question that is so 
                    often heard: &quot;If there is a God, why is there so much suffering in the world?&quot; 
                    The answer to that is we now live in a fallen world filled with opposites, a 
                    world in which God is the most powerful but decidedly not the only spiritual 
                    influence. As part of the doctrine of opposition, Satan is also at work in the 
                    world, and we knew before we came here that he would bring grief and anguish 
                    with him. Nevertheless, we (through Adam and Eve) made the conscious choice to 
                    live in and endure this mortal sphere of opposition in all things, for only 
                    through such an experience was godly progress possible. Adam and Eve-and 
                    we-knowingly and lovingly absolved God of the responsibility for the &quot;thorns and 
                    thistles&quot; of a fallen world that was personally chosen by us, not capriciously 
                    imposed by him. We wanted the chance to become like our heavenly parents, to 
                    face suffering and overcome it, to endure sorrow and still live rejoicingly, to 
                    confront good and evil and be strong enough to choose the good. In this 
                    telestial, mortal world filled with competing voices, enticements, and 
                    experiences, we get a lifetime of opportunity to refine and strengthen these 
                    virtues. </font></p>
                <p><font size="5">Understanding this doctrine, Lehi noted that for Adam and Eve 
                    to have remained in the garden of Eden would have kept all things &quot;<font color="#000099">in 
                    the same state in which they were after they were created</font>,&quot; a situation 
                    in which they would have &quot;<font color="#000099">remained in a state of 
                    innocence, having no joy, for they knew no misery; doing no good, for they knew 
                    no sin</font>.&quot; <br>
                    <br>
                    But Adam and Eve made their choice for an even more generous reason than those 
                    of godly knowledge and personal progress. They did it for the one overriding and 
                    commanding reason basic to the entire plan of salvation and all the discussions 
                    ever held in all the councils of heaven. They did it &quot;<font color="#000099">that 
                    men might be</font>.&quot; Had Adam and Eve never left the garden, Lehi noted, &quot;they 
                    would have had no children&quot; [2 Ne. 2:25].&nbsp; ...<br>
                    &nbsp;</font></p>
                <p align="center"><font size="5"><b>Natural Man </b></font></p>
                <p align="left"><font size="5"><br>
                    Many things happened in the process of the Fall, including changes that came to 
                    the physical bodies of Adam and Eve. For one thing, they fell into &quot;nature,&quot; the 
                    word becoming something of a synonym for the Adamic process. King Benjamin would 
                    say of little children, &quot;<font color="#000099">As in Adam, <i>or by nature</i>, 
                    they fall, even so the blood of Christ atoneth for their sins</font>.&quot; [Mosiah 
                    3:16; emphasis added]<br>
                    <br>
                    Part of the natural world Adam and Eve entered included the addition to their 
                    bodies of blood-a corruptible ingredient-in what had been to that point an 
                    uncorrupted body of bloodless flesh and bone. But even more important than such 
                    physical changes were the temptations of and threats to the spirit. Spiritual as 
                    well as physical separation from God came with the Fall. Humankind was cut off 
                    from the immediate personal companionship with God that Adam and Eve had enjoyed 
                    in the garden of Eden. As a result, they were distanced from the Holy Spirit and 
                    became less responsive to many of the things of righteousness. King Benjamin 
                    made that issue one of the central tasks for men and women to deal with in their 
                    fallen, or natural, state. <br>
                    <br>
                    &quot;<font color="#000099">The natural man is an enemy to God,&quot; he taught, &quot;and has 
                    been from the fall of Adam, and will be, forever and ever, unless he yields to 
                    the enticings of the Holy Spirit, and putteth off the natural man and becometh a 
                    saint through the atonement of Christ the Lord, and becometh as a child, 
                    submissive, meek, humble, patient, full of love, willing to submit to all things 
                    which the Lord seeth fit to inflict upon him, even as a child doth submit to his 
                    father.</font>&quot; [Mosiah 3:19] <br>
                    &nbsp;</font></p>
                <p><font size="5">More extreme language than that of &quot;natural man&quot; or even 
                    &quot;enemy to God&quot; is Abinadi's pronouncement that as a result of the Fall and the 
                    increased influence of Satan in the fallen world, &quot;<font color="#000099">all 
                    mankind [became] carnal, sensual, devilish, knowing evil from good, subjecting 
                    themselves to the devil. <br>
                    <br>
                    &quot;Thus all mankind were lost; and behold, they would have been endlessly lost 
                    were it not that God redeemed his people from their lost and fallen state. <br>
                    &nbsp;</font></font></p>
                <p><font size="5"><font color="#000099">&quot;But remember that he that persists in 
                    his own carnal nature, and goes on in the ways of sin and rebellion against God, 
                    remaineth in his fallen state and the devil hath all power over him. Therefore 
                    he is as though there was no redemption made, being an enemy to God; and also is 
                    the devil an enemy to God</font>.&quot; [Mosiah 16:3-5]<br>
                    <br>
                    The brother of Jared made reference to that mortal alienation between man and 
                    God when he plaintively cried to the Lord, &quot;<font color="#000099">O Lord, . . . 
                    do not be angry with thy servant because of his weakness before thee; for we 
                    know that thou art holy and dwellest in the heavens, and that we are unworthy 
                    before thee; because of the fall our natures have become evil continually.</font>&quot; 
                    [Ether 3:2]<br>
                    <br>
                    Because this doctrine is so basic to the plan of salvation and also because it 
                    is so susceptible to misunderstanding, we must note that these references to 
                    &quot;natural&quot; evil emphatically do <i>not</i> mean that men and women are 
                    &quot;inherently&quot; evil. There is a crucial difference. As spiritual sons and 
                    daughters of God, all mortal men and women are divine in origin and divine in 
                    their potential destiny. As Doctrine and Covenants 93:38-39 teaches, the spirit 
                    of every man, woman, and child &quot;<font color="#000099">was innocent in the 
                    beginning</font>.&quot; But it is also true that as a result of the Fall they are now 
                    in a &quot;natural&quot; (fallen) world where the devil &quot;<font color="#000099">taketh away 
                    light</font>&quot; and where some elements of nature-including temporal human 
                    nature-need discipline, restraint, and refinement. It is as if men and women are 
                    given, as part of their next step in development along the path to godhood, raw 
                    physical and spiritual ingredients-&quot;natural&quot; resources, if you will. Those 
                    resources are not to run rampant but are to be harnessed and focused so that 
                    their power and potential (as is sometimes done with a &quot;natural&quot; river or a 
                    &quot;natural&quot; waterfall) can be channeled and thereby made even more productive and 
                    beneficial. <br>
                    <br>
                    Natural man, with all of his new and wonderful but as yet unbridled and 
                    unregenerated potential, must be made &quot;<font color="#000099">submissive</font>&quot; 
                    to the Holy Spirit, a spirit that still entices and lifts us upward. The brother 
                    of Jared acknowledged the inherent goodness of the soul when he said that our 
                    mortal transgressions and temporal natures can be overcome when we call upon God 
                    and from him &quot;<font color="#000099">receive according to our desires</font>.&quot; 
                    Our deepest desires, our premortal yearnings, are still divine in their origins, 
                    and they are still deep in our souls. The echoes of our earlier innocence still 
                    reverberate, and the light that forsakes the evil one still shines. Our hearts 
                    can-and in their purity, do-desire that which is spiritual and holy rather than 
                    that which is &quot;<font color="#000099">carnal, sensual, and devilish</font>.&quot; If 
                    that were not so, we would be in a hopeless condition indeed, and the idea of 
                    real choice would be jeopardized forever. We praise God our Father that our true 
                    heritage is of him and that by yielding and submitting to his eternal influence 
                    we can overcome the enmity which separated us from him and turn those gifts from 
                    nature to our blessing rather than our cursing. </font></p>
                <p><font size="5">(<i>Christ and the New Covenant: The Messianic Message of the 
                        Book of Mormon</i> [Salt Lake City,UT: Deseret Book, 1997]&nbsp; pp.203 - 207)</font></p>

