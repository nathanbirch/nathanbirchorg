                                From </span><br style="font-style: italic;">
                            <big style="font-style: italic;"><font size="7">Joseph Fielding Smith </font> </big>
                            <font size="6"><br
                                style="font-style: italic;">
                            </font>
                            <span style="font-style: italic;"><font size="6">Concerning Baptism</font></span><font size="6"><br>
                            </font>
                        </span></big></big></p>
                <p style="font-family: arial;"><big><big><b
                            style="color: rgb(0, 0, 153);">
                            <img border="0" src="../Rel341/Pictures/People%20in%20Church%20History/Joseph%20Fiedling%20Smith.bmp" width="370" height="480" align="left"></b></big></big></p>
                <div style="margin-left: 40px; font-family: arial;"><big><big><span
                                style="font-weight: bold;">NATURE OF COVENANT OF BAPTISM</span>. Every
                            person baptized into this Church has made a covenant with the Lord to
                            keep his commandments. We are to serve the Lord with all the heart, and
                            all the mind, and all the strength that we have, and that too in the
                            name of Jesus Christ. Everything that we do should be done in the name
                            of Jesus Christ. </big></big><br>
                </div>
                <div style="margin-left: 40px; font-family: arial;"><big><big><br>
                            In the waters of baptism, we covenanted that we would keep these
                            commandments; that we would serve the Lord; that we would keep this
                            first and greatest of all commandments, and love the Lord our God; that
                            we would keep the next great commandment, we would love our neighbor as
                            ourselves; and with all the might that we have, with all the strength,
                            with all our hearts, we would prove to him that we would "live by every
                            word that proceedeth forth from the mouth of God"; that we would be
                            obedient and humble, diligent in his service, willing to obey, to
                            hearken to the counsels of those who preside over us and do all things
                            with an eye single to the glory of God. <small>(<i>Doctrines of
                                    Salvation</i>, 2:328) </small></big></big>
                </div>
                <div style="margin-left: 40px; font-family: arial;">&nbsp;<p><big><big>
                            <span
                                style="font-weight: bold;">WHY BAPTISM MUST BE BY IMMERSION.</span>
                            The mode of baptism is by immersion in water. Sprinkling or pouring did
                            not come into vogue until two or three centuries after Christ, and such
                            a practice was not universal until about the 13th century A.D. We have
                            to go into history to find these particulars. Baptism cannot be by any
                            other means than immersion of the entire body in water, for the
                            following reasons: </big></big><br>
                    <big><big>&nbsp;</big></big><br>
                    <big><big>1. It is in the similitude of the death, burial, and
                            resurrection of Jesus Christ, and of all others who have received the
                            resurrection. </big></big><br>
                </div>
                <p style="margin-left: 40px; font-family: arial;"><big><big>2. Baptism
                        is also a birth and is performed in the similitude of the birth of a
                        child into this world. <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big>3. Baptism
                        is not only a figure of the resurrection, but also is literally a
                        transplanting or resurrection from one life to another -- from the life
                        of sin to the life of spiritual life. <br>
                        &nbsp;<br>
                        I want to take up the second reason: Baptism is also a birth and is
                        performed in the similitude of the birth of a child into this world.
                        When this earth was created, it came into existence the same way. (I am
                        not speaking scientifically, and yet scientific doctrine tells us the
                        same thing.) This earth was born in water. Before the land appeared the
                        whole sphere was covered with water. <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big><span
                            style="font-weight: bold;">BIRTH COMES BY WATER, BLOOD, AND SPIRIT</span>.
                        In the Book of Moses we read: "Therefore I give unto you a commandment,
                        to teach these things freely unto your children, saying: That by reason
                        of transgression cometh the fall, which fall bringeth death, and
                        inasmuch as ye were born into the world by water, and blood, and the
                        spirit, which I have made, and so became of dust a living soul, even so
                        ye must be born again into the kingdom of heaven, of water, and of the
                        Spirit, and be cleansed by blood, even the blood of mine Only Begotten;
                        that ye might be sanctified from all sin, and enjoy the words of
                        eternal life in this world, and eternal life in the world to come, even
                        immortal glory; For by the water ye keep the commandment; by the Spirit
                        ye are justified, and by the blood ye are sanctified." That is one of
                        the finest passages I know, and we find practically the same thing
                        recorded by John. <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big><span
                            style="font-weight: bold;">SAME ELEMENTS PRESENT IN FIRST AND SECOND
                            BIRTHS</span>. Every child that comes into this world is carried in
                        water, is born of water, and of blood, and of the spirit, So when we
                        are born into the kingdom of God, we must be born in the same way. By
                        baptism, we are born of the water. Through the shedding of the blood of
                        Christ, we are cleansed and sanctified: and we are justified, through
                        the Spirit of God, for baptism is not complete without the baptism of
                        the Holy Ghost. You see the parallel between birth into the world and
                        birth into the kingdom of God. How foolish it is to think for a moment
                        that baptism could be accomplished by pouring water on a child's head,
                        it does not meet the requirements. <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big>I have
                        heard some of our young elders preaching on baptism say that the Lord
                        could have brought to pass the remission of sins in some other way.
                        They reasoned he could have done it by sprinkling, or in this way or
                        that way. The Lord could not consistently do it any other way, only by
                        being buried in the water, born of water and of the spirit, and
                        cleansed by the blood of Christ, just as a child is born into this
                        world of water, blood, and spirit. The comparison is very striking. <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big><span
                            style="font-weight: bold;">BAPTISM: A RESURRECTION TO LIFE</span>.
                        Coming now to the third reason: Baptism is not only a figure of the
                        resurrection, but also is literally a transplanting or resurrection
                        from one life to another -- from the life of sin to the life of
                        spiritual life. For proof of that, I am going to read first something
                        the Lord said to Joseph Smith. <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big>"Wherefore,
                        I, the Lord God, caused that he [Adam] should be cast out from the
                        Garden of Eden, from my presence, because of his transgression, wherein
                        he became spiritually dead, which is the first death, even that same
                        death which is the last death, which is spiritual, which shall be
                        pronounced upon the wicked when I shall say: Depart, ye cursed." <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big>Here we
                        have two conditions, spiritual life and spiritual death. The condition
                        of Adam in the Garden of Eden was spiritual life. He was in the
                        presence of God. Through his transgression, he was banished into
                        spiritual death -- a new life entirely. He was no longer in the
                        presence of God. He was shut out, separated, a veil drawn between him,
                        and the Lord. <br>
                        &nbsp;<br>
                        Adam, after the fall, was in spiritual death, and not only Adam but
                        every man and woman upon the face of the earth who is accountable
                        before God, We will not consider the last death more than to say it is
                        also banishment from the presence of God. <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big>"But,
                        behold, I say unto you that I, the Lord God, gave unto Adam and unto
                        his seed, that they should not die as to the temporal death, until I,
                        the Lord God, should send forth angels to declare unto them repentance
                        and redemption, through faith on the name of mine Only Begotten Son.
                        And thus did I, the Lord God, appoint unto man the days of his
                        probation -- that by his natural death he might be raised in
                        immortality unto eternal life, even as many as would believe; And they
                        that believe not unto eternal damnation; for they cannot be redeemed
                        from their spiritual fall, because they repent not." <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big>We find
                        Adam, then, in spiritual death, and all his posterity, excepting little
                        children. All men and women need repentance. Death is banishment. They
                        are in spiritual death. How are they going to get back? By being buried
                        in the water. They are dead and are buried in the water and come forth
                        in the resurrection of the Spirit back into spiritual life. That is
                        what baptism is. <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big><span
                            style="font-weight: bold;">WHY BAPTISM REMITS FUTURE SINS</span>. I
                        have heard some of our young men, and some not so young, when talking
                        on baptism, say they do not know why it is, since baptism is for the
                        remission of sins, that a man does not have to be baptized every time
                        he commits a sin. Do you see the reason? As long as a man sins and
                        stays within spiritual life, he is alive, he can repent and be
                        forgiven. He does not need to be baptized to be brought back to where
                        he already is, But there are sins, John says, "unto death," and if a
                        man commits a sin unto death, he is banished again and comes back into
                        spiritual death. <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big>Through
                        this kind of transgression he loses the effect of baptism and is
                        banished into spiritual death. When a man commits a sin unto death, he
                        is banished from spiritual life. The shedding of innocent blood is one
                        such sin and blasphemy against the Holy Ghost another. The enemies of
                        the Prophet Joseph Smith carried him off to Carthage and put him and
                        his brother to death. Deliberately murdering the servants of God is the
                        shedding of innocent blood. <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big>If a man
                        sins unto death, he goes back again to spiritual death, but as long as
                        he stays within spiritual life, he does not have to be baptized again. <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big><span
                            style="font-weight: bold;">DEATH TO SIN BRINGS RESURRECTION TO LIFE</span>.
                        Paul had that very clearly in mind. He says, in writing to the Romans:
                        "What shall we say then? Shall we continue in sin, that grace may
                        abound? God forbid. How shall we, that are dead to sin, live any longer
                        therein? Know ye not, that so many of us as were baptized into Jesus
                        Christ were baptized into his death? <br>
                    </big></big></p>
                <div style="margin-left: 40px;"><big style="font-family: arial;"><big>"Therefore
                            we are buried with him by baptism into death: that like as Christ was
                            raised up from the dead by the glory of the Father, even so we also
                            should walk in newness of life. For if we have been planted together in
                            the likeness of his death, we shall be also in the likeness of his
                            resurrection: Knowing this, that our old man is crucified with him,
                            that the body of sin might be destroyed, that henceforth we should not
                            serve sin." </big></big><br style="font-family: arial;">
                    <big style="font-family: arial;"><big>&nbsp;</big></big><br
                        style="font-family: arial;">
                    <big style="font-family: arial;"><big>Paul is speaking to members of
                            the Church. We are dead to sin because we have left it. Banishment
                            works one way as well as the other. In other words, when we are in
                            spiritual life (or in the Church or in the kingdom of God), we ought
                            not to sin. Through baptism we come back from the spiritual death which
                            is upon all unbaptized men. Whether they are Roman Catholic or
                            Protestant, if they are unrepentant and unbaptized they are in
                            spiritual death. But we who have received the gospel, why should we
                            live in sin when we have been baptized and are in spiritual life? </big></big><br
                        style="font-family: arial;">
                    <big style="font-family: arial;"><big>&nbsp;</big></big><br
                        style="font-family: arial;">
                    <big style="font-family: arial;"><big><span style="font-weight: bold;">HOW
                                SAINTS ARE IN PRESENCE OF GOD. </span>We are back in the presence of
                            God. The question might naturally be raised: How do we come back into
                            the presence of God if we do not see him? We do not see him now, but
                            are we not in his presence when we have the gift of the Holy Ghost, one
                            of the members of the Godhead, to lead and direct us in righteousness?
                            We are back in his presence, if we keep the commandments and do not
                            longer live in sin; then we are in spiritual life. That is an important
                            thing in connection with baptism not generally understood. <small>(<i>Doctrines
                                    of Salvation</i>, 2:323-328) </small></big></big>
