                        our
                        passions, control our appetites, and rise above carnal and evil things.
                        It is to overcome the world as did he who is our prototype and who
                        himself
                        was the most valiant of all our Father's children. It is to be morally
                        clean, to pay our tithes and offerings, to honor the Sabbath day, to
                        pray
                        with full purpose of heart, to lay our all upon the altar if called
                        upon
                        to do so.</big></font>
