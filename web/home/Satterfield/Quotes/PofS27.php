                                                           size="+1"><big>One thing we do know; one thing is clearly told us,
                            and
                            that is if we are not governed by the celestial law and cannot abide a
                            celestial law, we cannot inherit a celestial kingdom. What is it to
                            obey
                            a celestial law? Where does the celestial law come from to begin with?
                            From the heavens. Very well. What have the people here to do with it
                            generally
                            -- that is, outsiders? Nothing. They do not say they have had any
                            revelation.
                            They have had no principle of that kind unfolded to them. They are
                            living
                            under what might be termed a terrestrial law; and many of them, I
                            think,
                            under the circumstances, do quite as well as we do under our
                            circumstances.
                            We profess to be moving on a more elevated plane than they are. We
                            profess
                            to have come out from the world; to have separated from the ungodly. We
                            profess to be under the guidance of apostles and prophets, pastors and
                            teachers, etc., and to be living under the inspiration of the Most
                            High.
                            They do not profess anything of the kind. (<i>The Gospel Kingdom</i>,
