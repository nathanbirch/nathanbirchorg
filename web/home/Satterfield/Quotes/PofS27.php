                            and
                            practice of good it was a great trial to many, and some apostatized
                            because
                            God was not going to send to everlasting punishment heathens and
                            infants,
                            but had a place of salvation in due time for all, and would bless the
                            honest
                            and virtuous and truthful, whether they belonged to any church or not.
                            It was a new doctrine to this generation, and many stumbled at it." (<i>Journal
                                of Discourses</i> 16:42) [<i>Answers to Gospel Questions</i>, 4:11-13]</big></font>
