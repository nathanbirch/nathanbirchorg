                        believe
                        in Christ and his gospel with unshakable conviction. It is to know of
                        the
                        verity and divinity of the Lord's work on earth.</big></font>
                </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>But
