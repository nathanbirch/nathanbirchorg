                        service; it is not simply confessing with the mouth the divine Sonship
                        of the Savior. It is obedience and conformity and personal
                        righteousness.
                        "Not every one that saith unto me, Lord, Lord, shall enter into the
                        kingdom
                        of heaven; but he that doeth the will of my Father which is in heaven."
                        (Matt. 7:21.)</big></font>
                </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>To
                        be valiant in the testimony of Jesus is to "press
                        forward
                        with a steadfastness in Christ, having a perfect brightness of hope,
                        and
                        a love of God and of all men." It is to "endure to the end." (2 Ne.
                        31:20.)
                        It is to live our religion, to practice what we preach, to keep the
                        commandments.
                        It is the manifestation of "pure religion" in the lives of men; it is
                        visiting
                        "the fatherless and widows in their affliction" and keeping ourselves
                        "unspotted
                        from the world." (James 1:27.)</big></font>
                </big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><font size="+1"><big>To
