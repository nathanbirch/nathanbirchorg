                <ul style="font-family: arial;">
                    <big> </big><li><big> <font size="+1"><big>Through the mission of
                            Jesus Christ a law has been
                            given,
                            the law of the gospel, and that law is binding upon all those who hear
                            it, who come in contact with it; and provision has been made so that
                            those
                            who are without the law or who have not heard the name of Christ, who
                            are
                            ignorant of the plan of salvation, because the gospel has never reached
                            them in any form, shall not be under the same restrictions and
                            condemnation
                            as will those who have received that law. And they who are without the
                            law, Christ redeems through his blood, and does not require of them
                            that
                            which he requires of me and of you. ( <i>Doctrines of Salvation</i>,
                            2:29)</big></font></big></li>
                    <big> </big><li><big> <font size="+1"><big>We read in the Book of
                            Abraham that the Lord
                            revealed that
                            in the world of spirits some of the intelligences were greater than
                            others,
                            and these he made his rulers. Contrariwise there were those who were
                            less
                            intelligent and evidently less fit for the exaltation offered to the
                            faithful,
                            yet these also were entitled to salvation from death and the torment of
                            the damned. The Lord therefore had a place for these, thus showing his
                            great mercy for all.</big></font></big></li>
                    <big> <br>
                        <font size="+1"><big>&nbsp;&nbsp;&nbsp;&nbsp; We learn from the word
                            of
                            the
                            Lord to Moses that the Lord selected a place for the children of
                            Israel,
                            even before they were born, thus he indicated the number of spirits who
                            were assigned to become the descendants of Jacob (Deut. 32:8-9). We may
                            well believe that the Lord also parceled out the surface of the earth
                            for
                            all other peoples. Some of these places were evidently designed for
                            inhabitants
                            who had lost interest in or touch with the plan of salvation. We may
                            well
                            believe that the Lord did not permit the more progressive and more
                            worthy
                            spirits to come to the families of the ungodly and the less progressive
                            peoples of the earth.</big></font> <br>
                        <font size="+1"><big>&nbsp;&nbsp;&nbsp;&nbsp; It was the privilege of
                            this
                            less progressive class, however, to come to the earth, and it was
                            essential
                            for them to receive the blessings of morality. On this topic, however,
                            there is very little revealed, but we may feel certain that it was
                            essential
                            that the more progressive and intelligent spirits were not sent to the
                            tribes among the degraded heathen. These people naturally sank under
                            such
                            circumstances into a condition of ignorance and spiritual darkness.
                            Children
                            born under such circumstances could not be exalted, yet the Lord in his
                            mercy had decreed to them to do the very best that could be done. Not
                            having
                            knowledge of the things of God, they were to be judged without law and
                            assigned to a place after the resurrection that would be suited to them
                            without the fulness promised to the faithful.</big></font> <br>
                        <font size="+1"><big>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; President Brigham
                            Young,
                            by revelation, received a clear view of this truth and has spoken of
                            those
                            who are without law and understanding in the following words:</big></font>
                        <br>
                        <font size="+1"><big>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; "When God
                            revealed to
                            Joseph Smith and Sidney Rigdon that there was a place prepared for all,
