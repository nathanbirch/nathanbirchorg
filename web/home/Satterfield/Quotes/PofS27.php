                        be valiant in the testimony of Jesus is to take
                        the
                        Lord's side on every issue. It is to vote as he would vote. It is to
                        think
                        what he thinks, to believe what he believes, to say what he would say
                        and
                        do what he would do in the same situation. It is to have the mind of
                        Christ
                        and be one with him as he is one with his Father.</big></font>
