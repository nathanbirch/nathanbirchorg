                <ul style="font-family: arial;">
                    <big><big></big></big><li><big><big>To repent is to change one's mind
                            or one's heart with regard to past or
                            intended action, conduct, etc., on account of regret or
                            dissatisfaction. <small>(Conference Report, April 1948, p. 65 ; <i>Gospel
                                    Ideals</i>,
                                p.14)</small></big></big></li>
                    <big><big></big></big><li><big><big>The message of these young men
                            who are going in all parts of the world,
                            the message of the Church to all the world is: Repent of those things
                            which
                            contribute to the superiority of the physical senses over our love for
                            spirituality. That is why they cry repentance! What does repentance
                            mean?
                            A change of life, a change of thought, a change of action. If you have
                            been angry and hateful, change that hatred and enmity to love and
                            consideration.
                            If you have cheated a brother, let your conscience smite you and change
                            that, and ask his forgiveness, and never do it again. In thus changing
                            your life from those things which are on the animal plane, you repent
                            of
                            your sins. If you profane Deity, never do it again! Instead of
                            profaning
                            his name, worship him! And once that feeling of change comes to the
                            soul,
                            you desire to be born again, to have a new life. . . .This changing of
                            life, this repenting is what the world needs. It is a change of heart.
                            Men must change their way of thinking! Change their way of feeling!
                            Instead
                            of hating and fighting and crushing one another, they should learn to
                            love! <small>(<i>Gospel Ideals</i>, p.328)</small></big></big></li>
                    <big><big></big></big>
                </ul>
                <big style="font-family: arial;"><big><b style="color: rgb(0, 0, 153);">Ezra
                            Taft Benson</b>
                        <span style="color: rgb(102, 102, 102);">(President of Church)</span><br>
                    </big></big>
                <div style="margin-left: 40px; font-family: arial;"><big><big>Lehi
                            taught that "no flesh can dwell in the presence
                            of God, save it be through the merits, and mercy and grace of the Holy
                            Messiah" (2 Nephi 2:8). Even the most just and upright man cannot save
                            himself solely on his own merits, for, as the Apostle Paul tells us,
                            "all
                            have sinned, and come short of the glory of God" (Romans 3:23).
                            Therefore,
                            repentance means more than simply a reformation of behavior. Many men
                            and
                            women in the world demonstrate great will-power and self-discipline in
                            overcoming bad habits and the weaknesses of the flesh. Yet at the same
                            time they give no thought to the Master, sometimes even openly
                            rejecting
                            Him. Such changes of behavior, even if in a positive direction, do not
                            constitute true repentance. Repentance involves not just a change of
                            actions,
                            but a change of heart.
                        </big></big><br>
                    <big><big>&nbsp;</big></big><br>
                    <big><big>As we seek to qualify to be members of Christ's
                            Church -- members in the sense in which He uses the term, members who
                            have
                            repented and come unto Him -- let us remember these principles. The
                            gospel
                            is the Lord's plan of happiness and repentance is designed to bring us
                            joy. True repentance is based on and flows from faith in the Lord Jesus
                            Christ. There is no other way. True repentance involves a change of
                            heart
                            and not just a change of behavior (see Alma 5:13). Part of this mighty
                            change of heart is to feel godly sorrow for our sins. This is what is
                            meant
                            by a broken heart and a contrite spirit. God's gifts are sufficient to
                            help us overcome every sin and weakness if we will but turn to Him for
                            help. Most repentance does not involve sensational or dramatic changes,
                            but rather is a step by step, steady and consistent movement toward
                            godliness.
                            <small>(<i>Teachings of Ezra Taft Benson</i>, p.71)</small>
                        </big></big></div>
                <p style="font-family: arial;"><big><big><b
                            style="color: rgb(0, 0, 153);">Orson F. Whitney</b>&nbsp;
                        <span style="color: rgb(102, 102, 102);">(Quorum of the Twelve)</span><br>
