                <div style="margin-left: 40px; font-family: arial;"><big><big><span
                                style="font-weight: bold;">NATURE OF COVENANT OF BAPTISM</span>. Every
                            person baptized
                            into this Church has made a covenant with the Lord to keep his
                            commandments.
                            We are to serve the Lord with all the heart, and all the mind, and all
                            the strength that we have, and that too in the name of Jesus Christ.
                            Everything
                            that we do should be done in the name of Jesus Christ.
                        </big></big><br>
                </div>
                <div style="margin-left: 40px; font-family: arial;"><big><big><br>
                            In the waters of baptism, we covenanted that we
                            would keep these commandments; that we would serve the Lord; that we
                            would
                            keep this first and greatest of all commandments, and love the Lord our
                            God; that we would keep the next great commandment, we would love our
                            neighbor
                            as ourselves; and with all the might that we have, with all the
                            strength,
                            with all our hearts, we would prove to him that we would "live by every
                            word that proceedeth forth from the mouth of God"; that we would be
                            obedient
                            and humble, diligent in his service, willing to obey, to hearken to the
                            counsels of those who preside over us and do all things with an eye
                            single
                            to the glory of God. <small>(<i>Doctrines of Salvation</i>, 2:328)
                            </small></big></big><br>
                </div>
                <p style="font-family: arial;"><big><big><br>
                    </big></big></p>
                <div style="margin-left: 40px; font-family: arial;"><big><big><span
                                style="font-weight: bold;">WHY BAPTISM MUST BE BY IMMERSION.</span>
                            The mode of baptism
                            is by immersion in water. Sprinkling or pouring did not come into vogue
                            until two or three centuries after Christ, and such a practice was not
                            universal until about the 13th century A.D. We have to go into history
                            to find these particulars. Baptism cannot be by any other means than
                            immersion
                            of the entire body in water, for the following reasons:
                        </big></big><br>
                    <big><big>&nbsp;</big></big><br>
                    <big><big>1. It is in the similitude of the death, burial,
                            and resurrection of Jesus Christ, and of all others who have received
                            the
                            resurrection.
                        </big></big><br>
                </div>
                <p style="margin-left: 40px; font-family: arial;"><big><big>2. Baptism
                        is also a birth and is performed in the
                        similitude of the birth of a child into this world.
                        <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big>3. Baptism
                        is not only a figure of the resurrection,
                        but also is literally a transplanting or resurrection from one life to
                        another -- from the life of sin to the life of spiritual life.
                        <br>
                        &nbsp;<br>
                        I want to take up the second reason: Baptism is
                        also a birth and is performed in the similitude of the birth of a child
                        into this world. When this earth was created, it came into existence
                        the
                        same way. (I am not speaking scientifically, and yet scientific
                        doctrine
                        tells us the same thing.) This earth was born in water. Before the land
                        appeared the whole sphere was covered with water.
                        <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big><span
                            style="font-weight: bold;">BIRTH COMES BY WATER, BLOOD, AND SPIRIT</span>.
                        In the
                        Book of Moses we read: "Therefore I give unto you a commandment, to
                        teach
                        these things freely unto your children, saying: That by reason of
                        transgression
                        cometh the fall, which fall bringeth death, and inasmuch as ye were
                        born
                        into the world by water, and blood, and the spirit, which I have made,
                        and so became of dust a living soul, even so ye must be born again into
                        the kingdom of heaven, of water, and of the Spirit, and be cleansed by
                        blood, even the blood of mine Only Begotten; that ye might be
                        sanctified
                        from all sin, and enjoy the words of eternal life in this world, and
                        eternal
                        life in the world to come, even immortal glory; For by the water ye
                        keep
                        the commandment; by the Spirit ye are justified, and by the blood ye
                        are
                        sanctified." That is one of the finest passages I know, and we find
                        practically
                        the same thing recorded by John.
                        <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big><span
                            style="font-weight: bold;">SAME ELEMENTS PRESENT IN FIRST AND SECOND
                            BIRTHS</span>.
                        Every child that comes into this world is carried in water, is born of
                        water, and of blood, and of the spirit, So when we are born into the
                        kingdom
                        of God, we must be born in the same way. By baptism, we are born of the
                        water. Through the shedding of the blood of Christ, we are cleansed and
                        sanctified: and we are justified, through the Spirit of God, for
                        baptism
                        is not complete without the baptism of the Holy Ghost. You see the
                        parallel
                        between birth into the world and birth into the kingdom of God. How
                        foolish
                        it is to think for a moment that baptism could be accomplished by
                        pouring
                        water on a child's head, it does not meet the requirements.
                        <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big>I have
                        heard some of our young elders preaching
                        on baptism say that the Lord could have brought to pass the remission
                        of
                        sins in some other way. They reasoned he could have done it by
                        sprinkling,
                        or in this way or that way. The Lord could not consistently do it any
                        other
                        way, only by being buried in the water, born of water and of the
                        spirit,
                        and cleansed by the blood of Christ, just as a child is born into this
                        world of water, blood, and spirit. The comparison is very striking.
                        <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big><span
                            style="font-weight: bold;">BAPTISM: A RESURRECTION TO LIFE</span>.
                        Coming now to the
                        third reason: Baptism is not only a figure of the resurrection, but
                        also
                        is literally a transplanting or resurrection from one life to another
                        --
                        from the life of sin to the life of spiritual life. For proof of that,
                        I am going to read first something the Lord said to Joseph Smith.
                        <br>
                    </big></big></p>
                <p style="margin-left: 40px; font-family: arial;"><big><big>"Wherefore,
                        I, the Lord God, caused that he [Adam]
