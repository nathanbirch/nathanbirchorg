                            the
                            kingdom of God, or to be able to comprehend the ways of the Lord. Is
                            this
                            a mystery? No, it is plain and easily understood when we get the spirit
                            and light of God upon it. Jesus said also, "Except a man be born of the
                            water and of the spirit, he cannot enter into the kingdom of God." A
                            change
                            must take place in the human heart. Men must first be born of the word
                            of God, which lives and abides for ever. As the Apostle Peter says,
                            "being
                            born again, not of corruptible seed, but of incorruptible, by the word
                            of God, which liveth and abideth forever." The Apostle James says, "Of
