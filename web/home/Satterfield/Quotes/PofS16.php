                <p style="margin-left: 40px; font-family: arial;"><big><big>Baptism is
                        a sign to God, to angels, and to heaven
                        that we do the will of God, and there is no other way beneath the
                        heavens
                        whereby God hath ordained for man to come to Him to be saved, and enter
                        into the Kingdom of God, except faith in Jesus Christ, repentance, and
                        baptism for the remission of sins, and any other course is in vain;
                        then
                        you have the promise of the gift of the Holy Ghost.(<i>Teachings of the
                            Prophet Joseph Smith</i>, p.198)
