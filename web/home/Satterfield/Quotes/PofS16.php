                        see the reason? As long as a man sins and stays within spiritual life,
                        he is alive, he can repent and be forgiven. He does not need to be
                        baptized
                        to be brought back to where he already is, But there are sins, John
                        says,
                        "unto death," and if a man commits a sin unto death, he is banished
                        again
