                <div style="margin-left: 40px; font-family: arial;"><big><big>Repentance
                            is not that superficial sorrow felt by
                            the wrongdoer when "caught in the act"- a sorrow not for sin, but for
                            sin's
                            detection. Chagrin is not repentance. Mortification and shame alone
                            bring
                            no change of heart toward right feeling and right living. Even remorse
                            is not all there is to repentance. In the highest meaning and fullest
                            measure,
                            repentance is equivalent to reformation; the beginning of the
                            reformatory
                            process being resolve to "sin no more." <small>(<i>Cowley and Whitney
                                    on Doctrine</i>,
                                p. 435)
                            </small></big></big><br>
