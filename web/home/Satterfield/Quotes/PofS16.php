                            believe that the first principles and ordinances
                            of the gospel are: first, Faith in the Lord Jesus Christ--" and when I
                            say "faith in the Lord Jesus Christ," we want it distinctly understood
                            that we believe absolutely in Jesus Christ, that He was the Son of God,
                            and that He did come to the earth with a divinely-appointed mission to
                            die on the cross as the Redeemer of mankind. We do not believe that He
                            was just a "great moral teacher," but that He is our Redeemer . . . <small>(<i>Gospel
                                    Standards</i>, p.6).</small>
                        </big></big></div>
                <p style="font-family: arial;"><big><big><b><span
                                style="color: rgb(0, 0, 153);">Ezra Taft Benson</span></b> <span
                            style="color: rgb(102, 102, 102);">(President of Church)</span>
                    </big></big></p>
                <ul style="font-family: arial;">
                    <big><big></big></big><li><big><big>The fundamental principle of our
                            religion is faith in the Lord Jesus
                            Christ.
                            Why is it expedient to center confidence, hope, and trust in one
                            solitary
                            figure? Why is faith in Him so necessary to peace of mind in this life
                            and hope in the world to come? My answer to these questions is derived
                            from a lifetime in His service and the confirmation of the Holy Spirit
                            that only Jesus Christ is uniquely qualified to provide hope,
                            confidence,
                            and strength to overcome the world and rise above our human failings.
                            This
                            is the reason I place my faith and trust in Him and strive to abide by
                            His laws and teachings. <small>(<i>Teachings of Ezra Taft Benson</i>,
                                p.66)</small></big></big></li>
                    <big><big></big></big>
                </ul>
                <ul style="font-family: arial;">
                    <big><big></big></big><li><big><big>Now let me describe to you what
                            faith in Jesus Christ means. Faith in
                            Him
                            is more than mere acknowledgment that He lives. It is more than
                            professing
                            belief. Faith in Jesus Christ consists of complete reliance on Him. As
                            God, He has infinite power, intelligence, and love. There is no human
                            problem
                            beyond His capacity to solve. Because He descended below all things, He
                            knows how to help us rise above our daily difficulties. Faith in Him
                            means
                            believing that even though we do not understand all things, He does.
                            We,
                            therefore, must look to Him "in every thought; doubt not, fear not"
                            (D&amp;C
                            6:36). Faith in Him means trusting that He as power over all men and
                            all
                            nations. There is no evil that He cannot arrest. All things are in His
                            hands. This earth is His rightful dominion. Yet He permits evil so that
                            we can make choices between good and evil. <small>(<i>Teachings of
                                    Ezra Taft Benson</i>,
                                p.66)</small></big></big></li>
