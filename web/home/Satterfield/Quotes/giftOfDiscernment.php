                <center style="font-family: arial;"><big><i><font size="+2"><big>Teachings
                                Concerning</big></font></i>
                        <br>
                        <i><font size="+3"><big>The Gift of Discernment</big></font></i></big></center>
                <p style="font-family: arial;"><big><a href="../PDF/Discernment.pdf">PDF
                        Version</a>
                </big></p>
                <p style="font-family: arial;"><big><b>James E. Talmage</b>
                </big></p>
                <p style="font-family: arial;"><big>Oh, that we all had such power of
                    discernment. That is a gift of the
                    Spirit, to which we are entitled and we will have it as we live for it.
                    With that gift we shall be free, to a great extent, from the deception
                    that otherwise might lead us astray.
                </big></p>
                <p style="font-family: arial;"><big>As the Lord gives revelations, so
                    does Satan, each in his way. As the
                    Lord has revelators upon the earth, so has Satan, and he is operating
                    upon
                    those men by his power, and they are receiving revelations,
                    manifestations,
                    that are just as truly of the devil as was his manifestation to Moses,
                    to which I have referred.
                </big></p>
                <p style="font-family: arial;"><big>We need the power of discernment.
                    We need the inspiration of the Lord,
                    that we may know the spirits with whom we have to deal, and recognize
                    those
                    who are speaking and acting under the influence of heaven, and those
                    who
                    are the emissaries of hell. <b>(</b>Conference Report, April 1931, p.28<b>)</b>
                </big></p>
                <p style="font-family: arial;"><big><b>Delbert L. Stapley</b>
                </big></p>
                <p style="font-family: arial;"><big>Possessing, as we do, the endowment
                    of the Holy Ghost, if worthy, we
                    are entitled to the gift of discernment to guide and help us avoid the
                    pitfalls of scheming and designing men to trap and ensnare us into the
                    meshes of worldly lusts, influences, and pleasures.<b> (</b>Conference
                    Report, October 1961, p.21<b>)</b>
                </big></p>
                <p style="font-family: arial;"><big>The Holy Ghost is a revelator of
                    truth and has the related power of
                    discernment, which interprets the mind and motives of men; therefore it
                    has the function of preventing confusion and deception to the possessor
                    of this gift. (D&amp;C 50:23-24.) [Conference Report, October 1966,
                    p.112]
                </big></p>
                <p style="font-family: arial;"><big><b>Stephen L. Richards</b>
                </big></p>
                <p style="font-family: arial;"><big>First, I mention the gift of
                    discernment, embodying the power to discriminate,
                    which has been spoken of in our hearing before particularly as between
                    right and wrong. I believe that this gift when highly developed arises
                    largely out of&nbsp;<span style="text-decoration: underline;">an acute
                        sensitivity
                        to impressions</span> --&nbsp;<span style="text-decoration: underline;">spiritual
                        impressions</span>, if you will --&nbsp;<span
                        style="text-decoration: underline;">to
                        read under the surface as it were, to detect hidden evil, and more
                        importantly
                        to find the good that may be concealed</span>.&nbsp;<span
                        style="text-decoration: underline;">The
                        highest type of discernment is that which perceives in others and
                        uncovers
                        for them their better natures, the good inherent within them</span>.
                    It's
                    the gift every missionary needs when he takes the gospel to the people
                    of the world. He must make an appraisal of every personality whom he
                    meets.
                    He must be able to discern the hidden spark that may be lighted for
                    truth.
                    The gift of discernment will save him from mistakes and embarrassment,
                    and it will never fail to inspire confidence in the one who is rightly
                    appraised.
                </big></p>
                <p style="font-family: arial;"><big>The gift of discernment is
                    essential to the leadership of the Church.
                    I never ordain a bishop or set apart a president of a stake without
                    invoking
                    upon him this divine blessing, that he may read the lives and hearts of
                    his people and call forth the best within them.&nbsp;<span
                        style="text-decoration: underline;">The
                        gift and power of discernment in this world of contention between the
                        forces
                        of good and the power of evil is essential equipment for every son and
                        daughter of God</span>. There could be no such mass dissensions as
                    endanger
                    the security of the world, if its populations possessed this great gift
                    in larger degree.&nbsp;<span style="text-decoration: underline;">People
                        are generally so gullible</span> one is sometimes led to wonder whether
                    the great Lincoln was right, after all, in the conclusion of his
                    memorable
                    statement, "You can't fool all the people all the time." One does feel
                    at times, however, a sense of pity and sympathy for some of the peoples
                    of the world whose education, information, and exposure to higher
                    ideals
                    and exalted concepts have been so arbitrarily and ruthlessly
                    restricted.
                </big></p>
                <p style="font-family: arial;"><big>There is a class of people now
                    grown sizable in the world who should
                    possess this great gift in large degree. They know how the gift is
                    attained.
                    They have been educated in its spiritual foundations. They have been
                    blessed
                    with the counsels which foster it. They know how to order their lives
                    to
                    procure it. You know who they are, my brethren and sisters.<span
                        style="text-decoration: underline;">
                        Every member in the restored Church of Christ could have this gift if
                        he
                        willed to do so.</span><span style="text-decoration: underline;">He
                        could
                        not be deceived with the sophistries of the world</span>. He could not
                    be led astray by pseudo-prophets and subversive cults.&nbsp;<span
                        style="text-decoration: underline;">Even
                        the inexperienced would recognize false teachings, in a measure at least</span>.&nbsp;<span
                        style="text-decoration: underline;">With
                        this gift they would be able to detect something of the disloyal,
                        rebellious,
                        and sinister influences which not infrequently prompt those who
                        seemingly
                        take pride in the destruction of youthful faith and loyalties</span>.&nbsp;<span
                        style="text-decoration: underline;">Discerning
                        parents will do well to guard their children against such influences
                        and
                        such personalities and teachings before irreparable damage is done</span>.&nbsp;<span
                        style="text-decoration: underline;">The
                        true gift of discernment is often premonitory</span>.<span
                        style="text-decoration: underline;">
                        A sense of danger should be heeded to be of value</span>. We give
                    thanks
                    for a set of providential circumstances which avert an accident. We
                    ought
                    to be grateful every day of our lives for this sense which keeps alive
                    a conscience which constantly alerts us to the dangers inherent in
                    wrongdoers
                    and sin. (Conference Report, April 1950, pp. 162-163)
                </big></p>
                <p style="font-family: arial;"><big><b>George Q. Cannon</b>
                </big></p>
                <p style="font-family: arial;"><big>One of the gifts of the Gospel
                    which the Lord has promised to those
                    who enter into covenant with Him is the gift of discerning of
                    spirits--a
                    gift which is not much thought of by many and probably seldom prayed
                    for;<span style="text-decoration: underline;">
                        yet it is a gift that is of exceeding value and one that should be
                        enjoyed
                        by every Latter-day Saint</span>&#8230;.&nbsp;<span
                        style="text-decoration: underline;">No
                        Latter-day Saint should be without this gift</span>, because there is
                    such
                    a variety of spirits in the world which seek to deceive and lead
                    astray.
                    In a revelation to the Church upon the spirits which have gone abroad
                    in
                    the earth the Lord says:
                </big></p>
                <p style="font-family: arial;"><big>"Behold, verily I say unto you,
                    that there are many spirits which are
                    false spirits, which have gone forth in the earth, deceiving the
                    world."[D&amp;C
                    50:2]
                </big></p>
                <p style="font-family: arial;"><big>The Lord warns the Saints and says:
                    "Beware lest ye are deceived."[D&amp;C
                    46:8] And that they may not be deceived, He commands them to seek
                    earnestly
                    the best gifts.
                </big></p>
                <p style="font-family: arial;"><big>The Apostle John says:
                </big></p>
                <p style="font-family: arial;"><big>"Behold, believe not every spirit,
                    but try the spirits whether they
                    are of God: because many false prophets are gone out into the world."
                    [John
                    4:1]
                </big></p>
                <p style="font-family: arial;"><big>This counsel of the beloved Apostle
                    applies as much to us in these latter
                    days as it did to the Saints of his age. All manner of spirits have
                    gone
                    forth to deceive, to lead astray and to obtain possession of the
                    children
                    of men; and many people yield to them because they are invisible and
                    cannot,
                    perhaps, think that they can be possessed by invisible influences.
                    Anger,
                    backbiting, slander, falsehood and various passions are manifested by
                    people
                    under the influence of false and deceptive spirits&#8230;.
                </big></p>
                <p style="font-family: arial;"><big>Now, the gift of discerning of
                    spirits not only gives men and women
                    who have it the power to discern the spirit with which others may be
                    possessed
                    or influenced, but it gives them the power to discern the spirit which
                    influences themselves. They are able to detect a false spirit and also
                    to know when the Spirit of God reigns within them. In private life this
                    gift is of great importance to the Latter-day Saints. Possessing and
                    exercising
                    this gift they will not allow any evil influence to enter into their
                    hearts
                    or to prompt them in their thoughts, their words or their acts. They
                    will
                    repel it; and if perchance such a spirit should get possession of them,
                    as soon as they witness its effects they will expel it or, in other
                    words,
                    refuse to be led or prompted by it.
                </big></p>
                <p style="font-family: arial;"><big>The gift of discerning of spirits,
                    also, is one that is of great importance
                    to the Elders who are laboring in the ministry. We have known Elders
                    become
                    so filled with zeal and so desirous to do good, or what they supposed
                    to
                    be good, that they exposed themselves to the influence of the
                    adversary.
                    They would be filled with a species of what has been called "wildfire,"
                    and, carried away by zeal, they would go too far; they would say and do
                    imprudent things and yet, being prompted by the purest and best
                    motives,
                    would feel entirely justified in their course. In the history of the
                    Church
                    there have been many illustrations of this. Elders can work themselves
                    up beyond that which is proper and wise and be led to say and do many
                    imprudent
                    things and overstep the line of propriety. Now, the gift of discerning
                    of spirits is necessary to keep these kind of feelings in check.
                </big></p>
                <p style="font-family: arial;"><big>The gift of discerning spirits is
                    not only necessary for this purpose,
                    but it is necessary in the branches of the Church. Newly baptized
                    members,
                    anxious to obtain the gifts, are liable sometimes to be taken advantage
                    of by the adversary and to imbibe or yield to a wrong spirit. A newly
                    organized
                    branch of the Church, where the gifts are manifested, especially the
                    gift
                    of tongues, has to be watched with great care. The Elders laboring in
                    the
                    branch or presiding in the conference must be in a position to discern
                    between the Spirit of the Lord and other spirits that may seek to steal
                    in&#8230;.
                </big></p>
                <p style="font-family: arial;"><big>In all the situations in life,
                    therefore, in which Latter-day Saints
                    can be placed there is great need for them to possess the gift of
                    discerning
                    of spirits. Fathers and mothers need it for their own benefit. They
                    need
                    it in their families, in the training of their children. All Saints
                    need
                    it to enable them to escape from the many evil influences that are
                    abroad.
                    The Elders need it for their own sakes; they need it also in the
                    government
                    of the branches, of the conferences, of the wards, of the stakes and,
                    indeed,
                    the entire Church. It is a great and blessed gift, and it should be
                    sought
                    for by all. (<i>Gospel Truth</i>, 1:198-200)
                    <br>
                    &nbsp;
                </big></p>
                <p style="font-family: arial;"><big>There are now many things existing,
                    and which our people are brought
                    into contact with, that are calculated to deceive. Especially is this
                    the
                    case in the early lives of both sexes, who are apt to be deceived by
                    appearances--by
                    judging men by their outward appearance, expressions and sometimes by
                    their
                    dress and demeanor. There is a gift in the Church of God which if
                    obtained
                    enables us to escape deception, and detect all kinds of spirits and
                    evil.
                    It is the gift of discernment of spirits. The Lord has promised unto us
                    this gift, so that we shall be able through that gift to discern the
                    spirits
                    of men and women who may be brought in contact with us, so as not to be
                    deceived by them. Some persons have this gift. It is natural to them
                    while
                    others seem to be utterly destitute of it, and therefore easily
                    deceived,
                    because they have no discernment. Now we can ask God for this gift and
                    have it bestowed upon us. If we seek for it in faith it will be given
                    us.
                    (<i>Collected Discourses</i>, 2:249, July 5th, 1891)
                </big></p>
                <p style="font-family: arial;"><big><b>David O. McKay</b>
                </big></p>
                <p style="font-family: arial;"><big>From birth to death men differ.
                    They vary as much as do flowers in a
                    garden. In intellect, in temperament, in energy, and in training some
                    rise
                    to one level and some to another.
                </big></p>
                <p style="font-family: arial;"><big>The successful teacher is one who,
                    with a spirit of discernment, can
                    detect to a degree at least, the mentality and capability of the
                    members
                    of his class. He should be able to read the facial expressions and be
                    responsive
                    to the mental and spiritual attitudes of those whom he is teaching. The
                    great Teacher had this power of discernment in perfection as is well
                    illustrated
                    in his conversation with the woman of Samaria whose interests he not
                    only
                    interpreted, but whose soul he also read by virtue of her past deeds.
                    Too
                    few teachers have this gift, even to a limited degree, but every
                    teacher
                    has the responsibility of determining how best to approach the members
                    of the class in order to make appeals that will be lasting. -- RSM,
                    21:722
                    (1934).
                </big></p>
                <p style="font-family: arial;"><big>It is written that "he who governs
                    well leads the blind, but he that
                    teaches gives them eyes." -- CR, April 1914, p. 86. (<i>Gospel Ideals</i>,
                    p.439)
                </big></p>
                <p style="font-family: arial;"><big><b>Joseph Fielding Smith</b>
                </big></p>
                <p style="font-family: arial;"><big><span
                        style="text-decoration: underline;">To understand spiritual things,
                        a man must have spiritual discernment</span>, that is, guidance by the
                    Holy Ghost. For this reason we are confirmed and receive the gift of
                    the
                    Holy Ghost.
                </big></p>
                <p style="font-family: arial;"><big>The Lord expects us to use our
                    faculties and has given us reason as
                    a measuring rod to measure truth under certain conditions.<span
                        style="text-decoration: underline;">Primarily
                        in the search after gospel truth, there must be the teaching of the
                        Spirit</span>
                    -- Spirit speaking to spirit -- and this comes only through obedience
                    to
                    gospel law.
                </big></p>
                <p style="font-family: arial;"><big>The man who will not "do his will"
                    may search forever, but in vain;
                    but cannot find it! It is not to be found in psychology, in biology, or
                    sociology, no matter what other truth may be found therein.&nbsp;<span
                        style="text-decoration: underline;">When
                        we hearken to the Spirit who guides into all truth, we will see that
                        the
                        truth revealed is reasonable and consistent with all other truth</span>.
                </big></p>
                <p style="font-family: arial;"><big><span
                        style="text-decoration: underline;">Only by the aid of the Holy
                        Ghost, and through obedience to the principles of the gospel, will a
                        man
                        eventually attain to the knowledge of all truth</span>. In other words,
                    those who will not make their lives conform in every particular to the
                    Divine Life; who will not adjust their lives through faith and
                    repentance
                    and obedience to all divine law, will never be in a position to
                    comprehend
                    truth in its fulness. Therefore, only in the celestial kingdom will the
                    fulness of the truth be attained.
                </big></p>
                <p style="font-family: arial;"><big>All who will not place their lives
                    "at-one" with the Father and the
                    Son cannot comprehend the things of God. They are foolishness unto
                    them.
                    For this reason so many of the learned men in the world fail to
                    comprehend
                    the gospel and teach theories and philosophies at variance with
                    revealed
                    truth which they cannot understand. We are in that day when the people
                    are "ever learning, and never able to come to the knowledge of the
                    truth."
                    (<i>Doctrines of Salvation</i>, 1:298)
                </big></p>
                <p style="font-family: arial;"><big><b>Bruce R. McConkie</b>
                </big></p>
                <p style="font-family: arial;"><big>In its most important aspect,
                    discernment is used to distinguish between
                    good and evil (Moro. 7:12-18), between the righteous and the wicked (D.
                    &amp; C. 101:95; Mal. 3:18; 3 Ne. 24:18), between the false or evil
                    spirits
                    and those spirits who truly manifest the things of God. (D. &amp; C.
                    46:23;
                    1 Cor. 12:10.) In its fullest manifestation the gift of the discerning
                    of spirits is poured out upon presiding officials in God's kingdom;
                    they
                    have it given to them to discern all gifts and all spirits, lest any
                    come
                    among the saints and practice deception. (D. &amp; C. 46:27.)
                </big></p>
                <p style="font-family: arial;"><big>There is no perfect operation of
                    the power of discernment without revelation.
                    Thereby even "the thoughts and intents of the heart" are made known.
                    (D.
                    &amp; C. 33:1; Heb. 4:12.)&nbsp;<span
                        style="text-decoration: underline;">Where
                        the Saints are concerned</span> --<span
                        style="text-decoration: underline;">
                        since they have received the right to the constant companionship of the
                        Holy Ghost</span> --&nbsp;<span style="text-decoration: underline;">the
                        Lord expects them to discern, not only between the righteous and the
                        wicked,
                        but between false and true philosophies, educational theories,
                        sciences,
                        political concepts, and social schemes</span>.&nbsp;<span
                        style="text-decoration: underline;">Unfortunately,
                        in many instances, even good men hearken to "the tradition of their
                        fathers"
                        (D. &amp; C. 93:39) and rely on the learning of the world rather than
                        the
                        revelations of the Lord, so that they do not enjoy the full play of the
                        spirit of discernment</span>. (<i>Mormon Doctrine</i>, p.197)
                </big></p>
                <p style="font-family: arial;"><big><b>James E. Faust</b>
                </big></p>
                <p style="font-family: arial;"><big>Satan has had great success with
                    this gullible generation. As a consequence,
                    literally hosts of people have been victimized by him and his angels.
                    There
                    is, however, an ample shield against the power of Lucifer and his
                    hosts.
                    This protection lies in the spirit of discernment through the gift of
                    the
                    Holy Ghost. This gift comes undeviatingly by personal revelation to
                    those
                    who strive to obey the commandments of the Lord and to follow the
                    counsel
                    of the living prophets.
                </big></p>
                <p style="font-family: arial;"><big>This personal revelation will
                    surely come to all whose eyes are single
                    to the glory of God, for it is promised that their bodies will be
                    "filled
                    with light, and there shall be no darkness" in them. 27 Satan's efforts
                    can be thwarted by all who come unto Christ by obedience to the
                    covenants
                    and ordinances of the gospel. The humble followers of the divine Master
                    need not be deceived by the devil. Satan does not sustain and uplift
                    and
                    bless. He leaves those he has grasped in shame and misery. The spirit
                    of
                    God is a sustaining and uplifting influence. ("Serving the Lord and
                    Resisting
                    the Devil," <i>Ensign</i>, Sept. 1995, p. 6-7)
                </big></p>
                <p style="font-family: arial;"><big><b>Ezra Taft Benson</b>
                </big></p>
                <p style="font-family: arial;"><big>The Book of Mormon was written for
                    our day. Mormon, who compiled it,
                    saw us in vision and was directed to put into the book those things God
                    felt we would especially need in our time. We therefore should know the
                    Book of Mormon better than any other book. Not only should we know what
                    history and faith-promoting stories it contains, but we should
                    understand
                    its teachings. If we really do our homework and approach the Book of
                    Mormon
                    doctrinally, we can expose the errors and find the truths to combat
                    many
                    of the current false theories and philosophies of men.
                </big></p>
                <p><big><span style="text-decoration: underline; font-family: arial;">I
                        have noted within the Church
                        a difference in discernment, insight, conviction, and spirit between
                        those
                        who know and love the Book of Mormon and those who do not.</span><span
                        style="font-family: arial;"> That
                        book is a great sifter. ("Jesus Christ--Gifts and Expectations,"
                        Ensign,
                        Dec. 1988, 4)
